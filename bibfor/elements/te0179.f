C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT
C ......................................................................
      INCLUDE 'jeveux.h'
      CHARACTER*8 FAMI,POUM
      CHARACTER*16 OPTION,NOMTE
      INTEGER ICODRE,KPG,SPT
      REAL*8 POIDS,R,NX,NY,RHO
      INTEGER NNO,KP,NPG,IPOIDS,IVF,IDFDE,IGEOM
      INTEGER I,L,LI
      INTEGER IMATE,IVITN
      LOGICAL LTEATT,LAXI


      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)

      FAMI='FPG1'
      KPG=1
      SPT=1
      POUM='+'
      LAXI = .FALSE.
      IF (LTEATT(' ','AXIS','OUI')) LAXI = .TRUE.

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PVITENC','L',IVITN)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PVECTTC','E',IVECTT)

      MATER = ZI(IMATE)
      CALL RCVALB(FAMI,KPG,SPT,POUM,MATER,' ','FLUIDE',0,' ',
     &            R8B,1,'RHO',RHO,ICODRE,1)

      DO 30 KP = 1,NPG
        CALL VFF2DN(NDIM,NNO,KP,IPOIDS,IDFDE,ZR(IGEOM),NX,NY,POIDS)
        IF (LAXI) THEN
          R = 0.D0
          DO 10 I = 1,NNO
            L = (KP-1)*NNO + I
            R = R + ZR(IGEOM+2*I-2)*ZR(IVF+L-1)
   10     CONTINUE
          POIDS = POIDS*R
        END IF

        DO 20 I = 1,NNO
          LI = IVF + (KP-1)*NNO + I - 1
          ZC(IVECTT+I-1) = ZC(IVECTT+I-1) +
     &                     POIDS*ZR(LI)*ZC(IVITN+KP-1)*RHO
   20   CONTINUE
   30 CONTINUE
      END
