C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT NONE
      CHARACTER*16        OPTION , NOMTE
C ----------------------------------------------------------------------
C     CALCUL DES COORDONNEES DES POINTS DE GAUSS
C     POUR LES ELEMENTS ISOPARAMETRIQUES 3D
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER            NNO,KP
      INTEGER            IPOIDS,IVF,IGEOM
      INTEGER            NPG,NPG1
      INTEGER            ICOPG,INO,JIN,NDIM,JVAL
      CHARACTER*8        ELREFE
      CHARACTER*24       CHVAL,CHCTE
      REAL*8             XX,YY,ZZ
C DEB ------------------------------------------------------------------
C
      CALL ELREF1(ELREFE)
C
C ---- CARACTERISTIQUES DU TYPE D'ELEMENT :
C ---- GEOMETRIE ET INTEGRATION
C      ------------------------
      CHCTE = '&INEL.'//ELREFE//'.CARACTE'
      CALL JEVETE(CHCTE,'L',JIN)
      NNO   = ZI(JIN+2-1)
      NPG1  = ZI(JIN+4-1)
      NDIM  = ZI(JIN+1-1)
C     ON UTILISE SYSTEMATIQUEMENT LA PREMIERE FAMILLE DE POINTS DE GAUSS
C     CAR C'EST ELLE QUI EST UTILISEE POUR RIGI_MECA ET RIGI_THER
C     DANS TE0011 ET TE0051
      NPG   = NPG1
C
      CHVAL = '&INEL.'//ELREFE//'.FFORMES'
      CALL JEVETE(CHVAL,'L',JVAL)
      IPOIDS = JVAL + (NDIM+1)*NNO*NNO
      IVF    = IPOIDS + NPG1
C
C ---- RECUPERATION DES COORDONNEES DES CONNECTIVITES
C      ----------------------------------------------
      CALL JEVECH('PGEOMER','L',IGEOM)
C
      CALL JEVECH('PCOORPG','E',ICOPG )
C
      DO 100 KP=1,NPG
        XX=0.D0
        YY=0.D0
        ZZ=0.D0
        DO 50 INO=1,NNO
          XX=XX+ZR(IGEOM+3*(INO-1)+0)*ZR(IVF+(KP-1)*NNO+INO-1)
          YY=YY+ZR(IGEOM+3*(INO-1)+1)*ZR(IVF+(KP-1)*NNO+INO-1)
          ZZ=ZZ+ZR(IGEOM+3*(INO-1)+2)*ZR(IVF+(KP-1)*NNO+INO-1)
   50   CONTINUE
      ZR(ICOPG+3*(KP-1)+0)=XX
      ZR(ICOPG+3*(KP-1)+1)=YY
      ZR(ICOPG+3*(KP-1)+2)=ZZ
  100 CONTINUE
C
      END
