C
C    IN  : 
C          SIGN    : CONTRAINTE NORMALE AU P.GAUSS
C          SIGT    : CONTRAINTE TANGENTIELLE AU P.GAUSS
C          MU      : COEFFICIENT DE FROTTEMENT DE COULOMB
C
C    OUT : 
C          SIGT    : CONTRAINTE TANGENTIELLE AU P.GAUSS
C ------------------------------------------------------------------
      IMPLICIT REAL*8 (A-H,O-Z)
      REAL*8 SIGT(*),SIGN,MU,XX,XS
C
      XX = ABS(MU*SIGN)
      XS = 0.0D0
      DO 20 I=1,NDIM
         XS=XS+SIGT(I)*SIGT(I)
 20   CONTINUE
      XS=SQRT(XS)
      IF (XS.GE.XX)THEN
         IF (XS.GE.SGEPSI)THEN
            DO 10 I=1,NDIM
               SIGT(I)=SIGT(I)/XS*XX
10          CONTINUE
         ENDIF
      ENDIF
      END
