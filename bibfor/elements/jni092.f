C RESPONSABLE VABHHTS J.PELLET
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN DECLARATIONS NORMALISEES  JEVEUX ---------------------

      INTEGER NBPG(10),LCARAC,NNO,IRET,ICARAC,LFF
      INTEGER IFF,IPOIDS,IVF,IDFDE,I,K
      INTEGER NDIM
      INTEGER NDERIV,NFF,NPG,ICOG,IPG,NDF
      INTEGER IYTY,INO,NBFPG,NNOS,NMAXOB,NBOBJ
      REAL*8 DFF(3,27),X(27),XG(2)
      CHARACTER*24 CARAC,FFO,LIOBJ(NMAXOB)
C DEB -----------------------------------------------------------------
      CALL ASSERT(ELREFE.EQ.'CABPOU')

      CARAC = '&INEL.'//ELREFE//'.CARAC'
      FFO = '&INEL.'//ELREFE//'.FF'

      NBOBJ = 2
      CALL ASSERT(NMAXOB.GT.NBOBJ)
      LIOBJ(1) = CARAC
      LIOBJ(2) = FFO

      DO 10 I = 1,10
        NBPG(I) = 0
   10 CONTINUE

      CALL CARREF(ELREFE,NDIM,NNO,NNOS,NBFPG,NBPG,X)

      CALL JEEXIN(CARAC,IRET)
      IF (IRET.NE.0) GO TO 40


      LCARAC = 4
      CALL WKVECT(CARAC,'G V I',LCARAC,ICARAC)
      ZI(ICARAC) = NNO
      ZI(ICARAC+1) = 1
      ZI(ICARAC+2) = NBPG(1)
      NPG = NBPG(1)

      LFF = NPG + NPG*NNO*2 + NPG*9*NNO**2 + NPG

      CALL WKVECT(FFO,'G V R',LFF,IFF)

      IPOIDS = IFF
      IVF = IPOIDS + NPG
      IDFDE = IVF + NPG*NNO
      ICOG = IDFDE + NPG*NNO

      CALL ELRFGF(ELREFE,1,NBPG,NBPG(1),ZR(ICOG),27,ZR(IPOIDS))

      DO 30 IPG = 1,NBPG(1)
        K = (IPG-1)*NNO
        XG(1) = ZR(ICOG+IPG-1)
        CALL ELRFVF(ELREFE,XG(1),NNO,ZR(IVF+K),NFF)
        CALL ELRFDF(ELREFE,XG(1),NNO,DFF,NDF,NDERIV)
        DO 20 INO = 1,NNO
          ZR(IDFDE+K+INO-1) = DFF(1,INO)
   20   CONTINUE
   30 CONTINUE


      IYTY = IDFDE + NPG*NNO
      CALL PGYTY(NNO,NPG,ZR(IDFDE),ZR(IYTY))


   40 CONTINUE

      END
