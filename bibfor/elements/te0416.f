      IF (OPTION.EQ.'FORC_NODA')THEN
C        -- PASSAGE DES CONTRAINTES DANS LE REPERE INTRINSEQUE :
         CALL COSIRO (NOMTE,'PCONTMR','UI','G',IBID,'S')
      ENDIF


      CALL TECACH('ONN','PCOMPOR',1,ICOMPO,IRET)
      IF(ICOMPO.EQ.0) THEN
       CALL FORNPD (OPTION,NOMTE)
       GOTO 9999
      ELSE
       CALL JEVECH ('PCOMPOR','L',ICOMPO)
C
       IF (ZK16(ICOMPO+3) (1:9) . EQ . 'COMP_ELAS') THEN
C
C------- HYPER-ELASTICITE
C
         IF ( ZK16 ( ICOMPO + 2 ). EQ . 'GROT_GDEP' ) THEN
C
C------- DEFORMATION DE GREEN
C
             CALL FORNGR ( OPTION , NOMTE )
C
             GO TO 9999
C
          ELSE
C
C----------- AUTRES MESURES DE DEFORMATIONS
C
        CALL U2MESK('F','ELEMENTS3_93',1,ZK16(ICOMPO+2))
C
          ENDIF
C
       ELSE IF (ZK16(ICOMPO+3) (1:9) . EQ . 'COMP_INCR') THEN
         IF ( ZK16 ( ICOMPO + 2 ) . EQ . 'GROT_GDEP' ) THEN
C
C------- DEFORMATION DE GREEN
C
             CALL FORNGR ( OPTION , NOMTE )
C
             GO TO 9999
C
          ELSE
             CALL FORNPD (OPTION,NOMTE)
          ENDIF

       ELSE
C
C
          CALL FORNPD (OPTION,NOMTE)
C
C
       ENDIF
      ENDIF
C
C
 9999 CONTINUE
C
C
C
      END
