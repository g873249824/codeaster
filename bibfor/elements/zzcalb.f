C    ESTIMATEUR ZZ (2-EME VERSION 92)
C
C                                  T
C CETTE ROUTINE CALCULE  F = SIGMA( P(X   ,Y   ) * SIG (X   ,Y   ) )
C                         I   IPG      IPG  IPG       I  IPG  IPG
C
C    AVEC P(X,Y) = (1,X,Y,XY)
C       EN PRENANT LES NNO PREMIERS MONOMES
C
C     X(IPG),Y(IPG) SONT LES COORDONNEES DES PTS DE GAUSS SUR
C     L'ELEMENT COURANT IMA
C
      REAL*8 R,WI(1),X(1),Y(1),F(9,4),XX,YY,SIG(1),B(9)
      DO 1 IPG = 1,NPG
        XX = 0.D0
        YY = 0.D0
        DO 2 INO = 1,NNO
          XX = XX + WI(NNO*(IPG-1)+INO)*X(INO)
          YY = YY + WI(NNO*(IPG-1)+INO)*Y(INO)
2       CONTINUE
        XX = -1.D0+2.D0*(XX-XMIN)/(XMAX-XMIN)
        YY = -1.D0+2.D0*(YY-YMIN)/(YMAX-YMIN)
        B(1) =  1.D0
        B(2) =  XX
        B(3) =  YY
        B(4) =  XX*YY
        B(5) =  XX*XX
        B(6) =  YY*YY
        B(7) =  XX*B(4)
        B(8) =  YY*B(4)
        B(9) =  B(6)*B(5)
C
        IAD = (IMA-1)*4*NPG+(IPG-1)*4
        DO 3 I = 1,NNO
          DO 3 J = 1,4
          F(I,J) = F(I,J) + B(I) * SIG(IAD+J)
3         CONTINUE
1     CONTINUE
C
      END
