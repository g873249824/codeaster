C
C ----------------------------------------------------------------------
C
C ROUTINE CALCUL ELEMENTAIRE
C
C CALCUL DES RIGIDITES ELEMENTAIRES EN MECANIQUE CORRESPONDANT A UN
C CHARGEMENT EN PRESSION SUIVEUSE SUR DES FACES D'ELEMENTS
C ISOPARAMETRIQUES 3D
C
C LA PRESSION EST UNE FONCTION RELLE
C
C ----------------------------------------------------------------------
C
C
C IN  OPTION : OPTION DE CALCUL
C               RIGI_MECA_PRSU_F
C IN  NOMTE  : NOM DU TYPE ELEMENT
C
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER       MXNOEU,MXVECT,MXMATR
      PARAMETER     (MXNOEU=27,MXVECT=3*27,MXMATR=3*3*27*27)
      INTEGER       MXPARA
      PARAMETER     (MXPARA=4)
C
      CHARACTER*8   NOMPAR(MXPARA)
      REAL*8        VALPAR(MXPARA)
      INTEGER       IER
      REAL*8        X,Y,Z
C
      INTEGER       NDIM,NNO,NPG,NNOS,NDDL
      INTEGER       IDDL,INO,IPG
      INTEGER       JPOIDS,JVF,JDF,JGANO
      INTEGER       JMATR
      INTEGER       JGEOM,JDEPM,JDEPP,JPRES,JTIME
      INTEGER       KDEC,I,J,KK
C
      REAL*8        PR,P(MXNOEU)
      REAL*8        VECT(MXVECT),MATR(MXMATR)
C
C ----------------------------------------------------------------------
C

C
C --- CARACTERISTIQUES ELEMENT
C
      CALL ELREF4(' '   ,'RIGI',NDIM  ,NNO   ,NNOS  ,
     &            NPG   ,JPOIDS,JVF   ,JDF   ,JGANO )
      NDDL = 3*NNO
      CALL ASSERT(NNO.LE.MXNOEU)
      CALL ASSERT(NDIM.LE.3)
C
C --- ACCES CHAMPS
C
      CALL JEVECH('PGEOMER','L',JGEOM)
      CALL JEVECH('PDEPLMR','L',JDEPM)
      CALL JEVECH('PDEPLPR','L',JDEPP)
      CALL JEVECH('PPRESSF','L',JPRES)
      CALL JEVECH('PTEMPSR','L',JTIME)
      CALL JEVECH('PMATUNS','E',JMATR)
C
C --- REACTUALISATION DE LA GEOMETRIE PAR LE DEPLACEMENT
C
      DO 10 IDDL = 1,NDDL
        ZR(JGEOM+IDDL-1) = ZR(JGEOM+IDDL-1) +
     &                     ZR(JDEPM+IDDL-1) +
     &                     ZR(JDEPP+IDDL-1)
 10   CONTINUE
C
C --- CALCUL DE LA PRESSION AUX POINTS DE GAUSS (FONCTION)
C
      NOMPAR(1) = 'X'
      NOMPAR(2) = 'Y'
      NOMPAR(3) = 'Z'
      NOMPAR(4) = 'INST'
      VALPAR(4) = ZR(JTIME)
C
      DO 100 IPG = 1,NPG
        KDEC = (IPG-1) * NNO
        X = 0.D0
        Y = 0.D0
        Z = 0.D0
        DO 105 INO = 1,NNO
          X = X + ZR(JGEOM+NDIM*(INO-1)+1-1) * ZR(JVF+KDEC+INO-1)
          Y = Y + ZR(JGEOM+NDIM*(INO-1)+2-1) * ZR(JVF+KDEC+INO-1)
          Z = Z + ZR(JGEOM+NDIM*(INO-1)+3-1) * ZR(JVF+KDEC+INO-1)
 105    CONTINUE
        VALPAR(1) = X
        VALPAR(2) = Y
        VALPAR(3) = Z
        CALL FOINTE('FM',ZK8(JPRES),MXPARA,NOMPAR,VALPAR,
     &              PR,IER)
        P(IPG) = PR
 100  CONTINUE
C
C --- CALCUL EFFECTIF DE LA RIGIDITE
C
      CALL NMPR3D(2      ,NNO   ,NPG   ,ZR(JPOIDS),ZR(JVF),
     &            ZR(JDF),ZR(JGEOM),P  ,VECT,MATR)
C
C --- RECOPIE DE LA MATRICE ELEMENTAIRE (NON-SYMETRIQUE)
C
      KK = 0
      DO 110 I = 1,NDDL
        DO 120 J = 1,NDDL
          KK = KK + 1
          ZR(JMATR-1+KK) = MATR(J*(NDDL-1)+I)
  120   CONTINUE
  110 CONTINUE
C
      END
