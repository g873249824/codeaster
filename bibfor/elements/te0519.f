C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C.......................................................................
C
C       REACTUALISATION DES GEOMETRIES DES FACETTES DE CONTACT
C       (MAITRE ET ESCLAVE)
C
C  OPTION : 'GEOM_FAC' (X-FEM GEOMETRIE DES FACETTES DE CONTACT)
C
C  ENTREES  ---> OPTION : OPTION DE CALCUL
C           ---> NOMTE  : NOM DU TYPE ELEMENT
C
C......................................................................
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX --------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR ,DDOT
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX --------------------

      CHARACTER*8   ELREF,TYPMA
      INTEGER       IGEOM,JDEPL,JPINT,JLON,JGES,JGMA
      INTEGER       JOUT1,JOUT2,JOUT3,JOUT4
      INTEGER       IADZI,IAZK24,NDIM,IBID,NINTER
      INTEGER       I,J,K,IPT,CPT,DDLH,NNO,DDLS
      REAL*8        PTREE(2),PTREF(2),DEPR(2),FF(4)
      REAL*8        COORDE(6),COORDM(6),NORME(3),NORMM(3)
C---------------------------------------------------------------------

      CALL JEMARQ()
C
      CALL ELREF1(ELREF)
      CALL ELREF4(' ','RIGI',NDIM,NNO,IBID,IBID,IBID,IBID,IBID,IBID)
C     CAS 2D POUR L'INSTANT (DDLH et DDLS sont en dur)
C---------------------------------------------------------
      DDLH=2
      DDLS=6
C---------------------------------------------------------
      CALL LCINVN(6,0.D0,COORDE)
      CALL LCINVN(6,0.D0,COORDM)
      CALL LCINVN(3,0.D0,NORME)
C
C     RECUPERATION DES ENTRÉES / SORTIE
C----------IGEOM NE SERT À RIEN ICI!!!------------
      CALL JEVECH('PGEOMER','L',IGEOM)
C-------------------------------------------------
      CALL JEVECH('PDEPLA','L',JDEPL)
      CALL JEVECH('PPINTER','L',JPINT)
      CALL JEVECH('PLONCHA','L',JLON)
      CALL JEVECH('PGESCLO','L',JGES)
      CALL JEVECH('PGMAITO','L',JGMA)

      CALL JEVECH('PNEWGES','E',JOUT1)
      CALL JEVECH('PNEWGEM','E',JOUT2)

      CALL TECAEL(IADZI,IAZK24)
C      TYPMA=ZK24(IAZK24-1+3+ZI(IADZI-1+2)+3)

C     RECUPERATION DU NOMBRE DES POINTS D'INTERSECTION
      NINTER=ZI(JLON-1+1)
      
C     I. FACETTES ESCLAVES
C---------------------------------------------------------------
C    pour l'instant, en 2D, on a une seule facette par maille
C    losrqu'on passera au cas 3D, il faudra prendre en compte 
C   le nombre des facettes
C---------------------------------------------------------------
    
C     BOUCLE SUR LES POINTS D'INTERSECTION
      DO 100 IPT=1,NINTER
        CALL LCINVN(NDIM,0.D0,DEPR)
        DO 110 J=1,NDIM
          PTREF(J)=ZR(JPINT-1+NDIM*(IPT-1)+J)
          PTREE(J)=ZR(JGES-1+NDIM*(IPT-1)+J)
 110    CONTINUE
C     CALCUL DES DEPLACEMENTS DANS LE POINT COURANT
        CALL ELRFVF(ELREF,PTREF,NNO,FF,NNO)
        DO 120 K=1,NNO
          CPT=0
C       DDLS CLASSIQUES
          DO 111 I=1,NDIM
            CPT=CPT+1
            DEPR(I)=DEPR(I)+FF(K)*ZR(JDEPL-1+DDLS*(K-1)+CPT) 
 111      CONTINUE             
C       DDLS HEAVISIDE
          DO 112 I=1,DDLH
            CPT=CPT+1
            DEPR(I)=DEPR(I)-FF(K)*ZR(JDEPL-1+DDLS*(K-1)+CPT)
 112      CONTINUE
 120    CONTINUE
C      CALCUL DES NOUVELLES COORDONNEES DU POINT D'INTERSECTION
        DO 130 I=1,NDIM
          ZR(JOUT1-1+NDIM*(IPT-1)+I)= PTREE(I) + DEPR(I)
          COORDE(3*(IPT-1)+I)=ZR(JOUT1-1+NDIM*(IPT-1)+I)
 130    CONTINUE
 
 100  CONTINUE

C     II. FACETTES MAITRES

C     BOUCLE SUR LES POINTS D'INTERSECTION
      DO 200 IPT=1,NINTER
        CALL LCINVN(NDIM,0.D0,DEPR)
        DO 210 J=1,NDIM
          PTREF(J)=ZR(JPINT-1+NDIM*(IPT-1)+J)
          PTREE(J)=ZR(JGMA-1+NDIM*(IPT-1)+J)
 210    CONTINUE
        
C     CALCUL DES DEPLACEMENTS DANS LE POINT COURANT 
        CALL ELRFVF(ELREF,PTREF,NNO,FF,NNO)
        DO 220 K=1,NNO
          CPT=0
C       DDLS CLASSIQUES
          DO 211 I=1,NDIM
            CPT=CPT+1
            DEPR(I)=DEPR(I)+FF(K)*ZR(JDEPL-1+DDLS*(K-1)+CPT)
 211      CONTINUE             
C       DDLS HEAVISIDE
          DO 212 I=1,DDLH
            CPT=CPT+1
            DEPR(I)=DEPR(I)+FF(K)*ZR(JDEPL-1+DDLS*(K-1)+CPT)
 212      CONTINUE   
 220    CONTINUE
C       CALCUL DES NOUVELLES COORDONNEES DU POINT D'INTERSECTION
        DO 230 I=1,NDIM
          ZR(JOUT2-1+NDIM*(IPT-1)+I)= PTREE(I) + DEPR(I)  
          COORDM(3*(IPT-1)+I)=ZR(JOUT2-1+NDIM*(IPT-1)+I)
 230    CONTINUE
 
   
 200  CONTINUE

      CALL JEDEMA()
      END
