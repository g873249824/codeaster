      IMPLICIT NONE
      INTEGER NNO
      REAL*8 X(4),Y(4),SURF,CENTRE(2),DEUX,TROIS,QUATRE,PVECT
      PARAMETER (DEUX=2.D+0,TROIS=3.D+0,QUATRE=4.D+0)

C --- LA SURFACE D'UN TRIANGLE EST EGALE A LA MOITIE DE LA VALEUR
C --- ABSOLUE DU PRODUIT VECTORIEL DE 2 COTES
      PVECT = (X(2)-X(1))* (Y(3)-Y(1)) - (Y(2)-Y(1))* (X(3)-X(1))
      SURF = ABS(PVECT/DEUX)
C --- CAS DU TRIANGLE
      IF (NNO.EQ.3) THEN
        CENTRE(1) = (X(1)+X(2)+X(3))/TROIS
        CENTRE(2) = (Y(1)+Y(2)+Y(3))/TROIS
      ELSE
C --- SI QUADRILATERE, ON COUPE EN 2 TRIANGLES
        CENTRE(1) = (X(1)+X(2)+X(3)+X(4))/QUATRE
        CENTRE(2) = (Y(1)+Y(2)+Y(3)+Y(4))/QUATRE
        PVECT = (X(4)-X(1))* (Y(3)-Y(1)) - (Y(4)-Y(1))* (X(3)-X(1))
        SURF = SURF + ABS(PVECT/DEUX)
      END IF
      END
