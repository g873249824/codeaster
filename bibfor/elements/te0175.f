C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR   
C (AT YOUR OPTION) ANY LATER VERSION.                                 
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT 
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF          
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU    
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                            
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C.......................................................................
C
C     BUT: CALCUL DU VECTEUR INTENSITE ACTIVE AUX NOEUDS
C          ELEMENTS ISOPARAMETRIQUES 2D
C
C          OPTION : 'INTE_ELNO_ACTI'
C              OU : 'INTE_ELNO_REAC'
C
C     ENTREES  ---> OPTION : OPTION DE CALCUL
C              ---> NOMTE  : NOM DU TYPE ELEMENT
C.......................................................................
C
      IMPLICIT REAL*8 (A-H,O-Z)
      INTEGER            IDFDE,IGEOM,IDINO
      INTEGER            IINTE,IPRES,IMATE,IFREQ,NPG,IPOIDS,IVF
      INTEGER            NNO,INO,I,NDIM,NNOS,JGANO,MATER
      REAL*8             OMERHO,PI,DFDX(9),DFDY(9),JAC,R8B,R8PI,RHO
      CHARACTER*2        CODRET
      CHARACTER*16       NOMTE,OPTION
      COMPLEX*16         VITX,VITY
C
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
C
      CALL JEMARQ()

      CALL ELREF4(' ','NOEU',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)
C
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PINTEAC','E',IINTE)
      CALL JEVECH('PPRESSC','L',IPRES)
      CALL JEVECH('PMATERC','L',IMATE)
      MATER = ZI(IMATE)
      CALL RCVALA(MATER,'FLUIDE',0,'   ',R8B,1,'RHO',RHO,CODRET,'FM')
C
      DO 112 I=1,2*NNO
         ZR(IINTE + I -1) = 0.0D0
 112  CONTINUE
C
      CALL JEVEUO('FREQ.VALE','L',IFREQ)
      PI = R8PI()
      OMERHO = 2.D0* PI * ZR(IFREQ)*RHO
C
C    BOUCLE SUR LES NOEUDS
C
      DO 101 INO = 1,NPG
        IDINO = IINTE + (INO-1) * 2 -1
C
        CALL DFDM2D(NNO,INO,IPOIDS,IDFDE,ZR(IGEOM),DFDX,DFDY,JAC)
C
       VITX     = (0.0D0 ,0.0D0 )
       VITY     = (0.0D0 ,0.0D0 )
C
         DO 106 I=1 , NNO
C
       VITX      = VITX      + DFDX(I) * ZC(IPRES+I-1)
       VITY      = VITY      + DFDY(I) * ZC(IPRES+I-1)
 106     CONTINUE
C
       VITX      = VITX      *(0.D0,1.D0)/OMERHO
       VITY      = VITY      *(0.D0,1.D0)/OMERHO
C
      IF ( OPTION.EQ.'INTE_ELNO_ACTI') THEN
C
       ZR(IDINO+1) = 0.5D0* DBLE(ZC(IPRES+INO-1)*DCONJG(VITX     ))
       ZR(IDINO+2) = 0.5D0* DBLE(ZC(IPRES+INO-1)*DCONJG(VITY     ))
       ELSE
C
       ZR(IDINO+1) = 0.5D0*DIMAG(ZC(IPRES+INO-1)*DCONJG(VITX     ))
       ZR(IDINO+2) = 0.5D0*DIMAG(ZC(IPRES+INO-1)*DCONJG(VITY     ))
      ENDIF
 101  CONTINUE
C
      CALL JEDEMA()
      END
