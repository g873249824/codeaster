C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT NONE
C
C.......................................................................
C FONCTION REALISEE:
C
C      CALCUL 
C      DE LA FORME BILINEAIRE SYMETRIQUE G(U,V)
C      
C      POUR LES ELEMENTS ISOPARAMETRIQUES 3D
C
C      OPTION : 'CALC_G_BILI'   
C
C ENTREES  ---> OPTION : OPTION DE CALCUL
C          ---> NOMTE  : NOM DU TYPE ELEMENT
C.......................................................................
C
      CHARACTER*2  CODRET(2)
      CHARACTER*8  NOMRES(2),ELREFE
      CHARACTER*16 NOMTE,OPTION
      CHARACTER*24 CHVAL,CHCTE
C
      REAL*8   EPSI,R8PREM
      REAL*8   DFDI(81),F(3,3),EPS(6)
      REAL*8   DUDM(3,3),DVDM(3,3),DTDM(3,3),DER(4)
      REAL*8   RBID,E,NU
      REAL*8   THET,TG
      REAL*8   XG,YG
      REAL*8   C1,C2,C3
      REAL*8   VALRES(2),DEVRES(3)
      REAL*8   GELEM,GUV3,G,POIDS
C
      INTEGER  IPOIDS,IVF,IDFDE,IDFDK,NNO,KP,NPG1,COMPT
      INTEGER  IGEOM,ITHET,IFIC,IDEPU,IDEPV,ITEMP,IDFDN
      INTEGER  IMATE,JIN,JVAL,K,I,J,L,NDIM
      INTEGER  NBFPG,NBPG(10)
C
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      CALL JEMARQ()
      EPSI = R8PREM()
C
      CALL ELREF1(ELREFE)
C
      CHCTE = '&INEL.'//ELREFE//'.CARACTE'
      CALL JEVETE(CHCTE,' ',JIN)
      NDIM  = ZI(JIN+1-1)
      NNO   = ZI(JIN+2-1)
      NBFPG = ZI(JIN+3-1) 
      DO 5 I = 1,NBFPG
        NBPG(I) = ZI(JIN+3-1+I)
5     CONTINUE
      NPG1 = NBPG(1)
C
      CHVAL = '&INEL.'//ELREFE//'.FFORMES'
      CALL JEVETE(CHVAL,' ',JVAL)
      IPOIDS = JVAL +(NDIM+1)*NNO*NNO
      IVF    = IPOIDS + NPG1
      IDFDE  = IVF    + NPG1*NNO
      IDFDN  = IDFDE  + 1
      IDFDK  = IDFDN  + 1
C
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PDEPLAU','L',IDEPU)
      CALL JEVECH('PDEPLAV','L',IDEPV)
      CALL JEVECH('PTHETAR','L',ITHET)
      CALL JEVECH('PTEMPER','L',ITEMP)
      CALL JEVECH('PMATERC','L',IMATE)
C
      CALL JEVECH('PGTHETA','E',IFIC)
C
C - PAS DE CALCUL DE G POUR LES ELEMENTS OU LA VALEUR DE THETA EST NULLE
C
      GUV3   = 0.D0
      COMPT = 0
      DO 15 I=1,NNO
        THET = 0.D0
        DO 14 J=1,NDIM
          THET = THET + ABS(ZR(ITHET+NDIM*(I-1)+J-1))
14      CONTINUE
        IF(THET.LT.EPSI) COMPT = COMPT+1
15    CONTINUE
      IF(COMPT.EQ.NNO)  GOTO 9999
C
      NOMRES(1) = 'E'
      NOMRES(2) = 'NU'
C
C ======================================================================
C
      DO 800 KP=1,NPG1
        L  = (KP-1)*NNO
        TG = 0.D0
        XG = 0.D0
        YG = 0.D0
        DO 220 I=1,NDIM
          DO 210 J=1,NDIM
            DUDM(I,J) = 0.D0
            DVDM(I,J) = 0.D0
            DTDM(I,J) = 0.D0
210       CONTINUE
220     CONTINUE
C
C - CALCUL DES ELEMENTS GEOMETRIQUES
C
        CALL NMGEOM (NDIM,NNO,.FALSE.,.FALSE.,ZR(IGEOM),KP,
     &               ZR(IPOIDS+KP-1),ZR(IVF+L),ZR(IDFDE),ZR(IDFDN),
     &               ZR(IDFDK),ZR(IDEPU),POIDS,DFDI,F,EPS,RBID)
C
C - CALCULS DES GRADIENTS DE U ET V (DUDM ET DVDM),THETA (DTDM) 
C   DE LA TEMPERATURE AUX POINTS DE GAUSS (TG) 
C
        DO 320 I=1,NNO
          DER(1) = DFDI(I)
          DER(2) = DFDI(I+NNO)
          DER(3) = DFDI(I+2*NNO)
          DER(4) = ZR(IVF+L+I-1)
          TG = TG + ZR(ITEMP+I-1)*DER(4)
          XG = XG + ZR(IGEOM+2*(I-1)  )*DER(4)
          YG = YG + ZR(IGEOM+2*(I-1)+1)*DER(4)
          DO 310 J=1,NDIM
            DO 300 K=1,NDIM
              DUDM(J,K) = DUDM(J,K) + ZR(IDEPU+NDIM*(I-1)+J-1)*DER(K)
              DVDM(J,K) = DVDM(J,K) + ZR(IDEPV+NDIM*(I-1)+J-1)*DER(K)
              DTDM(J,K) = DTDM(J,K) + ZR(ITHET+NDIM*(I-1)+J-1)*DER(K)
300         CONTINUE
310       CONTINUE
320     CONTINUE
        CALL RCVADA (ZI(IMATE),'ELAS',TG,2,NOMRES,VALRES,DEVRES,
     &               CODRET)
        E     = VALRES(1)
        NU    = VALRES(2)
        C3 = E/(2.D0*(1.D0+NU))
        C1 = E*(1.D0-NU)/((1.D0+NU)*(1.D0-2.D0*NU))
        C2 = NU/(1.D0-NU)*C1
C
C   INTRODUCTION DE U1S ET U2S DANS G(U,V)
C
        GELEM =0.D0
        CALL GBIL3D(DUDM,DVDM,DTDM,POIDS,C1,C2,C3,GELEM)
        GUV3  = GUV3 + GELEM
800   CONTINUE
C
      G = GUV3
C
      ZR(IFIC)   = G
C
9999  CONTINUE
      CALL JEDEMA()
      END
