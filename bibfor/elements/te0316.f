      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      CALL ELREF1(ELREFE)
C
      CARAC = '&INEL.'//ELREFE//'.CARAC'
      CALL JEVETE(CARAC,'L',ICARAC)
      NNO = ZI(ICARAC)
      NDI = NNO*(NNO+1)/2
      NPG1 = ZI(ICARAC+2)

C
      CHVAL = '&INEL.'//ELREFE//'.FF'
      CALL JEVETE(CHVAL,'L',IFF)
C
      IPOIDS = IFF
      IVF    = IPOIDS + NPG1
      IDFDE  = IVF    + NPG1 * NNO
C
      CALL JEVECH('PGEOMER','L',IGEOM)
C

      CALL JEVECH('PMATTTR','E',IMATTT)
C
      DO 11 I = 1,NDI
         ZR(IMATTT + I -1) = 0.0D0
11    CONTINUE
C
C     BOUCLE SUR LES POINTS DE GAUSS
C
      DO 101 KP=1,NPG1
         LDEC=(KP-1)*NNO

C
         NX = 0.0D0
         NY = 0.0D0

C ON CALCULE L ACCEL AU POINT DE GAUSS


         CALL VFF2DN(NNO,ZR(IPOIDS+KP-1),ZR(IDFDE+LDEC),ZR(IGEOM),NX,
     &            NY,POIDS)

          NORM(1) = NX
          NORM(2) = NY

C CAS AXISYMETRIQUE

        IF ( NOMTE(3:4) .EQ. 'AX' ) THEN
           R = 0.D0
           DO 102 I=1,NNO
             R = R + ZR(IGEOM+2*(I-1))*ZR(IVF+LDEC+I-1)
102        CONTINUE
           POIDS = POIDS*R
        ENDIF


CCDIR$ IVDEP

        DO 103 I=1,NNO
          DO 104 J=1,I
            IJ = (I-1)*I/2 +J
           ZR(IMATTT + IJ -1) = ZR(IMATTT + IJ -1) + POIDS*NORM(1)*
     &                       ZR(IVF+LDEC+I-1)*ZR(IVF+LDEC+J-1)
104       CONTINUE
103     CONTINUE



101   CONTINUE
C
 9999 CONTINUE
      END
