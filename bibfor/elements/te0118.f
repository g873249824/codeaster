C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT NONE
      CHARACTER*16 OPTION,NOMTE
C ----------------------------------------------------------------------
C     BUT: CALCUL DE LA MATRICE DE PASSAGE NOEUDS -> GAUSS
C          POUR LES ELEMENTS ISOPARAMETRIQUES 2D
C ----------------------------------------------------------------------
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXR8,JEXATR
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      CHARACTER*24 CARAC,FF
      INTEGER NNO,KP,INO,K,ICARAC,IFF,IVF,NNO2
      INTEGER NPG,NPG1,JMANO,JTAB(7),IADZI,IAZK24

C
      LOGICAL AXI,DPLAN,TRAITE
      LOGICAL VOL2,BORD2,VOL3,BORD3
      LOGICAL ISTHT3
      LOGICAL ISTHQ4
      LOGICAL ISTHT6
      LOGICAL ISTHQ8
      LOGICAL ISTHS2
      LOGICAL ISTHS3
      LOGICAL ISTHF8,ISTHF6
C
      LOGICAL ISTH10,ISTH13,ISTH15,ISTH20
      INTEGER NSOM
C
      INTEGER NNOMAX,NVOMAX,NSOMAX
      PARAMETER(NNOMAX=20,NVOMAX=4,NSOMAX=8)
      INTEGER VOISIN(NVOMAX,NNOMAX)
      INTEGER NBVOS(NSOMAX)
C
      LOGICAL P2P1,LUMPED
      CHARACTER*8 ELREFE,KBID
      LOGICAL THLU
      INTEGER FAMLUM,NBFMAX,IFAM
      PARAMETER (NBFMAX=10)
      INTEGER NBPG(NBFMAX),NBFPG
      INTEGER I, II,IPOIDS,NPGPRE
C
C
C  ON APPELLE CAETHM AVEC POUR LES ELEMENTS VOLUMIQUES
C  2D EN METTANT VOL2 SACHANT QUE ALORS POUR LES ELEMENTS
C     TRAITE
C
      CALL ELREF1(ELREFE)
      BORD2 = .FALSE.
      BORD3 = .FALSE.
      VOL2  = .TRUE.
      VOL3  = .FALSE.
C
      CALL CAETHM(NOMTE,VOL2,BORD2,VOL3,BORD3,
     > KBID,AXI,DPLAN,TRAITE,
     > ISTHS2,ISTHS3,ISTHF8,ISTHF6,ISTH10,ISTH13,ISTH15,ISTH20,
     > ISTHT3,ISTHQ4,ISTHT6,ISTHQ8,
     > NNOMAX,NVOMAX,NSOMAX,
     > NSOM,VOISIN,NBVOS,P2P1,LUMPED)
C


      CALL TECAEL(IADZI,IAZK24)
      NNO=ZI(IADZI-1+2)
      CARAC = '&INEL.'//ELREFE//'.CARAC '
      CALL JEVETE(CARAC,'L',ICARAC)
      FF = '&INEL.'//ELREFE//'.FF'
      CALL JEVETE(FF,'L',IFF)
      NNO2 = ZI(ICARAC)
      CALL ASSERT(NNO.EQ.NNO2)
C
      IF (ISTHQ4.OR.ISTHT6.OR.ISTHQ8) THEN
       IF ( LUMPED) THEN
        CALL ALTHLU(ELREFE,THLU,FAMLUM)
        IFAM = FAMLUM
       ELSE
        IFAM = 2
       ENDIF
        NBFPG = ZI(ICARAC+2-1)
          IF ( NBFPG.GT.NBFMAX) THEN
            CALL UTMESS('F','TE0118','TROP DE FAMILLES DE GAUSS')
          ENDIF
        DO 1  I = 1,NBFPG
          NBPG(I) = ZI(ICARAC+2-1+I)
   1    CONTINUE
        NPG = NBPG(IFAM)
        IPOIDS=IFF
        IF (IFAM.GT.1) THEN
          NPGPRE=0
          DO 2  II = 1,IFAM-1
           NPGPRE=NPGPRE+NBPG(II)
  2       CONTINUE
          IPOIDS=IPOIDS + NPGPRE*(1+3*NNO)
        ENDIF
        IVF   =IPOIDS+NPG
      ELSE
C
       NPG1 = ZI(ICARAC+2)
       IVF = IFF + NPG1
       NPG = NPG1
      ENDIF

      CALL TECACH(.TRUE.,.TRUE.,'PMANOPG',7,JTAB)
      CALL ASSERT(JTAB(6).EQ.(NPG*NNO+2))
      JMANO = JTAB(1)

      ZR(JMANO-1+1) = NNO
      ZR(JMANO-1+2) = NPG

      DO 20 KP = 1,NPG
        K = (KP-1)*NNO
        DO 10 INO = 1,NNO
          ZR(JMANO-1+2+NNO* (KP-1)+INO) = ZR(IVF+K+INO-1)
   10   CONTINUE
   20 CONTINUE

      END
