C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C
      REAL*8  DLAGNO(36)
      INTEGER IGEOM, IPOIDS, IVF, IDFDE, NNO, NPG
      INTEGER ICONT, IDLAGS, IDEULS, ITHETA
      INTEGER NCMP, NBSP, NNOS, NDIM, JGANO
C     ------------------------------------------------------------------
C
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)
C
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PCONTRR','L',ICONT)
      CALL JEVECH('PVECTTH','L',ITHETA)
      CALL JEVECH('PDLAGSI','L',IDLAGS)
C
      CALL JEVECH('PDESINO','E',IDEULS)
C
C    PASSAGE DLSI GAUSS -> NOEUDS
C
      NCMP = 4
      NBSP = 1
C
      CALL PPGAN2 ( JGANO, NBSP, NCMP, ZR(IDLAGS), DLAGNO )
C
C    3E FAMILLE DE POINTS D'INTEGRATION : LES NOEUDS
C
      CALL ELREF4(' ','NOEU',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)
C
C    CALCUL DE LA DERIVEE EULERIENNE
C
      CALL SELE2D(NNO,IPOIDS,IDFDE,ZR(IGEOM),ZR(ITHETA),
     &            NCMP,ZR(ICONT),DLAGNO,ZR(IDEULS))
C
      END
