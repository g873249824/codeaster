C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C
C    - FONCTION REALISEE :
C         CALCUL DE LA DERIVEE EULERIENNE DU TENSEUR DES CONTRAINTES
C         A PARTIR DE LA DERIVEE LAGRANGIENNE DE CE MEME TENSEUR
C         CHAMP AUX NOEUDS PAR ELEMENT
C
C          OPTION : 'DESI_ELNO_DLSI'
C
C    DERIVEE EULERIENNE DE S = DERIVEE LAGRANGIENNE DE S - GRAD(S).THETA
C
C    - ELEMENTS ISOPARAMETRIQUES 2D
C
C    - ARGUMENTS:
C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT
C ......................................................................
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C
      CHARACTER*24 CARAC, FF
      CHARACTER*8  ELREFE
C
      REAL*8 DLAGNO(36)
      INTEGER IGEOM, ICARAC
      INTEGER IFF, IPOIDS, IVF, IDFDE, IDFDK, OFFSET
      INTEGER NNO, NPG1, NPG2, NPG3
      INTEGER ICONT, IDLAGS, IDEULS
      INTEGER ITHETA
      INTEGER NCMP, NNOS
C     ------------------------------------------------------------------
C
C    NOMBRE DE POINTS DE GAUSS (FAMILLE 1)
C
      CALL ELREF1(ELREFE)
C
      CARAC='&INEL.'//ELREFE//'.CARAC'
      CALL JEVETE(CARAC,'L',ICARAC)
      NPG1 = ZI(ICARAC+2)
C
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PCONTRR','L',ICONT)
      CALL JEVECH('PVECTTH','L',ITHETA)
      CALL JEVECH('PDLAGSI','L',IDLAGS)
C
      CALL JEVECH('PDESINO','E',IDEULS)
C
C    PASSAGE DLSI GAUSS -> NOEUDS
C
      NCMP = 4
      IF     (NOMTE(5:8).EQ.'TR6 ') THEN
         NNOS = 3
      ELSE IF(NOMTE(5:8).EQ.'QS8 ') THEN
         NNOS = 4
      ELSE IF(NOMTE(5:8).EQ.'QU8 ' .OR.  NOMTE(5:8).EQ.'QU9 ' ) THEN
         NNOS = 4
      ELSE
        CALL UTMESS('F','TE0559','ELEMENTS LINEAIRES INTERDITS')
      ENDIF
C
      CALL PPGANO(NNOS,NPG1,NCMP,ZR(IDLAGS),DLAGNO)
C
C    3E FAMILLE DE POINTS D'INTEGRATION : LES NOEUDS
C
      NNO  = ZI(ICARAC)
      NPG2 = ZI(ICARAC+3)
      NPG3 = ZI(ICARAC+4)
C
      FF   ='&INEL.'//ELREFE//'.FF'
      CALL JEVETE(FF,'L',IFF)
      OFFSET = (3*NNO+1) * (NPG1+NPG2)
      IPOIDS = IFF   + OFFSET
      IVF    = IPOIDS+ NPG3
      IDFDE  = IVF   + NPG3*NNO
      IDFDK  = IDFDE + NPG3*NNO
C
C    CALCUL DE LA DERIVEE EULERIENNE
      CALL SELE2D(NNO,ZR(IDFDE),ZR(IDFDK),ZR(IGEOM),ZR(ITHETA),
     &            NCMP,ZR(ICONT),DLAGNO,ZR(IDEULS))
C
      END
