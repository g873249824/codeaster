C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C     ------------------------------------------------------------------
C --- EFFORTS GENERALISES D'ORIGINE MECANIQUE AUX POINTS DE CALCUL
C --- POUR LES ELEMENTS COQUES A FACETTES PLANES :
C --- DST, DKT, DSQ, DKQ, Q4G
C     ------------------------------------------------------------------
C     IN  NOMTE        : NOM DU TYPE D'ELEMENT
C     IN  OPTION       : NOM DE L'OPTION
C     IN  XYZL(3,NNO)  : COORDONNEES DES CONNECTIVITES DE L'ELEMENT
C                        DANS LE REPERE LOCAL DE L'ELEMENT
C     IN  PGL(3,3)     : MATRICE DE PASSAGE DU REPERE GLOBAL AU REPERE
C                        LOCAL
C     IN  DEPL(1)      : VECTEUR DES DEPLACEMENTS AUX NOEUDS
C     OUT EFFG(1)      : EFFORTS  GENERALISES D'ORIGINE MECANIQUE
C                        AUX POINTS DE CALCUL
C     ------------------------------------------------------------------
      INTEGER MULTIC
C     ------------------------------------------------------------------
C
      IF (NOMTE.EQ.'MEDKTR3 ' .OR.
     &    NOMTE.EQ.'MEDKTG3 ' ) THEN
        CALL DKTEDG(XYZL,OPTION,PGL,DEPL,EFFG,MULTIC)
C
      ELSE IF (NOMTE.EQ.'MEDSTR3 ') THEN
        CALL DSTEDG(XYZL,OPTION,PGL,DEPL,EFFG)
C
      ELSE IF (NOMTE.EQ.'MEDKQU4 '.OR.
     &         NOMTE.EQ.'MEDKQG4 ') THEN
        CALL DKQEDG(XYZL,OPTION,PGL,DEPL,EFFG)
C
      ELSE IF (NOMTE.EQ.'MEDSQU4 ') THEN
        CALL DSQEDG(XYZL,OPTION,PGL,DEPL,EFFG)
C
      ELSE IF (NOMTE.EQ.'MEQ4QU4 ') THEN
        CALL Q4GEDG(XYZL,OPTION,PGL,DEPL,EFFG)
C
      ELSE IF (NOMTE.EQ.'MET3TR3 ') THEN
        CALL T3GEDG(XYZL,OPTION,PGL,DEPL,EFFG)
C
      ELSE
        CALL U2MESK('F','ELEMENTS_14',1,NOMTE(1:8))
      END IF
C
      END
