C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C.......................................................................
C
C                CONTACT X-FEM MÉTHODE CONTINUE : 
C             MISE À JOUR DU SEUIL DE FROTTEMENT GRAND GLISSEMENT
C
C
C  OPTION : 'XREACL' (X-FEM MISE À JOUR DU SEUIL DE FROTTEMENT)

C  ENTREES  ---> OPTION : OPTION DE CALCUL
C           ---> NOMTE  : NOM DU TYPE ELEMENT
C
C......................................................................
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX --------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR 
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX --------------------

      INTEGER      I,J,NFAES,INI,PLI
      INTEGER      NDIM,NDDL,NDDLC,NNE,NNES,NNM,NNC
      INTEGER      IDEPL,JPCAI,JPCPO,JSEUIL,IAINES
      INTEGER      IRET,CFACE(5,3),XOULA
      REAL*8       XPC,YPC
      REAL*8       FFPC(9),DFFPC(2,9),DDFFPC(3,9)
      CHARACTER*8  ESC,MAIT,ELC,ESQ
      CHARACTER*19 AINTES
      REAL*8       LAMBDA
C......................................................................

      CALL JEMARQ()
C
C --- INFOS SUR LA MAILLE DE CONTACT
C
      CALL XMELET(NOMTE,
     &            NDIM,NDDL,NDDLC,ESC,ESQ,NNE,MAIT,NNM,ELC,NNC)
      IF (NDDL.GT.81) THEN
        CALL U2MESS('F','ELEMENTS3_79')
      ENDIF
C
C --- INITIALISATIONS
C
      DO 10 I=1,5
        DO 20 J=1,3
          CFACE(I,J)=0
  20     CONTINUE
  10   CONTINUE
C
      AINTES='&&TE0588.AINTE'
C
C --RECUPERATION DES DONNEES DE LA CARTE CONTACT 'POINT' (VOIR MMCART)
      CALL JEVECH('PCAR_PT','L',JPCPO)
      XPC      = ZR(JPCPO-1+1)
      YPC      = ZR(JPCPO-1+10)
C
C---LES NUMEROS DES FACETTES DE CONTACT (ESCLAVE) DONT LE
C---PTC ET SON PROJETE APPARTIENT
      NFAES    = NINT(ZR(JPCPO-1+31))
C
C --RECUPERATION DES DONNEES DE LA CARTE CONTACT 'AINTER' (VOIR MMCART)
      CALL JEVECH('PCAR_AI','L',JPCAI)
C
C-----NOMBRE DE NOEUDS ESCLAVES SOMMETES(EN 3D IL FAUDRA MODIFIER ICI)
      NNES=NNE/2
C
C --- FONCTIONS DE FORMES ET DERIVEES POUR LE
C --- POINT DE CONTACT
C
      CALL XMMFFD(ELC,XPC,YPC,
     &            FFPC,DFFPC,DDFFPC,IRET)     
C
C --- RECUPERATION DE LA GEOMETRIE ET DES CHAMPS DE DEPLACEMENT
C
      CALL JEVECH('PDEPL_P','L',IDEPL)
C
C----CONSTRUCTION DU CHAMP AINTES (INFO SUR LES ARRETES
C----COUPÉS DANS LA MAILLE ESCLAVE)
C
      CALL WKVECT(AINTES,'V V R',24,IAINES)
      DO 30 I=1,24
        ZR(IAINES-1+I)= ZR(JPCAI-1+I)
  30  CONTINUE
C
C----ON CONSTRUIT LA MATRICE DE CONNECTIVITÉ CFACE (MAILLE ESCLAVE)
C      NFACE=1
      CFACE(1,1)=1
      CFACE(1,2)=2
C
C----CALCUL DU SEUIL DE FROTEMENT LAMBDA
C 
        LAMBDA=0.D0
C
C----BOUCLE SUR LES POINTS DE L'ELEMENT
C
      DO 40 I = 1,NNC
        INI=XOULA(CFACE,NFAES,I,IAINES,ESQ)
            CALL XPLMA2(NDIM,NNE,NNES,INI,PLI)
            LAMBDA = LAMBDA + FFPC(I) * ZR(IDEPL-1+PLI)
  40     CONTINUE
C
C-----RECUPERATION ET ENREGISTREMENT DES CHAMPS DE SORTIE
C
      CALL JEVECH('PSEUIL','E',JSEUIL)
      ZR(JSEUIL)=LAMBDA
C
      CALL JEDETR(AINTES)
C
      CALL JEDEMA()
      END
