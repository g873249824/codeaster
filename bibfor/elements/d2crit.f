      SUBROUTINE D2CRIT (ZIMAT,NMNBN,NMPLAS,NMDPLA,NMPROX,CNBN,
     &         CPLAS,RPARA,CIEF,CDEPS,CDTG,CIER,CDEPSP,DC1,DC2)
