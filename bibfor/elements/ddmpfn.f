      REAL*8  NMNBN(6)
      REAL*8  NMPLAS(2,3)
      REAL*8  NMDPLA(2,2)
      REAL*8  NMDDPL(2,2)
      REAL*8  NMZEF
      REAL*8  NMZEG
      INTEGER NMIEF
      INTEGER NMPROX(2)
      INTEGER I,IER0,IER1,IER2,ZIMAT
C--------------------------------------------
      CHARACTER*24  CGLR
      INTEGER       IMP1,IMP2,IDMP1,IDMP2,IDDMP1,IDDMP2,INN,INM
      CHARACTER*8   NOMRES(4),DOMRES(4),DDMRES(4)
C--------------------------------------------

C----- LECTURE DES OBJETS "GLRC"

      NOMRES(1) = 'FMEX1'
      NOMRES(2) = 'FMEX2'
      NOMRES(3) = 'FMEY1'
      NOMRES(4) = 'FMEY2'

      DOMRES(1) = 'DFMEX1'
      DOMRES(2) = 'DFMEX2'
      DOMRES(3) = 'DFMEY1'
      DOMRES(4) = 'DFMEY2'
      
      DDMRES(1) = 'DDFMEX1'
      DDMRES(2) = 'DDFMEX2'
      DDMRES(3) = 'DDFMEY1'
      DDMRES(4) = 'DDFMEY2'

      DO 10, I=1,2
        CALL CDNFON(ZIMAT,DDMRES(2*(I-1)+1),NMNBN(I),0,
     &                 NMDDPL(1,I),IER0)
        IF (IER0 .GT. 0) THEN
          CALL CDNFON(ZIMAT,DOMRES(2*(I-1)+1),NMNBN(I),1,
     &                 NMDDPL(1,I),IER1)
          IF (IER1 .GT. 0) THEN
            CALL CDNFON(ZIMAT,NOMRES(2*(I-1)+1),NMNBN(I),2,
     &                 NMDDPL(1,I),IER2)
             IF (IER2 .EQ. 3) THEN
               CALL U2MESS('F','ELEMENTS_24')
             ENDIF
          ENDIF
        ENDIF
        CALL CDNFON(ZIMAT,DDMRES(2*I),NMNBN(I),0,
     &                 NMDDPL(2,I),IER0)
        IF (IER0 .GT. 0) THEN
          CALL CDNFON(ZIMAT,DOMRES(2*I),NMNBN(I),1,
     &                 NMDDPL(2,I),IER1)
          IF (IER1 .GT. 0) THEN
            CALL CDNFON(ZIMAT,NOMRES(2*I),NMNBN(I),2,
     &                 NMDDPL(2,I),IER2)
             IF (IER2 .EQ. 3) THEN
               CALL U2MESS('F','ELEMENTS_24')
             ENDIF
          ENDIF
        ENDIF
 10   CONTINUE

      END
