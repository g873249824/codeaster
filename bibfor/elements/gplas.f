      Y = MOM(2)-PLASMO(2)
      COEF = 1.001D0
      IF (PLASMO(1) .GT. 0.D0) THEN
            GPLAS =  MAX(COEF*X + Y, X/COEF + Y)
      ELSE
            GPLAS =  MAX(-COEF*X - Y,-X/COEF - Y)               
      ENDIF
      END 
