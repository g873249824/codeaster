C     BUT    : CALCUL DE LA FONCTION G ASSOCIE A LA FONCTION SEUIL F   
C
C IN  R  MOM     : MOMENT - MOMENT DE RAPPEL (M-BACKM)
C     R  PLASMO  : MOMENTS LIMITES ELASTIQUES DANS LE REPERE ORTHOTROPE
