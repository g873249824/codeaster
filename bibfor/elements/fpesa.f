C ======================================================================
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16  NOMTE
C
C
      INTEGER NB1, NPGSN
      REAL*8 RHO,EPAIS,PESAN,RNORMC
      REAL*8 XI(3,*),VPESAN(3),VECL(51),VECL1(42)
C     REAL*8 VECTC(3),VECPTX(3,3)
C
C-----------------------------------------------------------------------
      INTEGER I ,INTSN ,JPESA ,LZI ,LZR 
C-----------------------------------------------------------------------
      CALL JEVECH ('PPESANR','L',JPESA)
      PESAN = ZR(JPESA)
      DO 5 I=1,3
         VPESAN(I) = PESAN*ZR(JPESA+I)
 5    CONTINUE
C
      CALL JEVETE('&INEL.'//NOMTE(1:8)//'.DESI',' ', LZI )
      NB1   = ZI(LZI-1+1)
      NPGSN = ZI(LZI-1+4)
C
      CALL JEVETE('&INEL.'//NOMTE(1:8)//'.DESR',' ', LZR )
C
      CALL DXROEP ( RHO, EPAIS )
C
      CALL R8INIR ( 42, 0.D0, VECL1, 1 )
C
      DO 40 INTSN = 1,NPGSN
         CALL VECTCI(INTSN,NB1,XI,ZR(LZR),RNORMC)
         CALL FORPES(INTSN,NB1,ZR(LZR),RHO,EPAIS,VPESAN,RNORMC,VECL1)
 40   CONTINUE
C
      CALL VEXPAN ( NB1, VECL1, VECL )
      DO 60 I=1,3
         VECL(6*NB1+I) = 0.D0
 60   CONTINUE
C
      END
