C    - FONCTION REALISEE:  CREATION DES STRUCTURE DE DONNEES LIGREL
C      DE CHARGE EVENTUELLES QUI PEUVENT ETRE ASSOCIEES AU SOUS
C      DOMAINE I
C
C IN VLIMA : LISTE DES NUMEROS DE MAILLES CONSTITUANT LE SOUS-DOMAINE
C IN NBMA  : TAILLE DE CETTE LISTE
C IN VLIGR : LISTE DES NOMS DES LIGRELS
C IN NBLIGR: TAILLE DE CETTE LISTE
C IN/OUT VKLIMA: LISTE DES NOMS DE CES MAILLES          
C   -------------------------------------------------------------------
C     SUBROUTINES APPELLEES:
C       MESSAGE:UTDEBM,UTFINM,UTIMPI/K.
C       JEVEUX:JEMARQ,JEDEMA,JEVEUO,JENUNO,JEXNUM.
