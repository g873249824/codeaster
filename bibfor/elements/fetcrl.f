C CORPS DU PROGRAMME
      IMPLICIT NONE

C DECLARATION PARAMETRES D'APPELS
      INTEGER      NBMA,NBLIGR
      INTEGER      VLIMA(NBMA)
      CHARACTER*8  VKLIMA(NBMA)
      CHARACTER*24 VLIGR(NBLIGR)
      
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      
C DECLARATION VARIABLES LOCALES
      INTEGER I,INT1,IMAIL
      CHARACTER*8 MAIL,NOMAIL
      CHARACTER*24 MAILMA
      CHARACTER*32 JEXNUM
      
C CORPS DU PROGRAMME
      CALL JEMARQ()

C NOM DU MAILLAGE SOUS-JACENT AU MODELE
      CALL JEVEUO(VLIGR(1)(1:19)//'.NOMA','L',IMAIL)
      MAIL=ZK8(IMAIL)
      MAILMA=MAIL//'.NOMMAI'

      DO 20 I=1,NBMA
C NUMERO GLOBAL DE LA IEME MAILLE DU SOUS-DOMAINE NUMSD      
        INT1=VLIMA(I)
        
C ENRICHISSEMENT DE LA LISTE DES NOMS DE MAILLES DU SOUS-DOMAINE        
        CALL JENUNO(JEXNUM(MAILMA,INT1),NOMAIL)
        VKLIMA(I)=NOMAIL           
   20 CONTINUE
 
      CALL JEDEMA()
      END
