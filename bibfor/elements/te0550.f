C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C.......................................................................
      IMPLICIT REAL*8 (A-H,O-Z)
C
C     BUT: CALCUL DES MATRICES ELEMENTAIRES EN MECANIQUE
C          CORRESPONDANT A UNE IMPEDANCE IMPOSEE
C          SUR DES FACES D'ELEMENTS ISOPARAMETRIQUES 2D
C
C          OPTION : 'IMPE_ABSO'
C
C     ENTREES  ---> OPTION : OPTION DE CALCUL
C          ---> NOMTE  : NOM DU TYPE ELEMENT
C.......................................................................
C
      CHARACTER*2        CODRET
      CHARACTER*8        ELREFE
      CHARACTER*16       NOMTE,OPTION
      CHARACTER*24       CHVAL,CARAC
      REAL*8             NX,NY,POIDS, RHO,RHO2,A(6,6)
      REAL*8             VITES(6)
      INTEGER            IPOIDS,IVF,IDFDE,IGEOM,IMATE
      INTEGER            NDI,NNO,KP,NPG1,IMATUU,IIMPE
      INTEGER            LDEC,ICARAC,IFF
C
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      CALL ELREF1(ELREFE)
C
      CARAC = '&INEL.'//ELREFE//'.CARAC'
      CALL JEVETE(CARAC,'L',ICARAC)
      NNO = ZI(ICARAC)
      NPG1 = ZI(ICARAC+2)
      NDI = 2*NNO
C
      CHVAL = '&INEL.'//ELREFE//'.FF'
      CALL JEVETE(CHVAL,'L',IFF)
      IPOIDS = IFF
      IVF    = IPOIDS + NPG1
      IDFDE  = IVF    + NPG1 * NNO
C
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PVITPLU','L',IVITE)
      CALL JEVECH('PVITENT','L',IVIEN)
C
      CALL RCVALA(ZI(IMATE),'FLUIDE',0,' ',R8B,1,'CELE_R',CELER,
     &           CODRET,'FM')
      IF (CELER.LT.1.D-1) GOTO 999

      CALL JEVECH('PVECTUR','E',IVECTU)
C
C --- INITIALISATION DU VECTEUR DE CORRECTION
      DO 10 I = 1,NDI
         ZR(IVECTU+I-1) = 0.D0
10    CONTINUE
C
C --- INITIALISATION DE LA MATRICE D'IMPEDANCE
      DO 11 I = 1,NDI
         DO 12 J = 1,NDI
            A(I,J) = 0.D0
12       CONTINUE
11    CONTINUE
C
C
C     BOUCLE SUR LES POINTS DE GAUSS
C
      DO 101 KP=1,NPG1

         LDEC = (KP-1)*NNO

         NX = 0.0D0
         NY = 0.0D0
C
         CALL VFF2DN(NNO,ZR(IPOIDS+KP-1),ZR(IDFDE+LDEC),ZR(IGEOM),NX,
     &              NY,POIDS)
C%
        IF ( NOMTE(3:4) .EQ. 'AX' ) THEN
           R = 0.D0
           DO 102 I=1,NNO
             R = R + ZR(IGEOM+2*(I-1))*ZR(IVF+LDEC+I-1)
102        CONTINUE
           POIDS = POIDS*R
        ENDIF
C%
        DO 103 I=1,NNO
C
           DO 104 J=1,NNO
              II = 2*I
              JJ = 2*J-1
C
              A(II,JJ)=A(II,JJ) - POIDS / CELER *
     &              ZR(IVF+LDEC+I-1) * ZR(IVF+LDEC+J-1)
C
104        CONTINUE
103     CONTINUE

C     CALCUL DE LA VITESSE ABSOLUE
      DO 109 I = 1,NDI 
         VITES(I) = ZR(IVITE+I-1) + ZR(IVIEN+I-1)
109   CONTINUE

      DO 110 I = 1,NDI
         DO 112 J = 1,NDI
            ZR(IVECTU+I-1)=ZR(IVECTU+I-1)
     &               -A(I,J)*VITES(J)
112      CONTINUE
110   CONTINUE

101   CONTINUE
999   CONTINUE
    
      END
