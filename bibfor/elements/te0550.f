C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C.......................................................................
      IMPLICIT REAL*8 (A-H,O-Z)

C     BUT: CALCUL DES MATRICES ELEMENTAIRES EN MECANIQUE
C          CORRESPONDANT A UNE IMPEDANCE IMPOSEE
C          SUR DES FACES D'ELEMENTS ISOPARAMETRIQUES 2D

C          OPTION : 'IMPE_ABSO'

C     ENTREES  ---> OPTION : OPTION DE CALCUL
C          ---> NOMTE  : NOM DU TYPE ELEMENT
C.......................................................................

      CHARACTER*2 CODRET
      CHARACTER*16 NOMTE,OPTION
      REAL*8 NX,NY,POIDS,RHO,RHO2,A(6,6)
      REAL*8 VITES(6)
      INTEGER IPOIDS,IVF,IDFDE,IGEOM,IMATE
      INTEGER NDI,NNO,KP,NPG,IMATUU,IIMPE
      INTEGER LDEC

C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------

      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)
      NDI = 2*NNO

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PVITPLU','L',IVITE)
      CALL JEVECH('PVITENT','L',IVIEN)

      CALL RCVALA(ZI(IMATE),' ','FLUIDE',0,' ',R8B,1,'CELE_R',CELER,
     &            CODRET,'FM')
      IF (CELER.LT.1.D-1) GO TO 110

      CALL JEVECH('PVECTUR','E',IVECTU)

C --- INITIALISATION DU VECTEUR DE CORRECTION
      DO 10 I = 1,NDI
        ZR(IVECTU+I-1) = 0.D0
   10 CONTINUE

C --- INITIALISATION DE LA MATRICE D'IMPEDANCE
      DO 30 I = 1,NDI
        DO 20 J = 1,NDI
          A(I,J) = 0.D0
   20   CONTINUE
   30 CONTINUE


C     BOUCLE SUR LES POINTS DE GAUSS

      DO 100 KP = 1,NPG
        LDEC = (KP-1)*NNO
        NX = 0.0D0
        NY = 0.0D0
        CALL VFF2DN(NDIM,NNO,KP,IPOIDS,IDFDE,ZR(IGEOM),NX,NY,POIDS)
C%
        IF (NOMTE(3:4).EQ.'AX') THEN
          R = 0.D0
          DO 40 I = 1,NNO
            R = R + ZR(IGEOM+2* (I-1))*ZR(IVF+LDEC+I-1)
   40     CONTINUE
          POIDS = POIDS*R
        END IF
C%
        DO 60 I = 1,NNO

          DO 50 J = 1,NNO
            II = 2*I
            JJ = 2*J - 1

            A(II,JJ) = A(II,JJ) - POIDS/CELER*ZR(IVF+LDEC+I-1)*
     &                 ZR(IVF+LDEC+J-1)

   50     CONTINUE
   60   CONTINUE

C     CALCUL DE LA VITESSE ABSOLUE
        DO 70 I = 1,NDI
          VITES(I) = ZR(IVITE+I-1) + ZR(IVIEN+I-1)
   70   CONTINUE

        DO 90 I = 1,NDI
          DO 80 J = 1,NDI
            ZR(IVECTU+I-1) = ZR(IVECTU+I-1) - A(I,J)*VITES(J)
   80     CONTINUE
   90   CONTINUE

  100 CONTINUE
  110 CONTINUE

      END
