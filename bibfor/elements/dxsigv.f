      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*16 NOMTE, OPTION
      CHARACTER*4  FAMI
      INTEGER      NPG,JGANO
      REAL*8       XYZL(3,1) , PGL(3,1)
      REAL*8       DEPL(1)
      REAL*8       SIGTOT(1)
C     ------------------------------------------------------------------
C --- CONTRAINTES 'VRAIES' (I.E. SIGMA_MECA - SIGMA_THERM)
C --- AUX POINTS DE CALCUL POUR LES ELEMENTS COQUES A
C --- FACETTES PLANES : DST, DKT, DSQ, DKQ, Q4G
C     ------------------------------------------------------------------
C     IN  NOMTE        : NOM DU TYPE D'ELEMENT
C     IN  OPTION       : NOM DE L'OPTION
C     IN  XYZL(3,NNO)  : COORDONNEES DES CONNECTIVITES DE L'ELEMENT
C                        DANS LE REPERE LOCAL DE L'ELEMENT
C     IN  PGL(3,3)     : MATRICE DE PASSAGE DU REPERE GLOBAL AU REPERE
C                        LOCAL
C     IN  IC           : NUMERO DE LA COUCHE
C     IN  INIV         : NIVEAU DANS LA COUCHE (-1:INF , 0:MOY , 1:SUP)
C     IN  DEPL(1)      : VECTEUR DES DEPLACEMENTS AUX NOEUDS
C     OUT SIGTOT(1)    : CONTRAINTES VRAIES AUX POINTS
C                        DE CALCUL
C                          (I.E. SIGMA_MECA - SIGMA_THERMIQUES)
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C
      REAL*8     SIGTH(24)
C
C --- CALCUL DES CONTRAINTES D'ORIGINE MECANIQUE
C --- AUX POINTS DE CALCUL
C     --------------------
      CALL DXSIGM(FAMI,NOMTE, OPTION, XYZL, PGL, IC, INIV, DEPL,
     &            SIGTOT,NPG)
C
C --- CALCUL DES CONTRAINTES D'ORIGINE THERMIQUE
C --- AUX POINTS DE CALCUL
C     --------------------
      CALL DXSIGT(NOMTE, XYZL, PGL, IC, INIV, SIGTH)
C
C --- CALCUL DES CONTRAINTES 'VRAIES'
C --- AUX POINTS DE CALCUL
C     ------------------------
      DO 10 I =1, 24
            SIGTOT(I) = SIGTOT(I) - SIGTH(I)
 10   CONTINUE
C
      END
