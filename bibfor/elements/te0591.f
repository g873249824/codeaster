      INTEGER            NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO
C ......................................................................
C
      CALL ELREF4(' ','NOEU',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)

C    LECTURE DES CHAMPS

      CALL JEVECH('PGEOMER','L',IGEOM )
      CALL JEVECH('PTEMPER','L',ITEMP )
      CALL JEVECH('PVECTTH','L',ITHETA)
      CALL JEVECH('PDLAGTE','L',IDLAGT)
      CALL JEVECH('PDETENO','E',IDEULT)

C    CALCUL DE LA DERIVEE EULERIENNE

      CALL SELE2D ( NNO, IPOIDS, IDFDE, ZR(IGEOM), ZR(ITHETA), 1, 
     &              ZR(ITEMP), ZR(IDLAGT), ZR(IDEULT) )

      END
