C
      INTEGER NDIM,NFH,NFE,IBID,ITEMPN,IGEOM,NNOP,ITEMPG,JPINTT
      INTEGER JCNSET,JHEAVT,JLONCH,JBASLO,JLSN,JLST
      LOGICAL ISELLI
      CHARACTER*8 ELREFP
C
C ----------------------------------------------------------------------
C --- PREALABLES AU CALCUL
C ----------------------------------------------------------------------
C
C     ON INTERDIT LES ELTS QUADRATIQUES
      CALL ELREF1(ELREFP)
      CALL ASSERT(ISELLI(ELREFP))
C
C     CHAMPS IN
      CALL JEVECH('PGEOMER','L',IGEOM )
      CALL JEVECH('PTEMPER','L',ITEMPN)
      CALL JEVECH('PPINTTO','L',JPINTT)
      CALL JEVECH('PCNSETO','L',JCNSET)
      CALL JEVECH('PHEAVTO','L',JHEAVT)
      CALL JEVECH('PLONCHA','L',JLONCH)
      CALL JEVECH('PBASLOR','L',JBASLO)
      CALL JEVECH('PLSN'   ,'L',JLSN  )
      CALL JEVECH('PLST'   ,'L',JLST  )
C     CHAMPS OUT
      CALL JEVECH('PTEMPPG','E',ITEMPG)
C
C     ELT DE REF PARENT : RECUP NDIM ET NNOP (NOEUDS PARENT)
C     -> RQ : 'RIGI' POUR LA FAMILLE DE PG EST DONC SANS CONSQUENCE
      CALL ELREF4(' ','RIGI',NDIM,NNOP,IBID,IBID,IBID,IBID,IBID,IBID)
C
C     RECUP DE NFH (NBRE FCT HEAVISIDE) ET NFE (NBRE FCT SINGULIER)
      CALL XTHINI(NOMTE,NFH,NFE)
C
C ----------------------------------------------------------------------
C --- CALCUL DU CHAMP DE TEMPERATURE AUX POINTS DE GAUSS
C ----------------------------------------------------------------------
C
      CALL XTELGA(NDIM,ELREFP,NNOP,IGEOM,ZR(ITEMPN),ZI(JLONCH),
     &            ZI(JCNSET),JPINTT,ZR(JLSN),ZR(JLST),ZR(JBASLO),
     &            ZI(JHEAVT),NFH,NFE,ZR(ITEMPG))
C
      END
