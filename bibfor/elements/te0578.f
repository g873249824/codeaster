C
C     BUT: THERMIQUE LINEAIRE / ELEMENTS X-FEM LINEAIRES
C          OPTION : 'TEMP_ELGA'
C
C     ENTREES  ---> OPTION : OPTION DE CALCUL
C              ---> NOMTE  : NOM DU TYPE ELEMENT
C
