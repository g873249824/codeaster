C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------

      LOGICAL  AXI
      INTEGER  NDIM, NNO1, NPG1
      INTEGER  ICARA1,IFF,  IPOI1, IVF1
      INTEGER  IDFDE1, IDFDK1, IDFDN1
      INTEGER  IGEOM, IMATE, IDEPL, ITEMPS, IECHLI

      CHARACTER*8   ELREFE
      CHARACTER*24 CARAC, FF
C ......................................................................


      CALL ELREF1(ELREFE)
      AXI  = .FALSE.
      NDIM = 3

      CARAC = '&INEL.'//ELREFE//'.CARACTE'
      CALL JEVETE(CARAC,'L',ICARA1)
      NNO1   = ZI(ICARA1+1)
      NPG1   = ZI(ICARA1+3)

      FF = '&INEL.'//ELREFE//'.FFORMES'
      CALL JEVETE(FF,'L',IFF)
      IPOI1   = IFF   +4*NNO1*NNO1
      IVF1    = IPOI1   + NPG1
      IDFDE1  = IVF1    + NPG1*NNO1
      IDFDN1  = IDFDE1  + 1
      IDFDK1  = IDFDN1  + 1


      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PDEPLAR','L',IDEPL)
      CALL JEVECH('PTEMPSR','L',ITEMPS)
      CALL JEVECH('PECHLI', 'E',IECHLI)


C - CALCUL DE LA CONTRIBUTION ELEMENTAIRE A LA CHARGE LIMITE

      CALL NMHOLI(NDIM, AXI, NNO1, NPG1, ZR(IVF1),
     &       ZR(IDFDE1), ZR(IDFDN1), ZR(IDFDK1), ZR(IPOI1), ZI(IMATE),
     &       ZR(ITEMPS), ZR(IGEOM), ZR(IDEPL), ZR(IECHLI))

      END
