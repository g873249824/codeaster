C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C.......................................................................
C
C     BUT: CALCUL DES RIGIDITES ELEMENTAIRES EN MECANIQUE
C          CORRESPONDANT A UN CHARGEMENT EN PRESSION SUIVEUSE
C          SUR DES FACES D'ELEMENTS ISOPARAMETRIQUES 3D
C
C          OPTION : 'RIGI_MECA_PRSU_R '
C
C     ENTREES  ---> OPTION : OPTION DE CALCUL
C              ---> NOMTE  : NOM DU TYPE ELEMENT
C.......................................................................
C
      IMPLICIT NONE
      CHARACTER*16  NOMTE,OPTION
      INTEGER       NDIM,NNO,NPG,NNOS,JGANO
      INTEGER       IPOIDS,IVF,IDF,IGEOM,IPRES,IMAT
      INTEGER       IDEPM,IDEPP
      INTEGER       KPG,KDEC,N,I,J,KK,COTE
C                            3*27      9*27*27
      REAL*8        PR, VECT(81), MATR(6561),  P(27)
C
C
C
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDF,JGANO)

C    AUTRES VARIABLES DE L'OPTION
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PDEPLMR','L',IDEPM)
      CALL JEVECH('PDEPLPR','L',IDEPP)
      CALL JEVECH('PPRESSR','L',IPRES)
      CALL JEVECH('PMATUUR','E',IMAT )


C    REACTUALISATION DE LA GEOMETRIE PAR LE DEPLACEMENT
      DO 10 I = 0,NNO*3-1
        ZR(IGEOM+I) = ZR(IGEOM+I) + ZR(IDEPM+I) + ZR(IDEPP+I)
 10   CONTINUE

C    CALCUL DE LA PRESSION AUX POINTS DE GAUSS (A PARTIR DES NOEUDS)
      DO 100 KPG = 0,NPG-1
        KDEC = KPG*NNO
        PR = 0.D0
        DO 105 N = 0,NNO-1
          PR = PR + ZR(IPRES+N)  * ZR(IVF+KDEC+N)
105     CONTINUE
        P(1+KPG) = PR
100   CONTINUE

C    CALCUL EFFECTIF DE LA RIGIDITE
      CALL NMPR3D(2,NNO,NPG,ZR(IPOIDS),ZR(IVF),ZR(IDF),ZR(IGEOM),P,
     &            VECT,MATR)

C    ON SYMETRISE LA RIGIDITE TANGENTE  (STOCKAGE COLONNES SUPERIEURES)
      KK = 0
      COTE = NNO*3
      DO 200 J = 0,COTE-1
       DO 210 I=0,J
          ZR(IMAT+KK) = ( MATR(J*COTE+I+1) + MATR(I*COTE+J+1) ) / 2
          KK = KK+1
 210    CONTINUE
 200  CONTINUE

      END
