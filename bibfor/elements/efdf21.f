C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================

      IMPLICIT NONE
      LOGICAL AXI
      INTEGER NDIM,NNO1,NNO2,KPG
      REAL*8 VFF2(NNO2),DFDE2(*),DFDN2(*),DFDK2(*)
      REAL*8 DFDE1(*),DFDN1(*),DFDK1(*)
      REAL*8 POIDSG,POIDS,R,GEOM(NDIM,NNO2),DFDI21(NNO1,NDIM)

C.......................................................................

C    DERIVEE DES FONCTIONS DE FORME P1 PAR RAPPORT A UNE GEOMETRIE P2
C.......................................................................

C IN  NDIM    : DIMENSION DE L'ESPACE
C IN  AXI     : INDICATEUR SI AXISYMETRIQUE
C IN  GEOM    : COORDONEES DES NOEUDS
C IN  KPG     : NUMERO DU POINT DE GAUSS (POUR L'ACCES AUX FCT. FORMES)
C IN  POIDSG  : POIDS DU POINT DE GAUSS DE L'ELEMENT DE REFERENCE
C IN  NNO     : NOMBRE DE NOEUDS (P2 OU P1)
C IN  VFF     : VALEUR DES FONCTIONS DE FORME (P2 OU P1)
C IN  DFDE    : DERIVEE DES FONCTIONS DE FORME DE REFERENCE (P2 OU P1)
C IN  DFDN    : DERIVEE DES FONCTIONS DE FORME DE REFERENCE (P2 OU P1)
C IN  DFDK    : DERIVEE DES FONCTIONS DE FORME DE REFERENCE (P2 OU P1)
C OUT POIDS   : POIDS D'INTEGRATION (*R EN AXI)
C OUT DFDI21  : DERIVEE DES FONCTIONS DE FORME
C OUT R       : DISTANCE DU POINT DE GAUSS A L'AXE (EN AXISYMETRIQUE)
C......................................................................

      INTEGER N,KK1,KK2


C -- DERIVEE DES FONCTIONS DE FORME

      IF (NDIM.EQ.3) THEN
        KK1 = (KPG-1)*NNO1*3 + 1
        KK2 = (KPG-1)*NNO2*3 + 1
        CALL EFDF3D(POIDSG,NNO2,DFDE2(KK2),DFDN2(KK2),DFDK2(KK2),NNO1,
     &              DFDE1(KK1),DFDN1(KK1),DFDK1(KK1),GEOM,DFDI21(1,1),
     &              DFDI21(1,2),DFDI21(1,3),POIDS)
      ELSE
        KK1 = (KPG-1)*NNO1 + 1
        KK2 = (KPG-1)*NNO2 + 1
        CALL EFDF2D(POIDSG,NNO2,DFDE2(KK2),DFDK2(KK2),NNO1,DFDE1(KK1),
     &              DFDK1(KK1),GEOM,DFDI21(1,1),DFDI21(1,2),POIDS)
      END IF


C -- DISTANCE A L'AXE (AXISYMETRIQUE)

      IF (AXI) THEN
        R = 0.D0
        DO 10 N = 1,NNO2
          R = R + VFF2(N)*GEOM(1,N)
   10   CONTINUE
        POIDS = POIDS*R
      END IF

      END
