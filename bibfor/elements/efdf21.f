      SUBROUTINE EFDF21(NDIM,AXI,GEOM,KPG,POIDSG,NNO2,VFF2,DFDE2,DFDN2,
     &                  DFDK2,NNO1,DFDE1,DFDN1,DFDK1,POIDS,DFDI21,R)

