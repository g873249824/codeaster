      SUBROUTINE TANMAT(ALPHA,BETA,GAMMA,K1,K2,DMAX1,DMAX2,
     &                DAM1,DAM2,CURV,DCURV,TANMA2)

        IMPLICIT  NONE
C-----------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 19/05/2009   AUTEUR SFAYOLLE S.FAYOLLE 
