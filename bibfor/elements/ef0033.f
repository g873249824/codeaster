C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C     CALCUL DE EFGE_ELNO
C     ------------------------------------------------------------------
      INTEGER NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDX,JGANO
      INTEGER I,IRET,JCARA
      INTEGER JDEPG,JEFFG,JGEOM
      INTEGER NP

      REAL*8 ALPHA,BETA,R8DGRD
      REAL*8 PGL(3,3),XYZL(3,4)
      REAL*8 DEPL(24)
      REAL*8 EFFGT(32)
      REAL*8 T2EV(4),T2VE(4),C,S

      CHARACTER*4 FAMI
C     ------------------------------------------------------------------
C

      FAMI='NOEU'
      CALL ELREF4(' ',FAMI,NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDX,JGANO)


      DO 10 I=1,32
        EFFGT(I)=0.D0
   10 CONTINUE

      CALL JEVECH('PGEOMER','L',JGEOM)

      NP=NNO

      IF (NNO.EQ.3) THEN
        CALL DXTPGL(ZR(JGEOM),PGL)
      ELSEIF (NNO.EQ.4) THEN
        CALL DXQPGL(ZR(JGEOM),PGL,'S',IRET)
      ENDIF

      CALL UTPVGL(NNO,3,PGL,ZR(JGEOM),XYZL)

      CALL JEVECH ('PCACOQU', 'L', JCARA)
      ALPHA = ZR(JCARA+1) * R8DGRD()
      BETA  = ZR(JCARA+2) * R8DGRD()
      CALL COQREP(PGL,ALPHA,BETA,T2EV,T2VE,C,S)


      CALL JEVECH('PDEPLAR','L',JDEPG)
      CALL UTPVGL(NNO,6,PGL,ZR(JDEPG),DEPL)


C --- CALCUL DES EFFORTS GENERALISES VRAIS AUX POINTS DE CALCUL
      CALL DXEFGV(NOMTE,'EFGE_ELNO',XYZL,PGL,DEPL,EFFGT)

C ---   PASSAGE DES EFFORTS GENERALISES DU REPERE INTRINSEQUE
C ---   A L'ELEMENT AU REPERE LOCAL DE LA COQUE
C       ---------------------------------------
      CALL JEVECH('PEFFORR','E',JEFFG)
      CALL DXEFRO(NP,T2VE,EFFGT,ZR(JEFFG))


      END
