      INTEGER NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDX,JGANO
      INTEGER I,IRET,JCARA,VALI(2)
      INTEGER JDEPG,JEFFG,JGEOM,JMATE,JSIGM
      INTEGER NP,MULTIC
      INTEGER NBCOU,ICOU
