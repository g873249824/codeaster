      SUBROUTINE DGSEUI(EM,NUM,EF,NUF,EB,NUB,SYTB,H,ICISAI,SYT,SYC,DXD,
     &                  SYF,DRD,PELAST,PELASF,ICOMPR)

      IMPLICIT NONE

C PARAMETRES ENTRANTS
      INTEGER ICOMPR, ICISAI
      REAL*8 EM,NUM,EF,NUF,H
      REAL*8 EB,NUB,SYTB,SYC

C PARAMETRES SORTANTS
      REAL*8 SYT,DXD,SYF,DRD,PELAST,PELASF

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 03/01/2011   AUTEUR SFAYOLLE S.FAYOLLE 
