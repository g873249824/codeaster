C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================

      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16 OPTION,NOMTE
C ----------------------------------------------------------------------
C FONCTION REALISEE:  CALCUL DES OPTION FORC_NODA ET REFE_FORC_NODA
C                     POUR LES ÉLÉMENTS MECA X-FEM

C    - ARGUMENTS:
C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT

      INTEGER    NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO,IGEOM,IVECTU
      INTEGER    JPINTT,JCNSET,JHEAVT,JLONCH,JBASLO,ICONTM,JLSN,JLST
      INTEGER    JPMILT,DDLM,NFISS,JFISNO,IDEPLM,ICOMPO
      INTEGER    NFH,DDLC,NFE,IBID,NBSIGM,DDLS,NBSIG,NDDL,JSTNO
      INTEGER    CONTAC,NNOM,SINGU
      LOGICAL    LBID
      REAL*8     RBID,SIGREF,DEPREF
      CHARACTER*8 ENR,ELREF
C DEB ------------------------------------------------------------------

C ---- CARACTERISTIQUES DU TYPE D'ELEMENT :
C ---- GEOMETRIE ET INTEGRATION
C      ------------------------

      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)
      CALL ELREF1(ELREF)
C     INITIALISATION DES DIMENSIONS DES DDLS X-FEM
      CALL XTEINI(NOMTE,NFH,NFE,SINGU,DDLC,NNOM,DDLS,NDDL,
     &            DDLM,NFISS,CONTAC)
C
C ---- NOMBRE DE CONTRAINTES ASSOCIE A L'ELEMENT
C      -----------------------------------------
      NBSIG = NBSIGM()

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PDEPLMR','L',IDEPLM)
      CALL JEVECH('PCOMPOR','L',ICOMPO)
      CALL JEVECH('PVECTUR','E',IVECTU)

C     PARAMÈTRES PROPRES À X-FEM
      CALL JEVECH('PPINTTO','L',JPINTT)
      CALL JEVECH('PCNSETO','L',JCNSET)
      CALL JEVECH('PHEAVTO','L',JHEAVT)
      CALL JEVECH('PLONCHA','L',JLONCH)
      CALL JEVECH('PBASLOR','L',JBASLO)
      CALL JEVECH('PLSN'   ,'L',JLSN)
      CALL JEVECH('PLST'   ,'L',JLST)
C     PROPRE AUX ELEMENTS 1D ET 2D (QUADRATIQUES)
      CALL TEATTR (NOMTE,'S','XFEM',ENR,IBID)
      IF ((IBID.EQ.0).AND.(NOMTE(3:4).NE.'AX').AND.(ENR.EQ.'XH'
     & .OR.ENR.EQ.'XHT'.OR.ENR.EQ.'XT'.OR.ENR.EQ.'XHC').AND. NDIM.LE.2)
     &  CALL JEVECH('PPMILTO','L',JPMILT)
      IF (NFISS.GT.1) CALL JEVECH('PFISNO','L',JFISNO)

      IF (OPTION.EQ.'FORC_NODA') THEN
C      --------------------
C VECTEUR SECOND MEMBRE DONNE EN ENTREE
        CALL JEVECH('PCONTMR','L',ICONTM)
        CALL JEVECH('PSTANO' ,'L',JSTNO)
C       CALCUL DU VECTEUR DES FORCES INTERNES (BT*SIGMA)
        CALL XBSIG(OPTION,NDIM,NNO,NFH,NFE,DDLC,DDLM,IGEOM,ZK16(ICOMPO),
     &       JPINTT,ZI(JCNSET),ZI(JHEAVT),ZI(JLONCH),ZR(JBASLO),
     &       ZR(ICONTM),NBSIG,IDEPLM,ZR(JLSN),ZR(JLST),IVECTU,JPMILT,
     &       NFISS,JFISNO)

        CALL XTEDDL(NDIM,NFH,NFE,DDLS,NDDL,NNO,NNOS,ZI(JSTNO),
     &              .FALSE.,LBID,OPTION,NOMTE,
     &              RBID,ZR(IVECTU),DDLM,NFISS,JFISNO)

      ELSE IF (OPTION.EQ.'REFE_FORC_NODA') THEN
C
C --- ON RECUPERE CONTRAINTE ET SAUT DE DEPLACEMENT DE REFERENCE
C
        CALL TEREFE('SIGM_REFE','MECA_INTERFACE',SIGREF)
        CALL TEREFE('DEPL_REFE','MECA_INTERFACE',DEPREF)
C
C --- ON COMMENCE PAR CALCULER LES CONTRIBUTIONS VOLUMIQUES
C
        CALL XBSIR(NDIM,NNO,NFH,NFE,DDLC,DDLM,IGEOM,ZK16(ICOMPO),JPINTT,
     &       ZI(JCNSET),ZI(JHEAVT),ZI(JLONCH),ZR(JBASLO),SIGREF,
     &       NBSIG,IDEPLM,ZR(JLSN),ZR(JLST),IVECTU,JPMILT,NFISS,JFISNO)
C
C --- SI ELEMENT DE CONTACT, ON Y AJOUTE LES CONTRIBUTIONS SURFACIQUES
C --- NOTAMMENT CELLE POUR LES EQUATIONS DUALES
C
         IF(ENR.EQ.'XHC') THEN
            CALL XBSIR2(ELREF,CONTAC,DDLC  ,DDLM  ,DDLS  ,IGEOM ,
     &                 JFISNO,JLST  ,IVECTU,SINGU ,
     &                 NDDL  ,NDIM  ,NFE   ,NFH   ,NFISS ,
     &                 NNO   ,NNOM  ,NNOS  ,DEPREF,SIGREF,
     &                 NOMTE)
         ENDIF
      ELSE
        CALL ASSERT(.FALSE.)
      END IF
C FIN ------------------------------------------------------------------

      END
