      REAL*8   A(3)
      REAL*8   D(2)
      REAL*8   ZERO
      REAL*8    AUX,DELTA,RDELTA,B,NORME
      DATA     ZERO /1.D-10/

      AUX = (A(1)-A(2))*(A(1)-A(2))
      AUX = 0.25D0 * AUX
      DELTA = AUX + A(3)*A(3)
      RDELTA=0.0D0
      NORME=ABS(A(1))+ABS(A(2))+ABS(A(3))
      IF(DELTA .GE. ZERO * NORME**2) THEN
           RDELTA = SQRT(DELTA)
      ELSE
          RDELTA=0.D0
      ENDIF

      B = 0.5D0 * (A(1)+A(2))
      D(1) = B + RDELTA
      D(2) = B - RDELTA

      END 
