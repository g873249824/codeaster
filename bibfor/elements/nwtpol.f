      INTEGER    DEG
      REAL*8     COEF(DEG+1)
      REAL*8     RAC,DPOLYN
      REAL*8 RAC0,DRAC,TOLE,FX,DFX,ERR,FX0,DPOLYH
      INTEGER ITER,ITERMX,KK

      ITER=0
      ITERMX=10
      TOLE=1.D-15

      FX=DPOLYH(DEG,COEF,RAC)
      ERR=ABS(FX)
      RAC0=RAC
      FX0=FX

       DO 9, KK = 1,10000000
       IF(ERR .LE. TOLE) GOTO 10
       
         ITER=ITER+1
         DFX=DPOLYN(DEG,COEF,RAC)
         IF ((ITER .GT. ITERMX).OR.(ABS(DFX) .LT. 1.D-12)) THEN
           IF(ABS(FX0) .LT. ABS(FX))  RAC=RAC0
           GOTO 10
         ENDIF

         RAC=RAC-FX/DFX
         FX=DPOLYH(DEG,COEF,RAC)
         ERR=ABS(FX)
         
 9      CONTINUE
 10     CONTINUE
      END 
