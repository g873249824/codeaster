C GENERAL PUBLIC LICENSE FOR MORE DETAILS.

C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16 OPTION,NOMTE
C ......................................................................
C    - FONCTION REALISEE:  CALCUL DES VECTEURS ELEMENTAIRES
C                          OPTION : 'CHAR_THER_FLUN_F'
C    - ARGUMENTS:
C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT
C ......................................................................

      INTEGER    NBRES
C-----------------------------------------------------------------------
      INTEGER ICODE ,J 
      REAL*8 FLUN ,FLUNP1 
C-----------------------------------------------------------------------
      PARAMETER (NBRES=3)
      CHARACTER*8 NOMPAR(NBRES), ELREFE, ALIAS8
      REAL*8 VALPAR(NBRES),POIDS,R,Z,FLUX,NX,NY,THETA
      REAL*8 COORSE(18),VECTT(9)
      INTEGER NNO,NNOS,NDIM,KP,NPG,IPOIDS,IVF,IDFDE,JGANO,IGEOM
      INTEGER ITEMPS,IVECTT,I,L,LI,IFLU
      INTEGER NNOP2,C(6,9),ISE,NSE, IBID
      LOGICAL LTEATT,LAXI

C
      CALL ELREF1(ELREFE)
C
      IF ( LTEATT(' ','LUMPE','OUI')) THEN
         CALL TEATTR(' ','S','ALIAS8',ALIAS8,IBID)
         IF (ALIAS8(6:8).EQ.'SE3')  ELREFE='SE2'
      END IF
C
      CALL ELREF4(ELREFE,'RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,
     +            IVF,IDFDE,JGANO)

      LAXI = .FALSE.
      IF (LTEATT(' ','AXIS','OUI')) LAXI = .TRUE.

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PTEMPSR','L',ITEMPS)
      CALL JEVECH('PFLUXNF','L',IFLU)
      CALL JEVECH('PVECTTR','E',IVECTT)

      THETA = ZR(ITEMPS+2)

      CALL CONNEC(NOMTE,NSE,NNOP2,C)

      DO 10 I = 1,NNOP2
        VECTT(I) = 0.D0
   10 CONTINUE

C BOUCLE SUR LES SOUS-ELEMENTS

      DO 70 ISE = 1,NSE
        DO 30 I = 1,NNO
          DO 20 J = 1,2
            COORSE(2* (I-1)+J) = ZR(IGEOM-1+2* (C(ISE,I)-1)+J)
   20     CONTINUE
   30   CONTINUE
        DO 60 KP = 1,NPG
          CALL VFF2DN(NDIM,NNO,KP,IPOIDS,IDFDE,COORSE,NX,NY,POIDS)
          R = 0.D0
          Z = 0.D0
          DO 40 I = 1,NNO
            L = (KP-1)*NNO + I
            R = R + COORSE(2* (I-1)+1)*ZR(IVF+L-1)
            Z = Z + COORSE(2* (I-1)+2)*ZR(IVF+L-1)
   40     CONTINUE
          IF (LAXI) POIDS = POIDS*R
          VALPAR(1) = R
          NOMPAR(1) = 'X'
          VALPAR(2) = Z
          NOMPAR(2) = 'Y'
          NOMPAR(3) = 'INST'
          VALPAR(3) = ZR(ITEMPS)
          CALL FOINTE('FM',ZK8(IFLU),3,NOMPAR,VALPAR,FLUNP1,ICODE)
          IF (THETA.NE.1.0D0) THEN
            VALPAR(3) = ZR(ITEMPS) - ZR(ITEMPS+1)
            CALL FOINTE('FM',ZK8(IFLU),3,NOMPAR,VALPAR,FLUN,ICODE)
          ELSE
            FLUN = 0.0D0
          END IF
          FLUX = THETA*FLUNP1 + (1.0D0-THETA)*FLUN
CCDIR$ IVDEP
          DO 50 I = 1,NNO
            LI = IVF + (KP-1)*NNO + I - 1
            VECTT(C(ISE,I)) = VECTT(C(ISE,I)) + POIDS*ZR(LI)*FLUX
   50     CONTINUE
   60   CONTINUE
   70 CONTINUE

      DO 80 I = 1,NNOP2
        ZR(IVECTT-1+I) = VECTT(I)
   80 CONTINUE

      END
