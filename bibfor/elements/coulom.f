C    BUT : TEST  DU CRITERE DE COULOM  (ADHER=.TRUE. SI ADHERENCE)
C
C    IN  : 
C          SIGN    : CONTRAINTE NORMALE AU P.GAUSS
C          GSG(NDDL): INCREMENT DEPLACEMENT AUX NOEUDS
C          MU      : COEFFICIENT DE COULOMB
C          ET      : VALEUR DE RAIDEUR TANG TANGENTE
C          GDELUT(NDDL): INCREMENT DEPLACEMENT TANG AUX NOEUDS
C          SGEPSI  : VALEUR EPSILON EN CONTRAINTE
C
C    OUT : 
C          ADHER   : BOOLEEN .TRUE. SI ADHERENCE
C          GSG(NDDL): INCREMENT DEPLACEMENT AUX NOEUDS
C          SIGT    : CONTRAINTE TANGENTIELLE AU P.GAUSS
C ------------------------------------------------------------------
      IMPLICIT REAL*8 (A-H,O-Z)
      REAL*8 GSG(*),SIGN,MU,ET,GDELUT(*),XAUXN,SIGT(*),SGEPSI
      REAL*8 XNO(3),XAUXT
      LOGICAL ADHER
C
      XAUXN=MU*ABS(SIGN)*MU*ABS(SIGN)
      XAUXT=0.0D0
      DO 10 IND=1,NDIM
         XNO(IND) = GSG(IND)+ET*GDELUT(IND)
         XNO(IND) = XNO(IND)*XNO(IND)
         XAUXT    = XAUXT+XNO(IND)
10    CONTINUE
      IF ((XAUXT-XAUXN).LE.SGEPSI) THEN
        ADHER=.TRUE.
        DO 20 I=1,NDIM
          GSG (I) = GSG(I)+ET*GDELUT(I)
          SIGT(I) = GSG(I)
20      CONTINUE
      ELSE
        ADHER=.FALSE.
      ENDIF
      END
