      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,
     &            IVF,IDFDX,JGANO)
     
      DO 555 I=1,12
         FSTAB(I)=CONTPG(6+I)
555   CONTINUE
      DO 556 I=1,NPG
         DO 557 J=1,6
            SIGMA(6*(I-1)+J)=CONTPG(18*(I-1)+J)
557      CONTINUE
556   CONTINUE

      END
