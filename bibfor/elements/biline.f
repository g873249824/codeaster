C ---------------------------------------------
C     BUT:   POUR LES ELEMENTS DE CABLE, CALCUL DE LA FORME BILINEAIRE:
C                         VECT1T * AMAT * VECT2
C            AMAT ETANT D'ORDRE NORDRE ET RANGEE PAR LIGNES DANS LE VEC-
C            TEUR AMAT: 1ERE LIGNE, PUIS 2EME LIGNE...
C     IN: NORDRE
C         VECT1
C         AMAT
C         VECT2
C     OUT: BILINE
C ---------------------------------------------
C
