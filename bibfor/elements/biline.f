      BILINE = ZERO
      K = 0
      DO 2 I=1,NORDRE
        PRODUI = ZERO
        DO 1 J=1,NORDRE
          K = K + 1
          PRODUI = PRODUI + AMAT(K)*VECT2(J)
    1   CONTINUE
        BILINE = BILINE + VECT1(I)*PRODUI
    2 CONTINUE
      END
