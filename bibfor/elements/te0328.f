      CHARACTER*16 NOMTE,OPTION

C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
      LOGICAL POSI,NEGA
      CHARACTER*24 VALK(2)
      REAL*8  POIDS
      INTEGER IGEOM,IPOIDS,IVF,IDFDE,NDIM,NPG,NNO,JGANO,NNOS
      INTEGER KP,ICODR
      INTEGER IADZI,IAZK24,CODRET

      CODRET=0
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PCODRET','E',ICODR)

      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)

      POSI=.FALSE.
      NEGA=.FALSE.
      DO 1 KP = 1,NPG
        IF (NDIM.EQ.3) THEN
          CALL DFDM3J(NNO,KP,IDFDE,ZR(IGEOM),POIDS )
        ELSEIF (NDIM.EQ.2) THEN
          CALL DFDM2J(NNO,KP,IDFDE,ZR(IGEOM),POIDS)
        ELSE
          GOTO 9999
        ENDIF
        IF (POIDS.LT.0.D0) NEGA=.TRUE.
        IF (POIDS.GT.0.D0) POSI=.TRUE.
1     CONTINUE


      IF (POSI.AND.NEGA) THEN
        CALL TECAEL(IADZI,IAZK24)
        NNO=ZI(IADZI-1+2)
        VALK(1)=ZK24(IAZK24-1+3)
        VALK(2)=ZK24(IAZK24-1+3+NNO+1)
        CALL U2MESK('A','CALCULEL_7',2,VALK)
        CODRET=1
      ENDIF

9999  CONTINUE
      ZI(ICODR-1+1)=CODRET

      END
