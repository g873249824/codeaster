C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C
      IMPLICIT REAL*8 (A-H,O-Z)
C
      INTEGER NB1,INTSX
      REAL*8 XI3,WGT,YOUNG,NU,ALPHA,ZR(1)
      REAL*8 TMOY(1),TINF(1),TSUP(1),BTILD(5,42),FORTHI(1),VECTHR(2)
C
      NB2=NB1+1
C      
C     CALCUL DE TEMPERATURE AUX PTS D'INTEGRATION
C
      P1XI3= 1-XI3*XI3
      P2XI3=-XI3*(1-XI3)/2.D0
      P3XI3= XI3*(1+XI3)/2.D0
C
      IF (INDIC.EQ.1) THEN
C
C     ON CALCULE LA TEMPERATURE AU PT DE GAUSS CORRESPONDANT
C     ET LA CHARGE THERMIQUE : CELA SERT POUR L'OPTION CHAR_MECA_TEMP_R
C
      L1=459+9*(INTSX-1)
C
      ELSE
C
C     ON CALCULE LA TEMPERATURE AU PT DE GAUSS CORRESPONDANT
C     : CELA POUR L'OPTION DE POST-TRAITEMENT
C
      L1=351+9*(INTSX-1)
C
      ENDIF
C
      TEMPER=0.D0
C
      DO 10 I=1,NB2
         I1=L1+I
      DO 20 J=1,3
         TI=TMOY(I)*P1XI3+TINF(I)*P2XI3+TSUP(I)*P3XI3
 20   CONTINUE
C
      TEMPER=TEMPER+ZR(I1)*TI
C
 10   CONTINUE
C
      TEMPER=TEMPER-TREF
C
      IF (INDIC.EQ.1) THEN
C
      VECTHR(1)=YOUNG*ALPHA*TEMPER/(1.D0-NU)
      VECTHR(2)=VECTHR(1)
C
C     CONSTRUCTION DES EFFORTS DUS AUX DILATATIONS THERMIQUES
C
      DO 30 I=1,5*NB1+2
         FORTHI(I)=0.D0
      DO 40 K=1,2
         FORTHI(I)=FORTHI(I)+BTILD(K,I)*VECTHR(K)*WGT
C        FORTHI(I)=FORTHI(I)+BTILD(K,I)*VECTHR(K)
 40   CONTINUE
 30   CONTINUE
C
      ENDIF
C
      END
