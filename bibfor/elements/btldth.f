C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
      IMPLICIT REAL*8 (A-H,O-Z)
C
      INTEGER NB1,KPG
      REAL*8 WGT,YOUNG,NU,ALPHA,XI3
      REAL*8 BTILD(5,42),FORTHI(1),VECTHR(2)
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      INTEGER JCOU,IMOY
      CHARACTER*4 FAMI
      REAL P1XI3,P2XI3,P3XI3
C
C
C     CALCUL DE TEMPERATURE AUX PTS D'INTEGRATION
C
C
      P1XI3= 1-XI3*XI3
      P2XI3=-XI3*(1-XI3)/2.D0
      P3XI3= XI3*(1+XI3)/2.D0
      CALL JEVECH('PNBSP_I','L',JCOU)
      IMOY=(3*ZI(JCOU)+1)/2
      CALL RCVARC('F','TEMP','REF',FAMI,1,1,TREF,IRET)
      CALL RCVARC('F','TEMP','+',FAMI,KPG,1,TINF,
     &            IRET)
      CALL RCVARC('F','TEMP','+',FAMI,KPG,IMOY,TMOY,
     &            IRET)
      CALL RCVARC('F','TEMP','+',FAMI,KPG,3*ZI(JCOU),TSUP,
     &            IRET)
C
      TEMPER=(TMOY*P1XI3+TINF*P2XI3+TSUP*P3XI3)-TREF

C
      IF (INDIC.EQ.1) THEN
C
      VECTHR(1)=YOUNG*ALPHA*TEMPER/(1.D0-NU)
      VECTHR(2)=VECTHR(1)
C
C     CONSTRUCTION DES EFFORTS DUS AUX DILATATIONS THERMIQUES
C
      DO 30 I=1,5*NB1+2
         FORTHI(I)=0.D0
      DO 40 K=1,2
         FORTHI(I)=FORTHI(I)+BTILD(K,I)*VECTHR(K)*WGT
C        FORTHI(I)=FORTHI(I)+BTILD(K,I)*VECTHR(K)
 40   CONTINUE
 30   CONTINUE
C
      ENDIF
C
      END
