      INCLUDE 'jeveux.h'

      COMPLEX*16 RHOSZ
      CHARACTER*8 FAMI,POUM
      CHARACTER*16 OPTION,NOMTE
      INTEGER ICODRE
      REAL*8 POIDS,R,NX,NY,RHO
      INTEGER NNO,KP,NPG,IPOIDS,IVF,IDFDE,IGEOM
      INTEGER IMATTT,I,J,IJ,L,LI,LJ
      INTEGER IMATE,IIMPE,KPG,SPT
      LOGICAL LTEATT, LAXI


C-----------------------------------------------------------------------
      INTEGER JGANO ,MATER ,NDIM ,NNOS
      REAL*8 R8B
C-----------------------------------------------------------------------
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)

      LAXI = .FALSE.
      IF (LTEATT(' ','AXIS','OUI')) LAXI = .TRUE.

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PIMPEDC','L',IIMPE)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PMATTTC','E',IMATTT)

      FAMI='FPG1'
      KPG=1
      SPT=1
      POUM='+'
      MATER = ZI(IMATE)
      CALL RCVALB(FAMI,KPG,SPT,POUM,MATER,' ','FLUIDE',0,' ',
     &            R8B,1,'RHO',RHO,ICODRE,1)

      IF (ZC(IIMPE).NE. (0.D0,0.D0)) THEN
        RHOSZ = RHO/ZC(IIMPE)
      ELSE
        GO TO 50
      END IF

      DO 40 KP = 1,NPG
        CALL VFF2DN(NDIM,NNO,KP,IPOIDS,IDFDE,ZR(IGEOM),NX,NY,POIDS)
        IF (LAXI) THEN
          R = 0.D0
          DO 10 I = 1,NNO
            L = (KP-1)*NNO + I
            R = R + ZR(IGEOM+2*I-2)*ZR(IVF+L-1)
   10     CONTINUE
          POIDS = POIDS*R
        END IF
        IJ = IMATTT - 1
        DO 30 I = 1,NNO
          LI = IVF + (KP-1)*NNO + I - 1
          DO 20 J = 1,I
            LJ = IVF + (KP-1)*NNO + J - 1
            IJ = IJ + 1
            ZC(IJ) = ZC(IJ) + POIDS*RHOSZ*ZR(LI)*ZR(LJ)
   20     CONTINUE
   30   CONTINUE
   40 CONTINUE
   50 CONTINUE
      END
