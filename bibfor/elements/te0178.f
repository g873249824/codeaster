C ......................................................................
C    - FONCTION REALISEE:  CALCUL DES MATRICES ELEMENTAIRES
      IMPLICIT NONE
C                          D'AMORTISSEMENT ACOUSTIQUE SUR DES ARETES
C                          D'ELEMENTS 2D
