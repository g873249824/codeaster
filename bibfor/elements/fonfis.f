      SUBROUTINE FONFIS(NOMA,NBNOFF,FONOEU,FONDFI)

      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER        NBNOFF
      CHARACTER*8    NOMA
      CHARACTER*24   FONDFI,FONOEU

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 24/07/2012   AUTEUR PELLET J.PELLET 
C ======================================================================
