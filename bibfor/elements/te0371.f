C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C.......................................................................
      IMPLICIT REAL*8 (A-H,O-Z)
C
C     BUT: CALCUL DES MATRICES DE RIGIDITE  ELEMENTAIRES EN MECANIQUE
C          ELEMENTS 2D DE COUPLAGE PESANTEUR-SURFACE LIBRE D'UN FLUIDE
C
C          OPTION : 'MASS_MECA'
C
C     ENTREES  ---> OPTION : OPTION DE CALCUL
C          ---> NOMTE  : NOM DU TYPE ELEMENT
C.......................................................................
      CHARACTER*8        ELREFE
      CHARACTER*2        CODRET
      CHARACTER*16       NOMTE,OPTION
      CHARACTER*24       CHVAL,CHCTE
      INTEGER            IGEOM,IMATE
      INTEGER            I,J,K,L,IK,IJKL,IDEC,JDEC,LDEC,KDEC,KCO,INO,JNO
      INTEGER            NBPG(10),NBFPG,NDIM,NNO,NPG1,IPG
      INTEGER            JIN,JVAL,IPOIDS,IVF,IDFRDE,IDFRDK,IMATUU
      REAL*8             A(2,2,27,27),SZ(27,27),RHO
      REAL*8             POIDS,DFRDE(1),DFRDK(1),COOR(1),JAC
      REAL*8             DXDE,DXDK,DYDE,DYDK
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C
      CALL ELREF1(ELREFE)

      CHCTE = '&INEL.'//ELREFE//'.CARACTE'
      CALL JEVETE(CHCTE,'L',JIN)
      NDIM = ZI(JIN+1-1)
      NNO = ZI(JIN+2-1)
      NBFPG = ZI(JIN+3-1)
      DO 110 I = 1,NBFPG
         NBPG(I) = ZI(JIN+3-1+I)
  110 CONTINUE
      NPG1 = NBPG(1)
      NPG2 = NBPG(2)
C
      CHVAL = '&INEL.'//ELREFE//'.FFORMES'
      CALL JEVETE(CHVAL,'L',JVAL)
      IPOIDS = JVAL + (NDIM+1)*NNO*NNO
      IPOIDS = IPOIDS + NPG1 + NPG1*NNO*(NDIM+1)
      IVF    = IPOIDS + NPG2
      IDFRDE = IVF    + NPG2*NNO
C
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PMATUUR','E',IMATUU)
C
      CALL RCVALA(ZI(IMATE),'FLUIDE',0,' ',R8B,1,'RHO',RHO,CODRET,'FM')
C
C     INITIALISATION DE LA MATRICE
C
      DO 112 K=1,2
         DO 112 L=1,2
            DO 112 I=1,NNO
            DO 112 J=1,I
                A(K,L,I,J) = 0.D0
112   CONTINUE
C
C    BOUCLE SUR LES POINTS DE GAUSS
C
      DO 113 IPG=1,NPG2
C
       KDEC = (IPG-1)*NNO*NDIM
       LDEC = (IPG-1)*NNO
C
       DXDE=0.D0
       DXDK=0.D0
       DYDE=0.D0
       DYDK=0.D0
       DO 100 I=1,NNO
        DXDE=DXDE+ZR(IGEOM+3*(I-1))*ZR(IDFRDE+KDEC+(I-1)*NDIM)
        DXDK=DXDK+ZR(IGEOM+3*(I-1))*ZR(IDFRDE+KDEC+(I-1)*NDIM+1)
        DYDE=DYDE+ZR(IGEOM+3*(I-1)+1)*ZR(IDFRDE+KDEC+(I-1)*NDIM)
        DYDK=DYDK+ZR(IGEOM+3*(I-1)+1)*ZR(IDFRDE+KDEC+(I-1)*NDIM+1)
100    CONTINUE
       JAC   = DXDE*DYDK-DXDK*DYDE
       POIDS = ABS(JAC)*ZR(IPOIDS+IPG-1)
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C       CALCUL DU TERME RHO * PHI * Z      C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
        DO 130 INO=1,NNO
           DO 140 JNO=1,INO
              A(1,2,INO,JNO) = A(1,2,INO,JNO) +
     &                         POIDS * RHO *
     &                         ZR(IVF+LDEC+INO-1) * ZR(IVF+LDEC+JNO-1)
140        CONTINUE
130      CONTINUE
113     CONTINUE
C
      DO 151 INO=1,NNO
        DO 152 JNO=1 ,INO
           A(2,1,INO,JNO) = A(1,2,INO,JNO)
152     CONTINUE
151   CONTINUE
C
C PASSAGE DU STOCKAGE RECTANGULAIRE AU STOCKAGE TRIANGULAIRE
C
      IJKL = 0
      IK = 0
      DO 160 K=1,2
         DO 160 L=1,2
            DO 160 I=1,NNO
                IK = ((2*I+K-3) * (2*I+K-2)) / 2
            DO 160 J=1,I
                IJKL = IK + 2 * (J-1) + L
                ZR(IMATUU+IJKL-1) = A(K,L,I,J)
160   CONTINUE
C
 9999 CONTINUE
      END
