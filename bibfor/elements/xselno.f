C
      INCLUDE 'jeveux.h'
      INTEGER MXVAL
      PARAMETER (MXVAL=6*3*4)
C     EN 2D :
C     MXVAL =  6 (NBSE MAX) * 3 (NBNOSE MAX) * 4 (NBCMP MAX)
C     EN 3D :
C     MXVAL = 32 (NBSE MAX) * 4 (NBNOSE MAX) * 6 (NBCMP MAX)

      INTEGER NDIM,NNOP,NNO
      INTEGER NBSIG,NBSECO(27)
      INTEGER JCNSET
      INTEGER JOUT2
      INTEGER I,J,NSE,ISE,IN,INO,IC

      REAL*8 TMP,SOMSIG(27,6)

      REAL*8 SISENO(MXVAL)

C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()

C     TABLEAUX DE LA SOMME DES CONTRAINTES
      DO 444 I=1,NNOP
        DO 445 J=1,NBSIG
          SOMSIG(I,J)=0
 445    CONTINUE
 444  CONTINUE

C     TABLEAUX DU NOMBRE DE SOUS-ELEMENTS CONNECTES AUX NOEUDS
      DO 446 I=1,NNOP
        NBSECO(I)=0
 446  CONTINUE

C       BOUCLE SUR LES NSE SOUS-ÉLÉMENTS
      DO 210 ISE=1,NSE

C       BOUCLE SUR LES 4/3 SOMMETS DU SOUS-TETRA/TRIA
        DO 211 IN=1,NNO
          INO=ZI(JCNSET-1+(NDIM+1)*(ISE-1)+IN)
          IF (INO.LT.1000) THEN
            NBSECO(INO)=NBSECO(INO)+1
            DO 212 IC = 1,NBSIG
              TMP =  SISENO(NBSIG*NNO*(ISE-1)+NBSIG*(IN-1)+IC)
              SOMSIG(INO,IC)=SOMSIG(INO,IC)+TMP
 212        CONTINUE
          ENDIF
 211    CONTINUE

 210  CONTINUE

C     MOYENNES DES CONTRAINTES AUX NOEUDS DE L'ELEMENT PARENT
      DO 300 INO = 1,NNOP
        CALL ASSERT(NBSECO(INO).GT.0)
        DO 310 IC = 1,NBSIG
          ZR(JOUT2-1+NBSIG*(INO-1)+IC) = SOMSIG(INO,IC) / NBSECO(INO)
 310    CONTINUE
 300  CONTINUE

      CALL JEDEMA()

      END
