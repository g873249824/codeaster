C    - FONCTIONS REALISEES :  ROUTINE X-FEM
C
C          PASSAGE DES CONTRAINTES
C          DES POINTS DE GAUSS DES SOUS-ELEMENTS :
C            * AUX NOEUDS DES ELEMENTS PARENTS
C              (OPTION 'SIGM_ELNO') ;
C            * AUX SOMMETS (NOEUDS) DES SOUS-ELEMENTS
C              (OPTION 'SISE_ELNO') ;
C
