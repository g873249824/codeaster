      IMPLICIT REAL*8 (A-H,O-Z)
      INCLUDE 'jeveux.h'
      CHARACTER*16  NOMTE
C
C
      INTEGER NB1
      REAL*8 RHO,EPAIS
      REAL*8 XI(3,*),VOMEGA(3),VECL(51),VECL1(42)
      REAL*8 XA(3)
C
      CALL JEVECH ('PROTATR','L',IROTA)
      VOMEGA(1)=ZR(IROTA)*ZR(IROTA+1)
      VOMEGA(2)=ZR(IROTA)*ZR(IROTA+2)
      VOMEGA(3)=ZR(IROTA)*ZR(IROTA+3)

      XA(1)=ZR(IROTA+4)
      XA(2)=ZR(IROTA+5)
      XA(3)=ZR(IROTA+6)
C
      CALL JEVETE('&INEL.'//NOMTE(1:8)//'.DESI',' ', LZI )
      NB1  =ZI(LZI-1+1)
      NPGSN=ZI(LZI-1+4)
C
      CALL JEVETE('&INEL.'//NOMTE(1:8)//'.DESR',' ', LZR )
C
      CALL DXROEP(RHO,EPAIS)
C
      CALL R8INIR(42,0.D0,VECL1,1)
C
      DO 40 INTSN=1,NPGSN
         CALL VECTCI(INTSN,NB1,XI,ZR(LZR),RNORMC)
         CALL FORCEN(RNORMC,INTSN,NB1,XI,ZR(LZR),RHO,EPAIS,VOMEGA,
     &               VECL1,XA)
 40   CONTINUE
C
      CALL VEXPAN(NB1,VECL1,VECL)
      DO 60 I=1,3
         VECL(6*NB1+I)=0.D0
 60   CONTINUE
C
      END
