C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT NONE
C ---  CALCUL DU MODULE TANGENT
      INTEGER CAS
      REAL*8 E,D1,D2,A,B,Y,Y0,BETA1,BETA2,SIGF1,EPS,ETAN
      REAL*8 DDDY,DDDEPS,TITI,TOTO
      REAL*8 ZERO,UN
      PARAMETER (ZERO=0.0D+0,UN=1.D+0)

      IF (Y-Y0.LE.ZERO) THEN
        ETAN = E
        GO TO 10
      END IF
      IF (CAS.EQ.1) THEN
C ---   DERIVE DE D PAR RAPPORT A Y
        DDDY = B* (A**B)* (Y-Y0)** (B-UN)/ (UN+ (A* (Y-Y0))**B)**2
C ---   DERIVE DE D PAR RAPPORT A EPSILON
        DDDEPS = DDDY* (E*EPS+BETA1-BETA2*D2/ (UN-D2))
        DDDEPS = DDDEPS/ (UN+DDDY*BETA1**2/ (E* (UN-D1)**3))
C ---   DERIVE DE SIGMA PAR RAPPORT A EPSILON ET E TANGENT
        ETAN = E* (UN-D1) - E*EPS*DDDEPS - BETA1*DDDEPS +
     &         BETA2*D2*DDDEPS/ (UN-D2)
      ELSE IF (CAS.EQ.3) THEN
C ---   DERIVE DE D PAR RAPPORT A Y
        DDDY = B* (A**B)* (Y-Y0)** (B-UN)/ (UN+ (A* (Y-Y0))**B)**2
C ---   DERIVE DE D PAR RAPPORT A EPSILON
        DDDEPS = DDDY* (E*EPS+BETA2)
        DDDEPS = DDDEPS/ (UN+DDDY*BETA2**2/ (E* (UN-D2)**3))
C ---   DERIVE DE SIGMA PAR RAPPORT A EPSILON ET E TANGENT
        ETAN = E* (UN-D2) - E*EPS*DDDEPS - BETA2*DDDEPS
      ELSE
        TITI = SIGF1 + BETA1*D1* (UN-D2)/ (UN-D1)
        TOTO = BETA1*D1* (UN-D2)/ (UN-D1)
        ETAN = E* (UN-D2)* (UN-TOTO/TITI)
      END IF

   10 CONTINUE
      END
