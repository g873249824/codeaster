      IMPLICIT NONE
C    -------------------------------------------------------------------
C     CALCUL DE : WI BT VS
C     VS = VECTEUR DE FORCES INTERNES (ISSUE DE L'INTE. SUR LES FIBRES)
C     WI = POIDS DU PT DE GAUSS EN COURS
C     B = MATRICE B A CE POINT DE GAUSS ET BT SA TRANSPOSEE
C
C    -------------------------------------------------------------------
C
C IN R*8  VS(1) = INT(SIG DS)   = N0 EFFORT NORMAL INTERNE
C IN R*8  VS(2) = INT(SIG.Y DS) = -MZ0 MOMENT FLECHISSANT Z INTERNE
C IN R*8  VS(3) = INT(SIG.Z DS) = MY0 MOMENT FLECHISSANT Y INTERNE
C IN R*8  B(4) : LES QUATRES VALEURS NON NULLE ET DIFFERENTES DE B
C
C OUT R*8 VE(12) : FORCES ELEMENTAIRES DUES A CE POINT DE GAUSS
C
      REAL*8 VS(3),B(4),WI,VE(12)

      VE(1)=-B(1)*VS(1)*WI
      VE(2)=-B(2)*VS(2)*WI
      VE(3)=-B(2)*VS(3)*WI
      VE(4)=0.D0

      VE(5)=B(3)*VS(3)*WI
      VE(6)=-B(3)*VS(2)*WI
      VE(7)=-VE(1)
      VE(8)=-VE(2)
      VE(9)=-VE(3)
      VE(10)=0.D0

      VE(11)=B(4)*VS(3)*WI
      VE(12)=-B(4)*VS(2)*WI

      END
