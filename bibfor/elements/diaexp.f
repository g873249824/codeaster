      IMPLICIT NONE
      REAL*8        MASCO(*) , MASDI(*)
      INTEGER       NNO, NDDL, LDIM
C     ------------------------------------------------------------------
C     PASSAGE D'UNE MATRICE MASSE CONSISTANTE A UNE MATRICE MASSE 
C     DIAGONALE SUIVANT LA TECHNIQUE DE SOMMATION PAR COLONNE
C     ------------------------------------------------------------------
C     IN  NNO   : NOMBRE DE NOEUDS
C     IN  NDDL  : NOMBRE DE DDL PAR NOEUD
C     IN  LDIM  : NOMBRE TOTAL DE DDL DE L ELEMENT
C     IN  MASCO : MATRICE DE MASSE CONSISTANTE
C     OUT MASDI : MATRICE DE MASSE DIAGONALE
C     ------------------------------------------------------------------
      
      INTEGER   IDIAG,I,K,I0,K0

      CALL R8INIR(LDIM*(LDIM+1)/2,0.D0,MASDI,1)
      DO 10 I=1,LDIM
        I0    = I*(I-1)/2
        IDIAG = I*(I+1)/2
        MASDI(IDIAG) = MASCO(IDIAG)
        DO 20 K = (I0+1),IDIAG-1
          MASDI(IDIAG) = MASDI(IDIAG) + MASCO(K)
          K0 = K - I0
          K0 = K0*(K0+1)/2
          MASDI(K0) = MASDI(K0) + MASCO(K)
 20     CONTINUE
 10   CONTINUE
      END 
