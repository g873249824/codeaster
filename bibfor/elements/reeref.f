      SUBROUTINE REEREF(ELREFP,AXI, NNOP,NNOS,GEOM,XG,IDEPL,GRAND ,
     &                  NDIM,HE,R, UR,
     &                  FISNO,NFISS,NFH,NFE,DDLS,DDLM,FE,DGDGL,CINEM,
     &                  XE,FF,DFDI,F,EPS,GRAD)
C
