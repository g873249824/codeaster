C    BUT : CALCUL DU VECTEUR ELEMENTAIRE DE FORCE TANGENTIELLE
C
C    IN  : NDN     : NB DE NOEUDS DE L ELEMENT
C          NDDL    : NB DE DDLS DE L ELEMENT
C          VFFAUX(NDN): VECTEUR DES DIFF FFORMES
C          NOR(3)  : (BASE ORTHONORMEE DIRECTE LOCALE)
C          SIGT(3)    : CONTRAINTE TANGENTIELLE
C
C    OUT : VRIGELT(NDDL): VECTEUR ELEM FORCES NODALES
C ------------------------------------------------------------------
      IMPLICIT REAL*8 (A-H,O-Z)
      REAL*8 VRIGLT(*),VFFAUX(*),NOR(*),SIGT(*)
      REAL*8 CNOR(3,3),PROD(48,3)

      DO 10 ILIG = 1,NDIM
         DO 20 ICOL = 1,NDIM
            CNOR(ILIG,ICOL) = -NOR(ILIG)*NOR(ICOL)
   20    CONTINUE
         CNOR(ILIG,ILIG) = 1.0D0 + CNOR(ILIG,ILIG)
   10 CONTINUE

      DO 25 IDN = 1,NDN
         DO 26 ILIG = 1,NDIM
            IGEN = NDIM* (IDN-1) + ILIG
            DO 27 ICOL = 1,NDIM
               PROD(IGEN,ICOL) = VFFAUX(IDN)*CNOR(ILIG,ICOL)
   27       CONTINUE
   26    CONTINUE
   25 CONTINUE

      DO 40 ILIG = 1,NDDL
         XAUX = 0.0D0
         DO 50 ICOL = 1,NDIM
            XAUX = XAUX + SIGT(ICOL)*PROD(ILIG,ICOL)
   50    CONTINUE
         VRIGLT(ILIG) = XAUX
   40 CONTINUE
      END
