      IMPLICIT NONE
      CHARACTER*(*) NOMTE
      INTEGER ICDMAT
      REAL*8 KLV(*)
C     ------------------------------------------------------------------
C     CALCULE LA MATRICE DE RIGIDITE DES ELEMENTS DE POUTRE MULTIFIBRES

C IN  NOMTE : NOM DU TYPE ELEMENT
C             'MECA_POU_D_EM'
C     ------------------------------------------------------------------
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------

      CHARACTER*16 CH16
      INTEGER LX,LSECT
      REAL*8 G,XJX,GXJX,XL,ZERO,CASECT(6)
      PARAMETER(ZERO=0.0D0)
C     ------------------------------------------------------------------

C        --- POUTRE DROITE D'EULER A 6 DDL ---
      IF (NOMTE.NE.'MECA_POU_D_EM') THEN
        CH16 = NOMTE
        CALL U2MESK('F','ELEMENTS2_42',1,CH16)
      END IF

C    --- APPEL INTEGRATION SUR SECTION ET CALCUL G TORSION
      CALL PMFITX(ICDMAT,1,CASECT,G)

C     --- RECUPERATION DE LA CONSTANTE DE TORSION
C     --- (A PARTIR DES CARACTERISTIQUES GENERALES DES SECTIONS
C          POUR L'INSTANT)
      CALL JEVECH('PCAGNPO','L',LSECT)
      XJX = ZR(LSECT+7)
      GXJX = G*XJX

C     --- RECUPERATION DES COORDONNEES DES NOEUDS ---
      CALL JEVECH('PGEOMER','L',LX)
      LX = LX - 1
      XL = SQRT((ZR(LX+4)-ZR(LX+1))**2+ (ZR(LX+5)-ZR(LX+2))**2+
     &     (ZR(LX+6)-ZR(LX+3))**2)
      IF (XL.EQ.ZERO) THEN
        CH16 = ' ?????????'
        CALL U2MESK('F','ELEMENTS2_43',1,CH16(:8))
      END IF

C     --- CALCUL DE LA MATRICE DE RIGIDITE LOCALE
C        --- POUTRE DROITE A SECTION CONSTANTE ---
      CALL PMFK01(CASECT,GXJX,XL,KLV)


      END
