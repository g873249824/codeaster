C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*(*) NOMTE
      INTEGER ICDMAT
      REAL*8 KLV(*)
C     ------------------------------------------------------------------
C     CALCULE LA MATRICE DE RIGIDITE DES ELEMENTS DE POUTRE MULTIFIBRES

C IN  NOMTE : NOM DU TYPE ELEMENT
C             'MECA_POU_D_EM'
C             'MECA_POU_D_TGM'
C     ------------------------------------------------------------------

      CHARACTER*8  NOMAIL
      CHARACTER*16 CH16
      INTEGER LX,LSECT,IADZI,IAZK24
      INTEGER INBFIB,NBFIB,JACF
      REAL*8 G,XJX,GXJX,XL,CASECT(6),R8PREM
      REAL*8 CARS1(6),A,ALFAY,ALFAZ,E,XIZ,XIY,EY,EZ,XJG,EA
C     ------------------------------------------------------------------


C        --- POUTRE DROITE D'EULER A 6 DDL ---
      IF ((NOMTE.NE.'MECA_POU_D_EM')  .AND.
     &    (NOMTE.NE.'MECA_POU_D_TGM'))THEN
        CH16 = NOMTE
        CALL U2MESK('F','ELEMENTS2_42',1,CH16)
      END IF

C     --- RECUPERATION DES COORDONNEES DES NOEUDS ---
      CALL JEVECH('PGEOMER','L',LX)
      LX = LX - 1
      XL = SQRT((ZR(LX+4)-ZR(LX+1))**2+ (ZR(LX+5)-ZR(LX+2))**2+
     &     (ZR(LX+6)-ZR(LX+3))**2)

      IF (XL.LE.R8PREM()) THEN
        CALL TECAEL(IADZI,IAZK24)
        NOMAIL = ZK24(IAZK24-1+3)(1:8)
        CALL U2MESK('F','ELEMENTS2_43',1,NOMAIL)
      END IF

C    --- APPEL INTEGRATION SUR SECTION ET CALCUL G TORSION
      CALL PMFITX(ICDMAT,1,CASECT,G)

C     --- RECUPERATION DE LA CONSTANTE DE TORSION
C     --- (A PARTIR DES CARACTERISTIQUES GENERALES DES SECTIONS
C          POUR L'INSTANT)
      CALL JEVECH('PCAGNPO','L',LSECT)


      IF (NOMTE.EQ.'MECA_POU_D_EM')THEN
        XJX = ZR(LSECT+7)
        GXJX = G*XJX
C       --- CALCUL DE LA MATRICE DE RIGIDITE LOCALE
C        --- POUTRE DROITE A SECTION CONSTANTE ---
        CALL PMFK01(CASECT,GXJX,XL,KLV)
      ELSEIF (NOMTE.EQ.'MECA_POU_D_TGM')THEN
        CALL JEVECH('PNBSP_I','L',INBFIB)
        NBFIB = ZI(INBFIB)
        CALL JEVECH('PFIBRES','L',JACF)
        CALL PMFITG(NBFIB,3,ZR(JACF),CARS1)
        A = CARS1(1)
        XIY = CARS1(5)
        XIZ = CARS1(4)




        CALL JEVECH ('PCAGNPO', 'L',LSECT)
        LSECT = LSECT-1
        ALFAY =  ZR(LSECT+4)
        ALFAZ =  ZR(LSECT+5)
        XJX   =  ZR(LSECT+8)
        EY    = -ZR(LSECT+6)
        EZ    = -ZR(LSECT+7)
        XJG   =  ZR(LSECT+12)


        EA   = CASECT(1)
        E= EA/A


        CALL PTKA21(KLV,E,A,XL,XIY,XIZ,XJX,XJG,G,ALFAY,ALFAZ,EY,EZ)
      ENDIF


      END
