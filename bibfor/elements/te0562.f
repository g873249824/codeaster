      INTEGER DLNS
      INTEGER NNO,NPG1,I,IMATUU,NDIM,NNOS,JGANO,IRET,ICODR1
      INTEGER IPOIDS,IVF,IDFDE,IGEOM,IMATE
      INTEGER NNOB,IVFB,IDFDEB,JGANOB

      CHARACTER*8 TYPMOD(2)
      CHARACTER*16 PHENOM

C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------

      CALL ELREFV(NOMTE,'MASS',NDIM,NNO,NNOB,NNOS,NPG1,IPOIDS,IVF,
     &  IVFB,IDFDE,IDFDEB,JGANO,JGANOB)

C    TYPE DE MODELISATION
      CALL TEATTR(' ','S','TYPMOD',TYPMOD(1),IRET)
      TYPMOD(2) = 'GRADVARI'

C     PARAMETRES EN ENTREE

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PMATUUR','E',IMATUU)

C    DEPLACEMENT + LAMBDA + VAR_REG
      CALL ASSERT(NDIM.EQ.2 .OR. NDIM.EQ.3)
      DLNS = NDIM + 2  

      CALL MASSUP(OPTION,NDIM,DLNS,NNO,NNOB,ZI(IMATE),PHENOM,
     &  NPG1,IPOIDS,IDFDE,ZR(IGEOM),ZR(IVF),
     &  IMATUU,ICODR1,IGEOM,IVF)

      END
