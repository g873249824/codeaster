C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
      IMPLICIT REAL*8 (A-H,O-Z)
C
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI,ITAB(8),IRET
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80 (1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C
      REAL*8        VALRES(26),VALPAR,VALPU(2)
      CHARACTER*2   CODRET(26)
      CHARACTER*8   NOMRES(26), NOMPAR,NOMPU(2)
      CHARACTER*10  PHENOM
      REAL*8 YOUNG, NU, ALPHA
      INTEGER NPG
      CHARACTER*4 FAMI
C

      INDITH=0
      NOMPAR = 'TEMP'
C
      CALL JEVECH ('PMATERC' , 'L' , JMATE)
C
      CALL RCCOMA(ZI(JMATE),'ELAS',PHENOM,CODRET)
C
      IF ( PHENOM .EQ. 'ELAS' )  THEN

         CALL JEVECH('PNBSP_I','L',JCOU)

         NOMRES(1)='E'
         NOMRES(2)='NU'
         NOMRES(3)='ALPHA'

         CALL MOYTEM(FAMI,NPG,3*ZI(JCOU),'+',TEMP,IRET)
         CALL RCVALA(ZI(JMATE),' ',PHENOM,
     &               1,'TEMP',TEMP,3 ,NOMRES,
     &               VALRES,CODRET, 'FM')
         IF (CODRET(3).NE.'OK') THEN
           INDITH = -1
           GOTO 9999
         ENDIF
C
C     MATERIAU ISOTROPE
C
         YOUNG = VALRES(1)
         NU    = VALRES(2)
         ALPHA = VALRES(3)
 
      ELSEIF ( PHENOM .EQ. 'ELAS_ORTH' )  THEN
         NOMRES(1)='ALPHA_L'
         NOMRES(2)='ALPHA_T'
         CALL RCVALB(FAMI,1,1,'+',ZI(JMATE),' ',PHENOM,
     &               0,NOMPAR,VALPAR,2 ,NOMRES,
     &               VALRES,CODRET, 'FM')
         IF (CODRET(1).NE.'OK') THEN
            INDITH = -1
            GOTO 9999
         ELSE
            IF ((VALRES(1).EQ.0.D0).AND.(VALRES(2).EQ.0.D0)) THEN
               INDITH = -1
               GOTO 9999
            ELSE
               CALL U2MESS('F','ELEMENTS2_33')
            ENDIF
         ENDIF
      ELSE
         CALL U2MESS('F','ELEMENTS_42')
      ENDIF
C
C
 9999 CONTINUE
      END
