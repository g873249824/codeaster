C IN  KFONC : NOM DES RESULTATS (EX: FMEX1,... )
C IN  XX : VALEURS DES PARAMETRES
C IN  DN : CODE
C
C OUT FXX : VALEURS DES RESULTATS APRES RECUPERATION ET INTERPOLATION
C OUT IER : CODE RETOUR

      INTEGER DN, IER, ZIMAT, KPG, SPT

      REAL*8 XX, FXX

      INTEGER CODRES
      CHARACTER*8 KFONC, KAUX, FAMI, POUM
      CHARACTER*16 PHENOM

      PHENOM = 'GLRC_DAMAGE'
      IER = 0
      FAMI='FPG1'
      KPG=1
      SPT=1
      POUM='+'

      IF(DN .EQ. 0) THEN
        CALL RCVALB(FAMI,KPG,SPT,POUM,ZIMAT,' ',PHENOM,1,'X ',XX,1,
     &              KFONC,FXX,CODRES,1)
      ELSEIF(DN .EQ. 1) THEN
        WRITE (KAUX,'(A1,A7)') 'D',KFONC(1:7)
        CALL RCVALB(FAMI,KPG,SPT,POUM,ZIMAT,' ',PHENOM,1,'X ',XX,1,
     &              KAUX,FXX,CODRES,1)
      ELSEIF(DN .EQ. 2) THEN
        WRITE (KAUX,'(A2,A6)') 'DD',KFONC(1:6)
        CALL RCVALB(FAMI,KPG,SPT,POUM,ZIMAT,' ',PHENOM,1,'X ',XX,1,
     &              KAUX,FXX,CODRES,1)
      ELSE
        IER = 3
      ENDIF

      END
