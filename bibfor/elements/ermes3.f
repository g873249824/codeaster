C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C =====================================================================
C  ERREUR EN MECANIQUE - TERME DE SAUT - DIMENSION 3
C  **        **                   *                *
C =====================================================================
C
C     BUT:
C         DEUXIEME TERME DE L'ESTIMATEUR D'ERREUR EN RESIDU EXPLICITE : 
C         CALCUL DU SAUT DE CONTRAINTE ENTRE UNE FACE D'UN ELEMENT 
C         ET SON VOISIN EN 3D.
C
C
C     ARGUMENTS:
C     ----------
C
C      ENTREE :
C-------------
C IN   NOMTE  : NOM DU TYPE_MAILLE
C IN   NNO    : NOMBRE DE NOEUDS DU TYPE_MAILLE
C IN   IPG    : NUMERO DU POINT DE GAUSS
C IN   IGEOM  : ADRESSE DANS ZR DU TABLEAU DES COORDONNEES
C IN   IVF    : ADRESSE DANS ZR DU TABLEAU DES FONCTIONS DE FORME
C IN   ISIG   : ADRESSE DANS ZR DU TABLEAU DES CONTRAINTES AUX NOEUDS
C IN   NBCMP  : NOMBRE DE COMPOSANTES
C IN   DFDX   : DERIVEES DES FONCTIONS DE FORME / X
C IN   DFDY   : DERIVEES DES FONCTIONS DE FORME / Y
C IN   POIDS  : POIDS
C
C      SORTIE :
C-------------
C OUT  DSX    : PREMIERE COMPOSANTE DE DIVERGENCE SIGMA
C OUT  DSY    : SECONDE COMPOSANTE DE DIVERGENCE SIGMA
C OUT  NORME  : NORME DE SIGMA AU POINT DE GAUSS
C
C ......................................................................
C
      IMPLICIT NONE
      INTEGER IFA,ITYP,NBNV,NPGF,IREF1,IVOIS,ISIG,NBCMP,NOE(9,6,3)
      REAL*8 DSG11(27),DSG22(27),DSG33(27),DSG12(27),DSG13(27),DSG23(27)
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------

      INTEGER        ZI
      COMMON /IVARJE/ZI(1)
      REAL*8         ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16     ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL        ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8    ZK8
      CHARACTER*16          ZK16
      CHARACTER*24                  ZK24
      CHARACTER*32                          ZK32
      CHARACTER*80                                  ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------

      INTEGER IAREPE,JCELD,JCELV,ICONX1,ICONX2,JAD,JADV,IMAV,IGREL,IEL
      INTEGER IAVAL,INO,NCHER,INOV,INDIIS,IPGF
      
      REAL*8 SIG11(27),SIG22(27),SIG33(27),SIG12(27),SIG13(27),SIG23(27)
      REAL*8 SIGV11(27),SIGV22(27),SIGV33(27),SIGV12(27),SIGV13(27)
      REAL*8 SIGV23(27)

C ----------------------------------------------------------------------
C ----- RECHERCHE DES ADRESSES POUR OBTENIR SIGMA SUR LES VOISINS ------
C
C
      IAREPE=ZI(IREF1)
      JCELD=ZI(IREF1+1)
      JCELV=ZI(IREF1+2)
C
C ----- CALCUL DE LA NUMEROTATION DU VOISIN ----------------------------
C
      ICONX1=ZI(IREF1+10)
      ICONX2=ZI(IREF1+11)
      JAD=ICONX1-1+ZI(ICONX2+ZI(IVOIS)-1)
      JADV=ICONX1-1+ZI(ICONX2+ZI(IVOIS+IFA)-1)
C
      IMAV=ZI(IVOIS+IFA)
      IGREL=ZI(IAREPE+2*(IMAV-1))
      IEL=ZI(IAREPE+2*(IMAV-1)+1)
      IAVAL=JCELV-1+ZI(JCELD-1+ZI(JCELD-1+4+IGREL)+8)
C	
C ----- BOUCLE SUR LES POINTS DE GAUSS DE LA FACE ----------------------
C
      DO 10 IPGF=1,NPGF
C      
        INO=NOE(IPGF,IFA,ITYP)
        NCHER=ZI(JAD-1+INO)
        INOV=INDIIS(ZI(JADV),NCHER,1,NBNV)
C
C ----- RECUPERATION DE SIGMA SUR LA MAILLE COURANTE ET LE VOISIN ------
C

        SIG11(IPGF)=ZR(ISIG-1+NBCMP*(INO-1)+1)
        SIG22(IPGF)=ZR(ISIG-1+NBCMP*(INO-1)+2)
        SIG33(IPGF)=ZR(ISIG-1+NBCMP*(INO-1)+3)
        SIG12(IPGF)=ZR(ISIG-1+NBCMP*(INO-1)+4)
        SIG13(IPGF)=ZR(ISIG-1+NBCMP*(INO-1)+5)
        SIG23(IPGF)=ZR(ISIG-1+NBCMP*(INO-1)+6)
C	 
        SIGV11(IPGF)=ZR(IAVAL-1+NBCMP*NBNV*(IEL-1)+NBCMP*(INOV-1)+1)
        SIGV22(IPGF)=ZR(IAVAL-1+NBCMP*NBNV*(IEL-1)+NBCMP*(INOV-1)+2)
        SIGV33(IPGF)=ZR(IAVAL-1+NBCMP*NBNV*(IEL-1)+NBCMP*(INOV-1)+3)
        SIGV12(IPGF)=ZR(IAVAL-1+NBCMP*NBNV*(IEL-1)+NBCMP*(INOV-1)+4)
        SIGV13(IPGF)=ZR(IAVAL-1+NBCMP*NBNV*(IEL-1)+NBCMP*(INOV-1)+5)
        SIGV23(IPGF)=ZR(IAVAL-1+NBCMP*NBNV*(IEL-1)+NBCMP*(INOV-1)+6)
C
C ----- CALCUL DES SAUTS DE CONTRAINTES --------------------------------
C
        DSG11(IPGF)=SIG11(IPGF)-SIGV11(IPGF)  
        DSG22(IPGF)=SIG22(IPGF)-SIGV22(IPGF)  
        DSG33(IPGF)=SIG33(IPGF)-SIGV33(IPGF)  
        DSG12(IPGF)=SIG12(IPGF)-SIGV12(IPGF)  
        DSG13(IPGF)=SIG13(IPGF)-SIGV13(IPGF)  
        DSG23(IPGF)=SIG23(IPGF)-SIGV23(IPGF)  
C        
  10  CONTINUE
      END
