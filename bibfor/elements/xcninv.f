      SUBROUTINE XCNINV(NNOTOT,NSE,NNOP,NNO,JCNSET,CNINV)
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER   NNOTOT,NSE,NNOP,NNO,JCNSET
      INTEGER   CNINV(NNOTOT,NSE+1)
