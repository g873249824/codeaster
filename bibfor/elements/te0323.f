C ----------------------------------------------------------------------
C    OPTION REFE_FORC_NODA POUR ELEMENTS DE JOINT HM
C ----------------------------------------------------------------------

      CHARACTER*8 LIELRF(10)
      LOGICAL AXI
      INTEGER NNO1,NNO2,NPG,IVF2,IDF2,NNOS,JGN,NDDL
      INTEGER IW,IVF1,IDF1,IGEOM,IREFCO,IVECTU,NDIM,NTROU
      INTEGER IU(3,16),IP(4)
      REAL*8  SIGREF,FHYREF,R8VIDE

      LOGICAL LTEATT
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------

      CALL ELREF2(NOMTE,2,LIELRF,NTROU)
      CALL ELREF4(LIELRF(1),'RIGI',NDIM,NNO1,NNOS,NPG,IW,IVF1,IDF1,JGN)
      CALL ELREF4(LIELRF(2),'RIGI',NDIM,NNO2,NNOS,NPG,IW,IVF2,IDF2,JGN)
      NDIM = NDIM + 1
      NDDL = 2*NDIM*NNO1 + NNO2
      AXI = LTEATT(' ','AXIS','OUI')

C - DECALAGE D'INDICE POUR LES ELEMENTS DE JOINT
      CALL EJINIT(NOMTE,IU,IP)

      CALL JEVECH('PVECTUR','E',IVECTU)
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PREFCO','L',IREFCO)
      SIGREF = ZR(IREFCO)
      FHYREF = ZR(IREFCO+1)

      IF (SIGREF.EQ.R8VIDE()) CALL U2MESS('F','ALGORITH10_36')
      IF (FHYREF.EQ.R8VIDE()) CALL U2MESS('F','ALGORITH10_37')

      CALL EJFORE(NDIM,NDDL,AXI,NNO1,NNO2,NPG,IW,ZR(IW),ZR(IVF1),
     &            ZR(IVF2),IDF2,ZR(IDF2),ZR(IGEOM),IU,IP,SIGREF,
     &            FHYREF,ZR(IVECTU))

      END
