C ======================================================================
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
C     CHARACTER*32 JEXNUM,JEXNOM,JEXR8,JEXATR
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL,VECTEU,MATRIC,TEMPNO
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      
      INTEGER RESTZO
C---------------------------------------------
        REAL*8  NMNBN(6)         
        REAL*8  NMPLAS(2,3)   
        REAL*8  NMDPLA(2,2)  
        REAL*8  NMDDPL(2,2)
        REAL*8  NMZEF        
        REAL*8  NMZEG         
        INTEGER NMIEF  
        INTEGER NMPROX(2)  

C---------------------------------------------
      INTEGER BEND

      INTEGER I 
      
      INTEGER MPFONC(2)
      REAL*8 DX,DY,DISTFO 

      REAL*8     NORMN,NORMM       
      INTEGER         MP1F(2),MP2F(2)
      CHARACTER*24    CGLR
      INTEGER       IMP1,IMP2,IDMP1,IDMP2,IDDMP1,IDDMP2,INN,INM

      CALL JEMARQ()
      
      CGLR = '&&GLRC.MP1FONC'
      CALL JEVEUO(CGLR,'L',IMP1)      
      
      CGLR = '&&GLRC.MP2FONC'
      CALL JEVEUO(CGLR,'L',IMP2)      

      CGLR = '&&GLRC.NORMN'
      CALL JEVEUO(CGLR,'L',INN)   
      
      CGLR = '&&GLRC.NORMM'
      CALL JEVEUO(CGLR,'L',INM)      
      
      DO 15, I = 1,2
        MP1F(I)   = ZI(IMP1-1   + I) 
        MP2F(I)   = ZI(IMP2-1   + I)
 15   CONTINUE
 
      NORMN = ZR(INN)
      NORMM = ZR(INM)
      
      
      RESTZO=0
      IF(BEND .EQ. 1) THEN
         DO 10, I = 1,2
           MPFONC(I) = MP1F(I)
 10      CONTINUE    

      ELSE
         DO 30, I = 1,2
           MPFONC(I) = MP2F(I)
 30      CONTINUE    

      ENDIF

      DX=DISTFO(MPFONC(1),NMNBN(1),NMNBN(4),NORMN,NORMM)
      DY=DISTFO(MPFONC(2),NMNBN(2),NMNBN(5),NORMN,NORMM)

      IF(SQRT(DX**2+DY**2) .LT. 5.0D-2) THEN
         RESTZO=1
      ENDIF

      CALL JEDEMA()     


      END 
