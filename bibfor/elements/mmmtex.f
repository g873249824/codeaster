     &                  NNE   ,NNM   ,NBCPS ,WPG   ,FFL   ,
     &                  COEFAC,JACOBI,MATRCC,MATRCE,MATRCM,
     &                  MATREC,MATRMC,MATRFF,MATRFE,MATRFM,
     &                  MATREF,MATRMF)
