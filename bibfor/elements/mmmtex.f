C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C TOLE CRP_21
C
      IMPLICIT NONE
      INTEGER      NDIM,NNE,NNL,NNM,NBCPS
      REAL*8       FFL(9)
      INTEGER      TYPBAR,TYPRAC,NDEXFR
      REAL*8       WPG,JACOBI
      REAL*8       COEFAC
      REAL*8       MATRCC(9,9)  
      REAL*8       MATREC(27,9),MATRMC(27,9)
      REAL*8       MATRCE(9,27),MATRCM(9,27)        
      REAL*8       MATRFF(18,18)
      REAL*8       MATREF(27,18),MATRFE(18,27)
      REAL*8       MATRMF(27,18),MATRFM(18,27)          
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - CALCUL)
C
C CALCUL DES MATRICES - MODIFICATIONS EXCLUSION/BARSOUM/RACCORD
C
C ----------------------------------------------------------------------
C
C
C IN  NDIM   : DIMENSION DU PROBLEME
C IN  NNE    : NOMBRE DE NOEUDS DE LA MAILLE ESCLAVE
C IN  NNM    : NOMBRE DE NOEUDS DE LA MAILLE MAITRE
C IN  NNL    : NOMBRE DE NOEUDS DE LAGRANGE
C IN  NBCPS  : NOMBRE DE COMPOSANTES/NOEUD DES LAGR_C+LAGR_F
C IN  WPG    : POIDS DU POINT INTEGRATION DU POINT DE CONTACT
C IN  FFL    : FONCTIONS DE FORMES LAGR.
C IN  JACOBI : JACOBIEN DE LA MAILLE AU POINT DE CONTACT
C IN  TYPRAC : ARETE SUR LAQUELLE UN NOEUD MILIEU EST A LINEARISER
C IN  TYPBAR : ARETE OU POINT EN FOND DE FISSURE
C IN  NDEXFR : ENTIER CODE POUR EXCLUSION DIRECTION DE FROTTEMENT
C IN  COEFAC : COEF_AUGM_CONT
C OUT MATRCC : MATRICE ELEMENTAIRE LAGR_C/LAGR_C
C OUT MATRCE : MATRICE ELEMENTAIRE LAGR_C/DEPL_E
C OUT MATRCM : MATRICE ELEMENTAIRE LAGR_C/DEPL_M
C OUT MATREC : MATRICE ELEMENTAIRE DEPL_E/LAGR_C
C OUT MATRMC : MATRICE ELEMENTAIRE DEPL_M/LAGR_C
C OUT MATRFF : MATRICE ELEMENTAIRE LAGR_F/LAGR_F
C OUT MATRFE : MATRICE ELEMENTAIRE LAGR_F/DEPL_E
C OUT MATRFM : MATRICE ELEMENTAIRE LAGR_F/DEPL_M
C OUT MATREF : MATRICE ELEMENTAIRE DEPL_E/LAGR_F
C OUT MATRMF : MATRICE ELEMENTAIRE DEPL_M/LAGR_F
C
C ----------------------------------------------------------------------
C
      INTEGER   NDEXCL(9),NDEXC2(10),NBCPF
C
C ----------------------------------------------------------------------
C
      NBCPF  = NBCPS - 1
C
C --- MODIFICATION DES TERMES SI FOND_FISSURE
C
      IF (TYPBAR.NE.0) THEN
        CALL MMEXN1(TYPBAR,NDEXCL)      
        CALL MMMTE1(NDIM  ,NNL   ,NNE   ,NNM   ,WPG   ,
     &              FFL   ,COEFAC,JACOBI,NDEXCL,MATRCC,
     &              MATRCE,MATRCM,MATREC,MATRMC)
      ENDIF
C
C --- MODIFICATION DES TERMES SI LINE_QUAD
C
      IF (TYPRAC.NE.0) THEN
        CALL MMEXN2(TYPRAC,NDEXCL)        
        CALL MMMTE1(NDIM  ,NNL   ,NNE   ,NNM   ,WPG   ,
     &              FFL   ,COEFAC,JACOBI,NDEXCL,MATRCC,
     &              MATRCE,MATRCM,MATREC,MATRMC)
      ENDIF
C
C --- MODIFICATION DES TERMES SI EXCLUSION DIRECTION FROTT. SANS_NO_FR
C
      IF (NDEXFR.NE.0) THEN
        CALL ISDECO(NDEXFR,NDEXC2,10)
        CALL MMMTE2(NDIM  ,NNL   ,NNE   ,NNM   ,NBCPF ,
     &              NDEXC2,MATRFF,MATRFE,MATRFM,MATREF,
     &              MATRMF)
      ENDIF
C
      END
