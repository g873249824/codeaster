      SUBROUTINE DCPNMF(MOM,DEPS,DC,DTG,NEXMOM,DCP)
      IMPLICIT NONE
      COMMON /TDIM/ N, ND
      REAL*8 MOM(3),    DEPS(6), DC(3,3), DTG(6,6), NEXMOM(3), DET
      REAL*8 INVDC(3,3),S,       TMP(3),  DCP(3),   MATMP(3,6)
      INTEGER N, ND
