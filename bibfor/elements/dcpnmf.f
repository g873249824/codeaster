C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C-----------------------------------------------------------------------
C     BUT    :  CALCUL DE L'INCREMENT DE COURBURE PLASTIQUE
C               QUAND LE PROCHAIN POINT M-BACKM EST FIXE ET UN CRITERE
C               DE PLASTICITE EGAL A 2
C
C IN    R  MOM    : MOMENT - MOMENT DE RAPPEL
C       R  DC     : CONSTANTE DE PRAGER
C       R  DEPS   : INCREMENT DE DEFORMATION (MEMBRANE, COURBURE)
C       R  DTG    : MATRICE TANGENTE
C       R  NEXMOM : NOUVEAU MOMENT
C        
C OUT   R  DCP    : INCREMENT DE COURBURE PLASTIQUE
C-----------------------------------------------------------------------
C   
C     DETERMINANT
      DET=DC(1,1)*DC(2,2)-DC(1,2)*DC(2,1)
C
      CALL INITMA(3,3,INVDC)
C
C     DETERMINATION DE INVDC
      INVDC(1,1)=DC(2,2)/DET
      INVDC(2,2)=DC(1,1)/DET
      INVDC(2,1)=-DC(1,2)/DET
      INVDC(1,2)=-DC(2,1)/DET
      INVDC(3,3)=1.D0/DC(3,3)
C
C     EXTRACTION DU BLOC INFERIEUR 3x6 DE DTG
      CALL EXTMAT(DTG,6,3,6,'IG',MATMP)
C
      CALL PRMRVE(MATMP,3,6,DEPS,TMP)
C
      N = 3
      CALL LCSOVE(TMP,MOM,TMP)
      CALL LCDIVE(TMP,NEXMOM,TMP)
      CALL PMAVEC('ZERO',3,INVDC,TMP,DCP)
C
      END
