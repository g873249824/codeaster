      INTEGER    IFONC1,IFONC2,KK
      REAL*8     NORMF,X0
      REAL*8     XRAC
      INTEGER IER1,IER2,ITER,ITERMX
      REAL*8 FX1,FX2,FX,DFX1,DFX2,DFX,TOLE,ERR
 
      ITER=0
      ITERMX=10
      XRAC=X0

      CALL CAFONC(IFONC1,XRAC,FX1)
      CALL CAFONC(IFONC2,XRAC,FX2)
      FX=FX1-FX2
      ERR=ABS(FX)
      TOLE=1.D-8*NORMF

       DO 9, KK=1,10000000
         IF(ERR .LE. TOLE) GOTO 10
       
         ITER=ITER+1
         CALL CDNFON(IFONC1,XRAC,1,DFX1,IER1)
         CALL CDNFON(IFONC2,XRAC,1,DFX2,IER2)
         DFX=DFX1-DFX2

         IF ( (ABS(DFX) .LT. 1.D-12)
     &        .OR.(IER1 .GT. 0)
     &        .OR.(IER2 .GT. 0)            ) THEN
           CALL UTMESS('F','INTERF','NO INTERSECTION FOUND')
         ENDIF
         IF ( ITER .GT. ITERMX) THEN
            XRAC = X0
         ENDIF

         XRAC=XRAC-FX/DFX
         CALL CAFONC(IFONC1,XRAC,FX1)
         CALL CAFONC(IFONC2,XRAC,FX2)
         FX=FX1-FX2
         ERR=ABS(FX)

 9     CONTINUE
 10    CONTINUE
  
      END 
