      INTEGER       NNO,IBID,IER,NNOS,NFISS
      INTEGER       DDLD,IADZI,JTAB(7),IRET
      CHARACTER*8   ENR
C
C ----------------------------------------------------------------------
C
      CALL ELREF4(' ','RIGI',IBID,NNO,NNOS,IBID,IBID,IBID,IBID,IBID)
C
C --- INITIALISATIONS
C
      NFH   = 0
      NFE   = 0
C
      CALL TEATTR (NOMTE,'S','XFEM',ENR,IER)
C
C --- DDL ENRICHISSEMENT : HEAVYSIDE, ENRICHIS (FOND)
C
      IF (ENR(1:2).EQ.'XH') THEN
        NFH = 1
C       NOMBRE DE FISSURES : 
        CALL TECACH('NOO','PLST','L',7,JTAB,IRET)
        NFISS = JTAB(7)
C       ON NE TRAITE PAS LA JONCTION DE FISSURES EN THERMMIQUE
        CALL ASSERT(NFISS.EQ.1)
      ENDIF
C
      IF (ENR(1:2).EQ.'XT'.OR.ENR(3:3).EQ.'T') THEN
        NFE = 1
      ENDIF
C
      CALL ASSERT(    ((NFH.EQ.1).AND.(NFE.EQ.0)) 
     &            .OR.((NFH.EQ.0).AND.(NFE.EQ.1))
     &            .OR.((NFH.EQ.1).AND.(NFE.EQ.1)) )
C
      END
