C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT NONE
C
      INTEGER NB1,INTSN
      REAL*8 RNORMC
      REAL*8 XI(3,*),XR(*),VECTA(2,3),VECTC(3)
C     REAL*8 VECTN(3),VECPTX(3,3)
C
C-----------------------------------------------------------------------
      INTEGER I1 ,I2 ,INTSX ,J ,K ,L1 ,L2 

C-----------------------------------------------------------------------
      L1=207
      L2=279
      INTSX=8*(INTSN-1)
      I1=L1+INTSX
      I2=L2+INTSX
C
C     CONSTRUCTION DES VECTEURS AA AUX X PTS D'INTEGRATION
C
      DO 10 K=1,3
         VECTA(1,K)=0.D0
         VECTA(2,K)=0.D0
      DO 20 J=1,NB1
         VECTA(1,K)=VECTA(1,K)+XR(I1+J)*XI(K,J)
         VECTA(2,K)=VECTA(2,K)+XR(I2+J)*XI(K,J)
 20   CONTINUE
 10   CONTINUE
C
C     CONSTRUCTION DU VECTEUR C AUX X PTS D'INTEGRATION
C
         VECTC(1)=VECTA(1,2)*VECTA(2,3)-VECTA(1,3)*VECTA(2,2)
         VECTC(2)=VECTA(1,3)*VECTA(2,1)-VECTA(1,1)*VECTA(2,3)
         VECTC(3)=VECTA(1,1)*VECTA(2,2)-VECTA(1,2)*VECTA(2,1)
C
C     NORME DU VECTEUR C AUX X PTS D'INTEGRATION
C
         RNORMC=SQRT(VECTC(1)*VECTC(1)+VECTC(2)*VECTC(2)
     &                                +VECTC(3)*VECTC(3))
C
C     CONSTRUCTION DES VECTEURS TA AUX X PTS D'INTEGRATION
C
C        VECTN(1)=VECTC(1)/RNORMC
C        VECTN(2)=VECTC(2)/RNORMC
C        VECTN(3)=VECTC(3)/RNORMC
C
C        RNORM=SQRT(VECTA(1,1)*VECTA(1,1)+VECTA(1,2)*VECTA(1,2)
C    &                                   +VECTA(1,3)*VECTA(1,3))
C
C        VECPTX(1,1)=VECTA(1,1)/RNORM
C        VECPTX(1,2)=VECTA(1,2)/RNORM
C        VECPTX(1,3)=VECTA(1,3)/RNORM
C
C        VECPTX(2,1)=VECTN(2)*VECPTX(1,3)-VECTN(3)*VECPTX(1,2)
C        VECPTX(2,2)=VECTN(3)*VECPTX(1,1)-VECTN(1)*VECPTX(1,3)
C        VECPTX(2,3)=VECTN(1)*VECPTX(1,2)-VECTN(2)*VECPTX(1,1)
C
C        VECPTX(3,1)=VECTN(1)
C        VECPTX(3,2)=VECTN(2)
C        VECPTX(3,3)=VECTN(3)
      END
