      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16      OPTION,NOMTE

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 11/03/2013   AUTEUR IDOUX L.IDOUX 
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C ======================================================================
