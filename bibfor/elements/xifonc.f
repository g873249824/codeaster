      IF (X .GT. 0) THEN
          DD = DPLUS
      ELSEIF (X .LT. 0) THEN
          DD = DMOINS
      ELSE
          DD = (DPLUS+DMOINS)/2.D0
      ENDIF

      XIFONC = (1.D0+GAMMA*DD)/(1.D0+DD)

      END
