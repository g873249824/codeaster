      CHARACTER*16 OPTION, NOMTE
C
C ......................................................................
C  CALCUL DES VARIABLES DE COMMANDE UTILISEES DANS LES CALCULS
C  MECANIQUES
C ......................................................................
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX --------------------

      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      REAL*8 R1,R8VIDE,RVID
      INTEGER NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO,IPG,IRET
      INTEGER JPVARC,IVRC
      INTEGER      NBVARC
      PARAMETER  ( NBVARC = 7)
      CHARACTER*8   NOMVRC(NBVARC)
C
C ---------------------------------------------------------------------

      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)
      CALL JEVECH('PVARC_R','E',JPVARC)
      RVID=R8VIDE()

C     VARC_R   = R    TEMP HYDR SECH IRRA CORR NEUT1 NEUT2
      NOMVRC(1) = 'TEMP'
      NOMVRC(2) = 'HYDR'
      NOMVRC(3) = 'SECH'
      NOMVRC(4) = 'IRRA'
      NOMVRC(5) = 'CORR'
      NOMVRC(6) = 'NEUT1'
      NOMVRC(7) = 'NEUT2'

      DO 1, IPG = 1, NPG

        DO 11, IVRC = 1 , NBVARC
          CALL RCVARC(' ',NOMVRC(IVRC),'+','RIGI',IPG,1,R1,IRET)
           IF (IRET.EQ.0) THEN
             ZR(JPVARC-1+NBVARC*(IPG-1)+IVRC)=R1
           ELSE
             ZR(JPVARC-1+NBVARC*(IPG-1)+IVRC)=RVID
           ENDIF
 11     CONTINUE

1     CONTINUE

      END
