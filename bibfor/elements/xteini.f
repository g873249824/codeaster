      SUBROUTINE XTEINI(NOMTE,DDLH,NFE,SINGU,DDLC,NNOM,DDLS,NDDL)
      IMPLICIT NONE

      CHARACTER*16   NOMTE
      INTEGER        DDLH,NFE,SINGU,DDLC,NNOM,DDLS,NDDL

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 08/12/2009   AUTEUR PROIX J-M.PROIX 
C ======================================================================
C COPYRIGHT (C) 1991 - 2005  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE GENIAUT S.GENIAUT
C
C          BUT : INITIALISER LES DIMENSIONS DES DDL DANS UN TE
C                POUR LES ELEMENTS X-FEM
C
C
C IN   NOMTE  : NOM DU TYPE ELEMENT
C OUT  DDLH   : NOMBRE DE DDL HEAVYSIDE (PAR NOEUD)
C OUT  NFE    : NOMBRE DE FONCTIONS SINGULIÈRES D'ENRICHISSEMENT
C OUT  SINGU  : 1 SI ELEMENT SINGULIER, 0 SINON
C OUT  DDLC   : NOMBRE DE DDL DE CONTACT (PAR NOEUD)
C OUT  NNOM   : NB DE NOEUDS MILIEU SERVANT À PORTER DES DDL DE CONTACT
C OUT  DDLS   : NOMBRE DE DDL (DEPL+CONTACT) À CHAQUE NOEUD SOMMET
C OUT  NDDL   : NOMBRE DE DDL TOTAL DE L'ÉLÉMENT

C     ------------------------------------------------------------------
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER          ZI
      COMMON  /IVARJE/ ZI(1)
      REAL*8           ZR
      COMMON  /RVARJE/ ZR(1)
      COMPLEX*16       ZC
      COMMON  /CVARJE/ ZC(1)
      LOGICAL          ZL
      COMMON  /LVARJE/ ZL(1)
      CHARACTER*8      ZK8
      CHARACTER*16             ZK16
      CHARACTER*24                      ZK24
      CHARACTER*32                               ZK32
      CHARACTER*80                                        ZK80
      COMMON  /KVARJE/ ZK8(1), ZK16(1), ZK24(1), ZK32(1), ZK80(1)
C     -----  FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      INTEGER       NDIM,NNO,IBID,LXLGUT,IER
      CHARACTER*8   ELREFP,ENR,LAG
      LOGICAL       LSTOP
C
C ----------------------------------------------------------------------
C
      CALL ELREF1(ELREFP)
      CALL ELREF4(' ','RIGI',NDIM,NNO,IBID,IBID,IBID,IBID,IBID,IBID)

C     INITIALISATIONS
      DDLH  = 0
      NFE   = 0
      SINGU = 0
      DDLC  = 0
      LSTOP=.FALSE.

      CALL TEATTR (NOMTE,'S','XFEM',ENR,IER)
      CALL TEATTR (NOMTE,'C','XLAG',LAG,IER)
      IF (IER.NE.0) LAG='ARETE'
C
C     DDLS PAR NOEUD SOMMET : HEAVYSIDE, ENRICHIS (FOND)
      IF (ENR.EQ.'XH'.OR.ENR.EQ.'XHC') THEN
        DDLH  = NDIM
      ELSEIF (ENR.EQ.'XT'.OR.ENR.EQ.'XTC') THEN
        NFE   = 4
        SINGU = 1
      ELSEIF (ENR.EQ.'XHT'.OR.ENR.EQ.'XHTC') THEN
        DDLH  = NDIM
        NFE   = 4
        SINGU = 1
      ELSE
        CALL ASSERT(LSTOP)
      ENDIF

C     DDLS DE CONTACT
      IF (ENR(1:3).EQ.'XHC'.OR.ENR(1:3).EQ.'XTC'
     &         .OR.ENR(1:4).EQ.'XHTC') DDLC = NDIM

C
C     NB DE 'NOEUDS MILIEU' SERVANT À PORTER DES DDL DE CONTACT
      IF (LAG.EQ.'ARETE') THEN
        IF (NDIM .EQ. 3) THEN
          NNOM=3*(NNO/2)
        ELSE
          NNOM=NNO
        ENDIF
      ELSE
        NNOM=0
      ENDIF

C     NOMBRE DE DDL (DEPL+CONTACT) À CHAQUE NOEUD SOMMET
      DDLS=NDIM+DDLH+NFE*NDIM+DDLC
C
C     NOMBRE DE DDL TOTAL DE L'ÉLÉMENT
      NDDL=(NNO*DDLS)+(NNOM*DDLC)
C
      END
