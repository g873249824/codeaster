C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE GENIAUT S.GENIAUT
C
C          BUT : INITIALISER LES DIMENSIONS DES DDL DANS UN TE
C                POUR LES ELEMENTS X-FEM 
C                    
C
C IN   NOMTE  : NOM DU TYPE ELEMENT
C OUT  DDLH   : NOMBRE DE DDL HEAVYSIDE (PAR NOEUD)
C OUT  NFE    : NOMBRE DE FONCTIONS SINGULIÈRES D'ENRICHISSEMENT
C OUT  SINGU  : 1 SI ELEMENT SINGULIER, 0 SINON
C OUT  DDLC   : NOMBRE DE DDL DE CONTACT (PAR NOEUD)
C OUT  NNOM   : NB DE NOEUDS MILIEU SERVANT À PORTER DES DDL DE CONTACT
C OUT  DDLS   : NOMBRE DE DDL (DEPL+CONTACT) À CHAQUE NOEUD SOMMET 
C OUT  NDDL   : NOMBRE DE DDL TOTAL DE L'ÉLÉMENT

C     ------------------------------------------------------------------
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER          ZI
      COMMON  /IVARJE/ ZI(1)
      REAL*8           ZR
      COMMON  /RVARJE/ ZR(1)
      COMPLEX*16       ZC
      COMMON  /CVARJE/ ZC(1)
      LOGICAL          ZL
      COMMON  /LVARJE/ ZL(1)
      CHARACTER*8      ZK8
      CHARACTER*16             ZK16
      CHARACTER*24                      ZK24
      CHARACTER*32                               ZK32
      CHARACTER*80                                        ZK80
      COMMON  /KVARJE/ ZK8(1), ZK16(1), ZK24(1), ZK32(1), ZK80(1)
C     -----  FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      INTEGER       NDIM,NNO,IBID,LXLGUT
C
C ----------------------------------------------------------------------
C
      CALL ELREF4(' ','RIGI',NDIM,NNO,IBID,IBID,IBID,IBID,IBID,IBID)
C
C     DDLS PAR NOEUD SOMMET : HEAVYSIDE, ENRICHIS (FOND), CONTACT
      IF (NOMTE(1:8).EQ.'MECA_XH_' .OR. 
     & (LXLGUT(NOMTE).EQ.10 .AND. NOMTE(9:10) .EQ. 'XH')) THEN
        DDLH=NDIM
        NFE=0
        DDLC=NDIM
        SINGU=0
      ELSEIF (NOMTE(1:8).EQ.'MECA_XT_'.OR. 
     & NOMTE(9:10) .EQ. 'XT') THEN
        DDLH=0
        NFE=4
        DDLC=NDIM
        SINGU=1
      ELSEIF (NOMTE(1:8).EQ.'MECA_XHT'.OR. 
     & (NOMTE(9:11) .EQ. 'XHT')) THEN
        DDLH=NDIM
        NFE=4
        DDLC=NDIM
        SINGU=1
      ELSE 
        CALL UTMESS('F','XTEINI','NOM D''ELEMENT FINI INCOMPATIBLE')
      ENDIF
C
C     NB DE 'NOEUDS MILIEU' SERVANT À PORTER DES DDL DE CONTACT
      IF (NDIM .EQ. 3) THEN
         NNOM=3*(NNO/2)
      ELSE
         NNOM=NNO
      ENDIF

C     NOMBRE DE DDL (DEPL+CONTACT) À CHAQUE NOEUD SOMMET 
      DDLS=NDIM+DDLH+NFE*NDIM+DDLC
C
C     NOMBRE DE DDL TOTAL DE L'ÉLÉMENT
      NDDL=(NNO*DDLS)+(NNOM*DDLC)
C
      END
