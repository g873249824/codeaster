      SUBROUTINE  XDEPLA(NDIM,ELREFP,INO,NNOP,IGEOM,PINTT,DEPL,HEI,
     &                   DDLH,NFE,DDLT,BASLOC,LSN,LST,DEPLA)
      IMPLICIT NONE
      INTEGER     NDIM,DDLT,INO, NNOP,IGEOM, HEI, DDLH, NFE
      REAL*8      DEPL(DDLT,NNOP),BASLOC(3*NDIM*NNOP),LSN(NNOP)
      REAL*8      LST(NNOP)
      CHARACTER*8 ELREFP

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 04/04/2006   AUTEUR CIBHHLV L.VIVAN 
C ======================================================================
