C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT REAL*8 (A-H,O-Z)
      INCLUDE 'jeveux.h'
      CHARACTER*16        OPTION , NOMTE
C ......................................................................
C    - FONCTION REALISEE:  CALCUL DES MATRICES ELEMENTAIRES
C                          OPTION : 'RIGI_THER'
C                          ELEMENTS FOURIER
C
C    - ARGUMENTS:
C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT
C ......................................................................
C
      INTEGER ICODRE
      CHARACTER*8 FAMI,POUM
      REAL*8             DFDR(9),DFDZ(9),POIDS,R,THETA, VALRES
      INTEGER            NNO,KP,NPG1,I,J,K,ITEMPS,IMATTT,NDIM,NNOS,JGANO
      INTEGER            IPOIDS,IVF,IDFDE,IGEOM,IMATE,KPG,SPT
C
C
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG1,IPOIDS,IVF,IDFDE,JGANO)
C
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PHARMON','L',IHARM)
      NH = ZI(IHARM)
      IF(NH.EQ.-1) THEN
          CALL U2MESS('F','ELEMENTS3_63')
      ENDIF
      XH = DBLE(NH)
      XH2 = XH*XH
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PMATTTR','E',IMATTT)
      CALL JEVECH('PTEMPSR','L',ITEMPS)
      THETA = ZR(ITEMPS+2)
C
      FAMI='FPG1'
      KPG=1
      SPT=1
      POUM='+'
      CALL RCVALB (FAMI,KPG,SPT,POUM, ZI(IMATE),' ','THER',1,'INST',
     &             ZR(ITEMPS),1,'LAMBDA', VALRES, ICODRE, 1)
C
      DO 101 KP=1,NPG1
         K = (KP-1)*NNO
         CALL DFDM2D(NNO,KP,IPOIDS,IDFDE,ZR(IGEOM),DFDR,DFDZ,POIDS)
C
         R = 0.D0
         DO 102 I=1,NNO
            R = R + ZR(IGEOM+2*(I-1))*ZR(IVF+K+I-1)
102      CONTINUE
         R2 = R*R
         POIDS = POIDS*R
C
         IJ = IMATTT - 1
         DO 103 I=1,NNO
C
          DO 103 J=1,I
            WIJ = ZR(IVF+K+I-1) * ZR(IVF+K+J-1)
            IJ = IJ + 1
            ZR(IJ) = ZR(IJ) + POIDS * VALRES * THETA
     &              * ( DFDR(I)*DFDR(J) + DFDZ(I)*DFDZ(J) + XH2*WIJ/R2)
103     CONTINUE
101   CONTINUE
      END
