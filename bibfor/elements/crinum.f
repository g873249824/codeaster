      IMPLICIT REAL*8 (A-H,O-Z)
      INTEGER CRINUM,I,J,L
      REAL*8  FPLAS,      GPLAS,   A
      REAL*8  MBACKM(3),  DEPS(6), MP1(3), MP2(3), DTG(6,6),
     &        ELPRED(3),  F1ELAS,  F2ELAS, G1ELAS, G2ELAS
