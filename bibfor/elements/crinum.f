C     BUT        : CALCUL DU TYPE DE CRITERE DE PLASTICITE
C
C IN  R          NBACKN : MOMENT DE RAPPEL CINEMATIQUE
C     R          DEPS   : ACCROISSEMENT DE MEMBRANE ET DE COURBURE
C     R          MP1    : MOMENT LIMITE ELASTIQUE EN FLEXION POSITIVE
C     R          MP2    : MOMENT LIMITE ELASTIQUE EN FLEXION NEGATIVE
C     R          DTG    : MATRICE ELASTIQUE
C
C OUT R  CRINUM  : TYPE DE CRITERE DE PLASTICITE
C-----------------------------------------------------------------------
C
C     PREDICTION ELASTIQUE
C
      DO 10 I=4,6
        L = I-3
        A = 0.D0
        DO 20 J=1,6
           A = A + DTG(I,J)*DEPS(J)
20      CONTINUE
        ELPRED(L) = MBACKM(L)+A
C
10    CONTINUE
C
      F1ELAS = FPLAS(ELPRED,MP1)
      F2ELAS = FPLAS(ELPRED,MP2)
      G1ELAS = GPLAS(ELPRED,MP1)
      G2ELAS = GPLAS(ELPRED,MP2)
C
      IF (F1ELAS.GT.0.D0) THEN
        IF (F2ELAS.GT.0.D0) THEN
C- CRITERES 1 et 2
          CRINUM = 12
        ELSE
C- CRITERE 1
          CRINUM = 1
        ENDIF
      ELSEIF (F2ELAS.GT.0.D0) THEN
C- CRITERE 1
        CRINUM = 2
      ELSEIF (G1ELAS.GT.0.D0) THEN
C- CRITERE 1
        CRINUM = 1
      ELSEIF (G2ELAS.GT.0.D0) THEN
C- CRITERE 1
        CRINUM = 2
      ELSE
C- CAS ELASTIQUE
        CRINUM = 0
      ENDIF
C
      END 
