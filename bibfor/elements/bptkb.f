      DO 10 I=1,NDIMC
      DO 20 J=1,NDDLE
         WMATCB(I,J)=0.D0
      DO 30 K=1,NDIMC
         WMATCB(I,J)=WMATCB(I,J)+WMATC(I,K)*BTILD(K,J)
 30   CONTINUE
 20   CONTINUE
 10   CONTINUE
C
      DO 40 I=1,NDDLE
      DO 50 J=1,NDDLE
         KTILDI(I,J)=0.D0
      DO 60 K=1,NDIMC
         KTILDI(I,J)=KTILDI(I,J)+DBTILD(K,I)*WMATCB(K,J)
 60   CONTINUE
 50   CONTINUE
 40   CONTINUE
      END
