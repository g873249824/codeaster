      SUBROUTINE ERNOZZ (MODELE,SIGMA,CHMAT,SIGNO,OPTION,LIGREL,
     &                   IORDR,TIME,RESUCO,RESUC1,CHAMP)
      IMPLICIT NONE
      INTEGER IORDR
      REAL*8 TIME
      CHARACTER*(*) MODELE,SIGMA,CHMAT,SIGNO,OPTION,LIGREL
      CHARACTER*(*) CHAMP,RESUCO
      CHARACTER*19 RESUC1
C ----------------------------------------------------------------------
C MODIF ELEMENTS  DATE 04/04/2006   AUTEUR CIBHHLV L.VIVAN 
C
C     BUT:
C         CALCULER LES ESTIMATEURS LOCAUX A PARTIR DES
C         CONTRAINTES.
C         CALCULER LES ESTIMATEURS GLOBAUX A PARTIR DES ESTIMATEURS
C         LOCAUX CONTENUS DANS CHAMP.
C
C
C     ARGUMENTS:
C     ----------
C
C      ENTREE :
C-------------
C IN   MODELE :  NOM DU MODELE
C IN   SIGMA  :  CONTRAINTES AUX POINTS DE GAUSS
C IN   CHMAT  :  NOM DU CHAMP DE MATERIAU
C IN   SIGNO  :  CONTRAINTES AUX NOEUDS
C IN   OPTION :  'ERZ1_ELEM_SIGM' OU 'ERZ2_ELEM_SIGM'
C IN   LIGREL :  NOM D'UN LIGREL SUR LEQUEL ON FERA LE CALCUL
C IN   IORDR  :  NUMERO D'ORDRE
C IN   TIME   :  INSTANT DE CALCUL
C IN   RESUCO :  NOM DE CONCEPT ENTRANT
C IN   RESUC1 :  NOM DE CONCEPT RESULTAT DE LA COMMANDE CALC_ERREUR
C
C      SORTIE :
C-------------
C OUT  CHAMP  :  CONTRAINTES AUX NOEUDS
C
C ......................................................................

C ----------------------------------------------------------------------

      INTEGER       IBID

      CHARACTER*8   K8B, CHTEMP

      COMPLEX*16    CBID

C ----------------------------------------------------------------------

C    CALCUL DE L'ESTIMATEUR D'ERREUR
C
      CHTEMP = '&&TEMP'
      CALL MECACT('V',CHTEMP,'LIGREL',LIGREL,'TEMP_R',1,'TEMP',
     &                                        IBID,0.0D0,CBID,K8B)
      CALL ZZLOCA(MODELE,LIGREL,CHMAT,CHTEMP,SIGMA,SIGNO,CHAMP)
      CALL ZZGLOB(CHAMP,OPTION,IORDR,TIME,RESUCO,RESUC1)
C
      END
