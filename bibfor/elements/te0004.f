C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE DELMAS J.DELMAS
C
C     BUT:
C       !! ROUTINE GENERIQUE !!
C       ASSURER LE PASSAGE ELGA -> ELNO
C
C.......................................................................
C
      INCLUDE 'jeveux.h'

C
      INTEGER NDIM,NNO,NNOS,NPG,NBSP
      INTEGER IPOIDS,IVF,IDFDE,JGANO
      INTEGER IRET,NBCMP,ITABIN(7),ITABOU(7)
      INTEGER IINPG,IOUTNO

      CHARACTER*4 FAMI
C
C ----------------------------------------------------------------------
C
      IF (OPTION.EQ.'DERA_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PDERAPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PDERANO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'DISS_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PDISSPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PDISSNO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'EFGE_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PEFGAR'  ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PEFFORR' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'ENDO_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PTRIAPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PTRIANO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'ENEL_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PENERPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PENERNO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'EPFD_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PDEFOPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PDEFONO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'EPFP_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PDEFOPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PDEFONO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'EPME_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PDEFOPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PDEFONO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'EPMG_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PDEFOPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PDEFONO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'EPSG_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PDEFOPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PDEFONO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'EPSI_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PDEFOPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PDEFONO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'EPSP_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PDEFOPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PDEFONO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'EPVC_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PDEFOPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PDEFONO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'ETOT_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PENERPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PENERNO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'FLUX_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PFLUXPG' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PFLUXNO' ,'E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'SIGM_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PCONTRR' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PSIEFNOR','E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'SIEF_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PCONTRR' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PSIEFNOR','E',7,ITABOU,IRET)

      ELSEIF (OPTION.EQ.'VARI_ELNO') THEN
        FAMI = 'RIGI'
        CALL TECACH('OOO','PVARIGR' ,'L',7,ITABIN,IRET)
        CALL TECACH('OOO','PVARINR' ,'E',7,ITABOU,IRET)

      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
C    -------------------------------------------------------------------
C    -- COMMUN A TOUTES LES OPTIONS
C    -------------------------------------------------------------------
C
C --- IL FAUDRAIT PENSER A RECUPERER FAMI DE MANIERE AUTOMATIQUE ET SURE
      CALL ELREF4(' ',FAMI,NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)
      IINPG=ITABIN(1)
      IOUTNO=ITABOU(1)

C --- NOMBRE DE COMPOSANTE
      NBCMP=ITABIN(2)/ITABIN(3)
      CALL ASSERT(NBCMP.GT.0)
      CALL ASSERT(NBCMP.EQ.ITABOU(2)/ITABOU(3))

C --- ON ESSAIE UNE AUTRE FAMILLE DE POINT DE GAUSS
      IF (ITABIN(3).NE.NPG) THEN
        FAMI='MASS'
        CALL ELREF4(' ',FAMI,NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)
      ENDIF
      CALL ASSERT(ITABIN(3).EQ.NPG)

C --- NOMBRE DE SOUS-POINT
      NBSP=ITABIN(7)
      CALL ASSERT(NBSP.EQ.ITABOU(7))

C --- NOMBRE DE COMPOSANTE DYNAMIQUE
      IF (OPTION.EQ.'VARI_ELNO') THEN
        NBCMP=ITABIN(6)
        CALL ASSERT(NBCMP.EQ.ITABOU(6))
      ENDIF

      CALL PPGAN2(JGANO,NBSP,NBCMP,ZR(IINPG),ZR(IOUTNO))

      END
