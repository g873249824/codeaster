      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*16 OPTION,NOMTE

C----------------------------------------------------------------------
C FONCTION REALISEE: CALCUL DE LA DURETE ASSOCIEE A LA METALLURGIE
C                    EN 3D
C OPTION :  'DURT_ELNO'
C - ARGUMENTS:
C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT
C----------------------------------------------------------------------
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------

      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      CHARACTER*8 FAMI,POUM
      CHARACTER*24 NOMRES(5)
      INTEGER ICODRE(5),KPG,SPT

      REAL*8 PHASE(5),VALRES(5),ZALPHA,DURTNO
      INTEGER JGANO,NNO,IPOIDS,MATOS,IMATE,KN


      CALL JEMARQ()

      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG1,IPOIDS,IVF,IDFDE,JGANO)
      DO 10 I = 1,1
   10 CONTINUE



      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PPHASIN','L',IPHASI)
      CALL JEVECH('PDURT_R','E',IDURT)
      MATOS = ZI(IMATE)
      FAMI='FPG1'
      KPG=1
      SPT=1
      POUM='+'
      DO 40 KN = 1,NNO

C----RECUPERATION DES CARACTERISTIQUES
        NOMRES(1) = 'F1_DURT'
        NOMRES(2) = 'F2_DURT'
        NOMRES(3) = 'F3_DURT'
        NOMRES(4) = 'F4_DURT'
        NOMRES(5) = 'C_DURT'

        CALL RCVALB(FAMI,KPG,SPT,POUM,MATOS,' ','DURT_META',1,'TEMP',
     &              0.D0,5,NOMRES,VALRES, ICODRE,2)


C----RECUPERATION Z POUR CHAQUE PHASE
        ZALPHA = 0.D0
        DO 20 I = 1,4
          PHASE(I) = ZR(IPHASI+7* (KN-1)+I-1)
          ZALPHA = ZALPHA + PHASE(I)
   20   CONTINUE
        PHASE(5) = 1 - ZALPHA

C----CALCUL DE LA DURETE ----------------------------------------------

        DURTNO = 0.D0
        DO 30 I = 1,5
          DURTNO = DURTNO + PHASE(I)*VALRES(I)
   30   CONTINUE
        ZR(IDURT+ (KN-1)) = DURTNO

   40 CONTINUE




      CALL JEDEMA()
      END
