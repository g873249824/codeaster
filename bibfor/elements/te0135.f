C----------------------------------------------------------------------
C
C   - FONCTION REALISEE: CALCUL DU REPERE LOCAL DONNE PAR L'UTILISATEUR
C   - POUTRES, TUYAUX, DISCRETS
C
C----------------------------------------------------------------------

      INTEGER JGEOM,JORIE,JREPL1,JREPL2,JREPL3
      INTEGER NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDX,JGANO
      INTEGER I
      REAL*8  PGL(3,3)
      REAL*8  UX(3),UY(3),UZ(3)
      REAL*8  ANG(3)
C
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDX,JGANO)
      CALL ASSERT(NNOS.LE.4)

      CALL JEVECH('PGEOMER','L',JGEOM)
      CALL JEVECH('PCAORIE','L',JORIE)
      CALL JEVECH('PREPLO1','E',JREPL1)
      CALL JEVECH('PREPLO2','E',JREPL2)
      CALL JEVECH('PREPLO3','E',JREPL3)

      ANG(1) = ZR(JORIE)
      ANG(2) = ZR(JORIE+1)
      ANG(3) = ZR(JORIE+2)
      CALL MATROT ( ANG , PGL )
C     (UX,UY,UZ) : VECTEUR LOCAUX UTILISATEUR DANS LE BASE GLOBAL
C     UX EST LA PREMIERE LIGNE DE PGL
C     UY LA DEUXIEME
C     UZ LA TROISIEME

      UX(1) = PGL(1,1)
      UX(2) = PGL(1,2)
      UX(3) = PGL(1,3)

      UY(1) = PGL(2,1)
      UY(2) = PGL(2,2)
      UY(3) = PGL(2,3)

      UZ(1) = PGL(3,1)
      UZ(2) = PGL(3,2)
      UZ(3) = PGL(3,3)


      DO 12 I = 1,3
        ZR(JREPL1-1+I)=UX(I)
        ZR(JREPL2-1+I)=UY(I)
        ZR(JREPL3-1+I)=UZ(I)
 12   CONTINUE

      END
