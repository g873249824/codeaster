      SUBROUTINE VERDIS(MODEL,NOMAIL,FOUE,I3D,I2D,NDIM,IER)
      IMPLICIT          NONE
      CHARACTER*1       FOUE
      CHARACTER*8       MODEL,NOMAIL
      INTEGER           I3D,I2D,NDIM,IER
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 19/01/2011   AUTEUR MASSIN P.MASSIN 
