C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE PELLET J.PELLET
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16 OPTION,NOMTE
C     ----------------------------------------------------------------
C     CALCUL DE L'OPTION EFGE_ELNO
C     CETTE ROUTINE NE SERT QUE D'INDIRECTION VERS LE BON TE00IJ
C     EN FONCTION DE LA MODELISATION ET DE LA NATURE DU CALCUL :
C     LINEAIRE OU NON-LINEAIRE
      INTEGER J1,INLIN,IBID
      LOGICAL LINE
      CHARACTER*3  CMOD
      CHARACTER*8  ALIAS8
C-----------------------------------------------------------------------

C     -- CALCUL DE LINE :
      CALL JEVECH('PNONLIN','L',J1)
      INLIN=ZI(J1-1+1)
      LINE=(INLIN.EQ.0)

C     -- CALCUL DE CMOD (CODE DE LA MODELISATION) :
      CALL TEATTR(' ','S','ALIAS8',ALIAS8,IBID)
      CMOD=ALIAS8(3:5)

      IF (LINE) THEN
C       -- QUAND LE CALCUL EST LINEAIRE :
        IF     (CMOD.EQ.'2DB') THEN
          CALL TE0154(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'2DT'.OR.CMOD.EQ.'2TR') THEN
          CALL TE0042(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'BAR') THEN
          CALL TE0154(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'CAB'.OR.CMOD.EQ.'CAP') THEN
C         CALL TE0--(OPTION,NOMTE)
          CALL ASSERT(.FALSE.)
        ELSEIF (CMOD.EQ.'CQ3') THEN
          CALL TE0410(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'CQA'.OR.CMOD.EQ.'CQC'.OR.CMOD.EQ.'CQD') THEN
          CALL TE0231(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'DIT') THEN
          CALL TE0042(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'DTG'.OR.CMOD.EQ.'Q4S') THEN
          CALL TE0033(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'DKT'.OR.CMOD.EQ.'DST'.OR.CMOD.EQ.'Q4G') THEN
          CALL TE0033(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'DTR') THEN
          CALL TE0042(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PCT') THEN
          CALL TE0142(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PDE') THEN
          CALL TE0142(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PDG') THEN
          CALL TE0344(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PDT') THEN
          CALL TE0142(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PGD') THEN
C         CALL TE00-1(OPTION,NOMTE)
          CALL ASSERT(.FALSE.)
        ELSEIF (CMOD.EQ.'PFM') THEN
          CALL TE0142(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PGM') THEN
C         CALL TE00-1(OPTION,NOMTE)
          CALL ASSERT(.FALSE.)
        ELSEIF (CMOD.EQ.'TU3'.OR.CMOD.EQ.'TU6') THEN
          CALL TE0585(OPTION,NOMTE)
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF



      ELSE
C     -- QUAND LE CALCUL EST NON-LINEAIRE :
        IF     (CMOD.EQ.'2DB') THEN
          CALL TE0156(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'2DT'.OR.CMOD.EQ.'2TR') THEN
          CALL TE0039(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'BAR') THEN
          CALL TE0156(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'CAB'.OR.CMOD.EQ.'CAP') THEN
          CALL TE0159(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'CQ3') THEN
          CALL TE0415(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'CQA'.OR.CMOD.EQ.'CQC'.OR.CMOD.EQ.'CQD') THEN
C         CALL TE00-1(OPTION,NOMTE)
          CALL ASSERT(.FALSE.)
        ELSEIF (CMOD.EQ.'DIT') THEN
          CALL TE0039(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'DTG'.OR.CMOD.EQ.'Q4S') THEN
          CALL TE0409(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'DKT'.OR.CMOD.EQ.'DST'.OR.CMOD.EQ.'Q4G') THEN
          IF (CMOD.EQ.'DST'.OR.CMOD.EQ.'Q4G') CALL ASSERT(.FALSE.)
          CALL TE0031(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'DTR') THEN
          CALL TE0039(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PCT') THEN
          CALL TE0039(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PDE') THEN
          CALL TE0347(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PDG') THEN
          CALL TE0347(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PDT') THEN
          CALL TE0347(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PGD') THEN
          CALL TE0394(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PFM') THEN
          CALL TE0517(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'PGM') THEN
          CALL TE0517(OPTION,NOMTE)
        ELSEIF (CMOD.EQ.'TU3'.OR.CMOD.EQ.'TU6') THEN
          CALL TE0587(OPTION,NOMTE)
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF
      ENDIF

9999  CONTINUE
      END
