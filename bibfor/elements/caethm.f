     >                  DIMDEP,DIMDEF,DIMCON,NMEC,NP1,NP2,NDIM,NNO,
     >                  NNOS,NNOM,NPI,NPG,NDDLS,NDDLM,DIMUEL,
     >                  IPOIDS,IVF,IDFDE,IPOID2,IVF2,IDFDE2,JGANO)
