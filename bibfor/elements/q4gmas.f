      SUBROUTINE Q4GMAS ( XYZL, OPTION, PGL, MAS, ENER )
      IMPLICIT NONE
      REAL*8         XYZL(3,*), PGL(*), MAS(*), ENER(*)
      CHARACTER*16   OPTION
