      IMPLICIT NONE
      INTEGER      NBDM,NDIM,NNL
      INTEGER      TYPBAR,TYPRAC
      INTEGER      JDEPDE
      REAL*8       FFL(9)
      REAL*8       DLAGC
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE)
C
C CALCUL INCREMENT DES LAGRANGES DE CONTACT
C
C ----------------------------------------------------------------------
C
C
C IN  NBDM   : NB DE DDL DE LA MAILLE ESCLAVE
C                NDIM = 2 -> NBDM = DX/DY/LAGR_C/LAGR_F1
C                NDIM = 3 -> NBDM = DX/DY/DZ/LAGR_C/LAGR_F1/LAGR_F2
C IN  NDIM   : DIMENSION DU PROBLEME
C IN  NNL    : NOMBRE DE NOEUDS LAGRANGE 
C IN  JDEPDE : ADRESSE JEVEUX POUR DEPDEL
C IN  FFL    : FONCTIONS DE FORMES LAGR
C IN  TYPBAR : ARETE OU POINT EN FOND DE FISSURE
C               1 - ARETE 1-2
C               2 - ARETE 2-3
C               3 - ARETE 3-4
C               4 - ARETE 4-1
C               5 - NOEUD 1
C               6 - NOEUD 2
C IN  TYPRAC : ARETE SUR LAQUELLE UN NOEUD MILIEU EST A LINEARISER
C               1 - ARETE 1-2
C               2 - ARETE 2-3
C               3 - ARETE 3-4
C               4 - ARETE 4-1
C OUT DLAGC  : INCREMENT DEPDEL DU LAGRANGIEN DE CONTACT
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER   INOC
C
C ----------------------------------------------------------------------
C 
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DLAGC   = 0.D0       
C
C --- LAGRANGE DE CONTACT AU POINT D'INTEGRATION
C
      DO 10 INOC = 1,NNL
        DLAGC = DLAGC  + FFL(INOC)*
     &                   ZR(JDEPDE+(INOC-1)*NBDM+(NDIM+1)-1)
 10   CONTINUE
C
C --- MODIF. RACCORD/BARSOUM
C
      CALL MMLAG2(NDIM  ,NBDM  ,NNL   ,JDEPDE,TYPBAR,
     &            TYPRAC,DLAGC )
C
      CALL JEDEMA()
C
      END
