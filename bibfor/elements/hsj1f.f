      IMPLICIT REAL*8 (A-H,O-Z)
      INTEGER INTSN,KWGT
      REAL*8 XR(*)
      REAL*8 VECTG(2,3),VECTT(3,3),MATJ(3,3),JM1(3,3),EPAIS,DETJ
      REAL*8 HSF(3,9),HSJ1FX(3,9),WGT
C
C     CONSTRUCTION DE J-1 AUX PTS DE GAUSS NORMAL.    J = JACOBIEN
C
      CALL JACBM1(EPAIS,VECTG,VECTT,MATJ,JM1,DETJ)
C
C     CONSTRUCTION DE HFM * S * JTILD-1 AUX PTS D'INTEGRATION NORMAL
C
C     HFM * S = HSF = ( HSF11 , HSF12 , HSF13 )  OU   HSF1J (3,3)
C
C               J-1 0  0
C     JTILD-1 = 0  J-1 0                       OU   J-1   (3,3)
C               0   0  J-1
C
C           IB=1,1
      DO 10 JB=1,3
      DO 20  I=1,3
      DO 30  J=1,3
         J1=J+3*(JB-1)
         HSJ1FX(I,J1)=0.D0
      DO 40 K=1,3
         K1=K+3*(JB-1)
         HSJ1FX(I,J1)=HSJ1FX(I,J1)+HSF(I,K1)*JM1(K,J)
 40   CONTINUE
 30   CONTINUE
 20   CONTINUE
 10   CONTINUE
C
C     DEFINTION DE WGT = PRODUIT DES POIDS ASSOCIES AUX PTS
C      DE GAUSS (NORMAL)  ET DE DETJ
C

      WGT1=  XR(127-1+INTSN)
C
      WGT =WGT1*DETJ
C
C     STOCKAGE DE WGT DANS XR : A PARTIR DE L'ADRESSE 1181
C
         KWGT=KWGT+1
         XR(1180+KWGT)=WGT
C
      END
