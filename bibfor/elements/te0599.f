C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE CUVILLIEZ
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16 OPTION,NOMTE
C
C-----------------------------------------------------------------------
C
C     BUT: THERMIQUE LINEAIRE / ELEMENTS PRINCIPAUX X-FEM LINEAIRES
C          ECHANGE_PAROI POUR FISSURES X-FEM 
C
C          OPTION : 'CHAR_THER_PARO_F' ET 'CHAR_THER_PARO_R'
C
C     ENTREES  ---> OPTION : OPTION DE CALCUL
C              ---> NOMTE  : NOM DU TYPE ELEMENT
C
C-----------------------------------------------------------------------
C
      INTEGER NDIM,NFH,NFE,IBID,IGEOM,NNOP,JPTINT,JAINT,JCFACE
      INTEGER JLONCH,JLST,ITPS,IHECHP,JSTNO,JBASEC
      INTEGER ITEMP,IVECTT,NDDLNO
      REAL*8  R8PREM,R8BID
      LOGICAL ISELLI
      CHARACTER*8 ELREFP
      CHARACTER*4 FONREE
C
C ----------------------------------------------------------------------
C --- PREALABLES AU CALCUL
C ----------------------------------------------------------------------
C
C     ON INTERDIT LES ELTS QUADRATIQUES
      CALL ELREF1(ELREFP)
      CALL ASSERT(ISELLI(ELREFP))
C
C     CHAMPS IN CLASSIQUES
      CALL JEVECH('PGEOMER','L',IGEOM )
      CALL JEVECH('PTEMPER','L',ITEMP )
      CALL JEVECH('PTEMPSR','L',ITPS  )
C
C     SI LE COEFF D'ECHANGE PAROI EST NUL, IL N'Y A RIEN A FAIRE
      IF (OPTION.EQ.'CHAR_THER_PARO_R') THEN
        FONREE = 'REEL'
        CALL JEVECH('PHECHPR','L',IHECHP)
        IF ( ABS(ZR(IHECHP)) .LT. R8PREM() ) GOTO 9999
      ELSEIF (OPTION.EQ.'CHAR_THER_PARO_F') THEN
        FONREE = 'FONC'
        CALL JEVECH('PHECHPF','L',IHECHP)
        IF ( ZK8(IHECHP) .EQ. '&FOZERO ' ) GOTO 9999
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
C     CHAMPS IN X-FEM
      CALL JEVECH('PPINTER','L',JPTINT)
      CALL JEVECH('PAINTER','L',JAINT)
      CALL JEVECH('PCFACE' ,'L',JCFACE)
      CALL JEVECH('PLONGCO','L',JLONCH)
      CALL JEVECH('PLST'   ,'L',JLST)
      CALL JEVECH('PSTANO' ,'L',JSTNO)
      CALL JEVECH('PBASECO','L',JBASEC)
C
C     CHAMPS OUT
      CALL JEVECH('PVECTTR','E',IVECTT)
C
C     ELT DE REF PARENT : RECUP NDIM ET NNOP (NOEUDS PARENT)
C     -> RQ : 'RIGI' POUR LA FAMILLE DE PG EST DONC SANS CONSQUENCE
      CALL ELREF4(' ','RIGI',NDIM,NNOP,IBID,IBID,IBID,IBID,IBID,IBID)
C
C     RECUP DE NFH (NBRE FCT HEAVISIDE) ET NFE (NBRE FCT SINGULIER)
      CALL XTHINI(NOMTE,NFH,NFE)
      NDDLNO = 1+NFH+NFE
C
C ----------------------------------------------------------------------
C --- CALCUL DU VECTEUR ELEMENTAIRE DU A ECHANGE_PAROI
C ----------------------------------------------------------------------
C
      CALL XVECHP(NDIM,ELREFP,NNOP,IGEOM,ITEMP,ITPS,IHECHP,JPTINT,JAINT,
     &            JCFACE,JLONCH,JLST,JBASEC,NFH,NFE,FONREE,IVECTT)
C
C ----------------------------------------------------------------------
C --- SUPPRESSION DES DDLS SUPERFLUS
C ----------------------------------------------------------------------
C
      CALL XTHDDL(NFH,NDDLNO,NNOP,ZI(JSTNO),OPTION,NOMTE,R8BID,
     &            ZR(IVECTT))
C
 9999 CONTINUE
C
      END
