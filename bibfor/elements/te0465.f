      CHARACTER*24       CARAC,FF
      REAL*8             TM,TP,TA,DT
      INTEGER            NNO,NPG1,NDIM,NBFPG,ICONTM,IINSTP,NNOS,JGANO
      INTEGER            IINSTM, ICARCR, IVECTU,IRET
      INTEGER            ICARAC,IFF,IPOIDS,IVF,IDFDX,IGEOM
      CHARACTER*8        ELREFE
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------

C - FONCTIONS DE FORMES, POINTS DE GAUSS
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG1,IPOIDS,IVF,IDFDX,JGANO)

C - PARAMETRES EN ENTREE
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PCONTMR','L',ICONTM)
      CALL JEVECH('PCARCRI','L',ICARCR)
      TA = ZR(ICARCR+3)
C
C  SI LES TEMPS PLUS ET MOINS SONT PRESENTS
C  C EST QUE L ON APPELLE DEPUIS STAT NON LINE ET
C  ALORS LES TERMES DEPENDANT DE DT SONT EVALUES
C
      CALL TECACH('ONN','PINSTMR ',1,IINSTM,IRET)
      CALL TECACH('ONN','PINSTPR ',1,IINSTP,IRET)
      IF ( IINSTM.GT.0.AND.IINSTP.GT.0) THEN
       TM = ZR(IINSTM)
       TP = ZR(IINSTP)
       DT = TP - TM
      ELSE
       TM=0.D0
       TP=0.D0
       DT = 0.D0
      ENDIF
C
C - PARAMETRES EN SORTIE
      CALL JEVECH('PVECTUR','E',IVECTU)
C
      CALL SOFOSU(NNO, NPG1, ZR(IPOIDS), ZR(IVF), ZR(IDFDX),ZR(IGEOM),
     &            ZR(ICONTM),ZR(IVECTU),DT,TA)
      END
