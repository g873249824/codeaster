      IMPLICIT REAL*8 (A-H,O-Z)
      REAL*8  XYZG(3,3)
      REAL*8  NORMAL(3)
C     -----------------------------------------------------------------
C     IN  XYZG    R  9  COORDONNEES DES NOEUDS DANS LE REPERE GLOBAL
C     OUT NORMAL  R  3   VALEUR DE LA NORMALE A L'ELEMENT
C     -----------------------------------------------------------------
      REAL*8   X21 , Y21 , Z21 , X31 , Y31 , Z31
C     -----------------------------------------------------------------
      X21 = XYZG(1,2) - XYZG(1,1)
      Y21 = XYZG(2,2) - XYZG(2,1)
      Z21 = XYZG(3,2) - XYZG(3,1)
      X31 = XYZG(1,3) - XYZG(1,1)
      Y31 = XYZG(2,3) - XYZG(2,1)
      Z31 = XYZG(3,3) - XYZG(3,1)
C
      NORMAL(1) =  (Y21 * Z31 - Z21 * Y31)/2
      NORMAL(2) =  (Z21 * X31 - X21 * Z31)/2
      NORMAL(3) =  (X21 * Y31 - Y21 * X31)/2
C
      END
