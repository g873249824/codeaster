      REAL*8             NORMN(3,9),E1N(3,9),E2N(3,9),CA(9)
      REAL*8             DFDE(9,9),DFDK(9,9),J1N(9),J2N(9)
      REAL*8             SX(9,9),SY(9,9),SZ(9,9),VIBARN(2,9)
      REAL*8             DIVSIG(9),J1(9),J2(9),CAN(9),SAN(9)
