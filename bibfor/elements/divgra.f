C     &                        *VIBARN(1,I))/J1(IPG)
C     &                      +(ZR(IDFDY+KDEC+IDEC)
C     &                        *VIBARN(2,I))*CA(IPG)/J2(IPG)
C50        CONTINUE
C40       CONTINUE

       END
