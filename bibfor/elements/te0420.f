      INTEGER            IGEOM,IDINO
      INTEGER            NNO,INO,I,J,IPDEB,IPRES
C
C
C
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      CALL ELREF1(ELREFE)
C
C     ELEMENTS VIBRO-ACOUSTIQUES 3D
C
      IF (NOMTE(1:5).EQ.'MEFL_') THEN
         CHCTE = '&INEL.'//ELREFE//'.CARACTE'
         CALL JEVETE(CHCTE,'L',JIN)
         NNO = ZI(JIN+2-1)
C
      ELSE IF ((NOMTE(1:5).EQ.'MEFLT').OR.
     &         (NOMTE(1:5).EQ.'MEFLQ').OR.
     &         (NOMTE(1:5).EQ.'MEAXF')) THEN
C
C     ELEMENTS VIBRO-ACOUSTIQUES 2D ET AXISYMETRIQUES
C
         CHCTE = '&INEL.'//ELREFE//'.CARAC'
         CALL JEVETE(CHCTE,'L',JIN)
         NNO  = ZI(JIN)
C
      ENDIF
C
         CALL JEVECH('PDBEL_R','E',IPDEB)
C
         CALL JEVECH('PDEPLAC','L',IPRES)
C
C    BOUCLE SUR LES NOEUDS
C
      DO 101 INO = 1,NNO
        IDINO = IPDEB +INO - 1
        IPINO = IPRES + 2*(INO - 1)
        ZR(IDINO) = 20.D0*LOG10(ABS(ZC(IPINO))/2.D-5)
101   CONTINUE
C
      END
