C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C TOLE CRP_21
      IMPLICIT NONE
      INTEGER  IP,NBGF,NBFIB,NBVALC,NUGF(*),ICDMAT,ISECAN
      CHARACTER*16 OPTION
      CHARACTER*24 SDCOMP(*)
      REAL*8   VARIM(*),VARIMP(*),VARIP(*),CONTM(*),DEFM(*),DEFP(*)
      REAL*8   CRIT(*),INSTAM,INSTAP,DEFAP(*),DEFAM(*),EPSM
      REAL*8   SIGF(*),MODF(*)
      INTEGER  IG,NGF,NBFIG,NPG
      INTEGER  IDCIPV,IDCIPC,IDECC,IDECV,ICP
      INTEGER  IPOSV,IPOSC
      INTEGER  CODRET,CODREP
C --- ------------------------------------------------------------------
      CODREP = 0
C --- BOUCLE SUR LES GROUPES DE FIBRE
      IDCIPC = NBFIB*(IP-1)
      IDCIPV = NBVALC*IDCIPC
      IDECC  = 1
      IDECV  = 1
      DO 510 IG=1,NBGF
C ---    NUMERO DU GROUPE DE FIBRE
         NGF = NUGF(IG)
         ICP = (NGF-1)*6
C ---    NOMBRE DE FIBRES DE CE GROUPE
         READ(SDCOMP(ICP+6),'(I24)') NBFIG
C ---    AIGUILLAGE SUIVANT COMPORTEMENT
C        POUR AVOIR MODULE ET CONTRAINTE SUR CHAQUE FIBRE
C        ATTENTION POSITION DU POINTEUR CONTRAINTE ET VARIABLES INTERNES
         IPOSV = IDECV + IDCIPV
         IPOSC = IDECC + IDCIPC
         CALL PMFCOM(IP,OPTION,SDCOMP(ICP+2),CRIT,NBFIG,
     &            INSTAM,INSTAP,NPG,NBFIG,ICDMAT,
     &            NBVALC,DEFAM,DEFAP,VARIM(IPOSV),VARIMP(IPOSV),
     &            CONTM(IPOSC),DEFM(IDECC),DEFP(IDECC),EPSM,MODF(IDECC),
     &            SIGF(IDECC),VARIP(IPOSV),ISECAN,CODREP)
         IF (CODREP.NE.0) THEN
            CODRET = CODREP
C           CODE 3: ON CONTINUE ET ON LE RENVOIE A LA FIN
C           AUTRE CODES: SORTIE IMMEDIATE
            IF (CODREP.NE.3) GOTO 900
         ENDIF
         IDECC = IDECC + NBFIG
         IDECV = IDECV + NBVALC*NBFIG
C --- FIN BOUCLE GROUPES DE FIBRES$
510   CONTINUE
C
900   CONTINUE
      END
