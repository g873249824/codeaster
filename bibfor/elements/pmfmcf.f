      SUBROUTINE PMFMCF(IP,NBGF,NBFIB,NUGF,SDCOMP,
     &                  CRIT,OPTION,NPG,INSTAM,INSTAP,ICDMAT,
     &                  NBVALC,DEFAM,DEFAP,VARIM,VARIMP,
     &                  CONTM,DEFM,DEFP,EPSM,MODF,
     &                  SIGF,VARIP,ISECAN,CODRET)
