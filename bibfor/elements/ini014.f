C BUT :  ROUTINE D'INITIALISATION DES ELEMENTS DE FISSURE JOINT 2D

C IN  NOMTE : NOM DU TYPE ELEMENT
C IN  CODE  : ' ' -> INUTILE
C IN  NMAX  : DIMENSION DES TABLEAUX ITABL ET K24TAB
C OUT NVAL  : NVAL EST LE NOMBRE D'OBJETS CREES PAR INI0IJ
C              (SI NVAL > NMAX : NVAL = -NVAL ET ITABL EST VIDE)
C OUT K24TAB: CE TABLEAU CONTIENT LES NOMS DES OBJETS
C             QUE L'ON PEUT RECUPERER DANS LES TE000I
C             VIA LA ROUTINE JEVETE.
C OUT ITABL : CE TABLEAU CONTIENT LES ADRESSES
C             DES OBJETS DE K24TAB DANS ZR, ZI, ...
C.......................................................................

C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------




      INTEGER IRET,JIN
      CHARACTER*24 NOMCAR


      NOMCAR = '&INEL.QUAD4_FI.CARAC'
      CALL JEEXIN(NOMCAR,IRET)
      IF (IRET.LE.0) THEN
        CALL WKVECT(NOMCAR,'G V I',5,JIN)
        ZI(JIN+1-1) = 2
        ZI(JIN+2-1) = 4
        ZI(JIN+3-1) = 1
        ZI(JIN+4-1) = 1
        ZI(JIN+5-1) = 4
      END IF


      NVAL = 1
      CALL ASSERT(NMAX.GE.NVAL)
      K24TAB(1) = NOMCAR
      CALL JEVEUO(NOMCAR,'L',ITABL(1))

      END
