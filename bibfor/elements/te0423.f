C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C
C     BUT: CALCUL DES VECTEURS ELEMENTAIRES EN MECANIQUE
C          CORRESPONDANT A UN CHARGEMENT EN PRESSION SUIVEUSE
C          SUR DES FACES D'ELEMENTS ISOPARAMETRIQUES 3D
C          (LA PRESSION EST DONNEE SOUS FORME D'UNE FONCTION)
C
C          OPTION : 'CHAR_MECA_PRSU_F '
C
C     ENTREES  ---> OPTION : OPTION DE CALCUL
C          ---> NOMTE  : NOM DU TYPE ELEMENT
C.......................................................................
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      CHARACTER*8   ELREFE,NOMPAR(4)
      CHARACTER*24  CHVAL,CHCTE,CHTRAV
      INTEGER       IER
      INTEGER       NDIM,NNO,NPG
      INTEGER       JIN,JVAL,JTRAV
      INTEGER       IPOIDS,IVF,IDF,IP,IGEOM,IPRES,ITEMPS,IRES,KBID
      INTEGER       IDEPM,IDEPP
      INTEGER       KPG,KDEC,N,I
      REAL*8        VALPAR(4),X,Y,Z,PR
C
C
      CALL ELREF1(ELREFE)

C
C    DIMENSIONS CARACTERISTIQUES DE L'ELEMENT
      CHCTE = '&INEL.'//ELREFE//'.CARACTE'
      CALL JEVETE(CHCTE,'L',JIN)
      NDIM = ZI(JIN)
      NNO = ZI(JIN+1)
      NPG = ZI(JIN+3)
C
C    TABLEAUX DES FONCTIONS DE FORME
      CHVAL = '&INEL.'//ELREFE//'.FFORMES'
      CALL JEVETE(CHVAL,'L',JVAL)
      IPOIDS = JVAL + (NDIM+1)*NNO*NNO
      IVF    = IPOIDS + NPG
      IDF    = IVF    + NPG * NNO
C
C    VECTEUR DE TRAVAIL (PRESSION AUX POINTS DE GAUSS)
      CHTRAV = '&INEL.' //ELREFE// '.TRAVAIL'
      CALL JEVETE (CHTRAV,'L',JTRAV)
      IP   = JTRAV
C
C    AUTRES VARIABLES DE L'OPTION
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PDEPLMR','L',IDEPM)
      CALL JEVECH('PDEPLPR','L',IDEPP)
      CALL JEVECH('PPRESSF','L',IPRES)
      CALL JEVECH('PTEMPSR','L',ITEMPS)
      CALL JEVECH('PVECTUR','E',IRES)
C
C    REACTUALISATION DE LA GEOMETRIE PAR LE DEPLACEMENT
      DO 10 I = 0,NNO*3-1
        ZR(IGEOM+I) = ZR(IGEOM+I) + ZR(IDEPM+I) + ZR(IDEPP+I)
 10   CONTINUE

C    CALCUL DE LA PRESSION AUX PTS DE GAUSS (FONCTION DE T ET/OU X,Y,Z)
      VALPAR(4) = ZR(ITEMPS)
      NOMPAR(4) = 'INST'
      NOMPAR(1) = 'X'
      NOMPAR(2) = 'Y'
      NOMPAR(3) = 'Z'
C
      DO 100 KPG=0,NPG-1
        KDEC = KPG*NNO
C
C      COORDONNEES DU POINT DE GAUSS
        X = 0.D0
        Y = 0.D0
        Z = 0.D0
        DO 105 N = 0,NNO-1
          X = X + ZR(IGEOM+3*N  ) * ZR(IVF+KDEC+N)
          Y = Y + ZR(IGEOM+3*N+1) * ZR(IVF+KDEC+N)
          Z = Z + ZR(IGEOM+3*N+2) * ZR(IVF+KDEC+N)
 105    CONTINUE
C
C      VALEUR DE LA PRESSION
        VALPAR(1) = X
        VALPAR(2) = Y
        VALPAR(3) = Z
        CALL FOINTE('FM',ZK8(IPRES),4,NOMPAR,VALPAR,PR,IER)
        ZR(IP+KPG) = PR
100   CONTINUE
C
C    CALCUL EFFECTIF DU SECOND MEMBRE
      KBID = JTRAV+NPG
      CALL NMPR3D(1,NNO,NPG,ZR(IPOIDS),ZR(IVF),ZR(IDF),ZR(IGEOM),ZR(IP),
     &            ZR(IRES),ZR(KBID))
C
      END
