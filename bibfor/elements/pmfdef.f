C ---  DEFORMATION AXIALE DES FIBRES
C --- IN : FIBRES
C          NF : NOMBRE DE FIBRES
C          NCF: NOMBRE DE CARACTERISTIQUES PAR FIBRE
C          VF(1,*) : Y FIBRES
C          VF(2,*) : Z FIBRES
C          VF(3,*) : S FIBRES
C          VF(4-6,*) : CARACTERISTIQUES SUPPLEMENTAIRES
C                      (PAS FAIT ACTUELLEMENT)
C --- IN : DEFORMATIONS GENERALISEES SUR L'ELEMENT
C          DEGE(6)
