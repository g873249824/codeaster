      SUBROUTINE Q4GB ( CARAQ4, XYZL, IGAU, JACGAU, BMAT )
      IMPLICIT  NONE
      INCLUDE 'jeveux.h'
      INTEGER   IGAU
      REAL*8    XYZL(3,1), CARAQ4(*), BMAT(8,1), JACGAU
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 13/06/2012   AUTEUR COURTOIS M.COURTOIS 
C ======================================================================
