      SUBROUTINE FONNO7 (NOMA,CNXINV,NDIM,NA,VECDIR,HMAX)
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER             NA,NDIM
      REAL*8              VECDIR(3),HMAX
      CHARACTER*8         NOMA
      CHARACTER*19        CNXINV
      
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 13/06/2012   AUTEUR COURTOIS M.COURTOIS 
C ======================================================================
