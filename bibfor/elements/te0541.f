      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16 OPTION,NOMTE
C ----------------------------------------------------------------------
C FONCTION REALISEE:  CALCUL DE L'OPTION CHAR_MECA_TEMP_R POUR LES
C                     ÉLÉMENTS MECA X-FEM

C    - ARGUMENTS:
C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT

      INTEGER    NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO,IGEOM,IVECTU
      INTEGER    JPINTT,JCNSET,JHEAVT,JLONCH,JBASLO,JLSN,JLST
      INTEGER    JPMILT,DDLM,NFISS,JFISNO,IDEPLM,ICONTT
      INTEGER    NFH,DDLC,NFE,IBID,NBSIGM,DDLS,NBSIG,NDDL,JSTNO
      INTEGER    CONTAC,NNOM,SINGU,ITAB(1)
      INTEGER    IRET,K,ITEMPS
      LOGICAL    LBID
      REAL*8     RBID
      CHARACTER*8  ENR,ELREF
      CHARACTER*16 COMPOR(4)
C ----------------------------------------------------------------------

C     CARACTERISTIQUES DU TYPE D'ELEMENT : GEOMETRIE ET INTEGRATION
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)
      CALL ELREF1(ELREF)

C     INITIALISATION DES DIMENSIONS DES DDLS X-FEM
      CALL XTEINI(NOMTE,NFH,NFE,SINGU,DDLC,NNOM,DDLS,NDDL,
     &            DDLM,NFISS,CONTAC)

C     NOMBRE DE CONTRAINTES ASSOCIE A L'ELEMENT
      NBSIG = NBSIGM()

C     RECUPERATION DES XHAMPS IN ET OUT
      CALL TECACH('ONO','PCOMPOR','L',1,ITAB,IRET)
      DO 100 K=1,4
        IF (IRET.EQ.0) THEN
          COMPOR(K) = ZK16(ITAB(1)-1+K)
        ELSE
          COMPOR(K) = ' '
        ENDIF
 100  CONTINUE
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PTEMPSR','L',ITEMPS)
      CALL JEVECH('PVECTUR','E',IVECTU)
      CALL JEVECH('PCONTRT','E',ICONTT)

C     PARAMETRES PROPRES A X-FEM
      CALL JEVECH('PPINTTO','L',JPINTT)
      CALL JEVECH('PCNSETO','L',JCNSET)
      CALL JEVECH('PHEAVTO','L',JHEAVT)
      CALL JEVECH('PLONCHA','L',JLONCH)
      CALL JEVECH('PBASLOR','L',JBASLO)
      CALL JEVECH('PLSN'   ,'L',JLSN)
      CALL JEVECH('PLST'   ,'L',JLST)
      CALL JEVECH('PSTANO' ,'L',JSTNO)
C     PROPRE AUX ELEMENTS 1D ET 2D (QUADRATIQUES)
      CALL TEATTR (NOMTE,'S','XFEM',ENR,IBID)
      IF ((IBID.EQ.0).AND.(NOMTE(3:4).NE.'AX').AND.(ENR.EQ.'XH'
     & .OR.ENR.EQ.'XHT'.OR.ENR.EQ.'XT'.OR.ENR.EQ.'XHC').AND. NDIM.LE.2)
     &  CALL JEVECH('PPMILTO','L',JPMILT)
      IF (NFISS.GT.1) CALL JEVECH('PFISNO','L',JFISNO)
C
C     CALCUL DES CONTRAINTES THERMIQUES
      CALL XSIGTH(NDIM,NNO,NFH,IGEOM,ZI(JLONCH),
     &            ZR(ITEMPS),NBSIG,ZR(ICONTT))
C 
C     CALCUL DU VECTEUR \INT BT*SIGMA_THERMIQUE
      CALL XBSIG(OPTION,NDIM,NNO,NFH,NFE,DDLC,DDLM,IGEOM,COMPOR,JPINTT,
     &     ZI(JCNSET),ZI(JHEAVT),ZI(JLONCH),ZR(JBASLO),ZR(ICONTT),
     &     NBSIG,IDEPLM,ZR(JLSN),ZR(JLST),IVECTU,JPMILT,NFISS,JFISNO)
C 
C     POUR LES DDLS HEAVISIDE ENRICHIS A TORT
      CALL XTEDDL(NDIM,NFH,NFE,DDLS,NDDL,NNO,NNOS,ZI(JSTNO),
     &            .FALSE.,LBID,OPTION,NOMTE,
     &            RBID,ZR(IVECTU),DDLM,NFISS,JFISNO)

C
      END
