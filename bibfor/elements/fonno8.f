      SUBROUTINE FONNO8 (RESU,NOMA,TABLEV,VNOR,VECT)
      IMPLICIT NONE
      CHARACTER*8         NOMA, RESU
      INTEGER             TABLEV(2)
      REAL*8              VECT(3),VNOR(2,3)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 21/02/2012   AUTEUR MACOCCO K.MACOCCO 
C ======================================================================
