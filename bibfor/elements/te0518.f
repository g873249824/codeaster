      IMPLICIT NONE
      CHARACTER*16 OPTION,NOMTE
C ......................................................................
C   CALCUL DES COEFFICIENTS A0 ET A1 POUR LE PILOTAGE PAR CRITERE 
C   ELASTIQUE OU PAR DEFORMATION POUR LES ELEMENTS GRAD_VARI
C ......................................................................
      INTEGER    NNOMAX,NPGMAX,EPSMAX,DDLMAX
      PARAMETER (NNOMAX=27,NPGMAX=27,EPSMAX=20,DDLMAX=15*NNOMAX)
C ......................................................................
      CHARACTER*8 TYPMOD(2)
      CHARACTER*16 TYPILO
      LOGICAL AXI
      INTEGER NNO,NNOB,NPG,NDIM,NDDL,NEPS,LGPG,JTAB(7)
      INTEGER IRET,NNOS,JGANO,IPOIDS,IVF,IDFDE,IVFB,IDFDEB,JGANOB
      INTEGER IGEOM,IMATE,ITYPE,ICONTM,IVARIM,ICOPIL,IBORNE,ICTAU
      INTEGER IDDLM,IDDLD,IDDL0,IDDL1,ICOMPO
      REAL*8  B(EPSMAX,NPGMAX,DDLMAX),W(NPGMAX),NI2LDC(EPSMAX)
      REAL*8  ETAMIN,ETAMAX
      CHARACTER*16 NOMELT
      COMMON /FFAUTO/ NOMELT

C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------

C - INITIALISATION

      NOMELT = NOMTE
      CALL TEATTR(' ','S','TYPMOD',TYPMOD(1),IRET)
      TYPMOD(2) = 'GRADVARI'
      AXI = TYPMOD(1).EQ.'AXIS'

      CALL ELREFV(NOMTE,'RIGI',NDIM,NNO,NNOB,NNOS,NPG,IPOIDS,IVF,
     &              IVFB,IDFDE,IDFDEB,JGANO,JGANOB)


C - CALCUL DES ELEMENTS CINEMATIQUES

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL NMGVMB(NDIM,NNO,NNOB,NPG,AXI,ZR(IGEOM),ZR(IVF),ZR(IVFB),
     &            IDFDE,IDFDEB,IPOIDS,NDDL,NEPS,B,W,NI2LDC)
     

C - TYPE DE PILOTAGE (IDENTIQUE A UNE SELECTION VIA LE NOM DE L'OPTION

      CALL JEVECH('PTYPEPI','L',ITYPE)
      TYPILO = ZK16(ITYPE)


C - PARAMETRES COMMUNS AUX MODELES DE PILOTAGE

      CALL JEVECH('PDEPLMR','L',IDDLM)
      CALL JEVECH('PDDEPLR','L',IDDLD)
      CALL JEVECH('PDEPL0R','L',IDDL0)
      CALL JEVECH('PDEPL1R','L',IDDL1)
      CALL JEVECH('PCDTAU', 'L',ICTAU)
      CALL JEVECH('PCOPILO','E',ICOPIL)


C - PARAMETRES SPECIFIQUES AU PILOTAGE PAR LA LOI DE COMPORTEMENT

      IF (TYPILO .EQ. 'PRED_ELAS') THEN
        CALL JEVECH('PMATERC','L',IMATE)
        CALL JEVECH('PCOMPOR','L',ICOMPO)
        CALL JEVECH('PCONTMR','L',ICONTM)
        CALL JEVECH('PVARIMR','L',IVARIM)
        CALL JEVECH('PBORNPI','L',IBORNE)

C      BORNES POUR LE PILOTAGE (SELON LOIS DE COMPORTEMENT)
        ETAMIN=ZR(IBORNE+1)
        ETAMAX=ZR(IBORNE)

C      NOMBRE DE VARIABLES INTERNES
        CALL TECACH('OON','PVARIMR',7,JTAB,IRET)
        LGPG = MAX(JTAB(6),1)*JTAB(7)
      END IF

      CALL NGPIPE(TYPILO,NPG,NEPS,NDDL,B,NI2LDC,TYPMOD,ZI(IMATE),
     &  ZK16(ICOMPO),LGPG,ZR(IDDLM),ZR(ICONTM),ZR(IVARIM),ZR(IDDLD),
     &  ZR(IDDL0),ZR(IDDL1),ZR(ICTAU),ETAMIN,ETAMAX,ZR(ICOPIL))

      END
