C BUT :  MATRICE DE PASSAGE POUR LES ELEMENTS ISOPARAMETRIQUES 1D
C        POUR LES ELEMENTS TUYAU A 3 ET 4 NOEUDS. 2 NOEUDS SOMMETS
C
C IN  XPG   : COORDONNEES DES POINTS DE GAUSS
C IN  NNOS   : NOMBRE DE NOEUDS  (4)
C IN  NPG   : POINTS DE GAUSS (4)
C VAR MAT   : MATRICE DE PASSAGE GAUSS - NOEUDS

      INTEGER  NNOS,NPG,I,J,KP
      REAL*8  XPG(NPG),MAT(NNOS,NPG),X,ZERO
      REAL*8  GM(NNOS,NNOS),FF(2,4)
      LOGICAL FAUX

      FAUX = .FALSE.
      ZERO = 0.D0

         DO 105 KP=1,NPG
            X=XPG(KP)
            FF(1,KP)= (1.D0-X)/2.D0
            FF(2,KP)= (1.D0+X)/2.D0
105      CONTINUE

      DO 122 I=1,NNOS
         DO 122 J=1,NNOS
            GM(I,J) = 0.D0
            DO 121 KP=1,NPG
               GM(I,J)=GM(I,J)+FF(I,KP)*FF(J,KP)
121         CONTINUE
122   CONTINUE

      CALL MGAUSS(GM,FF,NNOS,NNOS,NPG,ZERO,FAUX)

      DO 30 KP=1,NPG
      DO 30 I=1,NNOS
         MAT(I,KP) = FF(I,KP)
30    CONTINUE
      END
