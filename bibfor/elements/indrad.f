      SUBROUTINE INDRAD(OPTION,COMPOR,LIGREL,CHSIG1,CHSIG2,
     &                  CHVAR1,CHVAR2,CHELE2,CHELEM)
      IMPLICIT NONE
      CHARACTER*(*) OPTION,COMPOR,LIGREL,CHSIG1,CHSIG2
      CHARACTER*(*) CHVAR1,CHVAR2,CHELE2,CHELEM
