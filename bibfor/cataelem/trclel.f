      IMPLICIT REAL*8 (A-H,O-Z)
C
      COMMON /CIMP/IMP,IULMES,IULIST,IULVIG
C
C
C     ICLASS,IVAL,CVAL ---> CE QUI A ETET LU
C
C     RETOURNE FIN = O1 OU O2 ...  OU O9 SUIVANT QUE CVAL VAUT
C       : CLELE(1),...... CLELE(9)
C     RETOURNE FIN = O10 SI ICLASS = -1 ( EOF OU FIN)
C
C
C     O1 , ......O9 ETATS DE SORTIE LIES A LA RENCONTRE DES MOTS CLES
C
C     SI OI <0  ERREUR
C
C     FIN : ETAT DE SORTIE
C
C     SI ERREUR RETURN SUR ETIQUETTE *
C
C     INCLUDE($CMOTELE)
      CHARACTER*16 CLELE
      COMMON /CMELEN/NCLELE
      COMMON /CMELEC/CLELE(9)
C     EXCLUDE($CMOTELE)
      INTEGER FIN,O1,O2,O3,O4,O5,O6,O7,O8,O9,O10
      CHARACTER*(*) CVAL
      IRTETI = 0

      IF (ICLASS.EQ.-1) THEN
        FIN = O10
        GO TO 100

      ELSE
        IF (ICLASS.EQ.3) THEN
          IF (CVAL(1:IVAL).EQ.CLELE(1)) THEN
            FIN = O1
            GO TO 100

          ELSE IF (CVAL(1:IVAL).EQ.CLELE(2)) THEN
            FIN = O2
            GO TO 100

          ELSE IF (CVAL(1:IVAL).EQ.CLELE(3)) THEN
            FIN = O3
            GO TO 100

          ELSE IF (CVAL(1:IVAL).EQ.CLELE(4)) THEN
            FIN = O4
            GO TO 100

          ELSE IF (CVAL(1:IVAL).EQ.CLELE(5)) THEN
            FIN = O5
            GO TO 100

          ELSE IF (CVAL(1:IVAL).EQ.CLELE(6)) THEN
            FIN = O6
            GO TO 100

          ELSE IF (CVAL(1:IVAL).EQ.CLELE(7)) THEN
            FIN = O7
            GO TO 100

          ELSE IF (CVAL(1:IVAL).EQ.CLELE(8)) THEN
            FIN = O8
            GO TO 100

          ELSE IF (CVAL(1:IVAL).EQ.CLELE(9)) THEN
            FIN = O9
            GO TO 100

          ELSE
            FIN = -1
            GO TO 100

          END IF

        ELSE
          FIN = -1
          GO TO 100

        END IF

      END IF

  100 CONTINUE
      IF (FIN.LT.0) THEN
        CALL UTMESS('F','TRCLEL','PB. LECTURE CATALOGUES.')
        IRTETI = 1
        GOTO 9999

      ELSE
        IRTETI = 0
        GOTO 9999

      END IF

 9999 CONTINUE
      END
