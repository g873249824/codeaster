      CHARACTER*24 CLEGD
      COMMON /CMTGDN/ NCLEGD
      COMMON /CMTGDC/ CLEGD(3)
C     EXCLUDE($CMOTGD)
      CHARACTER*72 CVAL,CLE
      REAL*8 RVAL
      LOGICAL MOT
C
      NGD = 0
C
   10 CONTINUE
      CALL SNLIRE(ICLASS,IVAL,RVAL,CVAL)
      IF ( ICLASS.EQ.3) THEN
       IF ( CVAL(1:IVAL).EQ.'FIN') THEN
        ICLASS = -1
       ENDIF
      ENDIF
      IF ( ICLASS.EQ.-1 ) THEN
       GOTO 100
      ELSE IF(ICLASS.EQ.3) THEN
       MOT = .FALSE.
       DO 20 ICLEGD=1,NCLEGD
        IF(CVAL(1:IVAL).EQ.CLEGD(ICLEGD)) THEN
         MOT = .TRUE.
         CLE = CVAL
         GOTO 30
        ENDIF
   20  CONTINUE
C
   30  CONTINUE
       IF ( MOT ) THEN
         CALL SNLIRE(ICLASS,IVAL,RVAL,CVAL)
         IF ( ICLASS.NE.1) THEN
          WRITE(IULMES,1000) CLE
 1000 FORMAT('  ERREUR LECTURE CATALOGUE DES GRANDEURS ',/,
     * ' ON ATTEND UN ENTIER APRES LE MOT : ',/,
     *2X,A30)
          CALL UTMESS('F','NGDDEF','PB. LECTURE CATALOGUES.')
          GOTO 9999
         ELSE
          NGD = NGD + IVAL
         ENDIF
       ENDIF
      ENDIF
      GOTO 10
  100 CONTINUE
 9999 CONTINUE
      END
