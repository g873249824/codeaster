C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT REAL*8 (A-H,O-Z)
C
C     INCLUDE($CIMPERR)
C
      COMMON /CIMP/IMP,IULMES,IULIST,IULVIG
C
C     EXCLUDE($CIMPERR)
C
C
C    PASSAGE DU FORMAT PROFILE AU FORMAT LONGUEUR
C    POUR LES POINTEURS DE LONGUEUR
C
C     INCLUDE($CDEBUG)
      CHARACTER*8 CLEDBG
      CHARACTER*24 OBJDMP
      INTEGER PASDMP,TYOBDM
      COMMON /CMODBG/CLEDBG
      COMMON /CDEBUG/ICCDBG
      COMMON /CBDMPC/OBJDMP(30)
      COMMON /CBDMPN/NDMP,PASDMP(30),TYOBDM(30)
C
C       NDMP : NOMBRE D OBJETS A DUMPER
C       PASDMP(IDMP)  : PASSE OU ON DUMPE L OBJET IDMP
C       OBJDMP(IDMP)  : NOM DE L OBJET IDMP
C       TYOBDM(IDMP)  : GENRE DE L OBJET IDMP :  0 OBJET SIMPLE
C                                                1 COLLECTION NUMEROTEE
C                                                2 COLLECTION NOMME
C
C     EXCLUDE($CDEBUG)
C
      INTEGER PNT(L+1)
      CHARACTER*(*) OBJ
C
C
      DO 20 I = 1,L
C
C    SI PNT(I+1) = 0 C EST QUE LA LONGUEUR DE I N A PAS ETE
C    CALCULEE , ELLE EST DONC REPUTEE NULLE
C
C    CECI S APPUIE SUR LE FAIT QUE JEVEUX INITIALISE LES
C    VECTEURS D ENTIER A 0
C
        IF (PNT(I+1).EQ.0) THEN
          PNT(I+1) = PNT(I)
        END IF
C
        IF (ICCDBG.GT.0) THEN
          IF (PNT(I+1).LT.PNT(I)) THEN
            WRITE (IULMES,*) ' '
            WRITE (IULMES,*) ' '
            WRITE (IULMES,*) ' '
            WRITE (IULMES,*) ' '
            WRITE (IULMES,*) '*********** ERREUR ***************'
            WRITE (IULMES,*) ' ERREUR CONVERSION POINTEUR'
            WRITE (IULMES,*) ' POINTEUR ',OBJ
            WRITE (IULMES,*) ' I = ',I
            WRITE (IULMES,*) ' PNT(I) = ',PNT(I)
            WRITE (IULMES,*) ' PNT(I+1) = ',PNT(I+1)
            WRITE (IULMES,*) ' PNT '
            DO 10 J = 1,L
              WRITE (IULMES,*) '  PNT( ',J,' )= ',PNT(J)
   10       CONTINUE
            WRITE (IULMES,*)
     +        '***********  RETOUR SUPERVISEUR   ********'
            CALL UTMESS('F','CHFPNT','PB. LECTURE CATALOGUES.')
            GOTO 9999

          END IF

        END IF
C
        PNT(I) = PNT(I+1) - PNT(I)
   20 CONTINUE
      PNT(L+1) = 0
      GOTO 9999

 9999 CONTINUE
      END
