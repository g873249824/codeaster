      IMPLICIT REAL*8 (A-H,O-Z)
C
C     INCLUDE($CIMPERR)
C
      COMMON /CIMP/IMP,IULMES,IULIST,IULVIG
C
C     EXCLUDE($CIMPERR)
C
C
C     DONNE L ADRESSE D UN ELEMENT I   DANS UNE COLLECTION
C     DE NOM OBJ D ELEMENTS QUELCONQUES
C
C     LE TABLEAU FORTRAN DES ADRESSES  EST ADR
C
C     INCLUDE($FUNJEV)
C
C     FONCTIONS JEVEUX
C
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR
C
C     COMMUNS   JEVEUX
C
      CHARACTER*6 PGC
      COMMON /NOMAJE/PGC
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C     EXCLUDE($FUNJEV)
C     INCLUDE($CDEBUG)
      CHARACTER*8 CLEDBG
      CHARACTER*24 OBJDMP
      INTEGER PASDMP,TYOBDM
      COMMON /CMODBG/CLEDBG
      COMMON /CDEBUG/ICCDBG
      COMMON /CBDMPC/OBJDMP(30)
      COMMON /CBDMPN/NDMP,PASDMP(30),TYOBDM(30)
C
C       NDMP : NOMBRE D OBJETS A DUMPER
C       PASDMP(IDMP)  : PASSE OU ON DUMPE L OBJET IDMP
C       OBJDMP(IDMP)  : NOM DE L OBJET IDMP
C       TYOBDM(IDMP)  : GENRE DE L OBJET IDMP :  0 OBJET SIMPLE
C                                                1 COLLECTION NUMEROTEE
C                                                2 COLLECTION NOMME
C
C     EXCLUDE($CDEBUG)
      CHARACTER*(*) OBJ
C
C
      INTEGER ADR(2)
      CHARACTER*1 K1BID

      IF (ICCDBG.GT.0) THEN
        CALL JELIRA(OBJ,'NMAXOC',N,K1BID)
        IF ((I.LT.1) .OR. (I.GT.N)) THEN
          WRITE (IULMES,9000) OBJ,N,I

 9000     FORMAT (' IDCEL ERREUR ',/,' COLLEC ',A32,/,' DIM COLLEC ',I5,
     +           /,' I ',I10)

          CALL JEIMPM('VIGILE',
     +                ' DUMP PROVOQUE PAR CC PUIS  RETOUR SUPERVISEUR ')
          CALL UTMESS('F','IDCEL','PB. LECTURE CATALOGUES.')
          GOTO 9999

        END IF
C
      END IF
C
      IF (ICCDBG.GT.0) THEN
        CALL JEVEUO(JEXNUM(OBJ,I),'E',IAD)
        IDCEL = IAD

      ELSE
        IADC = ADR(1)
        IF (IADC.EQ.0) THEN
          CALL JEVEUO(OBJ,'E',IADC)
          ADR(1) = IADC
        END IF

        IDCEL = IADC - 1 + I
      END IF

      GOTO 9999

 9999 CONTINUE
      END
