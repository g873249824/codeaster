C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================

      IMPLICIT REAL*8 (A-H,O-Z)
C
C     INCLUDE($CIMPERR)
C
      COMMON /CIMP/IMP,IULMES,IULIST,IULVIG
C
C     EXCLUDE($CIMPERR)
C
C
C     DONNE L ADRESSE D UN ELEMENT NOM,J DANS UNE COLLECTION  NOMMEE
C     DE NOM OBJ DE VECTEURS DE N IMPORTE QUOI
C
C     LE TABLEAU FORTRAN DES ADRESSES  EST ADR
C
C     INCLUDE($FUNJEV)
C
C     FONCTIONS JEVEUX
C
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR
C
C     COMMUNS   JEVEUX
C
      CHARACTER*6 PGC
      COMMON /NOMAJE/PGC
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C     EXCLUDE($FUNJEV)
C     INCLUDE($CDEBUG)
      CHARACTER*8 CLEDBG
      CHARACTER*24 OBJDMP
      INTEGER PASDMP,TYOBDM
      COMMON /CMODBG/CLEDBG
      COMMON /CDEBUG/ICCDBG
      COMMON /CBDMPC/OBJDMP(30)
      COMMON /CBDMPN/NDMP,PASDMP(30),TYOBDM(30)
C
C       NDMP : NOMBRE D OBJETS A DUMPER
C       PASDMP(IDMP)  : PASSE OU ON DUMPE L OBJET IDMP
C       OBJDMP(IDMP)  : NOM DE L OBJET IDMP
C       TYOBDM(IDMP)  : GENRE DE L OBJET IDMP :  0 OBJET SIMPLE
C                                                1 COLLECTION NUMEROTEE
C                                                2 COLLECTION NOMME
C
C     EXCLUDE($CDEBUG)
      CHARACTER*(*) OBJ,NOM,PTNOM
      INTEGER ADR(2)
C
      CHARACTER*24 TYPC
      CHARACTER*1 K1BID
C
C     SI TYPC = 'CONSTANT'  ADR(2) = - (LONGUEUR D UN VECTEUR )
C
C       ELSE                ADR(2) = ADR/ZI DU POINT DE LONG CU
C
C
      CALL JENONU(JEXNOM(PTNOM,NOM),NUMOC)
C
      IF (ICCDBG.GT.0) THEN
        CALL JELIRA(JEXNUM(OBJ,NUMOC),'LONMAX',N1,K1BID)
        IF ((J.LT.1) .OR. (J.GT.N1)) THEN
          WRITE (IULMES,9001) OBJ,NOM,N1,J

 9001     FORMAT (' IDCVNO ERREUR ',/,' COLLEC ',A32,/,' OBJET ',A32,/,
     +           ' DIMENSION VECTEUR ',I10,' J ',I10)

          CALL JEIMPM('VIGILE',
     +                ' DUMP PROVOQUE PAR CC PUIS  RETOUR SUPERVISEUR ')
          CALL UTMESS('F','IDCVNO','PB. LECTURE CATALOGUES.')
          GOTO 9999

        END IF

      END IF
C
      IF (ICCDBG.GT.0) THEN
        CALL JEVEUO(JEXNUM(OBJ,NUMOC),'E',IAD)
        IDCVNO = IAD + J - 1

      ELSE
        CALL JENONU(JEXNUM(OBJ,NUMOC),I)
        IADC = ADR(1)
        IF (IADC.EQ.0) THEN
          CALL JEVEUO(OBJ,'E',IADC)
          ADR(1) = IADC
          CALL JELIRA(OBJ,'MODELONG',IBID,TYPC)
          IF (TYPC.EQ.'CONSTANT') THEN
            CALL JELIRA(OBJ,'LONMAX',ILONV,K1BID)
            ADR(2) = -ILONV

          ELSE
            CALL JEVEUO(JEXATR(OBJ,'LONCUM'),'L',IADPC)
            ADR(2) = IADPC
          END IF

        END IF

        IF (ADR(2).GT.0) THEN
          IADPC = ADR(2)
          IDCVNO = IADC - 1 + ZI(IADPC+I-1) + J - 1

        ELSE
          ILONV = -ADR(2)
          IDCVNO = IADC + ILONV* (I-1) + J - 1
        END IF

      END IF

      GOTO 9999

 9999 CONTINUE
      END
