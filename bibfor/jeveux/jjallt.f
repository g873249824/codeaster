      IMPLICIT REAL*8 (A-H,O-Z)
      INTEGER             LONOI, LTYPI, JCTAB
      CHARACTER*(*)       GENRI, TYPEI, CI
C-----------------------------------------------------------------------
C CHAPEAU A LA ROUTINE JJALLS POUR PLACER CORRECTEMENT LE SEGMENT DE
C VALEURS EN FONCTION DU TYPE ASSOCIE
C
C IN   LONOI  : LONGUEUR DU SEGMENT DE VALEURS
C IN   GENRI  : GENRE DE L'OBJET
C IN   TYPEI  : TYPE DE L'OBJET
C IN   LTYPI  : LONGUEUR DU TYPE
C IN   CI     : = 'INIT' POUR INITIALISER LE SEGMENT DE VALEUR
C OUT  JCTAB  : ADRESSE PAR RAPPORT AU COMMUN DE REFERENCE
C
C---------- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C---------- FIN  COMMUNS NORMALISES  JEVEUX ----------------------------
      INTEGER         IZR,IZC,IZL,IZK8,IZK16,IZK24,IZK32,IZK80,JBID
      EQUIVALENCE    (IZR,ZR),(IZC,ZC),(IZL,ZL),(IZK8,ZK8),(IZK16,ZK16),
     &               (IZK24,ZK24),(IZK32,ZK32),(IZK80,ZK80)
C DEB ------------------------------------------------------------------
      JCTAB = 0
      IF ( TYPEI(1:1) .EQ. 'I' ) THEN
        CALL JJALLS (LONOI,GENRI,TYPEI,LTYPI,CI,ZI ,JBID,JCTAB)
      ELSE IF ( TYPEI(1:1) .EQ. 'R' ) THEN
        CALL JJALLS (LONOI,GENRI,TYPEI,LTYPI,CI,IZR,JBID,JCTAB)
      ELSE IF ( TYPEI(1:1) .EQ. 'C' ) THEN
        CALL JJALLS (LONOI,GENRI,TYPEI,LTYPI,CI,IZC,JBID,JCTAB)
      ELSE IF ( TYPEI(1:1) .EQ. 'K' ) THEN
        IF ( LTYPI .EQ. 8 ) THEN
          CALL JJALLS (LONOI,GENRI,TYPEI,LTYPI,CI,IZK8 ,JBID,JCTAB)
        ELSE IF ( LTYPI .EQ. 16 ) THEN
          CALL JJALLS (LONOI,GENRI,TYPEI,LTYPI,CI,IZK16,JBID,JCTAB)
        ELSE IF ( LTYPI .EQ. 24 ) THEN
          CALL JJALLS (LONOI,GENRI,TYPEI,LTYPI,CI,IZK24,JBID,JCTAB)
        ELSE IF ( LTYPI .EQ. 32 ) THEN
          CALL JJALLS (LONOI,GENRI,TYPEI,LTYPI,CI,IZK32,JBID,JCTAB)
        ELSE IF ( LTYPI .EQ. 80 ) THEN
          CALL JJALLS (LONOI,GENRI,TYPEI,LTYPI,CI,IZK80,JBID,JCTAB)
        ENDIF
      ELSE IF ( TYPEI(1:1) .EQ. 'L' ) THEN
        CALL JJALLS (LONOI,GENRI,TYPEI,LTYPI,CI,IZL,JBID,JCTAB)
      ENDIF
C FIN ------------------------------------------------------------------
      END
