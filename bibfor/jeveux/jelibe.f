      CHARACTER *6     PGMA
      COMMON /KAPPJE/  PGMA
C     ==================================================================
      CHARACTER*75     CMESS
      CHARACTER*32     NOML32
      INTEGER          ICRE , IRET
C     ==================================================================
      PGMA = 'JELIBE'
      IF ( LEN(NOMLU) .LE. 0 ) THEN
         CMESS = 'LE 1ER ARGUMENT ==> NOM N''EST PAS DE TYPE CHARACTER'
         CALL JVMESS ( 'S' , PGMA//'01' , CMESS )
      ENDIF
      NOML32 = NOMLU(1:MIN(32,LEN(NOMLU)))
C
      ICRE = 0
      CALL JJVERN ( NOML32 , ICRE , IRET )
C
      IF ( IRET .EQ. 0 ) THEN
        CMESS = 'OBJET INEXISTANT DANS LES BASES OUVERTES'
        CALL JVMESS( 'S' , PGMA//'02' , CMESS)
      ELSE
        CALL JJLIDE ( 'JELIBE' , NOML32 , IRET )
      ENDIF
      END
