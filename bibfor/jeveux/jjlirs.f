C RESPONSABLE LEFEBVRE J-P.LEFEBVRE
      IMPLICIT NONE
      INCLUDE 'jeveux_private.h'
      INTEGER JADM,ICLAS,IUS,IST
C ----------------------------------------------------------------------
C     RELIT LES ENTIERS ENCADRANT UN SEGMENT DE VALEURS
C
C IN  JADM   : ADRESSE DU PREMIER MOT DU SEGMENT DE VALEUR
C IN  ICLAS  : CLASSE DE L'OBJET JEVEUX
C IN  IDOS   : IDENTIFICATEUR D'OBJET SIMPLE OU D'OBJET DE COLLECTION
C IN  IDCO   : IDENTIFICATEUR DE COLLECTION
C OUT IUS    : USAGE DU SEGMENT DE VALEUR
C OUT IST    : STATUT DU SEGMENT DE VALEUR
C ----------------------------------------------------------------------
      INTEGER          LK1ZON , JK1ZON , LISZON , JISZON 
      COMMON /IZONJE/  LK1ZON , JK1ZON , LISZON , JISZON
C ----------------------------------------------------------------------
      INTEGER          ISTAT
      COMMON /ISTAJE/  ISTAT(4)
C DEB ------------------------------------------------------------------
C-----------------------------------------------------------------------
      INTEGER ICLA2 ,IDATOC ,IDCO ,IDOS ,IS ,ISTA1 ,ISTA2 

C-----------------------------------------------------------------------
      ISTA1  = ISZON(JISZON+JADM-1)
      IDATOC = ISZON(JISZON+JADM-2)
      IF (IDATOC.NE.IDOS) THEN
        CALL U2MESI('F','JEVEUX1_54',1,JADM)
      END IF
C
      IF (ISTA1.NE.ISTAT(1) .AND. ISTA1.NE.ISTAT(2)) THEN
        CALL U2MESI('F','JEVEUX1_54',1,JADM)
      END IF
C
      IS = JISZON+ISZON(JISZON+JADM-4)
      ISTA2 = ISZON(IS-4)
      ICLA2 = ISZON(IS-2)
      IF (ICLA2.NE.ICLAS) THEN
        CALL U2MESI('F','JEVEUX1_55',1,JADM)
      END IF

      IF (ISTA2.NE.ISTAT(3) .AND. ISTA2.NE.ISTAT(4)) THEN
        CALL U2MESI('F','JEVEUX1_55',1,JADM)
      END IF
C
      IUS = ISTA1
      IST = ISTA2
C FIN ------------------------------------------------------------------
      END
