C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C     -----  FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      LOGICAL      FIRST,EXISDG
      CHARACTER*6  MOINS1
      CHARACTER*80 CBUF,KBID
      REAL*8       RBID
C
C  ------------ FIN DECLARATION -------------
C
C  -->N  D'UNITE LOGIQUE ASSOCIE AUX FICHIERS
      CALL JEMARQ()
      IUNV = IUNIFI('IDEAS')
C
      FIRST=.TRUE.
      MOINS1 = '    -1'
      INUS  = 10
      CALL JEEXIN('&&IDEAS.SYST',IRET)
      IF(IRET.NE.0) THEN
        CALL JEDETR('&&IDEAS.SYST')
        CALL UTMESS('A','SLECOR',' ATTENTION LE DATASET 2420'
     &    //' APPARAIT PLUSIEURS FOIS.')
      ENDIF
      CALL WKVECT('&&IDEAS.SYST','V V I',INUS,JSYS)
   1  CONTINUE
      READ(IUNV,'(A)') CBUF
      IF (CBUF(1:6).NE.MOINS1) THEN
C
        IF (FIRST) THEN
          READ(IUNV,'(A)') KBID
          READ(IUNV,'(3I10)') INUM,ICOOR,ICOL
        ELSE
          READ(CBUF,'(3I10)') INUM,ICOOR,ICOL
        ENDIF
        IF (INUM.GT.INUS) THEN
          INUS  = INUM
          CALL JUVECA('&&IDEAS.SYST',INUS)
          CALL JEVEUO('&&IDEAS.SYST','E',JSYS)
        ENDIF
C
        ZI(JSYS-1+INUM) = ICOOR
        READ(IUNV,'(A)') KBID
        READ(IUNV,'(3(1PD25.16))') RBID
        READ(IUNV,'(3(1PD25.16))') RBID
        READ(IUNV,'(3(1PD25.16))') RBID
        READ(IUNV,'(3(1PD25.16))') RBID
C
        FIRST = .FALSE.
        GOTO 1
      ENDIF
      CALL JEDEMA()
      END
