C 
      IF ( ( TM .EQ. 'TRIA3' ) .OR. ( TM .EQ. 'QUAD4' ) ) THEN
C
           DROI = .TRUE.
C
      ELSE
C
           CALL RVDET2 ( X3-X1, Y3-Y1, X2-X1, Y2-Y1, D )
           L2 = (X3-X1)*(X3-X1) + (Y3-Y1)*(Y3-Y1)
           CRIT = ABS( 2.0D0 * D ) / L2
C
           IF ( CRIT .LT. EPSI ) DROI = .TRUE.
C
      ENDIF
