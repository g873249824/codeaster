      SUBROUTINE NMASSE(ICODMA,MATERI,
     &                  INST,TEMP,
     &                  E,NU,ALPHA,DEUXMU,TROISK)

      IMPLICIT NONE
      INTEGER  ICODMA
      REAL*8   INST,TEMP
      REAL*8   E,NU,ALPHA,DEUXMU,TROISK
      CHARACTER*(*) MATERI
