C
C ROUTINE MECA_NON_LINE (ALGORITHME - DECOUPE)
C
C EXTENSION DE LA LISTE DES NIVEAUX DE DECOUPAGE
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION
C IN  NUMINS : NUMERO D'INSTANT
C IN  INSPAS : NOMBRE DE PAS DE TEMPS A INSERER
C IN  NBINS  : NOMBRE DE PAS DE TEMPS A INSERER
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C 
      INTEGER      IPAS,NBNEW
      CHARACTER*24 TPSDIN
      INTEGER      JNIVTP
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES SD
C         
      TPSDIN = SDDISC(1:19)//'.DINI'
C
C --- NOUVEAU NOMBRE D'INSTANTS
C
      NBNEW  = NBINI + NBINS   
C
C --- ALLONGEMENT DE LA LISTE DES NIVEAUX   
C
      CALL JUVECA(TPSDIN,NBNEW )
      CALL JEVEUO(TPSDIN,'E',JNIVTP)
C            
C --- RECOPIE DE LA PARTIE HAUTE DE LA LISTE
C
      DO 10 IPAS = NBINI, NUMINS+1, -1
        ZI(JNIVTP+IPAS+NBINS-1) = ZI(JNIVTP+IPAS-1)
 10   CONTINUE
C
C --- INCREMENTATION DU NIVEAU SUR LA PARTIE DECOUPEE
C
      DO 20 IPAS = NUMINS, NUMINS+NBINS-1
        ZI(JNIVTP+IPAS) = ZI(JNIVTP+NUMINS+NBINS)+1
 20   CONTINUE
C
      CALL JEDEMA()
      END
