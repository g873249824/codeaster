C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      INCLUDE 'jeveux.h'
      CHARACTER*(*) MODELE,CARELE,MATE,TEMPLU,VE,OPT
      REAL*8 INSTAP
C ---------------------------------------------------------------------
C     CALCUL DES VECTEURS ELEMENTAIRES DES FLUX FLUIDES

C IN  MODELE  : NOM DU MODELE
C IN  CARELE  : CARACTERISTIQUES DES POUTRES ET COQUES
C IN  MATE    : MATERIAU
C IN  TEMPLU  : CHAM_NO DE TEMPERATURE OU DE DEPL
C IN  INSTAP  : INSTANT DU CALCUL
C VAR VE  : VECT_ELEM



      CHARACTER*8 LPAIN(4),LPAOUT(1),K8BID,KBID
      CHARACTER*16 OPTION
      CHARACTER*19 VECEL
      CHARACTER*24 CHGEOM,CHTIME
      CHARACTER*24 LIGRMO,LCHIN(4),LCHOUT(1),VE2
      INTEGER IBID
      COMPLEX*16 CBID

C-----------------------------------------------------------------------
      INTEGER JLVE
C-----------------------------------------------------------------------
      CALL JEMARQ()

      VECEL = VE

      VE2 = VECEL//'.RELR'
      CALL DETRSD('VECT_ELEM',VECEL)
      CALL MEMARE('V',VECEL,MODELE(1:8),MATE,CARELE,'CHAR_THER')
      CALL WKVECT(VE2,'V V K24',1,JLVE)
      IF (TEMPLU(9:14).EQ.'.BIDON') THEN
        CALL JEECRA(VE2,'LONUTI',0,K8BID)
        GO TO 10
      END IF

      LIGRMO = MODELE(1:8)//'.MODELE'

      CALL MEGEOM(MODELE(1:8),CHGEOM)



      LPAIN(1) = 'PGEOMER'
      LCHIN(1) = CHGEOM
      CHTIME = '&&VECHME.CH_INST_R'
      CALL MECACT('V',CHTIME,'MODELE',LIGRMO,'INST_R  ',1,'INST   ',
     &            IBID,INSTAP,CBID,KBID)
      CALL MECACT('V','&VECTFL.VEC','MODELE',LIGRMO,'TEMP_R  ',1,
     &            'TEMP   ',IBID,0.D0,CBID,KBID)
      LPAIN(2) = 'PTEMPSR'
      LCHIN(2) = CHTIME
      LCHIN(3) = TEMPLU
      LPAIN(4) = 'PMATERC'
      LCHIN(4) = MATE
      IF (OPT.EQ.'R') THEN
        OPTION = 'CHAR_THER_ACCE_R'
        LPAIN(3) = 'PACCELR'
      ELSE
        IF (OPT.EQ.'X') THEN
          OPTION = 'CHAR_THER_ACCE_X'
          LPAIN(3) = 'PTEMPER'
        ELSE
          IF (OPT.EQ.'Y') THEN
            OPTION = 'CHAR_THER_ACCE_Y'
            LPAIN(3) = 'PTEMPER'
          ELSE
            IF (OPT.EQ.'Z') THEN
              OPTION = 'CHAR_THER_ACCE_Z'
              LPAIN(3) = 'PTEMPER'
            END IF
          END IF
        END IF
      END IF

      LPAOUT(1) = 'PVECTTR'

      LCHOUT(1) = '&&VECTFL.A'
      CALL CORICH('E',LCHOUT(1),-1,IBID)
      CALL CALCUL('S',OPTION,LIGRMO,4,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'V',
     &               'OUI')
      ZK24(JLVE) = LCHOUT(1)
      CALL JEECRA(VE2,'LONUTI',1,K8BID)

   10 CONTINUE

      CALL JEDEMA()
      END
