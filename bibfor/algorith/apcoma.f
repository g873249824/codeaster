      IMPLICIT     NONE
      INCLUDE 'jeveux.h'

      CHARACTER*19 SDAPPA
      CHARACTER*8  NOMA
      CHARACTER*19 NEWGEO
      INTEGER      NUMMA ,NNOSDM
      REAL*8       COORMA(27)
C
C ----------------------------------------------------------------------
C
C ROUTINE APPARIEMENT (UTILITAIRE)
C
C COORDONNEES D'UNE MAILLE
C
C ----------------------------------------------------------------------
C
C
C IN  SDAPPA : NOM DE LA SD APPARIEMENT
C IN  NOMA   : SD MAILLAGE
C IN  NEWGEO : CHAMP DE GEOMETRIE ACTUALISE
C IN  NUMMA  : NUMERO ABSOLU DE LA MAILLE DANS LE MAILLAGE
C IN  NNOSDM : NOMBRE DE NOEUDS DE LA MAILLE POUR LE CONTACT
C OUT COORMA : COORDONNEES DE LA MAILLE
C
C
C
C
      CHARACTER*32 JEXNUM
      CHARACTER*24 K8BID
C
      INTEGER      NBNMAX
      PARAMETER   (NBNMAX = 9)
C
      INTEGER      NO(NBNMAX),INO
      INTEGER      JCOOR,I,JDEC
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DO 10 I = 1,27
        COORMA(I) = 0.D0
  10  CONTINUE
C
C --- NOMBRE DE NOEUDS DE LA MAILLE
C
      CALL ASSERT(NNOSDM.GT.0)
      CALL ASSERT(NNOSDM.LE.NBNMAX)
C
C --- NUMEROS ABSOLUS DES NOEUDS DE LA MAILLE
C
      CALL JEVEUO(JEXNUM(NOMA//'.CONNEX',NUMMA ),'L',JDEC)
      DO 61 INO = 1,NNOSDM
        NO(INO) = ZI(JDEC+INO-1)
 61   CONTINUE
C
C --- COORDONNEES DES NOEUDS DE LA MAILLE
C
      CALL JEVEUO(NEWGEO(1:19)//'.VALE','L',JCOOR)
      DO 70 INO = 1,NNOSDM
        COORMA(3*(INO-1)+1) = ZR(JCOOR+3*(NO(INO)-1))
        COORMA(3*(INO-1)+2) = ZR(JCOOR+3*(NO(INO)-1)+1)
        COORMA(3*(INO-1)+3) = ZR(JCOOR+3*(NO(INO)-1)+2)
  70  CONTINUE
C
      CALL JEDEMA()
C
      END
