     &                  MAILLA,LIGRCF,CARTCF,MODELE,LISCHA,SOLVEU,
     &                  NUMEDD,MCONEL,SCONEL,MATASS,MEMASS,MASSE,
     &                  CMD,INST,VITMOI,ACCMOI)
