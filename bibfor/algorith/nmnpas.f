C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      LOGICAL      FONACT(*),PREMIE
      CHARACTER*24 VALMOI(8),VALPLU(8),DEPALG(8),POUGD(8)
      CHARACTER*19 SDNURO
      REAL*8       INSTAM,INSTAP
      CHARACTER*24 DEFICO,RESOCO,SDSENS
      CHARACTER*19 SDDYNA      
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C INITIALISATION DES CHAMPS D'INCONNUES POUR UN NOUVEAU PAS DE TEMPS
C      
C ----------------------------------------------------------------------
C
C
C IN  FONACT : FONCTIONNALITES ACTIVEES (VOIR NMFONC)
C IN  PREMIE : SI PREMIER PAS DE TEMPS
C IN  SDDYNA : SD DYNAMIQUE
C IN  SDSENS : SD SENSIBILITE
C IN  VALMOI : ETAT EN T-
C IN  VALPLU : ETAT EN T+
C IN  DEPALG : VECTEURS SOLUTIONS
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      LOGICAL      ISFONC,REAROT,NDYNLO,LDYNA 
      INTEGER      NEQ
      CHARACTER*8  K8BID
      CHARACTER*24 K24BID
      CHARACTER*24 DEPMOI,VARMOI,VITMOI,ACCMOI
      CHARACTER*24 DEPPLU,VARPLU,VITPLU,ACCPLU
      CHARACTER*24 DEPDEL
      INTEGER      JDEPP ,JDEPDE
      INTEGER      INDRO ,ISNNEM           
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C      
      INDRO = ISNNEM()
C
C --- FONCTIONNALITES ACTIVEES
C
      REAROT = ISFONC(FONACT,'REAROT') 
      IF (REAROT) CALL JEVEUO(SDNURO//'.NDRO','L',INDRO)
      LDYNA  = NDYNLO(SDDYNA,'DYNAMIQUE')
C
C --- DECOMPACTION DES VARIABLES CHAPEAUX
C
      CALL DESAGG(VALMOI,DEPMOI,K24BID,VARMOI,K24BID,
     &            VITMOI,ACCMOI,K24BID,K24BID)
      CALL DESAGG(VALPLU,DEPPLU,K24BID,VARPLU,K24BID,
     &            VITPLU,ACCPLU,K24BID,K24BID) 
      CALL DESAGG(DEPALG,K24BID,DEPDEL,K24BID,K24BID,
     &            K24BID,K24BID,K24BID,K24BID)
      CALL JELIRA(DEPMOI(1:19)//'.VALE','LONMAX',NEQ,K8BID)  
C    
C --- ESTIMATIONS INITIALES DES VARIABLES INTERNES 
C
      CALL COPISD('CHAMP_GD','V',VARMOI,VARPLU)
C
C --- INITIALISATION DES DEPLACEMENTS
C
      CALL COPISD('CHAMP_GD','V',DEPMOI,DEPPLU)
C
C --- INITIALISATION DE L'INCREMENT DE DEPLACEMENT      
C
      CALL JEVEUO(DEPDEL(1:19)//'.VALE','E',JDEPDE)
      CALL JEVEUO(DEPPLU(1:19)//'.VALE','E',JDEPP )
      CALL INITIA(NEQ,REAROT,ZI(INDRO),ZR(JDEPP),ZR(JDEPDE))
C      
C --- INITIALISATIONS EN DYNAMIQUE
C
      IF (LDYNA) THEN
        IF (PREMIE) THEN
          CALL COPISD('CHAMP_GD','V',VITPLU,VITMOI)
          CALL COPISD('CHAMP_GD','V',ACCPLU,ACCMOI)        
        ENDIF
        CALL DINIT (NEQ   ,INSTAM,INSTAP,FONACT,DEFICO,
     &              RESOCO,SDSENS,SDDYNA,VALMOI,POUGD ,
     &              DEPALG)
      ENDIF
C
      CALL JEDEMA()      
C
      END
