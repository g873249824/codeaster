     &                   NNOS  ,PLA   ,LACT  ,NFH   ,DDLS  ,
     &                   DDLM  ,NFISS ,IFISS ,JHEAFA,IFA   ,
     &                   NCOMPH,JFISNO,JAC   ,FFC   ,FFP   ,
     &                   SINGU ,RR    ,VTMP)
C
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER     NDIM,NNO,NNOL
      INTEGER     NFH,DDLS,PLA(27),LACT(8)
      INTEGER     SINGU
      REAL*8      VTMP(400)
      REAL*8      FFP(27),JAC,DEPREF,SIGREF
      REAL*8      RR,FFC(8)

