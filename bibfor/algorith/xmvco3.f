C ROUTINE CONTACT (METHODE XFEM HPP - CALCUL ELEM.)
C
C --- CALCUL DES SECONDS MEMBRES DE COHESION
C
C ----------------------------------------------------------------------
C
C IN  NDIM   : DIMENSION DE L'ESPACE
C IN  NNO    : NOMBRE DE NOEUDS DE L'ELEMENT DE REF PARENT
C IN  SIGMA  : VECTEUR CONTRAINTE EN REPERE LOCAL
C IN  NFH    : NOMBRE DE FONCTIONS HEAVYSIDE
C IN  DDLS   : NOMBRE DE DDL (DEPL+CONTACT) À CHAQUE NOEUD SOMMET
C IN  JAC    : PRODUIT DU JACOBIEN ET DU POIDS
C IN  FFP    : FONCTIONS DE FORME DE L'ELEMENT PARENT
C IN  SINGU  : 1 SI ELEMENT SINGULIER, 0 SINON
C IN  RR     : DISTANCE AU FOND DE FISSURE
C I/O VTMP   : VECTEUR ELEMENTAIRE DE CONTACT/FROTTEMENT
C
C
      INTEGER I,J,K,PLI,NLI,DDLM,IFA,IFH,IFISS
      INTEGER IN,JFISNO,JHEAFA,NCOMPH,NFISS,NNOS
      REAL*8  FFI,COEFI
      LOGICAL LMULTC
C
C ---------------------------------------------------------------------
C
      COEFI = 2
      LMULTC = NFISS.GT.1
       DO 10 I = 1,NNO
          CALL INDENT(I,DDLS,DDLM,NNOS,IN)
          DO 11 IFH = 1,NFH
            IF (LMULTC) THEN
              COEFI = ZI(JHEAFA-1+NCOMPH*(NFISS*(IFISS-1)
     &                  +ZI(JFISNO-1+NFH*(I-1)+IFH)-1)+2*IFA)
     &              - ZI(JHEAFA-1+NCOMPH*(NFISS*(IFISS-1)
     &                  +ZI(JFISNO-1+NFH*(I-1)+IFH)-1)+2*IFA-1)
            ENDIF
            DO 12 J = 1,NDIM
              VTMP(IN+NDIM*IFH+J) = VTMP(IN+NDIM*IFH+J) +
     &        ABS(COEFI*FFP(I)*SIGREF*JAC)
12          CONTINUE
11        CONTINUE
          DO 13 J = 1,SINGU*NDIM
            VTMP(IN+NDIM*(1+NFH)+J) = VTMP(IN+NDIM*(1+NFH)+J) +
     &      ABS(COEFI*FFP(I)*RR*SIGREF*JAC)
13        CONTINUE
10     CONTINUE
C
C SECOND MEMBRE DE L EQUATION D INTERFACE: EXPRESSION DIRECTE
C ATTENTION INVERSION DE CONVENTIONS
C
      DO 20 I = 1,NNOL
         PLI=PLA(I)
         FFI=FFC(I)
         NLI=LACT(I)
         DO 21 K=1,NDIM
C SI LAGRANGE ACTIF ON MET LA FORCE NODALE DE REF
            IF(NLI.NE.0) THEN
             VTMP(PLI-1+K) = VTMP(PLI-1+K)
     &     + ABS(DEPREF*FFI*JAC)
C SINON ON MET SIGREF
            ELSE IF(NLI.EQ.0) THEN
               VTMP(PLI-1+K) = VTMP(PLI-1+K)
     &     + ABS(SIGREF)
            ENDIF
21       CONTINUE
20     CONTINUE
C
       END
