      REAL*8  X, U, V
C
      IF ( WMOY .LE. A ) THEN
         CALL UTDEBM('F','GEVAT2',' WMOY < A ')
         CALL UTIMPR('L','   WMOY = ', 1, WMOY )
         CALL UTIMPR('L','   A    = ', 1, A )
         CALL UTFINM
      ENDIF
C
      V = WMOY - A
      CALL GETRAN ( U )
      X = -LOG(1-U)
      GEVAT2 = A + V*X
C
      END
