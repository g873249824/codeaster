C    DETERMINER LA NUMEROTATION GENERALISEE A PARTIR D'UN MODE_MECA
C    OU D'UN MODE_GENE
C    LA NUMEROTATION SERA PAR DEFAUT PLEINE
C
C NUGENE /I/ : NOM K14 DU NUME_DDL_GENE
C MODMEC /I/ : NOM K8 DU MODE_MECA OU DU MODE_GENE
C-----------------------------------------------------------------------
