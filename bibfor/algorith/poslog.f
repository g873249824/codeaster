      SUBROUTINE POSLOG(RESI,RIGI,TN,TP,FM,LGPG,VIP,NDIM,FP,G,DTDE,SIGM,
     &                  CPLAN,FAMI,MATE,INSTP,ANGMAS,GN,LAMB,LOGL,SIGP,
     &                  DSIDEP,PK2M,PK2,CODRET)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 28/08/2012   AUTEUR SFAYOLLE S.FAYOLLE 
C ======================================================================
