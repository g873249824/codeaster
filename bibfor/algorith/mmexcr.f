C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      LOGICAL       LFROTT,LSSCON,LSSRAC
      INTEGER       POSMAE,IZONE
      CHARACTER*24  DEFICO
      INTEGER       IPC    
      LOGICAL       EXNOEC,EXNOEF,EXNOER
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT)
C
C EXCLUE LES NOEUDS SUIVANT OPTIONS
C      
C ----------------------------------------------------------------------
C
C
C NB: CONFUSION NOEUD/POINT DE CONTACT CAR LES OPTIONS FONCTIONNENT
C QUE POUR INTEGRATION='NOEUD'
C
C IN  LFROTT : FROTTEMENT
C IN  LSSCON : IL Y A DES NOEUDS DANS SANS_GROUP_NO
C IN  LSSRAC : IL Y A DES NOEUDS DANS GROUP_RACC
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT 
C IN  POSMAE : MAILLE ESCLAVE CONTENANT LE POINT DE CONTACT
C IN  IPC    : NUMERO DU NOEUD DE LA MAILLE MAITRE
C OUT EXNOEC : VAUT .TRUE. SI LE NOEUD DOIT ETRE EXCLU COMPLETEMENT
C OUT EXNOEF : VAUT .TRUE. SI LE NOEUD DOIT ETRE EXCLU DE 
C              LA SURFACE DE CONTACT SUIVANT UNE DIRECTION (FROTTEMENT)
C OUT EXNOER : VAUT .TRUE. SI LE NOEUD DOIT ETRE EXCLU DE 
C              LA SURFACE DE CONTACT A CAUSE D'UN RACCORD_LINE_QUAD
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      POSNOE,NUMNOE
      CHARACTER*24 NOMACO,CONTNO,PNOMA
      INTEGER      JNOMA,JNOCO,JPONO
      INTEGER      SUPPOK
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ() 
C
C --- ACCES OBJETS
C 
      NOMACO = DEFICO(1:16)//'.NOMACO'
      CONTNO = DEFICO(1:16)//'.NOEUCO'
      PNOMA  = DEFICO(1:16)//'.PNOMACO'       
C           
      CALL JEVEUO(NOMACO,'L',JNOMA)
      CALL JEVEUO(CONTNO,'L',JNOCO)     
      CALL JEVEUO(PNOMA ,'L',JPONO) 
C
C --- INITIALISATIONS
C
      EXNOEC = .FALSE.
      EXNOER = .FALSE.
      EXNOEF = .FALSE.     
C
C --- ON TESTE SI NOEUD EST DANS SANS_GROUP_NO
C
      IF (LSSCON) THEN
        CALL MMNOEX(DEFICO,IZONE ,POSMAE,IPC   ,EXNOEC)
      ENDIF
C
C --- ON TESTE SI NOEUD EST DANS GROUP_RACC
C      
      IF (LSSRAC) THEN      
        CALL MMRACC(DEFICO,IZONE ,POSMAE,IPC   ,EXNOER)
      ENDIF  
C                 
C --- ON TESTE SI NOEUD EST DANS SANS_GROUP_NO_FR
C 
      IF (LFROTT) THEN 
        POSNOE = ZI(JNOMA+ZI(JPONO+POSMAE-1)+IPC-1)
        NUMNOE = ZI(JNOCO+POSNOE-1)
        CALL CFMMEX(DEFICO,'FROT',IZONE ,NUMNOE,SUPPOK)  
        IF (SUPPOK.EQ.1) THEN
          EXNOEF = .TRUE.
        ENDIF
      ENDIF
C
      CALL JEDEMA()      
C
      END
