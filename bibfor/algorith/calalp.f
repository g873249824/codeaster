      IMPLICIT NONE


      INCLUDE 'jeveux.h'
      CHARACTER*16 COMPOR(4)

C......................................................................
C     BUT:  CALCUL DE L'INVERSE DE KAPPA
C IN  KPG    : NUMERO DU POINT DE GAUSS
C IN  IMATE  : NUMERO DU MATERIAU
C IN  COMPOR : NOM DU COMPORTEMENT
C OUT ALPHA  : INVERSE DE KAPPA
C......................................................................

      INTEGER IMATE,KPG,ICODRE(2),ITEMPS,IRET
      REAL*8  E,NU,VALRES(2)
      REAL*8  ALPHA,VALPAR
      CHARACTER*8 NOMRES(2),NOMPAR

C INITIALISATION
      NOMPAR = 'INST'

      IF (.NOT.( COMPOR(1)(1:4) .EQ. 'ELAS'.OR.
     &     COMPOR(1)(1:9) .EQ. 'VMIS_ISOT' )) THEN
            CALL U2MESK('F','ALGORITH4_50',1,COMPOR(1))
      ENDIF

C - RECUPERATION DE L INSTANT
      CALL TECACH('NNN','PTEMPSR','L',1,ITEMPS,IRET)
      IF (ITEMPS.NE.0) THEN
        VALPAR = ZR(ITEMPS)
      ELSE
        VALPAR = 0.D0
      ENDIF

C - RECUPERATION DE E ET NU DANS LE FICHIER PYTHON
      NOMRES(1)='E'
      NOMRES(2)='NU'
      CALL RCVALB('RIGI',KPG,1,'+',IMATE,' ','ELAS',1,NOMPAR,VALPAR,2,
     &            NOMRES,VALRES,ICODRE,1)
      E  = VALRES(1)
      NU = VALRES(2)
      ALPHA=(3.D0*(1.D0-2.D0*NU))/E

      END
