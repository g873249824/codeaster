C
      IMPLICIT NONE
      LOGICAL       LSSFRO
      CHARACTER*8   NOMA,ALIAS
      INTEGER       NDIMG
      INTEGER       POSMAM,POSMAE,IZONE,NUMMAM
      REAL*8        KSI1,KSI2   
      CHARACTER*24  DEFICO,RESOCO,NEWGEO
      REAL*8        TAU1M(3),TAU2M(3)
      REAL*8        TAU1(3),TAU2(3)
      INTEGER       IPC
      INTEGER       TYCO
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT)
C
C MOD. LES VECTEURS TANGENTS LOCAUX SUIVANT OPTIONS
C      
C ----------------------------------------------------------------------
C
C
C IN  LFROTT : IL Y A DES NOEUDS DANS SANS_GROUP_NO_FR
C IN  NOMA   : NOM DU MAILLAGE
C IN  METHOD : TYPE DE METHODE DE CONTACT (CONTINUE OU DISCRET)
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT 
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  NEWGEO : COORDONNEES REACTUALISEES DES NOEUDS DU MAILLAGE
C IN  NDIMG  : DIMENSION DE L'ESPACE
C IN  IZONE  : ZONE DE CONTACT ACTIVE
C IN  POSMAM : MAILLE MAITRE QUI RECOIT LA PROJECTION
C IN  NUMMAM : NUMERO ABSOLU MAILLE MAITRE QUI RECOIT LA PROJECTION
C IN  POSMAE : MAILLE ESCLAVE CONTENANT LE POINT DE CONTACT
C IN  TYCO   : TYPE D'INTEGRATION
C IN  IPC    : NUMERO DU POINT DE CONTACT DANS LA MAILLE ESCLAVE
C               (DEPEND SCHEMA INTEGRATION)
C IN  KSI1   : PREMIERE COORDONNEE PARAMETRIQUE PT CONTACT PROJETE 
C              SUR MAILLE MAITRE
C IN  KSI2   : SECONDE COORDONNEE PARAMETRIQUE PT CONTACT PROJETE 
C              SUR MAILLE MAITRE
C IN  TAU1M  : PREMIERE TANGENTE SUR LA MAILLE MAITRE AU POINT ESCLAVE
C              PROJETE
C IN  TAU2M  : SECONDE TANGENTE SUR LA MAILLE MAITRE AU POINT ESCLAVE
C              PROJETE
C OUT TAU1   : PREMIERE TANGENTE LOCALE AU POINT ESCLAVE PROJETE
C OUT TAU2   : SECONDE TANGENTE LOCALE AU POINT ESCLAVE PROJETE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*32 JEXNUM
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      NUMENM,INO
      REAL*8       COORMA(27)
      CHARACTER*24 NOMACO,TGELNO
      INTEGER      JNOMA,JTGELN
      INTEGER      POSNOE,NBNOE,JDECNO 
      CHARACTER*4  TYPENM,TYPMAI
      CHARACTER*8  NOMMAE
      INTEGER      NUMMAE,NNO,NDIM
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ() 
C
C --- INITIALISATIONS
C         
      IF (NUMMAM.LE.0) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
      NUMENM = NUMMAM  
      TYPENM = 'MAIL'    
C      
C --- RECUPERATION DE QUELQUES DONNEES      
C
      NOMACO = DEFICO(1:16)//'.NOMACO' 
      TGELNO = RESOCO(1:14)//'.TGELNO' 
      CALL JEVEUO(NOMACO,'L',JNOMA)      
C
C --- NOEUD ESCLAVE SI INTEGRATION AUX NOEUDS
C
      IF (TYCO.EQ.1) THEN
C       GLUTE POUR QUAD8 POUR LEQUEL NBNOE=8<NBPC=9
        IF (ALIAS.EQ.'QU8') THEN
          POSNOE = -1
        ELSE
          CALL CFNBEN(DEFICO,POSMAE,'MAIL',NBNOE ,JDECNO)
          IF (IPC.LE.NBNOE) THEN
            POSNOE = ZI(JNOMA+JDECNO+IPC-1)
          ELSE
            CALL ASSERT(.FALSE.)
          ENDIF
        ENDIF
      ELSE
        POSNOE = 0  
      ENDIF      
C
C --- CHOIX DE LA NORMALE
C
      CALL CFTANR(NOMA  ,NDIMG ,DEFICO,RESOCO,IZONE ,
     &            POSNOE,TYPENM,POSMAM,NUMENM,KSI1  ,
     &            KSI2  ,TAU1M ,TAU2M ,TAU1  ,TAU2  )              
C                 
C --- TANGENTE AVEC SANS_GROUP_NO_FR -> TANGENTES FIXES
C 
      IF (LSSFRO) THEN 
        CALL JEVEUO(JEXNUM(TGELNO,POSMAE),'E',JTGELN)
        CALL CFCARM(NOMA  ,DEFICO,NEWGEO,POSMAE,TYPMAI,
     &              NUMMAE,ALIAS ,NOMMAE,NDIM  ,NNO   ,
     &              COORMA)
        IF (TYPMAI.NE.'MAIT') THEN
          CALL ASSERT(.FALSE.)
        ENDIF  
        CALL MMPGNO(ALIAS ,TYCO  ,COORMA,NNO   ,NDIM  ,
     &              IPC   ,INO   )
        TAU1(1) = ZR(JTGELN+6*(INO-1)+1 -1)
        TAU1(2) = ZR(JTGELN+6*(INO-1)+2 -1)
        TAU1(3) = ZR(JTGELN+6*(INO-1)+3 -1)
        TAU2(1) = ZR(JTGELN+6*(INO-1)+4 -1)
        TAU2(2) = ZR(JTGELN+6*(INO-1)+5 -1)
        TAU2(3) = ZR(JTGELN+6*(INO-1)+6 -1)
      ENDIF
C
      CALL JEDEMA() 
C
      END
