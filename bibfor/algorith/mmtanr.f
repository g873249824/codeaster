C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C TOLE CRP_21
C
      IMPLICIT NONE
      LOGICAL       LSSFRO
      CHARACTER*8   NOMA,ALIASE
      INTEGER       IPTM,IZONE
      INTEGER       NDIMG
      INTEGER       POSNOE,POSMAM,POSMAE,NUMMAM
      REAL*8        KSI1,KSI2   
      CHARACTER*24  DEFICO,RESOCO
      CHARACTER*19  NEWGEO
      REAL*8        TAU1M(3),TAU2M(3)
      REAL*8        TAU1(3),TAU2(3)
      REAL*8        COORPC(3)
      INTEGER       TYPINT
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT)
C
C MOD. LES VECTEURS TANGENTS LOCAUX SUIVANT OPTIONS
C      
C ----------------------------------------------------------------------
C
C
C IN  LSSFRO : IL Y A DES NOEUDS DANS SANS_GROUP_NO_FR
C IN  NOMA   : NOM DU MAILLAGE
C IN  ALIASE : NOM D'ALIAS DE L'ELEMENT ESCLAVE
C IN  COORPC : COORDONNEES DU POINT DE CONTACT
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT 
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  NEWGEO : COORDONNEES REACTUALISEES DES NOEUDS DU MAILLAGE
C IN  NDIMG  : DIMENSION DE L'ESPACE
C IN  IZONE  : ZONE DE CONTACT ACTIVE
C IN  POSMAM : POSITION DE LA MAILLE MAITRE DANS LES SD CONTACT
C IN  NUMMAM : NUMERO ABSOLU MAILLE MAITRE QUI RECOIT LA PROJECTION
C IN  POSMAE : POSITION DE LA MAILLE ESCLAVE DANS LES SD CONTACT
C IN  POSNOE : POSITION DU PT INTEG DANS LES SD CONTACT SI INTEG.NOEUDS
C IN  TYPINT : TYPE D'INTEGRATION
C IN  IPTM   : NUMERO DU POINT D'INTEGRATION DANS LA MAILLE
C IN  KSI1   : PREMIERE COORDONNEE PARAMETRIQUE PT CONTACT PROJETE 
C              SUR MAILLE MAITRE
C IN  KSI2   : SECONDE COORDONNEE PARAMETRIQUE PT CONTACT PROJETE 
C              SUR MAILLE MAITRE
C IN  TAU1M  : PREMIERE TANGENTE SUR LA MAILLE MAITRE AU POINT ESCLAVE
C              PROJETE
C IN  TAU2M  : SECONDE TANGENTE SUR LA MAILLE MAITRE AU POINT ESCLAVE
C              PROJETE
C OUT TAU1   : PREMIERE TANGENTE LOCALE AU POINT ESCLAVE PROJETE
C OUT TAU2   : SECONDE TANGENTE LOCALE AU POINT ESCLAVE PROJETE

C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*32 JEXNUM
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      INO
      REAL*8       COORMA(27)
      CHARACTER*24 TGELNO
      INTEGER      JTGELN
      CHARACTER*4  TYPMAI
      CHARACTER*8  NOMMAE
      INTEGER      NUMMAE,NNOMAE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ() 
C
C --- INITIALISATIONS
C         
      IF (NUMMAM.LE.0) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- CHOIX DE LA NORMALE
C
      CALL CFTANR(NOMA  ,NDIMG ,DEFICO,RESOCO,IZONE ,
     &            POSNOE,'MAIL',POSMAM,NUMMAM,KSI1  ,
     &            KSI2  ,TAU1M ,TAU2M ,TAU1  ,TAU2  )              
C                 
C --- TANGENTE AVEC SANS_GROUP_NO_FR -> TANGENTES FIXES
C 
      IF (LSSFRO) THEN 
        TGELNO = RESOCO(1:14)//'.TGELNO' 
        CALL JEVEUO(JEXNUM(TGELNO,POSMAE),'E',JTGELN)
        CALL CFCARM(NOMA  ,DEFICO,NEWGEO,POSMAE,TYPMAI,
     &              NUMMAE,ALIASE,NOMMAE,NDIMG ,NNOMAE,
     &              COORMA)
        IF (TYPMAI.NE.'MAIT') THEN
          CALL ASSERT(.FALSE.)
        ENDIF  
        CALL MMPGNO(TYPINT,COORMA,COORPC,NNOMAE,NDIMG ,
     &              IPTM  ,INO   )
        TAU1(1) = ZR(JTGELN+6*(INO-1)+1 -1)
        TAU1(2) = ZR(JTGELN+6*(INO-1)+2 -1)
        TAU1(3) = ZR(JTGELN+6*(INO-1)+3 -1)
        TAU2(1) = ZR(JTGELN+6*(INO-1)+4 -1)
        TAU2(2) = ZR(JTGELN+6*(INO-1)+5 -1)
        TAU2(3) = ZR(JTGELN+6*(INO-1)+6 -1)
      ENDIF
C
      CALL JEDEMA() 
C
      END
