      SUBROUTINE MMTANR(NOMA  ,NDIMG ,DEFICO,RESOCO,NEWGEO,
     &                  IZONE ,LSSFRO,TYPINT,POSMAE,POSNOE,
     &                  ALIASE,IPTM  ,KSI1  ,KSI2  ,COORPC,
     &                  POSMAM,NUMMAM,TAU1M ,TAU2M ,TAU1  ,
     &                  TAU2  )
C     
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 14/09/2010   AUTEUR ABBAS M.ABBAS 
C ======================================================================
