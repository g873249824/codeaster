      IMPLICIT NONE
      CHARACTER*19   LISCHA, PHASME
      REAL*8         INSTAN
C ----------------------------------------------------------------------
C     EXTRACTION DU CHAMP DE CHANGEMENTS DE PHASES METALLURGIQUES
C
C IN        LISCHA  : SD L_CHARGES
C IN        INSTAN  : INSTANT DE L'EXTRACTION
C IN/JXOUT  PHASME  : CHAM_ELEM DE CHANGEMENTS DE PHASES METALLURGIQUES
C ----------------------------------------------------------------------

C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER            NCHAR,IRET,NUMCHA,JCHAR,JINF, JTEMP, NBCHAM
      CHARACTER*8        TEMPE,K8BID
      CHARACTER*16       TYSD
      CHARACTER*24       CHARGE, INFCHA, NOM24, LIGRMO



      CALL JEMARQ()
      CALL DETRSD('CHAMP_GD',PHASME)

      CHARGE =  LISCHA // '.LCHA'
      INFCHA =  LISCHA // '.INFC'


C    EXTRACTION DU CONCEPT EVOL_NOLI

      NUMCHA =  0
      CALL JEEXIN(CHARGE,IRET)
      IF ( IRET .NE. 0 ) THEN
        CALL JELIRA(CHARGE,'LONMAX',NCHAR,K8BID)
        CALL JEVEUO(CHARGE,'L',JCHAR)
        CALL JEVEUO(INFCHA,'L',JINF)
        NUMCHA = ZI(JINF+2*NCHAR+1)
      ENDIF
      IF ( NUMCHA .LE. 0 ) GOTO 9999

      NOM24 = ZK24(JCHAR+NUMCHA-1)(1:8)//'.CHME.TEMPE.TEMP'
      CALL JEVEUO(NOM24,'L',JTEMP)
      TEMPE = ZK8(JTEMP)
      CALL GETTCO(TEMPE,TYSD)
      IF (TYSD(1:9).NE.'EVOL_THER') GOTO 9999

      CALL DISMOI('F','NB_CHAMP_UTI',TEMPE,'RESULTAT',NBCHAM,
     &            K8BID,IRET)
      IF (NBCHAM.EQ.0) GOTO 9999


C    RECUPERATION DU CHAMP METALLURGIQUE DANS  EVOL_THER
      CALL RSINCH(TEMPE,'META_ELGA_TEMP','INST',INSTAN,PHASME,
     &            'CONSTANT','CONSTANT',0,'V',IRET)


 9999 CONTINUE
      CALL JEDEMA()
      END
