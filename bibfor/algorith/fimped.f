      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*8 LPAIN(5),LPAOUT(1),K8BID
      CHARACTER*24 MODELE,MATE,NUMEDD,VITINI,VECCOR
      CHARACTER*24 VITENT,CHINST
      CHARACTER*24 VEANEC,VAANEC,LCHIN(5),LCHOUT(1)
      CHARACTER*24 CHGEOM,LIGREL
      REAL*8 FOIMPE(NEQ),TEMPS
      COMPLEX*16 CBID
C    ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C     ----- FIN COMMUNS NORMALISES  JEVEUX  ----------------------------

      CALL JEMARQ()

      CHINST = '&&CHINST'
      CALL MECACT('V',CHINST,'MODELE',MODELE(1:8)//'.MODELE','INST_R',1,
     &            'INST',IBID,TEMPS,CBID,K8BID)
      CALL JEDETR(VEANEC(1:19)//'.RELR')

      LIGREL = MODELE(1:8)//'.MODELE'
      CALL JEVEUO(LIGREL(1:19)//'.LGRF','L',JNOMA)
      CHGEOM = ZK8(JNOMA)//'.COORDO'
      LPAIN(1) = 'PGEOMER'
      LCHIN(1) = CHGEOM
      LPAIN(2) = 'PMATERC'
      LCHIN(2) = MATE

      LPAIN(3) = 'PVITPLU'
      LCHIN(3) = VITINI
      LPAIN(4) = 'PVITENT'
      LCHIN(4) = VITENT

      LPAIN(5) = 'PTEMPSR'
      LCHIN(5) = CHINST

      NPAIN = 5
      LPAOUT(1) = 'PVECTUR'
      LCHOUT(1) = VECCOR

      CALL CALCUL('S','IMPE_ABSO',LIGREL,NPAIN,LCHIN,LPAIN,1,LCHOUT,
     &            LPAOUT,'V','OUI')

      CALL CORICH('E',LCHOUT(1),-1,IBID)

      CALL REAJRE(VEANEC,LCHOUT(1),'V')
      CALL ASASVE(VEANEC,NUMEDD,'R',VAANEC)

      CALL JEVEUO(VAANEC,'L',JVAANC)
      CALL JEVEUO(ZK24(JVAANC) (1:19)//'.VALE','L',JREANE)

      DO 10 I = 1,NEQ
        FOIMPE(I) = ZR(JREANE+I-1)
   10 CONTINUE
      CALL DETRSD('CHAMP_GD',ZK24(JVAANC) (1:19))


      CALL JEDEMA()
      END
