C
C ROUTINE UTILITAIRE (LISTE_CHARGES)
C
C RETOURNE LE NOMBRE DE CHARGES DE TYPE GENCHA
C
C ----------------------------------------------------------------------
C
C
C IN  LISCHA : SD LISTE DES CHARGES
C IN  GENCHA : GENRE DE LA CHARGE (VOIR LISDEF)
C
C
C
C
      INTEGER      ICHAR,NBCHAR
      INTEGER      CODCHA
      LOGICAL      LOK,LISICO
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      LISNBG = 0
C
C --- NOMBRE DE CHARGES
C
      CALL LISNNB(LISCHA,NBCHAR)
      IF (NBCHAR.EQ.0) GOTO 999
C
C --- BOUCLE SUR LES CHARGES
C
      DO 10 ICHAR = 1,NBCHAR
        CALL LISLCO(LISCHA,ICHAR ,CODCHA)
        LOK = LISICO(GENCHZ,CODCHA)
        IF (LOK) LISNBG = LISNBG + 1
  10  CONTINUE
C
  999 CONTINUE
C
      CALL JEDEMA()
      END
