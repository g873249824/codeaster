C
C VALEUR MAXI SUR LA DIAGONALE DE LA MATR_ASSE DU SYSTEME MECANIQUE
C UTILISE POUR LA DETECTION DE PIVOT NUL
C
C IN  LMAT   : DESCRIPTEUR DE LA MATR_ASSE DU SYSTEME MECANIQUE
C OUT XMAX   : VALEUR DU PIVOT MAX
C
C
