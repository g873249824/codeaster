      IMPLICIT NONE
      LOGICAL       FONACT(*)
      CHARACTER*19  SDDYNA
      CHARACTER*19  VEASSE(*)
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (DYNAMIQUE - ALGORITHME)
C
C SAUVEGARDE DES VECTEURS SECONDS MEMBRES ET EFFORTS INTERIEURS
C      
C ----------------------------------------------------------------------
C
C
C IN  VEASSE : VARIABLE CHAPEAU POUR NOM DES VECT_ASSE
C IN  SDDYNA : SD DYNAMIQUE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24  NDYNKK
      CHARACTER*19  OLFEDO,OLFSDO,OLDIDO,OLDIDI,OLFINT
      CHARACTER*19  OLONDP,OLLAPL,OLCINE
      CHARACTER*19  NMCHEX
      CHARACTER*19  CNFEDO,CNFSDO,CNDIDO,CNDIDI,CNFINT
      CHARACTER*19  CNONDP,CNLAPL,CNCINE
      LOGICAL       NDYNLO,ISFONC
      LOGICAL       LONDE,LLAPL,LDIDI
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- FONCTIONNALITES ACTIVEES
C     
      LONDE  = NDYNLO(SDDYNA,'ONDE_PLANE')  
      LLAPL  = ISFONC(FONACT,'LAPLACE')
      LDIDI  = ISFONC(FONACT,'DIDI') 
C
C --- NOM DES CHAMPS PAS PRECEDENT
C   
      OLFEDO = NDYNKK(SDDYNA,'CNFEDO')
      OLFSDO = NDYNKK(SDDYNA,'CNFSDO') 
      OLDIDO = NDYNKK(SDDYNA,'CNDIDO')
      OLDIDI = NDYNKK(SDDYNA,'CNDIDI') 
      OLFINT = NDYNKK(SDDYNA,'CNFINT')
      OLONDP = NDYNKK(SDDYNA,'CNONDP') 
      OLLAPL = NDYNKK(SDDYNA,'CNLAPL')  
      OLCINE = NDYNKK(SDDYNA,'CNCINE') 
C
C --- NOM DES CHAMPS PAS COURANT
C           
      CNFEDO = NMCHEX(VEASSE,'VEASSE','CNFEDO')
      CNFSDO = NMCHEX(VEASSE,'VEASSE','CNFSDO') 
      CNDIDO = NMCHEX(VEASSE,'VEASSE','CNDIDO')
      CNDIDI = NMCHEX(VEASSE,'VEASSE','CNDIDI') 
      CNFINT = NMCHEX(VEASSE,'VEASSE','CNFINT')
      CNONDP = NMCHEX(VEASSE,'VEASSE','CNONDP') 
      CNLAPL = NMCHEX(VEASSE,'VEASSE','CNLAPL')  
      CNCINE = NMCHEX(VEASSE,'VEASSE','CNCINE') 
C
C --- RECOPIE DES CHAMPS
C
      CALL COPISD('CHAMP_GD','V',CNFINT,OLFINT)
      CALL COPISD('CHAMP_GD','V',CNFEDO,OLFEDO)
      CALL COPISD('CHAMP_GD','V',CNFSDO,OLFSDO)
      CALL COPISD('CHAMP_GD','V',CNDIDO,OLDIDO)
      CALL COPISD('CHAMP_GD','V',CNCINE,OLCINE)
      IF (LONDE) THEN
        CALL COPISD('CHAMP_GD','V',CNONDP,OLONDP)
      ENDIF
      
      IF (LDIDI) THEN
        CALL COPISD('CHAMP_GD','V',CNDIDI,OLDIDI)      
      ENDIF
      
      IF (LLAPL) THEN
        CALL COPISD('CHAMP_GD','V',CNLAPL,OLLAPL)      
      ENDIF
      
C
      CALL JEDEMA()
      END
