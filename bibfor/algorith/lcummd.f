      REAL*8 VARI(NVARI),CMAT(NMAT),SIGI(6)
C MODIFI DU 6 JANVIER 2003 - YLP SUPPRESSION DES DECLARATIONS
C IMPLICITES DES TABLEAUX
C      REAL*8 AN(NSTRS),BN(NSTRS,NSTRS),CN(NSTRS,NSTRS)
C      REAL*8 AFD(NSTRS),BFD(NSTRS,NSTRS),CFD(NSTRS,NSTRS)
C      REAL*8 AFP(NSTRS),BFP(NSTRS,NSTRS),CFP(NSTRS,NSTRS)
      REAL*8 AN(6),BN(6,6),CN(6,6)
      REAL*8 AFD(6),BFD(6,6),CFD(6,6)
      REAL*8 AFP(6),BFP(6,6),CFP(6,6)
      REAL*8 CFPD,CFPS,HFIN,HINI,TDT
C
C RECUPERATION DES VALEURS DES PARAMETRES MATERIAU
C
      IFPO = CMAT(13)
C
C INITIALISATION DES VARIABLES
C
C MODIFI DU 6 JANVIER 2003 6 YLP NSTRS --> 6
C      DO 11 I=1,NSTRS
      DO 11 I=1,6
        AFD(I) = 0.D0
        AFP(I) = 0.D0
C        DO 12 J=1,NSTRS
        DO 12 J=1,6
          BFP(I,J) = 0D0
          BFD(I,J)  = 0D0
          CFP(I,J)  = 0D0
          CFD(I,J)  = 0D0
12      CONTINUE
11    CONTINUE
C
C_______________________________________________________________________
C
C  FLUAGE DE DESSICCATION
C
C --> DEBRANCHE le 11 octobre 2002 - ylp.
C
C      IF ((IDES.EQ.1).OR.(IDES.EQ.2)) THEN
C        CALL FLUDES(SIGI,NSTRS,VARI,NVARI,CMAT,NMAT,TDT,HINI,HFIN,
C     $              AFD,BFD,CFD)
C      ENDIF
C
C  FLUAGE PROPRE
C
      IF (IFPO.NE.0) THEN
        CALL LCUMSD(VARI,NVARI,CMAT,NMAT,NSTRS,ISPH,TDT,HINI,HFIN,
     $              AFP,BFP,CFP,CFPS,CFPD)
      ENDIF
C
C  CONSTRUCTION DE LA MATRICE DE FLUAGE TOTAL : EQUATION (3.9-2)
C
      DO 10 I=1,NSTRS
        AN(I) = AFD(I) + AFP(I)
        DO 10 J=1,NSTRS
          BN(I,J) = BFD(I,J) + BFP(I,J)
          CN(I,J) = CFD(I,J) + CFP(I,J)
  10  CONTINUE
C
      END
