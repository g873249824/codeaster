      REAL*8      VITG(*), VITG0(*), DEPG(*), DEPG0(*)
      COMPLEX*16  ZIN(*)
      REAL*8      TRANS(2,2,*), OMEGAD(*)
      COMPLEX*16  S0(*)
C
C VARIABLES LOCALES
C -----------------
      INTEGER     I
C
C FONCTIONS INTRINSEQUES
C ----------------------
C     INTRINSIC   DIMAG
C
C-------------------   DEBUT DU CODE EXECUTABLE    ---------------------
C
      DO 10 I = 1, NBM
         VITG(I) = TRANS(1,1,I)*VITG0(I) + TRANS(1,2,I)*DEPG0(I)
     &             + DIMAG((S0(I)/OMEGAD(I))*ZIN(I))
         DEPG(I) = TRANS(2,1,I)*VITG0(I) + TRANS(2,2,I)*DEPG0(I)
     &             + DIMAG(ZIN(I)/OMEGAD(I))
  10  CONTINUE
C
C --- FIN DE ALGINT.
      END
