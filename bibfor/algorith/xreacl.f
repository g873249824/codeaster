C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================

      IMPLICIT NONE
      CHARACTER*8   MA,MODELE
      CHARACTER*24  DEPPLU,DEFICO
C ----------------------------------------------------------------------
C                    MISE À JOUR DU SEUIL DE FROTTEMENT
C
C  IN
C      MODELE  : OBJET MODELE
C      DEPPLU  : DÉPLACMENTS ACTUELS
C      DEFICO  : DONNÉES DU CONTACT
C
C  OUT
C      DEFICO  : DONNÉES DU CONTACT AVEC SEUILS MODIFIÉS

C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      CHARACTER*32       JEXNUM , JEXNOM , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------

      INTEGER       JFISS,NBMA,IBID,JCESD,JCESL,JCESV,IMA,IAD
      CHARACTER*8   LPAIN(4),LPAOUT,LICMP(2),KBID
      CHARACTER*19  LIGREL,AINTER,CFACE,LONCHA,LCHIN(4),LCHOUT,CSEUIL
C ----------------------------------------------------------------------


      CALL JEMARQ()

      LIGREL = MODELE//'.MODELE'

C     CRÉATION DU CHAMP ELEM SIMPLE DE SORTIE À 12*5 SS PG 
      CSEUIL = '&&XREACL.SEUIL'
      LICMP(1) = 'NPG_DYN'
      LICMP(2) = 'NCMP_DYN'
      CALL DISMOI('F','NB_MA_MAILLA',MA,'MAILLAGE',NBMA,KBID,IBID)
      CALL CESCRE('V',CSEUIL,'ELEM',MA,'DCEL_I',2,LICMP,-1,-1,-2)
      CALL JEVEUO(CSEUIL//'.CESD','L',JCESD)
      CALL JEVEUO(CSEUIL//'.CESL','E',JCESL)
      CALL JEVEUO(CSEUIL//'.CESV','E',JCESV)
      DO 10,IMA = 1,NBMA
        CALL CESEXI('C',JCESD,JCESL,IMA,1,1,1,IAD)
        IF (IAD.GE.0) CALL UTMESS('F','XREACL','STOP1')
        ZL(JCESL-1-IAD) = .TRUE.
        ZI(JCESV-1-IAD) = 60

        CALL CESEXI('C',JCESD,JCESL,IMA,1,1,2,IAD)
        IF (IAD.GE.0) CALL UTMESS('F','XREACL','STOP2')
        ZL(JCESL-1-IAD) = .TRUE.
        ZI(JCESV-1-IAD) = 1
 10   CONTINUE

C     RECUPERATION DE LA LEVEL SET NORMALE
C     ET DES DONNÉES TOPOLOGIQUES SUR LES FACETTES
      CALL JEVEUO(MODELE//'.FISS','L',JFISS)
      AINTER=ZK8(JFISS)//'.TOPOFAC.AINTER'
      CFACE =ZK8(JFISS)//'.TOPOFAC.CFACE'
      LONCHA=ZK8(JFISS)//'.TOPOFAC.LONCHAM'

C     CREATION DES LISTES DES CHAMPS IN ET OUT
      LPAIN(1) = 'PDEPL_P'
      LCHIN(1) = DEPPLU
      LPAIN(2) = 'PAINTER'
      LCHIN(2) = AINTER
      LPAIN(3) = 'PCFACE'
      LCHIN(3) = CFACE
      LPAIN(4) = 'PLONCHA'
      LCHIN(4) = LONCHA

      LPAOUT = 'PSEUIL'
      LCHOUT = CSEUIL 
      
      CALL CALCUL('S','XREACL',LIGREL,4,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'V')

C     ON COPIE CSEUIL DANS DEFICO.SE  
      CALL COPISD('CHAMP_GD','V',LCHOUT,DEFICO(1:16)//'.SE')
      
      CALL JEDEMA()
      END
