      IMPLICIT     NONE
      INCLUDE 'jeveux.h'
      CHARACTER*24 SDIETO
      INTEGER      ZIOCH,ICHAM
      CHARACTER*24 NOMCHS,NOMGD
      CHARACTER*24 MOTCEI,LOCCHA,MOTCOB
      LOGICAL      LETIN,LARCH
C
C ----------------------------------------------------------------------
C
C ROUTINE GESTION IN ET OUT
C
C ADDITION D'UN CHAMP DANS LA SDIETO
C
C ----------------------------------------------------------------------
C
C
C IN  SDIETO : SD GESTION IN ET OUT
C IN  ZIOCH  : NOMBRE DE PARAMETRES PAR CHAMP
C IN  ICHAM  : INDEX DU CHAMP DANS SDIETO
C IN  NOMCHS : NOM DU CHAMP DANS SD RESULTAT
C IN  NOMGD  : NOM DE LA GRANDEUR
C IN  MOTCEI : MOT-CLEF DANS ETAT_INIT, ' ' SI PAS DE MOT-CLEF
C IN  MOTCOB : MOT-CLEF DANS OBSERVATION, ' ' SI PAS DE MOT-CLEF
C IN  LOCCHA : LOCALISATION DU CHAMP
C IN  LETIN  : .TRUE. SI CHAMP LU DANS ETAT_INIT
C IN  LARCH  : .TRUE. SI CHAMP ECRIT DANS ARCHIVAGE
C
C
C
C
      CHARACTER*24 IOLCHA
      INTEGER      JIOLCH
      CHARACTER*24 CHETIN,CHARCH
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES SD
C
      IOLCHA = SDIETO(1:19)//'.LCHA'
      CALL JEVEUO(IOLCHA,'E',JIOLCH)
C
C --- INITIALISATIONS
C
      IF (LETIN) THEN
        CHETIN = 'OUI'
      ELSE
        CHETIN = 'NON'
      ENDIF
      IF (LARCH) THEN
        CHARCH = 'OUI'
      ELSE
        CHARCH = 'NON'
      ENDIF
C
C --- ECRITURE
C
      ZK24(JIOLCH+ZIOCH*(ICHAM-1)+1 -1) = NOMCHS
      ZK24(JIOLCH+ZIOCH*(ICHAM-1)+3 -1) = MOTCEI
      ZK24(JIOLCH+ZIOCH*(ICHAM-1)+5 -1) = LOCCHA
      ZK24(JIOLCH+ZIOCH*(ICHAM-1)+7 -1) = NOMGD
      ZK24(JIOLCH+ZIOCH*(ICHAM-1)+8 -1) = CHETIN
      ZK24(JIOLCH+ZIOCH*(ICHAM-1)+9 -1) = CHARCH
      ZK24(JIOLCH+ZIOCH*(ICHAM-1)+10-1) = MOTCOB
C
      CALL JEDEMA()
      END
