      INTEGER NBVAL, ICODMA, ICAS, NBCLEM
      PARAMETER (NBVAL=12, NBCLEM=7)
      REAL*8  COELMA(NBVAL),COELEM(NBCLEM)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 05/12/2001   AUTEUR F6BHHBO P.DEBONNIERES 
C ======================================================================
C COPYRIGHT (C) 1991 - 2001  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR   
C (AT YOUR OPTION) ANY LATER VERSION.                                 
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT 
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF          
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU    
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                            
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C
C     RECUPERATION DES CARACTERISTIQUES DU MATERIAU POUR LES ELEMENTS
C     DE POUTRE DANS LE CAS DES COMPORTEMENTS D'ASSEMBLAGE DE
C     COMBUSTIBLE.
C ----------------------------------------------------------------------
C
C IN :
C     ICODMA : NUMERO DU MATERIAU CODE
C
C OUT :
C     COELMA : COEFFICIENTS CONSTANTS POUR LE LMAB
C     COELEM : COEFFICIENTS CONSTANTS POUR LA LOI DE FLUAGE DE LEMAITRE
C     ICAS   : FLAG MATERIAU
C
C *************** DECLARATION DES VARIABLES LOCALES ********************
C
      INTEGER      NBPAR, NBRES
      REAL*8       VALPAR,VALRES(NBVAL),VALRET(NBVAL)
      CHARACTER*2  BL2, CODRES(NBVAL), CODRET(NBVAL)
      CHARACTER*8  NOMPAR,NOMLMA(NBVAL),NOMLEM(NBCLEM)
C
C *********** FIN DES DECLARATIONS DES VARIABLES LOCALES ***************
C
C ****************************** DATA  *********************************
C
C      DATA NOMLMA / 'K','N','DE_0','P','P1','P2','M','RM',
C     &              'M1','RM1','A_0','Y_0','Y_I','B'/
      DATA NOMLMA / 'K','N','DE_0','P','P1','P2','M','RM',
     &              'A_0','Y_0','Y_I','B'/
      DATA NOMLEM / 'N', 'UN_SUR_K', 'UN_SUR_M', 'QSR_K',
     &              'BETA','PHI_ZERO','L'/
C
C ********************* DEBUT DE LA SUBROUTINE *************************
C
C --- INITIALISATIONS
C
      CALL R8INIR (NBVAL,0.D0,COELMA,1)
      CALL R8INIR (NBCLEM,0.D0,COELEM,1)
      ICAS = 0
C
      BL2 = '  '
C
      CALL R8INIR (NBVAL,0.D0,VALRES,1)
      CALL R8INIR (NBCLEM,0.D0,VALRET,1)
C
C--  FLUAGE DES CRAYONS ZIRCALOY AVEC LOI DU LMAB
C    ET FLUAGE DES TUBES GUIDES AVEC LOI DE LEMAITRE
C
      NBPAR  = 0
      NOMPAR = ' '
      VALPAR = 0.D0
C
      NBRES = 12
      CALL RCVALA ( ICODMA,'LMARC',NBPAR,NOMPAR,VALPAR,
     &              NBRES,NOMLMA(1),VALRES(1),CODRES(1), BL2 )
      NBRES = 3
      CALL RCVALA ( ICODMA,'LEMAITRE',NBPAR,NOMPAR,VALPAR,
     &              NBRES,NOMLEM(1),VALRET(1),CODRET(1), BL2 )
      NBRES = 4
      CALL RCVALA ( ICODMA,'FLU_IRRA',NBPAR,NOMPAR,VALPAR,
     &              NBRES,NOMLEM(4),VALRET(4),CODRET(2), BL2 )
      IF(CODRES(1).EQ.'OK'.AND.CODRET(1).NE.'OK')THEN
         CALL R8COPY (NBVAL,VALRES,1,COELMA,1)
         ICAS = 1
      ELSEIF(CODRES(1).NE.'OK'.AND.CODRET(1).EQ.'OK')THEN
         CALL R8COPY (NBCLEM,VALRET,1,COELEM,1)
         ICAS = 2
      ELSE
         CALL UTMESS ('F','NMASSC',
     &  'DEFINITION MULTIPLE DU COMPORTEMENT POUR UN ELEMENT DE POUTRE')
      ENDIF
      END
