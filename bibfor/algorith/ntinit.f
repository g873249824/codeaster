      SUBROUTINE NTINIT(RESULT,MODELE,MATE  ,CARELE,COMPOR,
     &                  INFCHA,INFCH2,SOLVEU,PARA  ,NUMEDD,
     &                  LOSTAT,LEVOL ,LNONL ,SDDISC,NBPASE,
     &                  INPSCO,MAILLA,SDCRIT,TIME  )
