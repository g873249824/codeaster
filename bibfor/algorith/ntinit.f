      SUBROUTINE NTINIT(RESULT,MODELE,MATE  ,CARELE,LISCHA,
     &                  LISCH2,SOLVEU,PARA  ,NUMEDD,LOSTAT,
     &                  LEVOL ,LNONL ,SDDISC,SDIETO,
     &                  MAILLA,SDCRIT,TIME  )
