C
C RETOUR DU POINTEUR SUR LES DDLS ELIMINES PAR AFFE_CHAR_CINE
C      
C ----------------------------------------------------------------------
C
C
C IN  MATASS : MATRICE DU PREMIER MEMBRE ASSEMBLEE
