      IMPLICIT     NONE
      INCLUDE 'jeveux.h'
      CHARACTER*24 DEFICO     
      INTEGER      POSMAI
      INTEGER      POSNNO(9)
      INTEGER      NNOMAI
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES - UTILITAIRE)
C
C INDICES DANS CONTNO DES NOEUDS POUR UNE MAILLE DONNEE
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE CONTACT (DEFINITION)
C IN  POSMAI : INDICE DE LA MAILLE (DANS SD CONTACT)
C OUT POSNNO : INDICES DANS CONTNO DES NOEUDS
C OUT NNOMAI : NOMBRE DE NOEUDS DE LA MAILLE (DANS LES SD DE CONTACT)
C
C
C
C
      INTEGER      NBNMAX
      PARAMETER   (NBNMAX = 9)
C
      CHARACTER*24 NOMACO,PNOMA
      INTEGER      JNOMA,JPONO
      INTEGER      INO,IBID,JDEC
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES SD CONTACT
C
      NOMACO = DEFICO(1:16)//'.NOMACO'
      PNOMA  = DEFICO(1:16)//'.PNOMACO'
      CALL JEVEUO(NOMACO,'L',JNOMA)
      CALL JEVEUO(PNOMA ,'L',JPONO)        
C
C --- NOMBRE DE NOEUDS ATTACHES A CETTE MAILLE
C
      CALL CFNBEN(DEFICO,POSMAI,'CONNEX',NNOMAI,IBID  )
      CALL ASSERT(NNOMAI.LE.NBNMAX)  
C
C --- NUMERO DES NOEUDS ATTACHES A CETTE MAILLE
C
      JDEC = ZI(JPONO+POSMAI-1)
      DO 10 INO = 1,NNOMAI
        POSNNO(INO) = ZI(JNOMA+JDEC+INO-1)
   10 CONTINUE
C
      CALL JEDEMA()
      END
