        IF((VITS .GE. 1.0D0) .AND. (VIET .EQ. 0.0D0)) THEN
          VITS = -VITS
        ELSEIF((VITS .LT. 0.0D0) .AND. (VIET.GT.0.0D0)) THEN
          VITS = -VITS + 1.0D0
        ELSEIF((VITS .GT. TSEUIL) .AND. (VIET .GT. 0.0D0)) THEN
          VITS = VITS - TSRETU
          IF(VITS .LT. TSEUIL) VITS = TSEUIL
        ELSEIF((VITS .EQ. 0.0D0) .AND. (VIET .GT. 0.0D0)) THEN
          VITS = 1.0D0        
        ENDIF
      END
