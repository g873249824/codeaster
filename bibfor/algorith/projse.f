      SUBROUTINE PROJSE(MATYP,NDIM,
     &                  COORDA,COORDB,COORDC,COORDP,
     &                  PROJ,MOYEN,LISSA,TANGDF,VLISSA,DIAGNO,TOLEIN,
     &                  NORM,TANG,
     &                  COORDM,COEF,OLDJEU,JEU,
     &                  NOEUD,DEBORD)

