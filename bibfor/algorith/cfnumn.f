      IMPLICIT     NONE
      CHARACTER*8  NOMA
      CHARACTER*24 DEFICO
      INTEGER      NNO
      INTEGER      POSNNO(NNO)
      INTEGER      POSMAI
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (TOUTES METHODES - UTILITAIRE)
C
C DONNE LES NUMEROS (DANS SD CONTACT) DES NOEUDS D'UNE MAILLE DE CONTACT
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEFICO : SD DE DEFINITION DU CONTACT (ISSUE D'AFFE_CHAR_MECA)
C IN  POSMAI : POSITION DE LA MAILLE DANS LES SD CONTACT
C IN  NNO    : NOMBRE DE NOEUDS DE LA MAILLE
C OUT POSNNO : NUMERO DES NOEUDS DE LA MAILLE DANS SD CONTACT      
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*24 NOMACO,PNOMA
      INTEGER      JNOMA,JPONO
      INTEGER      INO,JDEC
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ () 
C
C --- ACCES SD CONTACT
C
      NOMACO = DEFICO(1:16)//'.NOMACO'
      PNOMA  = DEFICO(1:16)//'.PNOMACO'
      CALL JEVEUO(NOMACO,'L',JNOMA)
      CALL JEVEUO(PNOMA ,'L',JPONO)      
C
C --- NUMERO DES NOEUDS
C
      JDEC = ZI(JPONO+POSMAI-1)
      DO 10 INO = 1,NNO
        POSNNO(INO) = ZI(JNOMA+JDEC+INO-1)
   10 CONTINUE    
C
      CALL JEDEMA()
C
      END
