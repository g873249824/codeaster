      IMPLICIT      NONE
      INCLUDE 'jeveux.h'
      CHARACTER*19  SDAPPA
      INTEGER       IZONE 
      LOGICAL       VALL 
      CHARACTER*(*) QUESTZ
C      
C ----------------------------------------------------------------------
C
C ROUTINE APPARIEMENT (UTILITAIRE)
C
C INFO. DE TYPE LOGICAL SUR LA ZONE COURANTE
C
C ----------------------------------------------------------------------
C
C
C IN  SDAPPA : NOM DE LA SD APPARIEMENT
C IN  IZONE  : NUMERO DE LA ZONE
C IN  QUESTI : QUESTION
C               DIRE_APPA_FIXE DIRECTION FIXE D'APPARIEMENT ? 
C               APPA_MAIT_ESCL APPARIEMENT MAITRE-ESCLAVE ? 
C               CALC_NORM_ESCL CALCUL DE LA NORMALE SUR NOEUD ESCLAVE ?
C               CALC_NORM_MAIT CALCUL DE LA NORMALE SUR NOEUD MAITRE ?
C OUT VALL   : REPONSE A LA QUESTION 
C
C
C
C
      INTEGER      IFM,NIV
      CHARACTER*24 QUESTI
      INTEGER      IAPPA
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('APPARIEMENT',IFM,NIV)
C
C --- INITIALISATIONS
C
      VALL   = .FALSE.
      QUESTI = QUESTZ
C      
C --- REPONSE
C
      IF (QUESTI.EQ.'DIRE_APPA_FIXE') THEN
        CALL APZONI(SDAPPA,IZONE ,'DIRE_APPA',IAPPA )
        VALL   = IAPPA.EQ.1
      ELSEIF (QUESTI.EQ.'APPA_MAIT_ESCL') THEN
        CALL APZONI(SDAPPA,IZONE ,'TYPE_APPA',IAPPA )
        VALL   = IAPPA.EQ.1
      ELSEIF (QUESTI.EQ.'CALC_NORM_ESCL') THEN
        CALL APZONI(SDAPPA,IZONE ,'CALC_NORM_ESCL',IAPPA )
        VALL   = IAPPA.EQ.1
      ELSEIF (QUESTI.EQ.'CALC_NORM_MAIT') THEN
        CALL APZONI(SDAPPA,IZONE ,'CALC_NORM_MAIT',IAPPA )
        VALL   = IAPPA.EQ.1
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF     
C
      CALL JEDEMA()
C 
      END
