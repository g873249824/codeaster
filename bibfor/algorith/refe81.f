      SUBROUTINE  REFE81 (NOMRES,BASMOD,RAIDF,MASSF,AMORF,MAILLA,OPT)
      IMPLICIT REAL*8 (A-H,O-Z)
C MODIF ALGORITH  DATE 12/09/2005   AUTEUR NICOLAS O.NICOLAS 
C            CONFIGURATION MANAGEMENT OF EDF VERSION
