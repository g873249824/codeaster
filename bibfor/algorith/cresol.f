      SUBROUTINE CRESOL(SOLVEU,SUFFI2)
      IMPLICIT   NONE

      CHARACTER*19 SOLVEU
      CHARACTER*(*) SUFFI2
C ----------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 08/04/2008   AUTEUR LEFEBVRE J-P.LEFEBVRE 
C ======================================================================
C COPYRIGHT (C) 1991 - 2001  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C     SAISIE ET VERIFICATION DE LA COHERENCE DES DONNEES RELATIVES AU
C     SOLVEUR

C IN K19 SOLVEU  : NOM DU SOLVEUR DONNE EN ENTREE
C OUT    SOLVEU  : LE SOLVEUR EST CREE ET INSTANCIE
C ----------------------------------------------------------
C RESPONSABLE VABHHTS J.PELLET
C TOLE CRP_20

C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------

      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C --- FIN DECLARATIONS NORMALISEES JEVEUX --------------------

      INTEGER NMAXIT,ISTOP,NSOLVE,NBRPAS,IBID,NIREMP,IFM,
     &        NIV,NBMA,NBREOR,NPREC,NMAXI1,NBREO1,INUMSD,
     &        IMAIL,NBREOI,NBREO2,
     &        REACRE,REACR1
      REAL*8 RESIRE,TBLOC,JEVTBL,EPS,TESTCO,RESIR1,TESTC1
      CHARACTER*3 SYME
      CHARACTER*8 KSTOP,KVARI, KPREC
      CHARACTER*8 METHOD,RENUM,PRECO,K8BID,VERIF
      CHARACTER*8 TYREOR,SCALIN,PRECO1,VERIF1,TYREO1,SCALI1
      CHARACTER*8 STOGI,ACSM,ACSM1,ACMA,NOMRES
      CHARACTER*12 SUFFIX
      CHARACTER*16 NOMSOL,KDIS,TYPRES,NOMCOM
      CHARACTER*24 SDFETI,INFOFE
      LOGICAL EXISYM,GETEXM,TESTOK
C------------------------------------------------------------------
      CALL JEMARQ()


      SUFFIX=SUFFI2
      METHOD='MULT_FRO'
      PRECO='SANS'
      RENUM='MDA'
      SYME='NON'
      EXISYM=.FALSE.
      RESIRE=1.D-6
      EPS=0.D0
      NPREC=8
      NMAXIT=0
      ISTOP=0
      NIREMP=0
      TBLOC=JEVTBL()
      SDFETI='????'
      VERIF='????'
      TESTCO=0.D0
      NBREOR=0
      NBREOI=0
      TYREOR='SANS'
      SCALIN='SANS'
      STOGI='????'
      ACSM='NON'
      ACMA='NON'
      REACRE=0
      INFOFE='FFFFFFFFFFFFFFFFFFFFFFFF'


C     RECUPERATION ET MAJ DU NIVEAU D'IMPRESSION
      CALL INFNIV(IFM,NIV)

      IF ((SUFFIX(1:5).EQ.'_MECA') .OR. (SUFFIX(1:5).EQ.'_THER') .OR.
     &    (SUFFIX(1:10).EQ.'_NON_LOCAL')) THEN
        NOMSOL='SOLV'//SUFFIX
      ELSE
        NOMSOL='SOLVEUR'
      ENDIF
      CALL GETFAC(NOMSOL,NSOLVE)
      IF (NSOLVE.EQ.0)GOTO 10


      CALL GETVTX(NOMSOL,'METHODE',1,1,1,METHOD,IBID)



      IF (METHOD.EQ.'MUMPS') THEN
C     -----------------------------
        CALL CRSVMU(NOMSOL,SOLVEU)


      ELSEIF (METHOD.EQ.'FETI') THEN
C     -----------------------------
        CALL CRSVFE(NOMSOL,SOLVEU)


C     -- CAS : PETSC :
C     -----------------
      ELSEIF (METHOD.EQ.'PETSC') THEN
        SYME='NON'
        EXISYM = GETEXM(NOMSOL,'SYME')
        IF (EXISYM) CALL GETVTX(NOMSOL,'SYME',1,1,1,SYME,IBID)
        CALL GETVTX(NOMSOL,'VARIANTE',1,1,1,KVARI,IBID)
        CALL GETVTX(NOMSOL,'PRE_COND',1,1,1,KPREC,IBID)
        CALL GETVTX(NOMSOL,'RENUM',1,1,1,RENUM,IBID)
        CALL GETVR8(NOMSOL,'RESI_RELA',1,1,1,EPS,IBID)
        CALL GETVIS(NOMSOL,'NMAX_ITER',1,1,1,NMAXIT,IBID)
        CALL CRSVPE(SOLVEU,SYME,KVARI,KPREC,EPS,NMAXIT,RENUM)
      ELSE

C       LDLT, MULT_FRO, GCPC :
C     -----------------------------

C       -- CALCUL DE ISTOP, NBRPAS, SYME, NPREC
        KSTOP=' '
        CALL GETVTX(NOMSOL,'STOP_SINGULIER',1,1,1,KSTOP,IBID)
        CALL GETVIS(NOMSOL,'NPREC',1,1,1,NPREC,IBID)
        IF (KSTOP.EQ.'NON') THEN
          ISTOP=1
        ELSEIF (KSTOP.EQ.'DECOUPE') THEN
          CALL GETVIS('INCREMENT','SUBD_PAS',1,1,1,NBRPAS,IBID)
          IF (NBRPAS.LE.1) THEN
            CALL U2MESS('F','ALGORITH11_81')
          ELSE
            ISTOP=1
          ENDIF
        ENDIF

        EXISYM=GETEXM(NOMSOL,'SYME')
        IF (EXISYM) THEN
          CALL GETVTX(NOMSOL,'SYME',1,1,1,SYME,IBID)
        ENDIF


        IF (METHOD.EQ.'LDLT') THEN
C     -----------------------------
          CALL GETVTX(NOMSOL,'RENUM',1,1,1,RENUM,IBID)
          IF (IBID.EQ.0)RENUM='RCMK'

          IF (RENUM(1:4).NE.'RCMK' .AND.
     &        RENUM(1:4).NE.'SANS') CALL U2MESK('F','ALGORITH2_35',1,
     &        RENUM)


        ELSEIF (METHOD.EQ.'GCPC') THEN
C     -----------------------------
          CALL GETVTX(NOMSOL,'RENUM',1,1,1,RENUM,IBID)
          IF (IBID.EQ.0)RENUM='RCMK'
          IF (RENUM(1:4).NE.'RCMK' .AND.
     &        RENUM(1:4).NE.'SANS') CALL U2MESK('F','ALGORITH2_36',1,
     &        RENUM)
          PRECO='LDLT_INC'

          CALL GETVIS(NOMSOL,'NMAX_ITER',1,1,1,NMAXIT,IBID)
          CALL GETVR8(NOMSOL,'RESI_RELA',1,1,1,RESIRE,IBID)
          CALL GETVIS(NOMSOL,'NIVE_REMPLISSAGE',1,1,1,NIREMP,IBID)


        ELSEIF (METHOD.EQ.'MULT_FRO') THEN
C     --------------------------------------------------------------
          CALL GETVTX(NOMSOL,'RENUM',1,1,1,RENUM,IBID)
          IF (RENUM(1:2).NE.'MD' .AND. RENUM(1:2).NE.'ME') THEN
            CALL U2MESK('F','ALGORITH2_37',1,RENUM)
          ENDIF
        ENDIF


C     -- CREATION DU SOLVEUR :
C     --------------------------------------------------------------
        CALL CRESO1(SOLVEU,METHOD,PRECO,RENUM,SYME,SDFETI,EPS,RESIRE,
     &              TBLOC,NPREC,NMAXIT,ISTOP,NIREMP,IFM,0,NBMA,VERIF,
     &              TESTCO,NBREOR,TYREOR,SCALIN,INUMSD,IMAIL,K8BID,
     &              INFOFE,STOGI,TESTOK,NBREOI,ACMA,ACSM,REACRE)


      ENDIF

   10 CONTINUE


   20 CONTINUE
      CALL JEDEMA()
      END
