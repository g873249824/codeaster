      SUBROUTINE  XRIGEL(NNOP,DDLH,NFE,DDLC,DDLM,
     &                   IGEOM,
     &                   JPINTT,CNSET,HEAVT,LONCH,BASLOC,
     &                   LSN,LST,SIG,MATUU,JPMILT)

       IMPLICIT NONE
       INTEGER      NNOP,IGEOM,DDLM
       INTEGER      DDLH,NFE,DDLC,CNSET(4*32),HEAVT(36),LONCH(10)
       INTEGER      JPINTT,JPMILT
       REAL*8       LSN(NNOP)
       REAL*8       LST(NNOP),MATUU(*),SIG(*),BASLOC(*)
C ----------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 31/01/2012   AUTEUR REZETTE C.REZETTE 
C ======================================================================
C COPYRIGHT (C) 1991 - 2012  EDF R&D                  WWW.CODE-ASTER.ORG
