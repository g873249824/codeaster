      SUBROUTINE NIFN3O(NNO1  ,NNO2  ,NPG   ,IPOIDS,IVF1  ,IVF2  ,
     &                  IDFDE1,DFDI  ,GEOM  ,AXI   ,SIG   ,IMATE ,
     &                  COMPOR,DEPLM ,PRESM ,PIM   ,STAB  ,FINTU ,
     &                  FINTA ,FINTP)

