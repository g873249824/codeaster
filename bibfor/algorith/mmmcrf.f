C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT NONE
      CHARACTER*8  NOMA
      CHARACTER*19 DEPPLU
      CHARACTER*19 DDEPLA
      REAL*8       EPSFRO
      LOGICAL      MMCVFR
      CHARACTER*16 CVGNOE
      REAL*8       CVGVAL
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - ALGORITHME - UTILITAIRE)
C
C CALCUL DU CRITERE DE CONVERGENCE
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEPGEO : CHAMP GEOMETRIQUE A L'ITERATION GEOM. N-1
C IN  DEPPLU : CHAMP GEOMETRIQUE A L'ITERATION GEOM. N
C IN  EPSFRO : VALEUR DU CRITERE DE CONVERGENCE
C OUT CVGNOE : LIEU OU LE CRITERE EST MAX
C OUT CVGVAL : VALEUR DU CRITERE MAX
C OUT MMCONV : VAUT .TRUE. SI LE CRITERE EST OK
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      CHARACTER*32 JEXNUM
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      REAL*8       VMAX3,VMAX4,VMAXI
      REAL*8       CRILBD,R8VIDE
      CHARACTER*8  LICMP(3),NOMNOE
      INTEGER      NCMP,NUMNO3,NUMNO4,NUMNOE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      VMAX3  = 0.D0
      VMAX4  = 0.D0
      CRILBD = 0.D0
      CVGNOE = ' '
      CVGVAL = R8VIDE()
      MMCVFR = .FALSE.
C
C --- VARIATION RELATIVE SUR LES LAGS_C
C
      NCMP     = 1
      LICMP(1) = 'LAGS_C'
      LICMP(2) = ' '
      LICMP(3) = ' '

      CALL CNOMAX(DDEPLA,NCMP,LICMP(1),VMAX3,NUMNO3)
      CALL CNOMAX(DEPPLU,NCMP,LICMP(1),VMAX4,NUMNO4)
C
      IF (VMAX4.GT.0.D0) THEN
        CRILBD = VMAX3/VMAX4
      ELSE
        CRILBD = 0.D0
      END IF
C
C --- TEST DE CONVERGENCE
C
      IF (CRILBD.LT.ABS(EPSFRO)) THEN
        MMCVFR = .TRUE.
      ELSE
        MMCVFR = .FALSE.
      END IF
        
      NUMNOE = NUMNO3
      VMAXI  = CRILBD
      
      IF (NUMNOE.EQ.0) THEN
        NOMNOE = ' '
      ELSE
        CALL JENUNO(JEXNUM(NOMA//'.NOMNOE',NUMNOE),NOMNOE)
      ENDIF
      CVGNOE = NOMNOE//'        '
      CVGVAL = VMAXI
C
      CALL JEDEMA()
      END
