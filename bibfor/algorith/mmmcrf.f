      IMPLICIT     NONE
      INCLUDE 'jeveux.h'
      CHARACTER*8  NOMA
      CHARACTER*19 DEPPLU,DDEPLA
      CHARACTER*16 NFROT
      REAL*8       VFROT
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - ALGORITHME - UTILITAIRE)
C
C CALCUL RESIDU DE FROTTEMENT
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DDEPLA : INCREMENT SOLUTION
C IN  DEPPLU : SOLUTION A L'INSTANT COURANT
C OUT NFROT  : LIEU OU LE CRITERE EST MAX
C OUT VFROT  : VALEUR DU CRITERE MAX
C
C ----------------------------------------------------------------------
C
      INTEGER      NCMP
      PARAMETER    (NCMP=1)
      CHARACTER*8  LISCMP(NCMP)
C
      REAL*8       VMAX1,VMAX2,VMAXI
      REAL*8       CRILBD,R8VIDE
      CHARACTER*8  NOMNOE
      INTEGER      NUMNO1,NUMNO2,NUMNOE
C
      DATA LISCMP  /'LAGS_C'/
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      VMAX1  = 0.D0
      VMAX2  = 0.D0
      CRILBD = 0.D0
      NFROT  = ' '
      VFROT  = R8VIDE()
C
C --- TRI DES COMPOSANTES
C
      CALL CNOMAX(DDEPLA,NCMP,LISCMP,VMAX1,NUMNO1)
      CALL CNOMAX(DEPPLU,NCMP,LISCMP,VMAX2,NUMNO2)
C
C --- CRITERE: VARIATION RELATIVE SUR LES LAGS_C
C
      IF (VMAX2.GT.0.D0) THEN
        CRILBD = VMAX1/VMAX2
      ELSE
        CRILBD = 0.D0
      END IF
C
C --- INFORMATIONS SUR LE CRITERE
C
      NUMNOE = NUMNO1
      VMAXI  = CRILBD
      IF (NUMNOE.EQ.0) THEN
        NOMNOE = ' '
      ELSE
        CALL JENUNO(JEXNUM(NOMA//'.NOMNOE',NUMNOE),NOMNOE)
      ENDIF
      NFROT  = NOMNOE//'        '
      VFROT  = VMAXI
C
      CALL JEDEMA()
      END
