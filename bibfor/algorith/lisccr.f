      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      CHARGE = LISCHA(1:19)//'.LCHA'
      INFOCH = LISCHA(1:19)//'.INFC'
      FOMULT = LISCHA(1:19)//'.FCHA'
C      
      CALL DETRSD('LISTE_CHARGES',LISCHA)
      CALL WKVECT(CHARGE,BASE//' V K24',NCHAR    ,JALICH)
