      LOGICAL       LAFFE
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME - IMPRESSION)
C
C ENREGISTRE LES DONNEES REELLES DANS LE TABLEAU DE CONVERGENCE
C
C ----------------------------------------------------------------------
C
C
C IN  SDIMPR : SD AFFICHAGE
C IN  TYPCOL : CODE TYPE DE LA COLONNE
C IN  VALR   : VALEUR DE TYPE REAL POUR LES COLONNES DE TYPE REEL
C IN  LAFFE  : .TRUE. SI LA VALEUR EST AFFECTEE
C
C ----------------------------------------------------------------------
C
      CHARACTER*24 SDTABC,SLCOLO,SDCOLO
      CHARACTER*16 K16BID
      INTEGER      IBID
C
C ----------------------------------------------------------------------
C

C
C --- RECUPERATION DU TABLEAU DE CONVERGENCE
C
      CALL OBGETO(SDIMPR,'TABLEAU_CONV' ,SDTABC)
C
C --- LISTE DES COLONNES
C
      CALL OBGETO(SDTABC,'COLONNES_DISPOS',SLCOLO)
C
C --- COLONNE CORRESPONDANTE
C
      CALL OBLGOP(SLCOLO,TYPCOZ,SDCOLO)
C
C --- AFFECTATION DANS LE TABLEAU
C
      CALL IMPSDR(SDCOLO,K16BID,VALR  ,IBID  )
C
C --- VALIDATION DE LA VALEUR
C
      CALL OBSETB(SDCOLO,'VALE_AFFE',LAFFE)
C
      END
