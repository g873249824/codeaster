C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (STRUCTURES DE DONNES)
C
C CREATION SD AFFICHAGE
C
C ----------------------------------------------------------------------
C
C
C IN  SDIMPR : NOM DE LA SD POUR L'AFFICHAGE
C IN  SDSUIV : NOM DE LA SD POUR LE SUIVI
C IN  FONACT : FONCTIONNALITES ACTIVEES (VOIR NMFONC)
C
C
C
C
      INTEGER      ZDEF
      PARAMETER    (ZDEF=40)
      INTEGER      NCOL,UNITE
      INTEGER      IFM,NIV
      CHARACTER*19 COLACT
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> ... CREATION SD AFFICHAGE'
      ENDIF
C
C --- INITIALISATIONS
C
      IF (ZDEF.NE.40) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- LECTURE MOT_CLEF
C
      CALL IMPLEC(FONACT,SDSUIV,ZDEF  ,COLACT,NCOL  ,
     &            UNITE )
C
C --- CREATION SD AFFICHAGE
C
      CALL IMPINI(SDIMPR,UNITE ,NCOL  )
C
C --- ACTIVATION DES COLONNES
C
      CALL IMPDEF(SDIMPR,SDSUIV,COLACT,NCOL  )
C
C --- MENAGE
C
      CALL JEDETR(COLACT)
      CALL JEDEMA()
      END
