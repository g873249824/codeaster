C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C-----------------------------------------------------------------------
C  DESCRIPTION : OPERATEUR STAT_NON_LINE
C  -----------   RECUPERATION D'UN CHARGEMENT MECANIQUE DE TYPE
C                PRECONTRAINTE

C  IN     : LIGRMO : K19 : NOM DU LIGREL DU MODELE
C  IN     : COMPOR : K19 : NOM DE LA CARTE DE COMPORTEMENT
C  IN/OUT : SIGINI : K19 : NOM DU CHAMP DE CONTRAINTE INITIAL VIERGE.
C                    ON LUI ADDITIONNE LES EVENTUELS CHAMPS :
C                    LCHAR(I)//.CHME.SIGIN'

C-------------------   DECLARATION DES VARIABLES   ---------------------

C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNOM,JEXNUM,JEXATR
C     ----- FIN   COMMUNS NORMALISES  JEVEUX  --------------------------

C ARGUMENTS
C ---------
      CHARACTER*24 LIGRMO,SIGINI,COMPOR

C VARIABLES LOCALES
C -----------------
      INTEGER IBID,IOCC,IRET,NBOCC
      CHARACTER*8 CHARGE,LPAIN(2),LPAOUT(1)
      CHARACTER*16 OPTION
      CHARACTER*19 SIGCHA
      CHARACTER*24 LCHIN(2),LCHOUT(1)

C-------------------   DEBUT DU CODE EXECUTABLE    ---------------------

      CALL JEMARQ()

      CALL GETFAC('EXCIT',NBOCC)
      IF (NBOCC.GT.0) THEN
        OPTION = 'ADD_SIGM'
        LCHIN(1) = SIGINI
        LPAIN(1) = 'PEPCON1'
        LPAIN(2) = 'PEPCON2'
        LCHOUT(1) = '&&NMSIGI.PEPCON3'
        LPAOUT(1) = 'PEPCON3'
        DO 10 IOCC = 1,NBOCC
          CALL GETVID('EXCIT','CHARGE',IOCC,1,1,CHARGE,IBID)
          SIGCHA = CHARGE//'.CHME.SIGIN'
          CALL EXISD('CHAMP_GD',SIGCHA,IRET)
          IF (IRET.GT.0) THEN
            LCHIN(2) = SIGCHA
            CALL COPISD('CHAM_ELEM_S','V',COMPOR,LCHOUT(1))
            CALL CALCUL('S',OPTION,LIGRMO,2,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &                  'V')
            CALL DETRSD('CHAM_ELEM_S',LCHOUT(1))
            CALL COPISD('CHAMP_GD','V','&&NMSIGI.PEPCON3',SIGINI)
          END IF
   10   CONTINUE
        CALL DETRSD('CHAMP_GD','&&NMSIGI.PEPCON3')
      END IF

      CALL JEDEMA()

C --- FIN DE NMSIGI.
      END
