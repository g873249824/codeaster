      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*24 LIGRMO,COMPOR
      CHARACTER*19 SIGINI
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (INITIALISATION)
C
C RECUPERATION D'UN CHARGEMENT MECANIQUE DE TYPE PRECONTRAINTE
C
C ----------------------------------------------------------------------
C
C
C IN  LIGRMO : NOM DU LIGREL DU MODELE
C IN  COMPOR : NOM DE LA CARTE DE COMPORTEMENT
C I/O SIGINI : NOM DU CHAMP DE CONTRAINTE INITIAL VIERGE.
C                    ON LUI ADDITIONNE LES EVENTUELS CHAMPS :
C                    LCHAR(I)//.CHME.SIGIN'
C
C
C
C
      INTEGER      NBOUT,NBIN
      PARAMETER    (NBOUT=1, NBIN=2)
      CHARACTER*8  LPAOUT(NBOUT),LPAIN(NBIN)
      CHARACTER*19 LCHOUT(NBOUT),LCHIN(NBIN)
C
      INTEGER      IBID,IOCC,IRET,NBOCC
      CHARACTER*8  CHARGE
      CHARACTER*16 OPTION
      CHARACTER*19 SIGCHA
      INTEGER      IARG
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()

      CALL GETFAC('EXCIT',NBOCC)
      IF (NBOCC.GT.0) THEN
        OPTION    = 'ADD_SIGM'
        LCHIN(1)  = SIGINI
        LPAIN(1)  = 'PEPCON1'
        LPAIN(2)  = 'PEPCON2'
        LCHOUT(1) = '&&NMSIGI.PEPCON3'
        LPAOUT(1) = 'PEPCON3'
        DO 10 IOCC = 1,NBOCC
          CALL GETVID('EXCIT','CHARGE',IOCC,IARG,1,CHARGE,IBID)
          SIGCHA = CHARGE//'.CHME.SIGIN'
          CALL EXISD('CHAMP_GD',SIGCHA,IRET)
          IF (IRET.GT.0) THEN
            LCHIN(2) = SIGCHA
            CALL COPISD('CHAM_ELEM_S','V',COMPOR,LCHOUT(1))
            CALL CALCUL('S',OPTION,LIGRMO,2,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &                  'V','OUI')
            CALL DETRSD('CHAM_ELEM_S',LCHOUT(1))
            CALL COPISD('CHAMP_GD','V','&&NMSIGI.PEPCON3',SIGINI)
          END IF
   10   CONTINUE
        CALL DETRSD('CHAMP_GD','&&NMSIGI.PEPCON3')
      END IF

      CALL JEDEMA()
C.
      END
