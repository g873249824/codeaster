C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*24 DEFICO
      INTEGER      IZONE
      INTEGER      POSMAE
      INTEGER      NBPC
      INTEGER      NBEXFR,NDEXFR(3)     
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT - UTILITAIRE)
C
C EXCLUSION DES NOEUDS DONNES DANS SANS_GROUP_NO_FR
C      
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD POUR LA DEFINITION DU CONTACT
C IN  IZONE  : NUMERO DE LA ZONE DE CONTACT
C IN  POSMAE : NUMERO DE LA MAILLE MAITRE
C IN  NBPC   : NOMBRE DE NOEUDS DE LA MAILLE MAITRE
C OUT NBEXFR : NOMBRE DE NOEUDS A EXCLURE SUR LA MAILLE FROTTEMENT
C OUT NDEXFR : NUMERO DES NOEUDS EXCLUS SUR LA MAILLE FROTTEMENT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      ZBEXFR
      PARAMETER    (ZBEXFR=3)
      INTEGER      POSNOE,NUMNOE
      CHARACTER*24 NOMACO,CONTNO,PNOMA
      INTEGER      JNOMA,JNOCO,JPONO
      INTEGER      SUPPOK,IPC,I
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ() 
C
C --- ACCES OBJETS
C 
      NOMACO = DEFICO(1:16)//'.NOMACO'
      CONTNO = DEFICO(1:16)//'.NOEUCO'
      PNOMA  = DEFICO(1:16)//'.PNOMACO'       
C           
      CALL JEVEUO(NOMACO,'L',JNOMA)
      CALL JEVEUO(CONTNO,'L',JNOCO)     
      CALL JEVEUO(PNOMA ,'L',JPONO)        
C
C --- INITIALISATIONS 
C
      NBEXFR = 0
      DO 1 I=1,ZBEXFR
        NDEXFR(I) = 0
    1 CONTINUE   
C     
C --- REPERAGE SI LE NOEUD IPC EST UN NOEUD A EXCLURE (SUPPOK = 1)
C
      DO 50 IPC = 1,NBPC
        POSNOE = ZI(JNOMA+ZI(JPONO+POSMAE-1)+IPC-1)
        NUMNOE = ZI(JNOCO+POSNOE-1)
        CALL CFMMEX(DEFICO,'FROT',IZONE ,NUMNOE,SUPPOK)
        IF (SUPPOK .EQ. 1) THEN
          NBEXFR  = NBEXFR + 1
          IF (NBEXFR.GE.4) THEN
            CALL ASSERT(.FALSE.)
          ELSE  
            NDEXFR(NBEXFR) = IPC
          ENDIF
        ENDIF
 50   CONTINUE       
C
      CALL JEDEMA()      
      END
