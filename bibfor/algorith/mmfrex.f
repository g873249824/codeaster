      IMPLICIT NONE
      CHARACTER*8  NOMA
      CHARACTER*24 DEFICO
      INTEGER      POSMAI
      INTEGER      NBEXFR,NDEXFR(3)     
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT - UTILITAIRE)
C
C LISTE DES NOEUDS EXCLUS PAR SANS_GROUP_NO_FR
C      
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD POUR LA DEFINITION DU CONTACT
C IN  NOMA   : NOM DU MAILLAGE
C IN  POSMAI : NUMERO DE LA MAILLE 
C OUT NBEXFR : NOMBRE DE NOEUDS A EXCLURE SUR LA MAILLE FROTTEMENT
C OUT NDEXFR : NUMERO DES NOEUDS EXCLUS SUR LA MAILLE FROTTEMENT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      ZBEXFR
      PARAMETER    (ZBEXFR=3)
      INTEGER      POSNO,NUMNO,NBNO,POSNNO(9)
      CHARACTER*24 CONTNO,NOZOCO
      INTEGER      JNOCO,JZOCO
      INTEGER      SUPPOK,INO,I,IZONE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ() 
C
C --- ACCES OBJETS
C 
      NOZOCO = DEFICO(1:16)//'.NOZOCO'
      CONTNO = DEFICO(1:16)//'.NOEUCO'
      CALL JEVEUO(NOZOCO,'L',JZOCO)
      CALL JEVEUO(CONTNO,'L',JNOCO)
C
C --- INITIALISATIONS 
C
      NBEXFR = 0
      DO 1 I=1,ZBEXFR
        NDEXFR(I) = 0
    1 CONTINUE  
C
C --- NUMEROS DES NOEUDS DE LA MAILLE DANS SD CONTACT   
C    
      CALL CFPOSN(NOMA  ,DEFICO,POSMAI,POSNNO,NBNO  )         
C     
C --- REPERAGE SI LE NOEUD EST UN NOEUD A EXCLURE (SUPPOK = 1)
C
      DO 50 INO = 1,NBNO
        POSNO  = POSNNO(INO)
        NUMNO  = ZI(JNOCO+POSNO-1) 
        IZONE  = ZI(JZOCO+POSNO-1)       
        CALL CFMMEX(DEFICO,'FROT',IZONE ,NUMNO,SUPPOK)
        IF (SUPPOK .EQ. 1) THEN
          NBEXFR  = NBEXFR + 1
          IF (NBEXFR.GE.3) THEN
            CALL ASSERT(.FALSE.)
          ELSE  
            NDEXFR(NBEXFR) = INO
          ENDIF
        ENDIF
 50   CONTINUE       
C
      CALL JEDEMA()      
      END
