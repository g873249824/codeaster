      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER       NRPASE
      CHARACTER*6   TYPCHP
      CHARACTER*24  SDSENS
      CHARACTER*(*) NOMCHZ
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (UTILITAIRE)
C
C RETOURNE LE NOM D'UN CHAMP EN SENSIBILITE
C
C ----------------------------------------------------------------------
C
C
C IN  NRPASE : NUMERO DU PARAMETRE SENSIBLE CONCERNE
C IN  TYPCHP : TYPE DU CHAMP
C OUT NOMCHP : NOM DE LA STRUCTURE. ON COMPLETE PAR DES BLANCS
C IN  SDSENS : SD SENSIBILITE
C
C
C
C
      CHARACTER*13  INPSCO
      CHARACTER*24  SENSIN
      INTEGER       JSENSI
      INTEGER       TYPEST
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES SD SENSIBILITE
C
      SENSIN = SDSENS(1:16)//'.INPSCO '
      CALL JEVEUO(SENSIN,'L',JSENSI)
      INPSCO = ZK16(JSENSI+1-1)
C
C --- NOM CHAMP
C
      IF (TYPCHP.EQ.'NOPASE') THEN
        TYPEST = 1
      ELSEIF (TYPCHP.EQ.'RESULT') THEN
        TYPEST = 3
      ELSEIF (TYPCHP.EQ.'DEPPLU') THEN
        TYPEST = 4
      ELSEIF (TYPCHP.EQ.'DEPMOI') THEN
        TYPEST = 5
      ELSEIF (TYPCHP.EQ.'SIGPLU') THEN
        TYPEST = 8
      ELSEIF (TYPCHP.EQ.'SIGMOI') THEN
        TYPEST = 9
      ELSEIF (TYPCHP.EQ.'VARPLU') THEN
        TYPEST = 10
      ELSEIF (TYPCHP.EQ.'VARMOI') THEN
        TYPEST = 11
      ELSEIF (TYPCHP.EQ.'VITPLU') THEN
        TYPEST = 14
      ELSEIF (TYPCHP.EQ.'VITMOI') THEN
        TYPEST = 15
      ELSEIF (TYPCHP.EQ.'ACCPLU') THEN
        TYPEST = 16
      ELSEIF (TYPCHP.EQ.'ACCMOI') THEN
        TYPEST = 17
      ELSEIF (TYPCHP.EQ.'DEPENT') THEN
        TYPEST = 18
      ELSEIF (TYPCHP.EQ.'VITENT') THEN
        TYPEST = 20
      ELSEIF (TYPCHP.EQ.'ACCENT') THEN
        TYPEST = 22
      ELSEIF (TYPCHP.EQ.'STRPLU') THEN
        TYPEST = 24
      ELSEIF (TYPCHP.EQ.'STRMOI') THEN
        TYPEST = 25
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- NOM DU CHAMP
C
      CALL PSNSLE(INPSCO,NRPASE,TYPEST,NOMCHZ)

C
      CALL JEDEMA()
C
      END
