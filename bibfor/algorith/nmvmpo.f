      SUBROUTINE NMVMPO(FAMI, NPG, OPTION, NOMTE, NC,
     &                  XL, ICODMA, SECT, CARCRI, COMPOR,
     &                  U, DU, CONTM, VARIM, HOEL,
     &                  HOTA, D1B, WORK, RG0, VARIP,
     &                  CONTP, FL, KLV)

