      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*24  DEFICO
      INTEGER       POSMAE
      CHARACTER*(*) QUESTZ
      INTEGER       IREP
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES - UTILITAIRE)
C
C REPOND A UNE QUESTION SUR UNE OPTION/CARACTERISTIQUE DU CONTACT
C VARIABLE SUIVANT LA MAILLE
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD POUR LA DEFINITION DU CONTACT
C IN  POSMAE : POSITION DE LA MAILLE ESCLAVE DANS LES SD CONTACT
C IN  QUESTI : QUESTION POSEE
C OUT IREP   : VALEUR
C
C
C
C
      INTEGER      CFMMVD,ZMAES,ZTYPM   
      CHARACTER*24 MAESCL,TYPEMA
      INTEGER      JMAESC,JTYPMA
      CHARACTER*24 QUESTI
      INTEGER      INDMAE,POSMA2
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C 
C --- INITIALISATIONS
C
      IREP   = 0
      QUESTI = QUESTZ
C   
C --- ACCES AUX SDS
C 
      TYPEMA = DEFICO(1:16)//'.TYPEMA' 
      MAESCL = DEFICO(1:16)//'.MAESCL'
C
      ZMAES  = CFMMVD('ZMAES')
      ZTYPM  = CFMMVD('ZTYPM')
C
      CALL JEVEUO(TYPEMA,'L',JTYPMA)
      CALL JEVEUO(MAESCL,'L',JMAESC)
C
C --- INDICE
C
      INDMAE = ZI(JTYPMA+ZTYPM*(POSMAE-1)+2-1)
      POSMA2 = ZI(JMAESC+ZMAES*(INDMAE-1)+1-1)
      CALL ASSERT(POSMA2.EQ.POSMAE)
C
C --- QUESTION
C
      IF (QUESTI.EQ.'IZONE') THEN
        IREP   = ZI(JMAESC+ZMAES*(INDMAE-1)+2-1)
      ELSEIF (QUESTI.EQ.'NPTM') THEN
        IREP   = ZI(JMAESC+ZMAES*(INDMAE-1)+3-1)
      ELSEIF (QUESTI.EQ.'NDEXFR') THEN
        IREP   = ZI(JMAESC+ZMAES*(INDMAE-1)+4-1)        
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
      CALL JEDEMA()
      END
