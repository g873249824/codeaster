C
C IN  LISCHA : SD LISTE DES CHARGES
C IN  GENCHA : GENRE DE LA CHARGE (VOIR LISDEF)
C OUT NOMLIS : LISTE DES INDEX DES CHARGES
C OUT NBCH   : LONGUEUR DE NOMLIS
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      NBCHAR
      INTEGER      IBID,IPOSIT
      CHARACTER*8  K8BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      NBCH   = 0
      CALL LISNNB(LISCHA,NBCHAR)
C
      IF (NBCHAR.NE.0) THEN
C
C ----- POSITION DE L'ENTIER CODE POUR CE GENRE DE CHARGE
C
        CALL LISDEF('POEC',GENCHZ,IBID  ,K8BID ,IPOSIT)
C
C ----- LISTE DES INDEX DE CHARGE POUR CE GENRE
C
        CALL LISDEF('IDNS',NOMLIS,IPOSIT,K8BID ,NBCH  )
      ENDIF
C
      CALL JEDEMA()
      END
