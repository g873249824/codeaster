      SUBROUTINE MERESO ( RESULT,MODELE, MATE, CARELE, FOMULT, LISCHA,
     &                    ITPS, PARTPS,
     &                    NUMEDD, VECASS,
     &                    ASSMAT, SOLVEU, MATASS, MAPREC,
     &                    BASE, COMPOR )
