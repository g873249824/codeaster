       SUBROUTINE XMVCO2(NDIM  ,NNO   ,NNOL  ,NNOS  ,LAMB  ,
     &                   AM    ,DELTA ,PLA   ,LACT  ,NFH   ,
     &                   DDLS  ,DDLM  ,NFISS ,IFISS ,JHEAFA,
     &                   IFA   ,NCOMPH,JFISNO,JAC   ,FFC   ,
     &                   FFP   ,SINGU ,R     ,RR    ,VTMP  ,
     &                   P)
C
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER     NDIM,NNO,NNOL
      INTEGER     NFH,DDLS,PLA(27),LACT(8)
      INTEGER     SINGU
      REAL*8      VTMP(400),DELTA(6)
      REAL*8      FFP(27),JAC
      REAL*8      RR,FFC(8)

