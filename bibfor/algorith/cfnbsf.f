      IMPLICIT     NONE    
      INCLUDE 'jeveux.h'
      CHARACTER*24 DEFICO
      CHARACTER*4  TYPENT
      INTEGER      ISURF,JDEC
      INTEGER      NBENT
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES - UTILITAIRE)
C
C ACCES AUX NOEUDS/MAILLES D'UNE ZONE DONNEE
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  TYPENT : TYPE D'ENTITE
C               NOEU  NBRE NOEUDS ATTACHES A LA SURFACE
C               MAIL  NBRE MAILLES ATTACHEES A LA SURFACE
C IN  ISURF  : NUMERO DE LA SURFACE
C OUT NBENT  : NOMBRE D'ENTITES
C OUT JDEC   : DECALAGE DANS LES VECTEURS POUR LE PREMIER DE LA SURFACE
C               NOEU  DEFICO(1:16)//'.NOEUCO'
C               MAIL  DEFICO(1:16)//'.MAILCO'
C
C
C
C
      CHARACTER*24 PSURNO,PSURMA
      INTEGER      JSUNO,JSUMA
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C      
C --- RECUPERATION DE QUELQUES DONNEES      
C
      PSURNO = DEFICO(1:16)//'.PSUNOCO'
      PSURMA = DEFICO(1:16)//'.PSUMACO'
      CALL JEVEUO(PSURNO,'L',JSUNO )
      CALL JEVEUO(PSURMA,'L',JSUMA )     
C
C --- INITIALISATIONS
C
      IF (TYPENT.EQ.'NOEU') THEN
        NBENT  = ZI(JSUNO+ISURF) - ZI(JSUNO+ISURF-1)
        JDEC   = ZI(JSUNO+ISURF-1)
      ELSEIF (TYPENT.EQ.'MAIL') THEN
        NBENT  = ZI(JSUMA+ISURF) - ZI(JSUMA+ISURF-1)
        JDEC   = ZI(JSUMA+ISURF-1)       
      ELSE    
        CALL ASSERT(.FALSE.)
      ENDIF
C
      CALL JEDEMA()
C
      END
