C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE PROIX J.M.PROIX
      IMPLICIT NONE
C ----------------------------------------------------------------------
C
C     COMMANDE : MODI_REPERE
C
C ----------------------------------------------------------------------
C
      INCLUDE 'jeveux.h'

      INTEGER      N0    , NBORDR, IRET  , NOCC  , I , J  , NP, IORDR
      INTEGER      N1    , NBCMP , IORD  , IOC , IBID, NC
      INTEGER      JORDR , NBNOSY, JPA, IADIN, IADOU
      INTEGER      NBPARA, NBAC  , NBPA  , IFM   , NIV, NNCP
      REAL*8      PREC
      REAL*8      LCOER(2)
      COMPLEX*16   LCOEC(2)
      CHARACTER*8  CRIT  , K8B   , TYCH  , NOMMA , MODELE
      CHARACTER*8  CARELE, K8BID
      CHARACTER*16 CONCEP, NOMCMD, OPTION, TYSD, TYPE, REPERE
      CHARACTER*19 KNUM  , RESUOU, KBID , RESUIN
      CHARACTER*19 CHAMS1,CHAMS0, CHAFUS,CHS(2),LIGREL
      CHARACTER*24 NOMPAR, CHAMP0, CHAMP1
      CHARACTER*24 VALK(2)
      INTEGER      IARG

      LOGICAL     LREUSE,LCUMU(2),LCOC(2)

      DATA LCUMU/.FALSE.,.FALSE./
      DATA LCOC/.FALSE.,.FALSE./
      DATA LCOER/1.D0,1.D0/
C ---------------------------------------------------------------------
      CALL JEMARQ()
C
C ----- RECUPERATION DU NOM DE LA COMMANDE -----
C
      CALL INFMAJ
      CALL INFNIV(IFM, NIV)
C
      CALL GETRES ( RESUOU, CONCEP, NOMCMD )
      CALL GETVID ( ' ', 'RESULTAT', 1,IARG,1, RESUIN, N0 )
C
      CALL JELIRA ( RESUIN//'.DESC', 'NOMMAX', NBNOSY, K8B )
      IF ( NBNOSY .EQ. 0 ) GOTO 9999
C
C LE CONCEPT EST REENTRANT SI REPERE = 
C            'COQUE_INTR_UTIL' OU 'COQUE_UTIL_INTR'
C
C DANS CE CAS ON CREE UNE SD RESULTAT TEMPORAIRE POUR LES CALCULS
C ET ENSUITE ON SURCHARGE RESUIN PAR LES CHAMPS MODIFIES 
C STOCKES DANS RESUOU 
C
      LREUSE = .FALSE.
      IF(RESUIN.EQ.RESUOU) THEN
        LREUSE = .TRUE.
        RESUOU ='MODIREPE'
      ENDIF
C
      CALL GETTCO ( RESUIN, TYSD )
C
C ----- RECUPERATION DU NOMBRE DE CHAMPS SPECIFIER -----
C
      CALL GETFAC ( 'MODI_CHAM', NOCC )
C
C ----- DEFINITION DU REPERE UTILISE -----
C
      CALL GETVTX ( ' ', 'REPERE', 1,IARG,1, REPERE, I )
C
C ----- RECUPERATION DES NUMEROS D'ORDRE DE LA STRUCTURE DE
C ----- DONNEES DE TYPE RESULTAT RESU A PARTIR DES VARIABLES
C ----- D'ACCES UTILISATEUR 'NUME_ORDRE','FREQ','INST','NOEUD_CMP'
C ----- (VARIABLE D'ACCES 'TOUT_ORDRE' PAR DEFAUT)
C
      KNUM = '&&OP0191.NUME_ORDRE'
      CALL GETVR8 ( ' ', 'PRECISION', 1,IARG,1, PREC, NP )
      CALL GETVTX ( ' ', 'CRITERE'  , 1,IARG,1, CRIT, NC )
      CALL RSUTNU ( RESUIN, ' ', 1, KNUM, NBORDR, PREC, CRIT, IRET )
      IF (IRET.EQ.10) THEN
         CALL U2MESK('F','CALCULEL4_8',1,RESUIN)
      ENDIF
      IF (IRET.NE.0) THEN
         CALL U2MESS('F','ALGORITH3_41')
      ENDIF
      CALL JEVEUO ( KNUM, 'L', JORDR )
C
      CALL RSCRSD('G', RESUOU, TYSD, NBORDR )
C
      DO 10 IOC = 1 , NOCC
C
         CALL GETVTX ('MODI_CHAM','NOM_CHAM' ,IOC,IARG,1,OPTION,N0)
         CALL GETVTX ('MODI_CHAM','TYPE_CHAM',IOC,IARG,1,TYPE  ,N0)
         CALL GETVTX ('MODI_CHAM','NOM_CMP'  ,IOC,IARG,0,K8B   ,N1)
         NBCMP  = - N1
C
         DO 12 IORD = 1 , NBORDR
            CALL JEMARQ()
            CALL JERECU('V')
            IORDR = ZI(JORDR-1+IORD)
C
            CALL RSEXCH('F',RESUIN, OPTION, IORDR, CHAMP0, IRET)
            CALL DISMOI('F','NOM_MAILLA',CHAMP0(1:19),'CHAMP',
     &                   IBID, NOMMA, IRET)
            CALL DISMOI('C','TYPE_CHAMP',CHAMP0,'CHAMP',IBID,
     &                   TYCH, IRET)
C
            CALL RSEXCH(' ',RESUOU, OPTION, IORDR, CHAMP1, IRET )
C           CHAMP1 SERA ENSUITE RECREE SUR LA BASE GLOBALE
            CALL COPISD ( 'CHAMP_GD', 'V', CHAMP0, CHAMP1 )
C
C ----- RECUPERATION DU MODELE ASSOCIE AU CHAMP
            CALL RSLESD(RESUIN(1:8), IORDR, MODELE, K8BID,CARELE,
     &                  KBID, IBID)
C
C ----- RECUPERATION DE LA NATURE DES CHAMPS
C ----- (CHAM_NO OU CHAM_ELEM)
C
            IF (TYCH(1:4).EQ.'NOEU') THEN
               CALL CHRPNO( CHAMP1, REPERE, NBCMP, IOC, TYPE )
            ELSE IF (TYCH(1:2).EQ.'EL') THEN
               CALL CHRPEL( CHAMP1, REPERE, NBCMP, IOC, TYPE, OPTION,
     &                      MODELE, CARELE, CHAMP0 )
            ELSE
                VALK(1) = TYCH
                VALK(2) = CHAMP1
                CALL U2MESK('A','ALGORITH9_69', 2 ,VALK)
            ENDIF
C
            CALL RSNOCH ( RESUOU,OPTION,IORDR)
C
            CALL JEDEMA()
 12      CONTINUE
 10   CONTINUE
C
      NOMPAR = '&&OP0191.NOMS_PARA'
      CALL RSNOPA ( RESUIN, 2, NOMPAR, NBAC, NBPA )
      NBPARA = NBAC + NBPA
      CALL JEVEUO ( NOMPAR, 'L', JPA )
      DO 20 IORD = 1,NBORDR
         IORDR = ZI(JORDR-1+IORD)
         DO 22 J = 1,NBPARA
            CALL RSADPA(RESUIN,'L',1,ZK16(JPA+J-1),IORDR,1,IADIN,TYPE)
            CALL RSADPA(RESUOU,'E',1,ZK16(JPA+J-1),IORDR,1,IADOU,TYPE)
            IF (TYPE(1:1).EQ.'I') THEN
               ZI(IADOU) = ZI(IADIN)
            ELSE IF (TYPE(1:1).EQ.'R') THEN
               ZR(IADOU) = ZR(IADIN)
            ELSE IF (TYPE(1:1).EQ.'C') THEN
               ZC(IADOU) = ZC(IADIN)
            ELSE IF (TYPE(1:3).EQ.'K80') THEN
               ZK80(IADOU) = ZK80(IADIN)
            ELSE IF (TYPE(1:3).EQ.'K32') THEN
               ZK32(IADOU) = ZK32(IADIN)
            ELSE IF (TYPE(1:3).EQ.'K24') THEN
               ZK24(IADOU) = ZK24(IADIN)
            ELSE IF (TYPE(1:3).EQ.'K16') THEN
               ZK16(IADOU) = ZK16(IADIN)
            ELSE IF (TYPE(1:2).EQ.'K8') THEN
               ZK8(IADOU) = ZK8(IADIN)
            END IF
 22      CONTINUE
 20   CONTINUE

      CALL TITRE

      IF(NIV.EQ.2) CALL RSINFO(RESUOU,IFM)

 9999 CONTINUE

C     -- CREATION DE L'OBJET .REFD SI NECESSAIRE:
C     -------------------------------------------
      CALL AJREFD(RESUIN,RESUOU,'COPIE')

C
C TRAITEMENT DU CAS OU IL Y A REENTRANCE 
C UTILISE SI LE MOT CLE REPERE VAUT
C    'COQUE_INTR_UTIL' OU 'COQUE_UTIL_INTR'
C
      IF(LREUSE) THEN
C
        DO 25 IOC = 1 , NOCC
C
         CALL GETVTX ('MODI_CHAM','NOM_CHAM' ,IOC,IARG,1,OPTION,N0)
C
         DO 30 IORD = 1 , NBORDR
             CALL JEMARQ()
             CALL JERECU('V')
             IORDR = ZI(JORDR-1+IORD)
C
             CALL RSEXCH('F',RESUIN, OPTION, IORDR, CHAMP0, IRET)
             CALL RSEXCH(' ',RESUOU, OPTION, IORDR, CHAMP1, IRET )

             CHAMS0='&&CHRPEL.CHAMS0'
             CHAMS1='&&CHRPEL.CHAMS1'
             CHAFUS='&&CHRPEL.CHAFUS'
             CHS(1) =CHAMS0
             CHS(2) =CHAMS1 
             CALL CELCES(CHAMP0,'V',CHAMS0)
             CALL CELCES(CHAMP1,'V',CHAMS1)
             CALL CESFUS(2,CHS,LCUMU,LCOER,LCOEC,LCOC,'V',CHAFUS)
             CALL DISMOI( 'F', 'NOM_LIGREL', CHAMP0, 'CHAM_ELEM',
     &                    IBID, LIGREL, IBID )
             CALL CESCEL(CHAFUS,LIGREL,OPTION,' ','NAN',NNCP,'G',
     &                    CHAMP0,'F',IBID)
             CALL DETRSD('CHAMP',CHAMP1)
             CALL JEDEMA()

 30       CONTINUE
 25     CONTINUE

        CALL DETRSD('CHAMP',CHAMS0)
        CALL DETRSD('CHAMP',CHAMS1)
        CALL DETRSD('CHAMP',CHAFUS)
        CALL DETRSD('RESULTAT',RESUOU)
      ENDIF

      CALL JEDEMA( )
C
      END
