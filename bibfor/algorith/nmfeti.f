      CHARACTER*24 NUMEDD
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (UTILITAIRE)
C
C PREPARATION FETI
C      
C ----------------------------------------------------------------------
C
C
C IN  IFM    : NUMERO DU FICHIER LOGIQUE MESSAGE
C IN  NUMEDD : NUME_DDL
C OUT NIVMPI : NIVEAU IMPRESSION MPI
C OUT NBPROC : NOMBRE DE PROCESSEURS
C OUT LFETI  : .TRUE. SI SOLVEUR FETI
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      JREFN,IINF,IBID
      REAL*8       R8BID
      CHARACTER*24 KMET,INFOFE,K24BID     
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C    
      NBPROC = 1
      CALL JEVEUO(NUMEDD(1:14)//'.NUME.REFN','L',JREFN)
      KMET   = ZK24(JREFN+2)
      IF (KMET(1:4).EQ.'FETI') THEN
        LFETI  = .TRUE.
        CALL JEVEUO('&FETI.FINF','L',IINF)
        INFOFE = ZK24(IINF)
        IF (INFOFE(10:10).EQ.'T') THEN
          NIVMPI=2
        ELSE
          NIVMPI=1
        ENDIF
        CALL FETMPI(3     ,IBID  ,IFM   ,NIVMPI,IBID  ,
     &              NBPROC,K24BID,K24BID,K24BID,R8BID)
      ELSE
        LFETI=.FALSE.
      ENDIF
C
      CALL JEDEMA()
      END
