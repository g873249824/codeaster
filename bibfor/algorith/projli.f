      SUBROUTINE PROJLI(NDIM,
     &                  COORDA,COORDB,COORDP,
     &                  MOYEN,TANGDF,
     &                  NORM,TANG,
     &                  COORDM,LAMBDA,DEBORD,
     &                  OLDJEU,JEU)

