      IMPLICIT NONE 
      INTEGER      NUMINS
      CHARACTER*8  NOMA
      CHARACTER*19 VALINC(*),VEASSE(*)
      CHARACTER*24 RESOCO
      CHARACTER*19 SDDYNA,SDDISC,SDOBSE     
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C REALISER UNE OBSERVATION 
C      
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  SDOBSE : SD OBSERVATION
C IN  SDDYNA : SD DYNAMIQUE
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  VALINC : VARIABLE CHAPEAU POUR INCREMENTS VARIABLES
C IN  VEASSE : VARIABLE CHAPEAU POUR NOM DES VECT_ASSE
C IN  NUMINS : NUMERO DE L'INSTANT COURANT
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C      
C ----------------------------------------------------------------------
C 
      REAL*8       DIINST,INST
      LOGICAL      LOBSV
      CHARACTER*19 DEPPLU,SIGPLU,VARPLU,ACCPLU,VITPLU
      CHARACTER*19 DEPENT,VITENT,ACCENT,K19BLA,CNFINT
      LOGICAL      NDYNLO,LMUAP   
C      
C ----------------------------------------------------------------------
C 
C
C --- FONCTIONNALITES ACTIVEES
C
      LMUAP  = NDYNLO(SDDYNA,'MULTI_APPUI')
C
C --- INITIALISATIONS
C
      DEPENT = ' '
      VITENT = ' '
      ACCENT = ' '
      K19BLA = ' '
      LOBSV  = .FALSE.
C
C --- DECOMPACTION DES VARIABLES CHAPEAUX
C
      CALL NMCHEX(VALINC,'VALINC','DEPPLU',DEPPLU)
      CALL NMCHEX(VALINC,'VALINC','VITPLU',VITPLU)
      CALL NMCHEX(VALINC,'VALINC','ACCPLU',ACCPLU)
      CALL NMCHEX(VALINC,'VALINC','SIGPLU',SIGPLU)
      CALL NMCHEX(VALINC,'VALINC','VARPLU',VARPLU) 
      CALL NMCHEX(VEASSE,'VEASSE','CNFINT',CNFINT)
      IF (LMUAP) THEN
        CALL NDYNKK(SDDYNA,'DEPENT',DEPENT)
        CALL NDYNKK(SDDYNA,'VITENT',VITENT)
        CALL NDYNKK(SDDYNA,'ACCENT',ACCENT) 
      ENDIF
C
C --- TEMPS COURANT
C
      INST   = DIINST(SDDISC,NUMINS)
C
C --- DOIT-ON FAIRE UNE OBSERVATION  ?
C
      CALL LOBS  (SDOBSE,NUMINS,INST  ,LOBSV )
C
C --- AU MOINS UNE OBSERVATION
C
      IF (LOBSV) THEN
        CALL NMOBSE(NOMA  ,SDOBSE,RESOCO,INST  ,DEPPLU,
     &              VITPLU,ACCPLU,SIGPLU,VARPLU,DEPENT,
     &              VITENT,ACCENT,CNFINT,K19BLA)
      ENDIF
C
      END
