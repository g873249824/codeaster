      CHARACTER*8 RESU,TYPCH
      CHARACTER*19 CHACOU
      CHARACTER*24 CHAVAL
C
C-----------------------------------------------------------------------
      DATA PGC/'DCAPNO'/
C-----------------------------------------------------------------------
C
      CALL JEMARQ()
      CHAVAL = CHAVAZ
      RESU  = RESUZ
      TYPCH = TYPCHZ
      CALL RSEXCH(RESU,TYPCH,IORD,CHACOU,IER)
      IF (IER.NE.0) THEN
        CALL UTDEBM('F',PGC,' PROBLEME RECUPERATION CHAMNO')
        CALL UTIMPK('L','CONCEPT RESULTAT: ',1,RESU)
        CALL UTIMPI('L','NUMERO ORDRE: ',1,IORD)
        CALL UTFINM
      END IF

      CHAVAL = CHACOU//'.VALE'
      CALL JEVEUO(CHAVAL,'L',IAD)
C
      CHAVAZ = CHAVAL
      GOTO 9999

 9999 CONTINUE
      CALL JEDEMA()
      END
