      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*19 DDEPLA,DEPPR1
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- DECOMPACTION VARIABLES CHAPEAUX
C
      CALL NMCHEX(SOLALG,'SOLALG','DDEPLA',DDEPLA)
      CALL NMCHEX(SOLALG,'SOLALG','DEPPR1',DEPPR1)
C
C --- RECOPIE DE L'INC. PREDICTION EN INC. SOLUTION
C
      CALL COPISD('CHAMP_GD','V',DEPPR1,DDEPLA)
C
C --- RECHERCHE LINEAIRE DANS LA DIRECTION DE DESCENTE
C
      CALL NMRELP(MODELE,NUMEDD,MATE  ,CARELE,COMREF,
     &            COMPOR,LISCHA,CARCRI,FONACT,ITERAT,
     &            SDSTAT,SDNUME,SDDYNA,PARMET,METHOD,
     &            DEFICO,VALINC,SOLALG,VEELEM,VEASSE,
     &            SDTIME,CONV  ,LDCCVG)
C
      CALL JEDEMA()

      END
