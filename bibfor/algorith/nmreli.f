     &                  COMPOR,LISCHA,CARCRI,FONACT,ITERAT,
     &                  SDSTAT,SDNUME,SDDYNA,PARMET,METHOD,
     &                  DEFICO,VALINC,SOLALG,VEELEM,VEASSE,
     &                  SDTIME,CONV  ,LDCCVG)
C
