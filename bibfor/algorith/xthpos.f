C ======================================================================
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*24 RESULZ,MODELE
C ----------------------------------------------------------------------
C
C THER_LINEAIRE + XFEM : CALCUL ET STOCKAGE DE L'OPTION 'TEMP_ELGA'
C
C IN  RESULZ  : NOM DU RESULTAT
C IN  MODELE  : NOM DU MODELE
C
C ----------------------------------------------------------------------
      INTEGER      JORD,NBORDR,IOR,IORD,IRET
      CHARACTER*1  K1BID
      CHARACTER*8  RESULT
      CHARACTER*19 LIGRMO,CHTN,CHTPG,CELTMP
      CHARACTER*24 ORDR
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      RESULT=RESULZ(1:8)
      LIGRMO=MODELE(1:8)//'.MODELE'
      CELTMP='&&XTELGA.CELTMP'
C
      ORDR=RESULT//'           .ORDR'
      CALL JEVEUO(ORDR,'L',JORD)
      CALL JELIRA(ORDR,'LONUTI',NBORDR,K1BID)
C
C     ------------------------------------------------------------------
C     - BOUCLE SUR LES NBORDR NUMEROS D'ORDRE
C     ------------------------------------------------------------------
C
      DO 1000 IOR=1,NBORDR
C
        IORD=ZI(JORD-1+IOR)
C
C       SI LE CHAMP 'TEMP' N'EXISTE PAS DANS RESULT, ON PASSE...
        CALL RSEXCH(' ',RESULT,'TEMP',IORD,CHTN,IRET)
        IF (IRET.GT.0) GOTO 1000
C
C       ALLOCATION DU CHAM_ELEM TEMPORAIRE : CELTMP
C       RQ : LIGRMO CONTIENT TOUS LES EF DU MODELE, MAIS SEULS LES EF 
C       ---  X-FEM SAVENT CALCULER L'OPTION 'TEMP_ELGA' -> CELTMP N'EST
C            DONC DEFINI QUE SUR L'ENSEMBLE DES EF X-FEM
        CALL ALCHML(LIGRMO,'TEMP_ELGA','PTEMPPG','V',CELTMP,IRET,' ')
        CALL ASSERT(IRET.EQ.0)
C
C       CALCUL DE L'OPTION 'TEMP_ELGA' ET ECRITURE DANS CELTMP
        CALL XTHPOC(MODELE,CHTN,CELTMP)
C
C       RECUPERATION DU NOM DU CHAMP A ECRIRE : CHTPG
        CALL RSEXCH(' ',RESULT,'TEMP_ELGA',IORD,CHTPG,IRET)
        CALL ASSERT(IRET.EQ.100)
C       COPIE : CELTMP (BASE 'V') -> CHTPG (BASE 'G')
        CALL COPISD('CHAMP','G',CELTMP,CHTPG)
C       STOCKAGE DANS LA SD RESULTAT
        CALL RSNOCH(RESULT,'TEMP_ELGA',IORD)
C
C       DESTRUCTION DE CELTMP
        CALL DETRSD('CHAM_ELEM',CELTMP)
C
 1000 CONTINUE
C
      CALL JEDEMA()
C
      END
