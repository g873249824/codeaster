C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C TOLE CRP_20
C
      IMPLICIT      NONE
      INTEGER       DERNIE,NUMFIN
      REAL*8        INSTAN
      CHARACTER*8   RESULT,MAILLA
      CHARACTER*19  SDDISC,LISINS
      LOGICAL       EVOL,LOSTAT
      CHARACTER*19  SDOBSE
      CHARACTER*24  VTEMP,MODELE
C
C ----------------------------------------------------------------------
C
C ROUTINE THER_NON_LINE THER_LINEAIRE (STRUCTURES DE DONNES)
C
C CREATION SD DISCRETISATION, ARCHIVAGE ET OBSERVATION
C
C ----------------------------------------------------------------------
C
C
C IN  RESULT : NOM UTILISATEUR DU RESULTAT
C IN  VTEMP  : CHAMP DE TEMPERATURE
C IN  MAILLA : NOM DU MAILLAGE
C IN  INSTAN : PREMIER INSTANT DE CALCUL
C IN  LOSTAT : INDICATEUR SI CALCUL STATIONNAIRE PREALABLE OU PAS
C OUT SDDISC : SD DISCRETISATION
C OUT LISINS : NOM DE LA LISTE D'INSTANTS
C OUT SDOBSE : NOM DE LA SD POUR OBSERVATION
C OUT DERNIE : DERNIER NUMERO ARCHIVE (OU 0 SI NON REENTRANT)
C
C ----------------------------------------------------------------------
C
      INTEGER      N1
      REAL*8       DELMIN
      CHARACTER*8  K8BID,NOMO
      CHARACTER*16 NOMCMD,K16BID
      CHARACTER*24 TPSDIT
C
C ----------------------------------------------------------------------
C
      CALL GETVID('INCREMENT','LIST_INST',1,1,1,LISINS,N1)
      IF (N1.EQ.0) THEN
        IF (.NOT.LOSTAT) THEN
            CALL U2MESS('F','DISCRETISATION_8')
        ENDIF
        LISINS = ' '
        DERNIE = 0
        GOTO 999
      ENDIF
      NOMO  = MODELE(1:8)
C
C --- COMMANDE APPELANTE
C      
      CALL GETRES(K8BID ,K16BID,NOMCMD) 
C
C --- CREATION SD DISCRETISATION
C
      CALL NMCRLI(INSTAN,LISINS,SDDISC,DELMIN,NOMCMD)
C
C --- CREATION SD ARCHIVAGE
C 
      CALL NTCRAR(RESULT,SDDISC,DERNIE,DELMIN,NOMCMD)
C     
      IF (NOMCMD.EQ.'THER_LINEAIRE') THEN
        GOTO 999
      ENDIF      
C
C --- CREATION SD OBSERVATION
C
      CALL NTCROB(MAILLA,NOMO  ,RESULT,VTEMP ,SDOBSE)
C     
  999 CONTINUE
C
C --- DONNEES EN SORTIE
C      
      IF (N1.NE.0) THEN
        TPSDIT = SDDISC(1:19)// '.DITR'
        CALL JELIRA(TPSDIT,'LONMAX',NUMFIN,K8BID)
        NUMFIN = NUMFIN-1
        EVOL   = .TRUE.
      ELSE
        NUMFIN = 1
        EVOL   = .FALSE.
      ENDIF   

      END
