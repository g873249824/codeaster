      IMPLICIT NONE

      INTEGER      NDIM,NNO
      REAL*8       W,DFF1(NNO,NDIM)
      REAL*8       KBP(NDIM,NNO)
C-----------------------------------------------------------------------
C     BUT:  CALCUL LE TERME DE COUPLAGE KBP
C     ON UTILISE UNE FORMULATION AVEC UN SEULE POINT DE GAUSS
C     SITUE AU BARYCENTRE DE L ELEMENT ET DONT LE POIDS EST EGAL
C-----------------------------------------------------------------------
C IN  NDIM   : DIMENSION DE L'ESPACE
C IN  NNO    : NOMBRE DE NOEUDS DE L'ELEMENT
C IN  W      : POIDS DU POINT DE GAUSS
C IN  DFF1   : DERIVEE DES FONCTIONS DE FORME ELEMENT DE REFERENCE
C OUT KBP    : MATRICE KBP
C-----------------------------------------------------------------------

      INTEGER      IA,NA
      REAL*8       PBULLE
C-----------------------------------------------------------------------


C - INITIALISATION
      CALL R8INIR(NNO*NDIM,0.D0,KBP,1)

      IF(NDIM .EQ. 3)THEN
        PBULLE = 4.D0
      ELSEIF(NDIM .EQ. 2)THEN
        PBULLE = 3.D0
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF

C - TERME KBP
C - BOUCLE SUR LES NOEUDS DE PRESSION
      DO 100 NA = 1, NNO
C - BOUCLE SUR LA DIMENSION
        DO 99 IA = 1, NDIM
          KBP(IA,NA) = - W/PBULLE*DFF1(NA,IA)
 99     CONTINUE
 100  CONTINUE

      END
