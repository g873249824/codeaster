C IN  RESULT : NOM UTILISATEUR DU RESULTAT DE MECA_NON_LINE
C IN  MODELE : MODELE MECANIQUE
C IN  LISCHA : SD L_CHARGE
C IN  SOLVEU : NOM DU SOLVEUR DE NEWTON
C IN  SDTIME : SD TIMER
C IN  NUMEDD : NOM DE LA NUMEROTATION
C IN  COMPOR : NOM DE LA CARTE COMPOR         
C IN  SDNUME : NOM DE LA SD NUMEROTATION
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24 SDNURO,SDNUMO
C
C ----------------------------------------------------------------------
C      
      CALL JEMARQ()
C
C --- CREATION PROFIL NUME_DDL
C
      CALL NMPROF(MODELE,RESULT,LISCHA,SOLVEU,SDTIME,
     &            NUMEDD)
C
C --- REPERAGE DDL GRANDES ROTATIONS
C
      SDNURO = SDNUME(1:19)//'.NDRO'          
      CALL NUROTA(NUMEDD,COMPOR,SDNURO)
C
C --- REPERAGE AUTRES DDLS
C
      SDNUMO = SDNUME(1:19)//'.ENDO'
      CALL NUENDO(NUMEDD,COMPOR,SDNUMO)
C
C
C      
      CALL JEDEMA()      
C
      END
