      CHARACTER*8  RESULT
      CHARACTER*19 LISCHA,SOLVEU
      CHARACTER*19 SDNUME
      LOGICAL      LCONT
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (INITIALISATION)
C
C CREATION DE LA NUMEROTATION
C
C ----------------------------------------------------------------------
C
C
C IN  RESULT : NOM UTILISATEUR DU RESULTAT DE MECA_NON_LINE
C IN  DEFICO : NOM DE LA SD DEFINITION DU CONTACT
C IN  LCONT  : IL Y A DU CONTACT
C IN  MODELE : MODELE MECANIQUE
C IN  LISCHA : LISTE DES CHARGES
C IN  SOLVEU : NOM DU SOLVEUR
C IN  NUMEDD : NOM DE LA NUMEROTATION
C IN  COMPOR : NOM DE LA CARTE COMPOR
C IN  SDNUME : NOM DE LA SD NUMEROTATION
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24 SDNURO,SDNUMO,SDNUCO
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- CREATION PROFIL NUME_DDL
C
      CALL NMPROF(MODELE,RESULT,LISCHA,SOLVEU,NUMEDD)
C
C --- REPERAGE DDL GRANDES ROTATIONS
C
      SDNURO = SDNUME(1:19)//'.NDRO'
      CALL NUROTA(NUMEDD,COMPOR,SDNURO)
C
C --- REPERAGE DDL ENDOMMAGEMENT AUX NOEUDS
C
      SDNUMO = SDNUME(1:19)//'.ENDO'
      CALL NUENDO(NUMEDD,SDNUMO)
C
C --- REPERAGE DDL LAGRANGE DE CONTACT
C
      SDNUCO = SDNUME(1:19)//'.NUCO'
      CALL NUNUCO(NUMEDD,DEFICO,LCONT ,SDNUCO)
C
      CALL JEDEMA()
C
      END
