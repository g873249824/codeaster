      CHARACTER*8  SDLIST
      CHARACTER*16 K16BID
      CHARACTER*16 METLIS
      REAL*8       DTMIN
      INTEGER      IFM,NIV
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFMAJ()
      CALL INFNIV(IFM,NIV)
C
C --- NOM DU CONCEPT
C
      CALL GETRES(SDLIST,K16BID,K16BID)
C
C --- LECTURE DU TYPE DE CONSTRUCTION DE LA LISTE D'INSTANTS
C
      CALL DFLLTY(SDLIST,METLIS,DTMIN )
C
C --- LECTURE DES ECHECS
C
      CALL DFLLEC(SDLIST,DTMIN )
C
C --- ADAPTATION SEULEMENT SI METHODE AUTO
C
      IF (METLIS.EQ.'AUTO') THEN
        CALL DFLLAD(SDLIST)
      ENDIF
C
C --- DEBUG
C
      IF (NIV.GE.2) THEN
        CALL DFLLDB(SDLIST,IFM  )
      ENDIF
C
      CALL JEDEMA()
      END
