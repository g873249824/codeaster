C                   .ECHE.EVENK    : (K16) LEEVK*NOCC DE ECHE
C                   .ECHE.SUBDR    : (R)   LESUR*NOCC DE ECHE
C
C                   .ADAP.EVENR    : (R)   LAEVR*NOCC DE ADAP
C                   .ADAP.EVENK    : (K8)  LAEVK*NOCC DE ADAP
