      SUBROUTINE CFSUPM(NOMA,JAPPAR,JNOCO,JAPMEM,
     &                  ISUPP,POSSUP,TYPSUP,NESMAX)

C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 07/10/2004   AUTEUR MABBAS M.ABBAS 
