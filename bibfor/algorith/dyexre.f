C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
      IMPLICIT      NONE
      INCLUDE 'jeveux.h'
      CHARACTER*24  EXRECO,EXRESU
      INTEGER       NBEXRE
      CHARACTER*14  NUMDDL
      REAL*8        FREQ
      INTEGER       J2ND
C
C ----------------------------------------------------------------------
C
C DYNA_LINE_HARM
C
C APPLICATION EXCIT_RESU
C
C ----------------------------------------------------------------------
C
C
C IN  NBEXRE : NOMBRE DE EXCIT_RESU
C IN  NUMDDL : NOM DU NUME_DDL
C IN  EXRECO : LISTE DES COEFFICIENTS DANS EXCIT_RESU
C IN  EXRESU : LISTE DES RESULTATS DANS EXCIT_RESU
C IN  NUMDDL : NOM DU NUME_DDL
C IN  FREQ   : VALEUR DE LA FREQUENCE
C IN  J2ND   : ADRESSE DU VECTEUR ASSEMBLE SECOND MEMBRE
C
C
C
C
      CHARACTER*19 CHAMNO,CHAMN2
      REAL*8       PREC,EPS0
      INTEGER      IEQ,NEQ,IRESU,IBID,IFREQ,IRET
      CHARACTER*8  K8BID
      INTEGER      JLCCRE,JLRESU,JLVALE
      COMPLEX*16   C16BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      CHAMN2  = '&&DYEXRE.CHAMN2'
      CALL DETRSD('CHAM_NO',CHAMN2)
      CALL VTCREB(CHAMN2,NUMDDL,'V','C',NEQ)
      PREC  = 1.D-6
      EPS0  = 1.D-12
      CALL JEVEUO(EXRECO,'L',JLCCRE)
      CALL JEVEUO(EXRESU,'L',JLRESU)
      DO 10 IRESU = 1, NBEXRE
         IF (ABS(FREQ).GT.EPS0) THEN
           CALL RSORAC(ZK8(JLRESU+IRESU-1),'FREQ',IBID,FREQ,K8BID,
     &                 C16BID,PREC,'RELATIF',IFREQ,1,IBID)
         ELSE
           CALL RSORAC(ZK8(JLRESU+IRESU-1),'FREQ',IBID,FREQ,K8BID,
     &                 C16BID,EPS0,'ABSOLU',IFREQ,1,IBID)
         ENDIF
         CALL RSEXCH('F',ZK8(JLRESU+IRESU-1),'DEPL',IFREQ,CHAMNO,IRET)
         CALL VTCOPY(CHAMNO,CHAMN2,'F',IBID)
         CALL JEVEUO(CHAMN2//'.VALE','L',JLVALE)
         DO 20 IEQ  = 1,NEQ
           ZC(J2ND-1+IEQ) = ZC(J2ND-1+IEQ) +
     &                      ZC(JLVALE-1+IEQ)*ZC(JLCCRE-1+IRESU)
  20    CONTINUE
  10  CONTINUE
C
      CALL JEDEMA()
      END
