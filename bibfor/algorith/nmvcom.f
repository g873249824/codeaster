      CHARACTER*24 MODELE,MATE  ,CARELE
      CHARACTER*24 VALVAL(8)
      CHARACTER*24 COMVAL    
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C LECTURE DES VARIABLES DE COMMANDE
C      
C ----------------------------------------------------------------------
C
C
C IN  PHASE  : 'VREF', 'VMOI' OU 'VPLU'
C IN  MODELE : MODELE
C IN  MATE   : CHAMP MATERIAU
C IN  CARELE : CARACTERISTIQUES DES ELEMENTS DE STRUCTURE
C IN  LISCHA : L_CHARGES
C IN  VALVAL : ETAT EN T- OU T+
C IN  INSTAN : INSTANT 
C OUT COMVAL : VARI_COM CALCULE 
C OUT CODRET : POUR CHAQUE RESULTAT, 'OK' SI ON A TROUVE, 'NO' SINON
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24 K24BID            
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()      
C
C -- TRAITEMENT DES VARIABLES DE COMMANDE

      IF (PHASE.EQ.'VREF') THEN
        CALL NMVCRE(MODELE,MATE  ,CARELE,COMVAL)             
      ELSEIF (PHASE.EQ.'VMOI') THEN
        CALL DESAGG(VALVAL,K24BID,K24BID,K24BID,COMVAL,
     &              K24BID,K24BID,K24BID,K24BID)      
        CALL NMVCLE(MODELE,MATE  ,CARELE,LISCHA,INSTAN,COMVAL,CODRET)
      ELSEIF (PHASE.EQ.'VPLU') THEN
        CALL DESAGG(VALVAL,K24BID,K24BID,K24BID,COMVAL,
     &              K24BID,K24BID,K24BID,K24BID)      
        CALL NMVCLE(MODELE,MATE  ,CARELE,LISCHA,INSTAN,COMVAL,CODRET)
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF

C
      CALL JEDEMA()      
C
      END
