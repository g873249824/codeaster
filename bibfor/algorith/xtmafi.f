      IMPLICIT NONE
      INTEGER       NFISS,NBMA
      CHARACTER*8   NOMA,FISS(NFISS)
      CHARACTER*24  MESMAI
C      
C ----------------------------------------------------------------------
C
C ROUTINE XFEM
C
C  CREATION DE LA LISTE TOTALE DES NOMS DES MAILLES FISSUREES
C  (MAILLES HEAV + CTIP + HECT) POUR LES FISSURES CONTENUES
C  DANS LA LISTE FISS
C ----------------------------------------------------------------------
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  FISS   : LISTE DES NOMS DES SD FISS_XFEM
C IN  NFISS  : LONGUEUR DE FISS
C OUT MESMAI : NOM DE LA LISTE CREEE
C OUT NBMA   : LONGUEUR DE MESMAI                   
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*32 JEXNUM,JEXATR
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER         IFISS,JINDIC,KK,JGRP,NMAENR,I,IMA,JMA,CPT,IER
      INTEGER         NDIM
      CHARACTER*8     NOMAIL,K8B
      CHARACTER*24    NOMMAI,XINDIC,GRP(3)
      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()

      NOMMAI = NOMA//'.NOMMAI'
      CALL DISMOI ( 'F', 'DIM_GEOM', NOMA, 'MAILLAGE',NDIM,K8B,IER)

C     DIMENTIONNEMENT DE LA LISTE
      NBMA = 0
      DO 10 IFISS = 1,NFISS
        XINDIC = FISS(IFISS)//'.MAILFISS .INDIC'
        CALL JEVEUO(XINDIC,'L',JINDIC)  
        NBMA = NBMA + ZI(JINDIC-1+2) + ZI(JINDIC-1+4) + ZI(JINDIC-1+6)
 10   CONTINUE

C     CREATION DE LA LISTE
      CALL WKVECT(MESMAI,'V V K8',NBMA,JMA)

C     REMPLISSAGE DE LA LISTE
      CPT = 0
      DO 100 IFISS = 1,NFISS
      
        GRP(1) = FISS(IFISS)//'.MAILFISS  .HEAV'
        GRP(2) = FISS(IFISS)//'.MAILFISS  .CTIP'
        GRP(3) = FISS(IFISS)//'.MAILFISS  .HECT'
        XINDIC = FISS(IFISS)//'.MAILFISS .INDIC'
        
        CALL JEVEUO(XINDIC,'L',JINDIC)  

C       BOUCLE SUR LES 3 GROUPES : HEAV, CTIP ET HECT 
        DO 110 KK = 1,3

          IF (ZI(JINDIC-1+2*(KK-1)+1).EQ.1) THEN        
            CALL JEVEUO(GRP(KK),'L',JGRP)
            NMAENR = ZI(JINDIC-1+2*KK)

C           BOUCLE SUR LES MAILLES DE CHAQUE GROUPE
            DO 120 I = 1,NMAENR
              IMA = ZI(JGRP-1+I)
              CALL JENUNO(JEXNUM(NOMMAI,IMA),NOMAIL)
              CPT =CPT + 1
              ZK8(JMA-1+CPT) = NOMAIL
 120        CONTINUE

          ENDIF

 110    CONTINUE

 100  CONTINUE 

C     VERIFICATION
      CALL ASSERT(NBMA.EQ.CPT)
C
      CALL JEDEMA()
      END
