      IMPLICIT NONE
      CHARACTER*19 SDDISC   
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME - EVENEMENTS)
C
C REINITIALISATIONS DES EVENEMENTS
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      IBID
      INTEGER      PILESS,ITESUP,IECHEC,NECHEC
      REAL*8       R8BID
      CHARACTER*8  K8BID
      CHARACTER*16 ACTION,PILCHO,NOMEVD
      LOGICAL      LACTI
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- NOMBRE D'EVENT-DRIVEN : NECHEC
C
      CALL UTDIDT('L'   ,SDDISC,'LIST',IBID  ,'NECHEC',
     &            R8BID ,NECHEC,K8BID )
C
C --- DESACTIVATION DES EVENEMENTS
C
      DO 100 IECHEC = 1,NECHEC
C
C ----- RECUPERATION DU NOM DE L'EVENT-DRIVEN
C
        CALL UTDIDT('L'   ,SDDISC,'ECHE',IECHEC,'NOM_EVEN',
     &              R8BID ,IBID  ,NOMEVD)
        LACTI = .FALSE.
        CALL DIEVEN(SDDISC,IECHEC,LACTI )
        CALL UTDIDT('L'   ,SDDISC,'ECHE',IECHEC,'ACTION',
     &              R8BID ,IBID  ,ACTION)
        IF (ACTION.EQ.'AUTRE_PILOTAGE') THEN
          PILESS = 1                     
          CALL UTDIDT('E'   ,SDDISC,'ECHE',IECHEC,'ESSAI_ITER_PILO',
     &                R8BID ,PILESS,K8BID )
          PILCHO = 'NATUREL'
          CALL UTDIDT('E'   ,SDDISC,'ECHE',IECHEC,'CHOIX_SOLU_PILO',
     &                R8BID ,IBID  ,PILCHO)
        ENDIF
        IF (ACTION.EQ.'ITER_SUPPL') THEN
          ITESUP = 0
          CALL NMLERR(SDDISC,'E','ITERSUP',R8BID ,ITESUP)
        ENDIF  
 100  CONTINUE         
C
      CALL JEDEMA()      
C
      END
