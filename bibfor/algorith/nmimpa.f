C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
      IMPLICIT      NONE
      INTEGER       UNITM
      CHARACTER*(*) ERROR
      INTEGER       SUGGES

C ----------------------------------------------------------------------
C  GESTION DES IMPRESSIONS DE LA COMMANDE STAT_NON_LINE
C  MESSAGES D'ERREUR
C ----------------------------------------------------------------------
C IN  UNITM  : UNITE D'IMPRESSION
C IN  ERROR  : CODE ERREUR
C              'ECHEC_LDC' -> ECHEC DE L'INTEGRATION DE LA LDC
C              'ECHEC_PIL' -> ECHEC DU PILOTAGE
C              'ECHEC_CON' -> ECHEC DE TRAITEMENT DU CONTACT
C              'CONT_SING' -> MATRICE DE CONTACT SINGULIERE
C              'MATR_SING' -> MATRICE DU SYSTEME SINGULIERE
C IN  SUGGES : VALEUR DU MESSAGE DE SUGGESTION
C               0 PAS DE MESSAGE
C               1 SUBDIVISER LE PAS DE TEMPS
C ----------------------------------------------------------------------
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C

      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER          ZLIG
      PARAMETER       (ZLIG = 255)
      CHARACTER*(ZLIG) MSGERR,MSGSUG
      INTEGER          MAXERR
C
C ----------------------------------------------------------------------
C
C
C --- LONGUEUR MAXI DU MESSAGE D'ERREUR
C
      MAXERR = 70
C
C --- ECHEC DANS L'INTEGRATION DE LA LOI DE COMPORTEMENT
C
      IF (ERROR(1:9) .EQ. 'ECHEC_LDC') THEN
        MSGERR = 'ECHEC DANS L''INTEGRATION DE LA LOI '//
     &           'DE COMPORTEMENT'
C
C --- ECHEC DANS LE PILOTAGE
C
      ELSE IF (ERROR(1:9) .EQ. 'ECHEC_PIL') THEN
        MSGERR = 'ECHEC DANS LE PILOTAGE'
C
C --- NOMBRE MAX D'ITERATIONS ATTEINT
C
      ELSE IF (ERROR(1:9) .EQ. 'ITER_MAXI') THEN
        MSGERR = 'NOMBRE MAXIMUM D''ITERATIONS ATTEINT'
C
C --- ECHEC DE TRAITEMENT DU CONTACT
C
      ELSE IF (ERROR .EQ. 'CONT_ERR') THEN
        MSGERR = 'ECHEC DANS LE TRAITEMENT DU CONTACT'
C
C --- MATRICE DE CONTACT SINGULIERE
C
      ELSE IF (ERROR(1:9) .EQ. 'CONT_SING') THEN
        MSGERR = 'MATRICE DE CONTACT SINGULIERE'
C
C --- MATRICE DU SYSTEME SINGULIERE
C
      ELSE IF (ERROR(1:9) .EQ. 'MATR_SING') THEN
        MSGERR = 'MATRICE DU SYSTEME SINGULIERE'
      ELSE
        CALL ASSERT(.FALSE.)
      END IF

      CALL IMPFOK(MSGERR,MAXERR,UNITM)

      IF (SUGGES.EQ.1) THEN
        MSGSUG = 'VERIFIEZ VOTRE MODELE OU '//
     &           ' ESSAYEZ DE SUBDIVISER LE PAS DE TEMPS'
        CALL IMPFOK(MSGSUG,MAXERR,UNITM)
      ENDIF

      END
