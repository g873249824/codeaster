      IMPLICIT NONE
      CHARACTER*19  CHAPIN(*),CHAPOU(*)
      CHARACTER*(*) TYCHAZ,TYPSOZ
      CHARACTER*(*) NOMVAZ      
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (CALCUL - UTILITAIRE)
C
C RECOPIE UN VECTEUR CHAPEAU EN CHANGEANT EVENTUELLEMENT UN NOM DE
C VARIABLE
C
C ----------------------------------------------------------------------
C
C
C IN  CHAPIN : VARIABLE CHAPEAU ENTRANTE
C IN  TYCHAP : TYPE DE VARIABLE CHAPEAU
C                MEELEM - NOMS DES MATR_ELEM
C                MEASSE - NOMS DES MATR_ASSE
C                VEELEM - NOMS DES VECT_ELEM
C                VEASSE - NOMS DES VECT_ASSE
C                SOLALG - NOMS DES CHAM_NO SOLUTION
C                VALINC - VALEURS SOLUTION INCREMENTALE
C IN  TYPSOL : TYPE DE VARIABLE A REMPLACER
C              ' ' SI PAS DE CHANGEMENT
C IN  NOMVAR : NOM DE LA VARIABLE
C OUT CHAPOU : VARIABLE CHAPEAU SORTANTE
C      
C ----------------------------------------------------------------------
C
      INTEGER      INDEX,I,NMAX
      CHARACTER*19 CHTEMP,NOMVAR
      CHARACTER*6  TYCHAP,TYPSOL
C      
C ----------------------------------------------------------------------
C
      NOMVAR = NOMVAZ
      TYCHAP = TYCHAZ
      TYPSOL = TYPSOZ
      CALL NMCHAI(TYCHAP,'LONMAX',NMAX  )
      IF (TYPSOL.NE.' ') THEN
        CALL NMCHAI(TYCHAP,TYPSOL,INDEX)
      ENDIF  
C
C --- INITIALISATION DES NOMS
C
C     -- PARFOIS CHAPOU ET CHAPIN SONT IDENTIQUES (MEMES TABLEAUX)
C        CELA PROVOQUE UN MESSAGE DE VALGRIND.
C        POUR EVITER CE PROBLEME, ON RECOPIE EN 2 TEMPS
C
      DO 11 I = 1,NMAX 
        CHTEMP = CHAPIN(I)
        CHAPOU(I) = CHTEMP          
   11 CONTINUE      
C
C --- REMPLACEMENT
C
      IF (TYPSOL.NE.' ') THEN
        CHAPOU(INDEX) = NOMVAR
      ENDIF    
C
      END
