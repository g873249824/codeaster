      IMPLICIT NONE
      REAL*8      Z,T,A,QSR,TEQ,ZPOINT,TDR,TDC,TABS,R8T0,EPS,EPS2
      REAL*8      AR,BR,TK,M


      LOGICAL     LREFR

      EPS= 1.D-9
      EPS2 = 1.D-6
      IF (LREFR) THEN
         IF (T .GT. TDR) THEN
            ZPOINT=0.D0
         ELSE
           IF (Z .LT. EPS2) THEN
              ZPOINT = 0.D0
           ELSE
               ZPOINT=AR+BR*ABS(T-TEQ)
               ZPOINT=EXP(ZPOINT)
               ZPOINT=(T-TEQ)*ZPOINT
               IF (Z .LT. EPS2) Z=EPS2
               ZPOINT=ZPOINT*Z*(1-Z)
            ENDIF
          ENDIF
      ELSE
         IF (T .LT. TDC) THEN
            ZPOINT=0.D0
         ELSE
            IF ((1-Z) .LT. EPS2 ) THEN
               ZPOINT=0.D0
            ELSE
               TABS=R8T0()
               TK=T+TABS
               ZPOINT=A*EXP(-QSR/TK)*((ABS(T-TEQ))**M)
               IF ((T-TEQ) .LT. 0.D0) ZPOINT=-ZPOINT
            ENDIF
         ENDIF
      ENDIF
      IF (ABS(ZPOINT) .LT. EPS) ZPOINT = 0.D0
      END
