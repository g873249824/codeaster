C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : ALGOS DE CONTACT/FROTTEMENT
C ----------------------------------------------------------------------
C
C ON VERIFIE QUE LE NOEUD ESCLAVE N'EST PAS EXCLU DE L'APPARIEMENT
C
C IN  JAPMEM : POINTEUR VERS RESOCO(1:14)//'.APMEMO'
C IN  JAPPAR : POINTEUR VERS RESOCO(1:14)//'.APPARI'
C IN  IESCL  : NUMERO DU NOEUD ESCLAVE DANS LE TABLEAU D'APPARIEMENT
C RETOUR     : SI CFEXCL=.TRUE. CE NOEUD DOIT ETRE EXCLU
C              SINON, LE NOEUD EST BON
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*32 JEXNUM
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      ZAPMEM
      PARAMETER    (ZAPMEM=4)
      INTEGER      NUMSUP
      CHARACTER*8  NOMSUP
      INTEGER      POSNOE,TYPSUP
C ----------------------------------------------------------------------
      CALL JEMARQ()
C     
C --- RECUPERATION DU NUMERO DE NOEUD ABSOLU DU NOEUD ESCLAVE
C
      POSNOE = ZI(JAPPAR+3*(IESCL-1)+1)
C
C --- VERIFICATION QUE CE NOEUD N'EST PAS EXCLU
C   
      IF (ZI(JAPMEM+ZAPMEM*(POSNOE-1)).LT.0) THEN
        CFEXCL = .TRUE.
      ELSE
        CFEXCL = .FALSE.
      ENDIF

C
C ----------------------------------------------------------------------
C
      CALL JEDEMA()

      END
