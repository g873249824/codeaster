      SUBROUTINE VECHMC(MODELZ,CARELZ,MATE,CHARGZ,INFCHZ,INSTAP,VECELZ)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 03/02/2003   AUTEUR JMBHH01 J.M.PROIX 
