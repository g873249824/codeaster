C OUT SOLALG : VARIABLE CHAPEAU POUR INCREMENTS SOLUTIONS
C OUT VEELEM : VARIABLE CHAPEAU POUR NOM DES VECT_ELEM
C OUT MEELEM : VARIABLE CHAPEAU POUR NOM DES MATR_ELEM
C OUT VEASSE : VARIABLE CHAPEAU POUR NOM DES VECT_ASSE
C OUT MEASSE : VARIABLE CHAPEAU POUR NOM DES MATR_ASSE
C
C ----------------------------------------------------------------------
C
      INTEGER      IFM,NIV
      CHARACTER*19 VEFSDO,VEFINT,VEBUDI,VEDIDO,VESSTF
      CHARACTER*19 VEDIPI,VEFEDO,VEFEPI,VEONDP
      CHARACTER*19 VEDIDI,VEDIRI,VEFNOD,VELAPL
      CHARACTER*19 VEELTC,VEELTF
      CHARACTER*19 VEREFE,VEVCF0,VEVCF1
      CHARACTER*19 VEIMPP,VEIMPC
