C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*24 SDSENS 
      CHARACTER*19 SDDYNA     
      CHARACTER*24 VALMOI(8),VALPLU(8),POUGD(8)  
      CHARACTER*19 VEELEM(*),MEELEM(*)         
      CHARACTER*19 VEASSE(*),MEASSE(*)
      CHARACTER*19 SOLALG(*)
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (INITIALISATION)
C
C CREATION DES VARIABLES CHAPEAUX
C      
C ----------------------------------------------------------------------
C
C
C ON NE FAIT QUE REMPLIR LES VARIABLES CHAPEUAX AVEC LES NOMS
C LA CREATION DES SD EST FAITE PRINCIPALEMENT DANS NMCRCH
C
C IN  SDSENS : SD SENSIBILITE
C IN  SDDYNA : SD DYNAMIQUE
C OUT VALMOI : VARIABLE CHAPEAU POUR ETAT EN T-
C OUT VALPLU : VARIABLE CHAPEAU POUR ETAT EN T+
C OUT POUGD  : VARIABLE CHAPEAU POUR POUTRES EN GRANDES ROTATIONS
C OUT SOLALG : VARIABLE CHAPEAU POUR INCREMENTS SOLUTIONS
C OUT VEELEM : VARIABLE CHAPEAU POUR NOM DES VECT_ELEM
C OUT MEELEM : VARIABLE CHAPEAU POUR NOM DES MATR_ELEM
C OUT VEASSE : VARIABLE CHAPEAU POUR NOM DES VECT_ASSE
C OUT MEASSE : VARIABLE CHAPEAU POUR NOM DES MATR_ASSE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24 K24BLA    
      INTEGER      NRPASE
      INTEGER      IFM,NIV
      LOGICAL      NDYNLO,LDYNA  
C
      CHARACTER*19 VEFSDO,VEFINT,VEBUDI,VEDIDO,VESSTF
      CHARACTER*19 VEDIPI,VEFEDO,VEFEPI,VEONDP,VEIMPE
      CHARACTER*19 VEMSME,VEDIDI,VEDIRI,VEFNOD,VELAPL
      CHARACTER*19 VECTCC,VECTCF,VEXFEC,VEXFEF,VEXFTC
      CHARACTER*19 VEXFTF,VEREFE,VEVCF0,VEVCF1
C      
      CHARACTER*24 DEPMOI,SIGMOI,VARMOI,COMMOI,VITMOI
      CHARACTER*24 ACCMOI
      CHARACTER*24 DEPPLU,SIGPLU,VARPLU,COMPLU,VITPLU
      CHARACTER*24 ACCPLU
      CHARACTER*24 DEPKM1,VITKM1,ACCKM1,ROMKM1,ROMK 
C       
      CHARACTER*19 CNFEDO,CNFEPI,CNFSDO,CNDIDI,CNFINT
      CHARACTER*19 CNDIDO,CNDIPI,CNCINE,CNDIRI,CNFNOD
      CHARACTER*19 CNBUDI,CNLAPL,CNSSTR,CNCTDF,CNONDP 
      CHARACTER*19 CNCTCC,CNCTCF,CNXFEC,CNXFEF,CNXFTC
      CHARACTER*19 CNXFTF,CNIMPE,CNMSME,CNSSTF
      CHARACTER*19 CNREFE,CNVCF0,CNVCF1,CNGRFL,CNVCPR
      CHARACTER*19 CNDYNA,CNMODP,CNMODC
      CHARACTER*19 CNCTDC,CNUNIL,CNFEXT
C      
      CHARACTER*19 DEPOLD,DDEPLA,DEPDEL,DEPPR1,DEPPR2
      CHARACTER*19 VITOLD,DVITLA,VITDEL,VITPR1,VITPR2
      CHARACTER*19 ACCOLD,DACCLA,ACCDEL,ACCPR1,ACCPR2
C
      CHARACTER*19 DEPSO1,DEPSO2                  
C      
      CHARACTER*19 MEMASS,MEAMOR,MERIGI,MEDIRI,MESUIV
      CHARACTER*19 MESSTR,MEGEOM,MECTCC,MEXFEC,MEXFTC
      CHARACTER*19 MECTCF,MEXFEF,MEXFTF      
C      
      
      CHARACTER*19 MASSE ,AMORT ,RIGID ,SSTRU
C
      DATA DEPMOI,DEPPLU    /'&&OP0070.DEPMOI','&&OP0070.DEPPLU'/
      DATA SIGMOI,SIGPLU    /'&&OP0070.SIGMOI','&&OP0070.SIGPLU'/
      DATA VARMOI,VARPLU    /'&&OP0070.VARMOI','&&OP0070.VARPLU'/
      DATA VITMOI,VITPLU    /'&&OP0070.VITMOI','&&OP0070.VITPLU'/
      DATA ACCMOI,ACCPLU    /'&&OP0070.ACCMOI','&&OP0070.ACCPLU'/
      DATA COMMOI,COMPLU    /'&&OP0070.COMMOI','&&OP0070.COMPLU'/
C
      DATA DEPKM1,VITKM1    /'&&OP0070.DEPKM1','&&OP0070.VITKM1'/
      DATA ACCKM1           /'&&OP0070.ACCKM1'/
      DATA ROMKM1,ROMK      /'&&OP0070.ROMKM1','&&OP0070.ROMK  '/
C
      DATA DEPDEL,DDEPLA    /'&&OP0070.DEPDEL','&&OP0070.DDEPLA'/ 
      DATA DEPOLD           /'&&OP0070.DEPOLD'/
      DATA DEPPR1,DEPPR2    /'&&OP0070.DEPPR1','&&OP0070.DEPPR2'/
      DATA VITDEL,DVITLA    /'&&OP0070.VITDEL','&&OP0070.DVITLA'/ 
      DATA VITOLD           /'&&OP0070.VITOLD'/
      DATA VITPR1,VITPR2    /'&&OP0070.VITPR1','&&OP0070.VITPR2'/ 
      DATA ACCDEL,DACCLA    /'&&OP0070.ACCDEL','&&OP0070.DACCLA'/ 
      DATA ACCOLD           /'&&OP0070.ACCOLD'/
      DATA ACCPR1,ACCPR2    /'&&OP0070.ACCPR1','&&OP0070.ACCPR2'/ 
      DATA DEPSO1,DEPSO2    /'&&OP0070.DEPSO1','&&OP0070.DEPSO2'/ 
C      
      DATA MEMASS,MEDIRI    /'&&OP0070.MEMASS','&&OP0070.MEDIRI'/
      DATA MERIGI,MEAMOR    /'&&OP0070.MERIGI','&&OP0070.MEAMOR'/
      DATA MESUIV,MESSTR    /'&&OP0070.MATGME','&&OP0070.SSRELE'/ 
      DATA MEGEOM           /'&&OP0070.MEGEOM'/ 
      DATA MECTCC,MECTCF    /'&&OP0070.MECTCC','&&OP0070.MECTCF'/
      DATA MEXFEC,MEXFEF    /'&&OP0070.MEXFEC','&&OP0070.MEXFEF'/
      DATA MEXFTC,MEXFTF    /'&&OP0070.MEXFTC','&&OP0070.MEXFTF'/
C                
      DATA AMORT ,MASSE     /'&&OP0070.AMORT','&&OP0070.MASSE'/
      DATA RIGID ,SSTRU     /'&&OP0070.RIGID','&&OP0070.SSRASS'/  
C
      DATA VEFEDO,VEFSDO    /'&&OP0070.VEFEDO','&&OP0070.VEFSDO'/ 
      DATA VEDIDO,VEFEPI    /'&&OP0070.VEDIDO','&&OP0070.VEFEPI'/
      DATA VEDIPI,VEFINT    /'&&OP0070.VEDIPI','&&OP0070.VEFINT'/
      DATA VEBUDI,VEDIDI    /'&&OP0070.VEBUDI','&&OP0070.VEDIDI'/
      DATA VEONDP,VELAPL    /'&&OP0070.VEONDP','&&OP0070.VELAPL'/
      DATA VEIMPE,VEMSME    /'&&OP0070.VEIMPE','&&OP0070.VEMSME'/ 
      DATA VEDIRI,VEFNOD    /'&&OP0070.VEDIRI','&&OP0070.VEFNOD'/
      DATA VESSTF,VECTCC    /'&&OP0070.VESSTF','&&OP0070.VECTCC'/
      DATA VECTCF,VEXFEC    /'&&OP0070.VECTCF','&&OP0070.VEXFEC'/
      DATA VEXFTF,VEXFTC    /'&&OP0070.VEXFTF','&&OP0070.VEXFTC'/
      DATA VEXFEF,VEREFE    /'&&OP0070.VEXFEF','&&OP0070.VEREFE'/
      DATA VEVCF0,VEVCF1    /'&&OP0070.VEVCF0','&&OP0070.VEVCF1'/
C
      DATA CNFEDO,CNFSDO    /'&&OP0070.CNFEDO','&&OP0070.CNFSDO'/ 
      DATA CNDIDO,CNFEPI    /'&&OP0070.CNDIDO','&&OP0070.CNFEPI'/
      DATA CNDIPI,CNFINT    /'&&OP0070.CNDIPI','&&OP0070.CNFINT'/
      DATA CNBUDI,CNDIDI    /'&&OP0070.CNBUDI','&&OP0070.CNDIDI'/
      DATA CNONDP,CNLAPL    /'&&OP0070.CNONDP','&&OP0070.CNLAPL'/
      DATA CNIMPE,CNMSME    /'&&OP0070.CNIMPE','&&OP0070.CNMSME'/ 
      DATA CNDIRI,CNFNOD    /'&&OP0070.CNDIRI','&&OP0070.CNFNOD'/
      DATA CNSSTF,CNCTCC    /'&&OP0070.CNSSTF','&&OP0070.CNCTCC'/
      DATA CNCTCF,CNXFEC    /'&&OP0070.CNCTCF','&&OP0070.CNXFEC'/
      DATA CNXFTF,CNXFTC    /'&&OP0070.CNXFTF','&&OP0070.CNXFTC'/
      DATA CNXFEF,CNREFE    /'&&OP0070.CNXFEF','&&OP0070.CNREFE'/
      DATA CNVCF0,CNVCF1    /'&&OP0070.CNVCF0','&&OP0070.CNVCF1'/  
      DATA CNCINE,CNSSTR    /'&&OP0070.CNCINE','&&OP0070.CNSSTR'/  
      DATA CNCTDF,CNGRFL    /'&&OP0070.CNCTDF','&&OP0070.CNGRFL'/  
      DATA CNVCPR,CNMODP    /'&&OP0070.CNVCPR','&&OP0070.CNMODP'/
      DATA CNDYNA           /'&&OP0070.CNDYNA'/  
      DATA CNMODC,CNCTDC    /'&&OP0070.CNMODC','&&OP0070.CNCTDC'/  
      DATA CNUNIL,CNFEXT    /'&&OP0070.CNUNIL','&&OP0070.CNFEXT'/     
C   
C ----------------------------------------------------------------------
C      
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> ... CREATION VARIABLES CHAPEAUX'
      ENDIF
C
C --- FONCTIONNALITES ACTIVES
C
      LDYNA     = NDYNLO(SDDYNA,'DYNAMIQUE') 
C
C --- NOMS DES CHAMPS - ETAT T- (SANS SENSIBILITE)
C    
      NRPASE = 0
      CALL NMNSLE(SDSENS,NRPASE,'DEPMOI',DEPMOI)
      CALL NMNSLE(SDSENS,NRPASE,'SIGMOI',SIGMOI)
      CALL NMNSLE(SDSENS,NRPASE,'VARMOI',VARMOI)
      IF (LDYNA) THEN
        CALL NMNSLE(SDSENS,NRPASE,'VITMOI',VITMOI)
        CALL NMNSLE(SDSENS,NRPASE,'ACCMOI',ACCMOI)        
      END IF     
C
C --- VARIABLE CHAPEAU - ETAT T- 
C
      CALL AGGLOM(DEPMOI,SIGMOI,VARMOI,COMMOI,
     &            VITMOI,ACCMOI,K24BLA,K24BLA, 6, VALMOI)    
C
C --- NOMS DES CHAMPS - ETAT T+ (SANS SENSIBILITE)
C    
      NRPASE = 0
      CALL NMNSLE(SDSENS,NRPASE,'DEPPLU',DEPPLU)
      CALL NMNSLE(SDSENS,NRPASE,'SIGPLU',SIGPLU)
      CALL NMNSLE(SDSENS,NRPASE,'VARPLU',VARPLU)
      IF (LDYNA) THEN
        CALL NMNSLE(SDSENS,NRPASE,'VITPLU',VITPLU)
        CALL NMNSLE(SDSENS,NRPASE,'ACCPLU',ACCPLU)       
      END IF        
C
C --- VARIABLE CHAPEAU - ETAT T+ 
C          
      CALL AGGLOM(DEPPLU,SIGPLU,VARPLU,COMPLU,
     &            VITPLU,ACCPLU,K24BLA,K24BLA, 6, VALPLU)
C
C --- VARIABLE CHAPEAU  POUR POUTRES EN GRANDES ROTATIONS
C     
      CALL AGGLOM(DEPKM1,VITKM1,ACCKM1,ROMKM1,
     &            ROMK  ,K24BLA,K24BLA,K24BLA, 5, POUGD )    
C
C --- VARIABLE CHAPEAU POUR NOM DES MATR_ELEM
C     
      CALL NMCHA0('MEELEM',' ',' ',MEELEM) 
      CALL NMCHA0('MEELEM','MERIGI',MERIGI,MEELEM)   
      CALL NMCHA0('MEELEM','MEDIRI',MEDIRI,MEELEM)   
      CALL NMCHA0('MEELEM','MEMASS',MEMASS,MEELEM)   
      CALL NMCHA0('MEELEM','MEAMOR',MEAMOR,MEELEM)   
      CALL NMCHA0('MEELEM','MESUIV',MESUIV,MEELEM)   
      CALL NMCHA0('MEELEM','MESSTR',MESSTR,MEELEM)   
      CALL NMCHA0('MEELEM','MEGEOM',MEGEOM,MEELEM)   
      CALL NMCHA0('MEELEM','MECTCC',MECTCC,MEELEM)   
      CALL NMCHA0('MEELEM','MECTCF',MECTCF,MEELEM)   
      CALL NMCHA0('MEELEM','MEXFEC',MEXFEC,MEELEM)   
      CALL NMCHA0('MEELEM','MEXFEF',MEXFEF,MEELEM)   
      CALL NMCHA0('MEELEM','MEXFTC',MEXFTC,MEELEM)   
      CALL NMCHA0('MEELEM','MEXFTF',MEXFTF,MEELEM)    
C
C --- VARIABLE CHAPEAU POUR NOM DES MATR_ASSE
C 
      CALL NMCHA0('MEASSE',' ',' ',MEASSE)
      CALL NMCHA0('MEASSE','MERIGI',RIGID ,MEASSE)
      CALL NMCHA0('MEASSE','MEMASS',MASSE ,MEASSE)
      CALL NMCHA0('MEASSE','MEAMOR',AMORT ,MEASSE)
      CALL NMCHA0('MEASSE','MESSTR',SSTRU ,MEASSE)
C
C --- VARIABLE CHAPEAU POUR NOM DES VECT_ELEM
C   
      CALL NMCHA0('VEELEM',' ',' ',VEELEM) 
      CALL NMCHA0('VEELEM','CNFINT',VEFINT,VEELEM)
      CALL NMCHA0('VEELEM','CNDIRI',VEDIRI,VEELEM)
      CALL NMCHA0('VEELEM','CNBUDI',VEBUDI,VEELEM)
      CALL NMCHA0('VEELEM','CNFNOD',VEFNOD,VEELEM)
      CALL NMCHA0('VEELEM','CNDIDO',VEDIDO,VEELEM)
      CALL NMCHA0('VEELEM','CNDIPI',VEDIPI,VEELEM)
      CALL NMCHA0('VEELEM','CNFEDO',VEFEDO,VEELEM)
      CALL NMCHA0('VEELEM','CNFEPI',VEFEPI,VEELEM)
      CALL NMCHA0('VEELEM','CNLAPL',VELAPL,VEELEM)
      CALL NMCHA0('VEELEM','CNONDP',VEONDP,VEELEM)
      CALL NMCHA0('VEELEM','CNFSDO',VEFSDO,VEELEM)
      CALL NMCHA0('VEELEM','CNIMPE',VEIMPE,VEELEM)
      CALL NMCHA0('VEELEM','CNMSME',VEMSME,VEELEM)
      CALL NMCHA0('VEELEM','CNDIDI',VEDIDI,VEELEM)
      CALL NMCHA0('VEELEM','CNSSTF',VESSTF,VEELEM)
      CALL NMCHA0('VEELEM','CNCTCC',VECTCC,VEELEM)
      CALL NMCHA0('VEELEM','CNCTCF',VECTCF,VEELEM)
      CALL NMCHA0('VEELEM','CNXFEC',VEXFEC,VEELEM)
      CALL NMCHA0('VEELEM','CNXFEF',VEXFEF,VEELEM)
      CALL NMCHA0('VEELEM','CNXFTC',VEXFTC,VEELEM)
      CALL NMCHA0('VEELEM','CNXFTF',VEXFTF,VEELEM)
      CALL NMCHA0('VEELEM','CNREFE',VEREFE,VEELEM)
      CALL NMCHA0('VEELEM','CNVCF1',VEVCF1,VEELEM)
      CALL NMCHA0('VEELEM','CNVCF0',VEVCF0,VEELEM)                   
C
C --- VARIABLE CHAPEAU POUR NOM DES VECT_ASSE
C   
      CALL NMCHA0('VEASSE',' ',' ',VEASSE) 
      CALL NMCHA0('VEASSE','CNFINT',CNFINT,VEASSE)
      CALL NMCHA0('VEASSE','CNDIRI',CNDIRI,VEASSE)
      CALL NMCHA0('VEASSE','CNBUDI',CNBUDI,VEASSE)
      CALL NMCHA0('VEASSE','CNFNOD',CNFNOD,VEASSE)
      CALL NMCHA0('VEASSE','CNDIDO',CNDIDO,VEASSE)
      CALL NMCHA0('VEASSE','CNDIPI',CNDIPI,VEASSE)
      CALL NMCHA0('VEASSE','CNFEDO',CNFEDO,VEASSE)
      CALL NMCHA0('VEASSE','CNFEPI',CNFEPI,VEASSE)
      CALL NMCHA0('VEASSE','CNLAPL',CNLAPL,VEASSE)
      CALL NMCHA0('VEASSE','CNONDP',CNONDP,VEASSE)
      CALL NMCHA0('VEASSE','CNFSDO',CNFSDO,VEASSE)
      CALL NMCHA0('VEASSE','CNIMPE',CNIMPE,VEASSE)
      CALL NMCHA0('VEASSE','CNMSME',CNMSME,VEASSE)
      CALL NMCHA0('VEASSE','CNDIDI',CNDIDI,VEASSE)
      CALL NMCHA0('VEASSE','CNSSTF',CNSSTF,VEASSE)
      CALL NMCHA0('VEASSE','CNCTCC',CNCTCC,VEASSE)
      CALL NMCHA0('VEASSE','CNCTCF',CNCTCF,VEASSE)
      CALL NMCHA0('VEASSE','CNXFEC',CNXFEC,VEASSE)
      CALL NMCHA0('VEASSE','CNXFEF',CNXFEF,VEASSE)
      CALL NMCHA0('VEASSE','CNXFTC',CNXFTC,VEASSE)
      CALL NMCHA0('VEASSE','CNXFTF',CNXFTF,VEASSE)
      CALL NMCHA0('VEASSE','CNREFE',CNREFE,VEASSE)
      CALL NMCHA0('VEASSE','CNVCF1',CNVCF1,VEASSE)
      CALL NMCHA0('VEASSE','CNVCF0',CNVCF0,VEASSE)
C                    
C --- SANS VECT_ELEM POUR CONSTRUIRE   
C  
      CALL NMCHA0('VEASSE','CNCINE',CNCINE,VEASSE)
      CALL NMCHA0('VEASSE','CNSSTR',CNSSTR,VEASSE)
      CALL NMCHA0('VEASSE','CNCTDF',CNCTDF,VEASSE)
      CALL NMCHA0('VEASSE','CNGRFL',CNGRFL,VEASSE)
      CALL NMCHA0('VEASSE','CNVCPR',CNVCPR,VEASSE)
      CALL NMCHA0('VEASSE','CNDYNA',CNDYNA,VEASSE)
      CALL NMCHA0('VEASSE','CNMODP',CNMODP,VEASSE)
      CALL NMCHA0('VEASSE','CNMODC',CNMODC,VEASSE)
      CALL NMCHA0('VEASSE','CNCTDC',CNCTDC,VEASSE)
      CALL NMCHA0('VEASSE','CNUNIL',CNUNIL,VEASSE) 
      CALL NMCHA0('VEASSE','CNFEXT',CNFEXT,VEASSE)
C      
C --- VARIABLE CHAPEAU POUR INCREMENTS SOLUTIONS      
C      
      CALL NMCHA0('SOLALG',' ',' ',SOLALG) 
      CALL NMCHA0('SOLALG','DDEPLA',DDEPLA,SOLALG)
      CALL NMCHA0('SOLALG','DEPDEL',DEPDEL,SOLALG)
      CALL NMCHA0('SOLALG','DEPPR1',DEPPR1,SOLALG)
      CALL NMCHA0('SOLALG','DEPPR2',DEPPR2,SOLALG)
      CALL NMCHA0('SOLALG','DEPOLD',DEPOLD,SOLALG)
      CALL NMCHA0('SOLALG','DVITLA',DVITLA,SOLALG)
      CALL NMCHA0('SOLALG','VITDEL',VITDEL,SOLALG)
      CALL NMCHA0('SOLALG','VITPR1',VITPR1,SOLALG)
      CALL NMCHA0('SOLALG','VITPR2',VITPR2,SOLALG)
      CALL NMCHA0('SOLALG','VITOLD',VITOLD,SOLALG)
      CALL NMCHA0('SOLALG','DACCLA',DACCLA,SOLALG)
      CALL NMCHA0('SOLALG','ACCDEL',ACCDEL,SOLALG)
      CALL NMCHA0('SOLALG','ACCPR1',ACCPR1,SOLALG)
      CALL NMCHA0('SOLALG','ACCPR2',ACCPR2,SOLALG)
      CALL NMCHA0('SOLALG','ACCOLD',ACCOLD,SOLALG)  
      CALL NMCHA0('SOLALG','DEPSO1',DEPSO1,SOLALG)  
      CALL NMCHA0('SOLALG','DEPSO2',DEPSO2,SOLALG)  
C
      CALL JEDEMA()      
C
      END
