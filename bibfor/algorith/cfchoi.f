C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      INTEGER       IPROJ,IPROJM
      REAL*8        JEU,JEUMIN
      INTEGER       POSMAM,POSMIN
      REAL*8        TAU1(3),T1MIN(3)
      REAL*8        TAU2(3),T2MIN(3)
      REAL*8        DIST(3),DISTME(3)
      REAL*8        KSI1,XIMIN
      REAL*8        KSI2,YIMIN
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT - MAIT/ESCL)
C
C CHOIX DE LA MAILLE APPARIEE AU NOEUD ESCLAVE COURANT
C
C ----------------------------------------------------------------------
C
C
C IN  JEU    : JEU CALCULE SUR LE VECTEUR PM
C I/O JEUMIN : VALEUR DU JEU MINIMUM
C IN  POSMAM : MAILLE MAITRE APPARIEE
C OUT POSMIN : MAILLE MAITRE CORRESPONDANT AU JEU MINIMUM
C IN  IPROJ  : VAUT 0 SI POINT PROJETE DANS L'ELEMENT
C                   1 SI POINT PROJETE DANS LA ZONE DEFINIE PAR TOLEOU
C                   2 SI POINT PROJETE EN DEHORS (EXCLUS)
C OUT IPROJM : TYPE DE PROJECTION CORRESPONDANT AU JEU MINIMUM
C IN  TAU1   : PREMIER VECTEUR TANGENT
C IN  TAU2   : SECOND VECTEUR TANGENT
C OUT T1MIN  : PREMIER VECTEUR TANGENT CORRESPONDANT AU JEU MINIMUM
C OUT T2MIN  : SECOND VECTEUR TANGENT CORRESPONDANT AU JEU MINIMUM
C IN  KSI1   : COORD. PARAM. 1 DE LA PROJECTION SUR MAILLE MAITRE
C IN  KSI2   : COORD. PARAM. 2 DE LA PROJECTION SUR MAILLE MAITRE
C OUT XIMIN  : COORD. PARAM. 1 DE LA PROJECTION SUR MAILLE MAITRE
C               CORRESPONDANT AU JEU MINIMUM
C OUT YIMIN  : COORD. PARAM. 2 DE LA PROJECTION SUR MAILLE MAITRE
C               CORRESPONDANT AU JEU MINIMUM
C OUT DIST   : VECTEUR RELIANT LE POINT AVEC SON PROJETE
C OUT DISTME : VECTEUR E->M (CORRESPONDANT AU JEU MINIMUM)
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      REAL*8      ECAN,R8PREM
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ECART ANCIEN/NOUVEAU
C
      IF ((JEUMIN.EQ.0.D0).OR.(JEU.EQ.JEUMIN)) THEN
        ECAN = 0.D0
      ELSE
        ECAN = ABS((JEU - JEUMIN)/JEUMIN)
      ENDIF

C PREMIERE PROJECTION
      IF (IPROJM.EQ.-1) THEN
        JEUMIN = JEU
        POSMIN = POSMAM
        IPROJM = IPROJ
        CALL DCOPY(3,TAU1,1,T1MIN,1)
        CALL DCOPY(3,TAU2,1,T2MIN,1)
        CALL DCOPY(3,DIST,1,DISTME,1)
        XIMIN = KSI1
        YIMIN = KSI2
      ELSE
C PROJECTION SUIVANTES
        IF (IPROJM.NE.0) THEN
C ON N'A PAS ENCORE PROJETE DANS UN ELEMENT
          IF (IPROJ.EQ.0) THEN
C UN POINT SE PROJETE DANS UN ELEMENT
            JEUMIN = JEU
            POSMIN = POSMAM
            IPROJM = IPROJ
            CALL DCOPY(3,TAU1,1,T1MIN,1)
            CALL DCOPY(3,TAU2,1,T2MIN,1)
            CALL DCOPY(3,DIST,1,DISTME,1)
            XIMIN = KSI1
            YIMIN = KSI2
          ELSE
            IF (JEU.LT.JEUMIN) THEN
              JEUMIN = JEU
              POSMIN = POSMAM
              IPROJM = IPROJ
              CALL DCOPY(3,TAU1,1,T1MIN,1)
              CALL DCOPY(3,TAU2,1,T2MIN,1)
              CALL DCOPY(3,DIST,1,DISTME,1)
              XIMIN = KSI1
              YIMIN = KSI2
            ENDIF
          ENDIF
        ELSE
C ON A DEJA PROJETE DANS UN ELEMENT
          IF (IPROJ.EQ.0) THEN
            IF (JEU.LT.JEUMIN.AND.ECAN.GT.R8PREM()) THEN
C ON SELECTIONNE LE PROJETE MINIMISANT LA DISTANCE
              JEUMIN = JEU
              POSMIN = POSMAM
              IPROJM = IPROJ
              CALL DCOPY(3,TAU1,1,T1MIN,1)
              CALL DCOPY(3,TAU2,1,T2MIN,1)
              CALL DCOPY(3,DIST,1,DISTME,1)
              XIMIN = KSI1
              YIMIN = KSI2
            ENDIF
          ENDIF
        ENDIF
C
      ENDIF
C
      CALL JEDEMA()
      END
