C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      REAL*8       JEUPM
      INTEGER      ZONESY
      INTEGER      POSMA,PROYES
      INTEGER      POSMIN,PROMIN
      REAL*8       JEUMIN
      CHARACTER*3  PROJOP
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT - MAIT/ESCL)
C
C CHOIX DE LA MAILLE APPARIEE AU NOEUD ESCLAVE COURANT
C
C ----------------------------------------------------------------------
C
C
C I/O JEUMIN : VALEUR DU JEU MINIMUM
C I/O POSMIN : MAILLE MAITRE CORRESPONDANT AU JEU MINIMUM 
C               (INDICE DANS CONTMA)
C I/O PROMIN : TYPE DE PROJECTION CORRESPONDANT AU JEU MINIMUM
C I/O PROJOP : OPERATION DE PROJECTION 
C              'NOP' ON NE FAIT RIEN
C              'ADD' ON AJOUTE L'APPARIEMENT DE CE NOEUD ESCLAVE
C              'STC' ON STOCKE L'APPARIEMENT DE CE NOEUD ESCLAVE
C                     POUR VERIFIER PLUS TARD LES PIVOTS NULS 
C              'PIV' ON AJOUTE L'APPARIEMENT DE CE NOEUD ESCLAVE 
C                     POUR VERIFIER S'Il N'Y A PAS PIVOT NUL
C              'SUP' ON SUPPRIME L'APPARIEMENT DE CE NOEUD ESCLAVE
C IN  JEUPM  : JEU CALCULE SUR LE VECTEUR PM
C IN  ZONESY : INDICATEUR DE ZONE SYMETRIQUE
C                0 - PAS D'APPARIEMENT SYMETRIQUE
C                1 - APPARIEMENT SYMETRIQUE IZONE EST UNE 
C                    ZONE DE CONTACT PRINCIPALE
C                2 - APPARIEMENT SYMETRIQUE IZONE EST UNE 
C                    ZONE DE CONTACT SYMETRIQUE
C IN  POSMA  : POSITION DE LA MAILLE MAITRE APPARIEE DANS CONTMA
C IN  PROYES : TYPE DE PROJECTION 
C               0     PROJECTION NORMALE
C              -1000  PROJECTION EN DEHORS
C                   DE LA MAILLE MAITRE (TOLERANCE TOLEOU ET DEBORD) 
C              -30X   PROJECTION SUR
C                   ENTITE GEOMETRIQUE NOEUD
C                   DE LA MAILLE MAITRE (TOLERANCE TOLEIN ET NOEUD)
C                      X EST LE NUMERO DE NOEUD (1 A 4)
C              -20X   PROJECTION SUR
C                   ENTITE GEOMETRIQUE ARETE
C                   DE LA MAILLE MAITRE (TOLERANCE TOLEIN ET ARETE)
C                      X EST LE NUMERO D'ARETE (1 A 4)
C              -10X   PROJECTION SUR
C                   ENTITE GEOMETRIQUE DIAG (PSEUDO SUR QUADRANGLE)
C                   DE LA MAILLE MAITRE (TOLERANCE TOLEIN ET DIAG)
C                      X EST LE NUMERO DE DIAGONALE (1 A 2)

C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      REAL*8  ECAN
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ECART ANCIEN/NOUVEAU
C
      IF (JEUMIN.EQ.0.D0) THEN
        ECAN = 1.D0
      ELSE
        ECAN = ABS((JEUPM-JEUMIN)/JEUMIN)
      ENDIF
C
C --- PROJECTION SUR ARETE/DIAGONALE OU SIMPLEMENT SUR UNE MAILLE
C ---                             -> ON APPARIE
C --- PROJECTION SUR UN NOEUD
C --- 1/ PAS D'APPARIEMENT SYMETRIQUE
C ---                             -> ON APPARIE
C --- 2/ APPARIEMENT SYMETRIQUE
C ---     PASSAGE DANS LES ZONES PRINCIPALES
C ---                             -> ON STOCKE L'INFO
C --- 3/ APPARIEMENT SYMETRIQUE
C ---     PASSAGE DANS LES ZONES SYMETRIQUES
C ---                             -> ON VERIFIE UN EVENTUEL
C                                    PIVOT NUL
C
C --- PROJECTION HORS MAILLE: PROYES LE -1000
C --- -> SI NVELLE PROJECTION "LOIN" DE L'ANCIENNE (ECAN.GT.1D-8)
C ---                       ON SUPPRIME L'APPARIEMENT
C
      IF ((JEUPM.LE.JEUMIN).AND.
     &    (ECAN.GT.1D-15)) THEN

        JEUMIN = JEUPM
        POSMIN = POSMA
        PROMIN = PROYES

        IF (PROYES.LE.-1000) THEN
          IF (PROJOP.EQ.'ADD') THEN
            IF (ECAN.GE.1.D-8) THEN
              PROJOP = 'SUP'
            ENDIF
          ELSE
            PROJOP = 'SUP'
          ENDIF
        ELSE IF ((PROYES.GT.-1000).AND.(PROYES.LE.-300)) THEN
          IF (ZONESY.EQ.0) THEN
            PROJOP = 'ADD'
          ELSE IF (ZONESY.EQ.1) THEN
            PROJOP = 'STC'
          ELSE IF (ZONESY.EQ.2) THEN
            PROJOP = 'PIV'
          ENDIF
        ELSE IF (PROYES.GT.-300) THEN
          PROJOP = 'ADD'   
        ENDIF
      ELSE
        IF (PROJOP.EQ.'ADD') THEN
          PROJOP =  'NOP'
        ENDIF
      ENDIF
C 
      CALL JEDEMA()
      END
