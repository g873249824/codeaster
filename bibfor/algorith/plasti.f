      SUBROUTINE PLASTI ( FAMI,KPG,KSP,TYPMOD,IMAT,COMP,CRIT,TIMED,
     &                       TIMEF, TEMPD, TEMPF, TREF,EPSDT,DEPST,
     &                       SIGD,VIND,OPT,ANGMAS,SIGF,VINF,DSDE,ICOMP,
     &                       NVI,TAMPON,IRTETI)
      IMPLICIT NONE
