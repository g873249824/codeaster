C  BUT: ETABLIR LA LISTE DES DDL ASSOCIES A UNE LISTE DE NOEUDS
      IMPLICIT NONE
C       REMPLIR UNE MATRICE A DEUX COLONNES
C
C   COLONNE 1 : ENTIERS CODES POUR DES DDL PHYSIQUES ASSEMBLES
C   COLONNE 2 : ENTIERS CODES DES DDL LAGRANGE BLOQUAGE ASSEMBLES
C
C-----------------------------------------------------------------------
C
C  NBCMP   /I/: NOMBRE DE COMPOSANTE MAX DE LA GRANDEUR SOUS-JACENTE
C  LINO    /I/: VECTEUR LISTE DES NOEUDS
C  NBNO    /I/: NOMBRE DE NOEUDS DE LA LISTE LINO
C  IDEEQ   /I/: VECTEUR DEEQ DU NUMDDL
C  NEQ     /I/: NOMBRE DE DDL ASSEMBLES PAR L'UTILISATEUR
C  MCODDL  /O/: MATRICE CREEE (NBNO*2)
C  IDEC    /M/: VECTEUR ENTIER DE TRAVAIL DE  DECODAGE
C
C-----------------------------------------------------------------------
C
      INTEGER LINO(NBNO),IDEEQ(2,NEQ),MCODDL(NBNO*NBEC,2),IDEC(NBCMP,2)
      INTEGER I ,INO ,ITYP ,J ,JNO ,K ,NBCMP
      INTEGER NBEC ,NBNO ,NEQ
C-----------------------------------------------------------------------
C
C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
C
C
      IF(NBNO.EQ.0) GOTO 9999
C
C  MISE A ZERO DU COMPTEUR DE DDL ACTIFS
C
C
      DO 10 J=1,NBNO
        DO 30 K=1,NBCMP
          IDEC(K,1)=0
          IDEC(K,2)=0
 30     CONTINUE
C
        JNO=LINO(J)
        DO 20 I=1,NEQ
          INO=IDEEQ(1,I)
          IF(JNO.EQ.INO) THEN
            ITYP=IDEEQ(2,I)
            IF(ITYP.LT.0) IDEC(-ITYP,2)=1
            IF(ITYP.GT.0) IDEC(ITYP,1)=1
          ENDIF
 20     CONTINUE
        CALL ISCODE(IDEC(1,1),MCODDL((J-1)*NBEC+1,1),NBCMP)
        CALL ISCODE(IDEC(1,2),MCODDL((J-1)*NBEC+1,2),NBCMP)
 10   CONTINUE
C
 9999 CONTINUE
      END
