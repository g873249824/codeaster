C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*8  NOMA
      CHARACTER*24 DEFICO,RESOCO
      CHARACTER*19 DDEPLA
      INTEGER      CTCCVG
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE DISCRETE - ALGORITHME)
C
C POST-TRAITEMENT ALGORITHMES
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  DDEPLA : INCREMENT DE DEPLACEMENT
C OUT CTCCVG : CODE RETOUR CONTACT DISCRET
C                -1 : PAS DE CALCUL DU CONTACT DISCRET
C                 0 : CAS DU FONCTIONNEMENT NORMAL
C                 1 : NOMBRE MAXI D'ITERATIONS
C                 2 : MATRICE SINGULIERE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      CHARACTER*19 DDEPLC
      INTEGER      JDDEPC
      LOGICAL      CFDISL,LPENAC,LGCP
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> ...... POST-TRAITEMENT DU CALCUL'
      ENDIF
C
C --- PARAMETRES
C
      LPENAC = CFDISL(DEFICO,'CONT_PENA')
      LGCP   = CFDISL(DEFICO,'CONT_GCP' )
C
C --- SORTIE EN ERREUR
C
      IF (CTCCVG.NE.0) THEN
        IF (.NOT.LGCP) THEN
          IF (NIV.GE.2) THEN
            WRITE (IFM,*) '<CONTACT> ...... SORTIE DIRECTE CAR ERREUR'
          ENDIF
          GOTO 999
        ENDIF
      ENDIF
C
C --- ACCES AUX CHAMPS DE TRAVAIL
C --- DDEPLC: INCREMENT DE SOLUTION APRES CORRECTION DU CONTACT
C
      DDEPLC = RESOCO(1:14)//'.DELC'
      CALL JEVEUO(DDEPLC(1:19)//'.VALE','L',JDDEPC)
C
C --- RECOPIE CHAMP DE DEPLACEMENT SOLUTION
C
      IF ((.NOT.LPENAC).AND.(CTCCVG.EQ.0)) THEN
        CALL COPISD('CHAMP_GD','V',DDEPLC,DDEPLA)
      ENDIF
C
C --- CALCUL DES JEUX FINAUX
C
      CALL CFJEFI(NOMA  ,DEFICO,RESOCO,DDEPLA)
C
 999  CONTINUE
C
      CALL JEDEMA()
C
      END
