C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT NONE
      CHARACTER*(*) MODELZ,CARELZ,COMPLZ,VECELZ,MATE
      CHARACTER*24 COMPOR

C ----------------------------------------------------------------------
C     CALCUL DES VECTEURS ELEMENTAIRES DES CHARGEMENTS THERMIQUES

C IN  MODELZ  : NOM DU MODELE
C IN  CARELZ  : CARACTERISTIQUES DES POUTRES ET COQUES
C IN  MATE    : MATERIAU
C IN  COMPLU  : SD VARI_COM A L'INSTANT T+
C OUT/JXOUT  VECELZ  : VECT_ELEM RESULTAT.

      INTEGER      MXNBIN,MXNBOU,NBIN,NBOUT
      PARAMETER    (MXNBIN=27,MXNBOU=2)
      INTEGER      IBID,IRET,ICH
      CHARACTER*8  LPAIN(MXNBIN),LPAOUT(MXNBOU),NEWNOM
      CHARACTER*14 COMPLU
      CHARACTER*16 OPTION
      CHARACTER*19 VECELE,RESUEL,CHVREF,CHSITH
      CHARACTER*24 CHGEOM,CHCARA(18),CHTIME,LIGRMO,VRCPLU
      CHARACTER*24 LCHIN(MXNBIN),LCHOUT(MXNBOU),MODELE,CARELE
      LOGICAL EXICAR,LTEMP,LBID,LXFEM

      CALL JEMARQ()
      NEWNOM = '.0000000'
      MODELE = MODELZ
      CARELE = CARELZ
      COMPLU = COMPLZ
C     -- POUR NE CREER Q'UN SEUL CHAMP DE VARIABLES DE REFERENCE
      CHVREF = MODELE(1:8)//'.CHVCREF'
C
C     -- ALLOCATION DU VECT_ELEM RESULTAT :
C     -------------------------------------
      VECELE = '&&VEMTPP           '
      CALL DETRSD('VECT_ELEM',VECELE)
      CALL MEMARE('V',VECELE,MODELE(1:8),MATE,CARELE,'CHAR_MECA')
C
C     -- S'IL N'Y A PAS DE TEMPERATURE, IL N'Y A RIEN A FAIRE :
C     ---------------------------------------------------------
      CALL NMVCD2('TEMP',MATE,LTEMP,LBID)
      IF (.NOT.LTEMP) GOTO 9999
C
C     -- S'AGIT-IL D'UN MODELE X-FEM
      CALL EXIXFE(MODELE,IRET)
      LXFEM  = IRET.NE.0
C
C     -- EXTRACTION DES VARIABLES DE COMMANDE
      CALL NMVCEX('TOUT',COMPLU,VRCPLU)
      CALL NMVCEX('INST',COMPLU,CHTIME)
C
C     -- VARIABLE DE COMMANDE DE REFERENCE
      CALL VRCREF(MODELE(1:8),MATE(1:8),CARELE(1:8),CHVREF)
C
      LIGRMO = MODELE(1:8)//'.MODELE'
C
      CALL MEGEOM(MODELE(1:8),CHGEOM)
      CALL MECARA(CARELE(1:8),EXICAR,CHCARA)
C
      OPTION = 'CHAR_MECA_TEMP_R'
C
C     -- CHAMPS ET PARAMETRES IN
C     --------------------------
      LPAIN(1)  = 'PGEOMER'
      LCHIN(1)  = CHGEOM
      LPAIN(2)  = 'PTEMPSR'
      LCHIN(2)  = CHTIME
      LPAIN(3)  = 'PMATERC'
      LCHIN(3)  = MATE
      LPAIN(4)  = 'PCACOQU'
      LCHIN(4)  = CHCARA(7)
      LPAIN(5)  = 'PCAGNPO'
      LCHIN(5)  = CHCARA(6)
      LPAIN(6)  = 'PCADISM'
      LCHIN(6)  = CHCARA(3)
      LPAIN(7)  = 'PCAORIE'
      LCHIN(7)  = CHCARA(1)
      LPAIN(8)  = 'PCAGNBA'
      LCHIN(8)  = CHCARA(11)
      LPAIN(9)  = 'PCAARPO'
      LCHIN(9)  = CHCARA(9)
      LPAIN(10) = 'PCAMASS'
      LCHIN(10) = CHCARA(12)
      LPAIN(11) = 'PVARCPR'
      LCHIN(11) = VRCPLU
      LPAIN(12) = 'PVARCRR'
      LCHIN(12) = CHVREF
      LPAIN(13) = 'PCAGEPO'
      LCHIN(13) = CHCARA(5)
      LPAIN(14) = ' '
      LCHIN(14) = ' '
      LPAIN(15) = 'PNBSP_I'
      LCHIN(15) = CHCARA(1) (1:8)//'.CANBSP'
      LPAIN(16) = 'PFIBRES'
      LCHIN(16) = CHCARA(1) (1:8)//'.CAFIBR'
      LPAIN(17) = 'PCOMPOR'
      LCHIN(17) = COMPOR
      NBIN      = 17
      DO 100 ICH = NBIN+1,MXNBIN
        LCHIN(ICH) = ' '
        LPAIN(ICH) = ' '
  100 CONTINUE
C
C     -- CHAMPS IN SPECIFIQUE A X-FEM
      IF (LXFEM) THEN
        CALL XAJCIN(MODELE,OPTION,MXNBIN,LCHIN,LPAIN,NBIN)
      ENDIF
C
C     -- CHAMPS ET PARAMETRES OUT
C     ---------------------------
      CALL GCNCO2(NEWNOM)
      RESUEL = '&&VECTME.???????'
      RESUEL(10:16) = NEWNOM(2:8)
      CALL CORICH('E',RESUEL,-1,IBID)
      LPAOUT(1) = 'PVECTUR'
      LCHOUT(1) = RESUEL
      LPAOUT(2) = ' '
      LCHOUT(2) = ' '
      NBOUT     = 1
C
C     -- CHAMP OUT SPECIFIQUE A X-FEM
      IF (LXFEM) THEN
        CHSITH='&&VECTME.CHSITH'
        CALL ALCHML(LIGRMO,'SIEF_ELGA','PCONTRR','V',CHSITH,IRET,' ')
        CALL ASSERT(IRET.EQ.0)
        LPAOUT(2) = 'PCONTRT'
        LCHOUT(2) = CHSITH
        NBOUT     = NBOUT+1
      ENDIF
C
C     -- CALCUL DE L'OPTION
C     ---------------------
      CALL CALCUL('C',OPTION,LIGRMO,NBIN,LCHIN,LPAIN,NBOUT,LCHOUT,
     &            LPAOUT,'V','OUI')
      CALL REAJRE(VECELE,LCHOUT(1),'V')
C
C     -- MENAGE
C     ---------
      IF (LXFEM) THEN
        CALL DETRSD('CHAM_ELEM',CHSITH)
      ENDIF
C
9999  CONTINUE
      VECELZ = VECELE//'.RELR'

      CALL JEDEMA()
      END
