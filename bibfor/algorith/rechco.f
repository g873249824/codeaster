C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*8  NOMA
      CHARACTER*24 NEWGEO
      CHARACTER*24 DEFICO
      CHARACTER*24 RESOCO
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT)
C
C RECHERCHE DE LA MAILLE OU DU NOEUD MAITRE ASSOCIE A CHAQUE 
C NOEUD ESCLAVE
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  NEWGEO : GEOMETRIE ACTUALISEE EN TENANT COMPTE DU CHAMP DE
C              DEPLACEMENTS COURANT
C IN  DEFICO : SD DE DEFINITION DU CONTACT (ISSUE D'AFFE_CHAR_MECA)
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      CFDISI
      INTEGER      NTNOE,NBLIAI,NZOCO,MMINFI
      INTEGER      INOE,IZONE,ITEMAX
      INTEGER      IAPPA
      INTEGER      JCOOR
      INTEGER      ILIDEB,ILIFIN,NZLIAI    
      CHARACTER*24 APJEU,APPARI,APPOIN
      INTEGER      JAPJEU,JAPPAR,JAPPTR
      INTEGER      IFM,NIV
      REAL*8       CFDISR,EPSMAX
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()      
      CALL INFDBG('CONTACT',IFM,NIV)      
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> APPARIEMENT DES SURFACES '
      ENDIF 
C
C --- LECTURE DES SD POUR LE CONTACT POTENTIEL
C
      APJEU  = RESOCO(1:14)//'.APJEU'
      APPARI = RESOCO(1:14)//'.APPARI'
      APPOIN = RESOCO(1:14)//'.APPOIN'
C
      CALL JEVEUO(APJEU ,'E',JAPJEU)
      CALL JEVEUO(APPARI,'E',JAPPAR)
      CALL JEVEUO(APPOIN,'E',JAPPTR) 
C
C --- INITIALISATION DE VARIABLES
C
      NZOCO  = CFDISI(DEFICO,'NZOCO')
      NTNOE  = CFDISI(DEFICO,'NTNOE')          
C
C --- COORDONNEES DES NOEUDS DU MAILLAGE
C
      CALL JEVEUO(NEWGEO(1:19)//'.VALE','L',JCOOR)
C
C --- INFOS GENERIQUES POUR L'ALGORITHME D'APPARIEMENT
C         
      EPSMAX = CFDISR(DEFICO,'PROJ_NEWT_RESI')
      ITEMAX = CFDISI(DEFICO,'PROJ_NEWT_ITER')       
C
C --- CALCUL DES NORMALES/TANGENTES EN CHAQUE NOEUD (SI NECESSAIRE)
C     
      CALL CFCALN(NOMA  ,DEFICO,RESOCO,NEWGEO,ITEMAX,
     &            EPSMAX)
C
C --- APPARIEMENT
C    
      ZI(JAPPTR) = 0
C
C --- MISE A ZERO DU JEU
C
      DO 8 INOE = 1,NTNOE
        ZR(JAPJEU+INOE-1) = 0.D0
 8    CONTINUE
C
C --- INDICE DE DEBUT DE LA LIAISON COURANTE
C 
      ILIFIN = 0
      NBLIAI = 0
C
      DO 10 IZONE = 1,NZOCO
C
C --- REACTUALISATION DE L'APPARIEMENT POUR CETTE ZONE
C
        IF (NIV.GE.2) THEN
          WRITE (IFM,*) '<CONTACT> REAC. APPARIEMENT POUR ZONE ',IZONE 
        ENDIF        
C          
C --- DEBUT DU STOCKAGE DES INFOS SUR LES LIAISONS
C --- POUR LA ZONE COURANTE
C
        ILIDEB = ILIFIN + 1
        NZLIAI = 0
C
C --- TYPE D'APPARIEMENT
C
        IAPPA  = MMINFI(DEFICO,'APPARIEMENT'    ,IZONE )
C
C --- APPEL DE LA METHODE D'APPARIEMENT
C
        IF (IAPPA.EQ.0) THEN
          CALL RECHNO(NOMA  ,IZONE ,NEWGEO,DEFICO,RESOCO,
     &                ILIDEB,NZLIAI)
        ELSEIF (IAPPA .EQ. 1) THEN          
          CALL RECHME(NOMA  ,IZONE ,NEWGEO,DEFICO,RESOCO,
     &                ILIDEB,NZLIAI)  
        ELSE
          CALL ASSERT(.FALSE.)
        END IF                
C
C --- CARACTERISTIQUES DES LIAISONS POUR LA ZONE
C 
        ILIFIN = ILIDEB + NZLIAI - 1
        NBLIAI = NBLIAI + NZLIAI      
        CALL CFPARZ(DEFICO,RESOCO,NEWGEO,IZONE ,ILIDEB,
     &              ILIFIN)
C
 10   CONTINUE
C
C --- STOCKAGE DU NOMBRE DE LIAISONS
C
      ZI(JAPPAR) = NBLIAI
      IF (NBLIAI .GT. NTNOE) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
C
      CALL JEDEMA
      END
