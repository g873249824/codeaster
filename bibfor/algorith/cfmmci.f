      IMPLICIT      NONE
      CHARACTER*24  DEFICO,RESOCO
C      
C ----------------------------------------------------------------------
C
C ROUTINE UTILITAIRE POUR LE CONTACT (TOUTES METHODES)
C
C INITIALISE LES COEFFICIENTS VARIABLES 
C
C ----------------------------------------------------------------------
C
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT  
C
C ------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------
C 
      INTEGER      CFDISI,NZOCO,IZONE
      LOGICAL      CFDISL,LCTCD,LCTCC,LXFCM
      REAL*8       MMINFR,COEFCR,COEFFR,COEFCP,COEFFP,COEFCS,COEFFS
      REAL*8       COEFPN,COEFPT
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- FONCTIONNALITES ACTIVEES
C
      LXFCM  = CFDISL(DEFICO,'FORMUL_XFEM')
      LCTCC  = CFDISL(DEFICO,'FORMUL_CONTINUE')
      LCTCD  = CFDISL(DEFICO,'FORMUL_DISCRETE')
C
C --- INITIALISATIONS
C
      NZOCO  = CFDISI(DEFICO,'NZOCO')
C
C --- REMPLISSAGE INITIAL
C
      DO 10 IZONE = 1,NZOCO
        IF (LCTCC.OR.LXFCM) THEN
          COEFCR = MMINFR(DEFICO,'COEF_REGU_CONT'   ,IZONE )
          COEFFR = MMINFR(DEFICO,'COEF_REGU_FROT'   ,IZONE )
          COEFCP = MMINFR(DEFICO,'COEF_PENA_CONT'   ,IZONE )
          COEFFP = MMINFR(DEFICO,'COEF_PENA_FROT'   ,IZONE )
          COEFCS = MMINFR(DEFICO,'COEF_STAB_CONT'   ,IZONE )
          COEFFS = MMINFR(DEFICO,'COEF_STAB_FROT'   ,IZONE )
          CALL CFMMCO(DEFICO,RESOCO,IZONE,'COEF_REGU_CONT','E',
     &                COEFCR)
          CALL CFMMCO(DEFICO,RESOCO,IZONE,'COEF_REGU_FROT','E',
     &                COEFFR)     
          CALL CFMMCO(DEFICO,RESOCO,IZONE,'COEF_PENA_CONT','E',
     &                COEFCP)
          CALL CFMMCO(DEFICO,RESOCO,IZONE,'COEF_PENA_FROT','E',
     &                COEFFP)  
          CALL CFMMCO(DEFICO,RESOCO,IZONE,'COEF_STAB_CONT','E',
     &                COEFCS)
          CALL CFMMCO(DEFICO,RESOCO,IZONE,'COEF_STAB_FROT','E',
     &                COEFFS)      
        ELSEIF (LCTCD) THEN
          COEFPN = MMINFR(DEFICO,'E_N'              ,IZONE )
          COEFPT = MMINFR(DEFICO,'E_T'              ,IZONE )
          CALL CFMMCO(DEFICO,RESOCO,IZONE,'E_N','E',
     &                COEFPN)
          CALL CFMMCO(DEFICO,RESOCO,IZONE,'E_T','E',
     &                COEFPT)         
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF       
 10   CONTINUE
C
      CALL JEDEMA()
      END
