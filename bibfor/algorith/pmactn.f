      SUBROUTINE PMACTN(SDDISC,PARCRI,ITERAT,NUMINS,ITEMAX,SDERRO,
     &                  LICCVG,ACTITE,ACTION)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 19/12/2012   AUTEUR PELLET J.PELLET 
C ======================================================================
