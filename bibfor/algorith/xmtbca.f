C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE NISTOR I.NISTOR
C
      IMPLICIT NONE
C
      LOGICAL       MMCVCA
      CHARACTER*8   MA,MODELE
      CHARACTER*24  DEFICO,DEPPLU,DEPMOI,RESOCO

C ----------------------------------------------------------------------
C                    MISE À JOUR DU STATUT DES POINTS DE CONTACT
C              ET RENVOIE MMCVCA (INDICE DE CONVERGENCE DE LA BOUCLE
C                         SUR LES CONTRAINTES ACTIVES)
C ----------------------------------------------------------------------
C ROUTINE SPECIFIQUE A L'APPROCHE <<GRANDS GLISSEMENTS AVEC XFEM>>,
C TRAVAIL EFFECTUE EN COLLABORATION AVEC I.F.P.
C ----------------------------------------------------------------------
C IN  NOMO   : NOM DE L'OBJET MODÈLE
C IN  MA     : NOM DE L'OBJET MAILLAGE 
C IN  RESOCO : SD CONTACT (RESOLUTION)
C IN  DEFICO : SD CONTACT (DEFINITION)
C IN  DEPMOI : CHAM_NO DES DEPLACEMENTS A L'INSTANT ACTUEL
C IN  DEPMOI : CHAM_NO DES DEPLACEMENTS A L'INSTANT PRECEDENT
C OUT MMCVCA : INDICE DE CONVERGENCE DE LA BOUCLE SUR LES C.A.


C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------

      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------

      INTEGER       IPT,NBPC,SINCO,IPC,IBID
      INTEGER       JTABF,ZTABF,CFMMVD,IFM,NIV
      INTEGER       JVALV,JVALV1,JVALV2
      REAL*8        R8BID
      CHARACTER*8   CARTCF 
      CHARACTER*8   LPAIN(7),LPAOUT(3),K8BID
      CHARACTER*19  LIGRCF,CICOCA,CINDCO,LST,LCHIN(7),LCHOUT(3)
      CHARACTER*19  CINDME
      CHARACTER*24  OLDGEO
C ----------------------------------------------------------------------

      CALL JEMARQ()
      CALL INFDBG('XFEM',IFM,NIV)
      
C --- GEOMEYTRIE INITIALE     
      OLDGEO = MA(1:8)//'.COORDO'

C --- LIGREL DES ELEMENTS TARDIFS DE CONTACT/FROTTEMENT    
C
      LIGRCF = RESOCO(1:14)//'.LIGR'
C      
C --- CARTE POUR ELEMENTS TARDIFS DE CONTACT/FROTTEMENT    
C
      CARTCF = RESOCO(1:14)//'.CART'
      
C----RECUPERATION DE TABFIN---------------------------------------------
      
      CALL JEVEUO(DEFICO(1:16)//'.TABFIN','E',JTABF)
      ZTABF = CFMMVD('ZTABF') 

      CINDCO ='&&XMTBCA.INDCO'
      CINDME ='&&XMTBCA.INDME'
      CICOCA ='&&XMTBCA.CICOCA'

      NBPC = NINT(ZR(JTABF-1+1))

C-----INITIALISATION DE L'INDICATEUR DE CONVERGENCE DE LA BOUCLE
C     SUR LES CONTRAINTES ACTIVES (CONVERGENCE <=> INCOCA =1)

      SINCO=0
      MMCVCA=.TRUE.
      
C----CHAMPS D'ENTRÉE/SORTIE DANS LE TE----------------------------------

      LPAIN(1) = 'PGEOMER'
      LCHIN(1) = OLDGEO
      LPAIN(2) = 'PDEPL_M'
      LCHIN(2) = DEPMOI
      LPAIN(3) = 'PDEPL_P'
      LCHIN(3) = DEPPLU
      LPAIN(4) = 'PCAR_PT'
      LCHIN(4) = CARTCF//'.POINT'
      LPAIN(5) = 'PCAR_PI'
      LCHIN(5) = CARTCF//'.PINTER'
      LPAIN(6) = 'PCAR_AI'
      LCHIN(6) = CARTCF//'.AINTER'
      LPAIN(7) = 'PCAR_CF'
      LCHIN(7) = CARTCF//'.CFACE'
        
      LPAOUT(1) = 'PINCOCA'
      LCHOUT(1) = CICOCA
      LPAOUT(2) = 'PINDCOT'
      LCHOUT(2) = CINDCO
      LPAOUT(3) = 'PINDMEM'
      LCHOUT(3) = CINDME

      CALL CALCUL('S','XCVBCA',LIGRCF,7,LCHIN,LPAIN,3,
     &                                   LCHOUT,LPAOUT,'V')

C-----ON FAIT SINCO = SOMME DES CICOCA SUR LES ÉLTS DU LIGREL

      CALL JEVEUO(CICOCA//'.CELV','L',JVALV)    
       DO 10 IPC = 1,NBPC
        SINCO=SINCO+ZI(JVALV-1+IPC)
 10   CONTINUE    

C-----SI SINCO EST STRICTEMENT POSITIF, ALORS ON A EU UN CODE RETOUR
C     SUPERIEUR A ZERO SUR UN ELEMENT ET DONC ON A PAS CONVERGÉ

      IF (SINCO.GT.0) MMCVCA=.FALSE.

C --- MISE A JOUR DU STATUT DE CONTACT
      CALL JEVEUO(CINDCO//'.CELV','L',JVALV1)    
      DO 20 IPC = 1,NBPC
        ZR(JTABF+ZTABF*(IPC-1)+13)=ZR(JVALV1-1+IPC)
 20   CONTINUE

C --- MISE A JOUR DE LA MEMOIRE DE CONTACT
      CALL JEVEUO(CINDME//'.CELV','L',JVALV2)    
      DO 30 IPC = 1,NBPC
        ZR(JTABF+ZTABF*(IPC-1)+28)=ZI(JVALV2-1+IPC)
 30   CONTINUE


      CALL JEDEMA()
      END
