C ======================================================================
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXR8,JEXATR
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C
      CHARACTER*8  K8BID
      CHARACTER*16 K16BID,NOMCMD,NOMOPT
C
C ----------------------------------------------------------------------
C
C --- RECUPERATION DE LA COMMANDE : SI C'EST THER_MECA_NOLI ON AJOUTERA
C --- LE SUFFIXE _MECA AU NOM DE MOT-CLE 'OPTION'
C
      CALL JEMARQ()
      CALL GETRES (K8BID,K16BID,NOMCMD)
      IF (NOMCMD(1:14).EQ.'THER_MECA_NOLI') THEN
         NOMOPT = 'OPTION_MECA'
      ELSE
         NOMOPT = 'OPTION'
      END IF
C
C --- RECUPERATION DE LA LISTE DES OPTIONS DE CALCUL
C
      CALL GETVTX(' ',NOMOPT,0,1,0,K8BID,N7)
      NOPT = -N7
      LISOPT = ' '
      IF (NOPT.EQ.0) GOTO 9998
      LISOPT = '&&NMDOOP.OPTION'
      CALL JEEXIN(LISOPT,IER)
      IF (IER.NE.0) CALL JEDETR(LISOPT)
      IF (NOPT.EQ.1) THEN
        CALL GETVTX(' ',NOMOPT,0,1,NOPT,OPT,IBID)
        IF (OPT.EQ.'ELNO') THEN
          NOPT = 2
          CALL WKVECT(LISOPT,'V V K16',NOPT,JOPT)
          ZK16(JOPT) = 'SIEF_ELNO_ELGA'
          ZK16(JOPT+1) = 'VARI_ELNO_ELGA'
          GOTO 9998
        ELSEIF (OPT.EQ.'SANS') THEN
          NOPT = 0
          GOTO 9998
        END IF
      END IF
      CALL WKVECT(LISOPT,'V V K16',NOPT,JOPT)
      CALL GETVTX(' ',NOMOPT,0,1,NOPT,ZK16(JOPT),IBID)
 9998 CONTINUE
      CALL JEDEMA()
      END
