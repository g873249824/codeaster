C
C LA CHARGE EST UNE FONCTION DU TEMPS ?
C
C ----------------------------------------------------------------------
C
C RETOURNE .TRUE. SI LA CHARGE EST FONCTION DU TEMPS:
C  * AFFE_*_F : SI C'EST UNE FONCTION DU TEMPS
C  * AFFE_*   : S'IL Y A UNE FONC_MULT
C
C IN  LISCHA : SD LISTE DES CHARGES
C IN  ICHAR  : INDICE DE LA CHARGE
C
C
C
C
      CHARACTER*8  TYPECH
      CHARACTER*16 TYPFCT
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      LISCFT = .FALSE.
C
C --- TYPE DE LA CHARGE
C
      CALL LISLTC(LISCHA,ICHAR ,TYPECH)
C
C --- TYPE DE LA FONCTION MULTIPLICATRICE
C
      CALL LISLTF(LISCHA,ICHAR ,TYPFCT)
C
      IF (TYPECH.EQ.'FONC_FT')    LISCFT = .TRUE.
      IF (TYPFCT(1:5).EQ.'FONCT') LISCFT = .TRUE.
C
      CALL JEDEMA()
      END
