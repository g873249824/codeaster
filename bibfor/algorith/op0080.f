C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C***********************************************************************
C  P. RICHARD     DATE 12/03/91
C-----------------------------------------------------------------------
C  BUT : OPERATEUR DE CALCUL DE MODES PAR SOUS-STRUCTURATION CYCLIQUE
C-----------------------------------------------------------------------
C
C-------- DEBUT COMMUNS NORMALISES  JEVEUX  ----------------------------
C
      INTEGER          ZI
      COMMON  /IVARJE/ ZI(1)
      REAL*8           ZR
      COMMON  /RVARJE/ ZR(1)
      COMPLEX*16       ZC
      COMMON  /CVARJE/ ZC(1)
      LOGICAL          ZL
      COMMON  /LVARJE/ ZL(1)
      CHARACTER*8      ZK8
      CHARACTER*16              ZK16
      CHARACTER*24                        ZK24
      CHARACTER*32                                  ZK32
      CHARACTER*80                                            ZK80
      COMMON  /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C-----  FIN  COMMUNS NORMALISES  JEVEUX  -------------------------------
C
      INTEGER      IER
      CHARACTER*8  NOMRES,MODCY1,K8B,BASMOD,MODELE,CHMAT,CARAEL
      CHARACTER*16 NOMOPE,NOMCON
C
C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
C
C --- PHASE DE VERIFICATION
C
      IER    = 0
C
      CALL INFMAJ
C
      CALL GETRES(NOMRES,NOMCON,NOMOPE)
C
C --- RECUPERATION BASE MODALE OU RESULTAT CYCLIQUE ET CREATION .REFE
C
      CALL REFE80 (NOMRES)
C
C --- RECUPERATION DES PRINCIPAUX ARGUMENTS DE LA COMMANDE
C
      CALL ARGU80 (NOMRES)
C
C --- CREATION DE LA NUMEROTATION DES DDL CYCLIQUES
C
      CALL DESCCY(NOMRES)
C
C --- CALCUL DES SOUS-MATRICES PROJETEES
C
      CALL PROJCY(NOMRES)
C
C --- CALCUL DES MODES PROPRES DEMANDES
C
      CALL CALCYC(NOMRES)
C
C --- IMPRESSION DU CONCEPT RESULTAT
C
      CALL INFNIV(IFM,NIV)
      IF (NIV.GT.1) CALL IMMOCY(NOMRES,IFM)
C
C --- STOCKAGE
C
      CALL JEVEUO(NOMRES//'.CYCL_DESC','L',JNBMOD)
      NBMOD=ZI(JNBMOD)
      CALL GETVID(' ','BASE_MODALE',0,1,1,BASMOD,IOC1)
      CALL JEVEUO(BASMOD//'           .REFD','L',JRAID)
      CALL DISMOI('F','NOM_MODELE',ZK24(JRAID)(1:8),'MATR_ASSE',IBID,
     &     MODELE,IRET)
      CALL DISMOI('F','CHAM_MATER',ZK24(JRAID)(1:8),'MATR_ASSE',IBID,
     &     CHMAT,IRET)
      CALL DISMOI('F','CARA_ELEM',ZK24(JRAID)(1:8),'MATR_ASSE',IBID,
     &     CARAEL,IRET)
      CALL JECREO(NOMRES//'           .MODL' ,'G V K8')
      CALL JEECRA(NOMRES//'           .MODL' ,'LONMAX',NBMOD,K8B)
      CALL JEECRA(NOMRES//'           .MODL' ,'LONUTI',NBMOD,K8B)
      CALL JECREO(NOMRES//'           .MATE' ,'G V K8')
      CALL JEECRA(NOMRES//'           .MATE' ,'LONMAX',NBMOD,K8B)
      CALL JEECRA(NOMRES//'           .MATE' ,'LONUTI',NBMOD,K8B)
      CALL JECREO(NOMRES//'           .CARA' ,'G V K8')
      CALL JEECRA(NOMRES//'           .CARA' ,'LONMAX',NBMOD,K8B)
      CALL JEECRA(NOMRES//'           .CARA' ,'LONUTI',NBMOD,K8B)
      CALL JEVEUO(NOMRES//'           .MODL','E',JMODL)
      CALL JEVEUO(NOMRES//'           .MATE','E',JMATE)
      CALL JEVEUO(NOMRES//'           .CARA','E',JCARA)
      DO  10 I=1,NBMOD
        ZK8(JMODL+I-1)=MODELE
        ZK8(JMATE+I-1)=CHMAT
        ZK8(JCARA+I-1)=CARAEL
 10   CONTINUE

      END
