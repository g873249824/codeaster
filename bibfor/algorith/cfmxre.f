C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT      NONE
      CHARACTER*24  RESOCO,DEFICO,NUMEDD
      LOGICAL       FONACT(*)
      CHARACTER*8   MAILLA
      CHARACTER*19  MATASS,SDDISC
      CHARACTER*19  SOLALG(*),VEASSE(*)
      CHARACTER*(*) MODELZ
      INTEGER       NUMINS
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (POST_TRAITEMENT)
C
C POST_TRAITEMENT DU CONTACT (TOUTES METHODES)
C
C ----------------------------------------------------------------------
C
C      
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  MODELE : SD MODELE
C IN  FONACT : FONCTIONNALITES ACTIVEES (VOIR NMFONC)
C IN  MATASS : MATRICE DE RIGIDITE ASSEMBLE
C IN  MAILLA : NOM DU MAILLAGE
C IN  NUMEDD : NUME_DDL POUR LA CREATION DES CHAM_NO
C IN  NUMINS : NUMERO DU PAS DE CHARGE
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  VEASSE : VARIABLE CHAPEAU POUR NOM DES VECT_ASSE
C IN  SOLALG : VARIABLE CHAPEAU POUR INCREMENTS SOLUTIONS
C
C ------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------
C 
      INTEGER      IFM,NIV,IRET
      CHARACTER*19 CNSINR,CNSPER  
      LOGICAL      ISFONC,LCTCC,LCTCD,LXFCM,LCONT 
      CHARACTER*24 DDEPLA,DEPDEL 
      CHARACTER*19 NMCHEX
      CHARACTER*8  MODELE 
      REAL*8       DIINST,INSTAN
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFNIV(IFM,NIV)
C
C --- INSTANT
C
      INSTAN = DIINST(SDDISC,NUMINS)      
C
C --- INITIALISATIONS
C
      LCTCC  = ISFONC(FONACT,'CONT_CONTINU')
      LCTCD  = ISFONC(FONACT,'CONT_DISCRET') 
      LCONT  = ISFONC(FONACT,'CONTACT')       
      LXFCM  = ISFONC(FONACT,'CONT_XFEM')
      MODELE = MODELZ
      IF (.NOT.LCONT) THEN
        GOTO 999
      ENDIF
C
C --- DECOMPACTION DES VARIABLES CHAPEAUX
C      
      DDEPLA = NMCHEX(SOLALG,'SOLALG','DDEPLA')
      DEPDEL = NMCHEX(SOLALG,'SOLALG','DEPDEL')
C
C --- NOM DU CHAM_NO VALE_CONT ET PERCUSSION
C
      CNSINR = RESOCO(1:14)//'.VALE' 
      CNSPER = RESOCO(1:14)//'.PERC'       
      CALL JEEXIN(CNSINR(1:19)//'.CNSV',IRET)      
      IF (IRET.EQ.0) THEN
        CALL CFMXR0(DEFICO,RESOCO,MAILLA,FONACT)   
      ENDIF     
C
C --- POST-TRAITEMENT
C
      IF (LXFCM) THEN
        CALL XMMRES(DEFICO,DEPDEL,NUMEDD,MODELE,VEASSE,
     &              CNSINR)
      ELSEIF (LCTCC) THEN  
        CALL MMMRES(FONACT,DEFICO,RESOCO,DEPDEL,NUMEDD,
     &              MAILLA,CNSINR,CNSPER,INSTAN,VEASSE)
      ELSEIF (LCTCD) THEN 
        CALL CFRESU(NUMINS,INSTAN,MATASS,DEFICO,RESOCO,
     &              DEPDEL,DDEPLA,MAILLA,CNSINR,CNSPER)
      ELSE
        CALL ASSERT(.FALSE.) 
      ENDIF 
C
 999  CONTINUE              
C
      CALL JEDEMA()
      END
