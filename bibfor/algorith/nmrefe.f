      IMPLICIT NONE
      REAL*8       PARCON(5)
      CHARACTER*19 CNVFRE
      CHARACTER*24 MODELE
      CHARACTER*24 COMPOR
      CHARACTER*24 NUMEDD
      CHARACTER*24 CARCRI
      CHARACTER*24 MATE
      CHARACTER*24 CARELE
      CHARACTER*24 DEPMOI
C ---------------------------------------------------------------------
C
C       INITIALISATION POUR RESI_RELA_REFE
C       CALCUL DE LA CARTE
C
C ---------------------------------------------------------------------
C       IN MODELE K24  MODELE MECANIQUE
C       IN COMPOR K24  CARTE COMPORTEMENT
C      OUT NUMEDD K24  NOM DE LA NUMEROTATION MECANIQUE
C       IN MATE   K24  NOM DU CHAMP DE MATERIAU
C       IN CARELE K24  CARACTERISTIQUES DES ELEMENTS DE STRUCTURE
C IN/JXOUT CARCRI K24  CARTE DES CRITERES DE CONVERGENCE LOCAUX
C IN/JXOUT DEPMOI K24  DEPLACEMENTS INITIAUX
C       IN PARCON  R8  PARAMETRES DU CRITERE DE CONVERGENCE REFERENCE
C                     1 : SIGM_REFE
C                     2 : EPSI_REFE
C                     3 : FLUX_THER_REFE
C                     4 : FLUX_HYD1_REFE
C                     5 : FLUX_HYD2_REFE
C IN/JXOUT CNVFRE K19  FORCE DE REFERENCE POUR CONVERGENCE EN REFERENCE
C
C----------------------------------------------------------------------
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ---------------------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX -----------------------------
C
      LOGICAL      EXIGEO,EXICAR
      INTEGER      IBID,JLVE
      COMPLEX*16   CBID
      CHARACTER*8  K8BID,SIGERE(5),LPAIN(20),LPAOUT(1)
      CHARACTER*19 LIGRMO,VEREFE,CARTE
      CHARACTER*24 CHGEOM,LVEDIP
      CHARACTER*24 LCHOUT(1),LCHIN(20)
      CHARACTER*24 CHCARA(15)
      DATA         LVEDIP  /'&&VEREFE.LISTE_RESU'/
C
C ---------------------------------------------------------------------
C

C -- CONVERGENCE SUR CRITERE EN CONTRAINTE GENERALISEE

      CALL MECARA(CARELE(1:8),EXICAR,CHCARA)
      CARTE  = '&&NMREFE.SIGERE'
      VEREFE = '&&NMREFE.VEREFE'
      LIGRMO = MODELE(1:8) // '.MODELE'
      SIGERE(1) = 'SIGM'
      SIGERE(2) = 'EPSI'
      SIGERE(3) = 'FTHERM'
      SIGERE(4) = 'FHYDR1'
      SIGERE(5) = 'FHYDR2'
      CALL MECACT('V',CARTE,'MODELE',LIGRMO,'PREC', 5, SIGERE,
     &                IBID, PARCON, CBID, K8BID)

      CALL MEGEOM(MODELE, ' ', EXIGEO, CHGEOM)
      LPAIN(1)  = 'PGEOMER'
      LCHIN(1)  =  CHGEOM
      LPAIN(2)  = 'PREFCO'
      LCHIN(2)  =  CARTE
      LPAIN(3)  = 'PCAGNPO'
      LCHIN(3)  = CHCARA(6)
      LPAIN(4)  = 'PCAORIE'
      LCHIN(4)  = CHCARA(1)
      LPAIN(5)  = 'PCOMPOR'
      LCHIN(5)  = COMPOR
      LPAIN(6)  = 'PMATERC'
      LCHIN(6)  = MATE
      LPAIN(7)  = 'PDEPLMR'
      LCHIN(7)  = DEPMOI
      LPAIN(8)  = 'PDEPLPR'
      LCHIN(8)  = DEPMOI
      LPAIN(9)  = 'PCAARPO'
      LCHIN(9)  = CHCARA(9)
      LPAIN(10) = 'PCADISK'
      LCHIN(10) = CHCARA(2)
      LPAIN(11) = 'PCACOQU'
      LCHIN(11) = CHCARA(7)
      LPAIN(12) = 'PHARMON'
      LCHIN(12) = ' '
      LPAIN(13) = 'PCAMASS'
      LCHIN(13) = CHCARA(12)
      LPAIN(14) = 'PCARCRI'
      LCHIN(14) = CARCRI
      LPAIN(15) = 'PINSTMR'
      LCHIN(15) = ' '
      LPAIN(16) = 'PINSTPR'
      LCHIN(16) = ' '
      LPAIN(17) = ' '
      LCHIN(17) = ' '
      LPAIN(18) = 'PCAGEPO'
      LCHIN(18) = CHCARA(5)
      LPAIN(19) = 'PNBSP_I'
      LCHIN(19) = CHCARA(1) (1:8)//'.CANBSP'
      LPAIN(20) = 'PFIBRES'
      LCHIN(20) = CHCARA(1) (1:8)//'.CAFIBR'

      LPAOUT(1) = 'PVECTUR'
      LCHOUT(1) =  VEREFE


      CALL CALCUL('S','REFE_FORC_NODA', LIGRMO, 20, LCHIN, LPAIN,
     &            1 , LCHOUT, LPAOUT, 'V')

      CALL DETRSD('VECT_ELEM',LVEDIP(1:8))
      CALL MEMARE('V',LVEDIP(1:8),MODELE(1:8),' ',' ','CHAR_MECA')
      CALL WKVECT(LVEDIP,'V V K24',1,JLVE)
      CALL JEECRA(LVEDIP,'LONUTI',1,K8BID)
      ZK24(JLVE) = LCHOUT(1)
      CALL ASSMIV('V',CNVFRE,1,LVEDIP,1.D0,NUMEDD,' ','ZERO',1)


      END
