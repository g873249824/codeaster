      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C.......................................................................
C FIN DECLARATION JEVEUX
C.......................................................................

      INTEGER JMACO,JMAESC,JTABF,NTMA,NTPC,IMA,POSMA,NBN,INI
      REAL*8  LAMBDA,LAMBD1,XPG,YPG
      CHARACTER*24 CONTMA,MAESCL,TABFIN
C----------------------------------------------------------------------

      CALL JEMARQ()
C
C --- RECUPERATION DES QCQS DONNEES
C
      CONTMA = DEFICO(1:16)//'.MAILCO'
      MAESCL = DEFICO(1:16)//'.MAESCL'
      TABFIN = DEFICO(1:16)//'.TABFIN'
C
      CALL JEVEUO(CONTMA,'L',JMACO)
      CALL JEVEUO(MAESCL,'L',JMAESC)
      CALL JEVEUO(TABFIN,'E',JTABF)
C
C   BOUCLE SUR LES POINTS DE CONTACT
C
      NTMA = ZI(JMAESC)
      NTPC = 0.D0
C      LAMBDA =0.D0
      DO 20 IMA = 1,NTMA
        POSMA = ZI(JMAESC+3* (IMA-1)+1)
        NBN   = ZI(JMAESC+3* (IMA-1)+3)
        DO 10 INI = 1,NBN
          XPG = ZR(JTABF+16*NTPC+16* (INI-1)+3)
          YPG = ZR(JTABF+16*NTPC+16* (INI-1)+12)
          CALL   CALBET(NOMA,POSMA,DEPPLU,XPG,YPG,LAMBDA,
     +                NEWGEO,DEFICO)
          ZR(JTABF+16*NTPC+16* (INI-1)+14) = LAMBDA
   10   CONTINUE
        NTPC = NTPC + NBN
   20 CONTINUE
      CALL JEDEMA()
      END
