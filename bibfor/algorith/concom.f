C  ROUTINE CONCOM
C    RECHERCHE DES NOEUDS COMMUNS
C  DECLARATIONS
C    INC    : INDICE SUR LES NOEUD          POUR UNE MAILLE CONTACT
C    INR    : INDICE SUR LES NOEUD          POUR UNE MAILLE REFERENCE
C    MACOC  : TABLEAU DES NOMS DES NOEUDS   POUR UNE MAILLE CONTACT
C    MACOR  : TABLEAU DES NOMS DES NOEUDS   POUR UNE MAILLE REFERENCE
C    NBLIC  : NOMBRE DE NOEUD TESTES        POUR UNE MAILLE CONTACT
C    NBLIR  : NOMBRE DE NOEUD TESTES        POUR UNE MAILLE REFERENCE
C    NBNOCO : NOMBRE DE NOEUD COMMUNS
C    NOCOC  : TABLEAU DE RANG DES NOEUDS    POUR UNE MAILLE CONTACT
C
C  MOT_CLEF : ORIE_CONTACT
C
      IMPLICIT REAL*8 (A-H,O-Z)

C     ------------------------------------------------------------------

      INTEGER      NBNOCO,NBLIR,INR,NBLIC,INC,NOCOC(*)
C
      CHARACTER*8  MACOR(NBLIR+2),MACOC(NBLIC+2)

C     ------------------------------------------------------------------

      NBNOCO = 0
      DO 20 INC = 1,NBLIC
         DO 10 INR =1,NBLIR
            IF (MACOR(INR+2).EQ.MACOC(INC+2)) THEN
               NBNOCO = NBNOCO + 1
               NOCOC(NBNOCO) = INC
            ENDIF
   10    CONTINUE
   20 CONTINUE
C
      END
