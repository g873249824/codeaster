      CHARACTER*24 SDSENS,SDIETO
      CHARACTER*8  RESULT
      CHARACTER*19 SDDISC
      INTEGER      NUMARC,NRPASE
      REAL*8       INSTAN
      LOGICAL      FORCE
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME - ARCHIVAGE )
C
C ARCHIVAGE DES CHAMPS
C
C ----------------------------------------------------------------------
C
C
C IN  SDIETO : SD GESTION IN ET OUT
C IN  SDSENS : SD SENSIBILITE
C IN  RESULT : NOM UTILISATEUR DU CONCEPT RESULTAT
C IN  NRPASE : NUMERO PARAMETRE SENSIBLE (0 SI PAS SENSIBLE)
C IN  FORCE  : VRAI SI ON SOUHAITE FORCER L'ARCHIVAGE DE TOUS LES CHAMPS
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  INSTAN : INSTANT D'ARCHIVAGE
C IN  NUMARC : NUMERO D'ARCHIVAGE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24 IOINFO
      INTEGER      JIOINF
      INTEGER      NBCHAM
      INTEGER      ICHAM
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES SD IN ET OUT
C
      IOINFO = SDIETO(1:19)//'.INFO'
      CALL JEVEUO(IOINFO,'L',JIOINF)
      NBCHAM = ZI(JIOINF+1-1)
C
C --- BOUCLE SUR LES CHAMPS
C
      DO 10 ICHAM  = 1,NBCHAM
        CALL NMETEO(RESULT,SDDISC,SDIETO,SDSENS,FORCE ,
     &              NUMARC,INSTAN,NRPASE,ICHAM )     
   10 CONTINUE         
C
      CALL JEDEMA()
      END
