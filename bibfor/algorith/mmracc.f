C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
      IMPLICIT NONE
      CHARACTER*24 DEFICO
      INTEGER      IZONE
      INTEGER      POSMA
      INTEGER      INI
      INTEGER      RACCK
C
C ----------------------------------------------------------------------
C ROUTINE APPELLEE PAR MAPPAR
C ----------------------------------------------------------------------
C
C DIT SI UNE MAILLE ESCLAVE CONTIENT UN NOEUD DE TYPE GROUP_NO_RACC
C
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  IZONE  : NUMERO DE LA ZONE DE CONTACT
C IN  POSMA  : POSITION DE LA MAILLE ESCLAVE
C IN  INI    : NUMERO DU NOEUD ESCLAVE DANS LA MAILLE
C OUT RACCK  : VAUT UN SI NOEUD DE TYPE RACCORD SUR LA MAILLE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*24 NOMACO,PNOMA,CONTNO,RACCNO,PRACC
      INTEGER      JNOMA,JPONO,JNOCO,JRACC,JPRACC
      INTEGER      NUMRAC,NRACC,L,POSNOE,NUMNOE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C      
      NOMACO = DEFICO(1:16) // '.NOMACO'
      PNOMA  = DEFICO(1:16) // '.PNOMACO'
      CONTNO = DEFICO(1:16) // '.NOEUCO'
      RACCNO = DEFICO(1:16) // '.RANOCO'
      PRACC  = DEFICO(1:16) // '.PRANOCO'
                 
      CALL JEVEUO(NOMACO,'L',JNOMA)
      CALL JEVEUO(PNOMA,'L',JPONO)
      CALL JEVEUO(CONTNO,'L',JNOCO)
      CALL JEVEUO(RACCNO,'L',JRACC)
      CALL JEVEUO(PRACC,'L',JPRACC)     
C
C --- ON TESTE SI LA MAILLE CONTIENT UN NOEUD DE RACCORD
C ---- SURFACIQUE GROUP_NO_RACC OU NOEUD_RACC
C
      RACCK  = 0
      POSNOE = ZI(JNOMA+ZI(JPONO+POSMA-1)+INI-1)
      NUMNOE = ZI(JNOCO+POSNOE-1)
      NRACC  = ZI(JPRACC+IZONE) - ZI(JPRACC+IZONE-1)
      DO 10 L = 1,NRACC
        NUMRAC = ZI(JRACC+ZI(JPRACC+IZONE-1)+L-1)
        IF (NUMNOE .EQ. NUMRAC) THEN
          RACCK = 1
          GOTO 15
        END IF
 10   CONTINUE 
 15   CONTINUE
C
      CALL JEDEMA()      
      END
