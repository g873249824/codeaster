C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      INTEGER      IZONE
      CHARACTER*24 DEFICO
      INTEGER      POSMAE,TYPRAC
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT)
C
C EXCLUSION DES NOEUDS DONNES DANS GROUP_NO_RACC
C      
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  IZONE  : NUMERO DE LA ZONE DE CONTACT
C IN  POSMAE : NUMERO DE LA MAILLE ESCLAVE
C OUT TYPRAC : TYPE DE RACCORD LINE/QUAD (ON EST SUR UN QUAD8/9)
C               1 - NOEUD MILIEU 5 EXCLU
C               2 - NOEUD MILIEU 6 EXCLU
C               3 - NOEUD MILIEU 7 EXCLU
C               4 - NOEUD MILIEU 8 EXCLU
C
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      I,SUPPOK,NDRARA(9),POSNNO(9),NUMNNO(9)
      INTEGER      INOE,NNOMAE,NUMNOE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DO 10 I=1,9
        NDRARA(I) = 0
   10 CONTINUE      
      TYPRAC = 0
C
C --- ACCES A LA MAILLE ESCLAVE
C      
      CALL CFPOSN(DEFICO,POSMAE,POSNNO,NNOMAE)
      CALL ASSERT(NNOMAE.LE.9)
      CALL CFNUMN(DEFICO,NNOMAE,POSNNO,NUMNNO)      
C
C --- BOUCLE SUR LES NOEUDS
C
      DO 15 INOE = 1,NNOMAE
        NUMNOE = NUMNNO(INOE)
        CALL CFMMEX(DEFICO,'RACC',IZONE ,NUMNOE,SUPPOK)
        IF (SUPPOK.EQ.1) THEN
          NDRARA(INOE) = 1
        ENDIF
  15  CONTINUE
C
C --- QUEL TYPE DE RACCORD ?
C  
      IF (NDRARA(5).EQ.1) THEN
        TYPRAC = 1
      ELSEIF (NDRARA(6).EQ.1) THEN
        TYPRAC = 2
      ELSEIF (NDRARA(7).EQ.1) THEN
        TYPRAC = 3
      ELSEIF (NDRARA(8).EQ.1) THEN
        TYPRAC = 4
      ENDIF           
C
      CALL JEDEMA()      
      END
