       SUBROUTINE NMGP3D(NNO, NPG, IPOIDS, IVF, IDFDE, GEOMI,TYPMOD,
     &                   OPTION, IMATE, COMPOR, LGPG, CRIT,
     &                   INSTM, INSTP,
     &                   TM, TP, TREF,
     &                   HYDRM, HYDRP,
     &                   SECHM, SECHP, SREF,
     &                   NZ,PHASM, PHASP,
     &                   DEPLM, DDEPL,
     &                   ANGMAS,
     &                   SIGM, VIM,
     &                   DFDIM, DFDIP,
     &                   SIGP, VIP, MATNS, VECTU, CODRET)

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 23/01/2006   AUTEUR MABBAS M.ABBAS 
C ======================================================================
