C  BUT:    < DDL ACTIF CAS CRAIG BAMPTON >
C
C DETERMINER LES ENTIER CODES DES DDL ACTIFS A PARTIR DES ENTIERS CODES
C  DES DDL AUX NOEUDS ET DES ENTIERS CODES RELATIFS AUX DEFORMEES
C  CALCULEES POUR INTERFACE DE TYPE CRAIG-BAMPTON
C    ET DES DDL DEMANDES PAR L'UTILISATEUR
C
C-----------------------------------------------------------------------
C
C NBCMP    /I/: NOMBRE DE COMPOSANTES MAX DE LA GRANDEUR SOUS-JACENTE
C NBNO     /I/: NOMBRE DE NOEUDS DE LA TABLE
C MCODDL   /I/: TABLEAU DES ENTIERS CODES  DDL AUX NOEUDS
C ICONO    /I/: LISTE DES ENTIERS CODES DES DEFORMEES CALCULEES
C ICODAC   /M/: LISTE DES ENTIERS CODES DES DDL ACTIFS DEMANDES
C
C-----------------------------------------------------------------------
C
C-----------------------------------------------------------------------
      INTEGER I ,NBCMP ,NBCPMX ,NBEC ,NBECMX ,NBNO 
C-----------------------------------------------------------------------
      PARAMETER (NBCPMX = 300)
      PARAMETER (NBECMX =  10)
      INTEGER MCODDL(NBNO*NBEC,2),ICONO(NBNO*NBEC),ICODAC(NBNO*NBEC)
      INTEGER IDEC(NBCPMX), ITOUT(NBECMX), ICOCO(NBECMX), ICICI(NBECMX)
C
C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
C
      DO 10 I=1, NBECMX
        ITOUT(I) = 0
        ICOCO(I) = 0
        ICICI(I) = 0
 10    CONTINUE
C
      DO 20 I=1,NBCMP
        IDEC(I)=1
 20    CONTINUE
      CALL ISCODE(IDEC,ITOUT,NBCMP)
C
      IF(NBNO.EQ.0) GOTO 9999
C
      DO 30 I=1,NBNO
        CALL ISGECO(ITOUT,MCODDL((I-1)*NBEC+1,2),NBCMP,-1,ICOCO)
        CALL ISGECO(ICODAC((I-1)*NBEC+1),ICOCO,NBCMP,-1,ICICI)
        CALL ISGECO(ICICI,MCODDL((I-1)*NBEC+1,2),NBCMP,-1,ICOCO)
        CALL ISGECO(ICOCO,ICONO((I-1)*NBEC+1),NBCMP,1,
     +              ICODAC((I-1)*NBEC+1))
 30   CONTINUE
C
 9999 CONTINUE
      END
