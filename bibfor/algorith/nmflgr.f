C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C TOLE CRP_7
C ----------------------------------------------------------------------
      IMPLICIT REAL*8 (A-H,O-Z)
      PARAMETER (NEQ = 6, NBT = 21)
      CHARACTER*16 COMPOR
      REAL*8  E,ALPHA,XLONG0,A
      REAL*8  TMOINS,TPLUS,DLONG0,EFFNOM,TEMPM,TEMPP
      REAL*8  KLV(NBT),EFFNOP,FONO(NEQ)
C ----------------------------------------------------------------------
C
C     TRAITEMENT DE LA RELATION DE COMPORTEMENT NON LINEAIRE
C     (ELASTICITE OU FLUAGE + GRANDISSEMENT ) POUR LES ELEMENTS DE
C     BARRE : CALCUL DE LA MATRICE DE RAIDEUR TANGENTE ET DES FORCES
C     NODALES.
C
C ----------------------------------------------------------------------
C
C IN  : E      : MODULE D'YOUNG
C       ALPHA  : COEFFICIENT DE DILATATION THERMIQUE
C       XLONG0 : LONGUEUR DE L'ELEMENT DE BARRE AU REPOS
C       A      : SECTION DE LA BARRE
C       TMOINS : INSTANT PRECEDENT
C       TPLUS  : INSTANT COURANT
C       DLONG0 : INCREMENT D'ALLONGEMENT DE L'ELEMENT
C       EFFNOM : EFFORT NORMAL PRECEDENT
C       TEMPM  : TEMPERATURE IMPOSEE A L'INSTANT PRECEDENT
C       TEMPP  : TEMPERATURE IMPOSEE A L'INSTANT COURANT
C
C OUT : KLV    : MATRICE DE RAIDEUR TANGENTE
C       EFFNOP : EFFORT NORMAL COURANT
C       FONO   : FORCES NODALES COURANTES
C
C
C --- INITIALISATIONS
C
      CALL R8INIR (NBT,0.D0,KLV,1)
      CALL R8INIR (NEQ,0.D0,FONO,1)
C
C
C --- RIGIDITE ELASTIQUE ET COEFFICIENTS DE FLUAGE ET GRANDISSEMENT
C
      RIGELA = E*A/XLONG0
      DLONGT = XLONG0*ALPHA*(TEMPP-TEMPM)
C
C --- CALCUL DE L'EFFORT NORMAL COURANT
C
      EFFELA = EFFNOM + RIGELA*(DLONG0 - DLONGT)
C
      IF (COMPOR.EQ.'ELAS') THEN
         EFFNOP = EFFELA
      ELSE
         CALL UTMESS ('F','NMFLGR','COMPORTEMENT NON IMPLANTE '
     &                //'POUR LES ELEMENTS DE BARRE')
      END IF
C
C --- CALCUL DES COEFFICIENTS NON NULS DE LA MATRICE TANGENTE
C
      XRIG = RIGELA
      KLV(1)  =  XRIG
      KLV(7)  = -XRIG
      KLV(10) =  XRIG
C
C --- CALCUL DES FORCES NODALES
C
      FONO(1) = -EFFNOP
      FONO(4) =  EFFNOP
C
C ----------------------------------------------------------------------
C
      END
