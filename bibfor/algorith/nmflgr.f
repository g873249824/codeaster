      SUBROUTINE NMFLGR (COMPOR,E,ALPHA,XLONG0,A,
     &                   TMOINS,TPLUS,DLONG0,
     &                   EFFNOM,TEMPM,TEMPP,KLV,EFFNOP,FONO)
