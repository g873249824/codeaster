      COMMON  /IVARJE/ ZI(1)
      REAL*8           ZR
      COMMON  /RVARJE/ ZR(1)
      COMPLEX*16       ZC
      COMMON  /CVARJE/ ZC(1)
      LOGICAL          ZL
      COMMON  /LVARJE/ ZL(1)
      CHARACTER*8      ZK8
      CHARACTER*16           ZK16
      CHARACTER*24                    ZK24
      CHARACTER*32                            ZK32
      CHARACTER*80                                    ZK80
      COMMON  /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C     --- FIN DES COMMUNS JEVEUX ------------------------------------
      LOGICAL       VRAI
      INTEGER       I,J
      INTEGER       IMADE
      INTEGER       IPHI1,IPHI2
      REAL*8        MIJ,CIJ,KIJ,KIJ1,CIJ1,CIJ2
      REAL*8 VALR(2)
      CHARACTER*2   MODEL
      CHARACTER*8   MODMEC
      CHARACTER*9   OPTION
      CHARACTER*14  NUM
      CHARACTER*19  MAX,MAY,MAZ,CHAMNO
      CHARACTER*24  PHIB24
C -----------------------------------------------------------------
           IF (OPTION.EQ.'MASS_AJOU')THEN
             CALL CALMAJ(OPTION,MAX,MAY,MAZ,MODEL,ZK24(IPHI1+J-1)(1:19),
     &               MODMEC,CHAMNO,NUM,VRAI,I,J,MIJ)
           ENDIF

           IF (OPTION.EQ.'AMOR_AJOU')THEN
             CALL CALMAJ(OPTION,MAX,MAY,MAZ,MODEL,ZK24(IPHI2+J-1)(1:19),
     &               MODMEC,CHAMNO,NUM,VRAI,I,J,CIJ1)
             CALL CALAMR(PHIB24,ZK24(IPHI1+J-1)(1:19),ZK24(IMADE+I-1)
     &                ,NUM,J,CIJ2)
             CIJ=CIJ1+CIJ2
             VALR (1) = CIJ1
             VALR (2) = CIJ2
             CALL U2MESG('I','ALGORITH14_80',0,' ',0,0,2,VALR)
           ENDIF

           IF(OPTION.EQ.'RIGI_AJOU') THEN
             CALL CALAMR(PHIB24,ZK24(IPHI2+J-1)(1:19),ZK24(IMADE+I-1)
     +                   ,NUM,J,KIJ1)
             KIJ=KIJ1
           ENDIF
       END
