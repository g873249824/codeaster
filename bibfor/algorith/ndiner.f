C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT      NONE
      CHARACTER*24  NUMEDD      
      CHARACTER*19  SDDYNA
      CHARACTER*24  VITPLU
      CHARACTER*19  MASSE,FOINER     
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C CALCUL DE LA FORCE D'INERTIE DE REFERENCE
C      
C ----------------------------------------------------------------------
C
C
C IN  NUMEDD : NUMEROTATION
C IN  SDDYNA : SD LIEE A LA DYNAMIQUE (CF NDLECT)
C IN  VITPLU : VECT_ASSE DE LA VITESSE
C IN  MASSE  : MATR_ASSE DE LA MASSE
C OUT FOINER : VECTEUR DES FORCES D'INERTIE POUR CONVERGENCE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      IFM,NIV
      INTEGER      POINTM,JFOINE,JVITP,NEQ,IRET
      REAL*8       NDYNRE,COEFAC
      CHARACTER*8  K8BID     
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> CALCUL DES FORCES D''INERTIE' 
      ENDIF       
C
C --- INITIALISATIONS
C           
      CALL DISMOI('F','NB_EQUA',NUMEDD,'NUME_DDL',NEQ,K8BID,IRET)    
C
C --- CALCUL DU TERME D'INERTIE
C
      CALL JEVEUO(MASSE(1:19)//'.&INT','L',POINTM)
      CALL JEVEUO(FOINER(1:19)//'.VALE','E',JFOINE)
      CALL JEVEUO(VITPLU(1:19)//'.VALE','L',JVITP)
      CALL MRMULT('ZERO',POINTM,ZR(JVITP),'R',ZR(JFOINE),1)
      COEFAC = NDYNRE(SDDYNA,'COEF_ACCE_VITE')
      CALL DSCAL(NEQ,-COEFAC,ZR(JFOINE),1)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        CALL NMDEBG('VECT',VITPLU,IFM   )
        CALL NMDEBG('VECT',FOINER,IFM   )
        CALL NMDEBG('MATA',MASSE,IFM   )   
      ENDIF       
C
      CALL JEDEMA()
      END
