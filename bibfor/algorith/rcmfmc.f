      SUBROUTINE RCMFMC(CHMATZ,CHMACZ)
      IMPLICIT   NONE
      CHARACTER*(*) CHMATZ,CHMACZ
C ----------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 14/04/2008   AUTEUR PELLET J.PELLET 
C ======================================================================
