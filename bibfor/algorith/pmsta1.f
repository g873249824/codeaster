     &                  NBPAR,NOMPAR,VR,IGRAD,TYPPAR,NOMVI,
     &                  SDDISC, LICCVG,ITEMAX,CONVER,ACTITE)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 09/11/2012   AUTEUR DELMAS J.DELMAS 
