      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER      NCHINX,NCHIN
      CHARACTER*8  LPAIN(NCHINX)
      CHARACTER*16 OPTION
      CHARACTER*24 MODELE,LCHIN(NCHINX)
C
C ----------------------------------------------------------------------
C
C ROUTINE XFEM
C 
C   COMPLETER LES LISTES DE CHAMPS/PARAMETRES IN AVEC LES CHAMPS
C   SUPPLEMENTAIRES SPECIFIQUES A X-FEM POUR PREPARER L'APPEL A
C   CALCUL DANS VECHTH (CALCUL DES VECTEURS ELEMENTAIRES DES CHARGES
C   THERMIQUES POUR LES OPERATEURS DE THERMIQUE)
C
C  -> POUR L'INSTANT ON NE CALCULE QUE : - CHAR_THER_PARO_F
C                                        - CHAR_THER_PARO_R
C
C ----------------------------------------------------------------------
C
C IN     MODELE : NOM DU MODELE
C IN     NCHINX : TAILLE MAX DE LCHIN ET LPAIN
C IN     OPTION : OPTION DE CALCUL
C IN/OUT LCHIN  : LISTE DES PARAMETRES IN A COMPLETER
C IN/OUT LPAIN  : LISTE DES CHAMPS IN A COMPLETER
C IN/OUT NCHIN  : TAILLE DE LCHIN ET LPAIN A METTRE A JOUR
C
C ----------------------------------------------------------------------
C
      CALL ASSERT(NCHIN+7 .LE. NCHINX)
C
      IF(   OPTION.EQ.'CHAR_THER_PARO_F'
     &  .OR.OPTION.EQ.'CHAR_THER_PARO_R') THEN
C
        LPAIN(NCHIN+1) = 'PPINTER'
        LCHIN(NCHIN+1) = MODELE(1:8)//'.TOPOFAC.OE'
        LPAIN(NCHIN+2) = 'PAINTER'
        LCHIN(NCHIN+2) = MODELE(1:8)//'.TOPOFAC.AI'
        LPAIN(NCHIN+3) = 'PCFACE'
        LCHIN(NCHIN+3) = MODELE(1:8)//'.TOPOFAC.CF'
        LPAIN(NCHIN+4) = 'PLONGCO'
        LCHIN(NCHIN+4) = MODELE(1:8)//'.TOPOFAC.LO'
        LPAIN(NCHIN+5) = 'PLST'
        LCHIN(NCHIN+5) = MODELE(1:8)//'.LTNO'
        LPAIN(NCHIN+6) = 'PSTANO'
        LCHIN(NCHIN+6) = MODELE(1:8)//'.STNO'
        LPAIN(NCHIN+7) = 'PBASECO'
        LCHIN(NCHIN+7) = MODELE(1:8)//'.TOPOFAC.BA'
        NCHIN = NCHIN+7
C
      ELSE
C
        CALL ASSERT(.FALSE.)
C
      ENDIF
C
      END
