C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      PHASE  = 0.D0
      NPUIS  = 0
      EXIMCP = GETEXM(MOTFAC,'PHAS_DEG')
      IF (EXIMCP.EQ.1) THEN
