      REAL*8  COVA(3,3),METR(2,2),JAC

C.......................................................................
C     CALCUL DU TENSEUR METRIQUE (2X2) ET DE SON JACOBIEN
C.......................................................................
C IN  COVA    COORDONNEES DES VECTEURS DE LA BASE COVARAINTE
C OUT METR    TENSEUR METRIQUE (2X2)
C OUT JAC     JACOBIEN DE LA METRIQUE
C.......................................................................

      INTEGER I,J
      REAL*8  DDOT


C    CALCUL DE LA METRIQUE
      DO 10 I = 1,2
        DO 20 J = 1,2
          METR(I,J) = DDOT(3,COVA(1,I),1,COVA(1,J),1)
 20     CONTINUE
 10   CONTINUE


C    CALCUL DU JACOBIEN
      JAC = SQRT(ABS( METR(1,1)*METR(2,2) - METR(1,2)*METR(2,1) ))

      END
