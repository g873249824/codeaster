      CHARACTER*24 SDSENS
      INTEGER      NRPASE
      CHARACTER*8  NOPASE
      CHARACTER*13 INPSCO       
      CHARACTER*24 STYPSE
      INTEGER      TYPESE
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (CALCUL - CALCUL DES VECT_ELEM)
C
C DONNNES SI CHARGEMENT AVEC SENSIBILITE
C      
C ----------------------------------------------------------------------
C
C
C IN  SDSENS : STRUCTURE DE DONNEES SENSIBILITE
C IN  NRPASE : NUMERO DU PARAMETRE SENSIBLE CONCERNE
C OUT INPSCO : STRUCTURE CONTENANT LA LISTE DES NOMS
C OUT NOPASE : NOM DU PARAMETRE SENSIBLE
C OUT TYPESE : TYPE DE SENSIBILITE
C OUT STYPSE : SOUS-TYPE DE SENSIBILITE DANS CERTAINS CAS 
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      NBPASE
      CHARACTER*24 SENSNB,SENSIN
      INTEGER      JSENSN,JSENSI                 
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES SD SENSIBILITE
C
      SENSIN = SDSENS(1:16)//'.INPSCO '     
      SENSNB = SDSENS(1:16)//'.NBPASE '
      CALL JEVEUO(SENSNB,'L',JSENSN)
      CALL JEVEUO(SENSIN,'L',JSENSI) 
      INPSCO = ZK16(JSENSI+1-1)       
      NBPASE = ZI(JSENSN+1-1)   
C
C --- NOM DU PARAMETRE SENSIBLE
C            
      CALL NMNSLE(SDSENS,NRPASE,'NOPASE',NOPASE)
C
C --- TYPE DE SENSIBILITE
C
      CALL METYSE(NBPASE,INPSCO,NOPASE,TYPESE,STYPSE)             
C
      CALL JEDEMA()      
C
      END
