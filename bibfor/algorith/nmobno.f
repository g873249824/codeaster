C
C ----------------------------------------------------------------------
C
C ROUTINE *_NON_LINE (STRUCTURES DE DONNES - OBSERVATION)
C
C LECTURE NOM DES OBSERVATION
C
C ----------------------------------------------------------------------
C
C
C IN  MOTFAC : MOT-FACTEUR POUR LIRE
C IN  SDOBSE : NOM DE LA SD POUR OBSERVATION
C IN  NBOCC  : NOMBRE D'OCCURRENCES DE MOTFAC
C
C ----------------------------------------------------------------------
C
      INTEGER      IOCC,NBTIT,IBID
      CHARACTER*24 OBSNOM
      INTEGER      JOBSNO
      CHARACTER*16 K16BID
      CHARACTER*80 TITOBS
      CHARACTER*1  CHAINE
      INTEGER      IARG
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- SD POUR SAUVER LES TITRES
C
      OBSNOM = SDOBSE(1:14)//'     .TITR'
      CALL WKVECT(OBSNOM,'V V K80' ,NBOCC,JOBSNO)
C
      DO 10 IOCC = 1 , NBOCC
        CALL IMPFOI(0,1,IOCC,CHAINE)
        TITOBS  = 'OBSERVATION_'//CHAINE
        CALL GETVTX(MOTFAC,'TITRE',IOCC,IARG,0,K16BID,NBTIT )
        NBTIT = - NBTIT
        CALL ASSERT(NBTIT.LE.1)
        IF (NBTIT.NE.0) THEN
          CALL GETVTX(MOTFAC,'TITRE',IOCC,IARG,NBTIT,TITOBS,IBID  )
        ENDIF
        ZK80(JOBSNO+IOCC-1) = TITOBS
 10   CONTINUE
C
      CALL JEDEMA()
      END
