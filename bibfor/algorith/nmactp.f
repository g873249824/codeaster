C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*24 SDIMPR,SDERRO,DEFICO,RESOCO
      CHARACTER*19 SDDISC
      INTEGER      ACTPAS
      INTEGER      NUMINS,NBITER
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C GESTION DES ACTIONS SUITE A UN EVENEMENT DANS POST-TRAITEMENT
C      
C ----------------------------------------------------------------------
C
C
C IN  SDIMPR : SD AFFICHAGE
C IN  SDDISC : SD DISCRETISATION
C IN  NUMINS : NUMERO D'INSTANT
C IN  NBITER : NOMBRE D'ITERATIONS DE NEWTON
C OUT ACTPAS : BOUCLE TEMPS -> ACTION POUR LA SUITE
C     1 - BOUCLE TEMPS - PAS SUIVANT
C     2 - BOUCLE TEMPS - ON REFAIT LE PAS
C     3 - BOUCLE TEMPS - ARRET DU CALCUL     
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C 
      INTEGER      RETACT,IEVDAC,ITERAT
      CHARACTER*16 NOMEVD
      INTEGER      IBID
      REAL*8       R8BID
      LOGICAL      LTRUE
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INTIAILISATIONS
C
      RETACT = 0
      ACTPAS = 3
      ITERAT = NBITER - 1
      NOMEVD = ' '
      LTRUE  = .TRUE.
C
C --- RECHERCHE DES EVENEMENTS ACTIVES
C
      CALL NMACTO(SDDISC,IEVDAC)
C
C --- ACTIONS SUITE A UN EVENEMENT
C
      IF (IEVDAC.EQ.0) THEN
        RETACT = 0
      ELSE
        CALL NMEVAC(SDIMPR,SDDISC,DEFICO,RESOCO,IEVDAC,
     &              NUMINS,ITERAT,RETACT)
        CALL UTDIDT('L'   ,SDDISC,'ECHE',IEVDAC,'NOM_EVEN',
     &              R8BID ,IBID  ,NOMEVD)
      ENDIF
C
C --- TRAITEMENT
C
      IF (RETACT.EQ.1) THEN
        ACTPAS = 2
      ELSEIF (RETACT.EQ.2) THEN
        CALL ASSERT(.FALSE.)
      ELSEIF (RETACT.EQ.3) THEN
        ACTPAS = 2
        IF (NOMEVD.EQ.'COLLISION') THEN
          CALL NMERGE(SDERRO,'SET','CCC',LTRUE )
        ENDIF
        IF (NOMEVD.EQ.'INTERPENETRATION') THEN
          CALL NMERGE(SDERRO,'SET','CCP',LTRUE )
        ENDIF
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF   
C
      CALL JEDEMA()
      END
