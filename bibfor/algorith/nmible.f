      SUBROUTINE NMIBLE(NIVEAU,
     &                  PREMIE,MODELE,NOMA  ,DEFICO,RESOCO,
     &                  DEPMOI,FONACT,NEQ   ,DEPDEL,DDEPLA,
     &                  DEPPLU,LISCHA,SOLVEU,NUMEDD,MATASS,
     &                  MEMASS,MASSE ,VITPLU,VITMOI,ACCPLU,
     &                  ACCMOI,VITINI,ACCINI,INST  ,SDDYNA)
C
