      SUBROUTINE NMETC0(MODELE,SDIETO,COMPOR,RESOCO,NBCHAM,
     &                  ZIOCH,CARELE )
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 29/04/2013   AUTEUR ABBAS M.ABBAS 
C ======================================================================
C COPYRIGHT (C) 1991 - 2013  EDF R&D                  WWW.CODE-ASTER.ORG
