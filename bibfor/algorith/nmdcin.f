C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C DECOUPE INITIALE DU PAS DE TEMPS
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION
C IN  SDCRIT : INFORMATIONS RELATIVES A LA CONVERGENCE
C IN  NUMINS : NUMERO D'INSTANT
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C 
      INTEGER  IRET,IBID
      INTEGER  DININS,NOCC,IOCC,I
      REAL*8   R8BID
      CHARACTER*8 K8B
      CHARACTER*19 EVEN
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C      
C     RECHERCHE DU N° D'OCCURENCE DE L'EVENEMENT 'DIVERGENCE_ITER' 
      CALL UTDIDT('L',SDDISC,'ECHE',IBID,'NB_OCC',R8BID,NOCC,K8B)
      DO 30 I=1,NOCC
        CALL UTDIDT('L',SDDISC,'ECHE',I,'NOM_EVEN',R8BID,IBID,EVEN)
        IF (EVEN.EQ.'DIVERGENCE_ITER') IOCC = I
 30   CONTINUE

      CALL ASSERT(IOCC.NE.0)


 210  CONTINUE
      IF ((DININS(SDDISC,NUMINS-1)-DININS(SDDISC,NUMINS)).GT.1) THEN
        
        CALL DIDECO(SDDISC,NUMINS,0,IOCC ,IRET)
        IF (IRET.EQ.0) THEN
          GOTO 210
        ELSE
          CALL U2MESS('F','MECANONLINE2_36')
        ENDIF
      ENDIF      
C          
      CALL JEDEMA()
      END
