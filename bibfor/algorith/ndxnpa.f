      SUBROUTINE NDXNPA(MODELE,MATE  ,CARELE,LISCHA,FONACT,
     &                  SDIMPR,SDDISC,SDDYNA,SDNUME,NUMEDD,
     &                  NUMINS,VALINC,SOLALG)
