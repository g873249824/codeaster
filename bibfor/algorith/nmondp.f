C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C
      IMPLICIT      NONE
      INTEGER       NONDP
      CHARACTER*24  CHONDP
      CHARACTER*19  LISCHA
      LOGICAL       LONDE
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER       JINF,IALICH,NCHAR,ICH,IONDP,NOND
      CHARACTER*24  INFOCH,CHARGE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      LONDE  = .FALSE.
      INFOCH = LISCHA(1:19)//'.INFC'
      CHARGE = LISCHA(1:19)//'.LCHA'
      NONDP  = 0
      CALL JEVEUO(INFOCH,'L',JINF)
      CALL JEVEUO(CHARGE,'L',IALICH)
      CALL GETFAC('EXCIT',NCHAR)
      DO 30 ICH = 1,NCHAR
        IF (ZI(JINF+NCHAR+ICH).EQ.6) THEN
          NONDP = NONDP + 1
        END IF
   30 CONTINUE

C --- RECUPERATION DES DONNEES DE CHARGEMENT PAR ONDE PLANE
      CHONDP = '&&NMONDP.ONDP'
      IF (NONDP.EQ.0) THEN
        CALL WKVECT(CHONDP,'V V K8',1,IONDP)
      ELSE
        LONDE = .TRUE.
        CALL WKVECT(CHONDP,'V V K8',NONDP,IONDP)
        NOND = 0
        DO 40 ICH = 1,NCHAR
          IF (ZI(JINF+NCHAR+ICH).EQ.6) THEN
            NOND = NOND + 1
            ZK8(IONDP+NOND-1) = ZK24(IALICH+ICH-1)
          END IF
   40   CONTINUE
      END IF
      CALL JEDEMA()
      END
