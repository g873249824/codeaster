C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      LOGICAL      PREMIE,LNOPRE
      CHARACTER*8  NOMA
      CHARACTER*24 MODELE
      CHARACTER*24 DEFICO,RESOCO  
      INTEGER      FONACT(*)
      INTEGER      ITERAT
      CHARACTER*24 NUMEDD
C      
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGO - BOUCLE CONTACT)
C             
C REALISE L'APPARIEMENT ENTRE SURFACE ESCLAVE ET SURFACE MAITRE
C      
C ----------------------------------------------------------------------
C 
C      
C IN  NOMA   : NOM DU MAILLAGE
C IN  MODELE : NOM DU MODELE
C IN  PREMIE : VAUT .TRUE. SI PREMIER INSTANT DE STAT/DYNA_NON_LINE
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  NUMEDD : NOM DU NUME_DDL
C IN  LNOPRE : .TRUE. SI ON DOIT SAUTER LA PREDICTION
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      LOGICAL      ISFONC,CFDISL,LCTCD,LCTCC,LTFCM,LCTCG
      CHARACTER*24 DEPGEO,OLDGEO,NEWGEO
      CHARACTER*8  NOMO
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECANONLINE',IFM,NIV) 
C
C --- AFFICHAGE
C
      NOMO   = MODELE(1:8)
C
C --- TYPE DE CONTACT      
      
      LCTCD  = ISFONC(FONACT,'CONT_DISCRET')
      LCTCC  = ISFONC(FONACT,'CONT_CONTINU')
      LTFCM  = CFDISL(DEFICO,'CONT_XFEM_GG')
      LCTCG  = ISFONC(FONACT,'CONT_GEOM')
C
C --- REACTUALISATION DE LA GEOMETRIE
C      
      IF (LTFCM) THEN
        IF (NIV.GE.2) THEN
         WRITE (IFM,*) '<CONTACT> BOUCLE DE REACTUALISATION GEOMETRIQUE'
         WRITE (IFM,*) '<XFEM> ... REACTUALISATION DES FACETTES DE '//
     &                 'CONTACT' 
        ENDIF   
        CALL XREACG(NOMO  ,NOMA  ,DEFICO,RESOCO) 
        IF (NIV.GE.2) THEN
          WRITE (IFM,*) '<XFEM> APPARIEMENT' 
        ENDIF
        CALL XAPPAR(PREMIE,NOMA  ,NOMO,DEFICO,RESOCO)
      ELSEIF (LCTCC) THEN
        OLDGEO = NOMA(1:8)//'.COORDO'
        DEPGEO = RESOCO(1:14)//'.DEPG' 
        NEWGEO = '&&NMCTCG.ACTUGEO'             
        IF (NIV.GE.2) THEN
         WRITE (IFM,*) '<CONTACT> BOUCLE DE REACTUALISATION GEOMETRIQUE'
         WRITE (IFM,*) '<CONTACT> ... REACTUALISATION DES DEPLACEMENTS'
        ENDIF      
        CALL VTGPLD(OLDGEO,1.D0,DEPGEO,'V',NEWGEO)
        IF (NIV.GE.2) THEN
          WRITE (IFM,*) '<CONTACT> ... APPARIEMENT' 
        ENDIF
        CALL MAPPAR(PREMIE,NOMA  ,NUMEDD,DEFICO,RESOCO,
     &              NEWGEO)   
      ELSEIF (LCTCD) THEN
        IF (LCTCG) THEN
          IF (NIV.GE.2) THEN
            WRITE (IFM,*) '<CONTACT> BOUCLE DE REACTUALISATION '//
     &                    ' GEOMETRIQUE'
            WRITE (IFM,*) '<CONTACT> ... INITIALISATION DE LA BOUCLE'
          ENDIF       
          CALL CFINIG(RESOCO,ITERAT,LNOPRE)
        ELSE 
          IF (NIV.GE.2) THEN
            WRITE (IFM,*) '<CONTACT> BOUCLE DE REACTUALISATION '//
     &                    ' GEOMETRIQUE'
            WRITE (IFM,*) '<CONTACT> ... APPARIEMENT INITIAL '//
     &                    '(ET UNIQUE)'
          ENDIF              
        ENDIF 
      ENDIF    
C
      CALL JEDEMA()
      END
