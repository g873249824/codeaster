C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*8  NOMA
      CHARACTER*24 MODELE
      CHARACTER*24 DEFICO,RESOCO
      CHARACTER*24 SDSTAT,SDTIME,SDIMPR
      CHARACTER*19 SDDISC
      CHARACTER*24 NUMEDD
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGO - BOUCLE CONTACT)
C
C BOUCLE GEOMETRIQUE: REALISE L'APPARIEMENT ENTRE SURFACE ESCLAVE ET
C SURFACE MAITRE
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  MODELE : NOM DU MODELE
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  NUMEDD : NOM DU NUME_DDL
C IN  SDTIME : SD TIMER
C IN  SDSTAT : SD STATISTIQUES
C IN  SDIMPR : SD AFFICHAGE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      LOGICAL      PREMIE
      INTEGER      IRET
      CHARACTER*8  NOMO
      INTEGER      CFDISI,IFORM
      INTEGER      IBID
      CHARACTER*16 K16BLA
      REAL*8       R8VIDE,R8BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECANONLINE',IFM,NIV)
C
C --- PREMIER INSTANT ?
C
      CALL DIBCLE(SDDISC,'PREMIE','L',IRET  )
      PREMIE = IRET.EQ.0
C
C --- INITIALISATIONS
C
      NOMO   = MODELE(1:8)
      K16BLA = ' '
      R8BID  = R8VIDE()
C
C --- TYPE DE CONTACT
C
      IFORM  = CFDISI(DEFICO,'FORMULATION')
      IF (IFORM.EQ.1) GOTO 99
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*)
     &     '<MECANONLINE> BOUCLE DE REACTUALISATION GEOMETRIQUE'
      ENDIF
C
C --- REACTUALISATION DE LA GEOMETRIE ET APPARIEMENT
C
      CALL NMTIME(SDTIME,'INI','CONT_GEOM')
      CALL NMTIME(SDTIME,'RUN','CONT_GEOM')
      IF (IFORM.EQ.3) THEN
        CALL XREACG(NOMO  ,RESOCO)
        CALL XAPPAR(PREMIE,NOMA  ,NOMO,DEFICO,RESOCO)
      ELSEIF (IFORM.EQ.2) THEN
        CALL MREACG(NOMA  ,RESOCO)
        CALL MMAPPA(PREMIE,NOMA  ,NUMEDD,DEFICO,RESOCO)
      ENDIF
      CALL NMTIME(SDTIME,'END','CONT_GEOM')
      CALL NMRINC(SDSTAT,'CONT_GEOM')
      CALL IMPSDR(SDIMPR,'BOUC_NOEU',K16BLA,R8BID ,IBID)
      CALL IMPSDR(SDIMPR,'BOUC_VALE',K16BLA,R8BID ,IBID)
C
  99  CONTINUE
C
      CALL JEDEMA()
      END
