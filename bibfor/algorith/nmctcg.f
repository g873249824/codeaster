C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      LOGICAL      LNOPRE
      CHARACTER*8  NOMA
      CHARACTER*24 MODELE
      CHARACTER*24 DEFICO,RESOCO  
      INTEGER      ITERAT
      CHARACTER*19 SDDISC
      CHARACTER*24 NUMEDD
C      
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGO - BOUCLE CONTACT)
C             
C BOUCLE GEOMETRIQUE: REALISE L'APPARIEMENT ENTRE SURFACE ESCLAVE ET
C SURFACE MAITRE
C      
C ----------------------------------------------------------------------
C 
C      
C IN  NOMA   : NOM DU MAILLAGE
C IN  MODELE : NOM DU MODELE
C IN  ITERAT : ITERATION DE NEWTON
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  NUMEDD : NOM DU NUME_DDL
C IN  LNOPRE : .TRUE. SI ON DOIT SAUTER LA PREDICTION
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      LOGICAL      PREMIE
      INTEGER      IRET
      CHARACTER*8  NOMO
      INTEGER      CFDISI,IFORM
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECANONLINE',IFM,NIV) 
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> BOUCLE DE REACTUALISATION GEOMETRIQUE'
      ENDIF       
C
C --- PREMIER INSTANT ?
C
      CALL DIBCLE(SDDISC,'PREMIE','L',IRET  )
      PREMIE = IRET.EQ.0 
C
C --- INITIALISATIONS
C
      NOMO   = MODELE(1:8)
C
C --- TYPE DE CONTACT 
C     
      IFORM  = CFDISI(DEFICO,'FORMULATION')
C
C --- REACTUALISATION DE LA GEOMETRIE
C      
      IF (IFORM.EQ.3) THEN   
        CALL XREACG(NOMO  ,NOMA  ,DEFICO,RESOCO) 
        CALL XAPPAR(PREMIE,NOMA  ,NOMO,DEFICO,RESOCO)
      ELSEIF (IFORM.EQ.2) THEN   
        CALL MREACG(NOMA  ,RESOCO)
        CALL MMAPPA(PREMIE,NOMA  ,NUMEDD,DEFICO,RESOCO)   
      ELSEIF (IFORM.EQ.1) THEN   
        CALL CFINIG(RESOCO,ITERAT,LNOPRE) 
      ENDIF    
C
      CALL JEDEMA()
      END
