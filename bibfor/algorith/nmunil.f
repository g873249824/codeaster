C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT     NONE
      CHARACTER*8  NOMA
      CHARACTER*24 DEPPLU
      CHARACTER*24 DEPDEL
      CHARACTER*24 DDEPLA      
      CHARACTER*24 DEFICU 
      CHARACTER*24 RESOCU
      CHARACTER*24 CNCINE
      INTEGER      ITERAT
      REAL*8       INST
C
C ======================================================================
C ROUTINE APPELEE PAR : NMDEPL
C ======================================================================
C
C TRAITEMENT DES CONDITIONS UNILATERALES (SAUF CONTACT MECANIQUE)
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEPPLU : CHAMP DE DEPLACEMENTS A L'ITERATION DE NEWTON PRECEDENTE
C IN  DEPDEL : INCREMENT DE DEPLACEMENT CUMULE
C IN  DEPPLA : INCREMENT DE DEPLACEMENTS CALCULE EN IGNORANT LE CONTACT
C IN  DEFICU : SD DE DEFINITION (ISSUE D'AFFE_CHAR_MECA)
C IN  RESOCU : SD DE TRAITEMENT NUMERIQUE 
C IN  CNCINE : CHAM_NO CINEMATIQUE
C IN  ITERAT : ITERATION DE NEWTON
C IN  INST   : VALEUR DE L'INSTANT DE CALCUL
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*19 MATR,MATASS
      INTEGER      LDSCON,LMAT
      CHARACTER*24 METHCU
      INTEGER      JMETH
      INTEGER      IFM,NIV,IMETH,NEQ
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C ----------------------------------------------------------------------
      CALL INFNIV(IFM,NIV)      
C
C --- ALGORITHME
C
      METHCU = DEFICU(1:16)//'.METHCU'
      CALL JEVEUO(METHCU,'L',JMETH)
      IMETH  = ZI(JMETH)      
C
C --- RECUPERATION DU DESCRIPTEUR DE LA MATRICE DE LIAISON UNIL
C
      MATR = RESOCU(1:14)//'.MATR'
      CALL MTDSC3( MATR )
      CALL JEVEUO( MATR(1:19)//'.&INT', 'E', LDSCON )
C
C --- RECUPERATION DU DESCRIPTEUR DE LA MATRICE MECANIQUE
C     
      MATASS = '&&MATASS'
      CALL JEVEUO( MATASS//'.&INT', 'E', LMAT )  
C
C --- PRE-TRAITEMENT DES CONDITIONS UNILATERALES
C
      IF (ITERAT.EQ.0)  THEN
        NEQ = ZI(LMAT+2)
        CALL CUPREP(NEQ,DEFICU,RESOCU,DEPPLU,INST)
      ENDIF        
C
C --- CHOIX DE L'ALGO DE CONTACT/FROTTEMENT
C 
      IF (IMETH.EQ.0) THEN
        CALL ALGOCU(DEFICU,RESOCU,LMAT,LDSCON,NOMA,CNCINE,DDEPLA,
     &              DEPPLU)
      ELSE IF (IMETH.EQ.8) THEN
        CALL UTMESS('F','NMUNIL','METHODE NON IMPLANTEE')         
      ELSE
        CALL UTMESS('F','NMUNIL','METHODE NON IMPLANTEE')        
      ENDIF      
      

  999 CONTINUE

C ----------------------------------------------------------------------
      CALL JEDEMA()
      END
