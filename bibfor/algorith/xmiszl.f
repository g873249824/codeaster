C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT NONE
      CHARACTER*24  DEPPLU,DEFICO
      CHARACTER*8   NOMA
C
C ROUTINE APPELEE PAR OP0070, MISE A ZERO DES LAGRANG. DE CONTACT XFEM
C ----------------------------------------------------------------------
C ROUTINE SPECIFIQUE A L'APPROCHE <<GRANDS GLISSEMENTS AVEC XFEM>>,
C TRAVAIL EFFECTUE EN COLLABORATION AVEC I.F.P.
C ----------------------------------------------------------------------
C ...............................................................
C   DECLARATION JEVEUX
C.......................................................................

      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNOM,JEXNUM,JEXATR
C.......................................................................
C FIN DECLARATION JEVEUX
C.......................................................................

      INTEGER      JNOCO,JNOESC,JDIM,NTNO,NTMA,IMA,JCONX1,JCONX2
      INTEGER      INOE,POSNO,NUNOE,INO,IECPCO,JMAESC,POSMA,NNO
      INTEGER      IBID,COMP,IER,JCNS1B,JCNS1D,JCNS1E,JTAB,ZMESX
      INTEGER      CFMMVD
      LOGICAL      LCUMUL(4)
      REAL*8       LCOEFR(4)
      CHARACTER*19 CNS1,CNS1B,CNS1C,CNS1D,CNS1E,PRNO,LICHS(4)
      CHARACTER*24 CONTNO,NDIMCO,NOESCL,MAESCL
      COMPLEX*16   CBID
C----------------------------------------------------------------------

      CALL JEMARQ()

C --- REMPLISSAGE DE NU ABS DES NOEUDS

      MAESCL = DEFICO(1:16)// '.MAESCL'
      CALL JEVEUO(MAESCL,'L',JMAESC)
      NTMA = ZI(JMAESC)

      CALL JEVEUO(NOMA//'.CONNEX','L',JCONX1)
      CALL JEVEUO(JEXATR(NOMA//'.CONNEX','LONCUM'),'L',JCONX2)

      NNO=8
      ZMESX = CFMMVD('ZMESX')
      COMP = 0
C
C --- TABLEAU TEMPORAIRE
C
      CALL WKVECT('&&MISAZL.NNO','V V I',8*NTMA,JTAB)
C

      DO 10 IMA = 1,NTMA
        POSMA  = ZI(JMAESC+ZMESX*(IMA-1)+1)
        DO 20 INO=1,NNO
          NUNOE= ZI(JCONX1-1+ZI(JCONX2+POSMA-1)+INO-1)
          COMP=COMP+1
          ZI(JTAB+COMP-1) = NUNOE
 20     CONTINUE
 10   CONTINUE

C
      CNS1 ='&&MISAZL.CNS1'
      CNS1B='&&MISAZL.CNS1B'
      CNS1C='&&MISAZL.CNS1C'
      CNS1D='&&MISAZL.CNS1D'
      CNS1E='&&MISAZL.CNS1E'

C     CNS1 : CHAMP AUX NOEUDS SIMPLE

      CALL CNOCNS(DEPPLU,'V',CNS1)
      CALL DISMOI('F','PROF_CHNO',DEPPLU,'CHAM_NO',IBID,PRNO,IER)
C
C     CNS1B :CNR LAGS_C
C
      CALL CNSRED(CNS1,COMP,ZI(JTAB),1,'LAGS_C','V',CNS1B)
      CALL CNSRED(CNS1,COMP,ZI(JTAB),1,'LAGS_F1','V',CNS1D)
      CALL CNSRED(CNS1,COMP,ZI(JTAB),1,'LAGS_F2','V',CNS1E)
      CALL JEVEUO(CNS1B//'.CNSV','E',JCNS1B)
      CALL JEVEUO(CNS1D//'.CNSV','E',JCNS1D)
      CALL JEVEUO(CNS1E//'.CNSV','E',JCNS1E)

C     MIAZ

      DO 11,INO = 1,COMP
        ZR(JCNS1B-1+ZI(JTAB+INO-1))=0.D0
        ZR(JCNS1D-1+ZI(JTAB+INO-1))=0.D0
        ZR(JCNS1E-1+ZI(JTAB+INO-1))=0.D0
 11   CONTINUE

      LICHS(1)=CNS1
      LICHS(2)=CNS1B
      LICHS(3)=CNS1D
      LICHS(4)=CNS1E
      LCOEFR(1)=1.D0
      LCOEFR(2)=1.D0
      LCOEFR(3)=1.D0
      LCOEFR(4)=1.D0
      LCUMUL(1)=.FALSE.
      LCUMUL(2)=.FALSE.
      LCUMUL(3)=.FALSE.
      LCUMUL(4)=.FALSE.
C     RESULTAT CNS1C : EST DE TYPE CHAMP AUX NOEUDS SIMPLE

      CALL CNSFUS(4,LICHS,LCUMUL,LCOEFR,CBID,.FALSE.,'V',CNS1C)

C     CONSTRUCTION DE DEPPLU A PARTIR DE CNS (CNS1C)

      CALL CNSCNO(CNS1C,PRNO,'NON','V',DEPPLU,'F',IBID)

      CALL DETRSD('CHAM_NO_S',CNS1)
      CALL DETRSD('CHAM_NO_S',CNS1B)
      CALL DETRSD('CHAM_NO_S',CNS1C)
      CALL DETRSD('CHAM_NO_S',CNS1D)
      CALL DETRSD('CHAM_NO_S',CNS1E)
110    CONTINUE

      CALL JEDETR('&&MISAZL.NNO')
      CALL JEDEMA()
      END
