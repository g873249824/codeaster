      IMPLICIT NONE
      INTEGER      FONACT(*)
      INTEGER      ZDEF,NCOL,UNITE
      CHARACTER*19 SDSUIV,COLACT
C
C ----------------------------------------------------------------------
C
C ROUTINE UTILITAIRE (AFFICHAGE)
C
C LECTURE MOT_CLEF
C
C ----------------------------------------------------------------------
C
C
C IN  SDSUIV : NOM DE LA SD POUR LE SUIVI
C IN  FONACT : FONCTIONNALITES ACTIVEES (VOIR NMFONC)
C IN  ZDEF   : NOMBRE MAXI DE COLONNES DISPONIBLES POUR L'AFFICHAGE
C OUT NCOL   : NOMBRE DE COLONNES ACTIVES
C OUT COLACT : VECTEUR D'ACTIVATION DES COLONNES
C OUT UNITE  : UNITE DU FICHIER POUR AFFICHAGE TABLEAU EN PLUS DE .MESS
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      NBOCC
      CHARACTER*16 MOTFAC
      LOGICAL      LINFRE,LUNITE,LINFTP 
C      
C ----------------------------------------------------------------------
C      
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      MOTFAC = 'AFFICHAGE'    
      IF (ZDEF.NE.40) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- CREATION SD AFFICHAGE - PREPARATION
C
      CALL IMPIN0(ZDEF  ,MOTFAC,COLACT,LINFRE,LINFTP,
     &            UNITE )
      LUNITE = UNITE.NE.0
C
C --- NOMBRE D'OCCURRENCES DU MOT-CLEF FACTEUR
C
      CALL GETFAC(MOTFAC,NBOCC)
      CALL ASSERT(NBOCC.LE.1)
C
C --- CAS STANDARD
C
      CALL IMPSTD(FONACT,LINFRE,LUNITE,LINFTP,ZDEF  ,
     &            COLACT,NCOL  )
C
C --- SUIVIS EN TEMPS REEL
C
      CALL IMPSUI(SDSUIV,ZDEF  ,COLACT,NCOL  )
C
C --- NOMBRE DE COLONNES ACTIVES
C
      CALL ASSERT(NCOL.LE.ZDEF)
C      
      IF (NCOL.GE.15) THEN
        CALL U2MESI('F','MECANONLINE3_1',1,NCOL)
      ENDIF   
C
      CALL JEDEMA()

      END
