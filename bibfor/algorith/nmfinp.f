C
C ROUTINE MECA_NON_LINE (UTILITAIRE)
C
C FIN DU TRANSITOIRE
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  NUMINS : NUMERO D'INSTANT
C IN  LSTOP  : .TRUE. SI DERNIER INSTANT DE LA LISTE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      IBID
      REAL*8       R8BID,PREC,JALON,R8VIDE
      REAL*8       DIINST,INST
      CHARACTER*16 METLIS
      LOGICAL      DIDERN
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      LSTOP = .FALSE.
      INST   = DIINST(SDDISC,NUMINS)
C
C --- PRECISION SUR LES INSTANTS
C --- (LIEE A CELLE DE VAL_MIN DE PAS_MINI DANS DEFI_LIST_INST.CAPY)
C
      PREC   = 1.D-12
C
C --- METHODE DE GESTION DE LA LISTE D'INSTANTS
C
      CALL UTDIDT('L'   ,SDDISC,'LIST',IBID  ,'METHODE' ,
     &            R8BID ,IBID  ,METLIS)
C
C --- CONVERGENCE DU CALCUL: DERNIER PAS !
C
      IF (DIDERN(SDDISC,NUMINS)) LSTOP = .TRUE.
C
C --- CONVERGENCE DU CALCUL: CAS LISTE AUTOMATIQUE
C
      IF (METLIS.EQ.'AUTO') THEN
        CALL NMJALO(SDDISC,INST  ,PREC  ,JALON )
        IF (JALON.EQ.R8VIDE()) LSTOP = .TRUE.
      ENDIF
C
      CALL JEDEMA()
      END
