C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*4  PHASE
      REAL*8       INSTAM,INSTAP
      CHARACTER*19 LISCHA
      CHARACTER*24 MODELE,NUMEDD,MATE  ,CARELE,COMPOR
      CHARACTER*24 VALMOI(8),VALPLU(8)
      CHARACTER*24 COMREF
      CHARACTER*19 CNVCFO,CNVCF1
      
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C CALCUL DES FORCES NODALES LIEES AUX VAR. COMMANDES
C      
C ----------------------------------------------------------------------
C
C
C IN  PHASE  : 'INIT' OU 'INCR'
C IN  MODELE : MODELE
C IN  NUMEDD : NUME_DDL
C IN  MATE   : CHAMP MATERIAU
C IN  CARELE : CARACTERISTIQUES DES ELEMENTS DE STRUCTURE
C IN  COMPOR : COMPORTEMENT
C IN  LISCHA : L_CHARGES
C IN  VALMOI : ETAT EN T-
C IN  VALPLU : ETAT EN T+
C IN  INSTAM : INSTANT MOINS
C IN  INSTAP : INSTANT PLUS
C OUT COMREF : VARI_COM DE REFERENCE
C OUT CNVCFO : FORCE DE REFERENCE LIEE AUX VAR. COMMANDES INITIALE
C OUT CNVCF1 : FORCE DE REFERENCE LIEE AUX VAR. COMMANDES EN T+
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24 COMMOI,COMPLU
                
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()    
C
C -- TRAITEMENT DES VARIABLES DE COMMANDE
C
      IF (PHASE.EQ.'INIT') THEN  
        CALL NMVCOM('VREF',INSTAM,MODELE,MATE  ,CARELE,
     &              LISCHA,VALMOI,COMREF)
        CALL NMVCOM('VMOI',INSTAM,MODELE,MATE  ,CARELE,
     &              LISCHA,VALMOI,COMMOI)             
        CALL NMVCFO(MODELE,LISCHA,NUMEDD,MATE  ,CARELE,
     &              COMPOR,COMREF,COMMOI,CNVCF1)
      ELSEIF (PHASE.EQ.'INCR') THEN
        CALL NMVCOM('VPLU',INSTAP,MODELE,MATE  ,CARELE,
     &              LISCHA,VALPLU,COMPLU)  
        CALL NMVCFO(MODELE,LISCHA,NUMEDD,MATE  ,CARELE,
     &              COMPOR,COMREF,COMPLU,CNVCFO)
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF

C
      CALL JEDEMA()      
C
      END
