      SUBROUTINE PROJEN(IZONE ,NDIM  ,NEWGEO,RESOCO,DEFICO,
     &                  POSESC,POSMAI,NORM  ,TANG  ,COEF  ,
     &                  COFX  ,COFY  ,JEU   ,NBNOM ,POSNO ,
     &                  NBDDLT,DDL)
C     
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 13/03/2007   AUTEUR ABBAS M.ABBAS 
