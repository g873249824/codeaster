C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE MABBAS M.ABBAS
C TOLE CRP_21
C
      IMPLICIT NONE
      INTEGER      NIVEAU
      CHARACTER*8  NOMA
      CHARACTER*24 DEFICO,RESOCO,SDIMPR
      CHARACTER*24 MODELE
      CHARACTER*19 SDDYNA,VALINC(*)
      LOGICAL      MAXREL
      INTEGER      FONACT(*)   
C       
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C ROUTINE D'AIGUILLAGE DES ITERATIONS SE SITUANT ENTRE LES PAS DE TEMPS
C ET L'EQUILIBRE
C      
C ----------------------------------------------------------------------
C
C POUR LE MOMENT, SERT A LA METHODE CONTINUE DE CONTACT
C
C
C LES ITERATIONS ONT LIEU ENTRE CETTE ROUTINE ET SA COUSINE 
C (NMIBLE) QUI COMMUNIQUENT PAR LA VARIABLE NIVEAU
C
C IN  NIVEAU : INDICATEUR D'UTILISATION DE LA BOUCLE 
C                 -1     ON N'UTILISE PAS CETTE BOUCLE
C                  4     BOUCLE CONTACT METHODE CONTINUE
C                         INITIALISATION 
C                  3     BOUCLE CONTACT METHODE CONTINUE
C                         BOUCLE GEOMETRIE
C                  2     BOUCLE CONTACT METHODE CONTINUE
C                         BOUCLE SEUILS DE FROTTEMENT
C                  1     BOUCLE CONTACT METHODE CONTINUE
C                         BOUCLE CONTRAINTES ACTIVES
C IN  MODELE : NOM DU MODELE
C IN  NOMA   : NOM DU MAILLAGE
C IN  FONACT : FONCTIONNALITES ACTIVEES
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  SDIMPR : SD AFFICHAGE
C IN  SDDYNA : SD POUR DYNAMIQUE
C IN  VALINC : VARIABLE CHAPEAU POUR INCREMENTS VARIABLES
C IN  MAXREL : .TRUE. SI CRITERE RESI_GLOB_RELA ET CHARGEMENT = 0,
C                     ON UTILISE RESI_GLOB_MAXI
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C 
      LOGICAL      MMCVCA,MMCVFR,MMCVGO
      LOGICAL      CTCGEO
      REAL*8       R8BID
      INTEGER      INCR
      CHARACTER*16 K16BLA
      CHARACTER*24 CLREAC
      INTEGER      JCLREA
      LOGICAL      ISFONC,LCTCD,LELTC,LELTF,LBOUCF
C
C ----------------------------------------------------------------------
C     
      INCR   = 1  
      K16BLA = ' '  
C       
      IF (NIVEAU.LT.0) THEN 
        GOTO 9999
      ENDIF
C
C --- INFOS SUR LE CONTACT
C
      LCTCD  = ISFONC(FONACT,'CONT_DISCRET') 
      LELTC  = ISFONC(FONACT,'ELT_CONTACT') 
      LELTF  = ISFONC(FONACT,'ELT_FROTTEMENT')
      LBOUCF = ISFONC(FONACT,'BOUCLE_EXT_FROT')       
C      
      GO TO (101,102,103) NIVEAU
C
C --- NIVEAU: 1   BOUCLE CONTACT METHODE CONTINUE
C ---             BOUCLE CONTRAINTES ACTIVES
C
 101  CONTINUE
C
C --- CALCUL STATUTS DU CONTACT
C
      IF (LELTC) THEN
        CALL NMCTCC(NOMA  ,MODELE,FONACT,SDDYNA,DEFICO,
     &              RESOCO,VALINC,MMCVCA)
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
      CALL CFITER(RESOCO,'E','CONT',INCR  ,R8BID)
      CALL MMCONV(NIVEAU,NOMA  ,DEFICO,RESOCO,LELTF ,
     &            SDIMPR,VALINC,MAXREL,MMCVCA,MMCVFR,
     &            MMCVGO)
     
      IF (.NOT.MMCVCA) THEN
        NIVEAU = 1
        GOTO 999      
      ENDIF      
C
C --- NIVEAU: 2   BOUCLE CONTACT METHODE CONTINUE
C ---             BOUCLE SEUILS DE FROTTEMENT
C
 102  CONTINUE
C
C --- SI PAS DE FROTTEMENT -> PAS DE BOUCLE SUR SEUIL DE FROTTEMENT
C      
      IF (.NOT.LBOUCF) THEN 
        GOTO 103
      ENDIF       
      NIVEAU = 2
C
C --- CALCUL SEUILS DE FROTTEMENT
C
      IF (LELTF) THEN
        CALL NMCTCF(NOMA  ,MODELE,FONACT,DEFICO,RESOCO,
     &              VALINC)
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C     
      CALL CFITER(RESOCO,'E','FROT',INCR  ,R8BID)  
      CALL MMCONV(NIVEAU,NOMA  ,DEFICO,RESOCO,LELTF ,
     &            SDIMPR,VALINC,MAXREL,MMCVCA,MMCVFR,
     &            MMCVGO)
C       
      IF (.NOT.MMCVFR) THEN   
        NIVEAU = 2
        GOTO 999      
      ENDIF     
C
C --- NIVEAU: 3   BOUCLE CONTACT METHODE CONTINUE
C ---             BOUCLE GEOMETRIE
C
 103  CONTINUE
C
      NIVEAU = 3
C
      IF (LELTC) THEN 
        CALL MMCONV(NIVEAU,NOMA  ,DEFICO,RESOCO,LELTF ,
     &              SDIMPR,VALINC,MAXREL,MMCVCA,MMCVFR,
     &              MMCVGO)
        IF (MMCVGO) THEN 
          NIVEAU = 0
          GOTO 9999     
        ELSE 
          NIVEAU = 3     
        ENDIF      
      ELSEIF (LCTCD) THEN  
        CLREAC = RESOCO(1:14)//'.REAL'             
        CALL JEVEUO(CLREAC,'L',JCLREA)      
        CTCGEO = ZL(JCLREA+1-1)           
        IF (.NOT.CTCGEO) THEN        
          NIVEAU = 0
          CALL NMIMPR('IMPR','CNV_GEOME',K16BLA,0.D0,0)
          IF (MAXREL) THEN
            CALL NMCVGI('CVG_MX') 
          ELSE
            CALL NMCVGI('CVG_OK') 
          ENDIF
          GOTO 9999     
        ELSE  
          NIVEAU = 3
        ENDIF
      ELSE
        CALL ASSERT(.FALSE.)      
      ENDIF     
C
 999  CONTINUE  
C
C --- AFFICHAGE TABLEAU CONVERGENCE
C
      CALL IMPSDM(SDIMPR,'ITER_NEWT',' ')
      CALL NMIMPR('IMPR','LIGN_TABL',K16BLA,0.D0,0)    
      CALL NMIMPR('IMPR','LIGNE    ',K16BLA,0.D0,0)     
C
 9999 CONTINUE     
C

      END
