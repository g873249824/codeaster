      SUBROUTINE NMTBLE(NIVEAU, PREMIE,
     &                  MAILLA, DEFICO, OLDGEO, NEWGEO,
     &                  DEPMOI, DEPGEO, MAXB,   DEPLAM,
     &                  MMITGO, MMITFR, MMITCA,  IXFEM,  
     &                  DEPPLU, INST,   DECOL,  MODELE,
     &                  MAXREL, IMPRCO, USUFIX, VECNOD,
     &                  NEQ, VECNOX, VECNOY,VECNOZ)

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 09/02/2007   AUTEUR TORKHANI M.TORKHANI 
C ======================================================================
