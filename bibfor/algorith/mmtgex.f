C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*24 DEFICO
      CHARACTER*8  NOMA
      INTEGER      NDIM
      INTEGER      IZONE
      INTEGER      POSMAI
      INTEGER      INO
      INTEGER      NBEXFR,NDEXFR(3)   
      REAL*8       TAU1(3),TAU2(3)
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT - UTILITAIRE)
C
C DEFINI LA BASE TANGENTE LOCALE LORS D'UN NOEUD A EXCLURE
C      
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD POUR LA DEFINITION DU CONTACT
C IN  NOMA   : NOM DU MAILLAGE
C IN  NDIM   : DIMENSION DE L'ESPACE
C IN  IZONE  : NUMERO DE LA ZONE DE CONTACT
C IN  POSMAI : NUMERO DE LA MAILLE
C IN  INO    : NUMERO DU NOEUD DANS LA MAILLE
C IN  NBEXFR : NOMBRE DE NOEUDS A EXCLURE SUR LA MAILLE FROTTEMENT
C IN  NDEXFR : NUMERO DES NOEUDS EXCLUS SUR LA MAILLE FROTTEMENT
C OUT TAU1   : PREMIER VECTEUR TANGENT
C OUT TAU2   : SECOND VECTEUR TANGENT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      INTEGER      POSNOE,NUMNOE
      CHARACTER*24 NOMACO,CONTNO,PNOMA
      INTEGER      JNOMA,JNOCO,JPONO
      REAL*8       MMINFR,NORM(3),COON1(3),COON2(3)
      REAL*8       NORN,NORT1,NORT2,NORME1,R8PREM
      INTEGER      J,N1,N2,POSNO1,POSNO2
      INTEGER      JCOOR,SUPPOK,VALRET
      LOGICAL      MMINFL
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV) 
C
C --- ACCES OBJETS
C 
      NOMACO = DEFICO(1:16)//'.NOMACO'
      CONTNO = DEFICO(1:16)//'.NOEUCO'
      PNOMA  = DEFICO(1:16)//'.PNOMACO'       
C           
      CALL JEVEUO(NOMACO,'L',JNOMA)
      CALL JEVEUO(CONTNO,'L',JNOCO)     
      CALL JEVEUO(PNOMA ,'L',JPONO)        
      CALL JEVEUO(NOMA//'.COORDO    .VALE','L',JCOOR)  
C
C --- INITIALISATIONS 
C
      VALRET = 0
      POSNOE = ZI(JNOMA+ZI(JPONO+POSMAI-1)+INO-1)
      NUMNOE = ZI(JNOCO+POSNOE-1) 
C     
C --- REPERAGE SI LE NOEUD INO EST UN NOEUD A EXCLURE (SUPPOK = 1)
C
      CALL CFMMEX(DEFICO,'FROT',IZONE ,NUMNOE,SUPPOK)               
C
C --- REDEFINITION DU REPERE
C
      IF (SUPPOK.EQ.1) THEN
        IF (NBEXFR.EQ. 1) THEN 
          IF (.NOT.MMINFL(DEFICO,'EXCL_FROT',IZONE )) THEN
            CALL ASSERT(.FALSE.)
          ENDIF
          IF (NDIM .EQ. 2) THEN        
            TAU1(1) = MMINFR(DEFICO,'EXCL_FROT_1_DIRX',IZONE )
            TAU1(2) = MMINFR(DEFICO,'EXCL_FROT_1_DIRY',IZONE )
            TAU1(3) = MMINFR(DEFICO,'EXCL_FROT_1_DIRZ',IZONE )
          ELSEIF (NDIM .EQ. 3) THEN
            TAU2(1) = MMINFR(DEFICO,'EXCL_FROT_1_DIRX',IZONE )
            TAU2(2) = MMINFR(DEFICO,'EXCL_FROT_1_DIRY',IZONE )
            TAU2(3) = MMINFR(DEFICO,'EXCL_FROT_1_DIRZ',IZONE )
            TAU1(1) = MMINFR(DEFICO,'EXCL_FROT_2_DIRX',IZONE )
            TAU1(2) = MMINFR(DEFICO,'EXCL_FROT_2_DIRY',IZONE )
            TAU1(3) = MMINFR(DEFICO,'EXCL_FROT_2_DIRZ',IZONE )
          ELSE
            CALL ASSERT(.FALSE.)    
          ENDIF  
        ELSE
          CALL PROVEC(TAU2,TAU1,NORM)
          IF (INO .EQ. NDEXFR(1)) THEN
            N1     = NUMNOE
            POSNO2 = ZI(JNOMA+ZI(JPONO+POSMAI-1)+NDEXFR(2)-1)
            N2     = ZI(JNOCO+POSNO2-1)
          ELSE
            N2     = NUMNOE
            POSNO1 = ZI(JNOMA+ZI(JPONO+POSMAI-1)+NDEXFR(1)-1)
            N1     = ZI(JNOCO+POSNO1-1)
          END IF
          DO 59 J = 1,3
            COON1(J) = ZR(JCOOR+3*(N1-1)+J-1)
            COON2(J) = ZR(JCOOR+3*(N2-1)+J-1)
 59       CONTINUE
          CALL VDIFF(3,COON1,COON2,TAU2)
          CALL NORMEV(TAU2,NORME1)         
          CALL PROVEC(TAU2,NORM,TAU1)
          NORN  = SQRT(NORM(1)*NORM(1)+NORM(2)*NORM(2)+
     &                 NORM(3)*NORM(3))    
          IF (NORN.LT.R8PREM()) THEN   
            VALRET = 1                          
          ENDIF                   
        ENDIF          
        CALL NORMEV(TAU1,NORT1)
        CALL NORMEV(TAU2,NORT2)
        IF (NORT1.LT.R8PREM()) THEN   
          VALRET = 2
        ELSEIF (NORT2.LT.R8PREM().AND.(NDIM.EQ.3)) THEN   
          VALRET = 3                          
        ENDIF    
      ENDIF
      IF (VALRET.NE.0) THEN
        CALL ASSERT(.FALSE.)
      ENDIF         
C
      CALL JEDEMA()      
      END
