      IMPLICIT NONE
      CHARACTER*8  ALIAS      
      INTEGER      NDIM
      INTEGER      NNO
      REAL*8       COORMA(27)
      REAL*8       COORPT(3)
      LOGICAL      DIRAPP
      REAL*8       DIR(3)
      REAL*8       KSI1,KSI2
      REAL*8       TAU1(3),TAU2(3)
      LOGICAL      LDIST      
      REAL*8       TOLEOU
      INTEGER      NIVERR
      INTEGER      ITEMAX
      REAL*8       EPSMAX
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (TOUTES METHODES - APPARIEMENT)
C
C FAIRE LA PROJECTION D'UN POINT SUR UNE MAILLE DONNEE
C      
C ----------------------------------------------------------------------
C
C
C IN  ALIAS  : TYPE DE MAILLE
C IN  NNO    : NOMBRE DE NOEUD SUR LA MAILLE
C IN  NDIM   : DIMENSION DE LA MAILLE (2 OU 3)
C IN  COORMA : COORDONNEES DES NOEUDS DE LA MAILLE
C IN  COORPT : COORDONNEES DU NOEUD A PROJETER SUR LA MAILLE
C IN  ITEMAX : NOMBRE MAXI D'ITERATIONS DE NEWTON POUR LA PROJECTION
C IN  EPSMAX : RESIDU POUR CONVERGENCE DE NEWTON POUR LA PROJECTION 
C IN  TOLEOU : TOLERANCE POUR LE PROJETE HORS MAILLE
C IN  DIRAPP : VAUT .TRUE. SI APPARIEMENT DANS UNE DIRECTION DE 
C              RECHERCHE DONNEE (PAR DIR)
C IN  DIR    : DIRECTION D'APPARIEMENT
C OUT KSI1   : PREMIERE COORDONNEE PARAMETRIQUE DU POINT PROJETE
C OUT KSI2   : SECONDE COORDONNEE PARAMETRIQUE DU POINT PROJETE
C OUT TAU1   : PREMIER VECTEUR TANGENT EN (KSI1,KSI2)
C OUT TAU2   : SECOND VECTEUR TANGENT EN (KSI1,KSI2)
C OUT LDIST  : VAUT .FALSE. SI LA PROJECTION EST EN DEHORS
C               DE LA MAILLE
C OUT NIVERR : RETOURNE UN CODE ERREUR
C                0  TOUT VA BIEN
C                1  ECHEC DE NEWTON
C
C ----------------------------------------------------------------------
C
      NIVERR = 0
C 
C --- ALGO DE NEWTON POUR LA PROJECTION SUIVANT UNE DIRECTION DONNEE
C
      IF (DIRAPP) THEN
        CALL MMNEWD(ALIAS ,NNO   ,NDIM  ,COORMA,COORPT,
     &              ITEMAX,EPSMAX,DIR   ,KSI1  ,KSI2  ,
     &              TAU1  ,TAU2  ,NIVERR)
      ELSE 
        CALL MMNEWT(ALIAS ,NNO   ,NDIM  ,COORMA,COORPT,
     &              ITEMAX,EPSMAX,KSI1  ,KSI2  ,TAU1  ,
     &              TAU2  ,NIVERR)     
      ENDIF 
      IF (NIVERR.GT.0) THEN  
        GOTO 999  
      ENDIF    
C
C --- AJUSTEMENT PROJECTION HORS ZONE
C
      CALL MMTOLE(ALIAS ,NNO   ,NDIM  ,COORMA,TOLEOU,
     &            KSI1  ,KSI2  ,TAU1  ,TAU2  ,LDIST )
C      
  999 CONTINUE         
  
      END
