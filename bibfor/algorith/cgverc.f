      ELSE

C       POUR LES AUTRES TYPE DE RESULTAT, EXCIT N'EST PAS CONSEILLE
C       (SAUF SI LE RESU PROVIENT DE CREA_RESU, VOIR TEXTE ALARME)
        IF (NEXCI.NE.0) CALL U2MESS('A','RUPTURE0_55')

      ENDIF

      CALL JEDEMA()

      END
