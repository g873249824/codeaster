      CHARACTER*16 TYPSD

      CALL JEMARQ()

      CALL GETTCO(RESU,TYPSD)

      IF (TYPSD.EQ.'DYNA_TRANS') THEN

C       LES RESULTATS DE TYPE DYNA_TRANS NE CONTIENNENT PAS DE CHARGES
C       LE MOT-CLE EXCIT EST DONC OBLIGATOIRE
