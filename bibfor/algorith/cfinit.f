C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*8  MAILLA
      CHARACTER*24 DEFICO,RESOCO
      INTEGER      REINCR
      INTEGER      NUMINS
      LOGICAL      FONACT(*)
      CHARACTER*24 VALMOI(8),VALPLU(8)
      CHARACTER*19 SDDYNA
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES)
C
C INITIALISATION DES PARAMETRES DE CONTACT POUR LA REACTUALISATION
C GEOMETRIQUE
C
C ----------------------------------------------------------------------
C
C
C IN  FONACT : FONCTIONNALITES ACTIVEES
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  SDDYNA : SD DYNAMIQUE
C IN  VALMOI : ETAT EN T-
C IN  VALPLU : ETAT EN T+
C IN  NUMINS : NUMERO D'INSTANT
C IN  REINCR : VALEUR DE REACTUALISATION DE LA MATRICE TANGENTE
C                REAC_INCR
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      CFDISI
      INTEGER      II,IBID,MATTAN,IZONE,NEQ,ITERG
      INTEGER      VECONT(2)
      LOGICAL      LREAC(2),LBID
      CHARACTER*24 CLREAC,CIREAC
      INTEGER      JCLREA,JCIREA
      CHARACTER*24 AUTOC1,AUTOC2 
      INTEGER      JAUTO1,JAUTO2 
      REAL*8       R8BID  
      LOGICAL      ISFONC,NDYNLO,LCTCD,LCTCC,LVERIF,LXFEM  
      CHARACTER*24 K24BID,K24BLA,DEPMOI,VITPLU,ACCPLU  
      CHARACTER*8  K8BID    
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C 
C --- FONCTIONNALITES ACTIVEES
C     
      LCTCD  = ISFONC(FONACT,'CONT_DISCRET')
      LCTCC  = ISFONC(FONACT,'CONT_CONTINU')
      LVERIF = ISFONC(FONACT,'CONT_VERIF')
      LXFEM  = ISFONC(FONACT,'XFEM')
      K24BLA = ' '
C
C --- DECOMPACTION VARIABLES CHAPEAUX
C       
      CALL DESAGG(VALMOI,DEPMOI,K24BID,K24BID,K24BID,
     &            K24BID,K24BID,K24BID,K24BID)
      CALL DESAGG(VALPLU,K24BID,K24BID,K24BID,K24BID,
     &            VITPLU,ACCPLU,K24BID,K24BID) 
      CALL JELIRA(DEPMOI(1:19)//'.VALE','LONMAX',NEQ,K8BID)      
C      
      IF (LCTCD) THEN 
C
C --- ACCES OBJETS
C          
        AUTOC1 = RESOCO(1:14)//'.REA1'
        AUTOC2 = RESOCO(1:14)//'.REA2'
        CLREAC = RESOCO(1:14)//'.REAL' 
        CIREAC = RESOCO(1:14)//'.REAI'                
        CALL JEVEUO(AUTOC1(1:19)//'.VALE','E',JAUTO1)
        CALL JEVEUO(AUTOC2(1:19)//'.VALE','E',JAUTO2)              
        CALL JEVEUO(CLREAC,'E',JCLREA)
        CALL JEVEUO(CIREAC,'E',JCIREA)     
        LREAC(1) = .FALSE.
        LREAC(2) = .FALSE.
C
C --- AUTORISATION DE DESTRUCTION DE LA MATRICE TANGENTE (VOIR NMASFR)
C
        CALL CFDISC(DEFICO,' ',IBID,IBID,IBID,MATTAN)
        IZONE    = 0     
        IF (REINCR.EQ.0) THEN
          IF (MATTAN.EQ.1) THEN
            CALL U2MESS('F','CONTACT_88')
          ENDIF
        ENDIF 
C 
C --- PARAMETRES DE REACTUALISATION GEOMETRIQUE
C       
        VECONT(1) = CFDISI(DEFICO,'REAC_GEOM',IZONE)
        VECONT(2) = 0
        LREAC(1)  = .TRUE.
        DO 10 II = 1, NEQ
         ZR(JAUTO1-1+II) = 0.0D0
         ZR(JAUTO2-1+II) = 0.0D0
10      CONTINUE
C
C --- PAS DE REACTUALISATION GEOMETRIQUE
C
        IF (VECONT(1).EQ.0) THEN
          IF (NUMINS.NE.1) THEN
            LREAC(1) = .FALSE.
          ENDIF
        ENDIF
C
C --- METHODE VERIF: APPARIEMENT FORCE A CHAQUE ITERATION
C         
        IF (LVERIF) THEN
          LREAC(1) = .TRUE.
        ENDIF 
C
C --- SAUVEGARDE
C        
        ZL(JCLREA+1-1) = LREAC(1)
        ZL(JCLREA+2-1) = LREAC(2)
        ZI(JCIREA+1-1) = VECONT(1)
        ZI(JCIREA+2-1) = VECONT(2)                                 
      ENDIF 
C
C --- MISE A ZERO LAGRANGIENS POUR CONTACT CONTINU
C      
      IF (LCTCC) THEN
        CALL MMINFP(0     ,DEFICO,K24BLA,'ITER_GEOM_MAXI',
     &              ITERG ,R8BID ,K24BID,LBID)
        IF (LXFEM) THEN
          IF (ITERG.GT.0) THEN
            CALL XMISZL(DEPMOI,DEFICO,MAILLA)
          ENDIF
        ELSE
          CALL MISAZL(DEPMOI,DEFICO)
        ENDIF

        IF (NDYNLO(SDDYNA,'DYNAMIQUE')) THEN
          CALL MISAZL(ACCPLU,DEFICO)
          CALL MISAZL(VITPLU,DEFICO)
        END IF 
             
      ENDIF          
C 
      CALL JEDEMA()

      END
