C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*24 DEFICO,NEWGEO,RESOCO 
      INTEGER      POSNOM     
      INTEGER      IZONE
      INTEGER      MOYEN,LISSA
      REAL*8       COORDM(3),MNORM(3)
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT - NODAL)
C
C CARACTERISTIQUES DU NOEUD MAITRE 
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE CONTACT (DEFINITION)
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  POSNOM : INDICE DANS CONTNO DU NOEUD MAITRE
C IN  NEWGEO : COORDONNEES REACTUALISEES DES NOEUDS DU MAILLAGE
C IN  IZONE  : REPERAGE ZONE DE CONTACT
C OUT COORDM : COORDONNEES DU NOEUD MAITRE
C OUT MOYEN  : NORMALES D'APPARIEMENT
C               0 MAIT
C               1 MAIT_ESCL
C OUT LISSA  : LISSAGE DES NORMALES
C               0 PAS DE LISSAGE
C               1 LISSAGE
C OUT MNORM  : NORMALE AU NOEUD MAITRE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER       PRONOR,NO
      INTEGER       CFDISI
      CHARACTER*24  CONTNO,NORINI,NORLIS
      INTEGER       JNOCO,JNRINI,JNORLI,JCOOR
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- OBJETS JEVEUX
C
      CONTNO = DEFICO(1:16)//'.NOEUCO'
      NORINI = RESOCO(1:14)//'.NORINI'
      NORLIS = DEFICO(1:16)//'.NORLIS'             
C
      CALL JEVEUO(NORINI,'L',JNRINI)  
      CALL JEVEUO(CONTNO,'L',JNOCO)
      CALL JEVEUO(NORLIS,'L',JNORLI)      
      CALL JEVEUO(NEWGEO(1:19)//'.VALE','L',JCOOR)      
C
C --- COORDONNEES DES NOEUDS DE LA MAILLE MAITRE
C
      NO        = ZI(JNOCO+POSNOM-1)
      COORDM(1) = ZR(JCOOR+3*(NO-1))
      COORDM(2) = ZR(JCOOR+3*(NO-1)+1)
      COORDM(3) = ZR(JCOOR+3*(NO-1)+2)    
C
C --- PROPRIETES DES NORMALES
C
      PRONOR = CFDISI(DEFICO,'NORMALE',IZONE)
      PRONOR = PRONOR + 2 * ZI(JNORLI+1)
      MOYEN  = 0
      IF (PRONOR.EQ.1) THEN
        MOYEN = 1
      ENDIF
C
C --- PAS DE LISSAGE DES NORMALES EN NODAL
C
      LISSA = 0        
      IF (PRONOR.GE.2) THEN
        CALL U2MESS('F','CONTACT_32')
      ENDIF
C
C --- NORMALE MAITRE
C
      MNORM(1) = ZR(JNRINI+3*(POSNOM-1))
      MNORM(2) = ZR(JNRINI+3*(POSNOM-1)+1)
      MNORM(3) = ZR(JNRINI+3*(POSNOM-1)+2)     
C 
      CALL JEDEMA()
      END
