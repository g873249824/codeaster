      IMPLICIT NONE
      CHARACTER*8   RESULT
      CHARACTER*(*) TYPCHZ
      CHARACTER*19  NOMCHA
      INTEGER       NUMARC
C
C ----------------------------------------------------------------------
C
C ROUTINE *_NON_LINE (ALGORITHME - ARCHIVAGE)
C
C ARCHIVAGE D'UN CHAMP
C
C ----------------------------------------------------------------------
C
C
C IN  RESULT : NOM UTILISATEUR DU CONCEPT RESULTAT
C IN  NOMCHA : NOM DU CHAMP
C IN  TYPCHA : TYPE DU CHAMP
C IN  NUMARC : NUMERO D'ARCHIVAGE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      IRET
      CHARACTER*19 CHAMP
      CHARACTER*24 TYPCHA
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C      
      TYPCHA = TYPCHZ
      CHAMP  = ' '
C      
C --- ACCES AU CHAMP DANS SD RESULTAT
C
      CALL RSEXCH(RESULT,TYPCHA,NUMARC,CHAMP ,IRET)
      IF (IRET.GT.100) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- COPIE DU CHAMP DANS SD RESULTAT
C      
      CALL ASSDE2(NOMCHA)
      CALL COPISD('CHAMP_GD','G',NOMCHA,CHAMP)
      CALL RSNOCH(RESULT,TYPCHA,NUMARC,' ')  
C
      CALL JEDEMA()
      END
