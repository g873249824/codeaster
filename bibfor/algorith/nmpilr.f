      IMPLICIT NONE
      INTEGER      NEQ
      CHARACTER*19 CNFEXT,CNFINT,CNDIRI
      REAL*8       RESIDU
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME - PILOTAGE)
C
C CALCUL DE LA NORME MAX DU RESIDU D'EQUILIBRE
C      
C ----------------------------------------------------------------------
C
C
C IN  NEQ    : NOMBRE D'EQUATIONS
C IN  CNFINT : VECTEUR ASSEMBLE DES FORCES INTERIEURES    
C IN  CNDIRI : VECTEUR ASSEMBLE DES REACTIONS D'APPUI
C IN  CNFEXT : VECTEUR ASSEMBLE DES FORCES EXTERIEURS
C OUT RESIDU : NORME MAX DU RESIDU D'EQUILIBRE
C                MAX(CNFINT+CNDIRI-CNFEXT)
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      J,JFINT,JFEXT,JDIRI
      INTEGER      IFM,NIV     
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('PILOTAGE',IFM,NIV)
C
C --- INITIALISATIONS
C
      CALL JEVEUO(CNFINT(1:19)//'.VALE','L',JFINT)
      CALL JEVEUO(CNDIRI(1:19)//'.VALE','L',JDIRI)          
      CALL JEVEUO(CNFEXT(1:19)//'.VALE','L',JFEXT)
      RESIDU = 0.D0
C     
C --- CALCUL
C
      DO 10 J=1, NEQ
        RESIDU = MAX(RESIDU,ABS( ZR(JFINT+J-1)+
     &                           ZR(JDIRI+J-1)- 
     &                           ZR(JFEXT+J-1)) )
  10  CONTINUE
C
      CALL JEDEMA()
      END
