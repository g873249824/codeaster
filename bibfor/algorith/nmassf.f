C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      LOGICAL       FONACT(*)
      CHARACTER*24  RESOCO,SECMBR(8)
      CHARACTER*19  CNFEXT
      REAL*8        ETA
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C ASSEMBLAGE DES FORCES EXTERIEURES
C      
C ----------------------------------------------------------------------
C
C
C IN  FONACT : FONCTIONNALITES SPECIFIQUES ACTIVEES (CF NMFONC)
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      NBCH
      PARAMETER    (NBCH = 4)
      REAL*8       CONST(NBCH)
      CHARACTER*8  TYPCST(NBCH)
      CHARACTER*24 NOMCH(NBCH)
C      
      LOGICAL      ISFONC,LCTCD
      INTEGER      NEQ,I
      CHARACTER*24 K24BID
      CHARACTER*8  K8BID
      CHARACTER*24 CNFEDO,CNFEPI,CNFSDO,CNFSPI
      INTEGER      JFEXT,JATMU 
      INTEGER      IFM,NIV     
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- FONCTIONNALITES ACTIVEES
C    
      LCTCD  = ISFONC(FONACT,'CONT_DISCRET') 
C
C --- DECOMPACTION DES VARIABLES CHAPEAUX
C
      CALL DESAGG(SECMBR,CNFEDO,CNFEPI,K24BID,K24BID,
     &            CNFSDO,CNFSPI,K24BID,K24BID)      
C
C --- REMPLISSAGE DU CHAM_NO CNFEXT COMME SOMME DES SECONDS MEMBRES
C
      TYPCST(1) = 'R'
      TYPCST(2) = 'R'
      TYPCST(3) = 'R'
      TYPCST(4) = 'R'
      CONST(1)  = 1.D0
      CONST(2)  = 1.D0
      CONST(3)  = ETA
      CONST(4)  = ETA
      NOMCH(1)  = CNFEDO
      NOMCH(2)  = CNFSDO
      NOMCH(3)  = CNFEPI
      NOMCH(4)  = CNFSPI
      CALL VTCMBL(NBCH,TYPCST,CONST,TYPCST,NOMCH,'R',CNFEXT)
      IF (NIV.GE.2) THEN
        CALL NMDEBG('VECT',CNFEXT,6) 
      ENDIF      
C
C --- REACTIONS DE CONTACT (METHODES DISCRETES)
C
      IF (LCTCD) THEN
        CALL JEVEUO(RESOCO(1:14)//'.ATMU','L',JATMU)
        CALL JELIRA(CNFEXT(1:19)//'.VALE','LONMAX',NEQ,K8BID)
        CALL JEVEUO(CNFEXT(1:19)//'.VALE','E',JFEXT)
        DO 20 I = 1, NEQ
          ZR(JFEXT+I-1) = ZR(JFEXT+I-1) - ZR(JATMU+I-1)
 20     CONTINUE               
      ENDIF
C
      CALL JEDEMA()
      END
