      IMPLICIT NONE
      CHARACTER*16 MCFACT,EVEN
      INTEGER      IECHEC
      REAL*8       PASMIN,DTMIN
      CHARACTER*16 SUBMET,ACTION,SUBAUT
      INTEGER      NBRPAS,NIVEAU      
      REAL*8       PCPLUS,CMMAXI,PRCOLL,DUCOLL
C
C ----------------------------------------------------------------------
C
C OPERATEUR DEFI_LIST_INST
C
C LECTURE DES PARAMETRES DE L'ACTION
C
C ----------------------------------------------------------------------
C
C IN  MCFACT : MOT-CLEF FACTEUR POUR LIRE L'ECHEC
C IN  IECHEC : NUMERO OCCURRENCE ECHEC
C IN  EVEN   : NOM DE L'EVENEMENT
C IN  DTMIN  : INCREMENT MINIMUM DANS LA LISTE D'INSTANT
C OUT ACTION : ACTION
C OUT SUBMET : TYPE DE SUBDIVISION
C OUT SUBAUT : TYPE DE SUBDIVISION AUTOMATIQUE
C OUT PASMIN : VALEUR DE SUBD_PAS_MINI
C OUT NBRPAS : VALEUR DE SUBD_PAS
C OUT NIVEAU : VALEUR DE SUBD_NIVEAU
C OUT PCPLUS : VALEUR DE PCENT_ITER_PLUS
C OUT CMMAXI : VALEUR DE COEF_MULT_MAXI
C OUT PRCOLL : VALEUR DE PREC_COLLISION
C OUT DUCOLL : VALEUR DE DUREE_COLLISION
C
C ----------------------------------------------------------------------
C
      INTEGER IRET  
C
C ----------------------------------------------------------------------
C
      ACTION = ' '
C
C --- ACTION
C        
      CALL GETVTX(MCFACT,'ACTION',IECHEC,1,1,ACTION,IRET  )
      IF (ACTION.EQ.'ARRET') THEN
C ----- PAS D'OPTIONS
      ELSEIF (ACTION.EQ.'DECOUPE') THEN
        CALL DFLLDC(MCFACT,IECHEC,DTMIN ,EVEN  ,SUBMET,
     &              SUBAUT,PASMIN,NBRPAS,NIVEAU,PRCOLL,
     &              DUCOLL)
      ELSEIF (ACTION.EQ.'ITER_SUPPL') THEN
        CALL DFLLAE(MCFACT,IECHEC,PCPLUS)
        CALL DFLLDC(MCFACT,IECHEC,DTMIN ,EVEN  ,SUBMET,
     &              SUBAUT,PASMIN,NBRPAS,NIVEAU,PRCOLL,
     &              DUCOLL)
      ELSEIF (ACTION.EQ.'ADAPT_COEF_PENA') THEN
        CALL DFLLIN(MCFACT,IECHEC,CMMAXI)
      ELSEIF (ACTION.EQ.'AUTRE_PILOTAGE') THEN
C ----- PAS D'OPTIONS  
        IF ((EVEN.EQ.'DIVE_ERRE').OR.(EVEN.EQ.'COMP_NCVG')) THEN
          ACTION = 'DECOUPE'
        ENDIF  
        CALL DFLLDC(MCFACT,IECHEC,DTMIN ,EVEN  ,SUBMET,
     &              SUBAUT,PASMIN,NBRPAS,NIVEAU,PRCOLL,
     &              DUCOLL)
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
      END
