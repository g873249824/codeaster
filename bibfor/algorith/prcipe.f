      IMPLICIT  NONE
      INTEGER NT,ND,I
      INTEGER DIMCON,NDIM
      REAL*8  CONGEP(DIMCON),S1,S2,S3,T(6),EQUI(6),RAC2
      COMMON /TDIM/  NT,ND
C
        IF      ( NDIM .EQ. 3 )THEN
        NT = 6
        ND = 3
        ELSE IF ( NDIM .EQ. 2 ) THEN
        NT = 4
        ND = 3
        T(5)=0.D0
        T(6)=0.D0
        ELSE IF ( NDIM .EQ. 1 ) THEN
        NT = 1
        ND = 1
        ENDIF
        RAC2 = SQRT (2.D0)
        DO 1 I  = 1,ND
        T(I)   = CONGEP(I)
 1      CONTINUE
        DO 2 I  = ND+1 , NT
        T(I)   = CONGEP(I)/RAC2
 2      CONTINUE
C
       CALL FGEQUI(T,'SIGM',NDIM,EQUI)
       S1=EQUI(3)
       S2=EQUI(4)
       S3=EQUI(5)

      END
