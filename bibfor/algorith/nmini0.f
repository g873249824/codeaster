      SUBROUTINE NMINI0(ZFON  ,ZPMET ,ZPCRI ,ZCONV ,ZINST ,
     &                  ZPCON ,ZNMETH,ZLICC ,FONACT,PARMET,
     &                  PARCRI,CONV  ,INST  ,PARCON,METHOD,  
     &                  LICCVG,ETA   ,NUMINS,PREMIE,MTCPUI,
     &                  MTCPUP,FINPAS,LISCH2)
C     
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 19/12/2007   AUTEUR ABBAS M.ABBAS 
C ======================================================================
C COPYRIGHT (C) 1991 - 2005  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY  
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE MABBAS M.ABBAS
C TOLE CRP_21
C 
      IMPLICIT NONE
      INTEGER      ZFON  ,ZPMET ,ZPCRI ,ZCONV
      INTEGER      ZINST ,ZPCON ,ZNMETH,ZLICC
      LOGICAL      FONACT(ZFON)
      REAL*8       PARMET(ZPMET),PARCRI(ZPCRI),CONV(ZCONV)
      REAL*8       INST(ZINST)  ,PARCON(ZPCON)  
      CHARACTER*16 METHOD(ZNMETH)  
      INTEGER      LICCVG(ZLICC)         
      LOGICAL      PREMIE
      LOGICAL      MTCPUI, MTCPUP, FINPAS
      INTEGER      NUMINS
      REAL*8       ETA
      CHARACTER*19 LISCH2
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (INITIALISATIONS)
C
C PREMIERES INITIALISATIONS DE MECA_NON_LINE: MISES A ZERO
C      
C ----------------------------------------------------------------------
C
C
C IN  NOMPRO : NOM DE LA ROUTINE APPELANTE (OP0070 A PRIORI)
C OUT ETA    : PARAMETRE PILOTAGE
C OUT LONDE  : BOOLEEN D'EXISTENCE DE CHARGES 'ONDE_PLANE'
C OUT LIMPED : BOOLEEN DE PRESENCE D'ELEMENTS 'IMPE_ABSO'
C OUT LICCVG : CODE RETOUR CONVERGENCE CONTACT
C OUT NUMINS : COMPTEUR DE LA BOUCLE EN TEMPS
C OUT PREMIE : BOOLEEN POUR LE PREMIER PAS
C OUT MTCPUI : BOOLEEN DE MANQUE TEMPS CPU (ITERATION)
C OUT MTCPUP : BOOLEEN DE MANQUE TEMPS CPU (PAS DE TEMPS)
C OUT LISCH2 : NOM DE LA SD INFO CHARGE POUR STOCKAGE DANS LA SD
C              RESULTAT
C OUT BASENO : NOM DE BASE DES CONCEPTS
C OUT INPSCO : SD CONTENANT LA LISTE DES NOMS POUR LA SENSIBILITE
C OUT FINPAS : BOOLEEN POUR INDIQUER QU'ON EST A LA FIN DES PAS
C OUT FONACT : FONCTIONNALITES ACTIVEES (VOIR NMFONC)
C OUT FOPREC : BOOLEAN POUR DUPLICATION SECOND MEMBRE (DYNAMIQUE,
C                       ALPHA_METHODE)
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      REAL*8       ZERO
      PARAMETER    (ZERO=0.D0)
      REAL*8       R8VIDE
      CHARACTER*24 NOOBJ
      INTEGER      I
C
C ----------------------------------------------------------------------
C 
C
C --- INITIALISATION DES INDICATEURS DE CONVERGENCE
C 
C              LICCVG(1) : PILOTAGE
C                        =  0 CONVERGENCE
C                        =  1 PAS DE CONVERGENCE
C                        = -1 BORNE ATTEINTE
C              LICCVG(2) : INTEGRATION DE LA LOI DE COMPORTEMENT
C                        = 0 OK
C                        = 1 ECHEC DANS L'INTEGRATION : PAS DE RESULTATS
C                        = 3 SIZZ NON NUL (DEBORST) ON CONTINUE A ITERER
C              LICCVG(3) : TRAITEMENT DU CONTACT 
C                        = 0 OK
C                        = 1 ECHEC (ITER > 2*NBLIAI+1)
C              LICCVG(4) : MATRICE DE CONTACT
C                        = 0 OK
C                        = 1 MATRICE DE CONTACT SINGULIERE
C              LICCVG(5) : MATRICE DU SYSTEME (MATASS)
C                        = 0 OK
C                        = 1 MATRICE SINGULIERE
      DO 1 I=1,ZLICC
        LICCVG(I) = 0
  1   CONTINUE
C
C --- FONCTIONNALITES ACTIVEES               (NMFONC/ISFONC)
C
      DO 2 I=1,ZFON
        FONACT(I) = .FALSE.
  2   CONTINUE
C
C --- PARAMETRES DES METHODES DE RESOLUTION  (NMDOMT)
C
      DO 3 I=1,ZPMET
        PARMET(I) = ZERO
  3   CONTINUE  
C
C --- PARAMETRES DES CRITERES DE CONVERGENCE (NMLECT)
C
      DO 4 I=1,ZPCRI
        PARCRI(I) = ZERO
  4   CONTINUE   
C
C --- INFORMATIONS SUR LA CONVERGENCE DU CALCUL
C
      DO 5 I=1,ZCONV
        CONV  (I) = R8VIDE()
  5   CONTINUE 
      CONV(3)  = -1
      CONV(10) = -1
C
C --- PARAMETRES TEMPORELS
C      INST(1) : INSTAP
C      INST(2) : DELTAT
C      INST(3) : INUTILISE
C      INST(4) : ALPHA (PARAMETRE DYNAMIQUE)
C      INST(5) : DELTA (PARAMETRE DYNAMIQUE)
C
      DO 6 I=1,ZINST
        INST  (I) = ZERO
  6   CONTINUE
C
C --- PARAMETRES DU CRITERE DE CONVERGENCE EN CONTRAINTE (NMLECT)
C  
      DO 7 I=1,ZPCON
        PARCON(I) = ZERO
  7   CONTINUE  
C
C --- METHODES DE RESOLUTION
C  
      DO 8 I=1,ZNMETH
        METHOD(I) = ' '
  8   CONTINUE       
C
C --- INITIALISATION BOUCLE EN TEMPS
C
      NUMINS = 1
      PREMIE = .TRUE.
      MTCPUI = .FALSE.
      MTCPUP = .FALSE.
      ETA    = 0.D0
      FINPAS = .FALSE.  
C
C --- DETERMINATION DU NOM DE LA SD INFO_CHARGE STOCKEE
C --- DANS LA SD RESULTAT
C       
      NOOBJ ='12345678'//'.1234'//'.EXCIT.INFC'           
      CALL GNOMSD(NOOBJ,10,13)
      LISCH2 = NOOBJ(1:19)

      END
