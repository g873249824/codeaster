C ======================================================================
      IMPLICIT NONE
C
C     ARGUMENTS:
C     ----------
      REAL*8 NMFEDD,DR
C ----------------------------------------------------------------------
C    BUT:  EVALUER LA DERIVEE DE LA FONCTION DONT ON CHERCHE LE ZERO
C          POUR VENDOCHAB
C
C     IN:  DR     : DEFORMATION PLASTIQUE CUMULEE*(1-D)
C    OUT:  NMFEDD : DERIVEE
C
C ----------------------------------------------------------------------
C     VARIABLES LOCALES:
C     ------------------
C----- COMMONS NECESSAIRES A VENDOCHAB
      COMMON /FVENDO/MU,SYVP,KVP,RM,DM,SEQE,AD,DT,RD,IR,UNSURN,UNSURM
      REAL*8  MU,SYVP,KVP,RM,DM,SEQE,AD,DT,UNSURN,UNSURM
      REAL*8  DD,RD,R8MIEM,ARG,RMAX,UNMD,DTN,PUIS,EPSI,R8PREM
      REAL*8  GPRIME, DPRIME, COEF,GDER
      INTEGER IR
C      EPSI=R8PREM()
      EPSI=1.D-8
C
      DTN=DT**UNSURN
C
      IF (DR.LT.R8MIEM()) THEN
         GDER=0.D0
         GPRIME=0.D0
         DD=0.D0
         DPRIME=0.D0
      ELSE
         GDER=KVP*(DR**UNSURN)*((RM+DR)**UNSURM)+SYVP*DTN
         PUIS=1.D0-RD*UNSURN
         DD=(DT**PUIS)*((GDER/AD)**RD)
         IF (UNSURM.GT.EPSI) THEN
            GPRIME=(KVP*UNSURN)*(DR**(UNSURN-1.D0))*((RM+DR)**UNSURM)
     &             +KVP*(DR**UNSURN)*((RM+DR)**(UNSURM-1.D0))*UNSURM
         ELSE
            GPRIME=(KVP*UNSURN)*(DR**(UNSURN-1.D0))
         ENDIF
         COEF=RD*(DT**(1.D0-RD*UNSURN))/(AD**RD)
         DPRIME=COEF*GDER**(RD-1.D0)*GPRIME
      ENDIF
      UNMD=1.D0-DM-DD
      NMFEDD= GPRIME*UNMD-DPRIME*GDER+3.D0*MU*DTN+DPRIME*SEQE*DTN
C
 9999 CONTINUE
      END
