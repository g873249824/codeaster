C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT     NONE
      INTEGER      NEQ
      INTEGER      NBLIAI
      INTEGER      JAPPTR
      INTEGER      JAPCOE
      INTEGER      JAPDDL
      INTEGER      JRESU
      INTEGER      JAPJEU
      INTEGER      JAPCOF
      INTEGER      JAPJFX
C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : ALGOCL/ALGOCO
C ----------------------------------------------------------------------
C
C CALCUL DES JEUX FINAUX
C
C IN  NEQ    : NOMBRE D'EQUATIONS DU MODELE
C IN  NBLIAI : NOMBRE DE LIAISONS DE CONTACT
C IN  JAPPTR : POINTEUR VERS RESOCO(1:14)//'.APPOIN'
C IN  JAPCOE : POINTEUR VERS RESOCO(1:14)//'.APCOEF'
C IN  JAPDDL : POINTEUR VERS RESOCO(1:14)//'.APDDL'
C IN  JRESU  : POINTEUR VERS RESU, SOLUTION OBTENUE SANS TRAITER LE 
C              CONTACT
C IN  JAPJEU : POINTEUR VERS RESOCO(1:14)//'.APJEU'
C IN  JAPCOF : POINTEUR VERS RESOCO(1:14)//'.APCOFR'
C              VAUT ZERO SI JEU TANGENTIEL NON GERE
C IN  JAPJFX : POINTEUR VERS RESOCO(1:14)//'.APJEFX'
C              VAUT ZERO SI JEU TANGENTIEL NON GERE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ILIAI,JDECAL,NBDDL
      REAL*8  VAL
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ ()

      DO 10 ILIAI = 1,NBLIAI
        JDECAL = ZI(JAPPTR+ILIAI-1)
        NBDDL  = ZI(JAPPTR+ILIAI) - ZI(JAPPTR+ILIAI-1)
        CALL CALADU(NEQ,NBDDL,ZR(JAPCOE+JDECAL),ZI(JAPDDL+JDECAL),
     &              ZR(JRESU),VAL)
        ZR(JAPJEU-1+ILIAI) = ZR(JAPJEU-1+ILIAI) - VAL

        IF (JAPCOF.NE.0) THEN
          CALL CALADU(NEQ,NBDDL,ZR(JAPCOF+JDECAL),
     +                ZI(JAPDDL+JDECAL),ZR(JRESU),VAL)
          ZR(JAPJFX-1+ILIAI)  = ZR(JAPJFX-1+ILIAI) + VAL
        ENDIF
  10  CONTINUE
  
C  
      CALL JEDEMA()

  999 CONTINUE
      END
