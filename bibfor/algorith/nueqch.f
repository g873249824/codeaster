C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER *19      CHAMNO
      CHARACTER *8       NOMCMP(*)
      INTEGER            NBNO,NUMNO(*),NUEQ(*)
C ----------------------------------------------------------------------
C     PERMET DE RECUPERER LES NUMEROS DES EQUATIONS DANS LE .VALE A
C     PARTIR DES NOMS DE NOEUD ET DES NOMS DE COMPOSANTE
C
C IN  CHAMNO  : CHAM_NO A MODIFIER
C               PERMET ICI DE RECUPERER LA NUMEROTATION,LE MAILLAGE ET
C               LA GRANDEUR ASSOCIEE
C IN  NBNO    : NOMBRE DE NOEUDS
C IN  NUMNO   : LISTE DES NUMEROS DE NOEUD
C IN  NOMCMP  : LISTE DES NOMS DE COMPOSANTE
C OUT NUEQ    : LISTE DES POSITIONS DANS LE .VALE
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      CHARACTER *24      NUMEDD,MAILLA
      CHARACTER *8       NOMGD,KBID
      INTEGER            IBID,IER,NBCMPX,LONK8,ICO,ITROU
      LOGICAL            EXIS,EXISDG
      CHARACTER*8        K8BID
      CALL JEMARQ()
      CALL DISMOI('F','NOM_GD',      CHAMNO,'CHAM_NO',IBID,NOMGD , IER)
      CALL DISMOI('F','NOM_MAILLA',  CHAMNO,'CHAM_NO',IBID,MAILLA, IER)
      CALL DISMOI('F','NOM_NUME_DDL',CHAMNO,'CHAM_NO',IBID,NUMEDD, IER)
      CALL DISMOI('F','NB_EC',       NOMGD, 'GRANDEUR',NEC ,KBID,   IER)
      CALL DISMOI('F','NB_CMP_MAX',  NOMGD, 'GRANDEUR',NBCMPX,KBID, IER)
      CALL JEVEUO(JEXNOM('&CATA.GD.NOMCMP',NOMGD),'L',JK8)
      CALL JELIRA(JEXNOM('&CATA.GD.NOMCMP',NOMGD),'LONMAX',LONK8, K8BID)
      CALL JEVEUO(NUMEDD(1:19)//'.PRNO','L',JPRNO)
      CALL JEVEUO(NUMEDD(1:19)//'.NUEQ','L',JNUEQ)
C
      DO 10 K = 1,NBNO
        IDC = INDIK8(ZK8(JK8),NOMCMP(K),1,LONK8)
        ICO = 0
        DO 100 I=1,NBCMPX
          IF(EXISDG(ZI(JPRNO-1+(NEC+2)*(NUMNO(K)-1)+2+1),I)) THEN
            ICO = ICO + 1
            EXIS = .TRUE.
          ELSE
            EXIS = .FALSE.
          ENDIF
          IF ( I .EQ. IDC ) THEN
            IF ( EXIS ) THEN
              ITROU = ICO
              GOTO 101
            ELSE
              CALL UTDEBM('F','NUEQCH_01','COMPOSANTE NON DEFINIE '
     &                   //'DANS LA NUMEROTATION :' )
              CALL UTIMPK('S',' ',1,NOMCMP(K))
              CALL UTFINM()
            ENDIF
          ENDIF
 100    CONTINUE
        CALL UTMESS ('F','NUEQCH_02','COMPOSANTE NON TROUVEE '
     &                   //'DANS LA NUMEROTATION' )
 101    CONTINUE
        NUEQ(K)=ZI(JNUEQ-1+ZI(JPRNO-1+(NEC+2)*(NUMNO(K)-1)+1)+ITROU-1)
 10   CONTINUE
C FIN -----------------------------------------------------------------
      CALL JEDEMA()
      END
