      SUBROUTINE RECHMN(IZONE,NZOCO,NSYME,REAAPP,REACTU,
     &                  NOMA,NEWGEO,DEFICO,RESOCO,IESCL)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 23/01/2007   AUTEUR ABBAS M.ABBAS 
