      IMPLICIT     NONE
      CHARACTER*8  NOMA
      CHARACTER*24 DEFICO
      CHARACTER*19 NEWGEO
      INTEGER      POSNOM
      INTEGER      ITEMAX
      REAL*8       EPSMAX
      REAL*8       TOLEOU
      LOGICAL      DIRAPP,LDIST
      REAL*8       DIR(3),COORPT(3),DISTME(3)
      REAL*8       JEUPM
      INTEGER      POSMAM,NUMMAM
      REAL*8       TAU1M(3),TAU2M(3)
