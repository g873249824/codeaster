C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT 
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF          
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU    
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                            
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C
      IMPLICIT NONE
C
      INTEGER      IZONE,ESCL,REAAPP,REACTU,IESCL
      REAL*8       CMULT
      CHARACTER*8  NOMA
      CHARACTER*24 NEWGEO,DEFICO,RESOCO
C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : RECHCO
C ----------------------------------------------------------------------
C
C APPARIEMENT MAITRE - ESCLAVE : RECHERCHE POUR CHAQUE NOEUD ESCLAVE
C DE LA MAILLE MAITRE LA PLUS PROCHE DANS LA MEME SURFACE DE CONTACT,
C POUR UNE ZONE DE CONTACT DONNEE.
C
C IN  IZONE  : NUMERO DE LA ZONE DE CONTACT
C IN  ESCL   : NUMERO DE LA SURFACE ESCLAVE (1 OU 2)
C IN  REAAPP : > 0 SI ON CHERCHE LE NOEUD LE + PROCHE, PUIS LA MAILLE
C              < 0 SI ON CHERCHE DIRECTEMENT LA MAILLE LA PLUS PROCHE
C              1 SI ON CHERCHE LE NOEUD LE + PROCHE PAR "BRUTE FORCE"
C              +/-2 SI ON CHERCHE PAR VOISINAGE   (GRACE AU "PASSE")
C              +/-3 SI ON CHERCHE AVEC DES BOITES (SANS LE "PASSE")
C IN  REACTU : INDICATEUR DE REACTUALISATION POUR TOUTE LA ZONE
C IN  NOMA   : NOM DU MAILLAGE
C IN  NEWGEO : GEOMETRIE ACTUALISEE EN TENANT COMPTE DU CHAMP DE
C              DEPLACEMENTS COURANT
C IN  DEFICO : SD DE DEFINITION DU CONTACT (ISSUE D'AFFE_CHAR_MECA)
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C VAR IESCL  : NUMERO DU DERNIER NOEUD ESCLAVE CONNU
C IN  CMULT  : COEFFICIENT DE LA RELATION UNILATERALE (ISSU DU
C              MOT-CLE COEF_MULT_2 OU 1)
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*32       JEXNUM , JEXNOM
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      LOGICAL      MULNOR
      INTEGER      JDIM,IESCL0,NESCL,NDIM,JCHAM
      CHARACTER*24 NDIMCO,CHAMCO
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ ()
C
C ======================================================================
C               RECUPERATION D'ADRESSES ET DE DIMENSIONS
C ======================================================================
C
C --- LECTURE DES SD POUR LE CONTACT POTENTIEL
C
      NDIMCO = DEFICO(1:16)//'.NDIMCO'
      CHAMCO = DEFICO(1:16)//'.CHAMCO'
C
      CALL JEVEUO (NDIMCO,'L',JDIM)
      NDIM = ZI(JDIM)
C
C MULNOR : LOGIQUE QUI VAUT 1 LORSQU'ON MULTIPLIE LES COEFFICIENTS
C DE LA RELATION UNILATERALE PAR LES COMPOSANTES DES NORMALES
C (I.E. : ON EST DANS UNE ZONE OU LA RELATION UNILATERALE PORTE
C SUR LE DEPLACEMENT)
C
      CALL JEVEUO (CHAMCO,'L',JCHAM)
      MULNOR = (ABS(ZI(JCHAM+IZONE-1)).EQ.1)
C
C ======================================================================
C                           APPARIEMENT
C ======================================================================
C
C --- APPARIEMENT EN CHERCHANT PREALABLEMENT LE NOEUD MAITRE LE + PROCHE
C
      IF (REAAPP.GE.0) THEN
C
        IESCL0 = IESCL + 1
        CALL RECHNO ('PRELIM',IZONE,ESCL,REAAPP,REACTU,NEWGEO,DEFICO,
     &               RESOCO,IESCL,CMULT)
        NESCL = ZI(JDIM+8+IZONE)
        CALL CHMANO (IZONE,NDIM,NESCL,IESCL0,NOMA,NEWGEO,DEFICO,RESOCO,
     &               CMULT,MULNOR)
C
C --- APPARIEMENT EN CHERCHANT DIRECTEMENT LA MAILLE LA + PROCHE
C
      ELSE
C
        CALL UTMESS ('F','RECHME_01','LA RECHERCHE DIRECTE DE LA MAILLE'
     &               //' LA PLUS PROCHE N''EST PAS OPERATIONNELLE')
C
C - RECHERCHE PAR VOISINAGE DU PASSE
C - CHMAVO UTILISERA APMEMO(2) OU APMEMO(3), NOEUD ET MAILLE LES PLUS
C - PROCHES AU COUP D'AVANT
C
        IF (REAAPP.EQ.-2) THEN
C
C          CALL CHMAVO (NEWGEO,NDIMCO,PZONE,CONTMA,PSURMA,CONTNO,
C     &                 MAMACO,PMAMA,RESOCO)
C
C - RECHERCHE PAR BOITES
C
        ELSE IF (REAAPP.EQ.-3) THEN
C
C          CALL BOITMA (NEWGEO,CONTMA,NOMACO,PNOMA,APBOIT)
C          CALL CHMABO (NEWGEO,NDIMCO,PZONE,RESOCO)
C
        END IF
C
      END IF
C
C ----------------------------------------------------------------------
C
      CALL JEDEMA ()
      END
