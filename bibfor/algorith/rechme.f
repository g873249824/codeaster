      SUBROUTINE RECHME (IZONE,ESCL,REAAPP,REACTU,NOMA,NEWGEO,DEFICO,
     &                   RESOCO,IESCL,CMULT)
C ======================================================================
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 21/05/2002   AUTEUR PABHHHH N.TARDIEU 
