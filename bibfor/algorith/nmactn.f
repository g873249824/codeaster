C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*19 SDDISC
      REAL*8       PARCRI(*)  
      LOGICAL      ITEMAX
      INTEGER      ACTITE,ACTNIV
      INTEGER      ITERAT,NUMINS
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C GESTION DES ACTIONS SUITE A UN EVENEMENT DANS NEWTON
C
C PASSAGE DE NEWTON -> BOUCLE CONTACT
C      
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION
C IN  PARCRI : CRITERES DE CONVERGENCE
C IN  ITERAT : NUMERO D'ITERATION DE NEWTON
C IN  NUMINS : NUMERO D'INSTANT
C I/O ITEMAX : .TRUE. SI ITERATION MAXIMUM ATTEINTE 
C IN  ACTITE : BOUCLE NEWTON -> ACTION POUR LA SUITE
C     0 - NEWTON OK   - BOUCLE DE CONTACT SUIVANTE
C     1 - NEWTON NOOK - IL FAUT FAIRE QUELQUE CHOSE
C     2 - NEWTON NCVG - ON CONTINUE NEWTON
C     3 - NEWTON STOP - TEMPS/USR1
C OUT ACTNIV : BOUCLE CONTACT -> ACTION POUR LA SUITE
C     0 - BOUCLE CONTACT SUIVANTE
C     1 - BOUCLE CONTACT STOP - ON REFAIT LE PAS
C     2 - BOUCLE CONTACT STOP - ON CONTINUE NEWTON
C     3 - BOUCLE CONTACT STOP - ARRET DU CALCUL          
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C 
      REAL*8       R8BID
      INTEGER      IBID,NECHEC,IECHEC,RETOUR
      CHARACTER*8  K8BID
      CHARACTER*16 NOMEVD
      LOGICAL      LACTI,LDECO,ARRET
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      ARRET  = (NINT(PARCRI(4)).EQ.0)
      LDECO  = .FALSE.
      ACTNIV = 0
C
C --- CONTINUER LA BOUCLE DE NEWTON EST IMPOSSIBLE ICI
C
      IF (ACTITE.EQ.2) CALL ASSERT(.FALSE.)
C
C --- BOUCLE CONTACT SUIVANTE
C
      IF (ACTITE.EQ.0) THEN
        ACTNIV = 0
        GOTO 999
      ENDIF  
C
C --- SORTIE DE BOUCLE (PROBLEME)
C
      IF (ACTITE.EQ.3) THEN
        ACTNIV = 3
        GOTO 999
      ENDIF 

C
C --- ECHEC DE NEWTON: IL FAUT FAIRE QUELQUE CHOSE
C
      CALL ASSERT(ACTITE.EQ.1)
C
C --- NOMBRE D'EVENT-DRIVEN : NECHEC
C
      CALL UTDIDT('L'   ,SDDISC,'ECHE',IBID  ,'NB_OCC',
     &            R8BID ,NECHEC,K8BID )
C      
C --- BOUCLE SUR LES EVENT-DRIVEN
C --- DES QU'UN EVENT-DRIVEN EST SATISFAIT, ON SORT
C --- ON NE CHERCHE PAS A VERIFIER LES AUTRES EVENT
C --- ATTENTION, L'ORDRE D'EVALUATION A DONC UNE IMPORTANCE !
C
      DO 100 IECHEC = 1,NECHEC
C
C ----- RECUPERATION DU NOM DE L'EVENT-DRIVEN
C
        CALL UTDIDT('L'   ,SDDISC,'ECHE',IECHEC,'NOM_EVEN',
     &              R8BID ,IBID  ,NOMEVD)
C
C ----- EVENEMENT ACTIVE ?
C
        CALL DIEVEN(SDDISC,NOMEVD,LACTI )
C
        IF (LACTI) THEN
          IF (NOMEVD.EQ.'DIVE_ITER_PILO') THEN
            ACTNIV = 1
            GOTO 888
          ELSEIF (NOMEVD.EQ.'DIVERGENCE_ERRE') THEN
            LDECO  = .TRUE.
            GOTO 888
          ELSEIF (NOMEVD.EQ.'DIVERGENCE_ITER') THEN
            LDECO  = .TRUE.
            GOTO 888  
          ELSEIF (NOMEVD.EQ.'DELTA_GRANDEUR') THEN
            LDECO  = .TRUE.
            GOTO 888
          ELSE
            WRITE(6,*) 'NOMEVD: ',NOMEVD
            CALL ASSERT(.FALSE.)           
          ENDIF
        ENDIF     
 100  CONTINUE
C
 888  CONTINUE
C
C --- TENTATIVE DE DECOUPE DU PAS DE TEMPS
C
      CALL ASSERT(LACTI)
      IF (LDECO) THEN
        CALL DIDECO(SDDISC,NUMINS,ITERAT,IECHEC,RETOUR)
      ELSE
        RETOUR = 1
      ENDIF
C
C --- TRAITEMENT DE LA DECOUPE
C
      IF (RETOUR.EQ.0)  THEN
C      
C ----- LA SUBDIVISION S'EST BIEN PASSEE: ON REFAIT LE PAS
C
        ACTNIV = 1

      ELSE IF (RETOUR.EQ.3) THEN
C      
C ----- AUTORISATION DE FAIRE DES ITERATIONS DE NEWTON EN PLUS  
C
        ACTNIV = 2
        ITEMAX = .FALSE.

      ELSEIF ((RETOUR.EQ.1).AND.(.NOT.ARRET).AND.ITEMAX) THEN
C
C ----- CONVERGENCE FORCEE: ON VA AU PAS DE TEMPS SUIVANT
C
        CALL U2MESS('A','MECANONLINE2_37')
        ACTNIV = 0

      ELSEIF ((RETOUR.EQ.2).AND.(.NOT.ARRET)) THEN
C
C ----- CONVERGENCE FORCEE: ON VA AU PAS DE TEMPS SUIVANT
C
        CALL U2MESS('A','MECANONLINE2_37')
        ACTNIV = 0

      ELSE
C
C ----- ARRET DU CALCUL        
C
        ACTNIV = 3

      ENDIF 
C
 999  CONTINUE             
C
      CALL JEDEMA()
      END
