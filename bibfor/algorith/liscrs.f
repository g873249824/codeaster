      IMPLICIT      NONE
      INCLUDE 'jeveux.h'
      CHARACTER*19  LISCHA
      INTEGER       NBCHAR
      CHARACTER*1   BASE
C
C ----------------------------------------------------------------------
C
C ROUTINE UTILITAIRE (LISTE_CHARGES)
C
C CREATION DE LA SD LISTE_CHARGES
C
C ----------------------------------------------------------------------
C
C
C IN  LISCHA : NOM DE LA SD LISTE_CHARGES
C IN  NBCHAR : NOMBRE DE CHARGEMENTS DANS LA LISTE
C IN  BASE   : BASE POUR CREER LA SD
C
