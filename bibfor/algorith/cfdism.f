      IMPLICIT     NONE
      CHARACTER*24 DEFICO
      INTEGER      POSNOE
      REAL*8       DISTST
      LOGICAL      LDPOU,LDCOQ
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES - APPARIEMENT)
C
C CALCUL DU JEU SUPPLEMENTAIRE POUR POUTRE/COQUE
C SUR UN NOEUD ESCLAVE DONNE
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  POSNOE : INDICE DU NOEUD ESCLAVE DANS CONTNO
C IN  LDPOU  : SI DIST_POUTRE
C IN  LDCOQ  : SI DIST_COQUE
C OUT DISTST : JEU SUPPLEMENTAIRE DU AUX POUTRES/COQUES
C
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*24 JEUPOU,JEUCOQ
      INTEGER      JJPOU,JJCOQ
      INTEGER      NTMAE,JDECIV,IMAE,POSMAE
      REAL*8       DISTMA
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- LECTURE DES SD POUR LE CONTACT POTENTIEL
C
      JEUCOQ = DEFICO(1:16)//'.JEUCOQ'
      JEUPOU = DEFICO(1:16)//'.JEUPOU' 
C        
      CALL JEVEUO(JEUPOU,'L',JJPOU )
      CALL JEVEUO(JEUCOQ,'L',JJCOQ )              
C
C --- INITIALISATIONS
C
      DISTMA = 0.D0     
C
C --- NOMBRE DE MAILLES ATTACHEES AU NOEUD 
C --- DECALAGE POUR ACCES AU TABLEAU
C   
      CALL CFNBEN(DEFICO,POSNOE,'CONINV',NTMAE ,JDECIV)    
C
C --- BOUCLE SUR LES MAILLES MAITRES 
C 
      DO 50 IMAE = 1,NTMAE    
        CALL CFINVM(DEFICO,JDECIV,IMAE  ,POSMAE)
        IF (LDPOU) THEN
          DISTMA = DISTMA+ZR(JJPOU-1+POSMAE)
        ENDIF        
        IF (LDCOQ) THEN
          DISTMA = DISTMA+ZR(JJCOQ-1+POSMAE)
        ENDIF           
  50  CONTINUE           
C
C --- MOYENNE SIMPLE
C
      DISTST = DISTMA/NTMAE      
C
      CALL JEDEMA ()
      END
