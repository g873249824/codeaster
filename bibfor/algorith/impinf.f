      IMPLICIT NONE
      CHARACTER*(*) SDIMPZ
      INTEGER       IMPMAX,COLMAX,LIGMAX,TITMAX
      INTEGER       COLUTI,LIGUTI
      INTEGER       AFFDEF,UNIT,LARMAX
C      
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (AFFICHAGE - UTILITAIRE)
C
C RETOURNE DIVERSES INFOS SUR LA SD AFFICHAGE
C
C ----------------------------------------------------------------------
C
C
C IN  SDIMPR : SD AFFICHAGE DES COLONNES
C OUT IMPMAX : NOMBRE D'INFOS DANS LE VECTEUR SDIMPR(1:14)//'INFO'
C OUT COLMAX : NOMBRE MAXI DE COLONNES
C OUT LIGMAX : LARGEUR MAXI D'UNE LIGNE
C OUT TITMAX : NOMBRE MAXI DE LIGNES D'UN TITRE
C OUT COLUTI : NOMBRE EFFECTIF DE COLONNES
C OUT LIGUTI : LARGEUR DE LA LIGNE
C OUT AFFDEF : 0 SI AFFICHAGE PAR DEFAUT
C              1 SI AFFICHAGE PERSONNALISE
C              2 SI AFFICHAGE MINIMUM
C OUT UNIT   : UNITE DE SORTIE SUR FICHIER EXTERNE (0 SI PAS SORTIE)
C OUT LARMAX : LARGEUR MAXI D'UNE COLONNE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      JIMPIN
      CHARACTER*24 IMPIN
      INTEGER      IER
      CHARACTER*14 SDIMPR
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES SD
C
      SDIMPR = SDIMPZ
      IMPIN  = SDIMPR//'INFO'
      CALL JEEXIN(IMPIN,IER)

      IF (IER.EQ.0) THEN
        CALL ASSERT(.FALSE.)
      ENDIF

      CALL JEVEUO(IMPIN,'L',JIMPIN)

      IMPMAX = ZI(JIMPIN-1+1)
      COLMAX = ZI(JIMPIN-1+2)
      LIGMAX = ZI(JIMPIN-1+3)
      COLUTI = ZI(JIMPIN-1+4)
      LIGUTI = ZI(JIMPIN-1+5)
      AFFDEF = ZI(JIMPIN-1+6)
      TITMAX = ZI(JIMPIN-1+7)
      UNIT   = ZI(JIMPIN-1+8)
      LARMAX = ZI(JIMPIN-1+9)


      CALL JEDEMA()

      END
