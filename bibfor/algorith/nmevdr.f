      LOGICAL      CONVER,ITEMAX
      CHARACTER*19 SDDISC,VALINC(*)
      CHARACTER*24 SDERRO
      CHARACTER*24 RESOCO
      INTEGER      ACTITE
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C VERIFICATION DU DECLENCHEMENT DES EVENT-DRIVEN
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  SDERRO : SD GESTION DES ERREURS
C IN  VALINC : VARIABLE CHAPEAU INCREMENTS DES VARIABLES
C IN  ITEMAX : .TRUE. SI ITERATION MAXIMUM ATTEINTE
C IN  CONVER : .TRUE. SI CONVERGENCE REALISEE
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C OUT ACTITE : BOUCLE NEWTON -> ACTION POUR LA SUITE
C     0 - NEWTON OK   - ON SORT
C     1 - NEWTON NOOK - IL FAUT FAIRE QUELQUE CHOSE
C     2 - NEWTON NCVG - ON CONTINUE NEWTON
C     3 - NEWTON STOP - TEMPS/USR1
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER      IFM,NIV
      INTEGER      LDCCVG
      LOGICAL      ERROR,LPOST,LEVEN
      CHARACTER*24 K24BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- INITIALISATIONS
C
      LPOST  = .FALSE.
C
C --- NEWTON A CONVERGE ?
C
      IF (CONVER) THEN
        ACTITE = 0
      ELSE
        ACTITE = 2
      ENDIF
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> EVALUATION DES EVENT-DRIVEN'
      ENDIF
C
C --- RECUPERE LE CODE RETOUR LOI DE COMPORTEMENT
C
      CALL NMCRET('RECU','LDC',SDERRO,SDDISC,LDCCVG,
     &            K24BID)
C
C --- ERREUR OU PAS ?
C
      CALL NMERGE(SDERRO,'GET','ALL',ERROR )
C
C --- DETECTION DU PREMIER EVENEMENT DECLENCHE
C 
      CALL NMEVEV(SDDISC,CONVER,VALINC,ITEMAX,LDCCVG,
     &            ERROR ,RESOCO,LPOST ,LEVEN )
C
C --- UN EVENEMENT DECLENCHE
C
      IF (LEVEN) THEN
        ACTITE = 1
      ENDIF
C
      CALL JEDEMA()
      END
