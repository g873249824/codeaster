      INTEGER  I, TAILLE, IZONE
      REAL*8   TAB(IZONE+1), TAB2(IZONE+1), TAB3(IZONE+1), SUM
      REAL*8   DIST(100), INC

      TAB(1) = -1.D0
      TAILLE = IZONE + 1
      SUM = 0.D0
      INC = 2.D0 / IZONE
      
      DO 11 I = 1,100
         DIST(I) = 0.D0
 11   CONTINUE 

      DO 114 I = 2,TAILLE
         TAB(I) = TAB(I-1) + INC
 114  CONTINUE 
  
      DO 22 I = 1,TAILLE
         TAB2(I) = EXP((-1)*TAB(I)*TAB(I))
 22   CONTINUE 
  
      DO 33 I = 1,TAILLE
         SUM = SUM + TAB2(I)
 33   CONTINUE 
  
      DO 444 I = 1,TAILLE
         DIST(I) = TAB2(I) / SUM
444   CONTINUE 
  
      END
