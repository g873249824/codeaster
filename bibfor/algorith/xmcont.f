      SUBROUTINE XMCONT(ALGOCR,COEFCR,COEFCP,COHES ,COHEO ,DDLM  ,
     &                  DDLS  ,FFC   ,FFP   ,IDEPD ,IDEPM ,
     &                  IFA   ,IFISS ,JMATE ,INDCO ,IPGF  ,
     &                  JAC   ,JFISNO,JHEAFA,MMAT  ,LACT  ,
     &                  NCOMPH,ND    ,NDDL  ,NDIM  ,NFH   ,
     &                  NFISS ,NNO   ,NNOL  ,NNOS  ,
     &                  NVIT,PLA   ,RELA  ,RR    ,
     &                  SINGU,TAU1,TAU2)
       IMPLICIT NONE
      INCLUDE 'jeveux.h'
