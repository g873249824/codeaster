! LPAR   /I/  : ADRESSE POUR LE STOCKAGE DU PARAMETRE (MASSE, RAID...)
! BASMOD /K8/ : NOM UT DE LA BASE MODALE DE PROJECTION
! NBMOD  /I/  : NOMBRE DE MODES DANS LA BASE
!
