C
      IMPLICIT NONE
      CHARACTER*16 MCFACT,EVEN
      INTEGER      IECHEC
      REAL*8       PASMIN,DTMIN,PRCOLL,DUCOLL
      CHARACTER*16 SUBMET,SUBAUT
      INTEGER      NBRPAS,NIVEAU
C
C ----------------------------------------------------------------------
C
C OPERATEUR DEFI_LIST_INST
C
C LECTURE DES PARAMETRES DE L'ACTION DE TYPE DECOUPE DU PAS DE TEMPS
C
C ----------------------------------------------------------------------
C
C
C IN  MCFACT : MOT-CLEF FACTEUR POUR LIRE L'ECHEC
C IN  IECHEC : NUMERO OCCURRENCE ECHEC
C IN  EVEN   : NOM DE L'EVENEMENT
C IN  DTMIN  : INCREMENT MINIMUM DANS LA LISTE D'INSTANT
C OUT SUBMET : TYPE DE SUBDIVISION
C OUT SUBAUT : TYPE DE SUBDIVISION AUTOMATIQUE
C OUT PASMIN : VALEUR DE SUBD_PAS_MINI
C OUT NBRPAS : VALEUR DE SUBD_PAS
C OUT NIVEAU : VALEUR DE SUBD_NIVEAU
C OUT PRCOLL : VALEUR DE PREC_COLLISION
C OUT DUCOLL : VALEUR DE DUREE_COLLISION
C
C ----------------------------------------------------------------------
C
      INTEGER      IRET
C
C ----------------------------------------------------------------------
C

C
C --- INITIALISATIONS
C
      SUBMET = ' '
      SUBAUT = ' '
      NIVEAU = 0
      NBRPAS = 4
      PASMIN = 0.D0
      PRCOLL = 0.D0
      DUCOLL = 0.D0
C
C --- TYPE DE LA DECOUPE
C
      CALL GETVTX(MCFACT,'SUBD_METHODE',IECHEC,1,1,SUBMET,IRET)
C
C --- OPTIONS
C      
      CALL GETVR8(MCFACT,'SUBD_PAS_MINI',IECHEC,1,1,PASMIN,IRET)
      IF (PASMIN.GT.DTMIN) CALL U2MESS('F','DISCRETISATION_2')
      IF (SUBMET.EQ.'MANUEL') THEN
        CALL GETVIS(MCFACT,'SUBD_NIVEAU' ,IECHEC,1,1,NIVEAU,IRET)
        CALL GETVIS(MCFACT,'SUBD_PAS'     ,IECHEC,1,1,NBRPAS,IRET)
        IF (NBRPAS .LT. 2) CALL ASSERT(.FALSE.)
      ELSEIF (SUBMET.EQ.'AUTO') THEN
        IF (EVEN.EQ.'COLLISION') THEN
          CALL GETVR8(MCFACT,'PREC_COLLISION',IECHEC,1,1,PRCOLL,IRET)
C          CALL GETVR8(MCFACT,'DUREE_COLLISION',IECHEC,1,1,DUCOLL,IRET)
          SUBAUT = 'COLLISION'
        ELSE
          SUBAUT = 'EXTRAPOLE'
        ENDIF
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
      END
