C ROUTINE MECA_NON_LINE (OBSERVATION - CREATION SD)
C
C CREATION DE LA TABLE D'OBSERVATION
C
C ----------------------------------------------------------------------
C
C
C IN  RESULT : NOM UTILISATEUR DU RESULTAT
C OUT SDOBSE : NOM DE LA SD POUR OBSERVATION
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*19 TABOBS
      INTEGER      JOBST 
      INTEGER      IRET
      CHARACTER*19 NOMTAB 
      INTEGER      NBPARA
      PARAMETER   (NBPARA=8)
      CHARACTER*8  TYPARA(NBPARA)
      CHARACTER*16 NOPARA(NBPARA)    
C
      DATA NOPARA/'NUME_ORDRE','INST','NOM_CHAM','NOM_CMP','NOEUD',
     &     'MAILLE','POINT','VALE'/
      DATA TYPARA/'I','R','K16','K8','K8','K8','I','R'/           
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- CREATION DE LA LISTE DE TABLES SI ELLE N'EXISTE PAS
C 
      CALL JEEXIN(RESULT//'           .LTNT',IRET)
      IF (IRET.EQ.0) THEN 
        CALL LTCRSD(RESULT,'G')
      ENDIF
C
C --- RECUPERATION DE LA TABLE CORRESPONDANT AUX OBSERVATIONS
C
      NOMTAB = ' '
      CALL LTNOTB(RESULT,'OBSERVATION',NOMTAB)     
C
C --- CREATION DE LA TABLE CORRESPONDANT AUX OBSERVATIONS
C
      CALL JEEXIN(NOMTAB//'.TBBA',IRET)
      IF (IRET.EQ.0) THEN 
        CALL TBCRSD(NOMTAB,'G')
      ENDIF
C
C --- AJOUT DES PARAMETRES DANS LA TABLE
C
      CALL TBAJPA(NOMTAB,NBPARA,NOPARA,TYPARA)
C
C --- SAUVEGARDE NOM DU TABLEAU
C      
      TABOBS = SDOBSE(1:14)//'.TABL'
      CALL JEVEUO(TABOBS,'E',JOBST)
      ZK24(JOBST)   = NOMTAB
C
      CALL JEDEMA()
C
      END
