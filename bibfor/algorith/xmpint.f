C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
C                   CALCUL DES COORDONNÉES RÉELLE POUR LES POINTS
C              D'INTERSECTION CONSTITUENT LA MAILLE DE CONTACT
C              DANS L'ELEMENT DE CONTACT HYBRIDE X-FEM
C ----------------------------------------------------------------------
C ROUTINE SPECIFIQUE A L'APPROCHE <<GRANDS GLISSEMENTS AVEC XFEM>>,
C TRAVAIL EFFECTUE EN COLLABORATION AVEC I.F.P.
C ----------------------------------------------------------------------
C   ENTREE
C     ELREFP  : TYPE DE L'ELEMENT DE REF PARENT
C     NDIM    : DIMENSION DU PROBLEME
C     IGEOM   : COORDONNEES DES NOEUDS DE L'ELEMENT DE REF PARENT
C     JPINTE  : COORDONNÉES DES POINTS D'INTERSECTION DANS L'ELEM DE REF
C     IFACF   : NUM LOCAUX DES NOEUDS DES FACETTES DE CONTACT
C     IFA     : INDICE DE LA FACETTE DONT LE POINT DE CONTACT APPARTIENT
C     NNO     : NOMBRE DE NOEUDS DE L'ELEMENT DE REF PARENT
C
C   SORTIE
C     GEOPI  : COORDONNÉES RÉELES DES POINTS D'INTERSECTION
C
C     ------------------------------------------------------------------
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER          ZI
      COMMON  /IVARJE/ ZI(1)
      REAL*8           ZR
      COMMON  /RVARJE/ ZR(1)
      COMPLEX*16       ZC
      COMMON  /CVARJE/ ZC(1)
      LOGICAL          ZL
      COMMON  /LVARJE/ ZL(1)
      CHARACTER*8      ZK8
      CHARACTER*16             ZK16
      CHARACTER*24                      ZK24
      CHARACTER*32                               ZK32
      CHARACTER*80                                        ZK80
      COMMON  /KVARJE/ ZK8(1), ZK16(1), ZK24(1), ZK32(1), ZK80(1)
C     -----  FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      REAL*8          COOR(NDIM)
      REAL*8          FF(NNO)
      INTEGER         I,J,K

C ----------------------------------------------------------------------

      CALL JEMARQ()
C
C --- BOUCLE SUR LES POINTS D'INTERSECTION DE LA FACETTE
      DO 10 I=1,NDIM
C --- RECUPERATION DES COORDONNEES DU POINT DANS LE REPERE LOCALE
        DO 20 J=1,NDIM
          COOR(J)= ZR(JPINTE-1+
     &     NDIM*(INT(ZR(IFACF-1+NDIM*(IFA-1)+I))-1)+J)
 20     CONTINUE
C --- CALCUL DES FONCTIONS DE FORMES DES POINTS DE CONTACT
C     DANS L'ELEMENT PARENT
        CALL ELRFVF(ELREFP,COOR,NNO,FF,NNO)
C --- BOUCLE SUR LES NOEUDS DE L'ELEMENT
        DO 30 J=1,NNO
C --- CALCUL DES COORDONNEES DU POINT DANS LE REPÈRE RÉEL
          DO 40 K=1,NDIM
            GEOPI(NDIM*(I-1)+K) = GEOPI(NDIM*(I-1)+K)
     &       +FF(J)*ZR(IGEOM-1+NDIM*(J-1)+K)
 40       CONTINUE
 30     CONTINUE
 10   CONTINUE
C
      CALL JEDEMA()
      END
