C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*24 NOESYM,DEFICO,RESOCO
      CHARACTER*8  NOMA
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT - MAIT/ESCL SYME.)
C
C CREATION OBJET SPECIFIQUE POUR APPARIEMENT SYMETRIQUE
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  NOESYM : NOM DE LA SD POUR GERER APPARIEMENT SYMETRIQUE
C IN  DEFICO : SD DE DEFINITION DU CONTACT (ISSUE D'AFFE_CHAR_MECA)
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      INTEGER      JSYEXC,IZONPR,IZONSY,ISURFM,NBNOM,JDECM,SUPPOK
      INTEGER      L,POSNOM,NUMNOM,IZONE
      INTEGER      CFDISI,NNOCO,NZOCO,NSYME,NZOCP
      CHARACTER*24 SYMECO,SANSNO,PSANS,PZONE,CONTNO,PSURNO,APMEMO
      INTEGER      JSYME,JSANS,JPSANS,JZONE,JNOCO,JSUNO,JAPMEM
      CHARACTER*24 NDIMCO
      INTEGER      JDIM
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()      
      CALL INFDBG('CONTACT',IFM,NIV)
C   
C --- AFFICHAGE
C 
      CALL CFIMPE(IFM,NIV,'CFSYME',1)
C
C --- LECTURE DES STRUCTURES DE DONNEES DE CONTACT
C
      SYMECO = DEFICO(1:16)//'.SYMECO'   
      SANSNO = DEFICO(1:16)//'.SSNOCO'
      PSANS  = DEFICO(1:16)//'.PSSNOCO'
      PZONE  = DEFICO(1:16)//'.PZONECO'
      CONTNO = DEFICO(1:16)//'.NOEUCO'
      PSURNO = DEFICO(1:16)//'.PSUNOCO'
      NDIMCO = DEFICO(1:16)//'.NDIMCO'       
      APMEMO = RESOCO(1:14)//'.APMEMO'                    
      CALL JEVEUO(SYMECO,'L',JSYME ) 
      CALL JEVEUO(SANSNO,'L',JSANS )
      CALL JEVEUO(PSANS, 'L',JPSANS)
      CALL JEVEUO(PZONE, 'L',JZONE )    
      CALL JEVEUO(CONTNO,'L',JNOCO )
      CALL JEVEUO(PSURNO,'L',JSUNO )  
      CALL JEVEUO(APMEMO,'E',JAPMEM)  
      CALL JEVEUO(NDIMCO,'E',JDIM)                         
C
C --- INITIALISATION DE VARIABLES
C
      IZONE  = 0  
      NNOCO  = CFDISI(DEFICO,'NNOCO',IZONE)
      NZOCO  = CFDISI(DEFICO,'NZOCO',IZONE)
      NSYME  = ZI(JSYME)
      NZOCP  = NZOCO - NSYME    
C
C --- CREATION DE L'OBJET 
C
      CALL WKVECT(NOESYM,'V V I',NNOCO+1,JSYEXC)
C
C --- CREATION DES VECTEURS DE TRAVAIL POUR STOCKER LES NOEUDS ESCLAVES
C --- SE PROJETANT SUR UN NOEUD MAITRE EXACTEMENT DANS LE CAS D'UN
C --- APPARIEMENT SYMETRIQUE
C

C
C --- VERIF DANS LES SURFACES SYMETRIQUES QUE LEURS NOEUDS MAITRES
C --- N'ONT PAS ETE EXCLUS PAR 'SANS_GROUP_NO' DANS LA ZONE PRINCIPALE
C --- ASSOCIEE
C
      DO 100 IZONSY = NZOCP,NZOCO
        IZONPR = IZONSY - NSYME
        ISURFM = ZI(JZONE+IZONSY-1) + 1
        NBNOM  = ZI(JSUNO+ISURFM)-ZI(JSUNO+ISURFM-1)
        JDECM  = ZI(JSUNO+ISURFM-1)
        DO 110 L = 1,NBNOM
          POSNOM = JDECM+L
          NUMNOM = ZI(JNOCO+POSNOM-1)
          CALL CFELSN(IZONPR,NOMA ,POSNOM,JDIM  ,JAPMEM,
     &                JNOCO ,JPSANS,JSANS,SUPPOK)
          IF (SUPPOK.EQ.1) THEN
            ZI(JSYEXC) = ZI(JSYEXC)+1
            ZI(JSYEXC+ZI(JSYEXC)) = NUMNOM
          ENDIF
  110   CONTINUE
  100 CONTINUE
C
      CALL JEDEMA()
      END
