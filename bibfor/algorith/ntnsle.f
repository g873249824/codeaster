      IMPLICIT NONE
      INTEGER       NRPASE
      CHARACTER*6   TYPCHP
      CHARACTER*24  SDSENS
      CHARACTER*(*) NOMCHZ
C 
C ----------------------------------------------------------------------
C
C ROUTINE THER_* (UTILITAIRE)
C
C RETOURNE LE NOM D'UN CHAMP EN SENSIBILITE
C      
C ----------------------------------------------------------------------
C
C
C IN  NRPASE : NUMERO DU PARAMETRE SENSIBLE CONCERNE
C IN  TYPCHP : TYPE DU CHAMP
C OUT NOMCHP : NOM DE LA STRUCTURE. ON COMPLETE PAR DES BLANCS
C IN  SDSENS : SD SENSIBILITE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*13  INPSCO 
      CHARACTER*24  SENSIN
      INTEGER       JSENSI 
      INTEGER       TYPEST              
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES SD SENSIBILITE
C
      SENSIN = SDSENS(1:16)//'.INPSCO '     
      CALL JEVEUO(SENSIN,'L',JSENSI) 
      INPSCO = ZK16(JSENSI+1-1) 
C
C --- NOM CHAMP
C
      IF (TYPCHP.EQ.'NOPASE') THEN
        TYPEST = 1
      ELSEIF (TYPCHP.EQ.'RESULT') THEN
        TYPEST = 3        
      ELSEIF (TYPCHP.EQ.'VTEMP ') THEN
        TYPEST = 4
      ELSEIF (TYPCHP.EQ.'VTEMPM') THEN
        TYPEST = 5
      ELSEIF (TYPCHP.EQ.'VTEMPP') THEN
        TYPEST = 6
      ELSEIF (TYPCHP.EQ.'VTEMPR') THEN
        TYPEST = 7              
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF  
C
C --- NOM DU CHAMP
C
      CALL PSNSLE(INPSCO,NRPASE,TYPEST,NOMCHZ)             
C 
      CALL JEDEMA()      
C
      END
