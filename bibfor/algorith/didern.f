C
C IN  SDDISC : SD DISCRETISATION
C IN  NUMINS : NUMERO DE L'INSTANT
C OUT DIINST : VRAI SI ON SORT DE LA LISTE D'INSTANT
C
C SI LE CALCUL N'EST PAS UN TRANSITOIRE -> TPSDIT N'EXISTE PAS ET ON
C SORT DE LA LISTE TOUT DE SUITE (LE CALCUL EST TERMINE)
C
C
C
C
      INTEGER      NBTEMP,IRET
      CHARACTER*8  K8BID
      CHARACTER*24 TPSDIT
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES SD LISTE D'INSTANTS
C    
      TPSDIT = SDDISC(1:19)//'.DITR' 
      CALL JEEXIN(TPSDIT,IRET)
      IF (IRET.EQ.0) THEN
        DIDERN = .TRUE.
      ELSE 
        CALL JELIRA(TPSDIT,'LONMAX',NBTEMP,K8BID)
        NBTEMP = NBTEMP - 1
        DIDERN = NUMINS .EQ. NBTEMP
      ENDIF  
C
      CALL JEDEMA()
      END
