C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      INCLUDE 'jeveux.h'
      CHARACTER*19 SDDISC
      CHARACTER*8  RESULT
      INTEGER      FONACT(*)
      INTEGER      NUMREO
C
C ----------------------------------------------------------------------
C
C ROUTINE *_NON_LINE (STRUCTURES DE DONNES)
C
C CREATION SD ARCHIVAGE
C
C ----------------------------------------------------------------------
C
C
C IN  RESULT : NOM DE LA SD RESULTAT
C IN  FONACT : FONCTIONNALITES ACTIVEES (VOIR NMFONC)
C IN  SDDISC : SD DISCRETISATION
C OUT NUMREO : NUMERO DE REUSE POUR LA TABLE D'OBSERVATION
C
C ----------------------------------------------------------------------
C
      INTEGER      NOCC,IOCC
      INTEGER      NUMDER,NUMREP
      CHARACTER*16 MOTFAC,MOTPAS
      INTEGER      NUMARC
      CHARACTER*24 ARCINF
      INTEGER      JARINF
      CHARACTER*19 SDARCH
      INTEGER      IFM,NIV
      LOGICAL      ISFONC,LREUSE
      CHARACTER*1  BASE
      REAL*8       INSDER
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> ... CREATION SD ARCHIVAGE'
      ENDIF
C
C --- FONCTIONNALITES ACTIVEES
C
      LREUSE = ISFONC(FONACT,'REUSE')
C
C --- INITIALISATIONS
C
      MOTFAC = 'ARCHIVAGE'
      MOTPAS = 'PAS_ARCH'
      BASE   = 'V'
      IOCC   = 1
      NUMARC = -1
      NUMREO = -1
      NUMREP = -1
      CALL GETFAC(MOTFAC,NOCC  )
      CALL ASSERT(NOCC.LE.1)
C
C --- NOM SD ARCHIVAGE
C
      SDARCH = SDDISC(1:14)//'.ARCH'
      ARCINF = SDARCH(1:19)//'.AINF'
C
C --- DERNIER NUMERO ARCHIVE DANS L'EVOL  SI REUSE
C
      CALL NMDIDE(LREUSE,RESULT,NUMDER,INSDER)
C
C --- LECTURE LISTE INSTANTS D'ARCHIVAGE
C
      CALL NMCRPX(MOTFAC,MOTPAS,IOCC  ,SDARCH,BASE  )
C
C --- CONSTRUCTION CHAMPS EXCLUS DE L'ARCHIVAGE
C
      CALL NMAREX(MOTFAC,SDARCH)
C
C --- RECUPERATION DU PREMIER NUMERO A ARCHIVER
C
      CALL NMARPR(RESULT,SDDISC,LREUSE,NUMDER,INSDER,
     &            NUMARC)
C
C --- RECUPERATION NUMERO REUSE - TABLE OBSERVATION
C
      CALL NMARNR(RESULT,'OBSERVATION',NUMREO)
C
C --- RECUPERATION NUMERO REUSE - TABLE PARA_CALC
C
      CALL NMARNR(RESULT,'PARA_CALC'  ,NUMREP)
C
C --- NUMERO D'ARCHIVE COURANT ET NUMERO DE REUSE
C
      CALL ASSERT(NUMARC.GE.0)
      CALL ASSERT(NUMREO.GE.0)
      CALL ASSERT(NUMREP.GE.0)
      CALL WKVECT(ARCINF,'V V I',3,JARINF)
      ZI(JARINF-1+1) = NUMARC
      ZI(JARINF-1+2) = NUMREO
      ZI(JARINF-1+3) = NUMREP
C
      CALL JEDEMA()

      END
