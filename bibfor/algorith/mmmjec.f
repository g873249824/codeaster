      IMPLICIT NONE
      INTEGER NDIM
      REAL*8  NORM(3)
      REAL*8  BETA,GAMMA,DELTAT
      REAL*8  DDEPLE(3),DEPLME(3)
      REAL*8  DDEPLM(3),DEPLMM(3)      
      REAL*8  ACCME(3),ACCMM(3)
      REAL*8  VITME(3),VITMM(3)
      REAL*8  JEVITP
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE)
C
C CALCUL DU JEU ACTUALISE - SAUT DE VITESSE POUR COMPLIANCE
C
C ----------------------------------------------------------------------
C
C
C IN  NDIM   : DIMENSION DU PROBLEME
C IN  NORM   : VALEUR DE LA NORMALE
C IN  BETA   : PARAMETRE SCHEMA NEWMARK
C IN  GAMMA  : PARAMETRE SCHEMA NEWMARK
C IN  DELTA  : INCREMENT DE TEMPS
C IN  DDEPLE : DEPLACEMENTS DEPDEL DU POINT DE CONTACT
C IN  DDEPLM : DEPLACEMENTS DEPDEL DU PROJETE DU POINT DE CONTACT
C IN  DEPLME : DEPLACEMENTS PRECEDENTS DU POINT DE CONTACT
C IN  DEPLMM : DEPLACEMENTS PRECEDENTS DU PROJETE DU POINT DE CONTACT
C IN  VITME  : VITESSE DU POINT DE CONTACT
C IN  ACCME  : ACCELERATION DU POINT DU CONTACT
C IN  VITMM  : VITESSE DU PROJETE DU POINT DE CONTACT
C IN  ACCMM  : ACCELERATION DU PROJETE DU POINT DU CONTACT
C OUT JEVITP : VALEUR DU JEU (SAUT DE VITESSE NORMALE POUR COMPLIANCE)
C
C ----------------------------------------------------------------------
C
      INTEGER IDIM
      REAL*8  JDEPP,JDEPM,JEVITM,JEACCM
C
C ----------------------------------------------------------------------
C
      JDEPP  = 0.D0
      JDEPM  = 0.D0
      JEVITM = 0.D0
      JEACCM = 0.D0  
      JEVITP = 0.D0
      DO 20 IDIM = 1,NDIM
        JDEPP  = JDEPP  + (DDEPLE(IDIM)-DDEPLM(IDIM))*NORM(IDIM)
        JDEPM  = JDEPM  + (DEPLME(IDIM)-DEPLMM(IDIM))*NORM(IDIM)
        JEVITM = JEVITM + (VITME(IDIM) -VITMM(IDIM)) *NORM(IDIM)
        JEACCM = JEACCM + (ACCME(IDIM) -ACCMM(IDIM)) *NORM(IDIM)
 20   CONTINUE
      JEVITP = (JDEPP-JDEPM)*GAMMA/(BETA*DELTAT) +
     &          JEVITM*(BETA-GAMMA)/BETA +
     &          JEACCM*DELTAT*(2*BETA-GAMMA)/(2*BETA)       
      END
