      SUBROUTINE NMVCFO(MODELZ,MATE  ,CARELE,
     &                  COMPOR,COMREF,COMVAL,VECELZ)
C
C MODIF ALGORITH  DATE 29/04/2013   AUTEUR DELMAS J.DELMAS 
C ======================================================================
