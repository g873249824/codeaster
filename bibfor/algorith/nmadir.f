      IMPLICIT NONE     
      INCLUDE 'jeveux.h'
      INTEGER       FONACT(*)
      CHARACTER*24  NUMEDD
      CHARACTER*19  VEASSE(*)
      CHARACTER*19  VEDIRI,CNDIRI
      CHARACTER*24  DEFICO
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C ASSEMBLAGE DU VECTEUR DES REACTIONS D'APPUI BT.LAMBDA
C
C ----------------------------------------------------------------------
C
C
C IN  NUMEDD : NOM DE LA NUMEROTATION
C IN  DEFICO : SD DEFINITION CONTACT
C IN  FONACT : FONCTIONNALITES ACTIVEES
C IN  VEASSE : VARIABLE CHAPEAU POUR NOM DES VECT_ASSE
C IN  VEDIRI : VECT_ELEM DES REACTIONS D'APPUIS
C OUT CNDIRI : VECT_ASSE DES REACTIONS D'APPUIS
C
C
C
C
      INTEGER      IFM,NIV
      CHARACTER*1  BASE    
      CHARACTER*19 CNCONT
      LOGICAL      ISFONC,LCONT
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> ... ASSEMBLAGE DES REAC. APPUI'
      ENDIF               
C
C --- INITIALISATIONS
C  
      BASE   = 'V' 
      CNCONT = '&&CNCHAR.DUMM'
      LCONT  = ISFONC(FONACT,'CONTACT')      
      CALL VTZERO(CNDIRI)  
      CALL VTZERO(CNCONT)        
C
C --- CONTRIBUTIONS DU CONTACT
C
      IF (LCONT) THEN
        CALL NMASCO('CNDIRI',FONACT,DEFICO,VEASSE,CNCONT)    
      ENDIF   
C
C --- ASSEMBLAGE DES REACTIONS D'APPUI
C   
      CALL ASSVEC(BASE  ,CNDIRI,1     ,VEDIRI,1.D0  ,
     &            NUMEDD,' '   ,'ZERO',1     )
C
C --- CONTRIBUTIONS DU CONTACT
C          
      IF (LCONT) THEN
        CALL VTAXPY(1.D0,CNCONT,CNDIRI)
      ENDIF       
C
C --- AFFICHAGE
C      
      IF (NIV.GE.2) THEN
        CALL NMDEBG('VECT',CNDIRI,6)
      ENDIF        
C
      CALL JEDEMA()
      END
