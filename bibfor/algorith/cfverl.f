C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES)
C
C VERIFICATION FACETTISATION
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C
C ------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------
C 
      CHARACTER*8  NOMNOE,K8BID
      INTEGER      NNOEU,IER
      INTEGER      INO,NBNO
      REAL*8       ANGLE
      CHARACTER*19 SDAPPA
      CHARACTER*24 APVERK,APVERA
      INTEGER      JLISTN,JLISTA
      LOGICAL      CFDISL,LLISS
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      SDAPPA = RESOCO(1:14)//'.APPA'
      CALL JEEXIN(SDAPPA(1:19)//'.VERK',IER)
      LLISS  = CFDISL(DEFICO,'LISSAGE')
C
C --- SD VERIFICATION FACETTISATION
C
      APVERK = SDAPPA(1:19)//'.VERK'
      APVERA = SDAPPA(1:19)//'.VERA'
      CALL JEVEUO(APVERK,'L',JLISTN)
      CALL JEVEUO(APVERA,'L',JLISTA)
      CALL JELIRA(APVERK,'LONUTI',NNOEU,K8BID)
      CALL JELIRA(APVERK,'LONMAX',NBNO ,K8BID)
      IF ((NNOEU.EQ.0).OR.(LLISS)) GOTO 999
C
      CALL U2MESI('I','CONTACT3_19',1,NNOEU)
C
      DO 10 INO = 1,NBNO
        NOMNOE = ZK8(JLISTN+INO-1)
        ANGLE  = ZR(JLISTA+INO-1)
        IF (NOMNOE.NE.' ') THEN
          WRITE(6,1000) NOMNOE,ANGLE
        ENDIF   
   10 CONTINUE
C
 1000 FORMAT (A8,3X,F8.2)
C
 999  CONTINUE
C
      CALL JEDEMA()
      END
