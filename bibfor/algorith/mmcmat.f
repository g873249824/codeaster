C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*24 MODELE,RESOCO
      CHARACTER*24 DEPMOI,DEPDEL,ACCMOI,VITMOI
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - CALCUL)
C
C CALCUL DES MATRICES ELEMENTAIRES 
C      
C ----------------------------------------------------------------------
C 
C
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  DEPMOI : CHAM_NO DES DEPLACEMENTS A L'INSTANT PRECEDENT
C IN  MODELE : NOM DU MODELE
C IN  DEPDEL : INCREMENT DE DEPLACEMENT CUMULE
C IN  ACCMOI : CHAM_NO DES ACCELERATIONS A L'INSTANT PRECEDENT
C IN  VITMOI : CHAM_NO DES VITESSES A L'INSTANT PRECEDENT
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*8  LPAIN(7),LPAOUT,K8BID
      CHARACTER*19 CHGEOM,LCHIN(7),LCHOUT,USUPLU
      INTEGER      JLIRES
      LOGICAL      EXIGEO
      INTEGER      IFM,NIV
      CHARACTER*19 LIGRCF,CARTCF      
      CHARACTER*24 MELEME
      INTEGER      JMELEM      
      CHARACTER*8  MCONEL     
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> CALCUL DES MATRICES ELEMENTAIRES'
      ENDIF
C      
C --- LIGREL DES ELEMENTS TARDIFS DE CONTACT/FROTTEMENT    
C
      LIGRCF = RESOCO(1:14)//'.LIGR'
C      
C --- CARTE POUR ELEMENTS TARDIFS DE CONTACT/FROTTEMENT    
C
      CARTCF = RESOCO(1:14)//'.CART'      
C
C --- NOMS DES MATR_ELEM CONTACT
C      
      MELEME = RESOCO(1:14)//'.MELEM'
      CALL JEVEUO(MELEME,'L',JMELEM)
      MCONEL = ZK8(JMELEM+1-1)
      USUPLU = RESOCO(1:14)//'.USUP'
C
C --- ON DETRUIT MCONEL ET SCONEL S'ILS EXISTENT
C
      CALL DETRSD('MATR_ELEM',MCONEL)
C
C --- RECUPERATION DE LA GEOMETRIE
C
      CALL MEGEOM(MODELE,' ',EXIGEO,CHGEOM)
      IF (.NOT.EXIGEO) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- CREATION DES LISTES DES CHAMPS IN ET OUT
C --- GEOMETRIE ET DEPLACEMENTS
C
      LPAIN(1) = 'PGEOMER'
      LCHIN(1) = CHGEOM
      LPAIN(2) = 'PDEPL_M'
      LCHIN(2) = DEPMOI
      LPAIN(3) = 'PDEPL_P'
      LCHIN(3) = DEPDEL
      LPAIN(4) = 'PCONFR'
      LCHIN(4) = CARTCF
      LPAIN(5) = 'PDEPLAR'
      LCHIN(5) = VITMOI
      LPAIN(6) = 'PCHDYNR'
      LCHIN(6) = ACCMOI
      LPAIN(7) = 'PUSULAR'
      LCHIN(7) = USUPLU
C
C --- CALCUL DE MCONEL: MATRICES ELEMENTAIRES
C
      CALL MEMARE('V',MCONEL(1:8),MODELE,' ',' ','RIGI_MECA')
      CALL WKVECT(MCONEL(1:8)//'.LISTE_RESU','V V K24',2,JLIRES)
C
C --- PARTIE CONTACT
C
      LPAOUT = 'PMATUUR'
      LCHOUT = MCONEL(1:8) // '.ME001'
      CALL CALCUL('S','RIGI_CONT',LIGRCF,7,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &           'V')
      ZK24(JLIRES-1+1) = LCHOUT
C
C --- PARTIE FROTTEMENT
C
      LPAOUT = 'PMATUUR'
      LCHOUT = MCONEL(1:8) // '.ME002'
      CALL CALCUL('S','RIGI_FROT',LIGRCF,7,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &           'V')
      ZK24(JLIRES-1+2) = LCHOUT
      CALL JEECRA(MCONEL(1:8)//'.LISTE_RESU','LONUTI',2,K8BID)
C
      CALL JEDEMA()
      END
