C OUT IRET   : RETOURNE UN CODE ERREUR
C                0  TOUT VA BIEN
C                1  ELEMENT INCONNU
C
C ----------------------------------------------------------------------
C
      INTEGER TYPBAR
C
C ----------------------------------------------------------------------
C
      TYPBAR = -1
      IF (FFORME.EQ.'CONTINUE') THEN
        CALL MMMFFD(ALIAS ,KSI1  ,KSI2   ,TYPBAR,
     &              FF    ,DFF   ,DDFF   ,IRET)
      ELSEIF (FFORME.EQ.'STANDARD') THEN
        CALL ASSERT(.FALSE.)
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF

      
      END
