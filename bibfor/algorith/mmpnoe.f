      IMPLICIT NONE
      CHARACTER*8   ALIAS
      CHARACTER*24  DEFICO
      INTEGER       IPTM
      INTEGER       POSMAE,POSNOE
      INTEGER       TYPINT
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE)
C
C DONNE LE NUMERO DU NOEUD DANS SD CONTACT SI INTEGRATION AUX NOEUDS
C      
C ----------------------------------------------------------------------
C
C
C IN  TYPINT : TYPE D'INTEGRATION
C IN  IPTM   : NUMERO DU POINT D'INTEGRATION DANS LA MAILLE
C IN  ALIAS  : NOM D'ALIAS DE L'ELEMENT
C IN  POSMAE : POSITION DE LA MAILLE ESCLAVE DANS LES SD CONTACT
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT 
C IN  TYPINT : TYPE D'INTEGRATION
C OUT POSNOE : POSITION DU NOEUD ESCLAVE SI INTEG. AUX NOEUDS
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      NBNOE,JDECNE,INOE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ() 
C
C --- INITIALISATIONS
C         
      POSNOE = 0
C
C --- NOEUD ESCLAVE SI INTEGRATION AUX NOEUDS
C
      IF (TYPINT.EQ.1) THEN
        INOE   = IPTM
C       GLUTE POUR QUAD8 POUR LEQUEL NBNOE=8<NBPC=9
        IF (ALIAS.EQ.'QU8') THEN
          POSNOE = -1
        ELSE
          CALL CFNBEN(DEFICO,POSMAE,'CONNEX',NBNOE ,JDECNE)
          IF (INOE.LE.NBNOE) THEN
            CALL CFCONN(DEFICO,JDECNE,INOE  ,POSNOE)
          ELSE
            CALL ASSERT(.FALSE.)
          ENDIF
        ENDIF
      ELSE
        POSNOE = 0  
      ENDIF 
C
      CALL JEDEMA() 
C
      END
