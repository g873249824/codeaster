C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      INTEGER      ACTION
      CHARACTER*24 SDTIME
      INTEGER      NUMINS
      CHARACTER*19 SDDISC,SDCRIT   
      REAL*8       PARCRI(*)  
      LOGICAL      ITEMAX
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C GESTION DE LA DECOUPE DU PAS DE TEMPS
C      
C ----------------------------------------------------------------------
C
C
C IN  SDTIME : SD TIMER
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  NUMINS : NUMERO D'INSTANTS
C IN  SDCRIT : SD CRITERE
C I/O ITEMAX : .TRUE. SI ITERATION MAXIMUM ATTEINTE 
C IN  PARCRI : CRITERES DE CONVERGENCE
C OUT ACTION : CODE RETOUR ACTION
C               0 ARRET DU CALCUL
C               1 NOUVEAU PAS DE TEMPS
C               2 ON FAIT DES ITERATIONS DE NEWTON EN PLUS
C               3 ON FINIT LE PAS DE TEMPS
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C 
      INTEGER  RETOUR
      LOGICAL  ARRET,LBID
      REAL*8   R8BID
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      ARRET  = (NINT(PARCRI(4)).EQ.0)
      ACTION = 0
C
C --- DECOUPE DU PAS DE TEMPS
C     SI ON A PAS ITEMAX ET QUE L'ON ARRIVE ICI C'EST UN PROBLEME DE
C        PILOTAGE, DE MATRICE SINGULIERE, DE CONTACT, DE LOI DE
C        COMPORTEMENT. DANS CE CAS ON NE PEUT PAS UTILISER LES RESIDUS,
C        ON FORCE LA SUBDIVISION (FDECUP=1 ON FORCE)
C     SI ON A ITEMAX ON NE DOIT PAS FORCER LA SUBDIVISION MAIS UTILISER
C        UNE DES METHODES DE SUBDIVISION (FDECUP=0 ON NE FORCE PAS)
C
      IF (ITEMAX) THEN
        CALL DIDECO(SDDISC,NUMINS,SDCRIT,0,RETOUR)
      ELSE
        CALL DIDECO(SDDISC,NUMINS,SDCRIT,1,RETOUR)
      ENDIF        
C   
C --- FIN MESURE TEMPS ITERATION DE NEWTON
C        
      CALL NMTIME('FIN','ITE',SDTIME,LBID  ,R8BID )
C
      IF (RETOUR.EQ.0)  THEN
C      
C --- LA SUBDIVISION S'EST BIEN PASSEE
C
        CALL NMTIME('FIN','PAS',SDTIME,LBID  ,R8BID)
        ACTION = 1
      ELSE IF (RETOUR.EQ.3) THEN
C      
C --- AUTORISATION DE FAIRE DES ITERATIONS DE NEWTON EN PLUS
C      
        ACTION = 2
        ITEMAX = .FALSE.
      ELSEIF ((RETOUR.EQ.1).AND.(.NOT.ARRET).AND.ITEMAX) THEN
C
C --- ON VA A LA SUBDIVISION OU AU PAS DE TEMPS SUIVANT
C      
        ACTION = 3
      ELSEIF ((RETOUR.EQ.2).AND.(.NOT.ARRET)) THEN
C
C --- ON VA A LA SUBDIVISION OU AU PAS DE TEMPS SUIVANT
C       
        ACTION = 3
      ELSE
C
C --- ARRET DU CALCUL
C        
        ACTION = 0
      ENDIF      
C
      CALL JEDEMA()
      END
