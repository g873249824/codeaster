      INTEGER     I,J,K,IBID
      
C     P : OPÉRATEUR DE PROJECTION 
      DO 10 I = 1,NDIM
        DO 20 J = 1,NDIM
          P(I,J) = -1.D0 * ND(I)*ND(J)
 20     CONTINUE
 10   CONTINUE
          
      DO 30 I = 1,NDIM
         P(I,I) = 1.D0 + P(I,I)
 30   CONTINUE
  
      END
