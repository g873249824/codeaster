      IMPLICIT NONE
      CHARACTER*16 MOTFAC
      INTEGER      IOCC
      INTEGER      UNIT
C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : IMPINI
C ----------------------------------------------------------------------
C
C RECUPERATION DES PRECISIONS POUR AFFICHAGE DES COLONNES DANS LE
C TABLEAU DE CONVERGENCE
C
C IN  MOTFAC : MOT-CLEF FACTEUR
