C               SI UNITE DIFFERENTE DE ZERO EN ENTREE -> ERREUR
C               DEFINITION MULTIPLE DU MOT-CLEF FICHIER
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      NBVAL
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C ---
C
      CALL GETVIS(MOTFAC,'UNITE',IOCC,1,1,UNIT,NBVAL)
      IF (NBVAL.NE.0) THEN
        IF (IOCC.NE.1) THEN
          CALL U2MESS('F','MECANONLINE4_5')
        ENDIF
        IF (NBVAL.EQ.0) THEN
          CALL U2MESS('F','MECANONLINE4_6')
        ENDIF
      ELSE
        UNIT = 0
      ENDIF
C
C ---
C
      CALL JEDEMA()

      END
