      SUBROUTINE CHMANO(NOMA  ,IZONE ,NEWGEO,DEFICO,RESOCO,
     &                  ILIDEB,NZLIAI)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 06/07/2010   AUTEUR DESOZA T.DESOZA 
C ======================================================================
