      SUBROUTINE CFIMP3(DEFICO,RESOCO,NOMA  ,IFM   ,NUMORD,
     &                  INSTAP,NBLIAI,NBLIAC,JCNSVR)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 19/09/2011   AUTEUR ABBAS M.ABBAS 
C ======================================================================
