C
      IMPLICIT     NONE
      CHARACTER*24 DEFICO
      CHARACTER*4  TYPENT
      INTEGER      POSENT
      INTEGER      NBENT
      INTEGER      JDECEN
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (TOUTES METHODES - UTILITAIRE)
C
C ACCES AUX TABLEAUX DE CONNECTIVITES INVERSES
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE DEFINITION DU CONTACT 
C IN  POSENT : POSITION DE L'ENTITE DANS LES SD CONTACT
C IN  TYPENT : TYPE D'ENTITE
C               'NOEU' POSENT EST UN NOEUD
C               'MAIL' POSENT EST UNE MAILLE
C OUT NBENT  : NOMBRE D'ENTITES ATTACHES
C OUT JDECEN : DECALAGE POUR TABLEAU CONNEC. INV. 
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*24 PNOMA,PMANO
      INTEGER      JPONO,JPOMA
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ ()
C      
C --- RECUPERATION DE QUELQUES DONNEES      
C
      PNOMA  = DEFICO(1:16)//'.PNOMACO'
      PMANO  = DEFICO(1:16)//'.PMANOCO'     
      CALL JEVEUO(PNOMA, 'L',JPONO)
      CALL JEVEUO(PMANO ,'L',JPOMA)      
C
C --- INITIALISATIONS
C
      IF (TYPENT.EQ.'MAIL') THEN
        NBENT  = ZI(JPONO+POSENT) - ZI(JPONO+POSENT-1)
        JDECEN = ZI(JPONO+POSENT-1)
      ELSEIF (TYPENT.EQ.'NOEU') THEN
        NBENT  = ZI(JPOMA+POSENT) - ZI(JPOMA+POSENT-1)   
        JDECEN = ZI(JPOMA+POSENT-1) 
      ELSE    
        CALL ASSERT(.FALSE.)
      ENDIF
C
      CALL JEDEMA()
C
      END
