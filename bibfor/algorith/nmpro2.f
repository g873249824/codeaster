      IMPLICIT NONE
      CHARACTER*24 NUMEDD,NUMFIX
      CHARACTER*19 SDDYNA
      INTEGER      FONACT(*)
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (INITIALISATION)
C
C DUPLICATION DU NUME_DDL - CREATION DU NUME_DDL 'FIXE'
C      
C ----------------------------------------------------------------------
C
C IN  FONACT : FONCTIONNALITES ACTIVEES (VOIR NMFONC)
C IN  NUMEDD : NUME_DDL (VARIABLE AU COURS DU CALCUL)
C IN  SDDYNA : NOM SD DYNAMIQUE
C OUT NUMFIX : NUME_DDL (FIXE AU COURS DU CALCUL)
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      LOGICAL  ISFONC,NDYNLO,LMACR,LELTC,LDYNA     
C      
C ----------------------------------------------------------------------
C      
      CALL JEMARQ()
C
C --- FONCTIONNALITES ACTIVEES
C
      LMACR  = ISFONC(FONACT,'MACR_ELEM_STAT')
      LELTC  = ISFONC(FONACT,'ELT_CONTACT')
      LDYNA  = NDYNLO(SDDYNA,'DYNAMIQUE')
C
C --- DUPLICATION (OU PAS !)      
C      
      IF (LELTC) THEN
        IF (LMACR.OR.LDYNA) THEN
          NUMFIX = '&&NMPRO2.NUMFIX'
          CALL COPISD('NUME_DDL','V',NUMEDD,NUMFIX)
        ENDIF
      ELSE
        NUMFIX = NUMEDD
      ENDIF  
C
      CALL JEDEMA()      
C
      END
