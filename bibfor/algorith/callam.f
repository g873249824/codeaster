C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*8   NOMA,ALIASZ
      INTEGER       POSMAE,NNOZ
      CHARACTER*19  CNS1Z
      CHARACTER*24  DEFICO,RESOCO
      REAL*8        MCON(9)
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE)
C
C CALCUL DU MULTIPLICATEUR DE CONTACT EN UN POINT ESCLAVE
C      
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  RESOCO : SD DE RESOLUTION DU CONTACT
C IN  CNS1   : DEPLACEMENT APRES CONVERGENCE DE NEWTON (CHAM_NO_S)
C IN  POSMAE : INDICE DE LA MAILLE ESCLAVE DANS CONTAMA
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C           
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER JNOCO,JMACO,JNOMA,JPONO,NUMA
      INTEGER NNO,JDEC,INO,NO(9),POSNNO(9),IBID
      INTEGER JCNSV2
      CHARACTER*8 ALIAS
      CHARACTER*19 CNS1,CNS2
      CHARACTER*24 CONTMA,CONTNO,NOMACO,PNOMA
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C      
      CONTMA = DEFICO(1:16)//'.MAILCO'
      CONTNO = DEFICO(1:16)//'.NOEUCO'
      NOMACO = DEFICO(1:16)//'.NOMACO'
      PNOMA  = DEFICO(1:16)//'.PNOMACO'
      CALL JEVEUO(CONTNO,'L',JNOCO)
      CALL JEVEUO(CONTMA,'L',JMACO)
      CALL JEVEUO(NOMACO,'L',JNOMA)
      CALL JEVEUO(PNOMA ,'L',JPONO) 
C
C --- INITIALISATIONS
C
      CNS1   = CNS1Z
      CNS2   = '&&CALLAM.CNS2'

      DO 10,INO = 1,9
        MCON(INO) = 0.D0
   10 CONTINUE
C
C --- INFOS SUR LA MAILLE ESCLAVE 
C 
      NUMA = ZI(JMACO+POSMAE-1)
      JDEC = ZI(JPONO+POSMAE-1)      
      CALL MMELTY(NOMA,NUMA,ALIAS,NNO,IBID)       
C
C --- COORDONNEES DES NOEUDS DE LA MAILLE ESCLAVE
C
      DO 20 INO = 1,NNO
        POSNNO(INO+1) = ZI(JNOMA+JDEC+INO-1)
        NO(INO)       = ZI(JNOCO+POSNNO(INO+1)-1)
   20 CONTINUE   
C
C --- EXTRACTION DES LAGRANGIENS DE CONTACT DU VECTEUR DEPLACEMENT
C     
      CALL CNSRED(CNS1,NNO,NO,1,'LAGS_C','V',CNS2)
      CALL JEVEUO(CNS2//'.CNSV','L',JCNSV2)
C
C --- CALCUL DU MULTIPLICATEUR DE CONTACT
C
      DO 30,INO = 1,NNO
        MCON(INO) = ZR(JCNSV2-1+NO(INO))
   30 CONTINUE
C
      NNOZ   = NNO
      ALIASZ = ALIAS

      CALL DETRSD('CHAM_NO_S',CNS2)    
C  
      CALL JEDEMA()
      END
