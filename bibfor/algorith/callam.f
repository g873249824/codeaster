C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR   
C (AT YOUR OPTION) ANY LATER VERSION.                                 
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT 
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF          
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU    
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                            
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*8   NOMA
      INTEGER       POSMAE
      REAL*8        KSI1,KSI2
      CHARACTER*24  DEPPLU
      CHARACTER*24  DEFICO,RESOCO
      REAL*8        LAMBDA 
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE)
C
C CALCUL DU MULTIPLICATEUR DE CONTACT EN UN POINT ESCLAVE
C      
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  RESOCO : SD DE RESOLUTION DU CONTACT
C IN  DEPPLU : DEPLACEMENT APRES CONVERGENCE DE NEWTON
C IN  POSMAE : INDICE DE LA MAILLE ESCLAVE DANS CONTAMA
C IN  KSI1   : COORDONNEE PARAMETRIQUE KSI DU POINT
C IN  KSI2   : COORDONNEE PARAMETRIQUE ETA DU POINT
C OUT LAMBDA : MULTIPLICATEUR DE LAGRANGE DU CONTACT POUR LE POINT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C           
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER JNOCO,JMACO,JNOMA,JPONO,NUMA
      INTEGER NNO,JDEC,INO,NO(9),POSNNO(9),J,IBID
      INTEGER JCNSV2
      REAL*8 FF(9)
      REAL*8 MCON(9)
      CHARACTER*8 ALIAS
      CHARACTER*19 CNS1,CNS2
      CHARACTER*24 CONTMA,CONTNO,NOMACO,PNOMA
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C      
      CONTMA = DEFICO(1:16)//'.MAILCO'
      CONTNO = DEFICO(1:16)//'.NOEUCO'
      NOMACO = DEFICO(1:16)//'.NOMACO'
      PNOMA  = DEFICO(1:16)//'.PNOMACO'
      CALL JEVEUO(CONTNO,'L',JNOCO)
      CALL JEVEUO(CONTMA,'L',JMACO)
      CALL JEVEUO(NOMACO,'L',JNOMA)
      CALL JEVEUO(PNOMA ,'L',JPONO) 
C
C --- INITIALISATIONS
C
      CNS1   = '&&CALLAM.CNS1'
      CNS2   = '&&CALLAM.CNS2'
      LAMBDA = 0.D0      
C
C --- INFOS SUR LA MAILLE ESCLAVE 
C 
      NUMA = ZI(JMACO+POSMAE-1)
      JDEC = ZI(JPONO+POSMAE-1)      
      CALL MMELTY(NOMA,NUMA,ALIAS,NNO,IBID)       
C
C --- COORDONNEES DES NOEUDS DE LA MAILLE ESCLAVE
C
      DO 10 INO = 1,NNO
        POSNNO(INO+1) = ZI(JNOMA+JDEC+INO-1)
        NO(INO)       = ZI(JNOCO+POSNNO(INO+1)-1)
   10 CONTINUE   
C
C --- EXTRACTION DES LAGRANGIENS DE CONTACT DU VECTEUR DEPLACEMENT
C     
      CALL CNOCNS(DEPPLU,'V',CNS1)
      CALL CNSRED(CNS1,NNO,NO,1,'LAGS_C','V',CNS2)
      CALL JEVEUO(CNS2//'.CNSV','L',JCNSV2)
C
C --- CALCUL DU MULTIPLICATEUR DE CONTACT
C
      DO 20,INO = 1,NNO
        MCON(INO) = ZR(JCNSV2-1+NO(INO))
   20 CONTINUE
      CALL CALFFX(ALIAS,KSI1,KSI2,FF)
      DO 30 J = 1,NNO
        LAMBDA = FF(J)*MCON(J) + LAMBDA
   30 CONTINUE
C
      CALL DETRSD('CHAM_NO_S',CNS1)
      CALL DETRSD('CHAM_NO_S',CNS2)    
C  
      CALL JEDEMA()
      END
