C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*8   NOMA
      INTEGER       POSMAE,NNO
      CHARACTER*19  CNS1Z
      CHARACTER*24  DEFICO
      REAL*8        MCON(9) 
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE)
C
C CALCUL DU MULTIPLICATEUR DE CONTACT EN UN POINT ESCLAVE
C      
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  CNS1   : DEPLACEMENT APRES CONVERGENCE DE NEWTON (CHAM_NO_S)
C IN  POSMAE : INDICE DE LA MAILLE ESCLAVE DANS CONTAMA
C IN  NNO    : NOMBRE DE NOEUDS DE LA MAILLE ESCLAVE
C OUT MCON   : MULTIPLICATEURS DE CONTACT SUR LES NOEUDS ESCLAVES
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C           
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER  NBNMAX
      PARAMETER (NBNMAX = 9)
C
      INTEGER      INO
      INTEGER      NO(NBNMAX),POSNNO(NBNMAX)
      INTEGER      JCNSV2
      CHARACTER*19 CNS1,CNS2
      INTEGER      CODRET
C  
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      CNS1   = CNS1Z
      CNS2   = '&&CALLAM.CNS2'
      DO 10,INO = 1,9
        MCON(INO) = 0.D0
   10 CONTINUE
C
C --- NUMEROS DANS SD CONTACT DES NOEUDS DE LA MAILLE ESCLAVE
C
      CALL CFNUMN(NOMA  ,DEFICO,POSMAE,NNO   ,POSNNO) 
C
C --- NUMEROS ABSOLUS DES NOEUDS DE LA MAILLE ESCLAVE
C
      CALL CFPOSM(NOMA  ,DEFICO,'NOEU',NNO   ,POSNNO,
     &            NO    ,CODRET)
      IF (CODRET.LT.0) THEN
        CALL ASSERT(.FALSE.)
      ENDIF             
C
C --- EXTRACTION DES LAGRANGIENS DE CONTACT DU VECTEUR DEPLACEMENT
C     
      CALL CNSRED(CNS1,NNO,NO,1,'LAGS_C','V',CNS2)
      CALL JEVEUO(CNS2//'.CNSV','L',JCNSV2)
C
C --- CALCUL DU MULTIPLICATEUR DE CONTACT
C
      DO 20 INO = 1,NNO
        MCON(INO) = ZR(JCNSV2-1+NO(INO))
   20 CONTINUE
C
      CALL DETRSD('CHAM_NO_S',CNS2)    
C 
      CALL JEDEMA()
      END
