C =============================================================
C   BUT : RECUPERER TOUS LES INDICES DU VECTEUR ZR DANS LEQUEL
C         SE TROUVE LES COORD., LES DFDE et DFDK, LE POIDS DE
C         LA DEUXIEMME FAMILLE DE PT DE GAUSS DE QUAS4.
C
C        IN   :  NNO  NOMBRE DE NOEUDS
C        OUT  :  DFDE   DERIVEE DES FF DANS REP DE REF
C        OUT  :  DFDK   DERIVEE DES FF DANS REP DE REF
C        OUT  :  POIDS  POIDS DES PTS DE GAUSS
C        OUT  :  COOPG  COORD.DES PTS DE GAUSS
C
C =============================================================

      IMPLICIT NONE

      CHARACTER*8 ELREFE
      INTEGER NNO,I
      INTEGER ICARAC,IFF,IPOIDS,IVF,IDFDE,IDFDK
      INTEGER ITREF

C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN DECLARATIONS NORMALISEES  JEVEUX ---------------------


C        AJ. VARIABLES

      INTEGER K
      INTEGER IFAP,NBPG(10)
      CHARACTER*24 CARAC,FFO
      REAL*8 SDFDE(4,4),SDFDK(4,4),COOPG(8),POIPG(4),J

      CALL JEMARQ()

      CALL ELREF1(ELREFE)

      FFO = '&INEL.'//ELREFE//'.FF'
      CARAC = '&INEL.'//ELREFE//'.CARAC'

      CALL JEVETE(FFO,'L',IFF)
      CALL JEVETE(CARAC,'L',ICARAC)

      DO 10 I = 1,10
        NBPG(I) = 0
   10 CONTINUE

      NBPG(1) = ZI(ICARAC+2)
      NBPG(2) = ZI(ICARAC+3)

      DO 20 IFAP = 1,2
        IF (IFAP.EQ.1) THEN
          IPOIDS = IFF
        ELSE
          IPOIDS = IDFDK + NBPG(1)*NNO
        END IF
        IVF = IPOIDS + ZI(ICARAC+IFAP+1)
        IDFDE = IVF + ZI(ICARAC+IFAP+1)*NNO
        IDFDK = IDFDE + ZI(ICARAC+IFAP+1)*NNO
   20 CONTINUE

      CALL ELRFGF(ELREFE,2,NBPG,27*3,COOPG,27,POIPG)

      DO 40 I = 1,NNO
        K = NNO* (I-1)
        DO 30 J = 1,NNO
          SDFDE(I,J) = ZR(IDFDE+K+J-1)
          SDFDK(I,J) = ZR(IDFDK+K+J-1)
   30   CONTINUE
   40 CONTINUE

      CALL JEDEMA()
      END
