      SUBROUTINE MMBCLC(NOMA  ,ITERAT,NUMINS,SDDISC,SDDYNA,
     &                  DEFICO,RESOCO,VALINC,SOLALG,SDTIME,
     &                  SDSTAT,CTCSTA,MMCVCA)
