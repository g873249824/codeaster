C IN  NOMA   : NOM DU MAILLAGE
C IN  NUMINS : NUMERO D'INSTANT
C IN  SDDISC : SD DISCRETISATION
C IN  SDDYNA : SD DYNAMIQUE
C IN  DEFICO : SD DEFINITION CONTACT
C IN  RESOCO : SD RESOLUTION CONTACT
C IN  VALINC : VARIABLE CHAPEAU POUR INCREMENTS VARIABLES
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C    

      LOGICAL CFDISL,LALLV,MMCVCA
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- FONCTIONNALITES ACTIVEES
C
      LALLV  = CFDISL(DEFICO,'ALL_VERIF')
      IF (LALLV) GOTO 999
C
C --- CHANGEMENT DES STATUTS
C
      CALL MMMBCA(NOMA  ,SDDYNA,DEFICO,RESOCO,VALINC,
     &            MMCVCA)
C
C --- MISE A JOUR DE LA CARTE
C
      CALL MMCHML(NOMA  ,DEFICO,RESOCO,SDDISC,SDDYNA,
     &            NUMINS)
C
 999  CONTINUE
C
      CALL JEDEMA()
C
      END
