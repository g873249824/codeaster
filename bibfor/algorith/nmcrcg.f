C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C CREATION ET INITIALISATION DE LA SD CONVERGENCE
C
C ----------------------------------------------------------------------
C
C IN  FONACT : FONCTIONNALITES ACTIVEES (VOIR NMFONC)
C OUT SDCONV : SD GESTION DE LA CONVERGENCE
C
