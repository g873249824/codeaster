      IMPLICIT     NONE
      INTEGER      FONACT(*)
      CHARACTER*19 SDDISC,VALINC(*)
      INTEGER      NUMINS
      CHARACTER*24 SDERRO
      CHARACTER*24 DEFICO,RESOCO
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C ETAT DE LA CONVERGENCE DU PAS DE TEMPS
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  NUMINS : NUMERO D'INSTANT
C IN  SDERRO : SD GESTION DES ERREURS
C IN  VALINC : VARIABLE CHAPEAU INCREMENTS DES VARIABLES
C IN  FONACT : FONCTIONNALITES ACTIVEES
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  RESOCO : SD DE RESOLUTION DU CONTACT
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      IEVDAC
      CHARACTER*4  ETFIXE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ETAT DE LA BOUCLE DE POINT FIXE
C
      CALL NMLEEB(SDERRO,'FIXE',ETFIXE)
C
C --- SI PAS DE CONVERGENCE DU POINT FIXE -> TRANSFERT ETAT DE LA BOUCLE
C
      IF (ETFIXE.NE.'CONV') THEN
        IF (ETFIXE.EQ.'STOP') THEN
          CALL NMECEB(SDERRO,'INST','STOP')
        ELSEIF (ETFIXE.EQ.'ERRE') THEN
          CALL NMECEB(SDERRO,'INST','ERRE')
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF
        GOTO 99
      ENDIF
C
C --- EVALUATION CONVERGENCE PAS DE TEMPS
C
      CALL NMEVCV(SDERRO,FONACT,'INST')
C
C --- DETECTION DU PREMIER EVENEMENT DECLENCHE
C
      CALL NMEVEV(SDDISC,NUMINS,VALINC,SDERRO,DEFICO,
     &            RESOCO,'INST')
C
C --- UN EVENEMENT SE DECLENCHE
C
      CALL NMACTO(SDDISC,IEVDAC)
      IF (IEVDAC.GT.0) CALL NMECEB(SDERRO,'INST','EVEN')
C
  99  CONTINUE
C
      CALL JEDEMA()
      END
