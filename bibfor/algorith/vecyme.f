C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.

C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.

C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*(*) MODELZ,CARELZ,COMPLZ,MATE,CNCHTP
      CHARACTER*14 COMPLU
      CHARACTER*24 MODELE,CARELE,NUMEDD,COMPOR

C ----------------------------------------------------------------------
C     CALCUL DU VECTEUR ASSEMBLE DU CHARGEMENT HYDRIQUE

C IN  MODELZ  : NOM DU MODELE
C IN  CARELZ  : CARACTERISTIQUES DES POUTRES ET COQUES
C IN  MATE    : MATERIAU
C IN  COMPLU  : SD VARI_COM
C IN  NUMEDD : NOM DE LA NUMEROTATION
C VAR CNCHTP  : VECTEUR ASSEMBLE DU CHARGEMENT HYDRIQUE

C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------

      CHARACTER*32 JEXNUM,JEXNOM,JEXR8,JEXATR
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
      CHARACTER*1 TYPRES
      CHARACTER*8 VECEL,LPAIN(15),LPAOUT(1)
      CHARACTER*8 K8BID,REPK,NOMGD,NEWNOM
      CHARACTER*16 OPTION
      CHARACTER*24 CHGEOM,CHMATE,CHCARA(15),CHTIME,CHTREF
      CHARACTER*24 LIGRMO,LCHIN(15),LCHOUT(1)
      CHARACTER*24 VECHYP,CNCHYP,VRCPLU,SECPLU
      CHARACTER*19 CHVREF
      LOGICAL EXIGEO,EXICAR,EXITRF,EXITEM
      COMPLEX*16 CBID
      DATA CNCHYP/' '/
      DATA TYPRES/'R'/


      CALL JEMARQ()
      NEWNOM = '.0000000'
      MODELE = MODELZ
      CARELE = CARELZ
      COMPLU = COMPLZ
      CHVREF='&&VECYME.CHVREF'

C    EXTRACTION DES VARIABLES DE COMMANDE
      CALL NMVCEX('INST',COMPLU,CHTIME)
      CALL NMVCEX('TOUT',COMPLU,VRCPLU)


C    EXISTENCE D'UN VRAI CHAMP D'HYDRATATION

      VECEL = '&&VECYME'
      CALL DETRSD('VECT_ELEM',VECEL)
      VECHYP = VECEL//'.LISTE_RESU'
      CALL MEMARE('V',VECEL,MODELE(1:8),MATE,CARELE,'CHAR_MECA')
      CALL WKVECT(VECHYP,'V V K24',1,JLVE)

      LIGRMO = MODELE(1:8)//'.MODELE'

      CALL MEGEOM(MODELE(1:8),VRCPLU,EXIGEO,CHGEOM)
      CALL DISMOI('F','ELAS_F_TEMP',MATE,'CHAM_MATER',IBID,REPK,IERD)
      CALL MECARA(CARELE(1:8),EXICAR,CHCARA)
      CALL VRCREF(MODELE(1:8),MATE(1:8),CARELE(1:8),CHVREF)
      CALL NMVCD2('TEMP',MATE,EXITEM,EXITRF)



      LPAIN(1) = 'PGEOMER'
      LCHIN(1) = CHGEOM
      LPAIN(2) = 'PTEMPSR'
      LCHIN(2) = CHTIME
      LPAIN(3) = 'PMATERC'
      LCHIN(3) = MATE
      LPAIN(4) = 'PCACOQU'
      LCHIN(4) = CHCARA(7)
      LPAIN(5) = 'PCAGNPO'
      LCHIN(5) = CHCARA(6)
      LPAIN(6) = 'PCADISM'
      LCHIN(6) = CHCARA(3)
      LPAIN(7) = 'PCAORIE'
      LCHIN(7) = CHCARA(1)
      LPAIN(8) = 'PCAGNBA'
      LCHIN(8) = CHCARA(11)
      LPAIN(9) = 'PCAARPO'
      LCHIN(9) = CHCARA(9)
      LPAIN(10) = 'PCAMASS'
      LCHIN(10) = CHCARA(12)
      LPAIN(11) = 'PVARCPR'
      LCHIN(11) = VRCPLU
      LPAIN(12) = 'PVARCRR'
      LCHIN(12) = CHVREF
      LPAIN(13) = 'PCOMPOR'
      LCHIN(13) = COMPOR
      OPTION = 'CHAR_MECA_HYDR_R'
      LPAOUT(1) = 'PVECTUR'

      LCHOUT(1) = '&&VECYME.???????'
      CALL GCNCO2(NEWNOM)
      LCHOUT(1) (10:16) = NEWNOM(2:8)
      CALL CORICH('E',LCHOUT(1),-1,IBID)
      CALL CALCUL('S',OPTION,LIGRMO,13,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'V')
      ZK24(JLVE) = LCHOUT(1)
      CALL JEECRA(VECHYP,'LONUTI',1,K8BID)

C ----- ASSEMBLAGE DU VECT_ELEM

      CALL ASASVE(VECHYP,NUMEDD,TYPRES,CNCHYP)

C ----- SOMMATION DES CHAMPS DE DEFORMATION THERMIQUE

      CALL JEEXIN(CNCHTP,IRET)
      IF (IRET.EQ.0) THEN
        CNCHTP = CNCHYP
      ELSE IF (.NOT.EXITEM) THEN
        CNCHTP = CNCHYP
      ELSE
        CALL JEVEUO(CNCHTP,'L',JTP)
        CALL JELIRA(ZK24(JTP) (1:19)//'.VALE','LONMAX',LONCH,K8BID)
        CALL JEVEUO(ZK24(JTP) (1:19)//'.VALE','E',JCHTP)
        CALL JEVEUO(CNCHYP,'L',JYP)
        CALL JEVEUO(ZK24(JYP) (1:19)//'.VALE','E',JCHYP)
        DO 10 I = 1,LONCH
          ZR(JCHTP+I-1) = ZR(JCHTP+I-1) + ZR(JCHYP+I-1)
   10   CONTINUE
      END IF

   20 CONTINUE

      CALL JEDEMA()
      END
