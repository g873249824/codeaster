C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C-----------------------------------------------------------------------
C DESCRIPTION : CALCUL DES PARAMETRES (ALGO METHODE INTEGRALE)
C -----------   POUR PASSAGE DU TRANSITOIRE
C
C               APPELANT : CALTRA
C
C-------------------   DECLARATION DES VARIABLES   ---------------------
C
C ARGUMENTS
C ---------
      INTEGER     NP4, NFOUR, NBM
      REAL*8      TTRANS, AMOR(*), PULS(*), PULSD(*)
      COMPLEX*16  S0(*), Z0(*)
      REAL*8      OMEGAF(*)
      COMPLEX*16  ZA4(NP4,*), ZA5(NP4,*)
C
C VARIABLES LOCALES
C -----------------
      INTEGER     I, J, N2M1
      REAL*8      KSI0, ZERO
      COMPLEX*16  ZOMEGA
C
C FONCTIONS INTRINSEQUES
C ----------------------
C     INTRINSIC   DCMPLX, EXP, SQRT
C
C ROUTINES EXTERNES
C -----------------
C     EXTERNAL    UTMESS
C
C-------------------   DEBUT DU CODE EXECUTABLE    ---------------------
C
      ZERO = 0.0D0
      N2M1 = (NFOUR/2) - 1
C
      DO 10 I = 1, NBM
         KSI0 = AMOR(I)/(2.0D0*PULS(I))
         IF ( KSI0.GE.1.0D0 ) THEN
            KSI0 =  0.99D0
            CALL U2MESS('A','ALGORITH9_74')
         ELSE IF ( KSI0.LE.-1.0D0 ) THEN
            KSI0 = -0.99D0
            CALL U2MESS('A','ALGORITH9_75')
         ENDIF
         PULSD(I) = PULS(I) * SQRT( 1.0D0 - KSI0*KSI0 )
         S0(I)    = DCMPLX( -KSI0*PULS(I), PULSD(I) )
         Z0(I)    = EXP( S0(I)*TTRANS )
C
         DO 11 J = 1, N2M1
            ZOMEGA = DCMPLX( ZERO, OMEGAF(J) )
            ZA4(J,I) = (Z0(I) - EXP(ZOMEGA*TTRANS)) /  (S0(I) - ZOMEGA)
            ZA5(J,I) = (Z0(I) - EXP(-ZOMEGA*TTRANS)) /  (S0(I) + ZOMEGA)
  11     CONTINUE
C
  10  CONTINUE
C
C --- FIN DE PARMTR.
      END
