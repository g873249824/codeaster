      SUBROUTINE PARMTR(NP4,NFOUR,NBM,TTRANS,AMOR,PULS,PULSD,S0,Z0,
     &                  OMEGAF,ZA4,ZA5)
      IMPLICIT NONE
C-----------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 29/09/2006   AUTEUR VABHHTS J.PELLET 
