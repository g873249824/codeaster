      DATA CNVCPR,CNMODP    /'&&NMCH5P.CNVCPR','&&NMCH5P.CNMODP'/
      DATA CNDYNA           /'&&NMCH5P.CNDYNA'/
      DATA CNMODC,CNCTDC    /'&&NMCH5P.CNMODC','&&NMCH5P.CNCTDC'/
