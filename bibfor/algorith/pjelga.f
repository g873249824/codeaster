C  ELLE EST CONSTITUEE DE TROIS TEMPS
C    APPEL A ECLPGC (ECLATEMENT DU CHAMP)
C    APPEL A PJXXCH (USUEL POUR TOUS LES CHAMPS, CF. OP0166 OU PJXXPR)
C    APPEL A PJCORR (RETOUR AUX POINTS DE GAUSS)

C
      IMPLICIT   NONE
C
C 0.1. ==> ARGUMENTS
C
      INTEGER IER
C
C 0.2. ==> COMMUNS
C ----------------------------------------------------------------------
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR,R8B
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC,C16B
      COMMON /CVARJE/ZC(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24,NOOJB
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
C 0.3. ==> VARIABLES LOCALES
C
C
      INTEGER IRET,NNCP,JCH2SV
      CHARACTER*8 K8B,PROL0,MA1P
      CHARACTER*8 NUME
      CHARACTER*16 CORRES,OPTION
      CHARACTER*19 CHAM1,CHAM1E,CHAUXS
      CHARACTER*19 LERES1,LIGRE1,LIGRE2
      CHARACTER*19 PRFCH2,PRFCHN
      CHARACTER*19 CNS1,CH2S
      CHARACTER*24 NOMFPG

      INTEGER JCNSV,I,JCESV
      CHARACTER*19 CES1


C DEB ------------------------------------------------------------------
      CALL JEMARQ()
      CALL INFMAJ()
      IER=0
      CALL TITRE


      NOMFPG='&&OP0166.NOMFPG'
      CALL CELFPG(CHAM1,NOMFPG,IRET)


      PRFCH2 = '12345678.00000.NUME'
C     ON ESSAYE DE RECUPERER LA NUMEROTATION IMPOSEE
      CALL GETVID(' ','NUME_DDL',1,1,1,NUME,IER)
      IF (IER.NE.0) THEN
        PRFCH2 = NUME(1:8)//'      .NUME'
      ENDIF

      IF (PRFCH2.NE.'12345678.00000.NUME') THEN
        PRFCHN = PRFCH2
      ELSE
C       ON DEFINIT UNE NUMEROTATION 'BIDON"
        NOOJB = '12345678.00000.NUME.PRNO'
        CALL GNOMSD(NOOJB,10,14)
        PRFCHN = NOOJB(1:19)
      ENDIF


C ECLATEMENT DU CHAMP VIVANT SUR LE MAILLAGE 1
C EN UN CHAMP VIVANT SUR LE MAILLAGE 1 PRIME (MAILLAGE ECLATE)


      MA1P='&&PJELC1'
      CHAM1E='&&PJELGA'//'.CHAM1E'

      CALL ECLPGC(CHAM1,CHAM1E,LIGRE1,MA1P,PRFCHN,NOMFPG)




      CNS1 = '&&OP0166'//'.CH1S'



      CALL CNOCNS(CHAM1E,'G',CNS1)
      CALL JEVEUO(CNS1//'.CNSV','L',JCNSV)



      CHAUXS = '&&PJELGA'//'.CHAS'

      CALL PJXXCH(CORRES,CHAM1E,
     &            CHAUXS,' ',PROL0,' ','G',IRET)




C ON TRANSFORME LE CHAMP_NO PROJETE EN UN CHAM_NO_S

      CNS1 = '&&OP0166'//'.CH1S'




      CALL CNOCNS(CHAUXS,'G',CNS1)



      CALL JEVEUO(CNS1//'.CNSV','L',JCNSV)




C IL FAUT MAINTENANT REVENIR AUX CHAM_ELEM (ELGA)
C
C


      CH2S = '&&OP0166'//'.CH2S'

      CALL PJCORR(CNS1,'V',CH2S,IRET,LIGRE2,CORRES)
C

      PRINT *, 'PROL0', PROL0


      CALL CESCEL(CH2S,LIGRE2,' ',' ',PROL0,
     &           NNCP,'G',LERES1,'A',IRET)



      CALL JEVEUO(LERES1//'.CELV','L',JCH2SV)    

      CALL DETRSD('MAILLAGE',MA1P)


      CALL JEDEMA()
      END
