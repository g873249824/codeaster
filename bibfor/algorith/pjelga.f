C  COMMANDE:  PROJ_CHAMP
C  ROUTINE "CHAPEAU" CONCERNANT LA PROJECTION DE CHAM_ELEM (ELGA)
C                                  *  *                     ****
C
C  ELLE EST CONSTITUEE DE TROIS TEMPS
C    APPEL A ECLPGC (ECLATEMENT DU CHAMP)
C    APPEL A PJXXCH (USUEL POUR TOUS LES CHAMPS, CF. OP0166 OU PJXXPR)
C    APPEL A PJCORR (RETOUR AUX POINTS DE GAUSS)

C
      IMPLICIT   NONE
C
C 0.1. ==> ARGUMENTS
C
      CHARACTER*8 NOMO2,PROL0
      CHARACTER*16 CORRES
      CHARACTER*19 CHAM1,LIGRE1
      CHARACTER*19 LERES1,LIGRE2
      INTEGER IRET
C
C 0.2. ==> COMMUNS
C ----------------------------------------------------------------------
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR,R8B
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC,C16B
      COMMON /CVARJE/ZC(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24,NOOJB
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
C 0.3. ==> VARIABLES LOCALES
C
C
      INTEGER IER,NNCP,JCH2SV
      CHARACTER*8 K8B,NUME,MA1P,KBID
      CHARACTER*8 NOMGD,NOMPAR
      CHARACTER*16 OPTION
      CHARACTER*19 CHAM1E,CHAUXS,CHBID
      CHARACTER*19 PRFCH2,PRFCHN
      CHARACTER*19 CNS1,CH2S
      CHARACTER*24 NOMFPG

      INTEGER JCNSV,JCESV,NBGREL,JCELD,NBVARI,IBID
      CHARACTER*19 CES1


C DEB ------------------------------------------------------------------
      CALL JEMARQ()
      CALL INFMAJ()
      IER=0
      CALL TITRE

      CHBID=CHAM1


      NOMFPG='&&OP0166.NOMFPG'
      CALL CELFPG(CHAM1,NOMFPG,IRET)


      PRFCH2 = '12345678.00000.NUME'
C     ON ESSAYE DE RECUPERER LA NUMEROTATION IMPOSEE
      CALL GETVID(' ','NUME_DDL',1,1,1,NUME,IER)
      IF (IER.NE.0) THEN
        PRFCH2 = NUME(1:8)//'      .NUME'
      ENDIF

      IF (PRFCH2.NE.'12345678.00000.NUME') THEN
        PRFCHN = PRFCH2
      ELSE
C       ON DEFINIT UNE NUMEROTATION 'BIDON"
        NOOJB = '12345678.00000.NUME.PRNO'
        CALL GNOMSD(NOOJB,10,14)
        PRFCHN = NOOJB(1:19)
      ENDIF



C ON RECUPERE LE NOM DE L'OPTION ET LE NOM DU PARAMETRE
C   POUR LE CHAM_ELEM VIVANT SUR LE MAILLAGE 1

      CALL DISMOI ( 'F', 'NOM_OPTION', CHAM1, 'CHAM_ELEM',
     &              IBID, OPTION, IBID )

      CALL DISMOI ( 'F', 'NOM_PARAM', CHAM1, 'CHAM_ELEM',
     &              IBID, NOMPAR, IBID )



C ECLATEMENT DU CHAMP VIVANT SUR LE MAILLAGE 1
C   EN UN CHAMP VIVANT SUR LE MAILLAGE 1 PRIME (MAILLAGE ECLATE)

      MA1P='&&PJELC1'
      CHAM1E='&&PJELGA'//'.CHAM1E'

      CALL JEVEUO(CHAM1//'.CELD','L',JCELD)
      NBVARI=ZI(JCELD+4-1)

      CALL ECLPGC(CHAM1,CHAM1E,LIGRE1,MA1P,PRFCHN,NOMFPG)


      CNS1 = '&&OP0166'//'.CH1S'

      CALL CNOCNS(CHAM1E,'G',CNS1)
      CALL JEVEUO(CNS1//'.CNSV','L',JCNSV)


      CHAUXS = '&&PJELGA'//'.CHAS'

      CALL PJXXCH(CORRES,CHAM1E,
     &            CHAUXS,' ',PROL0,' ','G',IRET)



C ON TRANSFORME LE CHAMP_NO PROJETE EN UN CHAM_NO_S

      CNS1 = '&&OP0166'//'.CH1S'
      CALL CNOCNS(CHAUXS,'G',CNS1)
      CALL JEVEUO(CNS1//'.CNSV','L',JCNSV)


C IL FAUT MAINTENANT REVENIR AUX CHAM_ELEM (ELGA)


      CH2S = '&&OP0166'//'.CH2S'


      CALL PJCORR(NOMO2,CHBID,CNS1,'V',CH2S,
     &      LIGRE2,CORRES,NBVARI,OPTION,NOMPAR,IRET)
C

      CALL CESCEL(CH2S,LIGRE2,' ',' ',PROL0,
     &           NNCP,'G',LERES1,'A',IRET)


      CALL DETRSD('MAILLAGE',MA1P)
      CALL DETRSD('CHAM_NO_S',CNS1)
      CALL DETRSD('CHAM_NO',CHAM1E)
      CALL DETRSD('CHAM_NO',CHAUXS)

      CALL JEDEMA()
      END
