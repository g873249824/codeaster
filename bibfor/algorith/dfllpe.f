      VALERE = 0.D0
      NOCHAM = ' '
      NOCMP  = ' '
      CRICMP = ' '
C
C --- EVENEMENT
C        
      IF (EVEN.EQ.'DELTA_GRANDEUR') THEN
        CALL GETVR8(MCFACT,'VALE_REF',IECHEC,IARG,1,VALERE,IBID)
        CALL GETVTX(MCFACT,'NOM_CHAM',IECHEC,IARG,1,NOCHAM,IBID)
        CALL GETVTX(MCFACT,'NOM_CMP' ,IECHEC,IARG,1,NOCMP ,IBID)
        CRICMP = 'GT'
      ELSEIF (EVEN.EQ.'INTERPENETRATION') THEN
        CALL GETVR8(MCFACT,'PENE_MAXI',IECHEC,IARG,1,PENMAX,IBID)
      ENDIF
C
      END
