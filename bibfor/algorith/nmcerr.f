C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*19 SDDISC
      INTEGER      ITER1,ITER2
      INTEGER      IDIV
      REAL*8       ELASDT,INIKRY
      REAL*8       RGMAXI,RGRELA
C
C ----------------------------------------------------------------------
C
C ROUTINE *_NON_LINE (STRUCTURES DE DONNES - SD DISCRETISATION)
C
C CREATION SD STOCKAGE DES INFOS EN COURS DE CALCUL
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION
C IN  ITER1  : ITER_GLOB_MAXI
C IN  ITER2  : ITER_GLOB_ELAS
C IN  IDIV   : OCCURENCE OU ON A EVENEMENT ='DIVERGENCE_ITER' 0 SINON
C IN  ELASDT : PAS_MINI_ELAS
C IN  INIKRY : PRECISION INITIALE POUR NEWTON-KRYLOV
C IN  RGMAXI : RESI_GLOB_MAXI
C IN  RGRELA : RESI_GLOB_RELA
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER      ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8       ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16   ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL      ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8  ZK8
      CHARACTER*16    ZK16
      CHARACTER*24        ZK24
      CHARACTER*32            ZK32
      CHARACTER*80                ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      REAL*8       R8BID
      CHARACTER*8  K8BID
      REAL*8       R8VIDE
      INTEGER      ITERAT,TYPRES
      INTEGER      NBITER,NBPLUS,MXITER,MNITER
      INTEGER      CEIL
      CHARACTER*24 TPSERR
      INTEGER      JERRE
C     
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      MXITER = MAX(ITER1,ITER2)
      MNITER = MIN(ITER1,ITER2)
      ITERAT = 0
C
C --- NOMBRE MAXIMUM D'ITERATIONS
C
      IF (IDIV.EQ.0) THEN
        NBITER = MXITER
      ELSE
C ----- NOMBRE D'ITERATIONS AUTORISEES EN PLUS
        CALL UTDIDT('L'   ,SDDISC,'ECHE',IDIV,'SUBD_ITER_PLUS',
     &              R8BID ,NBPLUS,K8BID )
        NBITER = CEIL(MXITER*(1.D0 + NBPLUS/100.0D0))
      ENDIF
C
C --- AJOUTER LA PREDICTION
C
      NBITER = NBITER+1 
C
C --- CREATION DU VECTEUR
C
      TPSERR = SDDISC(1:19)//'.ERRE'
      CALL WKVECT(TPSERR,'V V R8',9+NBITER*2,JERRE)
C
C --- JERRE + 1-1 : MAX( ITER_GLOB_MAXI , ITER_GLOB_ELAS )
C     JERRE + 2-1 : MIN( ITER_GLOB_MAXI , ITER_GLOB_ELAS )
C     JERRE + 3-1 : NBITER (DIMENSION DU VECTEUR)
C     JERRE + 4-1 : PAS_MINI_ELAS
C     JERRE + 5-1 : RESI_GLOB_RELA
C     JERRE + 6-1 : RESI_GLOB_MAXI
C
      CALL NMLERI(SDDISC,'E','MXITER'        ,ITERAT,MXITER)
      CALL NMLERI(SDDISC,'E','MNITER'        ,ITERAT,MNITER)
      CALL NMLERI(SDDISC,'E','NBITER'        ,ITERAT,NBITER)
      CALL NMLERR(SDDISC,'E','PAS_MINI_ELAS' ,ITERAT,ELASDT)
C
C --- JERRE + 7-1 : =1 ON A DONNE RESI_GLOB_RELA
C                   =2 ON A DONNE RESI_GLOB_MAXI
C                   =3 ON A DONNE RESI_GLOB_RELA ET RESI_GLOB_MAXI
C                   =0 ON A RIEN DONNE ==> =1
C
      TYPRES = 0
      IF (RGRELA.NE.R8VIDE()) THEN
        TYPRES = TYPRES + 1
      ENDIF
      IF (RGMAXI.NE.R8VIDE()) THEN
        TYPRES = TYPRES + 1 + 1
      ENDIF
      IF ((RGRELA.EQ.R8VIDE()) .AND. (RGMAXI.NE.R8VIDE())) THEN
        RGRELA = 1.0D-06
        RGMAXI = 1.0D-06
        TYPRES = 1
      ENDIF
      CALL NMLERR(SDDISC,'E','RESI_GLOB_RELA',ITERAT,RGRELA)
      CALL NMLERR(SDDISC,'E','RESI_GLOB_MAXI',ITERAT,RGMAXI)
      CALL NMLERI(SDDISC,'E','TYPE_RESI'     ,ITERAT,TYPRES)
C
C --- JERRE + 8-1 : RESIDU INITIAL POUR NEWTON-KRYLOV
C
      CALL NMLERR(SDDISC,'E','INIT_NEWTON_KRYLOV',ITERAT,INIKRY)
C
C --- JERRE + 9-1 : RESIDU COURANT POUR NEWTON-KRYLOV
C
      CALL NMLERR(SDDISC,'E','ITER_NEWTON_KRYLOV',ITERAT,INIKRY)
       
      CALL JEDEMA()

      END
