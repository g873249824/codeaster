C I/O ICOL   : INDICE DANS LE VECTEUR JIMCOL
C
C ----------------------------------------------------------------------
C
      INTEGER ICOD
C
C ----------------------------------------------------------------------
C
      IF ((ICOL.GT.ZDEF).OR.(ICOL.LE.0)) THEN
        WRITE(6,*) 'ICOL:',ICOL
        CALL ASSERT(.FALSE.)
      ENDIF

      CALL IMPCOD(COLONN,ICOD  )

      IF (ICOD.EQ.0) THEN
        WRITE(6,*) 'COLONN:',COLONN
        CALL ASSERT(.FALSE.)
      ENDIF

      IMPTMP(ICOL) = ICOD
      ICOL         = ICOL + 1

      IF (ICOL.GT.ZDEF) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
