C BUT:   CALCUL DES DEPLACEMENTS ET DE LA GEOMETRIE D'UN POINT DONNE

C ENTREES  ---> NNO         : NOMBRE DE NOEUDS DE L'ELEMENT
C
C
C          ---> FFE         : LES FF AU POINT DE CONTACT
C          ---> IGEOM       : ADRESSE DE LA GEOMETRIE DE L'ELEMENT
C          ---> IDEPL       : ADRESSE DU DEPLACEMENT DE L'ELEMENT
C          ---> NDIM        : DIMENSION DE L'ESPACE
C          ---> NDDL        : NOMBRE TOTAL DES DDLS
C          ---> IES         : 1 ESCLAVE 2 MAITRE

C SORTIES  <--- GEOM        : LA GEOMETRIE AU PC
C          <--- DEPL : LE DEPLACEMENT ET LES MULTIPLICATEURS DE CONTACT

C.......................................................................

C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------

      INTEGER NNO,IGEOM,IDEPL,IES
      INTEGER I,J,NDIM,NDDL
      REAL*8 GEOM(3),FFE(9),DEPL(6)

      DO 20 I = 1,NDIM
        GEOM(I) = 0.D0
        DO 10 J = 1,NNO
          GEOM(I) = GEOM(I) +FFE(J)*ZR(IGEOM+(J-1)*NDIM+I-1)
   10   CONTINUE
   20 CONTINUE

      IF(IES.EQ.1) THEN
      DO 40 I = 1,NDDL
        DEPL(I)=0.D0
        DO 30 J = 1,NNO
          DEPL(I) = DEPL(I) +FFE(J)*ZR(IDEPL+ (J-1)*(NDIM+1)+I-1)
   30   CONTINUE
   40 CONTINUE

       ELSE IF(IES.EQ.2) THEN

      DO 41 I = 1,NDDL
        DEPL(I)=0.D0
        DO 31 J = 1,NNO
          DEPL(I) = DEPL(I) +FFE(J)*ZR(IDEPL+ (J-1)*NDIM+I-1)
   31   CONTINUE
   41 CONTINUE
      ELSE
          CALL UTMESS('F','GETGDP','STOP1')
      END IF

      END
