C                        SUR LA NORME DU RESIDU
C       ----------------------------------------------------------------
C       IN
C            DY     :  VECTEUR SOLUTION DY = ( DSIG DVINT)
C            DDY    :  VECTEUR CORRECTION SUR LA SOLUTION
