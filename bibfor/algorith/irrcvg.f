        REAL*8   TOLER,DDY(NR),DY(NR),R(NR),RINI(NR),MATER(NMAT,2)

        REAL*8   ER,VALRM(2)
C       ----------------------------------------------------------------
C
C     CALCUL DE LA NORME DE RINI ET DE R(Y)
      ER = 0.0D0
      DO 10 II=1,NR
         ER = ER + R(II)*R(II)
10    CONTINUE
      ER = SQRT(ER)
C     ER < TOLER , ON A CONVERGE
      IF ( ER .LT. TOLER ) THEN
C        VERIFICATION SUR LE FRANCHISSEMENT DU SEUIL
         IF ( ABS(MATER(21,2)) .GT. MATER(20,2) ) THEN
            VALRM(1) = MATER(20,2)
            VALRM(2) = MATER(21,2)
            CALL U2MESR('I','COMPOR1_58',2,VALRM)
C           REDECOUPAGE GLOBAL
            IRTETI = 4
            GOTO 9999
         ENDIF
         IRTETI = 0
         GOTO 9999
      ENDIF

      IF ( ITER .LT. ITMAX ) THEN
C         CALL LCNRVN(NR,RINI,ERINI)
C          WRITE(*,*) 'ER ERINI', ER,ERINI,ER/ERINI,TOLER
C        SI ER/ERINI < TOLER ON A CONVERGE
C         IF ( (ER/ERINI) .LE. TOLER ) THEN
C            IRTETI = 0
C            GOTO 9999
C         ENDIF
C -      EVALUATION DE :  ERRR(1)  = MAX( ABS(R(I)/RINI(I)) )
C         CALL LCVERR(RINI,R,NR,0,ERREUR)
C -      CONVERGENCE    MAX(R,RINI)
C         IF ( ERREUR(1) .LE. TOLER ) THEN
C            IRTETI = 0
C            GOTO 9999
C         ENDIF
C -      NON CONVERGENCE ITERATION SUIVANTE
         IRTETI = 1
         GOTO 9999
C      ELSE
C -      EVALUATION DE :  ERRR(1)  = MAX( ABS(R(I)/RINI(I)) )
C         CALL LCVERR(RINI,R,NR,0,ERREUR)
C        RESIDU NON NUL MAIS STABLE. ON ACCEPTE
C         IF ( ERREUR(1) .LE. TOLER ) THEN
C            IRTETI = 0
C            GOTO 9999
C         ENDIF
C -      EVALUATION DE :  ERRDY(1)  = MAX( ABS(DDY(I)/DY(I)) )
C         CALL LCVERR(DY,DDY,NR,0,ERREUR)
C        RESIDU NON NUL MAIS SOLUTION STABLE. ON ACCEPTE : MAX(DDY,DY)
C         IF ( ERREUR(1) .LE. TOLER ) THEN
C            IRTETI = 0
C            GOTO 9999
C         ENDIF
      ENDIF
C -   NON CONVERGENCE ET ITMAX ATTEINT : REDECOUPAGE LOCAL
      IRTETI = 3
C
 9999 CONTINUE
      END
