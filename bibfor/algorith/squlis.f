         IF ( NUMNOE(I) .EQ. I1 ) GOTO 32
 30   CONTINUE
      NBNOEU = NBNOEU + 1
      NUMNOE(NBNOEU) = I1
C
 32   CONTINUE
      DO 40 I = 1 , NBNOEU
         IF ( NUMNOE(I) .EQ. I2 ) GOTO 9999
 40   CONTINUE
      NBNOEU = NBNOEU + 1
      NUMNOE(NBNOEU) = I2
C
 9999 CONTINUE
      END
