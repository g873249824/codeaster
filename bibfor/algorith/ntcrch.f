      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*24 MODELE
      CHARACTER*24 NUMEDD
      CHARACTER*24 HYDR0,VHYDR
C
C ----------------------------------------------------------------------
C
C ROUTINE THER_NON_LINE (ALGORITHME - INITIALISATIONS)
C
C CREATION DES VECTEURS D'INCONNUS
C
C ----------------------------------------------------------------------
C
C IN  NUMEDD : NUME_DDL
C
C
C
C
      CHARACTER*19 HYDRIC,HYDRIS,LIGRMO
      INTEGER      IBID,NNCP,IRET,NEQ
      CHARACTER*8  K8BID
      CHARACTER*24 VTEMP
      COMPLEX*16   C16BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      HYDRIC = '&&NTCRCH.HYDR_C'
      HYDRIS = '&&NTCRCH.HYDR_S'
      HYDR0  = '&&NTCRCH.HYDR0'
      CALL DISMOI('F','NOM_LIGREL',MODELE,'MODELE',IBID  ,LIGRMO,IRET)
C
C --- CREATION CHAMP HYDRATATION
C
      IF (VHYDR.NE.' ') THEN
        CALL MECACT('V',HYDRIC,'MODELE',LIGRMO,'HYDR_R',1,
     &              'HYDR',IBID,0.D0,C16BID,K8BID)
        CALL CARCES(HYDRIC,'ELNO',' ','V',HYDRIS,'A',IRET)
        CALL CESCEL(HYDRIS,LIGRMO,'RESI_RIGI_MASS','PHYDRPP',
     &              'NON',NNCP,'V',HYDR0 ,'F',IBID)
        CALL COPISD('CHAMP_GD','V',HYDR0,VHYDR)
      ENDIF
C
C --- CREATION DES CHAMPS
C
      VTEMP='&&NXLECTVAR_____'
      CALL VTCREB(VTEMP ,NUMEDD,'V','R',NEQ)
C
      CALL DETRSD('CHAMP',HYDRIC)
      CALL DETRSD('CHAMP',HYDRIS)
C
      CALL JEDEMA()
      END
