C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*8  RESULT
      INTEGER      NUMARC
      CHARACTER*19 SDDYNA
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (DYNAMIQUE)
C
C ARCHIVAGE DEPL/VITE/ACCEL GENERALISES DANS SD_RESULT
C      
C ----------------------------------------------------------------------
C
C  
C IN  RESULT : NOM UTILISATEUR DU CONCEPT RESULTAT 
C IN  NUMARC : NUMERO DE L'ARCHIVAGE 
C IN  SDDYNA : SD DYNAMIQUE 
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C      
      INTEGER      JPARA
      CHARACTER*8  K8BID
      CHARACTER*6  CHFORD
      CHARACTER*19 DGEN,VGEN,AGEN
      INTEGER      JRESTD,JRESTV,JRESTA  
      INTEGER      NBMODP,NDYNIN    
      CHARACTER*24 TRGENE     
      CHARACTER*19 DEPGEP,VITGEP,ACCGEP
      INTEGER      JDEPGP,JVITGP,JACCGP      
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- NOM TRAN_GENE_NOLI
C      
      CALL CODENT(NUMARC,'D0',CHFORD)
      TRGENE = RESULT(1:8)//'.TRG'//CHFORD(1:6)
C
C --- ARCHIVAGE NOM TRAN_GENE_NOLI
C
      CALL RSADPA(RESULT,'E',1,'TRAN_GENE_NOLI',NUMARC,0,JPARA,K8BID)
      ZK24(JPARA) = TRGENE
      NBMODP = NDYNIN(SDDYNA,'NBRE_MODE_PROJ')
C
C --- NOM VECTEURS DEPL/VITE/ACCE GENERALISES
C      
      DGEN   = TRGENE(1:18)//'D'
      VGEN   = TRGENE(1:18)//'V'
      AGEN   = TRGENE(1:18)//'A' 
C
C --- CREATION DEPL/VITE/ACCE GENERALISES
C       
      CALL WKVECT(DGEN,'G V R',NBMODP,JRESTD)
      CALL WKVECT(VGEN,'G V R',NBMODP,JRESTV)
      CALL WKVECT(AGEN,'G V R',NBMODP,JRESTA)                   
C
C --- ARCHIVAGE
C 
      CALL NDYNKK(SDDYNA,'PRMO_DEPGEP',DEPGEP)
      CALL NDYNKK(SDDYNA,'PRMO_VITGEP',VITGEP)
      CALL NDYNKK(SDDYNA,'PRMO_ACCGEP',ACCGEP)
      NBMODP = NDYNIN(SDDYNA,'NBRE_MODE_PROJ')              
      CALL JEVEUO(ACCGEP,'L',JACCGP)
      CALL JEVEUO(VITGEP,'L',JVITGP)
      CALL JEVEUO(DEPGEP,'L',JDEPGP)  
      CALL DCOPY(NBMODP,ZR(JDEPGP),1,ZR(JRESTD),1)
      CALL DCOPY(NBMODP,ZR(JVITGP),1,ZR(JRESTV),1)
      CALL DCOPY(NBMODP,ZR(JACCGP),1,ZR(JRESTA),1) 
C    
      CALL JEDEMA()

      END
