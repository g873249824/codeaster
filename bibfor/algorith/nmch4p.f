      CALL NMCHA0('VEELEM','CNFINT',VEFINT,VEELEM)
      CALL NMCHA0('VEELEM','CNDIRI',VEDIRI,VEELEM)
      CALL NMCHA0('VEELEM','CNBUDI',VEBUDI,VEELEM)
