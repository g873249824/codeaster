      IMPLICIT     NONE
      INCLUDE 'jeveux.h'
      CHARACTER*8  RESULT,NOMA,NOMO
      INTEGER      NUMREO
      CHARACTER*19 SDOBSE
      CHARACTER*24 SDIETO
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (STRUCTURES DE DONNES)
C
C CREATION SD OBSERVATION
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  NOMO   : NOM DU MODELE
C IN  SDIETO : SD GESTION IN ET OUT
C IN  SDOBSE : NOM DE LA SD POUR OBSERVATION
C IN  RESULT : NOM UTILISATEUR DU RESULTAT
C IN  NUMREO : NUMERO DE REUSE POUR LA TABLE OBSERVATION
C
C ----------------------------------------------------------------------
C
      INTEGER      IFM,NIV
      INTEGER      NTOBS,NBOCC
      CHARACTER*16 MOTFAC
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFNIV(IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<    NONLINE> ... CREATION SD OBSERVATION'
      ENDIF
C
C --- INITIALISATIONS
C
      NTOBS  = 0
      MOTFAC = 'OBSERVATION'
C
C --- NOMBRE OCCURRENCES
C
      CALL GETFAC(MOTFAC,NBOCC)
      CALL ASSERT(NBOCC.LE.99)
C
C --- LECTURE DES DONNEES
C
      CALL NMEXTR(NOMA  ,NOMO  ,SDOBSE,SDIETO,MOTFAC,
     &            NBOCC ,NUMREO,NTOBS )
C
C --- CONTROLE
C
      IF (NTOBS.NE.0) THEN
        CALL U2MESI('I','OBSERVATION_3',1,NTOBS)
      ELSE
        GOTO 999
      ENDIF
C
C --- LECTURE LISTES INSTANTS
C
      CALL NMCROI(SDOBSE,MOTFAC,NBOCC )
C
C --- NOM DES COLONNES
C
      CALL NMOBNO(SDOBSE,MOTFAC,NBOCC )
C
C --- CREATION DE LA TABLE D'OBSERVATION
C
      CALL NMCROT(RESULT,SDOBSE)
C
 999  CONTINUE
C
      CALL JEDEMA()

      END
