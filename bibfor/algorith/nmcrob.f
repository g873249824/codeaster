      IMPLICIT     NONE
      CHARACTER*8  RESULT,MAILLA
      CHARACTER*14 SDOBSE
      CHARACTER*24 SDSUIV 
      CHARACTER*19 SDDISC
      INTEGER      NUMINI 
      CHARACTER*16 NOMCMD              
C
C ----------------------------------------------------------------------
C
C ROUTINE *_NON_LINE (STRUCTURES DE DONNES)
C
C CREATION SD OBSERVATION
C
C ----------------------------------------------------------------------
C
C
C IN  NOMCMD : NOM DE LA COMMANDE APPELANTE (THER, STAT OU DYNA)
C IN  SDDISC : SD LOCALE SUR LA GESTION DE LA LISTE D'INSTANTS
C IN  MAILLA : NOM DU MAILLAGE 
C IN  RESULT : NOM UTILISATEUR DU RESULTAT
C IN  NUMINI : NUMERO PREMIER INSTANT DE CALCUL
C OUT SDSUIV : NOM DE LA SD POUR SUIVI DDL
C OUT SDOBSE : NOM DE LA SD POUR OBSERVATION
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER      ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8       ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16   ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL      ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8  ZK8
      CHARACTER*16    ZK16
      CHARACTER*24        ZK24
      CHARACTER*32            ZK32
      CHARACTER*80                ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      CHARACTER*19 LISOBS,TABOBS,INFOBS
      INTEGER      JOBSE ,JOBST ,JOBSI
      INTEGER      NBINST,NBPAS,IBID
      CHARACTER*8  K8BID
      INTEGER      IFM,NIV 
      CHARACTER*16 TYPECO
      REAL*8       R8B
C ----------------------------------------------------------------------
C      
      CALL JEMARQ()
C
C --- AFFICHAGE
C
      IF (NOMCMD(1:4).EQ.'THER') THEN
        CALL INFDBG('THER_NON_LINE',IFM,NIV)
        IF (NIV.GE.2) THEN
          WRITE (IFM,*) '<THERNONLINE> ... CREATION SD OBSERVATION'
        ENDIF    
      ELSE
        CALL INFDBG('MECA_NON_LINE',IFM,NIV)
        IF (NIV.GE.2) THEN
          WRITE (IFM,*) '<MECANONLINE> ... CREATION SD OBSERVATION'
        ENDIF          
      ENDIF      
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- INITIALISATIONS
C
      CALL UTDIDT('L',SDDISC,'LIST',IBID,'NBINST',R8B,NBINST,K8BID)
      NBPAS = NBINST - 1
C
C --- CREATION DE LA SD
C
      LISOBS = SDOBSE(1:14)//'.LIST'
      TABOBS = SDOBSE(1:14)//'.TABL' 
      INFOBS = SDOBSE(1:14)//'.INFO'       
      CALL WKVECT(LISOBS,'V V I'  ,NBINST,JOBSE)
      CALL WKVECT(TABOBS,'V V K24',     1,JOBST)
      CALL WKVECT(INFOBS,'V V I'  ,     6,JOBSI)            
C
C --- REMPLISSAGE DE LA SD   
C
      CALL DYOBSE(MAILLA,RESULT,NBPAS,SDDISC,NUMINI,
     &            SDOBSE,SDSUIV)
C
      CALL JEDEMA()

      END
