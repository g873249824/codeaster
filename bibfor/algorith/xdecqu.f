      SUBROUTINE XDECQU(IT,NDIM,CONNEC,JLSN,JGRLSN,IGEOM,PINTER,
     &               NINTER,NPTS,AINTER,PMILIE,NMILIE,MFIS)
      IMPLICIT NONE 

      INTEGER       IT,NDIM,CONNEC(6,6),NINTER,IGEOM,NPTS,NMILIE,MFIS
      INTEGER       JGRLSN,JLSN
      CHARACTER*24  PINTER,AINTER,PMILIE

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 11/10/2010   AUTEUR COURTOIS M.COURTOIS 
C ======================================================================
