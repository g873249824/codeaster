C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*8  NOMA
      CHARACTER*24 DEFICO,RESOCO
      CHARACTER*19 VALINC(*)
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE VERIF)
C
C ROUTINE PRINCIPALE
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  NOMA   : NOM DU MAILLAGE
C IN  VALINC : VARIABLE CHAPEAU POUR INCREMENTS VARIABLES
C
C
C
C
      INTEGER      IFM,NIV
      CHARACTER*19 SDAPPA,OLDGEO,NEWGEO,DEPPLU
      LOGICAL      CFDISL,LCTCC,LCTCD,LALLV
      CHARACTER*24 JEUX,LOCA,ENTI,ZONE
      INTEGER      NPT
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> MODE VERIF'
      ENDIF
C
C --- TYPE DE CONTACT
C
      LCTCC  = CFDISL(DEFICO,'FORMUL_CONTINUE')
      LCTCD  = CFDISL(DEFICO,'FORMUL_DISCRETE')
      LALLV  = CFDISL(DEFICO,'ALL_VERIF')
C
C --- DECOMPACTION VARIABLES CHAPEAUX
C
      OLDGEO = NOMA(1:8)//'.COORDO'
      CALL NMCHEX(VALINC,'VALINC','DEPPLU',DEPPLU)
C
C --- NOM DES SDs
C
      SDAPPA = RESOCO(1:14)//'.APPA'
      NEWGEO = RESOCO(1:14)//'.NEWG'
C
C --- MAJ GEOMETRIE
C
      IF (LALLV) THEN
        CALL VTGPLD('CUMU',OLDGEO,1.D0  ,DEPPLU,'V'   ,
     &              NEWGEO)
      ENDIF
C
C --- CREATION SD APPARIEMENT EN MODE ALL VERIF
C
      IF (LALLV) THEN
C
C ----- RE-REMPLISSAGE DE LA SD APPARIEMENT - POINTS (COORD. ET NOMS)
C
        IF (LCTCC) THEN
          CALL MMPOIN(NOMA  ,DEFICO,NEWGEO,SDAPPA)
        ELSEIF (LCTCD) THEN
          CALL CFPOIN(NOMA  ,DEFICO,NEWGEO,SDAPPA)
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF
C
C ----- REALISATION DE L'APPARIEMENT
C
        CALL APCALC(SDAPPA)
C
      ENDIF
C
C --- CREATION SD PROVISOIRES POUR VERIF
C
      CALL CFMMVC(DEFICO,JEUX  ,LOCA  ,ENTI  ,ZONE  ,
     &            NPT   )
C
C --- EVALUATION DES POINTS EN MODE VERIF
C
      IF (LCTCC) THEN
        CALL MMVERI(NOMA  ,DEFICO,RESOCO,NEWGEO,SDAPPA,
     &              NPT   ,JEUX  ,LOCA  ,ENTI  ,ZONE  )
      ELSEIF (LCTCD) THEN
        CALL CFVERI(NOMA  ,DEFICO,RESOCO,NEWGEO,SDAPPA,
     &              NPT   ,JEUX  ,LOCA  ,ENTI  ,ZONE  )
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- AFFICHAGE DES INTERPENETRATIONS EVENTUELLES
C
      CALL CFSANS(DEFICO,NPT   ,JEUX  ,ENTI  ,ZONE)
C
C --- SAUVEGARDE DANS LA SD RESULTAT
C
      CALL CFMMVS(RESOCO,NPT   ,JEUX  ,LOCA  )
C
C --- NETTOYAGE
C
      CALL JEDETR(JEUX  )
      CALL JEDETR(LOCA  )
      CALL JEDETR(ENTI  )
      CALL JEDETR(ZONE  )
C
      CALL JEDEMA()

      END
