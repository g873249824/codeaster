      SUBROUTINE XAJPIN(NDIM,LIST,LONG,IPT,CPT,NEWPT,LONGAR,AINTER,IA,
     &                  IN,AL)
      IMPLICIT NONE

      INCLUDE 'jeveux.h'
      INTEGER       NDIM,LONG,IPT,CPT,IA,IN
      REAL*8        NEWPT(3),LONGAR,AL,LIST(*),AINTER(*)
C     ------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
