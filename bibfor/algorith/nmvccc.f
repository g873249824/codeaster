      SUBROUTINE NMVCCC(MODELE,NBIN  ,NBOUT ,LPAIN ,LCHIN ,
     &                  LPAOUT,LCHOUT,EXITEM,EXIHYD,EXISEC,
     &                  EXIEPA,EXIPHA,VECEL )
