      IMPLICIT NONE
      INTEGER       NMAFON,NMAEN1,NMAEN2,NMAEN3
      INTEGER       JMAFON,JMAEN1,JMAEN2,JMAEN3
      CHARACTER*8   NOMA

C      
C ----------------------------------------------------------------------
C
C ROUTINE XFEM
C
C IMPRESSION DU STATUT DES MAILLES
C
C ----------------------------------------------------------------------
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  NMAFON : NOMBRE DE MAILLES CONTENANT LE FOND DE FISSURE
C IN  NMAEN1 : NOMBRE DE MAILLES 'HEAVISIDE'
C IN  NMAEN2 : NOMBRE DE MAILLES 'CRACKTIP'
C IN  NMAEN3 : NOMBRE DE MAILLES 'HEAVISIDE-CRACKTIP'
C IN  JMAFON : POINTEUR SUR MAILLES 'CONTENANT LE FOND DE FISSURE
C IN  JMAEN1 : POINTEUR SUR MAILLES 'HEAVISIDE'
C IN  JMAEN2 : POINTEUR SUR MAILLES 'CRACKTIP'
C IN  JMAEN3 : POINTEUR SUR MAILLES 'HEAVISIDE-CRACKTIP'
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*32  JEXATR,JEXNUM
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER       IFM,NIV,IMA
      CHARACTER*8   NOMAIL
      CHARACTER*19  NOMMAI

      CALL JEMARQ()
      CALL INFDBG('XFEM',IFM,NIV)  

      NOMMAI = NOMA//'.NOMMAI'

      WRITE(IFM,*)'NOMBRE DE MAILLES DE MAFON  :',NMAFON
      IF (NIV.GT.1) THEN
        DO 320 IMA=1,NMAFON
          CALL JENUNO(JEXNUM(NOMMAI,ZI(JMAFON-1+IMA)),NOMAIL)
          WRITE(IFM,*)'MAILLE ',NOMAIL
 320    CONTINUE
      ENDIF

      WRITE(IFM,*)'NOMBRE DE MAILLES DE MAENR1 :',NMAEN1
      IF (NIV.GT.1) THEN
        DO 321 IMA=1,NMAEN1
          CALL JENUNO(JEXNUM(NOMMAI,ZI(JMAEN1-1+IMA)),NOMAIL)
          WRITE(IFM,*)'MAILLE ',NOMAIL
 321    CONTINUE
      ENDIF

      WRITE(IFM,*)'NOMBRE DE MAILLES DE MAENR2 :',NMAEN2
      IF (NIV.GT.1) THEN
        DO 322 IMA=1,NMAEN2
          CALL JENUNO(JEXNUM(NOMMAI,ZI(JMAEN2-1+IMA)),NOMAIL)
          WRITE(IFM,*)'MAILLE ',NOMAIL
 322    CONTINUE
      ENDIF

      WRITE(IFM,*)'NOMBRE DE MAILLES DE MAENR3 :',NMAEN3
      IF (NIV.GT.1) THEN
        DO 323 IMA=1,NMAEN3
          CALL JENUNO(JEXNUM(NOMMAI,ZI(JMAEN3-1+IMA)),NOMAIL)
          WRITE(IFM,*)'MAILLE ',NOMAIL
 323    CONTINUE
      ENDIF

      CALL JEDEMA()
      END
