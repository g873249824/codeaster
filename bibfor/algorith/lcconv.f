     &        ITER,INTG, NMAT,MATER, R, RINI,EPSTR,TYPESS, ESSAI,
     &        ICOMP, NVI,VIND,VINF, VIND1, INDI,BNEWS,MTRAC,LRELI,
     &        IRET)
