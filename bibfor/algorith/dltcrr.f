     &                    T0, LCREA, TYPRES,
     &                    MASSE, RIGID, AMORT,
     &                    DEP0, VIT0, ACC0,
     &                    FEXTE,FAMOR,FLIAI,
     &                    NUMEDD, NUME, NBTYAR, TYPEAR )
