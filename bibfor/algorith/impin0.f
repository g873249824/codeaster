      IMPLICIT NONE
      INTEGER      ZDEF
      CHARACTER*16 MOTFAC
      LOGICAL      LINFRE,LINFTP
      CHARACTER*19 COLACT
      INTEGER      UNITE
C
C ----------------------------------------------------------------------
C
C ROUTINE UTILITAIRE (AFFICHAGE)
C
C CREATION SD AFFICHAGE - PREPARATION
C
C ----------------------------------------------------------------------
C
C
C IN  ZDEF   : NOMBRE MAXI DE COLONNES DISPONIBLES POUR L'AFFICHAGE
C IN  MOTFAC : MOT-CLEF FACTEUR POUR LIRE INFORMATIONS AFFICHAGES
C OUT COLACT : VECTEUR D'ACTIVATION DES COLONNES
C OUT LINFRE : .TRUE. SI INFO_RESIDU
C OUT LINFTP : .TRUE. SI INFO_TEMPS
C OUT UNITE  : UNITE DU FICHIER POUR AFFICHAGE TABLEAU EN PLUS DE .MESS
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IMPTMP
      CHARACTER*3  CMP
      INTEGER      NOC     
      INTEGER      IARG
C      
C ----------------------------------------------------------------------
C      
      CALL JEMARQ()
C
C --- NOM SD PROVISOIRE COLONNES ACTIVES
C
      COLACT = '&&IMPIN0.COLACT'  
      LINFRE = .FALSE.
      UNITE  = 0
C
C --- INFO SUR LES RESIDUS      
C
      CALL GETVTX(MOTFAC,'INFO_RESIDU',1,IARG,1,CMP,NOC)
      IF (NOC.EQ.0) THEN
        LINFRE = .FALSE.
      ELSE
        IF (CMP.EQ.'OUI') THEN
          LINFRE = .TRUE.
        ELSEIF (CMP.EQ.'NON') THEN
          LINFRE = .FALSE.
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF
      ENDIF
C
C --- INFO SUR LE TEMPS     
C
      CALL GETVTX(MOTFAC,'INFO_TEMPS',1,IARG,1,CMP,NOC)
      IF (NOC.EQ.0) THEN
        LINFTP = .FALSE.
      ELSE
        IF (CMP.EQ.'OUI') THEN
          LINFTP = .TRUE.
        ELSEIF (CMP.EQ.'NON') THEN
          LINFTP = .FALSE.
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF
      ENDIF
C
C --- UNITE FICHIER CSV
C
      CALL GETVIS(MOTFAC,'UNITE',1,IARG,1,UNITE,NOC)
C
C --- CREATION SD PROVISOIRE COLONNES ACTIVES
C
      CALL WKVECT(COLACT,'V V I',ZDEF,IMPTMP)   
C
C --- MENAGE
C
      CALL JEDEMA()

      END
