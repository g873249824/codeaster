C ROUTINE CALCULANT LA MASSE AJOUTEE SUR LE MODELE THERMIQUE
C                    D INTERFACE
C IN: POTENTIELS PHIBARRE ET PHI1
C OUT : CIJ1 :COEFFICIENT D'AMORTISSEMENT CORRESPONDANT AU POTENTIEL
C PHI1
      INCLUDE 'jeveux.h'
      INTEGER       IBARRE,IPHI1,IMADE,NPHI1
      REAL*8        CIJ1,DDOT
      CHARACTER*1   K1BID
      CHARACTER*19  PHIB24,PHI1J,BJ
C--------------------------------------------------------------------

C-----------------------------------------------------------------------
      INTEGER IPROD
C-----------------------------------------------------------------------
      CALL JEMARQ()
           CALL JEVEUO(PHI1J//'.VALE','L',IPHI1)
           CALL JEVEUO(PHIB24//'.VALE','L',IBARRE)
           CALL JELIRA(PHI1J//'.VALE','LONMAX',NPHI1,K1BID)

           CALL WKVECT('&&CALCI.PRODUIT','V V R',NPHI1,IPROD)

C --- RECUPERATION DES DESCRIPTEURS DE MATRICE ASSEMBLEE MADE

           CALL MTDSCR(BJ)
           CALL JEVEUO(BJ(1:19)//'.&INT','E',IMADE)

C ---PRODUIT MATRICE BJ ET LE VECTEUR POTENTIEL PHI1J

           CALL MRMULT('ZERO',IMADE,ZR(IPHI1),ZR(IPROD),1,.TRUE.)


C--PRODUITS SCALAIRES VECTEURS  PHI1J PAR LE VECTEUR RESULTAT PRECEDENT

           CIJ1= DDOT(NPHI1,ZR(IBARRE), 1,ZR(IPROD), 1)

C---------------- MENAGE SUR LA VOLATILE ---------------------------
C
           CALL JEDETR('&&CALCI.PRODUIT')

      CALL JEDEMA()
