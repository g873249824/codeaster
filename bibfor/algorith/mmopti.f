C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*24 RESOCO
      INTEGER      IPTC
      REAL*8       SEUILI,EPSINT,JEUSGN
      LOGICAL      PREMIE,CTCINI,LCOMPL,LGLISS
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT)
C
C REMPLIT LA SD APPARIEMENT POUR LES OPTIONS
C      
C ----------------------------------------------------------------------
C
C
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  SEUILI : VALEUR DU SEUIL_INIT
C IN  PREMIE : VAUT .TRUE. SI PREMIER INSTANT DE STAT/DYNA_NON_LINE
C IN  CTCINI : VAUT .TRUE. SI CONTACT_INIT
C IN  LCOMPL : VAUT .TRUE. SI COMPLIANCE
C IN  LGLISS : SI CONTACT GLISSIERE
C IN  IPTC   : NUMERO DE LA LIAISON DE CONTACT
C IN  EPSINT : TOLERANCE POUR CONTACT_INIT
C IN  JEUSGN : JEU SIGNE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      CFMMVD,ZTABF      
      CHARACTER*24 TABFIN
      INTEGER      JTABF
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ() 
C
C --- ACCES SD CONTACT
C
      TABFIN = RESOCO(1:14)//'.TABFIN'
      CALL JEVEUO(TABFIN,'E',JTABF)
C
      ZTABF = CFMMVD('ZTABF')
C
C --- SEUIL_INIT
C
      IF (PREMIE) THEN
        ZR(JTABF+ZTABF*(IPTC-1)+16) = SEUILI
      END IF
C
C --- CONTACT_INIT
C
      IF (PREMIE) THEN
        IF (CTCINI) THEN
          IF (JEUSGN.LE.EPSINT) THEN
            ZR(JTABF+ZTABF*(IPTC-1)+22) = 1.D0
            IF (LGLISS) THEN
              ZR(JTABF+ZTABF*(IPTC-1)+17) = 1.D0
            ENDIF
          ENDIF
        ELSE
          ZR(JTABF+ZTABF*(IPTC-1)+22) = 0.D0
        ENDIF
      ENDIF
C
C --- INDICATEUR DE COMPLIANCE
C
      IF (PREMIE) THEN
        ZR(JTABF+ZTABF*(IPTC-1)+23) = 0.D0
      END IF
      IF (.NOT.LCOMPL) THEN
        ZR(JTABF+ZTABF*(IPTC-1)+23) = 1.D0
      END IF
C
C --- NOEUDS EXCLUS: PAS EN CONTACT
C
      IF (ZR(JTABF+ZTABF*(IPTC-1)+18) .EQ. 1.D0) THEN
        ZR(JTABF+ZTABF*(IPTC-1)+22) = 0.D0
      END IF      
C
      CALL JEDEMA()      
C
      END
