      IMPLICIT NONE
      CHARACTER*19 LISINS
      REAL*8       DTMIN
      INTEGER      NBINST
C      
C ----------------------------------------------------------------------
C
C ROUTINE GESTION LISTE INSTANTS
C
C VERIFICATIONS LISTE D'INSTANTS
C
C ----------------------------------------------------------------------
C
C
C IN  LISINS : NOM DE LA SD LISTE
C OUT DTMIN  : PAS DE TEMPS MINIMUM DE LA LISTE
C OUT NBINST : NOMBRE D'INSTANTS
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C 
      INTEGER      I
      INTEGER      JINST
      REAL*8       DELTAT,R8MAEM
      CHARACTER*8  K8BID 
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DTMIN  = R8MAEM()
C
C --- RECUPERATION DE LA LISTE D'INSTANTS FOURNIE
C
      CALL JEVEUO(LISINS//'.VALE','L',JINST)
      CALL JELIRA(LISINS//'.VALE','LONMAX',NBINST,K8BID) 
C
C --- VERIFICATION IL Y A AU MOINS UN INSTANT DE CALCUL
C
      IF (NBINST.LT.2) CALL U2MESS('F','DISCRETISATION_86')
C
C --- INTERVALLE DE TEMPS MINIMAL : DTMIN
C
      DO 30 I = 1,NBINST-1
        DELTAT = ZR(JINST+I) - ZR(JINST+I-1)
        DTMIN  = MIN(DELTAT,DTMIN)
30    CONTINUE
C
C --- VERIFICATION DE LA STRICTE CROISSANCE DE LA LISTE D'INSTANTS
C
      IF (DTMIN.LE.0.D0) CALL U2MESS('F','DISCRETISATION_87') 
C
      CALL JEDEMA()
      END
