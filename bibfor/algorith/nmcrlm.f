      CHARACTER*24 TPSINF
      CHARACTER*19 PROVLI
      CHARACTER*19 SDDISC,LISINS     
C
C ----------------------------------------------------------------------
C
C ROUTINE *_NON_LINE (STRUCTURES DE DONNES - DISCRETISATION)
C
C CREATION LISTE D'INSTANT PROVISOIRE ET TPSINF A PARTIR DE LIST_R8
C
C ----------------------------------------------------------------------
C
C IN  LISINS : LISTE D'INSTANTS (SD_LISTR8 OU SD_LIST_INST)
C IN  SDDISC : SD DISCRETISATION
C OUT TPSINF : INFORMATIONS SUR LA DISCRETISATION
C OUT PROVLI : LISTE D'INSTANT PROVISOIRE
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER      ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8       ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16   ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL      ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8  ZK8
      CHARACTER*16    ZK16
      CHARACTER*24        ZK24
      CHARACTER*32            ZK32
      CHARACTER*80                ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER      DFLLVD,LLINR,JLINR
      INTEGER      NBINST,IBID
      REAL*8       DTMIN,R8BID
      CHARACTER*8  METLIS,K8BID
C      
C ----------------------------------------------------------------------
C      
      CALL JEMARQ() 
C
C --- INITIALISATIONS
C     
      LLINR  = DFLLVD('LLINR')
C
C --- QUELQUES VERIFICATIONS SUR LA LISTE FOURNIE
C
      CALL DFLLLI(LISINS,DTMIN ,NBINST)
C
C --- DUPLICATION LISTE D'INSTANTS DANS OBJET PROVISOIRE
C
      CALL JEDUP1(LISINS(1:19)//'.VALE','V',PROVLI)
C
C --- CREATION SD TPSINF
C        
      CALL WKVECT(TPSINF,'V V R',LLINR ,JLINR )  
C
C --- REMPLISSAGE SD TPSINF
C
      METLIS = 'MANUEL'
      CALL UTDIDT('E'   ,SDDISC,'LIST',IBID  ,'METHODE',
     &            R8BID ,IBID  ,METLIS)
      CALL UTDIDT('E'   ,SDDISC,'LIST',IBID  ,'DTMIN'  ,
     &            DTMIN ,IBID  ,K8BID )
      CALL UTDIDT('E'   ,SDDISC,'LIST',IBID  ,'NBINST' ,
     &            R8BID ,NBINST,K8BID )
C
      CALL JEDEMA()

      END
