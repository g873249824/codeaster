C
C DETECTION D'UN CYCLE
C
C ----------------------------------------------------------------------
C
C
C CETTE ROUTINE DETECTE UN CYCLE DE TYPE 0/1 SUR UNE LONGUEUR DONNEE
C
C CYCLE LONGUEUR 3: 0 1 0 -> 4
C                   1 0 1 -> 10
C
C IN  CCYCLE : ENTIER CODE REPRESENTANT LE CYCLE
C IN  LONGCY : LONGUEUR DU CYCLE
C
C ----------------------------------------------------------------------
