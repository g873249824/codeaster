C
C ROUTINE CONTACT (TOUTES METHODES - APPARIEMENT)
C
C DIT SI LA NORMALE DOIT ETRE CALCULEE SUR LE TYPE
C DE NOEUD DONNE
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  IZONE  : ZONE DE CONTACT
C IN  TYPNOE : TYPE DU NOEUD 'MAIT' OU 'ESCL'
C OUT CFCALD : .TRUE. SI NORMALE A CALCULER
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      MMINFI,IAPPA
      LOGICAL      MMINFL,CFDISL,LLISS,LMAIT,LESCL,LMAES,LPIVOT
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      CFCALD = .FALSE.
C
C --- FONCTIONNALITES ACTIVEES
C
      LLISS  = CFDISL(DEFICO,'LISSAGE')
      LMAIT  = MMINFL(DEFICO,'MAIT'             ,IZONE)
      LESCL  = MMINFL(DEFICO,'ESCL'             ,IZONE)
      LMAES  = MMINFL(DEFICO,'MAIT_ESCL'        ,IZONE)
      LPIVOT = MMINFL(DEFICO,'EXCLUSION_PIV_NUL',IZONE)
      IAPPA  = MMINFI(DEFICO,'APPARIEMENT'      ,IZONE)
C
C --- CALCUL OU NON ?
C
      IF (TYPNOE.EQ.'MAIT') THEN
        IF (LLISS) THEN
          IF (LMAIT.OR.LMAES) THEN
            CFCALD = .TRUE.
          ENDIF
        ENDIF
        IF (IAPPA.EQ.0) THEN
          IF (LMAIT.OR.LMAES) THEN
            CFCALD = .TRUE.
          ENDIF
        ENDIF
      ELSEIF (TYPNOE.EQ.'ESCL') THEN
        IF (LESCL.OR.LMAES.OR.LPIVOT) THEN
          CFCALD = .TRUE.
        ENDIF
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
      CALL JEDEMA()
      END
