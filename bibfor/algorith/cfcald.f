C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (TOUTES METHODES - APPARIEMENT)
C
C DIT SI LA NORMALE DOIT ETRE CALCULEE SUR LE TYPE
C DE NOEUD DONNE 
C      
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  IZONE  : ZONE DE CONTACT
C IN  TYPNOE : TYPE DU NOEUD 'MAIT' OU 'ESCL'
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      CFDISI,IAPPA
      INTEGER      IBID
      REAL*8       R8BID
      CHARACTER*24 K24BLA,K24BID
      LOGICAL      LLISS,LMAIT,LESCL,LMAES
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      CFCALD = .FALSE.
      K24BLA = ' '
C
C --- FONCTIONNALITES ACTIVEES
C
      CALL MMINFP(IZONE ,DEFICO,K24BLA,'LISSAGE'  ,
     &            IBID  ,R8BID ,K24BID,LLISS) 
      CALL MMINFP(IZONE ,DEFICO,K24BLA,'MAIT'     ,
     &            IBID  ,R8BID ,K24BID,LMAIT)
      CALL MMINFP(IZONE ,DEFICO,K24BLA,'ESCL'     ,
     &            IBID  ,R8BID ,K24BID,LESCL) 
      CALL MMINFP(IZONE ,DEFICO,K24BLA,'MAIT_ESCL',
     &            IBID  ,R8BID ,K24BID,LMAES)   
      IAPPA  = CFDISI(DEFICO,'APPARIEMENT',IZONE)                      
C
C --- CALCUL OU NON ?
C
      IF (TYPNOE.EQ.'MAIT') THEN
        IF (LLISS) THEN
          IF (LMAIT.OR.LMAES) THEN
            CFCALD = .TRUE.
          ENDIF
        ENDIF 
        IF (IAPPA.EQ.0) THEN
          IF (LMAIT.OR.LMAES) THEN
            CFCALD = .TRUE.
          ENDIF        
        ENDIF  
      ELSEIF (TYPNOE.EQ.'ESCL') THEN
        IF (LESCL.OR.LMAES) THEN
          CFCALD = .TRUE.
        ENDIF      
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
      CALL JEDEMA()
      END
