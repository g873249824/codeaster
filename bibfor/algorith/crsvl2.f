C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------

      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C --- FIN DECLARATIONS NORMALISEES JEVEUX --------------------

      INTEGER      ISLVK,ISLVI,ISLVR
      REAL*8       JEVTBL
      CHARACTER*8  RENUM

C------------------------------------------------------------------
      CALL JEMARQ()

C --- OPTION INVALIDE
      IF (CASPAR.NE.1) CALL ASSERT(.FALSE.)
     
C --- ON REMPLIT LA SD_SOLVEUR
      CALL JEVEUO(SOLVEU//'.SLVK','E',ISLVK)
      CALL JEVEUO(SOLVEU//'.SLVR','E',ISLVR)
      CALL JEVEUO(SOLVEU//'.SLVI','E',ISLVI)

      IF (CASPAR.EQ.1) THEN
        RENUM='SANS'
      ENDIF      
      ZK24(ISLVK-1+1) = 'LDLT'
      ZK24(ISLVK-1+2) = 'XXXX'
      ZK24(ISLVK-1+3) = 'XXXX'
      ZK24(ISLVK-1+4) = RENUM
      ZK24(ISLVK-1+5) = 'XXXX'
      ZK24(ISLVK-1+6) = 'XXXX'
      ZK24(ISLVK-1+7) = 'XXXX'
      ZK24(ISLVK-1+8) = 'XXXX'
      ZK24(ISLVK-1+9) = 'XXXX'
      ZK24(ISLVK-1+10)= 'XXXX'
      ZK24(ISLVK-1+11)= 'XXXX'

      ZR(ISLVR-1+1) = 0.D0
      ZR(ISLVR-1+2) = 0.D0
      ZR(ISLVR-1+3) = JEVTBL()
      ZR(ISLVR-1+4) = 0.D0

      ZI(ISLVI-1+1) = NPREC
      ZI(ISLVI-1+2) = -9999
      ZI(ISLVI-1+3) = -9999
      ZI(ISLVI-1+4) = -9999      
      ZI(ISLVI-1+5) = -9999      
      ZI(ISLVI-1+6) = -9999      
      ZI(ISLVI-1+7) = -9999      

      CALL JEDEMA()
      END
