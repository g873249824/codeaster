C  BUT : REMPLISSAGE SD_SOLVEUR POUR UN CALCUL MODAL DONT LES MATRICES
C        NE S'APPUIENT PAS SUR DES NOEUDS D'UN MAILLAGE (NUME_DDL_GENE)
C        DANS CE CAS SEUL LDLT EST LICITE.
