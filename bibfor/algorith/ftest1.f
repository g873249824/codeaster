C DESCRIPTION : CALCUL DE LA FONCTION TEST CHOC SUR BUTEE IC
C -----------
C               APPELANTS : MDCHOE, MDCHOF
C
C-------------------   DECLARATION DES VARIABLES   ---------------------
C
C ARGUMENTS
C ---------
      INTEGER    NP3
      REAL*8     RC(NP3,*), THETA(NP3,*)
      INTEGER    TYPCH(*), NBSEG(*)
      REAL*8     XLOC(*)
      INTEGER    IC, ITESTC
      REAL*8     TOLN
C
C VARIABLES LOCALES
C -----------------
C
      INTEGER    TYPOBS, NBS
      REAL*8     XJEU, DNORM, COST, SINT
      REAL*8     FTEST, TOLCH
C
C FONCTIONS INTRINSEQUES
C ----------------------
C     INTRINSIC  ABS
C
C ROUTINES EXTERNES
C -----------------
C     EXTERNAL   DISBUT
C
C-------------------   DEBUT DU CODE EXECUTABLE    ---------------------
C
      ITESTC = 0
      TOLCH  = 10.0D0 * TOLN
C
C --- RECUPERATION DES VALEURS RELATIVES A LA BUTEE IC
C
      TYPOBS = TYPCH(IC)
      NBS    = NBSEG(IC)
      IF ( (TYPOBS.EQ.0).OR.(TYPOBS.EQ.1).OR.(TYPOBS.EQ.2) )
     &   XJEU = RC(1,IC)
C
C --- CALCUL DE LA DISTANCE NORMALE A LA BUTEE
C
      CALL DISBUT(NP3,IC,XLOC,TYPOBS,XJEU,RC,THETA,NBS,COST,SINT,DNORM)
C
C --- CALCUL DE LA FONCTION TEST CHOC SUR BUTEE IC
C
      FTEST = ABS(DNORM)
      IF ( FTEST.LT.TOLCH ) THEN
         DNORM = 0.0D0
         ITESTC = -1
      ENDIF
      IF ( DNORM.LT.0.0D0 ) ITESTC = 1
C
C --- FIN DE FTEST1.
      END
