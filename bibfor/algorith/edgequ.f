C ----------------------------------------------------------------------
C     MODELE VISCOPLASTIQUE SANS SEUIL DE EDGAR
C     CALCUL DU TENSEUR EQUIVALENT AU SENS DE HILL
C     EQUI = DSQRT(TENS(I)*ANI(I,J)*TENS(J))
C IN  DIM  : DIMENSION DE L'ESPACE
C IN  TENS : TENSEUR
C IN  ANI  : MATRICE D ANISOTROPIE DE HILL

C OUT EQUI    : TENSEUR EQUIVALENT
C ----------------------------------------------------------------------

      INTEGER  I,J
      REAL*8   EQUI,PDTSCA(6)
      REAL*8   R8PREM
C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
      DATA     PDTSCA/1.D0,1.D0,1.D0,2.D0,2.D0,2.D0/

      EQUI = 0.D0
      DO 5 I=1,NDIMSI
        DO 10 J=1,NDIMSI
          EQUI=EQUI+PDTSCA(I)*TENS(I)*ANI(I,J)*PDTSCA(J)*TENS(J)
 10     CONTINUE
 5    CONTINUE

      IF(EQUI.GT.0.D0)THEN
        EQUI     = SQRT(EQUI)
      ELSE
        IF(ABS(EQUI).LT.R8PREM())THEN
          EQUI = 0.D0
        ELSE
          CALL U2MESS('F','COMPOR1_71')
        ENDIF
      ENDIF

      EDGEQU = EQUI
      
      END
