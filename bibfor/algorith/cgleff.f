      SUBROUTINE CGLEFF(TYPFIS,NOMFIS,FONOEU,CHFOND,BASFON,LNOFF)
      IMPLICIT NONE
      INCLUDE 'jeveux.h'

      CHARACTER*8  TYPFIS,NOMFIS

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 17/12/2012   AUTEUR DELMAS J.DELMAS 
C ======================================================================
