C
C IMPRESSION D'UNE LIGNE DU TABLEAU DE CONVERGENCE
C
C ----------------------------------------------------------------------
C
C
C IN  SDIMPR : SD AFFICHAGE
C
C ----------------------------------------------------------------------
C
      CHARACTER*24  SDTABC
      LOGICAL       LCSV,LPRINT
      INTEGER       LARLIG,IUNIFI,UNIMES,UNICSV
      CHARACTER*1   SEPCOL
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- TABLEAU DE CONVERGENCE ET OPTIONS
C
      CALL OBGETO(SDIMPR,'TABLEAU_CONV' ,SDTABC)
      CALL OBGETB(SDTABC,'SORTIE_CSV'   ,LCSV  )
      CALL OBGETI(SDTABC,'UNITE_CSV'    ,UNICSV)
      CALL OBGETI(SDTABC,'LARGEUR_LIGNE',LARLIG)
      UNIMES = IUNIFI('MESSAGE')
C
C --- AFFICHAGE POUR CE PAS ?
C
      CALL OBGETB(SDIMPR,'PRINT',LPRINT)
C
C --- IMPRESSION LIGNE DANS LE FICHIER MESSAGE
C
      SEPCOL = '|'
      IF (LPRINT) CALL IMPSDL(SDTABC,SEPCOL,UNIMES)
C
C --- IMPRESSION LIGNE DANS LE FICHIER CSV
C
      IF (LCSV) THEN
        SEPCOL = ','
        CALL IMPSDL(SDTABC,SEPCOL,UNICSV)
      ENDIF
C
      CALL JEDEMA()
      END
