C***********************************************************************
C    CALCULE LA FORCE DUE A LA PRESENCE D'UN DISPOSITIF ANTI SISMIQUE
C
C-----------------------------------------------------------------------
C   IN :  DNORM : DISTANCE NORMALE
C   OUT :  VNORM : VITESSE  NORMALE
C   IN :  VITLOC : VITESSE DANS LE REPERE LOCAL
C   IN :  ACCLOC : ACCELERATION DANS LE REPERE LOCAL
C   IN :  COST,SINT : DIRECTION DE LA NORMALE A L'OBSTACLE
C   IN :  COEFK1 : VALEUR DE RIGI_K1
C   IN :  COEFK2 : VALEUR DE RIGI_K2
C   IN :  COEFPY : VALEUR DE SEUIL_FX
C   IN :  COEFC : VALEUR DE C
C   IN :  COEFAD : VALEUR DE PUIS_ALPHA
C   IN :  XMAX : VALEUR DE DX_MAX
C   IN :  CNORM : AMORTISSEUR NORMALE DE CHOC
C   OUT :  FDISPO : FORCE NORMALE DUE AU DISPO ANTI SISMIQUE
C   OUT :  FLOCAL : FORCE NORMALE DE CHOC REP. LOCAL
C-----------------------------------------------------------------------
      IMPLICIT NONE
      REAL*8 VITLOC(6),FLOCAL(3),FDISPO
      REAL*8 COEFK1, COEFK2, COEFPY, COEFC, COEFAD,XMAX, COST, SINT
C-----------------------------------------------------------------------
      REAL*8 DNORM ,VNORM 
C-----------------------------------------------------------------------
      VNORM = VITLOC(2)*COST + VITLOC(3)*SINT
      IF (VNORM.EQ.0.D0) THEN
          FDISPO = -COEFK2*DNORM -
     &         (COEFK1-COEFK2)*DNORM/SQRT(1.D0+(COEFK1*DNORM/COEFPY)**2)
      ELSE
          FDISPO = -COEFK2*DNORM -
     &         (COEFK1-COEFK2)*DNORM/SQRT(1.D0+(COEFK1*DNORM/COEFPY)**2)
     &        - COEFC*SIGN(1.D0,VNORM)*ABS(VNORM*DNORM/XMAX)**COEFAD
      ENDIF
      FLOCAL(1)=0.D0
      FLOCAL(2)=FDISPO*COST
      FLOCAL(3)=FDISPO*SINT
      END
