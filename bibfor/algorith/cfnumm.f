C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES - UTILITAIRE)
C
C DONNE LES NUMEROS ABSOLUS DES MAILLES DE CONTACT
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE CONTACT (DEFINITION)
C IN  NMA    : NOMBRE DE MAILLES
C IN  POSNMA : INDICE DANS CONTNO DES MAILLES
C OUT NUMNMA : INDICE ABSOLUS DES MAILLES DANS LE MAILLAGE   
C
C
C
C
      INTEGER      IMA,POSMA
      CHARACTER*24 CONTMA
      INTEGER      JMACO
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()       
C
C --- ACCES SD CONTACT
C
      CONTMA = DEFICO(1:16)//'.MAILCO'
      CALL JEVEUO(CONTMA,'L',JMACO)         
C
C --- NUMERO DES MAILLES
C
      DO 10 IMA = 1,NMA
        POSMA       = POSNMA(IMA)
        NUMNMA(IMA) = ZI(JMACO+POSMA-1)
   10 CONTINUE 
C
      CALL JEDEMA()
C
      END
