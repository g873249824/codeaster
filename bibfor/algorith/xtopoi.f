C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE GENIAUT S.GENIAUT
C
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*8   NOMA, MODELE
C
C ----------------------------------------------------------------------
C
C ROUTINE XFEM (METHODE XFEM - PREPARATION)
C
C AJOUTER À LA SD FISS_XFEM LES DONNÉES TOPOLOGIQUES CONCERNANT
C LA DÉCOUPE DES ÉLÉMENTS POUR L'INTÉGRATION
C
C ----------------------------------------------------------------------
C
C
C  IN  MODELE : NOM DE L'OBJET MODELE
C  I/O FISS   : NOM DE LA SD FISS_XFEM
C
C
C
C
C
      INTEGER      NBOUT,NBIN
      PARAMETER    (NBOUT=5, NBIN=4)
      CHARACTER*8  LPAOUT(NBOUT),LPAIN(NBIN),LICMP(2),K8BID
      CHARACTER*19 LCHOUT(NBOUT),LCHIN(NBIN)
C
      INTEGER      JNOMA
      CHARACTER*19 LIGREL,CHGEOM
      CHARACTER*19 PINTTO,CNSETO,HEAVTO,LONCHA,PMILTO
      LOGICAL      DEBUG
      INTEGER      IFM,NIV,IFMDBG,NIVDBG,JNBSP,IBID,IMA,NBMA,IRET
      INTEGER     JCESD,JCESV,JCESL,IAD
      CHARACTER*16 OPTION
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('XFEM',IFM,NIV)
      CALL INFDBG('PRE_CALCUL',IFMDBG,NIVDBG)
C
C --- INITIALISATIONS
C
      LIGREL = MODELE//'.MODELE'
      OPTION = 'TOPOSE'
      IF (NIVDBG.GE.2) THEN
        DEBUG  = .TRUE.
      ELSE
        DEBUG  = .FALSE.
      ENDIF
      CALL JEVEUO(MODELE//'.MODELE    .LGRF','L',JNOMA)
      CHGEOM = ZK8(JNOMA)//'.COORDO'
C
C --- INITIALISATION DES CHAMPS POUR CALCUL
C
      CALL INICAL(NBIN  ,LPAIN ,LCHIN ,
     &            NBOUT ,LPAOUT,LCHOUT)
C
C --- RECUPERATION DES DONNEES XFEM (TOPOSE)
C
      PINTTO = MODELE(1:8)//'.TOPOSE.PIN'
      CNSETO = MODELE(1:8)//'.TOPOSE.CNS'
      HEAVTO = MODELE(1:8)//'.TOPOSE.HEA'
      LONCHA = MODELE(1:8)//'.TOPOSE.LON'
      PMILTO = MODELE(1:8)//'.TOPOSE.PMI'
C
      LICMP(1) = 'NPG_DYN'
      LICMP(2) = 'NCMP_DYN'
      CALL CESCRE('V',HEAVTO,'ELEM',NOMA,'DCEL_I',2,LICMP,
     &              IBID,-1,-2)
      CALL JEVEUO(HEAVTO//'.CESD','L',JCESD)
      CALL JEVEUO(HEAVTO//'.CESV','E',JCESV)
      CALL JEVEUO(HEAVTO//'.CESL','E',JCESL)
C
C --- RECUPERATION DU NOMBRE DE FISSURES VUES
C
      CALL JEVEUO('&&XTYELE.NBSP','L',JNBSP)
C
C --- REMPLISSAGE DES SOUS POINT DE HEAVTO
C
      CALL DISMOI('F','NB_MA_MAILLA',NOMA,'MAILLAGE',NBMA,K8BID,IRET)
      DO 10 IMA=1,NBMA
        CALL CESEXI('S',JCESD,JCESL,IMA,1,1,1,IAD)
        ZL(JCESL-1-IAD) = .TRUE.
        ZI(JCESV-1-IAD) = ZI(JNBSP-1+IMA)
C
 10   CONTINUE
C
C --- CREATION DES LISTES DES CHAMPS IN
C
      LPAIN(1)  = 'PGEOMER'
      LCHIN(1)  = CHGEOM
      LPAIN(2)  = 'PLEVSET'
      LCHIN(2)  = MODELE(1:8)//'.LNNO'
      LPAIN(3)  = 'PGRADLN'
      LCHIN(3)  = MODELE(1:8)//'.GRLNNO'
      LPAIN(4)  = 'PFISCO'
      LCHIN(4)  = MODELE(1:8)//'.FISSCO'
C
C --- CREATION DES LISTES DES CHAMPS OUT
C
      LPAOUT(1) = 'PPINTTO'
      LCHOUT(1) = PINTTO
      LPAOUT(2) = 'PCNSETO'
      LCHOUT(2) = CNSETO
      LPAOUT(3) = 'PHEAVTO'
      LCHOUT(3) = HEAVTO
      LPAOUT(4) = 'PLONCHA'
      LCHOUT(4) = LONCHA
      LPAOUT(5) = 'PPMILTO'
      LCHOUT(5) = PMILTO
C
C --- APPEL A CALCUL
C
      CALL CALCUL('C',OPTION,LIGREL,NBIN ,LCHIN ,LPAIN ,
     &                              NBOUT,LCHOUT,LPAOUT,'G','OUI')
C
      IF (DEBUG) THEN
        CALL DBGCAL(OPTION,IFMDBG,
     &              NBIN  ,LPAIN ,LCHIN ,
     &              NBOUT ,LPAOUT,LCHOUT)
      ENDIF
C
      CALL JEDEMA()
      END
