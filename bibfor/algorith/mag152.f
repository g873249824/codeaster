C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR   
C (AT YOUR OPTION) ANY LATER VERSION.                                 
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT 
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF          
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU    
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                            
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C---------------------------------------------------------------------
C AUTEUR : G.ROUSSEAU
C CREATION DE LA MATRICE ASSEMBLEE GENERALISEE
C ET REMPLISSAGE DE SES OBJETS AUTRES QUE LE .VALE
C---------------------------------------------------------------------
C--------- DEBUT DES COMMUNS JEVEUX ----------------------------------
      CHARACTER*32     JEXNUM, JEXNOM, JEXR8, JEXATR
      INTEGER          ZI
      COMMON  /IVARJE/ ZI(1)
      REAL*8           ZR
      COMMON  /RVARJE/ ZR(1)
      COMPLEX*16       ZC
      COMMON  /CVARJE/ ZC(1)
      LOGICAL          ZL
      COMMON  /LVARJE/ ZL(1)
      CHARACTER*8      ZK8
      CHARACTER*16           ZK16
      CHARACTER*24                    ZK24
      CHARACTER*32                            ZK32
      CHARACTER*80                                    ZK80
      COMMON  /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C     --- FIN DES COMMUNS JEVEUX ------------------------------------
      LOGICAL       VRAI,EXIGEO
      INTEGER       LDBLO,IBID,NPREC,MATER,NBVALE,NBREFE,NBDESC
      INTEGER       NBMO,NBMODE,INDICE,IMODEG
      INTEGER       IAVALE,IADESC,IAREFE,I,J,IMODE,IABLO,IACONL
      INTEGER       IADIA,IADIRG,IALIME,IBLO,IBLODI,IERD,IERR
      INTEGER       IEXEC,IHCOL,ILIRES,IMADE,IMDG,IND,INEQU
      INTEGER       IPHI1,IPHI2,IPRSTO,IRANG,IREFE,IRET,ITXSTO,SOMME
      INTEGER       ITYSTO,ITZSTO,IVALK,JMDG,JND,JRANG,KCOMPT,LDABLO
      INTEGER       LDADIA,LDDESC,LDHCOL,LDIABL,LLDESC,MUN,N1BLOC,N2BLOC
      INTEGER       NBID,NBLOC,NBMODT,NSTOC,NTBLOC,NTERM,NUEQ,IMPR,NHMAX
      INTEGER       N1,N2,N3,N4,N5,N6,N7,N8,N9,N10,N11,N12,N13
      REAL*8        TPS(6),EPS,R8BID,MIJ,CIJ,KIJ,KIJ1,CIJ1,CIJ2
      REAL*8        BID,EBID
      CHARACTER*1   DIR
      CHARACTER*2   MODEL
      CHARACTER*3   INCR
      CHARACTER*8   NOMRES, K8BID,MODMEC,REP,PHIBAR,NUMMOD
      CHARACTER*8   MOFLUI,MOINT,MA,MATERI,NOMCMP(6)
      CHARACTER*8   CHAR,NUMGEN,MODGEN, NOMTMP,MATGEN,LPAIN(2),LPAOUT(1)
      CHARACTER*9   OPTION
      CHARACTER*14  NU,NUM,NUDDL,NUM14
      CHARACTER*16  TYPE(3),RK16
      CHARACTER*16  TYPRES,NOMCOM
      CHARACTER*19  CH19
      CHARACTER*19  MAX,MAY,MAZ,CHAMNO,PHIB19
      CHARACTER*19  CHAINE,NOMNUM,NOMSTO
      CHARACTER*24  CHGEOM,LCHIN(2)
      CHARACTER*24  NOMCHA,CH24,CARELE,TIME,NOCHAM
      CHARACTER*24  MATE,LIGRMO,PHIB24,MADE
      CHARACTER*72  K72B
      COMPLEX*16    C16B,CBID
C -----------------------------------------------------------------
C
C        CAS NUME_DDL_GENE  PRESENT
C
      CALL JEMARQ()
         IF ((N9.GT.0)) THEN

           CALL JEVEUO(NOMSTO//'.DESC','L',LLDESC)
           NUEQ = ZI(LLDESC)
           NTBLOC = ZI(LLDESC+1)
           NBLOC = ZI(LLDESC+2)
           NHMAX = ZI(LLDESC+3)


C TEST SUR LE MODE DE STOCKAGE : SI ON N EST PAS EN STOCKAGE
C LIGNE DE CIEL PLEIN ON PLANTE

           IF(NUEQ.NE.NHMAX) THEN
            CALL UTMESS('A','MAG152', 'LE PROFIL DE LA MATRICE '//
     +          'N EST SUREMENT PAS PLEIN. '//
     +          'ON  CONTINUE MAIS S''IL VOUS ARRIVE DES '//
     +           'PROBLEMES PLUS LOIN...')
           ENDIF

           IF((NUEQ*(NUEQ+1)/2).GT.(NBLOC*NTBLOC)) THEN
            CALL UTMESS('A','MAG152', 'LE PROFIL DE LA MATRICE '//
     +          'N EST SUREMENT PAS PLEIN. '//
     +          'ON  CONTINUE MAIS ATTENTION ....')
           ENDIF

C CALCUL DU NOMBRE DE TERME PAR BLOC ET TOTAL

        CALL JEVEUO(NOMSTO//'.ABLO','L',IABLO)
        CALL JEVEUO(NOMSTO//'.HCOL','L',IHCOL)

        SOMME=0

      DO 20 IBLO=1,NBLOC

C----------------------------------------------------------------
C
C         BOUCLE SUR LES COLONNES DE LA MATRICE ASSEMBLEE
C
          N1BLOC=ZI(IABLO+IBLO-1)+1
          N2BLOC=ZI(IABLO+IBLO)
C
C
          DO 10 I = N1BLOC,N2BLOC
              SOMME=SOMME+ZI(IHCOL+I-1)
10        CONTINUE
20      CONTINUE

           WRITE(6,*)'SOMME=',SOMME
           IF((NUEQ*(NUEQ+1)/2).NE.SOMME) THEN
            CALL UTMESS('F','MAG152', 'LE PROFIL DE LA MATRICE '//
     +          'N EST  PAS PLEIN. '//
     +          'ON ARRETE TOUT ....')
           ENDIF



          CALL JECREC(NOMRES//'           .VALE','G V R','NU',
     &                'DISPERSE','CONSTANT',NBLOC)
       CALL JEECRA(NOMRES//'           .VALE','LONMAX',NTBLOC,K8BID)
       CALL JEECRA(NOMRES//'           .VALE','DOCU',NBID,'MS')


        CALL WKVECT(NOMRES//'           .REFA','G V K24',4,IAREFE)
        CALL JEECRA(NOMRES//'           .REFA','DOCU',NBID,'SLCS')
        CALL WKVECT(NOMRES//'           .LIME','G V K8',1,IALIME)
        CALL WKVECT(NOMRES//'           .CONL','G V R',NUEQ,IACONL)
C
C       CAS DU CHAM_NO
C
        ELSE
C
           CALL JEVEUO(NOMSTO//'.DESC','L',LLDESC)
           NUEQ = ZI(LLDESC)
           NBLOC = 1
           NTBLOC = NUEQ*(NUEQ+1)/2

        CALL JECREC(NOMRES//'           .VALE','G V R','NU',
     &                'DISPERSE','CONSTANT',NBLOC)
       CALL JEECRA(NOMRES//'           .VALE','LONMAX',NTBLOC,K8BID)
       CALL JEECRA(NOMRES//'           .VALE','DOCU',NBID,'MS')
        CALL WKVECT(NOMRES//'           .REFA','G V K24',4,IAREFE)
        CALL JEECRA(NOMRES//'           .REFA','DOCU',NBID,'SLCS')
        CALL WKVECT(NOMRES//'           .LIME','G V K8',1,IALIME)
        CALL WKVECT(NOMRES//'           .CONL','G V R',NUEQ,IACONL)

        ENDIF

C ----------- REMPLISSAGE DU .REFA ET DU .LIME---------------
C---------------------ET DU .CONL ---------------------------


           IF (N10.GT.0) THEN
               ZK24(IAREFE) = ' '
           ELSEIF(INDICE.EQ.1) THEN
                CALL GETVID(' ','NUME_DDL_GENE',0,1,1,NUMMOD,NBID)
                NUM14 =NUMMOD
                CALL JEVEUO(NUM14//'.NUME.REFN','L',IMODEG)
                ZK24(IAREFE) = ZK24(IMODEG)
           ELSE
               ZK24(IAREFE) = MODMEC
           ENDIF

               ZK24(IAREFE+1)=NOMNUM
               ZK24(IAREFE+2)=NOMSTO

           IF (N10.GT.0) THEN
               ZK8(IALIME) = MODGEN
           ELSE
               ZK8(IALIME) = '  '
           ENDIF

       DO 3000 I = 1,NUEQ

          ZR(IACONL+I-1) = 1.0D0

3000   CONTINUE
      CALL JEDEMA()
       END
