C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT     NONE
      CHARACTER*24 RESOCO
      INTEGER      NEQ,NBLIAI,NBLIAC
      REAL*8       TOLE
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (RESOLUTION - GCP)
C
C ACTIVATION DES LIAISONS ET CALCUL DE LA FORCE DE CONTACT
C
C ----------------------------------------------------------------------
C 
C
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  NBLIAI : NOMBRE DE LIAISONS DE CONTACT
C IN  NEQ    : NOMBRE D'EQUATIONS
C IN  TOLE   : TOLERANCE POUR DETECTER PRESSION NULLE
C OUT NBLIAC : NOMBRE DE LIAISONS ACTIVES
C
C --------------- DEBUT DECLARATIONS NORMALISEES JEVEUX ---------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      IFM,NIV
      INTEGER      ILIAI,JDECAL,NBDDL
      CHARACTER*24 APCOEF,APDDL,APPOIN
      INTEGER      JAPCOE,JAPDDL,JAPPTR
      CHARACTER*19 MU ,ATMU,LIAC
      INTEGER      JMU,JATMU,JLIAC
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV)
C
C --- LECTURE DES STRUCTURES DE DONNEES DE CONTACT
C
      APPOIN = RESOCO(1:14)//'.APPOIN'
      APCOEF = RESOCO(1:14)//'.APCOEF'
      APDDL  = RESOCO(1:14)//'.APDDL'
      MU     = RESOCO(1:14)//'.MU'  
      ATMU   = RESOCO(1:14)//'.ATMU'
      LIAC   = RESOCO(1:14)//'.LIAC'  
C
      CALL JEVEUO(APPOIN,'L',JAPPTR)
      CALL JEVEUO(APCOEF,'L',JAPCOE)
      CALL JEVEUO(APDDL ,'L',JAPDDL)     
      CALL JEVEUO(MU    ,'L',JMU   ) 
      CALL JEVEUO(ATMU,  'E',JATMU )
      CALL JEVEUO(LIAC,  'E',JLIAC )
C
C --- CALCUL DE ATMU SUR TOUTES LES LIAISONS
C
      CALL R8INIR(NEQ,0.D0,ZR(JATMU),1)
      DO 120 ILIAI = 1,NBLIAI
        JDECAL = ZI(JAPPTR+ILIAI-1)
        NBDDL  = ZI(JAPPTR+ILIAI) - ZI(JAPPTR+ILIAI-1)
        CALL CALATM(NEQ,NBDDL,ZR(JMU+ILIAI-1),ZR(JAPCOE+JDECAL),
     &              ZI(JAPDDL+JDECAL),ZR(JATMU))
  120 CONTINUE
C
C --- COMPTE DES LIAISONS ACTIVES ET ACTIVATION
C
      NBLIAC = 0
      DO 130 ILIAI = 1,NBLIAI
        IF (ZR(JMU+ILIAI-1).GT.TOLE) THEN
          NBLIAC = NBLIAC + 1
          ZI(JLIAC+NBLIAC-1) = ILIAI
        END IF
  130 CONTINUE 
C
      CALL JEDEMA()
C
      END
