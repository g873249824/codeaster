C
C IN  MODELE : NOM DU MODELE
C IN  NUMEDD : NOM DE LA NUMEROTATION
C IN  LISCHA : LISTE DES CHARGES
C IN  VECLAG : VECTEUR D'INCONNUES PORTANT LES LAGRANGES
C OUT VEBUDI : VECT_ELEM DES CONDITIONS DE DIRICHLET B.U
