      INTEGER      IFM,NIV
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ() 
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> ... ASSEMBLAGE DES REACTIONS '//
     &                'D''APPUI'
      ENDIF                   
C
C --- CALCUL <CNBUDI>
C
      CALL VEBUME(MODELE,MATASS,VECLAG,LISCHA,VEBUDI) 
C
C --- ASSEMBLAGE <CNBUDI>
C
      CALL ASSVEC('V'   ,CNBUDI,1     ,VEBUDI,1.D0  ,
     &            NUMEDD,' '   ,'ZERO',1     )     
C
      IF (NIV.GE.2) THEN
        CALL NMDEBG('VECT',CNBUDI,6)
      ENDIF    
C
      CALL JEDEMA()
      END
