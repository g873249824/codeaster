C    CALCUL DE LA TAILLE ASSOCIE AU POINT DE GAUSS DANS LA DIRECTION VF3
      IMPLICIT NONE
      REAL*8 L3(3),T33(3,3),N33(3,3),VSE33(3,3)
      REAL*8 X(3),Y(3),SN,DN
      INTEGER I,J,K

      DO 230 K=1,3
       SN=0.D0
       DN=0.D0
       DO 240 I=1,3
        X(I)=0.D0
        Y(I)=0.D0
        DO 250 J=1,3
         X(I)=X(I)+T33(I,J)*VSE33(J,K)
         Y(I)=Y(I)+N33(I,J)*VSE33(J,K)
 250    CONTINUE
        SN=SN+X(I)*VSE33(I,K)
        DN=DN+Y(I)*VSE33(I,K)
 240   CONTINUE
       L3(K)=SN/DN
 230  CONTINUE
      END
