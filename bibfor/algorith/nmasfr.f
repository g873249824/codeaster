C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*24 RESOCO
      CHARACTER*19 MATASS
      CHARACTER*24 DEFICO
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - ALGORITHME)
C
C CREATION DE LA MATRICE DE CONTACT/FROTTEMENT
C      
C ----------------------------------------------------------------------
C     
C
C IN  DEFICO  : SD DE DEFINITION DU CONTACT
C IN  RESOCO  : SD CONTACT
C I/O MATASS  : IN  MATR_ASSE TANGENTE
C               OUT MATR_ASSE TANGENTE + MATRICE CONTACT/FROTTEMENT
C                  (EVENTUELLE)
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      CHARACTER*14 NUMEDF
      CHARACTER*24 NOSDCO
      INTEGER      JNOSDC
      INTEGER      CFDISD
      INTEGER      IBID,IER,NBLIAC
      CHARACTER*19 MATRCF
      CHARACTER*24 LIMAT(2)
      REAL*8       COEFMU(2)
      CHARACTER*1  TYPCST(2)
      LOGICAL      CFDISL,LMODIM
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV)    
C
C --- INITIALISATIONS
C 
      LMODIM = CFDISL(DEFICO,'MODI_MATR_GLOB')
      NBLIAC = CFDISD(RESOCO,'NBLIAC')
      IF (NBLIAC.EQ.0) THEN
        GOTO 9999
      ENDIF
      IF (.NOT.LMODIM) THEN
        GOTO 9999
      ENDIF
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> AJOUT MATRICE CONTACT/FROTTEMENT' 
      ENDIF      
C
      MATRCF    = RESOCO(1:14)//'.MATR'
      NOSDCO    = RESOCO(1:14)//'.NOSDCO'
      CALL JEVEUO(NOSDCO,'L',JNOSDC)    
      LIMAT(1)  = MATASS
      LIMAT(2)  = MATRCF
      COEFMU(1) = 1.D0
      COEFMU(2) = 1.D0
      TYPCST(1) = 'R'
      TYPCST(2) = 'R'
      NUMEDF    = ZK24(JNOSDC+1-1)(1:14)
      CALL DETRSD('NUME_DDL',NUMEDF)
C
      CALL INFMUE()
      CALL MTCMBL(2,TYPCST,COEFMU,LIMAT,MATASS,' ',NUMEDF,'ELIM1')
      CALL INFBAV()
      CALL DISMOI('F','NOM_NUME_DDL',MATRCF,'MATR_ASSE',
     &            IBID,NUMEDF,IER)
      CALL DETRSD('MATR_ASSE',MATRCF)
      CALL DETRSD('NUME_DDL' ,NUMEDF)

 9999 CONTINUE
C 
      CALL JEDEMA()

      END
