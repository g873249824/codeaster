      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER      NDIM,NNE,NNM
      REAL*8       FFE(9),FFM(9)
      REAL*8       GEOMM(3),GEOME(3)
      REAL*8       GEOMAE(9,3),GEOMAM(9,3)
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE)
C
C CALCUL DES INCREMENTS - GEOMETRIE
C
C ----------------------------------------------------------------------
C
C
C DEPDEL - INCREMENT DE DEPLACEMENT DEPUIS DEBUT DU PAS DE TEMPS
C DEPMOI - DEPLACEMENT DEBUT DU PAS DE TEMPS
C GEOMAx - GEOMETRIE ACTUALISEE GEOM_INIT + DEPMOI 
C
C
C IN  NDIM   : DIMENSION DU PROBLEME
C IN  NNE    : NOMBRE DE NOEUDS DE LA MAILLE ESCLAVE
C IN  NNM    : NOMBRE DE NOEUDS DE LA MAILLE MAITRE
C IN  FFE    : FONCTIONS DE FORMES DEPL. ESCL.
C IN  FFM    : FONCTIONS DE FORMES DEPL. MAIT.
C IN  GEOMAE : GEOMETRIE ACTUALISEE SUR NOEUDS ESCLAVES
C IN  GEOMAM : GEOMETRIE ACTUALISEE SUR NOEUDS MAITRES
C OUT GEOME  : COORDONNEES ACTUALISEES DU POINT DE CONTACT
C OUT GEOMM  : COORDONNEES ACTUALISEES DU PROJETE DU POINT DE CONTACT
C
C
C
C
      INTEGER   IDIM,INOE
C
C ----------------------------------------------------------------------
C
      DO 9 IDIM = 1,3
        GEOME(IDIM)  = 0.D0
        GEOMM(IDIM)  = 0.D0
 9    CONTINUE
C
C --- COORDONNEES ACTUALISEES DU POINT DE CONTACT
C 
      DO 11 IDIM = 1,NDIM
        DO 12 INOE = 1,NNE
          GEOME(IDIM) = GEOME(IDIM) + FFE(INOE)*GEOMAE(INOE,IDIM)
12      CONTINUE
11    CONTINUE
C
C --- COORDONNEES ACTUALISEES DE LA PROJECTION DU POINT DE CONTACT
C 
      DO 21 IDIM = 1,NDIM
        DO 22 INOE = 1,NNM
          GEOMM(IDIM) = GEOMM(IDIM) + FFM(INOE)*GEOMAM(INOE,IDIM)
22      CONTINUE
21    CONTINUE
C
      END
