      SUBROUTINE MMGEOM(IRESOG,NDIM  ,NNE   ,NNM   ,FFE   ,
     &                  FFM   ,DDFFM ,GEOMAE,GEOMAM,TAU1  ,
     &                  TAU2  ,NORM  ,MPROJN,MPROJT,GEOME ,
     &                  GEOMM ,MPRT1N,MPRT2N,GENE11,GENE21)
