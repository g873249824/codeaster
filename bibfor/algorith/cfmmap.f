C ROUTINE CONTACT (METHODES MAILLEES - SD APPARIEMENT)
C
C CREATION DE LA SD POUR L'APPARIEMENT
C      
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C
C
C
C
      INTEGER      IFM,NIV
      INTEGER      CFDISI,NZOCO,NTPT,NDIMG,NTMANO,NTMA,NNOCO
      INTEGER      NBNO,IER
      CHARACTER*8  K8BID
      CHARACTER*19 SDAPPA,NEWGEO
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV)
C
C --- INITIALISATIONS
C
      NZOCO  = CFDISI(DEFICO,'NZOCO' )
      NNOCO  = CFDISI(DEFICO,'NNOCO' )      
      NTPT   = CFDISI(DEFICO,'NTPT'  ) 
      NDIMG  = CFDISI(DEFICO,'NDIM'  )
      NTMA   = CFDISI(DEFICO,'NMACO' )
      NTMANO = CFDISI(DEFICO,'NTMANO')
      CALL DISMOI('F','NB_NO_MAILLA',NOMA,'MAILLAGE',NBNO,K8BID,IER)
C
C --- NOM DES SD
C
      SDAPPA = RESOCO(1:14)//'.APPA'
      NEWGEO = RESOCO(1:14)//'.NEWG'
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> ...... CREATION SD APPARIEMENT' 
      ENDIF
C
C --- CREATION DE LA SD APPARIEMENT
C
      CALL APCRSD(SDAPPA,NZOCO ,NTPT  ,NTMA  ,NNOCO ,
     &            NTMANO,NBNO  ) 
C
C --- REMPLISSAGE DE LA SD APPARIEMENT
C
      CALL CFMMAR(NOMA  ,DEFICO,NEWGEO,SDAPPA,NZOCO ,
     &            NTPT  ,NDIMG ,NTMA  ,NNOCO ,NTMANO)
C      
      CALL JEDEMA()     
C
      END
