     &                  FFC   ,FFP   ,JAC   ,
     &                  NFH   ,ND    ,CPENCO,
     &                  SINGU ,RR    ,DDLS  ,DDLM  ,
     &                  JFISNO,NFISS ,IFISS ,JHEAFA,NCOMPH,IFA,
     &                  MMAT )

      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER     NDIM,NNO,NNOS,NNOL
      INTEGER     NFH,DDLS,DDLM
      INTEGER     SINGU,PLA(27),JFISNO,NFISS,IFISS,JHEAFA,NCOMPH,IFA
      REAL*8      MMAT(216,216),ND(3)
      REAL*8      FFC(8),FFP(27),JAC
      REAL*8      CPENCO,RR

