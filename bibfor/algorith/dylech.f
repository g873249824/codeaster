C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
      IMPLICIT      NONE
      CHARACTER*8   NOMO
      CHARACTER*19  LISCHA
      CHARACTER*24  EXRECO,EXRESU
      INTEGER       NBEXRE
C
C ----------------------------------------------------------------------
C
C DYNA_LINE_HARM
C
C LECTURE DES CHARGEMENTS
C
C ----------------------------------------------------------------------
C
C
C IN  NOMO   : NOM DU MODELE
C OUT LISCHA : SD LISTE DES CHARGES
C OUT NBEXRE : NOMBRE DE EXCIT_RESU
C OUT EXRECO : LISTE DES COEFFICIENTS DANS EXCIT_RESU
C OUT EXRESU : LISTE DES RESULTATS DANS EXCIT_RESU
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*16 MOTFAC,NOMCMD
      INTEGER      IRESU,JLCCRE,JLRESU,IARG,N
      INTEGER      LISNBG,NBVEAS,NBVEAG,NBVEAT,NBCHAR
      INTEGER      IFM,NIV
C
C ----------------------------------------------------------------------
C
      MOTFAC = 'EXCIT'
      NOMCMD = 'DYNA_LINE_HARM'
      NBEXRE = 0
      CALL INFNIV(IFM,NIV)
C
C --- LECTURE DONNNES CHARGEMENTS
C
      CALL LISLEC(MOTFAC,'MECANIQUE','V',LISCHA)
C
C --- AFFICHAGE DE LA LISTE DES CHARGES
C
      IF (NIV.GE.2) CALL LISIMP(LISCHA,IFM)
C
C --- VERIFICATIONS DE LA LISTE DES CHARGES
C
      CALL LISCHK(NOMO  ,'MECANIQUE',NOMCMD,LISCHA)
C
C --- LECTURE INFORMATIONS EXCIT_RESU
C
      CALL GETFAC('EXCIT_RESU',NBEXRE)
      EXRECO = '&&DYLECH.COEF_CRE'
      EXRESU = '&&DYLECH.LISTRESU'
      IF ( NBEXRE.NE.0 ) THEN
        CALL WKVECT(EXRECO,'V V C  ',NBEXRE,JLCCRE)
        CALL WKVECT(EXRESU,'V V K8 ',NBEXRE,JLRESU)
        DO 252 IRESU = 1, NBEXRE
          CALL GETVID('EXCIT_RESU','RESULTAT',IRESU,IARG,1,
     &                ZK8(JLRESU+IRESU-1),N)
          CALL GETVC8('EXCIT_RESU','COEF_MULT_C',IRESU,IARG,1,
     &                ZC (JLCCRE+IRESU-1),N)
 252    CONTINUE
      ENDIF
C
C --- EXCLUSION VECT_ASSE/CHARGE
C
      NBVEAS = LISNBG(LISCHA,'VECT_ASSE'     )
      NBVEAG = LISNBG(LISCHA,'VECT_ASSE_GENE')
      NBVEAT = NBVEAS+NBVEAG
      CALL LISNNB(LISCHA,NBCHAR)
      IF (NBVEAT.NE.0) THEN
        IF (NBVEAT.NE.NBCHAR) THEN
          CALL U2MESS('F','CHARGES5_1')
        ENDIF
      ENDIF
C
      END
