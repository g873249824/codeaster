C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      INTEGER       FONACT(*)
      CHARACTER*19  COLACT
      LOGICAL       LINFRE,LUNITE,LINFTP
      INTEGER       ZDEF,NCOL
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (AFFICHAGE)
C
C VALEURS PAR DEFAUT DE L'AFFICHAGE DU TABLEAU DE CONVERGENCE
C
C ----------------------------------------------------------------------
C
C
C IN  LINFRE : .TRUE. SI INFO_RESIDU
C IN  LUNITE : .TRUE. SI SORTIE FICHIER
C IN  LINFTP : .TRUE. SI INFO_TEMPS
C IN  ZDEF   : NOMBRE MAXI DE COLONNES DISPONIBLES POUR L'AFFICHAGE
C IN  FONACT : FONCTIONNALITES ACTIVEES
C IN  COLACT : VECTEUR D'ACTIVATION DES COLONNES
C OUT NCOL   : NOMBRE DE COLONNES ACTIVES DANS COLACT
C
C COLACT CONTIENT LA LISTE DES CODES D'AFFICHAGES
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      LOGICAL      ISFONC,LRELI,LPILO,LCTCD,LCTCC
      LOGICAL      LBORST,LREFE,LFETI,LCOMP
      LOGICAL      LBOUCC,LBOUCF,LBOUCG,LALLV,LNEWTF,LNEWTC
      INTEGER      JIMCOL
      INTEGER      ICOL
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      ICOL   = 1
      NCOL   = 0
C
C --- FONCTIONNALITES ACTIVEES
C
      LRELI  = ISFONC(FONACT,'RECH_LINE')
      LPILO  = ISFONC(FONACT,'PILOTAGE')
      LCTCD  = ISFONC(FONACT,'CONT_DISCRET')
      LCTCC  = ISFONC(FONACT,'CONT_CONTINU')
      LREFE  = ISFONC(FONACT,'RESI_REFE')
      LBORST = ISFONC(FONACT,'DEBORST')
      LFETI  = ISFONC(FONACT,'FETI')
      LBOUCF = ISFONC(FONACT,'BOUCLE_EXT_FROT')
      LBOUCG = ISFONC(FONACT,'BOUCLE_EXT_GEOM')
      LBOUCC = ISFONC(FONACT,'BOUCLE_EXT_CONT')
      LNEWTF = ISFONC(FONACT,'FROT_NEWTON')
      LNEWTC = ISFONC(FONACT,'CONT_NEWTON')
      LCOMP  = ISFONC(FONACT,'RESI_COMP')
      LALLV  = ISFONC(FONACT,'CONT_ALL_VERIF')
C
C --- ACCES
C
      CALL JEVEUO(COLACT,'E',JIMCOL)
C
C --- PAS DE TEMPS
C
      IF (LUNITE) THEN
        CALL IMPDEA(ZDEF,'INCR_INST',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- CONTACT - BOUCLE GEOMETRIQUE
C
      IF (LBOUCG) THEN
        CALL IMPDEA(ZDEF,'BOUC_GEOM',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- CONTACT - BOUCLE FROTTEMENT
C
      IF (LBOUCF) THEN
        CALL IMPDEA(ZDEF,'BOUC_FROT',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- CONTACT - BOUCLE CONTACT
C
      IF (LBOUCC) THEN
        CALL IMPDEA(ZDEF,'BOUC_CONT',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- ITERATIONS DE NEWTON
C
      CALL IMPDEA(ZDEF,'ITER_NUME',ZI(JIMCOL),ICOL)
C
C --- RESI_GLOB_RELA
C
      CALL IMPDEA(ZDEF,'RESI_RELA',ZI(JIMCOL),ICOL)
      IF (LINFRE) THEN
        CALL IMPDEA(ZDEF,'RELA_NOEU',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- RESI_GLOB_MAXI
C
      CALL IMPDEA(ZDEF,'RESI_MAXI',ZI(JIMCOL),ICOL)
      IF (LINFRE) THEN
        CALL IMPDEA(ZDEF,'MAXI_NOEU',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- SEUIL DE COULOMB (NEWTON GENERALISE)
C
      IF (LNEWTF) THEN
        CALL IMPDEA(ZDEF,'FROT_NEWT',ZI(JIMCOL),ICOL)
        IF (LINFRE) THEN
          CALL IMPDEA(ZDEF,'FROT_NOEU',ZI(JIMCOL),ICOL)
        ENDIF
      ENDIF
C
C --- STATUTS DE CONTACT (NEWTON GENERALISE)
C
      IF (LNEWTC) THEN
        CALL IMPDEA(ZDEF,'CONT_NEWT',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- RESIDU PAR REFERENCE
C
      IF (LREFE) THEN
        CALL IMPDEA(ZDEF,'RESI_REFE',ZI(JIMCOL),ICOL)
        IF (LINFRE) THEN
          CALL IMPDEA(ZDEF,'REFE_NOEU',ZI(JIMCOL),ICOL)
        ENDIF
      ENDIF
C
C --- RESIDU PAR FORCES NODALES CMP (THM)
C
      IF (LCOMP) THEN
        CALL IMPDEA(ZDEF,'RESI_COMP',ZI(JIMCOL),ICOL)
        IF (LINFRE) THEN
          CALL IMPDEA(ZDEF,'COMP_NOEU',ZI(JIMCOL),ICOL)
        ENDIF
      ENDIF
C
C --- BOUCLES CONTACT
C
      IF (LBOUCG.OR.LBOUCF.OR.LBOUCC) THEN
        CALL IMPDEA(ZDEF,'BOUC_VALE',ZI(JIMCOL),ICOL)
        IF (LINFRE) THEN
          CALL IMPDEA(ZDEF,'BOUC_NOEU',ZI(JIMCOL),ICOL)
        ENDIF
      ENDIF
C
C --- METHODE DE BORST
C
      IF (LBORST) THEN
        CALL IMPDEA(ZDEF,'DEBORST  ',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- RECHERCHE LINEAIRE
C
      IF (LRELI) THEN
        CALL IMPDEA(ZDEF,'RELI_NBIT',ZI(JIMCOL),ICOL)
        CALL IMPDEA(ZDEF,'RELI_COEF',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- PILOTAGE
C
      IF (LPILO) THEN
        CALL IMPDEA(ZDEF,'PILO_COEF',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- OPTION D'ASSEMBLAGE
C
      CALL IMPDEA(ZDEF,'MATR_ASSE',ZI(JIMCOL),ICOL)
C
C --- CONTACT DISCRET - COLONNES SUP.
C
      IF (LCTCD.AND.(.NOT.LALLV)) THEN
        CALL IMPDEA(ZDEF,'CTCD_NBIT',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- CONTACT CONTINUE - COLONNES SUP.
C
      IF (LCTCC.AND.(.NOT.LALLV)) THEN
        CALL IMPDEA(ZDEF,'CTCC_INFO',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- FETI
C
      IF (LFETI) THEN
        CALL IMPDEA(ZDEF,'FETI_NBIT',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- TEMPS PASSE DANS L'ITERATION
C
      IF (LINFTP) THEN
        CALL IMPDEA(ZDEF,'ITER_TIME',ZI(JIMCOL),ICOL)
      ENDIF
C
C --- NOMBRE DE COLONNES ACTIVES
C
      NCOL  = ICOL - 1
C
      CALL JEDEMA()

      END
