      IMPLICIT     NONE
      INCLUDE 'jeveux.h'
      CHARACTER*19 DEPMOI,DEPDEL,DEPPLU
C
C ----------------------------------------------------------------------
C
C ROUTINE CALCUL (INITIALISATION)
C
C PREPARATION DEPLACEMENTS AVEC TRANSFERT DE NUMEROTATION
C
C ----------------------------------------------------------------------
C
C IN  DEPMOI : DEPLACEMENTS EN T-
C IN  DEPDEL : INCREMENT DE DEPLACEMENT
C OUT DEPPLU : DEPLACEMENTS EN T+
C
C ----------------------------------------------------------------------
C
      INTEGER         IBID,IRET
      CHARACTER*19    PFCHN1,PFCHN2
      CHARACTER*1     TYPCST(2),TYPECH(2),TYPRES
      REAL*8          CONST(2)
      CHARACTER*24    NOMCH(2),CHPRES,DEPMO1
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      DEPMO1 = '&&MVNUME.DEPMO1'
C
C --- SI LES CHAMPS N'ONT PAS LA MEME NUMEROTATION, ON TRANSFERT DEPMOI
C --- DANS LA NUMEROTATION DE DEPDEL
C
      CALL DISMOI('F','PROF_CHNO',DEPMOI,'CHAM_NO',IBID,PFCHN1,IBID)
      CALL DISMOI('F','PROF_CHNO',DEPDEL,'CHAM_NO',IBID,PFCHN2,IBID)
      IF (PFCHN1.NE.PFCHN2) THEN
        CALL COPISD('CHAMP_GD','V',DEPDEL,DEPMO1)
        CALL VTZERO(DEPMO1)
        CALL VTCOPY(DEPMOI,DEPMO1,'F',IRET)
      ELSE
        DEPMO1 = DEPMOI
      ENDIF
C
C --- ON CALCULE LE CHAMP DEPPLU=DEPMO1+DEPDEL
C
      TYPCST(1) = 'R'
      TYPCST(2) = 'R'
      CONST(1)  = 1.D0
      CONST(2)  = 1.D0
      TYPECH(1) = 'R'
      TYPECH(2) = 'R'
      NOMCH(1)  = DEPMO1
      NOMCH(2)  = DEPDEL
      TYPRES    = 'R'
      CHPRES    = DEPPLU
      CALL VTCMBL(2,TYPCST,CONST,TYPECH,NOMCH,TYPRES,CHPRES)
C
      CALL JEDEMA()
      END
