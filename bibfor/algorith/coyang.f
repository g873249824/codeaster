      REAL*8 DIST,DTETA,RAYON,OMEGA,UC,UCT,L,LT

C
C VARIABLES LOCALES
C -----------------
      REAL*8 COYANG,CODIST,COTETA

C CALCUL DE LA FONCTION DE COHERENCE EN X

      CODIST=EXP(-DIST/L)*COS(OMEGA*DIST/UC)

C CALCUL DE LA FONCTION DE COHERENCE EN TETA

      COTETA=EXP(-RAYON*DTETA/LT)*COS(RAYON*OMEGA*DTETA/UCT)*RAYON*RAYON

C CALCUL DE LA FONCTION DE COHERENCE SUIVANT AU-YANG

      COYANG = CODIST * COTETA
