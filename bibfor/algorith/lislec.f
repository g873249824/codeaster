C ======================================================================
C
      IMPLICIT      NONE
      CHARACTER*(*) PHENOZ
      CHARACTER*16  MOTFAC
      CHARACTER*19  LISCHA
      CHARACTER*1   BASE
C
C ----------------------------------------------------------------------
C
C ROUTINE UTILITAIRE (LISTE_CHARGES)
C
C LECTURE DES CHARGEMENTS
C
C ----------------------------------------------------------------------
C
C
C IN  PHENOM : TYPE DE PHENOMENE (MECANIQUE, THERMIQUE, ACOUSTIQUE)
C IN  MOTFAC : MOT-CLEF FACTEUR DES EXCITATIONS
C IN  BASE   : BASE DE CREATION DE LA SD LISCHA
C OUT LISCHA : SD LISTE DES CHARGES
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      IEXCI,NBEXCI,IBID
      CHARACTER*8  CHARGE
      CHARACTER*16 TYPAPP,TYPFCT
      INTEGER      CODCHA
      CHARACTER*8  TYPECH,NOMFCT
      CHARACTER*13 PREFOB
      REAL*8       PHASE
      INTEGER      NPUIS
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- NOMBRE D'EXCITATIONS
C
      CALL GETFAC(MOTFAC,NBEXCI)
C
C --- CREATION SD CHARGEMENT
C
      CALL LISCRS(LISCHA,NBEXCI,BASE  )
C
C --- LECTURE OCCURRENCES EXCIT
C
      DO 100 IEXCI = 1,NBEXCI
C
C ----- LECTURE NOM DE LA CHARGE (PROVENANT DE AFFE_CHAR_*)
C
        CALL LISNNN(MOTFAC,IEXCI ,CHARGE)
C
C ----- PREFIXE DE L'OBJET DE LA CHARGE
C
        CALL LISNNL(PHENOZ,CHARGE,PREFOB)
C
C ----- GENRES DE LA CHARGE
C
        CALL LISGEN(PREFOB,CODCHA)
C
C ----- TYPE DE LA CHARGE (COMPLEXE, FONCTION, REELLE)
C
        CALL LISDEF('TYPC',PREFOB,CODCHA,TYPECH,IBID  )
C
C ----- TYPE D'APPLICATION DE LA CHARGE
C
        CALL LISTAP(MOTFAC,IEXCI ,TYPAPP)
C
C ----- RECUPERATION FONCTION MULTIPLICATRICE
C
        CALL LISLEF(MOTFAC,IEXCI ,NOMFCT,TYPFCT,PHASE ,
     &              NPUIS )
C
C ----- SAUVEGARDE DES INFORMATIONS
C
        CALL LISSAV(LISCHA,IEXCI ,CHARGE,TYPECH,CODCHA,
     &              PREFOB,TYPAPP,NOMFCT,TYPFCT,PHASE ,
     &              NPUIS )
C
 100  CONTINUE
C
      CALL JEDEMA()
      END
