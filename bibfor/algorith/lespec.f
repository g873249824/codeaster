C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C     ------------------------------------------------------------------
C IN  IFSIG      : I : NUMERO LOGIQUE DU FICHIER DE LECTURE
C IN  NBFREQ     : I : NOMBRE DE FREQUENCE
C IN  BMP        : B : .TRUE. SI LES VALEURS A LIRE SONT SOUS LA FORME
C                      MODULE/PHASE
C OUT SPECTR     : R : VALEUR DU SPECTRE
C
      REAL*8  M,P
      INTEGER I,J
C      
      DO 2 I=1,NBFREQ
         READ(IFSIG,*)(SPECTR(J+(3*(I-1))),J=1,3)
    2 CONTINUE
C
C     SI LES VALEURS SONT EN MODULE/PHASE, IL FAUT CONVERTIR EN
C     REEL/IMAGINAIRE
      IF (BMP) THEN
         DO 10 I=1,NBFREQ
            J=3*(I-1)
            M=SPECTR(J+2)
            P=SPECTR(J+3)
            SPECTR(J+2)=M*COS(P)
            SPECTR(J+3)=M*SIN(P)
   10    CONTINUE
      ENDIF
C      
      END
