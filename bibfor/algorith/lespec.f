      SUBROUTINE LESPEC(IFSIG,NBFREQ,BMP,SPECTR)
      IMPLICIT NONE
      INTEGER  IFSIG,NBFREQ
      LOGICAL  BMP
      REAL*8   SPECTR(3*NBFREQ)
C     ------------------------------------------------------------------
C     LECTURE DES VALEURS D'UN INTERSPECTRE
C            CONFIGURATION MANAGEMENT OF EDF VERSION
