      IMPLICIT NONE
      CHARACTER*8  ALIAS      
      INTEGER      NDIM
      INTEGER      NNO
      REAL*8       COORMA(27)
      REAL*8       KSI1,KSI2
      REAL*8       TAU1(3),TAU2(3)
      REAL*8       FF(9)
      LOGICAL      LDIST      
      REAL*8       TOLEOU
      INTEGER      NIVERR
      CHARACTER*8  FFORME      
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT)
C
C RAMENE POINT PROJETE HORS DE LA MAILLE
C      
C ----------------------------------------------------------------------
C
C
C IN  ALIAS  : TYPE DE MAILLE
C IN  NNO    : NOMBRE DE NOEUD SUR LA MAILLE
C IN  NDIM   : DIMENSION DE LA MAILLE (2 OU 3)
C IN  COORMA : COORDONNEES DES NOEUDS DE LA MAILLE
C IN  TOLEOU : TOLERANCE POUR LE PROJETE HORS SURFACE MAITRE
C IN  FFORME : TYPE DES FONCTIONS DE FORME
C               'CONTINUE' POUR ELTS DE CONTACT
C               'STANDARD' POUR ELTS STANDARDS
C I/O KSI1   : PREMIERE COORDONNEE PARAMETRIQUE DU POINT PROJETE
C I/O KSI2   : SECONDE COORDONNEE PARAMETRIQUE DU POINT PROJETEKSI1,KSI2
C OUT FF     : FONCTIONS DE FORME EN (KSI1,KSI2)
C OUT TAU1   : PREMIER VECTEUR TANGENT EN (KSI1,KSI2)
C OUT TAU2   : SECOND VECTEUR TANGENT EN (KSI1,KSI2)
C OUT LDIST  : VAUT .FALSE. SI LA PROJECTION EST EN DEHORS
C               DE LA MAILLE
C OUT NIVERR : RETOURNE UN CODE ERREUR
C                0  TOUT VA BIEN
C                1  ELEMENT INCONNU
C                2  MATRICE SINGULIERE (VECTEURS TANGENTS COLINEAIRES)
C                3  DEPASSEMENT NOMBRE ITERATIONS MAX
C                4  VECTEURS TANGENTS NULS
C      
C ----------------------------------------------------------------------
C
      REAL*8       DFF(2,9),DDFF(3,9)
C
C ----------------------------------------------------------------------
C
C
C --- AJUSTEMENT PROJECTION HORS ZONE
C
      CALL MAJUST(ALIAS,KSI1,KSI2,TOLEOU,LDIST)     
C       
C --- CALCUL DES FONCTIONS DE FORME ET DE LEUR DERIVEES
C
      CALL MMFONF(FFORME,ALIAS ,KSI1   ,KSI2  ,
     &            FF    ,DFF   ,DDFF   ,NIVERR)        
C      
C --- RE-CALCUL DES TANGENTES APRES AJUSTEMENT
C
      CALL MMTANG(NDIM  ,NNO   ,COORMA,DFF   ,
     &            TAU1  ,TAU2)
C
      END
