      SUBROUTINE NMCALM(TYPMAT,MODELZ,LISCHA,MATE  ,CARELE,
     &                  COMPOR,INSTAM,INSTAP,CARCRI,VALMOI,
     &                  VALPLU,SOLALG,OPTMAZ,BASE  ,MEELEM,
     &                  RESOCO,MATELE)
