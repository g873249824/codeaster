C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C     RECUPERATION DES CARACTERISTIQUES DE GRANDISSEMENT
      IMPLICIT NONE
      INTEGER        ICDMAT,KPG,KSP,NBPAR,CODRET
      REAL*8         IRRAM,IRRAP,TM,TP,VALRES
      REAL*8         DEPSGM,DEPSGP,DEPSGR,VALPAR(2)
      CHARACTER*8    MATERI,NOMGRD,NOMPAR(2)
      CHARACTER*16   COMPO
      CHARACTER*(*)  FAMI
      DATA           NOMGRD /'GRAN_FO'/

      DEPSGR = 0.0D0
      IF (COMPO(1:13).EQ.'GRAN_IRRA_LOG'.OR.
     +    COMPO(1:13).EQ.'LEMAITRE_IRRA') THEN
         NBPAR=2
         NOMPAR(1)='TEMP'
         NOMPAR(2)='IRRA'

C        INSTANT -
         VALPAR(1)=TM
         VALPAR(2)=IRRAM
         IF (IRRAM.GT.0.0D0) THEN
            CALL RCVALB(FAMI,KPG,KSP,'+',ICDMAT,MATERI,COMPO,NBPAR,
     &                          NOMPAR,VALPAR,1,NOMGRD,VALRES,CODRET,0)
            DEPSGM = VALRES
         ELSE
            DEPSGM = 0.0D0
         ENDIF

C        INSTANT +
         VALPAR(1)=TP
         VALPAR(2)=IRRAP
         IF (IRRAP.GT.0.0D0) THEN
            CALL RCVALB(FAMI,KPG,KSP,'+',ICDMAT,MATERI,COMPO,NBPAR,
     &                         NOMPAR,VALPAR,1,NOMGRD,VALRES,CODRET,0)
            DEPSGP = VALRES
         ELSE
            DEPSGP = 0.0D0
         ENDIF

         DEPSGR = (DEPSGP - DEPSGM)/100.0D0
         IF (DEPSGR.LT.0.0D0) THEN
            DEPSGR = 0.0D0
         ENDIF
      ENDIF
      END
