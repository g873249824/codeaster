C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      INTEGER      IFM
      CHARACTER*8  NOMA
      CHARACTER*24 DEFICO
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE - IMPRESSIONS)
C
C AFFICHAGE APPARIEMENT
C      
C ----------------------------------------------------------------------
C
C
C IN  IFM    : UNITE D'IMPRESSION DU MESSAGE
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  NOMA   : NOM DU MAILLAGE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*32 JEXNUM
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      CFMMVD,ZTABF,ZMAES
      INTEGER      NUMMAE,NUMMAM,IZONE
      CHARACTER*8  NOMESC,NOMMAI 
      REAL*8       KSIPC1,KSIPC2,KSIPR1,KSIPR2,WPC,R8BID
      REAL*8       TAU1(3),TAU2(3),NORM(3)
      CHARACTER*24 MAESCL,TABFIN,NDIMCO
      INTEGER      JMAESC,JTABF,JDIM
      INTEGER      NTPC,NBPC,IPC,NDIMG  
      INTEGER      NTMAE,IMAE            
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()   
      WRITE (IFM,*) '<CONTACT> ... RESULTAT DE L''APPARIEMENT'      
C      
C --- RECUPERATION DE QUELQUES DONNEES      
C
      MAESCL = DEFICO(1:16)//'.MAESCL'
      TABFIN = DEFICO(1:16)//'.TABFIN'
      NDIMCO = DEFICO(1:16)//'.NDIMCO'
      CALL JEVEUO(MAESCL,'L',JMAESC)
      CALL JEVEUO(TABFIN,'L',JTABF)
      CALL JEVEUO(NDIMCO,'L',JDIM)
      ZTABF = CFMMVD('ZTABF')     
      ZMAES = CFMMVD('ZMAES')              
C
C --- INITIALISATIONS
C
      NTMAE  = ZI(JMAESC)
      NTPC   = 0 
      NDIMG  = ZI(JDIM)
C    
C --- BOUCLE SUR LES MAILLES ESCLAVES
C
      DO 20 IMAE = 1,NTMAE
        IZONE  = ZI(JMAESC+ZMAES*(IMAE-1)+2)
        NBPC   = ZI(JMAESC+ZMAES*(IMAE-1)+3)
        IPC    = 1
C
C --- REPERAGE MAILLE ESCLAVE
C
        NUMMAE = NINT(ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+1))
        CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NUMMAE),NOMESC)
C
C --- REPERAGE MAILLE MAITRE
C
        NUMMAM = NINT(ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+2))
        CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NUMMAM),NOMMAI)  
C
C --- INFOS SUR MAILLE ESCLAVE
C        
        WRITE (IFM,1000) NOMESC,IZONE,NBPC       
 1000   FORMAT (' <CONTACT>     * MAILLE ESCLAVE ',A8,' ( ZONE ',
     &           I5,') AVEC ',I5,' POINTS DE CONTACT')  
C  
C --- BOUCLE SUR LES POINTS DE CONTACT
C              
        DO 10 IPC = 1,NBPC      
          WRITE(IFM,2000) IPC
C
C --- POINT DE CONTACT EN COURS
C
          KSIPC1 = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+3)       
          KSIPC2 = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+12)      
          WPC    = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+16) 
          WRITE(IFM,3000) KSIPC1,KSIPC2,WPC           
 2000 FORMAT (' <CONTACT>     ** POINT DE CONTACT ',I3)
 3000 FORMAT (' <CONTACT>        SITUE EN  : <',
     &         E10.3,',',E10.3,'> - POIDS INTEGRATION: ',E10.3)
C
C --- ETAT DU NOEUD
C
          KSIPR1 = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+4)
          KSIPR2 = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+5)
          IF (ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+22).EQ.1.D0) THEN
            WRITE(IFM,*) ' <CONTACT>        EXCLUS (HORS ZONE)'
          ELSEIF (ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+17).GE.1.D0) THEN
            WRITE(IFM,*) ' <CONTACT>        EXCLUS (SANS_NOEUD_FR)'
          ELSEIF (ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+24).NE.0.D0) THEN
            WRITE(IFM,*) ' <CONTACT>        EXCLUS (GROUP_NO_FOND)'
          ELSEIF (ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+28).NE.0.D0) THEN
            WRITE(IFM,*) ' <CONTACT>        EXCLUS (GROUP_NO_RACC)'
          ELSEIF (ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+29).EQ.1.D0) THEN
            WRITE(IFM,*) ' <CONTACT>        EXCLUS (SANS_NOEUD)'
          ELSE
            WRITE(IFM,2003) NOMMAI,KSIPR1,KSIPR2             
          ENDIF  
 2003 FORMAT (' <CONTACT>        SE PROJETTE SUR LA MAILLE MAITRE ',
     &        A8,' EN  <',E10.3,',',E10.3,'>')          
C
C --- REPERE LOCAL
C      
          TAU1(1) = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+6)
          TAU1(2) = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+7)
          TAU1(3) = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+8)
          TAU2(1) = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+9)
          TAU2(2) = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+10)
          TAU2(3) = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+11)      
          WRITE(IFM,2002) TAU1(1),TAU1(2),TAU1(3),
     &                TAU2(1),TAU2(2),TAU2(3)           
 2002 FORMAT (' <CONTACT>        TANGENTES : <',
     &         E10.3,',',E10.3,',',E10.3,'> <',
     &         E10.3,',',E10.3,',',E10.3,'>')  
      
          CALL MMNORM(NDIMG,TAU1,TAU2,NORM,R8BID)
          WRITE(IFM,2001) NORM(1),NORM(2),NORM(3)
          
 2001 FORMAT (' <CONTACT>        NORMALE   : <',
     &         E10.3,',',E10.3,',',E10.3,'>')          
 10     CONTINUE
        NTPC = NTPC + NBPC
 20   CONTINUE                            
C
      CALL JEDEMA()
           
      END
