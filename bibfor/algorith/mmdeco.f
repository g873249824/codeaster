C
C ROUTINE CONTACT (METHODE CONSTINUE - POST-TRAITEMENT)
C
C GESTION DE LA DECOUPE
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C
C ------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------
C 
      CHARACTER*24 TABFIN,ETATCT
      INTEGER      JTABF ,JETAT 
      INTEGER      CFMMVD,ZETAT,ZTABF
      INTEGER      CFDISI,NTPC,IPC
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- LECTURE DES STRUCTURES DE DONNEES DE CONTACT
C 
      ETATCT = RESOCO(1:14)//'.ETATCT'
      TABFIN = RESOCO(1:14)//'.TABFIN'
      CALL JEVEUO(TABFIN,'L',JTABF)
      CALL JEVEUO(ETATCT,'E',JETAT)
      ZTABF  = CFMMVD('ZTABF')
      ZETAT  = CFMMVD('ZETAT')
C
C --- INITIALISATION
C 
      NTPC   = CFDISI(DEFICO,'NTPC')   
C
C --- SAUVEGARDE DE L ETAT DE CONTACT EN CAS DE REDECOUPAGE
C
      DO 1000 IPC = 1,NTPC
C       STATUT DE CONTACT
        ZR(JETAT-1+ZETAT*(IPC-1)+1) = ZR(JTABF+ZTABF*(IPC-1)+22)
C       SEUIL DE FROTTEMENT
        ZR(JETAT-1+ZETAT*(IPC-1)+2) = ZR(JTABF+ZTABF*(IPC-1)+16)
C       COMPLIANCE (ASPERITE)
        ZR(JETAT-1+ZETAT*(IPC-1)+3) = ZR(JTABF+ZTABF*(IPC-1)+23)
C       MEMOIRE DE GLISSIERE
        ZR(JETAT-1+ZETAT*(IPC-1)+4) = ZR(JTABF+ZTABF*(IPC-1)+17)
1000  CONTINUE
C
      CALL JEDEMA()
C 
      END
