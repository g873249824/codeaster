C-----------------------------------------------------------------------
C  BUT:    < DDL ACTIF CAS AUCUN >
C
C DETERMINER LES ENTIER CODES DES DDL ACTIFS A PARTIR DES ENTIERS CODES
C  DES DDL PHYSIQUE ET DES LAGRANGES ASSEMBLES AUX NOEUDS
C  POUR UNE INTERFACE DE TYPE AUCUN
C
C-----------------------------------------------------------------------
C
C NBCMP    /I/: NOMBRE DE COMPOSANTES MAX DE LA GRANDEUR SOUS-JACENTE
C NBNO     /I/: NOMBRE DE NOEUDS DE LA TABLE
C MCODDL   /I/: TABLEAU DES ENTIERS CODES DDL AUX NOEUDS
C ICODAC   /O/: LISTE DES ENTIERS CODES DES DDL ACTIFS DEMANDES
C
C-----------------------------------------------------------------------
C
      PARAMETER (NBCPMX = 300)
      PARAMETER (NBECMX =  10)
      INTEGER MCODDL(NBNO*NBEC,2),ICODAC(NBNO*NBEC)
      INTEGER IDEC(NBCPMX), ITOUT(NBECMX), ICOCO(NBECMX), ICICI(NBECMX)
C
C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
C
C    INITIALISATION DU VECTEUR A CODER (SERA INUTILE QUAND LA BOUCLE
C   INTERNE 20  SERA DE 1 A 30)
C
      DO 10 I=1, NBECMX
        ITOUT(I) = 0
        ICOCO(I) = 0
        ICICI(I) = 0
 10    CONTINUE
C
      DO 20 I=1,NBCMP
        IDEC(I)=1
 20    CONTINUE
       CALL ISCODE(IDEC,ITOUT,NBCMP)
C
      IF(NBNO.EQ.0) GOTO 9999
C
      DO 30 I=1,NBNO
        CALL ISGECO(ITOUT,MCODDL((I-1)*NBEC+1,1),NBCMP,-1,ICOCO)
        CALL ISGECO(ICOCO,MCODDL((I-1)*NBEC+1,2),NBCMP,1,ICICI)
        CALL ISGECO(ICODAC((I-1)*NBEC+1),ICICI,NBCMP,-1,ICOCO)
        DO 40 IEC = 1, NBEC
          ICODAC((I-1)*NBEC+IEC)=ICOCO(IEC)
 40     CONTINUE
 30   CONTINUE
C
 9999 CONTINUE
      END
