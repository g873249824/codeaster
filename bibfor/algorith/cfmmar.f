C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C 
      IMPLICIT NONE
      CHARACTER*8  NOMA
      CHARACTER*19 SDAPPA
      CHARACTER*24 DEFICO
      CHARACTER*19 NEWGEO
      INTEGER      NZOCO,NTPT,NDIMG
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES - SD APPARIEMENT)
C
C REMPLISSAGE DE LA SD POUR L'APPARIEMENT
C      
C ----------------------------------------------------------------------
C
C /!\ SAUF COORDONNNEES DES POINTS (VOIR MMPOIN ET CFPOIN)
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  SDAPPA : NOM DE LA SD APPARIEMENT
C IN  NEWGEO : GEOMETRIE ACTUALISEE
C IN  NZOCO  : NOMBRE DE ZONES
C IN  NTPT   : NOMBRE TOTAL DE POINT A APPARIER
C IN  NDIMG  : DIMENSION DE L'ESPACE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      CHARACTER*24 NOMSD
      INTEGER      JNOMSD
      CHARACTER*24 APINZI,APINZR
      INTEGER      JPINZI,JPINZR      
      CHARACTER*24 APINFI,APINFR
      INTEGER      JPINFI,JPINFR   
      INTEGER      IZONE
      INTEGER      CFDISI,MMINFI
      REAL*8       CFDISR,MMINFR
      INTEGER      APPARI,ITYPA
      INTEGER      NBPT,NBNOM,NBNOE,NBMAM,NBMAE
      INTEGER      JDECNM,JDECMM,JDECNE,JDECME
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV)      
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> ...... REMPLISSAGE SD APPARIEMENT' 
      ENDIF                      
C
C --- SD NOMS
C
      NOMSD  = SDAPPA(1:19)//'.NOSD'        
      CALL JEVEUO(NOMSD ,'E',JNOMSD)               
C
      ZK24(JNOMSD+1 -1) = NOMA
      ZK24(JNOMSD+2 -1) = NEWGEO
      ZK24(JNOMSD+3 -1) = DEFICO
C
C --- SD INFORMATIONS GLOBALES
C
      APINFI = SDAPPA(1:19)//'.INFI'
      APINFR = SDAPPA(1:19)//'.INFR'
      CALL JEVEUO(APINFI,'E',JPINFI) 
      CALL JEVEUO(APINFR,'E',JPINFR)       
C      
      ZI(JPINFI+1-1) = NZOCO
      ZI(JPINFI+2-1) = NTPT
      ZI(JPINFI+3-1) = 0
      ZI(JPINFI+4-1) = CFDISI(DEFICO,'PROJ_NEWT_ITER')
      ZI(JPINFI+5-1) = NDIMG
C
      ZR(JPINFR+1-1) = CFDISR(DEFICO,'PROJ_NEWT_RESI') 
C
C --- SD INFORMATIONS PAR ZONE
C
      APINZI = SDAPPA(1:19)//'.INZI'
      APINZR = SDAPPA(1:19)//'.INZR'     
      CALL JEVEUO(APINZI,'E',JPINZI)
      CALL JEVEUO(APINZR,'E',JPINZR)
      DO 30 IZONE = 1,NZOCO
        APPARI = MMINFI(DEFICO,'APPARIEMENT',IZONE )
        ITYPA  = MMINFI(DEFICO,'TYPE_APPA'  ,IZONE )
        NBPT   = MMINFI(DEFICO,'NBPT'       ,IZONE )
        NBNOM  = MMINFI(DEFICO,'NBNOM'      ,IZONE )
        NBNOE  = MMINFI(DEFICO,'NBNOE'      ,IZONE )
        NBMAM  = MMINFI(DEFICO,'NBMAM'      ,IZONE )
        NBMAE  = MMINFI(DEFICO,'NBMAE'      ,IZONE )
        JDECNM = MMINFI(DEFICO,'JDECNM'     ,IZONE )
        JDECMM = MMINFI(DEFICO,'JDECMM'     ,IZONE )
        JDECNE = MMINFI(DEFICO,'JDECNE'     ,IZONE )
        JDECME = MMINFI(DEFICO,'JDECME'     ,IZONE )
      
      
        ZI(JPINZI+11*(IZONE-1)+1 -1) = APPARI
        ZI(JPINZI+11*(IZONE-1)+2 -1) = NBPT
        ZI(JPINZI+11*(IZONE-1)+3 -1) = NBNOM
        ZI(JPINZI+11*(IZONE-1)+4 -1) = NBNOE
        ZI(JPINZI+11*(IZONE-1)+5 -1) = NBMAM
        ZI(JPINZI+11*(IZONE-1)+6 -1) = NBMAE
        ZI(JPINZI+11*(IZONE-1)+7 -1) = JDECNM
        ZI(JPINZI+11*(IZONE-1)+8 -1) = JDECMM
        ZI(JPINZI+11*(IZONE-1)+9 -1) = JDECNE
        ZI(JPINZI+11*(IZONE-1)+10-1) = JDECME
C
        ITYPA  = MMINFI(DEFICO,'TYPE_APPA'  ,IZONE )
        ZI(JPINZI+11*(IZONE-1)+11-1) = ITYPA
C
        IF (ITYPA.EQ.1) THEN
          ZR(JPINZR+5*(IZONE-1)+1 -1) = 
     &      MMINFR(DEFICO,'TYPE_APPA_DIRX',IZONE )
          ZR(JPINZR+5*(IZONE-1)+2 -1) = 
     &      MMINFR(DEFICO,'TYPE_APPA_DIRY',IZONE ) 
          ZR(JPINZR+5*(IZONE-1)+3 -1)= 
     &      MMINFR(DEFICO,'TYPE_APPA_DIRZ',IZONE )
        ENDIF
C 
        ZR(JPINZR+5*(IZONE-1)+4 -1) = 
     &    MMINFR(DEFICO,'TOLE_APPA'     ,IZONE )
        ZR(JPINZR+5*(IZONE-1)+5 -1)= 
     &    MMINFR(DEFICO,'TOLE_PROJ_EXT' ,IZONE ) 
C
  30  CONTINUE
C      
      CALL JEDEMA()     
C
      END
