      SUBROUTINE CFMMAR(NOMA  ,DEFICO,NEWGEO,SDAPPA,NZOCO ,
     &                  NTPT  ,NDIMG ,NTMA  ,NTNO  ,NTMANO)
C      
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 03/01/2011   AUTEUR ABBAS M.ABBAS 
C ======================================================================
