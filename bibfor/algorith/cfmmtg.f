C ROUTINE CONTACT (METHODES MAILLEES - SD TANGENTES)
C
C CREATION DES SDS POUR LES TANGENTES
C      
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C OUT RESOCO : SD POUR LA RESOLUTION DE CONTACT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*32  JEXNUM
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      INTEGER      IBID,POSMAI,IMA,NNOSD,LONGC,LONGT
      INTEGER      CFDISI,NNOCO,NTMANO,NMACO
      CHARACTER*24 TGNOEU,TGELNO
      INTEGER      JTGNOE
      CHARACTER*8  K8BID 
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV)
C
C --- INITIALISATIONS
C
      NNOCO  = CFDISI(DEFICO,'NNOCO' ) 
      NTMANO = CFDISI(DEFICO,'NTMANO') 
      NMACO  = CFDISI(DEFICO,'NMACO' )
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> ...... CREATION SD CALCUL DES '//
     &                ' TANGENTES' 
      ENDIF 
C
C --- TANGENTES EN TOUS LES NOEUDS      
C           
      TGNOEU = RESOCO(1:14)//'.TGNOEU'
      CALL WKVECT(TGNOEU,'V V R',6*NNOCO,JTGNOE)
C
C --- TANGENTES AUX NOEUDS PAR ELEMENT
C        
      TGELNO = RESOCO(1:14)//'.TGELNO'
      CALL JECREC(TGELNO,'V V R','NU','CONTIG','VARIABLE',NMACO)
C
      LONGT  = 0
      CALL JEECRA(TGELNO,'LONT',6*NTMANO,K8BID)
      DO 20 IMA=1,NMACO
        POSMAI = IMA
        CALL CFNBEN(DEFICO,POSMAI,'CONNEX',NNOSD ,IBID  )
        LONGC  = 6*NNOSD
        CALL JEECRA(JEXNUM(TGELNO,IMA),'LONMAX',LONGC ,K8BID)
        CALL JECROC(JEXNUM(TGELNO,IMA))
        LONGT = LONGT + LONGC
 20   CONTINUE
      CALL ASSERT(LONGT.EQ.6*NTMANO)
C      
      CALL JEDEMA()     
C
      END
