      IF ( K .EQ. 1 ) THEN
        LIMOCL(1)='GROUP_MA_1'
        LIMOCL(2)='GROUP_NO_1'
        LIMOCL(3)='MAILLE_1'
        LIMOCL(4)='NOEUD_1'
        LIMOCL(5)='TOUT_1'
      ELSE
        LIMOCL(1)='GROUP_MA_2'
        LIMOCL(2)='GROUP_NO_2'
        LIMOCL(3)='MAILLE_2'
        LIMOCL(4)='NOEUD_2'
        LIMOCL(5)='TOUT_2'
      ENDIF

      TYMOCL(1)='GROUP_MA'
      TYMOCL(2)='GROUP_NO'
      TYMOCL(3)='MAILLE'
      TYMOCL(4)='NOEUD'
      TYMOCL(5)='TOUT'

      CALL RELIEM(' ',MA8,'NU_NOEUD','VIS_A_VIS',IOCC,5,LIMOCL,TYMOCL,
     &                  LNO,N1)
      CALL ASSERT(N1.GT.0)

      CALL JEDEMA()
      END
