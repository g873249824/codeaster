      SUBROUTINE NSPL3D(NNO,NPG,IPOIDS,IVF,IDFDE,GEOM,TYPMOD,OPTION,
     &                  IMATE,IMATSE,COMPOR,LGPG,DEPMOI,DEPPLU,SIGMOS,
     &                  VARMOS,VARMOI,SIGMOI,STYPSE,DEPMOS,DEPPLS,
     &                  VARPLU,SIGPLU,VECTU,SIGPLS,VARPLS,
     &                  SIGPAS,DFDI,DEF)

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 20/04/2011   AUTEUR COURTOIS M.COURTOIS 
C ======================================================================
