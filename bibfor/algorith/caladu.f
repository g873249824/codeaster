C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : ALGOCL/ALGOCO/ALGOCP/CFACA2/CFADU/FRO2GD
C                       FROGDP/FROLGD/FROPGD/REAJEU/RESUCO
C ----------------------------------------------------------------------
C
C CALCUL DU TERME II DE (A.DEPL) OU A EST LA MATRICE DE CONTACT ET
C DEPL UN VECTEUR QUELCONQUE.
C
C IN  NEQ    : NOMBRE D'EQUATIONS
C IN  NBDDL  : NOMBRE DE DDLS IMPLIQUES DANS LA LIAISON UNILATERALE
C IN  COEF   : COEFFICIENTS IMPLIQUES DANS LA LIAISON UNILATERALE
C IN  DDL    : NUMEROS DES DDLS IMPLIQUES DANS LA LIAISON UNILATERALE
C IN  DEPL   : VECTEUR A MULTIPLIER PAR LA MATRICE A
C OUT VAL    : VALEUR DU TERME OBTENU
C
C ----------------------------------------------------------------------
C
      INTEGER J
C
C ----------------------------------------------------------------------
C
      VAL = 0.D0
      DO 10 J = 1,NBDDL
        VAL = VAL + COEF(J) * DEPL(DDL(J))
 10   CONTINUE
