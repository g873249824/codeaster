C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*24 DEFICO,RESOCO
      CHARACTER*24 SAUVAP,SAUVPT,SAUVIO  
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT)
C
C SAUVEGARDE DES RESULTATS DE L'APPARIEMENT DU PASSE
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE DEFINITION DU CONTACT (ISSUE D'AFFE_CHAR_MECA)
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  SAUVAP : NOM VECTEUR POUR SAUVEGARDE RESOCO(1:14)//'.APPARI'
C IN  SAUVPT : NOM VECTEUR POUR SAUVEGARDE RESOCO(1:14)//'.APPOIN'
C IN  SAUVIO : NOM VECTEUR POUR SAUVEGARDE POINTEUR NDS ESCLAVES/ZONE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      CFMMVD,CFDISI,ZAPPA
      INTEGER      NESCL,NZOCO,K,IZONE  
      INTEGER      IFM,NIV   
      CHARACTER*24 APPARI,APPOIN
      INTEGER      JAPPAR,JAPPTR  
      INTEGER      JOLDAP,JOLDPT,JIOLD
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()      
      CALL INFDBG('CONTACT',IFM,NIV)     
C   
C --- AFFICHAGE
C   
      CALL CFIMPE(IFM,NIV,'SAUVCO',1)
C
C --- LECTURE DES SD POUR LE CONTACT POTENTIEL
C   
      APPARI = RESOCO(1:14)//'.APPARI'
      APPOIN = RESOCO(1:14)//'.APPOIN'   
      CALL JEVEUO(APPARI,'L',JAPPAR)
      CALL JEVEUO(APPOIN,'L',JAPPTR)         
C
C --- INITIALISATIONS
C
      IZONE  = 0
      ZAPPA  = CFMMVD('ZAPPA') 
      NESCL  = ZI(JAPPAR)
      NZOCO  = CFDISI(DEFICO,'NZOCO',IZONE)   
C
C --- CREATION DES OBJETS DE SAUVEGARDE
C
      CALL WKVECT(SAUVAP,'V V I',ZAPPA*NESCL+1,JOLDAP)
      CALL WKVECT(SAUVPT,'V V I',NESCL+1      ,JOLDPT)     
      CALL WKVECT(SAUVIO,'V V I',NZOCO+1      ,JIOLD )
C
C --- STOCKAGE DE APPARI
C
      DO 5 K = 1,ZAPPA*NESCL+1
        ZI(JOLDAP+K-1) = ZI(JAPPAR+K-1)
 5    CONTINUE
C
C --- STOCKAGE DE APPOIN
C
      DO 6 K = 1,NESCL+1
        ZI(JOLDPT+K-1) = ZI(JAPPTR+K-1)
 6    CONTINUE
C
C --- STOCKAGE DU DECALAGE DES ADRESSES DE DEBUT 
C
      ZI(JIOLD) = 0
      DO 8 IZONE = 1,NZOCO
        ZI(JIOLD+IZONE) = ZI(JIOLD+IZONE-1) + 
     &                    CFDISI(DEFICO,'NESCL_ZONE',IZONE)
 8    CONTINUE
C
      CALL JEDEMA()
      END
