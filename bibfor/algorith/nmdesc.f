      SUBROUTINE NMDESC(MODELE,NUMEDD,MATE  ,CARELE,COMREF,
     &                  COMPOR,LISCHA,RESOCO,METHOD,SOLVEU,
     &                  PARMET,CARCRI,FONACT,NUMINS,ITERAT,
     &                  SDPILO,SDDISC,SDTIME,SDDYNA,MATASS,
     &                  MAPREC,DEFICO,VALMOI,VALPLU,POUGD ,
     &                  SOLALG,ETA   ,LICCVG,MEELEM,MEASSE,
     &                  VEASSE,VEELEM)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 23/09/2008   AUTEUR ABBAS M.ABBAS 
C ======================================================================
