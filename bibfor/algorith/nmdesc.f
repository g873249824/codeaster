C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MABBAS M.ABBAS
C TOLE CRP_21
C
      IMPLICIT NONE
      INTEGER      NUMINS, ITERAT, LICCVG(*)
      REAL*8       PARMET(*), ETA
      CHARACTER*14 SDPILO
      CHARACTER*16 METHOD(*) 
      CHARACTER*19 MATASS,MAPREC      
      CHARACTER*19 LISCHA, SOLVEU, SDDISC,SDDYNA
      CHARACTER*24 MODELE, NUMEDD, MATE, CARELE, COMREF, COMPOR,CARCRI
      CHARACTER*24 VALMOI(8),POUGD(8),VALPLU(8)
      CHARACTER*24 DEFICO,RESOCO,SDTIME
      LOGICAL      FONACT(*)
      CHARACTER*19 MEELEM(*),VEELEM(*),SOLALG(*)      
      CHARACTER*19 MEASSE(*),VEASSE(*)      
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C CALCUL DE LA DIRECTION DE DESCENTE
C      
C ----------------------------------------------------------------------
C
C
C IN  MODELE : MODELE
C IN  NUMEDD : NUME_DDL
C IN  MATE   : CHAMP MATERIAU
C IN  CARELE : CARACTERISTIQUES DES ELEMENTS DE STRUCTURE
C IN  COMREF : VARI_COM DE REFERENCE
C IN  COMPOR : COMPORTEMENT
C IN  LISCHA : L_CHARGES
C IN  SDDISC : SD DISC_INST
C IN  SDTIME : SD TIMER
C IN  ITERAT : NUMERO D'ITERATION DE NEWTON
C IN  NUMINS : NUMERO D'INSTANT
C IN  RESOCO : SD RESOLUTION CONTACT
C IN  METHOD : INFORMATIONS SUR LES METHODES DE RESOLUTION
C IN  ETA    : PARAMETRE DE PILOTAGE
C IN  VALMOI : VARIABLE CHAPEAU POUR ETAT EN T-
C IN  VALPLU : VARIABLE CHAPEAU POUR ETAT EN T+
C IN  POUGD  : VARIABLE CHAPEAU POUR POUTRES EN GRANDES ROTATIONS
C IN  SOLALG : VARIABLE CHAPEAU POUR INCREMENTS SOLUTIONS
C IN  VEELEM : VARIABLE CHAPEAU POUR NOM DES VECT_ELEM
C IN  VEASSE : VARIABLE CHAPEAU POUR NOM DES VECT_ASSE
C IN  MEELEM : VARIABLE CHAPEAU POUR NOM DES MATR_ELEM
C IN  MEASSE : VARIABLE CHAPEAU POUR NOM DES MATR_ASSE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24 CODERE
      CHARACTER*19 NMCHEX,CNCINE,CNDONN,CNPILO,CNCINX
      INTEGER      FACCVG,LDCCVG
      REAL*8       R8BID
      LOGICAL      LBID
      INTEGER      IFM,NIV
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> CALCUL DIRECTION DE DESCENTE...' 
      ENDIF 
C
C --- INITIALISATIONS
C
      FACCVG = 0                
C
C --- DECOMPACTION DES VARIABLES CHAPEAU
C
      CNCINE = NMCHEX(VEASSE,'VEASSE','CNCINE')
      CNDONN = '&&CNCHAR.DONN'
      CNCINX = '&&CNCHAR.CINE'      
      CNPILO = '&&CNCHAR.PILO'   
      CALL VTZERO(CNDONN)   
      CALL VTZERO(CNPILO)
      CALL VTZERO(CNCINX)     
C
C --- CALCUL DE LA MATRICE GLOBALE
C  
      CALL NMCOMA(MODELE,MATE  ,CARELE,COMPOR,CARCRI,
     &            PARMET,METHOD,LISCHA,NUMEDD,SOLVEU,
     &            COMREF,SDDISC,SDDYNA,SDTIME,NUMINS,
     &            ITERAT,FONACT,DEFICO,RESOCO,VALMOI,
     &            VALPLU,SOLALG,POUGD ,VEELEM,MEELEM,
     &            MEASSE,VEASSE,MAPREC,MATASS,CODERE,
     &            FACCVG,LDCCVG)      
C     
      IF ((FACCVG.EQ.1).OR.(FACCVG.EQ.2)) GOTO 9999
C
C --- PREPARATION DU SECOND MEMBRE
C
      CALL NMASSC(FONACT,SDDYNA,DEFICO,ETA   ,VEASSE,
     &            CNPILO,CNDONN)
C
C --- POUR LE CALCUL DE DDEPLA, IL FAUT METTRE CNCINE A ZERO 
C
      CALL COPISD('CHAMP_GD','V',CNCINE,CNCINX)
      CALL VTZERO(CNCINX)
C
C --- RESOLUTION
C
      CALL NMTIME('INIT' ,'TMP',SDTIME,LBID  ,R8BID )
      CALL NMTIME('DEBUT','TMP',SDTIME,LBID  ,R8BID )
      CALL NMRESD(FONACT,SDPILO,SDDYNA,SOLVEU,NUMEDD,
     &            R8BID ,MAPREC,MATASS,CNDONN,CNPILO,
     &            CNCINX,'CORR',SOLALG)
      CALL NMTIME('FIN'  ,'TMP',SDTIME,LBID  ,R8BID )
      CALL NMTIME('RESOL','TMP',SDTIME,LBID  ,R8BID )       
C
9999  CONTINUE
C
C --- RECOPIE CODE RETOUR ERREURS      
C
      LICCVG(5) = FACCVG
C      
      CALL JEDEMA()
      END
