      SUBROUTINE XRECFF(FISS,CHFOND,BASFON,LNOFF)
      IMPLICIT NONE

      INTEGER       LNOFF
      CHARACTER*8   FISS
      CHARACTER*24  CHFOND,BASFON


C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 11/07/2011   AUTEUR GENIAUT S.GENIAUT 
