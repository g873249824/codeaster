      SUBROUTINE XRECFF(FISS,CHFOND,LNOFF)
      IMPLICIT NONE

      INTEGER       LNOFF
      CHARACTER*8   FISS
      CHARACTER*24  CHFOND


C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 26/04/2011   AUTEUR DELMAS J.DELMAS 
C ======================================================================
