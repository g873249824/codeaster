      IMPLICIT     NONE
      INTEGER      IZONE
      CHARACTER*24 DEFICO       
      INTEGER      NDIM,MOYEN    
      REAL*8       ENORM(3),MNORM(3)
      REAL*8       COORDP(3),COORDM(3)
      REAL*8       JEU
      REAL*8       COEFNO(9)         
      REAL*8       TANG(6),NORM(3)        
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT - NODAL)
C
C CALCUL DU JEU
C
C ----------------------------------------------------------------------
C   
C
C IN  DEFICO : SD DE DEFINITION DU CONTACT (ISSUE D'AFFE_CHAR_MECA)
C IN  IZONE  : ZONE DE CONTACT
C IN  NDIM   : DIMENSION DE L'ESPACE (2 OU 3)
C IN  ENORM  : NORMALE AU NOEUD ESCLAVE 
C IN  MNORM  : NORMALE AU NOEUD MAITRE 
C IN  COORDP : COORDONNEES DU NOEUD ESCLAVE
C IN  COORDM : COORDONNEES DU NOEUD MAITRE
C OUT NORM   : VECTEUR NORMAL UTILISE
C OUT TANG   : VECTEUR TANGENTS SUR REPERE NORM/MAILLE
C OUT COEFNO : VALEURS EN M DES FONCTIONS DE FORME ASSOCIEES AU NOEUD 
C               MAITRE
C OUT JEU    : VALEUR DU JEU SUIVANT LA NORMALE
C      
C ----------------------------------------------------------------------
C
      INTEGER      IBID
      REAL*8       R8BID3(3)  
C      
C ----------------------------------------------------------------------
C
C
C --- CALCUL DES COEFFICIENTS DE LA RELATION LINEAIRE SUR NOEUD MAITRE
C      
      CALL CFRELI('NODE',IBID,IBID,R8BID3,COEFNO)  
C
C --- MOYENNAGE DES NORMALES (SI DEMANDE PAR MOYEN)
C
      CALL CFMOYN('NODAL',MOYEN,MNORM,ENORM,NORM)    
C
C --- RECALCUL DES TANGENTES
C    
      CALL CFTANG(DEFICO,IZONE,NDIM,NORM,TANG)   
C
C --- CALCUL DU JEU
C
      CALL CFNEWJ(NDIM,COORDP,COORDM,NORM,JEU)

           
      END
