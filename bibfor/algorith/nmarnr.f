C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT      NONE
      INCLUDE 'jeveux.h'
      CHARACTER*8   RESULT
      CHARACTER*(*) TYPTAZ
      INTEGER       NUMREU
C
C ----------------------------------------------------------------------
C
C ROUTINE *_NON_LINE (ARCHIVAGE)
C
C RECUPERATION NUMERO DE REUSE COURANT POUR LA TABLE
C
C ----------------------------------------------------------------------
C
C
C IN  RESULT : NOM DE LA SD RESULTAT
C IN  TYPTAB : TYPE DE LA TABLE
C OUT NUMREU : NUMERO DE REUSE POUR LA TABLE
C
C ----------------------------------------------------------------------
C
      INTEGER      IRET,JTBNP,NBLIGN
      CHARACTER*19 NOMTAB
      CHARACTER*16 TYPTAB
      CHARACTER*2  TYPVAR
      LOGICAL      LEXIST
      CHARACTER*19 LISRES
      INTEGER      JLISRE,NVAL,IVAL,VALI
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NUMREU = -1
      LISRES = '&&NMARNR.NUME_REUSE'
      TYPTAB = TYPTAZ
C
C --- RECUPERATION DE LA LISTE DE TABLES SI ELLE EXISTE
C 
      CALL JEEXIN(RESULT//'           .LTNT',IRET)
      IF (IRET.EQ.0) THEN
        NUMREU = 0
        GOTO 99
      ENDIF
C
C --- RECUPERATION DU NOM DE LA TABLE
C
      NOMTAB = ' '
      CALL LTNOTB(RESULT,TYPTAB,NOMTAB)
C
C --- LA TABLE EXISTE-T-ELLE ?
C
      CALL EXISD('TABLE',NOMTAB,IRET)
      IF (IRET.EQ.0) THEN
        NUMREU = 0
        GOTO 99
      ELSE
        CALL TBEXIP(NOMTAB,'NUME_REUSE',LEXIST,TYPVAR)
        IF (.NOT.LEXIST.OR.TYPVAR.NE.'I') CALL ASSERT(.FALSE.)
C
C ----- NOMBRE DE LIGNES
C
        CALL JEVEUO(NOMTAB//'.TBNP','L', JTBNP )
        NBLIGN = ZI(JTBNP+1)
        IF (NBLIGN.EQ.0) THEN
          NUMREU = 0
          GOTO 99
        ENDIF
C
C ----- EXTRACTION DE LA COLONNE 'NUME_REUSE' DANS UN OBJET TEMPORAIRE
C
        CALL TBEXVE(NOMTAB,'NUME_REUSE',LISRES,'V',NVAL,TYPVAR)
        CALL JEVEUO(LISRES,'L',JLISRE)
C
C ----- RECUPERATION DU MAX
C
        DO 10 IVAL = 1,NVAL
          VALI = ZI(JLISRE-1+IVAL)
          IF (VALI.GT.NUMREU) NUMREU = VALI
  10    CONTINUE
        NUMREU = NUMREU + 1
      ENDIF
C
  99  CONTINUE
C
      CALL JEDETR(LISRES)
      CALL ASSERT(NUMREU.GE.0)
C
      CALL JEDEMA()
      END
