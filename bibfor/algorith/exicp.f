      FUNCTION EXICP(MODELE,MESMAI,NBMA)
      IMPLICIT NONE
C
      CHARACTER*8   MODELE
      CHARACTER*24  MESMAI
      INTEGER       NBMA
      LOGICAL       EXICP
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 13/12/2011   AUTEUR GENIAUT S.GENIAUT 
C ======================================================================
C COPYRIGHT (C) 1991 - 2011  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY  
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
C     RENVOIE .TRUE. SI PARMI LES ELEMENTS FINIS ASSOCIES AUX MAILLES
C     DONNEES, IL EN EXISTE UN QUI SOIT EN C_PLAN
C     SI AUCUNE MAILLE N'EST DONNEE EN ENTREE (NBMA=0), ALORS
C     ON REGARDE TOUTES LES MAILLES DU MAILLAGE
C
C IN  MODELE : NOM DU MODELE
C IN  MESMAI : LISTE DES MAILLES SUR LESQUELLES ON FAIT LE TEST
C IN  NBMA   : NOMBRE DE MAILLES DANS MESMAI
C              SI NBMA = 0  ON FAIT LE TEST SUR TOUTES LES MAILLES DU
C              MAILLAGE ASSOCIÉE AU MODELE
C OUT EXICP : .TRUE. SI C_PLAN TROUVE
C
C ----------------------------------------------------------------------
C
      INTEGER      NBMAT,JMA,IRET,IMA,JTYPEL,NUMA,ITYPEL,IBID
      CHARACTER*8  NOMA,K8BID,DMO,DMA
      CHARACTER*16 NOTYPE,TYPMOD,BORD
      CHARACTER*24 MAIL
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
      INTEGER        ZI
      COMMON /IVARJE/ZI(1)
      REAL*8         ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16     ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL        ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8    ZK8
      CHARACTER*16          ZK16
      CHARACTER*24                  ZK24
      CHARACTER*32                          ZK32
      CHARACTER*80                                  ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32     JEXNUM, JEXNOM
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      CALL JEMARQ()
C
      EXICP = .FALSE.

      IF (NBMA.NE.0) THEN
C       DES MAILLES ONT ETE DONNEES
        CALL JEVEUO(MESMAI,'L',JMA)
        NBMAT = NBMA
      ELSE
C       ON PREND TOUTES LES MAILLES DU MAILLAGE
        CALL DISMOI('F','NOM_MAILLA',MODELE,'MODELE',IBID,NOMA,IRET)
        CALL DISMOI('F','NB_MA_MAILLA',NOMA,'MAILLAGE',NBMAT,K8BID,IRET)
      ENDIF

      MAIL = MODELE//'.MAILLE'
      CALL JEVEUO(MAIL,'L',JTYPEL)

C     BOUCLE SUR LES MAILLES
      DO 10 IMA=1,NBMAT

C       RECUP DU NUMERO DE LA MAILLE COURANTE
        IF (NBMA.NE.0) THEN
          NUMA = ZI(JMA-1+IMA)
        ELSE
          NUMA = IMA
        ENDIF
        
C       RECUP DU NUMERO DU TYPE D'ELEMENT ASSOCIE
        ITYPEL=ZI(JTYPEL-1+NUMA)

C       SI LA MAILLE N'EST PAS AFFECTEE D'UN ELEMENT FINI ON ZAPPE
        IF (ITYPEL.EQ.0) GOTO 10

C       RECUP DU NOM DU TYPE D'ELEMENT ASSOCIE
        CALL JENUNO(JEXNUM('&CATA.TE.NOMTE',ITYPEL),NOTYPE)

        CALL TEATTR (NOTYPE,'S','DIM_TOPO_MODELI',DMO,IRET)
        CALL TEATTR (NOTYPE,'S','DIM_TOPO_MAILLE',DMA,IRET)
C       SI ELEMENT DE BORD ON ZAPPE
        IF (DMO.NE.DMA) GOTO 10
        
        CALL TEATTR(NOTYPE,'C','TYPMOD',TYPMOD,IRET)

C       SI L'ATTRIBUT TYPMOD N'EST PAS TROUVE ON ZAPPE
        IF (IRET.NE.0) GOTO 10
         
        IF(TYPMOD(1:6).EQ.'C_PLAN') THEN
          EXICP=.TRUE.
          GOTO 999
        ENDIF

 10   CONTINUE

999   CONTINUE
      CALL JEDEMA()
      END
