C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*24 NEWGEO
      CHARACTER*24 DEFICO
      CHARACTER*24 RESOCO
      INTEGER      IZONE
      INTEGER      ILIDEB,ILIFIN
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT)
C
C CARACTERISTIQUES DES LIAISONS POUR LA ZONE
C
C ----------------------------------------------------------------------
C
C
C IN  IZONE  : ZONE COURANTE
C IN  ILIDEB : INDICE DE LA PREMIERE LIAISON POUR LA ZONE
C IN  ILIFIN : INDICE DE LA DERNIERE LIAISON POUR LA ZONE
C IN  NEWGEO : GEOMETRIE ACTUALISEE EN TENANT COMPTE DU CHAMP DE
C              DEPLACEMENTS COURANT
C IN  DEFICO : SD DE DEFINITION DU CONTACT (ISSUE D'AFFE_CHAR_MECA)
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      INTEGER      ILIAI
      INTEGER      CFMMVD,ZTACF
      INTEGER      POSMAE,POSNOE,NUMNOE
      REAL*8       COORDE(3),DIST
      INTEGER      JCOOR
      CHARACTER*24 APJEU,APPARI,CONTNO
      INTEGER      JAPJEU,JAPPAR,JNOCO
      CHARACTER*24 TACFIN,JEUSUP
      INTEGER      JTACF,JJSUP
      REAL*8       MMINFR,COEFFF,COEFPN,COEFPT,COEFTE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()      
      CALL INFDBG('CONTACT',IFM,NIV)      
C
C --- INITIALISATION DE VARIABLES
C
      POSMAE = 0  
C
C --- LECTURE DES SD POUR LE CONTACT POTENTIEL
C
      APPARI = RESOCO(1:14)//'.APPARI'
      CONTNO = DEFICO(1:16)//'.NOEUCO'
      APJEU  = RESOCO(1:14)//'.APJEU'
      TACFIN = RESOCO(1:14)//'.TACFIN' 
      JEUSUP = RESOCO(1:14)//'.JSUPCO' 
C
      CALL JEVEUO(APPARI,'L',JAPPAR)
      CALL JEVEUO(CONTNO,'L',JNOCO )
      CALL JEVEUO(APJEU ,'E',JAPJEU)
      CALL JEVEUO(TACFIN,'E',JTACF )
      CALL JEVEUO(JEUSUP,'E',JJSUP )      
C
      ZTACF  = CFMMVD('ZTACF')  
C 
C --- COEFFICIENTS DE PENALISATION
C 
      COEFPN = MMINFR(DEFICO,'E_N'            ,IZONE )
      COEFPT = MMINFR(DEFICO,'E_T'            ,IZONE )
C
C --- COEFFICIENT DE FROTTEMENT
C             
      COEFFF = MMINFR(DEFICO,'COEF_COULOMB'   ,IZONE )
C
C --- COEFFICIENT THETA POUR FROTTEMENT
C   
      COEFTE = MMINFR(DEFICO,'COEF_MATR_FROT' ,IZONE )            
C
C --- COORDONNEES DES NOEUDS DU MAILLAGE
C
      CALL JEVEUO(NEWGEO(1:19)//'.VALE','L',JCOOR)
      
      DO 60 ILIAI = ILIDEB,ILIFIN
C
C --- CARACTERISTIQUES NOEUD ESCLAVE
C
        POSNOE = ZI(JAPPAR+ILIAI)
        NUMNOE = ZI(JNOCO+POSNOE-1)
        COORDE(1) = ZR(JCOOR+3*(NUMNOE-1))
        COORDE(2) = ZR(JCOOR+3*(NUMNOE-1)+1)
        COORDE(3) = ZR(JCOOR+3*(NUMNOE-1)+2) 
C
C --- CALCUL DU JEU FICTIF DE LA ZONE
C  
        CALL CFDIST(DEFICO,IZONE ,'NOEU',POSNOE,POSMAE,
     &              COORDE,DIST  )   
        ZR(JJSUP+ILIAI-1) = DIST
C
C --- ADDITION DU JEU FICTIF DE LA ZONE DANS APJEU
C
        ZR(JAPJEU+ILIAI-1) = ZR(JAPJEU+ILIAI-1) - DIST
C
C --- RECOPIE CARACTERISTIQUES ZONE -> NOEUDS ESCLAVES
C           
        ZR(JTACF+ZTACF*(ILIAI-1)+0) = COEFFF
        ZR(JTACF+ZTACF*(ILIAI-1)+1) = COEFPN
        ZR(JTACF+ZTACF*(ILIAI-1)+2) = COEFPT
        ZR(JTACF+ZTACF*(ILIAI-1)+3) = COEFTE
 60   CONTINUE 
C
      CALL JEDEMA
      END
