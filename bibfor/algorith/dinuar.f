      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*19 SDDISC
      INTEGER      NUMINS,NUMARC,NUMREP
      LOGICAL      FORCE
C
C ----------------------------------------------------------------------
C
C ARCHIVAGE - UTILITAIRE
C
C NUMERO D'ARCHIVAGE
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION
C IN  NUMINS : NUMERO INSTANT COURANT
C IN  FORCE  : VRAI SI ON SOUHAITE FORCER L'ARCHIVAGE DE TOUS LES CHAMPS
C OUT NUMARC : NUMERO D'ARCHIVAGE
C OUT NUMREP : NUMERO DE REUSE POUR LA TABLE PARA_CALC
C
C ----------------------------------------------------------------------
C
      CHARACTER*24 ARCINF
      INTEGER      JARINF
      REAL*8       DIINST,INST      
      LOGICAL      LARCH
      CHARACTER*19 SDARCH     
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      LARCH  = .FALSE.  
C
C --- ACCES SD ARCHIVAGE
C
      SDARCH = SDDISC(1:14)//'.ARCH'
      ARCINF = SDARCH(1:19)//'.AINF'
      CALL JEVEUO(ARCINF,'E',JARINF)
C
C --- ARCHIVAGE INSTANT INITIAL FORCE
C
      IF (NUMINS.EQ.0) THEN
        LARCH  = .TRUE.
      ELSE
        INST   = DIINST(SDDISC,NUMINS)
        CALL NMCRPO(SDARCH,NUMINS,INST  ,LARCH ) 
      ENDIF
C
C --- ARCHIVAGE FORCE
C
      IF (FORCE) THEN
        LARCH  = .TRUE.
      ENDIF
C
C --- NUMERO D'ARCHIVAGE
C
      IF (LARCH) THEN
        NUMARC = ZI(JARINF-1+1)
      ELSE
        NUMARC = - 1
      ENDIF
C
C --- NOUVEAU NUMERO D'ARCHIVAGE (POUR LE PROCHAIN APPEL)
C 
      IF (LARCH) THEN
        ZI(JARINF-1+1) =  ZI(JARINF-1+1) + 1
      ENDIF
C
C --- NUMERO DE REUSE POUR LA TABLE PARA_CALC
C
      NUMREP = ZI(JARINF-1+3)
C
      CALL JEDEMA()
C      
      END
