      INTEGER       DDLS

C ----------------------------------------------------------------------
      CALL ASSERT(N.LE.(NNOS+NNOM))

C     NOMBRE DE DDL PAR NOEUD SOMMET
      DDLS=NDIM*(1+NFH+NFE)+DDLC

C     PLACE DU PREMIER DDL DE CONTACT POUR CHAQUE N
