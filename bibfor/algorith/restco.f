C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*24 DEFICO,RESOCO
      CHARACTER*24 SAUVAP,SAUVPT,SAUVIO  
      INTEGER      IZONE,IESCL    
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT)
C
C RESTITUTION DES RESULTATS DE L'APPARIEMENT DU PASSE SUR UNE ZONE
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE DEFINITION DU CONTACT (ISSUE D'AFFE_CHAR_MECA)
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  SAUVAP : NOM VECTEUR POUR SAUVEGARDE RESOCO(1:14)//'.APPARI'
C IN  SAUVPT : NOM VECTEUR POUR SAUVEGARDE RESOCO(1:14)//'.APPOIN'
C IN  SAUVIO : NOM VECTEUR POUR SAUVEGARDE POINTEUR NDS ESCLAVES/ZONE
C IN  IZONE  : NUMERO DE LA ZONE DONT IL FAUT RESTITUER LES INFO.
C I/O IESCL  : DEBUT DU STOCKAGE DANS TABLEAU DES NOEUDS ESCLAVES
C               IN  : DEBUT DU STOCKAGE POUR LA ZONE IZONE 
C               OUT : DEBUT DU STOCKAGE POUR LA ZONE SUIVANTE 
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      K,IOLD,NESOLD,NESMAX
      INTEGER      CFMMVD,CFDISI,ZAPPA,ZREAC
      CHARACTER*24 APPARI,APPOIN,APREAC
      INTEGER      JAPPAR,JAPPTR,JREAC 
      INTEGER      JOLDAP,JOLDPT,JIOLD   
      INTEGER      IFM,NIV                      
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()      
      CALL INFDBG('CONTACT',IFM,NIV)      
C   
C --- AFFICHAGE
C   
      CALL CFIMPE(IFM,NIV,'RESTCO',1)
C
C --- LECTURE DES SD CONTACT 
C   
      APPARI = RESOCO(1:14)//'.APPARI'
      APPOIN = RESOCO(1:14)//'.APPOIN' 
      APREAC = RESOCO(1:14)//'.APREAC'        
      CALL JEVEUO(APPARI,'E',JAPPAR)
      CALL JEVEUO(APPOIN,'E',JAPPTR)
      CALL JEVEUO(APREAC,'L',JREAC)
C
C --- ACCES AUX DES OBJETS DE SAUVEGARDE
C        
      CALL JEVEUO(SAUVAP,'L',JOLDAP)
      CALL JEVEUO(SAUVPT,'L',JOLDPT)     
      CALL JEVEUO(SAUVIO,'L',JIOLD)                 
C
C --- INITIALISATIONS
C      
      ZAPPA  = CFMMVD('ZAPPA') 
      ZREAC  = CFMMVD('ZREAC')
      NESMAX = CFDISI(DEFICO,'NESMAX',IZONE)   
C
C --- INDICE DE DEBUT DE RANGEMENT DES NOEUDS ESCLAVES DE LA ZONE
C      
      IOLD = ZI(JIOLD+IZONE-1)          
C
C --- ANCIEN NOMBRE DE NDS ESCLAVES SUR LA ZONE
C
      NESOLD = ZI(JIOLD+IZONE) - IOLD
      IF ((NESOLD.LE.0).OR.
     &    (NESOLD.GT.NESMAX)) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- RECOPIE DES MORCEAUX ENCORE VALABLES DES TABLEAUX
C      
      DO 40 K = 1,NESOLD
C
C --- INDICE DANS CONTNO DU NOEUD ESCLAVE
C        
        ZI(JAPPAR+ZAPPA*(IESCL+K-1)+1) = ZI(JOLDAP+ZAPPA*(IOLD+K-1)+1)
C
C --- INDICE DANS CONTMA/CONTNO DE LA MAILLE/NOEUD MAITRE APAPRIE
C
        ZI(JAPPAR+ZAPPA*(IESCL+K-1)+2) = ZI(JOLDAP+ZAPPA*(IOLD+K-1)+2)
C
C --- INDICATEUR DE REACTUALISATION
C
        ZI(JAPPAR+ZAPPA*(IESCL+K-1)+3) = ZI(JREAC+ZREAC*(IZONE-1)+2)
C
C --- TABLEAU D'APPARIEMENT DES DDLS       
C 
        ZI(JAPPTR+IESCL+K)             = ZI(JOLDPT+IOLD+K)
 40   CONTINUE
C
C --- INCREMENTATION DU NOMBRE DE NOEUDS ESCLAVES
C
      IESCL = IESCL + NESOLD 
C
      CALL JEDEMA()
      END
