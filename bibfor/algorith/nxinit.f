C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C TOLE CRP_21
C
      IMPLICIT NONE      
      LOGICAL       LOSTAT,LEVOL,LNONL
      INTEGER       NBPASE
      CHARACTER*19  INFCHA,INFCH2,SOLVEU
      CHARACTER*19  SDDISC,SDCRIT,SDOBSE
      CHARACTER*24  MODELE,MATE  ,CARELE
      CHARACTER*24  RESULT,NUMEDD,VHYDR,COMPOR,TIME
      CHARACTER*(*) INPSCO
      CHARACTER*8   MAILLA
      REAL*8        PARA(*)
C
C ----------------------------------------------------------------------
C
C ROUTINE THER_NON_LINE (ALGORITHME)
C
C INITIALISATIONS
C
C ----------------------------------------------------------------------
C
C
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER      ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8       ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16   ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL      ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8  ZK8
      CHARACTER*16    ZK16
      CHARACTER*24        ZK24
      CHARACTER*32            ZK32
      CHARACTER*80                ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER      NEQ,IRET,NRPASE,INITPR,IAUX,JAUX,IBID
      INTEGER      NIV,IFM
      CHARACTER*14 NUPOSS
      CHARACTER*24 VTEMP,NOOJB
      REAL*8       INSTIN
      LOGICAL      LREUSE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFNIV(IFM,NIV)
C
C --- INITIALISATIONS
C
      LREUSE = .FALSE.
      LOSTAT = .FALSE.
      LNONL  = .TRUE.
      LEVOL  = .FALSE.
      TIME   = RESULT(1:8)//'.CHTPS'
      CALL DISMOI('F','NOM_MAILLA',MODELE,'MODELE',IBID,MAILLA,IBID)
C
C --- CONCEPT REENTRANT ?
C      
      CALL GCUCON(RESULT,'EVOL_THER',IRET)
      IF (IRET.GT.0) THEN
        LREUSE = .TRUE.
      ENDIF
C
C --- NUMEROTATION ET CREATION DU PROFIL DE LA MATRICE
C
      NUMEDD = '12345678.NUMED'
      NOOJB  = '12345678.00000.NUME.PRNO'
      CALL GNOMSD ( NOOJB,10,14 )
      NUMEDD = NOOJB(1:14)
      CALL RSNUME(RESULT,'TEMP',NUPOSS)
      CALL NUMERO(NUPOSS,MODELE,INFCHA,SOLVEU,'VG',NUMEDD)
C 
C --- CREATION DE LA SD POUR ARCHIVAGE DES INFORMATIONS DE CONVERGENCE
C
      CALL NTCRCV(SDCRIT)      
C
C --- CREATION DES CHAMPS
C
      DO 30  NRPASE = 0 , NBPASE
        IAUX = NRPASE
        JAUX = 4
        CALL PSNSLE(INPSCO,IAUX  ,JAUX  ,VTEMP )
        CALL VTCREB(VTEMP ,NUMEDD,'V','R',NEQ)
   30 CONTINUE
      CALL VTCREB(VHYDR ,NUMEDD,'V','R',NEQ)   
C 
C --- LECTURE ETAT INITIAL
C
      CALL NXDOET(MODELE,NUMEDD,LREUSE,LOSTAT,LNONL ,
     &            INPSCO,NBPASE,INITPR,VTEMP ,VHYDR ,
     &            INSTIN)
C
C --- CREATION SD DISCRETISATION, ARCHIVAGE ET OBSERVATION
C
      CALL TIINIT(MAILLA,MODELE,RESULT,LOSTAT,LREUSE,
     &            LNONL ,VTEMP ,INSTIN,SDDISC,SDOBSE,
     &            LEVOL  )
C
C --- CREATION DE LA SD EVOL_THER
C
      CALL NXNOLI(MODELE,MATE  ,CARELE,COMPOR,LOSTAT,
     &            LREUSE,LNONL ,LEVOL ,PARA  ,SDDISC,
     &            SDCRIT,NBPASE,INPSCO,VHYDR ,INFCH2)
      CALL COPISD('LISTE_CHARGES','G',INFCHA,INFCH2)
      CALL JEDEMA()      
C
      END
