C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE      
      INTEGER      IEVDAC
      CHARACTER*19 SDDISC
      CHARACTER*24 NOMLIS
      CHARACTER*16 METLIS
      INTEGER      INSPAS
      REAL*8       INSTAM,DELTAT,DTMIN
      LOGICAL      LDECO
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (GESTION DES EVENEMENTS - DECOUPE)
C
C CAS MANUEL - REMPLISSAGE DE LA LISTE
C      
C ----------------------------------------------------------------------
C
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  IEVDAC : INDICE DE L'EVENEMENT ACTIF
C IN  NOMLIS : NOM DE LA LISTE DES INSTANTS A AJOUTER
C IN  METLIS : METHODE DE GESTION DE LA LISTE DES INSTANTS
C IN  INSTAM : INSTANT AU DEBUT
C IN  DELTAT : INCREMENT DE TEMPS COURANT
C OUT INSPAS : NOMBRE DE PAS DE TEMPS A INSERER
C OUT DTMIN  : INTERVALLE DE TEMPS MINIMAL SUR LA LISTE CREEE
C OUT LDECO  : .FALSE. SI LA DECOUPE A ECHOUE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C 
      INTEGER      IPAS,JINST
      REAL*8       INST,INCINS
      INTEGER      NBRPAS
      REAL*8       R8BID,R8GAEM
      CHARACTER*8  K8BID
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DTMIN  = R8GAEM()
      LDECO  = .TRUE.
C
C --- NOMBRE DE PAS DANS LA SUBDIVISION
C
      CALL UTDIDT('L'   ,SDDISC,'ECHE',IEVDAC,'SUBD_PAS'   ,
     &            R8BID ,NBRPAS,K8BID )
C
C --- TAILLE DU P1S  
C     
      INCINS = DELTAT / NBRPAS
      CALL U2MESI('I','SUBDIVISE_10',1,NBRPAS)
      CALL U2MESR('I','SUBDIVISE_11',1,INCINS)
C
C --- NOMBRE D'INSTANTS A AJOUTER
C
      IF (METLIS.EQ.'AUTO') THEN
        INSPAS = 1
      ELSEIF (METLIS.EQ.'MANUEL') THEN
        INSPAS = NBRPAS - 1
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- CONSTRUCTION DE LA LISTE 
C
      CALL WKVECT(NOMLIS,'V V R',INSPAS,JINST )  
C
C --- CONSTRUCTION DE LA LISTE DES INSTANTS
C
      INST   = INSTAM
      DO 10 IPAS = 1,INSPAS
        INST             = INST + INCINS
        ZR(JINST+IPAS-1) = INST
        DTMIN            = MIN(DTMIN ,INCINS)
  10  CONTINUE
      
C
      CALL JEDEMA()
      END
