C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      INTEGER      IEVDAC
      CHARACTER*19 SDDISC
      CHARACTER*24 NOMLIS
      INTEGER      NBRPAS
      REAL*8       INSTAM,DELTAT,DTMIN
      INTEGER      RETDEC
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (GESTION DES EVENEMENTS - DECOUPE)
C
C DECOUPE DU PAS DE TEMPS - CAS MANUEL
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  IEVDAC : INDICE DE L'EVENEMENT ACTIF
C IN  NOMLIS : NOM DE LA LISTE DES INSTANTS A AJOUTER
C IN  INSTAM : INSTANT AU DEBUT
C IN  DELTAT : INCREMENT DE TEMPS COURANT
C IN  TYPDEC : TYPE DE DECOUPE
C     'SUBD' - SUBDIVISION PAR UN NOMBRE DE PAS DONNE
C     'DELT' - SUBDIVISION PAR UN INCREMENT DONNE
C OUT NBRPAS : NOMBRE DE PAS DE TEMPS
C OUT DTMIN  : INTERVALLE DE TEMPS MINIMAL SUR LA LISTE CREEE
C OUT RETDEC : CODE RETOUR DECOUPE
C     0 - ECHEC DE LA DECOUPE
C     1 - ON A DECOUPE
C     2 - PAS DE DECOUPE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      REAL*8       R8BID,R8GAEM,RATIO,DELTAC
      CHARACTER*8  K8BID
      CHARACTER*16 OPTDEC
      CHARACTER*4  TYPDEC
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DTMIN  = R8GAEM()
      RETDEC = 0
      RATIO  = 1.D0
      NBRPAS = -1
      DELTAC = -1.D0
      OPTDEC = 'UNIFORME'
      TYPDEC = 'SUBD'
C
C --- DONNEES
C
      IF (TYPDEC.EQ.'SUBD') THEN
        CALL UTDIDT('L'   ,SDDISC,'ECHE',IEVDAC,'SUBD_PAS'   ,
     &              R8BID ,NBRPAS,K8BID )
C      ELSEIF (TYPDEC.EQ.'DELT') THEN
C        CALL UTDIDT('L'   ,SDDISC,'ECHE',IEVDAC,'SUBD_INCR'  ,
C     &              DELTAC,IBID  ,K8BID )
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- CONSTRUCTION DE LA LISTE DES INSTANTS
C
      CALL NMDECC(NOMLIS,.TRUE.,OPTDEC,DELTAT,INSTAM,
     &            RATIO ,TYPDEC,NBRPAS,DELTAC,DTMIN ,
     &            RETDEC)
C
      CALL JEDEMA()
      END
