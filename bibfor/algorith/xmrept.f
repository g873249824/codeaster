      SUBROUTINE XMREPT(MODELE,NDIM,GEOM,JMAESC,ZMAIT,MMAIT,
     &                  AMAIT,NMAIT,PMAIT)

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 08/10/2007   AUTEUR NISTOR I.NISTOR 
C ======================================================================
