C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT NONE
      CHARACTER*8  MODELE
      REAL*8       GEOM(3)
      INTEGER      NDIM,JMAESC,MMAIT,AMAIT,NMAIT,PMAIT,ZMAIT
C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : XAPPAR
C ----------------------------------------------------------------------
C ROUTINE SPECIFIQUE A L'APPROCHE <<GRANDS GLISSEMENTS AVEC XFEM>>,
C TRAVAIL EFFECTUE EN COLLABORATION AVEC I.F.P.
C ----------------------------------------------------------------------
C ON CHERCHE LE POINT D'INTERSECTION MAITRE LE PLUS PROCHE DE 
C POINT DE CONTACT
C
C IN  MODELE : NOM DE MODELE
C IN  NDIM   : DIMENSION DU MODELE
C IN  GEOM   : COORDONNEES DU POINT DE CONTACT (SUR MAILLE ESCLAVE)
C IN  JMAESC : POINTEUR VERS LA LISTE DE MAILLES FISSURES
C OUT MMAIT  : LE NUMÉRO GLOBAL DE LA MAILLE MAÎTRE CONTENANT LE POINT 
C              D'INTERSECTION DE PLUS PROCHE
C OUT AMAIT  : LE NUMÉRO LOCAL DE L'ARRETE CONTENANT LE POINT 
C              D'INTERSECTION DE PLUS PROCHE
C OUT NMAIT  : LE NUMÉRO LOCAL DU NOEUD CONTENANT LE POINT 
C              D'INTERSECTION DE PLUS PROCHE
C OUT PMAIT  : LE NUMÉRO LOCAL DU POINT D'INTERSECTION DE PLUS PROCHE
C
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX ---------------
C
      INTEGER POSMA,IZONE,NBN,NTMA,IMA,IFACE,IAD,NFIS
      INTEGER JNFIS,JMOFIS,CFMMVD,ZMESX
      INTEGER JCSD1,JCSV1,JCSL1,JCSD2,JCSV2,JCSL2
      INTEGER JCOOR,INI,J,I,STATUT
      REAL*8 COORD(3),DMIN,DIST,NORMD,NORMV,R8GAEM
      CHARACTER*8 NOMFIS
      CHARACTER*19 CHS1,CHS2
      CHARACTER*24 PZONE,CONTNO,PSURNO
C
C --------------------------------------------------------------------
C
      CALL JEMARQ()
C      
      CHS1 = '&&XMREPT.CHS1'
      CHS2 = '&&XMREPT.CHS2' 

C-----     
      DMIN = R8GAEM()
      NTMA = ZI(JMAESC)
      ZMESX = CFMMVD('ZMESX')         
      DO 10 I=1,3
        COORD(I)=0.D0
  10  CONTINUE

      CALL CELCES(MODELE//'.TOPOFAC.GM','V',CHS1)
      CALL CELCES(MODELE//'.TOPOFAC.AI','V',CHS2)
      CALL JEVEUO(CHS1//'.CESD','L',JCSD1)
      CALL JEVEUO(CHS1//'.CESV','L',JCSV1)
      CALL JEVEUO(CHS1//'.CESL','L',JCSL1)
      CALL JEVEUO(CHS2//'.CESD','L',JCSD2)
      CALL JEVEUO(CHS2//'.CESV','L',JCSV2)
      CALL JEVEUO(CHS2//'.CESL','L',JCSL2)

C-----BOUCLE SUR LES MAILLES FISSURÉES---------------------------------
      DO 100 IMA=1,NTMA
        POSMA  = ZI(JMAESC+ZMESX*(IMA-1)+1)
        IZONE  = ZI(JMAESC+ZMESX*(IMA-1)+2)
        STATUT = ZI(JMAESC+ZMESX*(IMA-1)+4)
C        NBN    = ZI(JMAESC+ZMESX*(IMA-1)+3)

        IF (STATUT.EQ.-1) GO TO 100
C---ici on mettra, pour le cas 3D, une boucle sur les facettes de ct
C        IFACE=1
C-------BOUCLE SUR LES POINT D'INTERSECTION---------------------------
        DO 110 INI=1,NDIM
C---------COORDONNEES DE GEOMETRIQUES DU POINT D'INTERSECTION 	
          DO 120 J=1,NDIM
            CALL CESEXI('S',JCSD1,JCSL1,POSMA,1,1,NDIM*(INI-1)+J,IAD)
            CALL ASSERT(IAD.GT.0)
            COORD(J)=ZR(JCSV1-1+IAD)
  120     CONTINUE
C---------CALCUL DE LA DISTANCE--------------------------------------
          DIST = SQRT((COORD(1)-GEOM(1))**2+ (COORD(2)-GEOM(2))**2+
     &           (COORD(3)-GEOM(3))**2)
          IF (DIST.LT.DMIN) THEN
            DMIN=DIST
            MMAIT=POSMA
            CALL CESEXI('S',JCSD2,JCSL2,POSMA,1,1,4*(INI-1)+1,IAD)
            CALL ASSERT(IAD.GT.0)
            AMAIT=ZR(JCSV2-1+IAD)
            IF (AMAIT.EQ.0) THEN
              CALL CESEXI('S',JCSD2,JCSL2,POSMA,1,1,4*(INI-1)+2,IAD)
              CALL ASSERT(IAD.GT.0)
              NMAIT=ZR(JCSV2-1+IAD)
            ELSE
              NMAIT=0
            ENDIF

            PMAIT=INI
            ZMAIT=IZONE
          ENDIF

  110   CONTINUE

  100 CONTINUE

      CALL JEDEMA()
      END
