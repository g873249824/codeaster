C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*24      NOMLIS
      REAL*8            RVAL,EPSI
      INTEGER           NUTROU
C ----------------------------------------------------------------------
C     RECHERCHE DU NUMERO D'ORDRE D'UN REEL DANS UNE LISTE
C
C IN  NOMLIS  : LISTE DE REELS
C IN  RVAL    : VALEUR REELLE
C IN  EPSI    : TOLERANCE RELATIVE
C               SI RVAL EST NUL, ON PREND EPSI/10000 POUR TOLER. ABSOLUE
C OUT NUTROU  : NUMERO DE RVAL DANS NOMLIS
C             : 0 SI RVAL N'APPARTIENT PAS A NOMLIS
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      REAL*8   ZERO,EPS1D4
      CHARACTER*1 K1BID
      CALL JEMARQ()
        ZERO = 0.D0
        EPS1D4 = EPSI / 1.D4
        NUTROU = 0
        CALL JELIRA (NOMLIS(1:19)//'.VALE','LONUTI',NVAL,K1BID)
        CALL JEVEUO (NOMLIS(1:19)//'.VALE','L',JVALE)
        IF (RVAL .EQ. ZERO) THEN
          DO 1 N=1,NVAL
          IF (ABS (ZR(JVALE-1+N)-RVAL) .LT. EPS1D4) GO TO 3
1         CONTINUE
          GO TO 4
        ELSE
          DO 2 N=1,NVAL
          IF (ABS ((ZR(JVALE-1+N)-RVAL) / RVAL) .LT. EPSI) GO TO 3
2         CONTINUE
          GO TO 4
        ENDIF
3       CONTINUE
        NUTROU = N
        GOTO 9999
C
4       CONTINUE
        CALL UTDEBM ('F', 'RNLIR8', 'ON NE TROUVE PAS LE REEL SUIVANT'//
     &  ' DANS LA LISTE '//NOMLIS)
        CALL UTIMPR ('L','RVAL = ',1,RVAL)
        CALL UTFINM
        GOTO 9999
C FIN ------------------------------------------------------------------
 9999 CONTINUE
      CALL JEDEMA()
      END
