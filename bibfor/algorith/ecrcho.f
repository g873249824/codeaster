C               APPELANT : MDITM2
C
C-------------------   DECLARATION DES VARIABLES   ---------------------
C
C ARGUMENTS
C ---------
      INTEGER  IORDRE, NBNL
      REAL*8   OLD(9,*),
     &         DEPBUT(NBNL,3,*), VITBUT(NBNL,3,*), FORBUT(NBNL,3,*) 
C
C VARIABLES LOCALES
C -----------------
      INTEGER  IC, NBR
C
C-------------------   DEBUT DU CODE EXECUTABLE    ---------------------
C
      NBR = IORDRE + 1
C
C  1. FORCES DE CONTACT
C     -----------------
      DO 10 IC = 1, NBNL
         FORBUT(IC,1,NBR) = OLD(8,IC)
         FORBUT(IC,2,NBR) = OLD(3,IC)
         FORBUT(IC,3,NBR) = OLD(4,IC)
  10  CONTINUE
C
C  2. DEPLACEMENTS DES NOEUDS DE CHOC
C     -------------------------------
      DO 20 IC = 1, NBNL
         DEPBUT(IC,1,NBR) = OLD(5,IC)
         DEPBUT(IC,2,NBR) = OLD(6,IC)
         DEPBUT(IC,3,NBR) = OLD(7,IC)
  20  CONTINUE
C
C  3. VITESSES DES NOEUDS DE CHOC
C     ---------------------------
      DO 30 IC = 1, NBNL
         VITBUT(IC,1,NBR) = OLD(9,IC)
         VITBUT(IC,2,NBR) = OLD(1,IC)
         VITBUT(IC,3,NBR) = OLD(2,IC)
  30  CONTINUE
C
C --- FIN DE ECRCHO.
      END
