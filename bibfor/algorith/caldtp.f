      INTEGER  NNO
      REAL*8 TPM(*),TPP(*)
      REAL*8 DTP(*),DDT(*),VMAX0,VMAX1
C ......................................................................
C    IN TPM     : TEMPERATURE INSTANT -
C    IN TPP     : TEMPERATURE INSTANT +
C    IN NNO     : NOMBRE DE NOEUDS
C    OUT DDT    : DT ENTRE TEMPS- ET +
C    IN/OUT DTP : INTEGRALE DT DEPUIS DERNIERE INSERTION
C    VMAX0      : MAX DE DDT SUR TOUS LES NOEUDS
C    VMAX1      : MAX DE DTP SUR TOUS LES NOEUDS
C ......................................................................

      INTEGER  KP

      VMAX0 =0.D0
      VMAX1=0.D0
      DO 10 KP = 1,NNO
       DDT(KP)=ABS(TPP(KP)-TPM(KP))
       DTP(KP)=DTP(KP)+ABS(TPP(KP)-TPM(KP))
       VMAX0=MAX(VMAX0,DDT(KP))
       VMAX1=MAX(VMAX1,DTP(KP))
 10   CONTINUE

      END
