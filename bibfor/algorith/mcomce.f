      IMPLICIT NONE
      CHARACTER*8  NOMA
      CHARACTER*19 NEWGEO
      INTEGER      NUMMAI
      REAL*8       COOR(27)
      CHARACTER*8  ALIAS
      INTEGER      NNO
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT - UTILITAIRE)
C
C CALCUL DES COORDONNEES DES NOEUDS D'UNE MAILLE
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  NEWGEO : COORDONNES DE TOUS LES NOEUDS
C IN  NUMMAI : NUMERO ABSOLU DE LA MAILLE DANS LE MAILLAGE
C OUT COOR   : COORDONNEES DES NOEUDS DE LA MAILLE
C OUT ALIAS  : TYPE DE LA MAILLE
C OUT NNO    : NOMBRE DE NOEUDS DE LA MAILLE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNUM
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      JCOOR,JDES
      INTEGER      INO,NO(9),I,IBID
C
C-----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      CALL JEVEUO(NEWGEO(1:19)//'.VALE','L',JCOOR)
      CALL JEVEUO(JEXNUM(NOMA//'.CONNEX',NUMMAI),'L',JDES)
C
C --- INITIALISATIONS
C
      DO 10 I = 1,27
        COOR(I) = 0.D0
   10 CONTINUE
C
C --- INFOS SUR LA MAILLE
C
      CALL MMELTY(NOMA,NUMMAI,ALIAS,NNO,IBID)
C
C --- NUMEROS ABSOLUS DES NOEUDS DE LA MAILLE
C
      DO 23 INO = 1,NNO
        NO(INO) = ZI(JDES+INO-1)
   23 CONTINUE
C
C --- COORDONNEES DES NOEUDS DE LA MAILLE
C
      DO 20 INO = 1,NNO
        COOR(3*(INO-1)+1) = ZR(JCOOR+3*(NO(INO)-1))
        COOR(3*(INO-1)+2) = ZR(JCOOR+3*(NO(INO)-1)+1)
        COOR(3*(INO-1)+3) = ZR(JCOOR+3*(NO(INO)-1)+2)
   20 CONTINUE
C
      CALL JEDEMA()
      END
