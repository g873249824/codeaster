      IMPLICIT NONE
      INTEGER       NUMINS
      CHARACTER*19  PARTPS
      CHARACTER*(*) NOMCHZ
      CHARACTER*16  NOMCHA
      LOGICAL       FORCE

C ----------------------------------------------------------------------
C        SD DISCRETISATION :   INDIQUE S'IL FAUT ARCHIVER UN CHAMP
C ----------------------------------------------------------------------
C
C  IN PARTPS K19 : SD DISCRETISATION
C  IN NUMINS  I  : NUMERO D'INSTANT
C  IN NOMCHA K16 : NOM DU CHAMP
C  IN FORCE   L  : INDIQUE SI ON FORCE L'ARCHIVAGE 
C                  POUR LES CHAMPS EXCLUS
C OUT DIINCL  L  : VRAI SI LE CHAMP DOIT ETRE SAUVE
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------

      LOGICAL     DIDERN
      INTEGER     IRET, JEXCL, NB, I
      CHARACTER*8 K8BID


      CALL JEMARQ()
      NOMCHA = NOMCHZ

C    DERNIER PAS : ON ARCHIVE TOUS LES CHAMPS
      IF (DIDERN(PARTPS, NUMINS)) THEN
        DIINCL = .TRUE.
        GOTO 9999
      END IF

C    AUCUN CHAMP N'EST EXCLU
      CALL JEEXIN(PARTPS // '.DIEK',IRET)
      IF (IRET.EQ.0) THEN
        DIINCL = .TRUE.
        GOTO 9999
      END IF
      
      DIINCL = .FALSE.
      CALL JEVEUO(PARTPS // '.DIEK','L',JEXCL)
      CALL JELIRA(PARTPS // '.DIEK','LONMAX',NB,K8BID)
      DO 10 I = 1,NB
        IF (NOMCHA .EQ. ZK16(JEXCL-1 + I)) GOTO 9999
 10   CONTINUE
      DIINCL = .TRUE.

 9999 CONTINUE
C
C     ON STOCKE LES CHAMPS EXCLUS SI ON FORCE L'ARCHIVAGE
      IF(.NOT.DIINCL .AND. FORCE)DIINCL = .TRUE.

      CALL JEDEMA()
      END
