      IMPLICIT NONE
C-----------------------------------------------------------------------
C           OPERATEUR    CALC_POINT_MAT STOCKAGE DANS LA TBLE RESULTAT
C-----------------------------------------------------------------------
      INTEGER      NBVARI,NBPAR,I,ITER,NBVITA,IGRAD,NCMP
      CHARACTER*8  K8B,TABLE
      CHARACTER*16 NOMPAR(*)
      REAL*8       DEPS(9),SIGM(6),SIGP(6),EPSP(9),EPSM(9)
      REAL*8       VIM(*),VIP(*),VR(*),EQUI(16),VALIMP(9)
      REAL*8       RAC2,INSTAM,INSTAP
      COMPLEX*16 CBID

      RAC2=SQRT(2.D0)
      IF (IGRAD.NE.0) THEN
         NCMP=9
      ELSE
         NCMP=6
      ENDIF

C     STOCKAGE DE LA SOLUTION DANS LA TABLE
      IF (NCMP.EQ.6) THEN
         CALL DCOPY(NCMP,EPSM,1,EPSP,1)
         CALL DAXPY(NCMP,1.D0,DEPS,1,EPSP,1)
         CALL DCOPY(NCMP,EPSP,1,EPSM,1)
      ELSE
         CALL DCOPY(NCMP,VALIMP,1,EPSP,1)
         CALL DCOPY(NCMP,VALIMP,1,EPSM,1)      
      ENDIF
      CALL DCOPY(NCMP,EPSP,1,VR(2),1)
      IF (IGRAD.EQ.0) CALL DSCAL(3,1.D0/RAC2,VR(5),1)

      CALL DCOPY(6,SIGP,1,SIGM,1)
      CALL DCOPY(6,SIGP,1,VR(NCMP+2),1)
      CALL DSCAL(3,1.D0/RAC2,VR(NCMP+5),1)
      
      CALL FGEQUI ( VR(NCMP+2), 'SIGM_DIR', 3, EQUI)
      VR(NCMP+8)=EQUI(16)
      VR(NCMP+9)=EQUI(1)
      
      CALL DCOPY(NBVARI,VIP,1,VIM,1)
      CALL DCOPY(NBVITA,VIP,1,VR(1+NCMP+6+2+1),1)
      
      INSTAM=INSTAP
      VR(1)=INSTAP
      VR(NBPAR)=ITER
      
      CALL TBAJLI(TABLE,NBPAR,NOMPAR,I,VR,CBID,K8B,0)

      END
