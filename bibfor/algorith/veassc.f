C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT      NONE
      CHARACTER*19  LISCHA
      CHARACTER*19  VECELE
C
C ----------------------------------------------------------------------
C
C CALCUL DES VECTEURS ELEMENTAIRES DES CHARGEMENTS MECANIQUES
C
C VECT_ASSE_CHAR
C
C ----------------------------------------------------------------------
C
C
C IN  LISCHA : SD LISTE DES CHARGES
C OUT VECELE : VECT_ELEM RESULTAT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      ICHAR,NBCHAR,IBID
      CHARACTER*8  NEWNOM
      CHARACTER*19 CHAMNO,LCHOUT
      CHARACTER*13 PREFOB
      INTEGER      CODCHA
      LOGICAL      LISICO,LVEAC
      INTEGER      NBVEAC,LISNBG,IEXIS
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NEWNOM = '.0000000'
C
C --- NOMBRE DE CHARGES
C
      CALL LISNNB(LISCHA,NBCHAR)
C
C --- NOMBRE DE CHARGES DE TYPE VECT_ASSE_CHAR
C
      NBVEAC = LISNBG(LISCHA,'VECT_ASSE_CHAR')
      IF (NBVEAC.EQ.0) GOTO 99
C
C --- BOUCLE SUR LES CHARGES
C
      DO 10 ICHAR = 1,NBCHAR
C
C ----- CODE DU GENRE DE LA CHARGE
C
        CALL LISLCO(LISCHA,ICHAR ,CODCHA)
        LVEAC  = LISICO('VECT_ASSE_CHAR',CODCHA)
        IF (LVEAC) THEN
C
C ------- NOM DU CHAM_NO
C
          CALL LISLLC(LISCHA,ICHAR ,PREFOB)
          CALL LISCVA(PREFOB,CHAMNO)
C
C ------- NOM DU CHAMP
C
          CALL EXISD('CHAMP_GD',CHAMNO,IEXIS)
          CALL ASSERT(IEXIS.GT.0)
C
C ------- ON RECOPIE SIMPLEMENT LE CHAMP DANS VECT_ELEM
C
          CALL GCNCO2(NEWNOM)
          LCHOUT = '&&VEASSE.'//NEWNOM(2:8)
          CALL CORICH('E',LCHOUT,ICHAR,IBID)
          CALL COPISD('CHAMP_GD','V',CHAMNO,LCHOUT)
          CALL REAJRE(VECELE,LCHOUT,'V')
        ENDIF
   10 CONTINUE
C
  99  CONTINUE
C
      CALL JEDEMA()
      END
