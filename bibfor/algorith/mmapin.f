C
C ROUTINE CONTACT (METHODE CONTINUE)
C
C APPARIEMENT INITIAL
C
C ----------------------------------------------------------------------
C
C
C IN  MODELE : NOM DU MODELE
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  SDDYNA : SD DEDIEE A LA DYNAMIQUE
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  SDTIME : SD TIMER
C IN  SDSTAT : SD STATISTIQUES
C IN  SDIMPR : SD AFFICHAGE
C IN  NUMEDD : NOM DU NUME_DDL
C IN  VALINC : VARIABLE CHAPEAU POUR INCREMENTS VARIABLES
C
C --------------- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------
C

      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      LOGICAL      CFDISL,LCTCC,LTFCM,LALLV
      INTEGER      MMITGO
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- FONCTIONNALITES ACTIVES
C
      LCTCC  = CFDISL(DEFICO,'FORMUL_CONTINUE')
      LTFCM  = CFDISL(DEFICO,'CONT_XFEM_GG')
      LALLV  = CFDISL(DEFICO,'ALL_VERIF')
C
C --- REALISATION DE L'APPARIEMENT
C
      IF (.NOT.LALLV.AND.(LCTCC.OR.LTFCM)) THEN
        CALL MMBOUC(RESOCO,'GEOM','INIT',MMITGO)
        CALL NMCTCG(MODELE,NOMA  ,DEFICO,RESOCO,SDDISC,
     &              SDSTAT,SDTIME,SDIMPR,NUMEDD)
        CALL MMBOUC(RESOCO,'GEOM','INCR',MMITGO)
      ENDIF
C
      CALL JEDEMA()
      END
