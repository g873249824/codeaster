      CHARACTER*8  NOMA
      CHARACTER*24 DEFICO,RESOCO
      CHARACTER*24 SDSTAT,SDTIME,SDIMPR
      CHARACTER*19 SDDISC
      CHARACTER*24 MODELE,NUMEDD
C
