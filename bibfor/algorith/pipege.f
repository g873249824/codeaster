C ----------------------------------------------------------------------
C     LOI DE COMPORTEMENT ELASTIQUE FRAGILE (EN DELOCALISE)
C
C IN  NDIM    : DIMENSION DE L'ESPACE
C IN  TYPMOD  : TYPE DE MODELISATION
C IN  IMATE   : NATURE DU MATERIAU
C IN  EPSM    : DEFORMATION EN T-
C OUT A0      : LINEARISATION DU CRITERE : FEL = A0 + A1*ETA
C OUT A1      : IDEM A0
C ----------------------------------------------------------------------

      LOGICAL     CPLAN
      INTEGER     NDIMSI, K, NRAC
      REAL*8      TREPSP, TREPSD, COPLAN, SIGELP(6), SIGELD(6)
      REAL*8      EPS1(6), EPS2(6), D1, D2
      REAL*8      FPD, D, P0, P1, P2, ETA, RAC(2)
      REAL*8      E, NU, GAMMA, SEUIL, SEUREL
      REAL*8      R8NRM2,RTEMP

      CHARACTER*2 CODRET(2)
      CHARACTER*8 NOMRES(2)
      REAL*8      VALRES(2)

      REAL*8      R8DOT



C -- OPTION ET MODELISATION

      CPLAN = (TYPMOD(1).EQ.'C_PLAN  ')
      NDIMSI = 2*NDIM

C -- CALCUL DES DEFORMATIONS EN PRESENCE DE CONTRAINTES PLANES
C
      IF (CPLAN) THEN
        EPSM(3)  = 0.D0
        EPSP(3)  = 0.D0
        EPSD(3)  = 0.D0
      END IF

      RTEMP=R8NRM2(NDIMSI,EPSM,1)
      RTEMP=RTEMP
      
      IF (RTEMP.GT.0.D0) THEN
        A0=R8DOT(NDIMSI,EPSM,1,EPSP,1)/RTEMP-RTEMP
        A1=R8DOT(NDIMSI,EPSM,1,EPSD,1)/RTEMP
      ELSE
        A0=0.D0
        A1=0.D0
      ENDIF


 9999 CONTINUE
      END
