       INTEGER       SPLIAI
       INTEGER       INDFAC
       INTEGER       LMAT
       REAL*8        XJVMAX
       CHARACTER*24  DEFICU
       CHARACTER*24  RESOCU
       CHARACTER*24  CINE
C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : ALGOCU
C ----------------------------------------------------------------------
C
C  ROUTINE MERE POUR LE CALCUL DE ACM1AT 
C  PERMETTANT DE POINTER SUR LES ROUTINES DE CALCUL ADAPTE 
C  SELON QUE L'ON AJOUTE OU RETIRE DES LIAISONS 
C
C IN  DEFICU : SD DE DEFINITION
C IN  RESOCU : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  LMAT   : DESCRIPTEUR DE LA MATR_ASSE DU SYSTEME MECANIQUE
C IN  CINE   : CHAM_NO CINEMATIQUE
C IN  NBLIAC : NOMBRE DE LIAISONS ACTIVES
C I/O AJLIAI : INDICE DANS LA LISTE DES LIAISONS ACTIVES DE LA DERNIERE
C              LIAISON CORRECTE DU CALCUL 
C              DE LA MATRICE DE CONTACT ACM1AT
C I/O XJVMAX : VALEUR DU PIVOT MAX
C I/O SPLIAI : INDICE DANS LA LISTE DES LIAISONS ACTIVES DE LA DERNIERE
C              LIAISON AYANT ETE CALCULEE POUR LE VECTEUR CM1A
C I/O INDFAC : INDICE DE DEBUT DE LA FACTORISATION
C I/O INDIC  : +1 ON A RAJOUTE UNE LIAISON 
C              -1 ON A ENLEVE UNE LIAISON  
C ======================================================================
      CALL JEMARQ()
C ----------------------------------------------------------------------
      IF (INDIC.NE.-1) THEN
        CALL CUACA1(DEFICU,RESOCU,LMAT,CINE,NBLIAC,
     &              AJLIAI)
      ENDIF
      CALL CUACA2(DEFICU,RESOCU,NBLIAC,SPLIAI,INDFAC,LMAT,XJVMAX)
      INDIC = 1    
C ----------------------------------------------------------------------
      CALL JEDEMA ()
      END
