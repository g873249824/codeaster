      REAL*8       R8BID
      INTEGER      IBID,NECHEC,IECHEC
      CHARACTER*8  K8BID
      LOGICAL      LACTI
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- NOMBRE D'EVENT-DRIVEN : NECHEC
C
      CALL UTDIDT('L'   ,SDDISC,'LIST',IBID  ,'NECHEC',
     &            R8BID ,NECHEC,K8BID )
      LACTI = .FALSE.
C
C --- BOUCLE SUR LES EVENT-DRIVEN
C --- DES QU'UN EVENT-DRIVEN EST SATISFAIT, ON SORT
C --- ON NE CHERCHE PAS A VERIFIER LES AUTRES EVENEMENTS
C --- ATTENTION, L'ORDRE D'EVALUATION A DONC UNE IMPORTANCE !
C
      IEVDAC = 0
      DO 100 IECHEC = 1,NECHEC
        CALL DIEVEN(SDDISC,IECHEC,LACTI )
        IF (LACTI) THEN
          IEVDAC = IECHEC
          GOTO 888
        ENDIF
 100  CONTINUE
C
 888  CONTINUE
C
      CALL JEDEMA()
      END
