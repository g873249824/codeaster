C
C ROUTINE UTILITAIRE (AFFICHAGE - TABLEAU)
C
C AFFECTATION DES VALEURS POUR LA COLONNE DANS UN TABLEAU
C
C ----------------------------------------------------------------------
C
C
C IN SDCOLO : SD COLONNE DU TABLEAU
C IN VALK   : VALEUR DE TYPE K16 POUR LES COLONNES DE TYPE CHAINE
C IN VALR   : VALEUR DE TYPE REAL POUR LES COLONNES DE TYPE REEL
C IN VALI   : VALEUR DE TYPE ENTIER POUR LES COLONNES DE TYPE ENTIER
C
C ----------------------------------------------------------------------
C
      LOGICAL      LINTE,LREEL,LCHAI
C
C ----------------------------------------------------------------------
C

C
C --- TYPE DE LA VALEUR DANS LA COLONNE
C
      CALL OBGETB(SDCOLO,'ENTIER',LINTE)
      CALL OBGETB(SDCOLO,'REEL'  ,LREEL)
      CALL OBGETB(SDCOLO,'CHAINE',LCHAI)
C
C --- AFFECTATION DE LA VALEUR
C
      IF (LINTE) THEN
        CALL OBSETI(SDCOLO,'VALE_I',VALI)
      ELSE IF (LREEL) THEN
        CALL OBSETR(SDCOLO,'VALE_R',VALR)
      ELSE IF (LCHAI) THEN
        CALL OBSETK(SDCOLO,'VALE_K',VALK)
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C

      END
