      subroutine MMCMEM(MODELE,DEPMOI,DEPDEL,VITMOI,ACCMOI,
     &                  LIGREL,CARCON,MCONEL,SCONEL)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 06/09/2005   AUTEUR TORKHANI M.TORKHANI 
C ======================================================================
