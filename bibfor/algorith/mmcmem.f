C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      implicit none
      character*8 MCONEL
      character*8 SCONEL
      character*19 CARCON,LIGREL
      character*24 DEPMOI,DEPDEL,MODELE,ACCMOI,VITMOI
C ----------------------------------------------------------------------
C     CALCUL DES MATRICES ELEMENTAIRES DES ELEMENTS DE CONTACT
C              ET DES SECONDS MEMBRES DE TYPE CONTACT FROTTANT
C
C IN/JXIN  LIGREL : NOM DE LIGREL (DES ELEMENTS DE CONTACT MALEK)
C IN/JXIN  CARCON  : NOM DE LA CARTE CONTENANT LES INFORMATIONS
C DE CONTACT
C IN  TYPCAL : CONTACT /FROTTEMENT /CONTFROT
C
C IN/JXIN  DEPMOI : DEPLACEMENT A L'INSTANT PRECEDENT
C IN/JXIN  DEPDEL : DEPLACEMENT COURANT A L'ITERATION DE NEWTON
C IN/JXOUT MCONEL : MATR_ELEM : MATRICES ELEMENTAIRES
C IN/JXOUT SCONEL : VECT_ELEM : S M ELEMENTAIRES
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      character*32 JEXNUM,JEXNOM,JEXR8,JEXATR
      integer ZI
      common /IVARJE/ ZI(1)
      real*8 ZR
      common /RVARJE/ ZR(1)
      complex*16 ZC
      common /CVARJE/ ZC(1)
      logical ZL,EXIGEO
      common /LVARJE/ ZL(1)
      character*8 ZK8
      character*16 ZK16
      character*24 ZK24
      character*32 ZK32
      character*80 ZK80
      common /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      character*8 NOMCHA,LPAIN(6),LPAOUT,MATEL,K8BID,VECCON,VECFRO
      character*19 CHGEOM,LCHIN(6),LCHOUT
      integer IRET,JLIRES,JLICON,JLIFRO
C
      call JEMARQ
      VECCON = '&&MMCCON'
      VECFRO = '&&MMCFRO'
C
C
C     0.1 : ON DETRUIT MCONEL ET SCONEL S'ILS EXISTENT
C     ------------------------------------------------
      call DETRSD('MATR_ELEM',MCONEL)
      call DETRSD('VECT_ELEM',SCONEL)
      call DETRSD('VECT_ELEM',VECCON)
      call DETRSD('VECT_ELEM',VECFRO)
C
C
C     0.2 : RECUPERATION DE LA GEOMETRIE
C
      call MEGEOM(MODELE,' ',EXIGEO,CHGEOM)
C
C CREATION DES LISTES DES CHAMPS IN ET OUT
C
C LA GEOMETRIE,LES DEPLACEMENTS
C
      LPAIN(1) = 'PGEOMER'
      LCHIN(1) = CHGEOM
      LPAIN(2) = 'PDEPL_M'
      LCHIN(2) = DEPMOI
      LPAIN(3) = 'PDEPL_P'
      LCHIN(3) = DEPDEL
      LPAIN(4) = 'PCONFR'
      LCHIN(4) = CARCON
      LPAIN(5) = 'PDEPLAR'
      LCHIN(5) = VITMOI
      LPAIN(6) = 'PCHDYNR'
      LCHIN(6) = ACCMOI
C
C     1. CALCUL DE MCONEL :
C     --------------------
      LPAOUT = 'PMATUUR'
C
      call MEMARE('V',MCONEL(1:8),MODELE,' ',' ','RIGI_MECA')
      call WKVECT(MCONEL(1:8)//'.LISTE_RESU','V V K24',2,JLIRES)
C
C
      LCHOUT = MCONEL(1:8) // '.ME001'
      call CALCUL('S','RIGI_CONT',LIGREL,6,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &           'V')
      ZK24(JLIRES-1+1) = LCHOUT
      LCHOUT = MCONEL(1:8) // '.ME002'
      call CALCUL('S','RIGI_FROT',LIGREL,6,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &           'V')
      ZK24(JLIRES-1+2) = LCHOUT
      call JEECRA(MCONEL(1:8)//'.LISTE_RESU','LONUTI',2,K8BID)
C
C
C
C      2. CALCUL DE SCONEL :
C      -----------------------
      LPAOUT = 'PVECTUR'
C
      call MEMARE('V',SCONEL,MODELE,' ',' ','CHAR_MECA')
      call WKVECT(SCONEL//'.LISTE_RESU','V V K24',2,JLIRES)
C
      call MEMARE('V',VECCON,MODELE,' ',' ','CHAR_MECA')
      call WKVECT(VECCON//'.LISTE_RESU','V V K24',1,JLICON)
C
      call MEMARE('V',VECFRO,MODELE,' ',' ','CHAR_MECA')
      call WKVECT(VECFRO//'.LISTE_RESU','V V K24',1,JLIFRO)
C
      LCHOUT = SCONEL // '.VE001'
      call CALCUL('S','CHAR_MECA_CONT',LIGREL,6,LCHIN,LPAIN,1,LCHOUT,
     &           LPAOUT,'V')
      ZK24(JLIRES-1+1) = LCHOUT
      ZK24(JLICON) = LCHOUT
      LCHOUT = SCONEL // '.VE002'
      call CALCUL('S','CHAR_MECA_FROT',LIGREL,6,LCHIN,LPAIN,1,LCHOUT,
     &           LPAOUT,'V')
      ZK24(JLIRES-1+2) = LCHOUT
      ZK24(JLIFRO) = LCHOUT
      call JEECRA(SCONEL//'.LISTE_RESU','LONUTI',2,K8BID)
      call JEECRA(VECCON//'.LISTE_RESU','LONUTI',1,K8BID)
      call JEECRA(VECFRO//'.LISTE_RESU','LONUTI',1,K8BID)
C
C
 10   continue
C FIN ------------------------------------------------------------------
C
      call JEDEMA
      end
