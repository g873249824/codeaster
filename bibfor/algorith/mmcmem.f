        OPTION = 'RIGI_CONT'
      ELSEIF (PHASE.EQ.'FROT') THEN
        OPTION = 'RIGI_FROT'      
      ELSE    
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- CALCUL DU MATR_ELEM
C
      CALL MMCMAT(OPTION,
     &            MODELE,RESOCO,DEPMOI,DEPDEL,VITMOI,
     &            VITPLU,ACCMOI,MECTCE)
C
      END
