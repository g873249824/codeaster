      REAL*8 MS(6)
      INTEGER NBFSYS,I,IFA,NBSYS,IS
C     ----------------------------------------------------------------

C         CALCUl DES TENSEURS MS POUR GAGNER DU TEMPS
      NBFSYS=NBCOMM(NMAT,2)
      IF (NBFSYS.GT.5) THEN
         CALL UTMESS('F','CALCMS','TROP DE FAMILLES DE SYSTEMES'//
     &       ' DE GLISSEMENT. AUGMENTER LA LIMITE ACTUELLE (5)')
      ENDIF
      DO 2 IFA=1,NBFSYS            
         NOMFAM=CPMONO(5*(IFA-1)+1)
         CALL LCMMSG(NOMFAM,NBSYS,0,PGL,MS)         
         DO 3 IS=1,NBSYS
            CALL LCMMSG(NOMFAM,NBSYS,IS,PGL,MS)
            DO 4 I=1,6
               TOUTMS(IFA,IS,I)=MS(I)
 4          CONTINUE
 3       CONTINUE
 2    CONTINUE

      END
