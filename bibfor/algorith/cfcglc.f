C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT     NONE
      CHARACTER*24 RESOCO
      INTEGER      ILIAI,JDECAL
      INTEGER      NDIM,NEQ,NESMAX,NBDDL
      INTEGER      JAPCOF,JAPDDL,JDEPC
      REAL*8       GLIS
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (RESOLUTION)
C
C CALCUL DU GLISSEMENT - CAS DUALISE: JEU CORRIGE
C
C ----------------------------------------------------------------------
C 
C
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  ILIAI  : INDICE DE LA LIAISON COURANTE
C OUT GLIS   : GLISSEMENT TANGENT
C
C --------------- DEBUT DECLARATIONS NORMALISEES JEVEUX ---------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24 JEUITE,JEUX
      INTEGER      JJEUIT,JJEUX
      REAL*8       JEXOLD,JEYOLD,JEXINC,JEYINC,JEXNEW,JEYNEW
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- LECTURE DES STRUCTURES DE DONNEES DE CONTACT
C
      JEUITE = RESOCO(1:14)//'.JEUITE'
      JEUX   = RESOCO(1:14)//'.JEUX'
      CALL JEVEUO(JEUITE,'L',JJEUIT)
      CALL JEVEUO(JEUX  ,'L',JJEUX )
C
C --- JEUX TANGENTS AVANT ITERATION DE NEWTON
C
      JEXOLD = ZR(JJEUIT+3*(ILIAI-1)+2-1)
      JEYOLD = ZR(JJEUIT+3*(ILIAI-1)+3-1) 
C                                 
C --- INCR. CORRIGE DES JEUX TANGENTS
C
      JEXINC = 0.D0
      JEYINC = 0.D0
      CALL CALADU(NEQ,NBDDL,ZR(JAPCOF+JDECAL),
     &            ZI(JAPDDL+JDECAL),ZR(JDEPC),JEXINC)
      IF (NDIM.EQ.3) THEN
        CALL CALADU(NEQ,NBDDL,ZR(JAPCOF+JDECAL+30*NESMAX),
     &              ZI(JAPDDL+JDECAL),ZR(JDEPC),JEYINC)
      ENDIF
C                                 
C --- CALCUL DES JEUX TANGENTS
C
      JEXNEW = JEXOLD - JEXINC
      JEYNEW = JEYOLD - JEYINC
      GLIS   = SQRT(JEXNEW**2+JEYNEW**2)
C
      CALL JEDEMA()
C
      END
