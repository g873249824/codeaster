C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER      JINST, N1, NBARCH, NBOCC
      CHARACTER*8  K8B
C DEB ------------------------------------------------------------------
      CALL JEMARQ()
        CALL GETVID('INCREMENT','LIST_INST',1,1,1,LISINS,N1)
        CALL JEVEUO(LISINS(1:19)//'.VALE','L',JINST)
        CALL JELIRA(LISINS(1:19)//'.VALE','LONUTI',NOINS2,K8B)
C
C     --- ARCHIVAGE ---
C      LISARC = '&&NDDOIN.ARCHIVAGE'
      CALL DYARCH ( NOINS2, LISINS, LISARC, NBARCH, 1, NBEXCL, CHEXCL )
C      CALL JEVEUO(LISARC,'E',JARCH)
C
C *** ALGORITHME D'INTEGRATION ET PARAMETRES
C
      CALL GETFAC('NEWMARK',NBOCC)
      IF (NBOCC.EQ.1) THEN
         IALGO = 1
         CALL GETVR8('NEWMARK','ALPHA',1,1,1,ALPHA,N1)
         CALL GETVR8('NEWMARK','DELTA',1,1,1,DELTA,N1)
C        IF ( DELTA.LT.(0.5-TOL) .OR. ALPHA.LT.(RES-TOL) ) THEN

C    &                              'CAS CONDITIONNELLEMENT STABLE.')
C        ENDIF
      ELSE
         IALGO = 2
         CALL GETVR8('HHT'    ,'ALPHA',1,1,1,ALPHA,N1)
      ENDIF
C
      CALL JEDEMA()
      END
