C
C ROUTINE MECA_NON_LINE (UTILITAIRE)
C
C NUMERO COURANT D'ARCHIVAGE ET INCREMENTATION
C
C ----------------------------------------------------------------------
C
C
C I/O SDDISC : SD DISCRETISATION
C                 OUT : INCREMENTATION DU NUMERO D'ARCHIVAGE QUAND IL
C                         FAUT ARCHIVER
C IN NUMINS  : NUMERO DE L'INSTANT
C IN FORCE   : VRAI SI ON SOUHAITE FORCER L'ARCHIVAGE
C IN INCR    : VRAI S'IL FAUT INCREMENTER LE COMPTEUR D'ARCHIVAGE
C OUT DIARCH : NUMERO COURANT D'ARCHIVAGE
C                 = -1 S'IL N'Y PAS LIEU D'ARCHIVER CE PAS DE TEMPS
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      JARCH ,JINFO
      CHARACTER*24 TPSDIA,TPSDII
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES SD LISTE D'INSTANTS
C    
      TPSDII = SDDISC(1:19)//'.DIIR'
      TPSDIA = SDDISC(1:19)//'.DIAL'
      CALL JEVEUO(TPSDIA,'L',JARCH)
      CALL JEVEUO(TPSDII,'E',JINFO)
C      
      IF (ZL(JARCH+NUMINS) .OR. FORCE) THEN

C      IL FAUT ARCHIVER LE PAS DE TEMPS
        
        DIARCH = NINT(ZR(JINFO))

C      ON INCREMENTE LE NUMERO POUR UN PROCHAIN ARCHIVAGE
C      SI BESOIN EST
        IF (INCR) ZR(JINFO) = ZR(JINFO) + 1


      ELSE

C      PAS D'ARCHIVAGE DE CE PAS
        DIARCH = -1

      END IF

      CALL JEDEMA()
      END
