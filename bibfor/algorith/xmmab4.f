     &                  MU,SINGU,RR,COEFBU,DDLS,DDLM,
     &                  MMAT)

      IMPLICIT NONE
      INTEGER     NDIM,NNO,NNOS
      INTEGER     NFH,DDLS,DDLM
      INTEGER     SINGU
      REAL*8      MMAT(204,204),PTKNP(3,3)
      REAL*8      FFP(27),JAC
      REAL*8      RR,SEUIL,MU,COEFBU

