C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*(*) SDIMPZ
      INTEGER       ICOL
      CHARACTER*16  VALK
      REAL*8        VALR
      INTEGER       VALI
      CHARACTER*1   MARQ
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (AFFICHAGE - ACCES SD)
C
C LECTURE DES VALEURS POUR LA COLONNE
C
C ----------------------------------------------------------------------
C
C
C SI LA COLONNE N'EXISTE PAS POUR L'AFFICHAGE COURANT: ON IGNORE
C
C IN  SDIMPR : SD SUR L'AFFICHAGE DES COLONNES
C IN  ICOL   : NUMERO DE LA COLONNE
C OUT VALK   : VALEUR DE TYPE K16 POUR LES COLONNES DE TYPE CHAINE
C OUT VALR   : VALEUR DE TYPE REAL POUR LES COLONNES DE TYPE REEL
C OUT VALI   : VALEUR DE TYPE ENTIER POUR LES COLONNES DE TYPE ENTIER
C OUT MARQ   : VALEUR DE LA MARQUE EVENTUELLE
C
C
C
C
      INTEGER      JIMPCO,JIMPTY,JIMPIN,JIMPMA
      CHARACTER*24 IMPCOL,IMPTYP,IMPINF,IMPMAR
      INTEGER      JIMPRR,JIMPRK,JIMPRI
      CHARACTER*24 IMPRER,IMPREK,IMPREI
      INTEGER      NBCOL,FORCOL
      CHARACTER*14 SDIMPR
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C ---
C
      SDIMPR = SDIMPZ
      IMPINF = SDIMPR(1:14)//'.INFO'
      IMPTYP = SDIMPR(1:14)//'.DEFI.TYP'
      IMPCOL = SDIMPR(1:14)//'.DEFI.COL'
      IMPRER = SDIMPR(1:14)//'.DEFI.RER'
      IMPREI = SDIMPR(1:14)//'.DEFI.REI'
      IMPREK = SDIMPR(1:14)//'.DEFI.REK'
      IMPMAR = SDIMPR(1:14)//'.DEFI.MAR'

      CALL JEVEUO(IMPINF,'L',JIMPIN)
      CALL JEVEUO(IMPTYP,'L',JIMPTY)
      CALL JEVEUO(IMPCOL,'L',JIMPCO)
      CALL JEVEUO(IMPRER,'L',JIMPRR)
      CALL JEVEUO(IMPREI,'L',JIMPRI)
      CALL JEVEUO(IMPREK,'L',JIMPRK)
      CALL JEVEUO(IMPMAR,'L',JIMPMA)
C
C --- NOMBRE DE COLONNES
C
      NBCOL = ZI(JIMPIN-1+1)
      IF ((ICOL.LE.0).OR.(ICOL.GT.NBCOL)) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- RECUP DU FORMAT DE LA COLONNE
C
      FORCOL    = ZI(JIMPTY-1+ICOL)
C
C --- ECRITURE DE LA VALEUR DE LA COLONNE
C
      IF (FORCOL.EQ.1) THEN
        VALI = ZI(JIMPRI-1+ICOL)
      ELSE IF (FORCOL.EQ.2) THEN
        VALR = ZR(JIMPRR-1+ICOL)
      ELSE IF (FORCOL.EQ.3) THEN
        VALK = ZK16(JIMPRK-1+ICOL)
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF

      MARQ = ZK8(JIMPMA-1+ICOL)(1:1)
C
      CALL JEDEMA()

      END
