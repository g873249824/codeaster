C ======================================================================
C RESPONSABLE NISTOR I.NISTOR

      IMPLICIT NONE
      CHARACTER*8  MODELE
      CHARACTER*24 DEPMOI,DEPDEL,RESOCO
      
C ----------------------------------------------------------------------
C        CALCUL DES MATRICES ELEMENTAIRES ET DES SECONDS MEMBRES 
C             DE TYPE CONTACT FROTTANT DANS LE CADRE D'X-FEM
C                       'GRANDS GLISSEMENTS'
C ----------------------------------------------------------------------
C ROUTINE SPECIFIQUE A L'APPROCHE <<GRANDS GLISSEMENTS AVEC XFEM>>,
C TRAVAIL EFFECTUE EN COLLABORATION AVEC I.F.P.
C ----------------------------------------------------------------------

C IN MODELE : NOM DU MODELE
C IN LIGRCF : NOM DE LIGREL (DES ELEMENTS TARDIFS DE CONTACT)
C IN CARTCF : NOM DE LA CARTE DE CONTACT
C IN  DEPMOI : DÉPLACEMENT À L'ÉQUILIBRE PRÉCÉDENT
C IN  DEPDEL : INCRÉMENT DE DÉPLACEMENT DEPUIS L'ÉQUILIBRE PRÉCÉDENT
C IN/JXOUT MCONEL : MATR_ELEM : MATRICES ELEMENTAIRES
C IN/JXOUT SCONEL : VECT_ELEM : S M ELEMENTAIRES

C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------

      CHARACTER*32 JEXNUM,JEXNOM,JEXR8,JEXATR
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL,EXIGEO
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)


C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------

      CHARACTER*8  LPAIN(7),LPAOUT(1),K8BID
      CHARACTER*8  MCONEL,SCONEL,CARTCF       
      CHARACTER*8  VECCON,VECFRO
      CHARACTER*19 CHGEOM,LCHIN(7),LCHOUT(1)
      CHARACTER*19 PINTER,AINTER,CFACE,LIGRCF
      CHARACTER*24 MELEME
      INTEGER      JNOMA,JLIRES,IFM,NIV
      INTEGER      JMELEM,JLICON,JLIFRO
      
C
C ----------------------------------------------------------------------
C           
      CALL JEMARQ()
      CALL INFDBG('XFEM',IFM,NIV)
      
C --- NOMS DES MATR_ELEM ET VECT_ELEM CONTACT
C      
      MELEME = RESOCO(1:14)//'.MELEM'
      CALL JEVEUO(MELEME,'L',JMELEM)
      MCONEL = ZK8(JMELEM+1-1)
      SCONEL = ZK8(JMELEM+2-1)

      VECCON = '&&MMCCON'
      VECFRO = '&&MMCFRO'
            
C --- LIGREL DES ELEMENTS TARDIFS DE CONTACT/FROTTEMENT    
C
      LIGRCF = RESOCO(1:14)//'.LIGR'
C      
C --- CARTE POUR ELEMENTS TARDIFS DE CONTACT/FROTTEMENT    
C
      CARTCF = RESOCO(1:14)//'.CART'
      

C     RECUPERATION DE LA GEOMETRIE
      CALL MEGEOM(MODELE,' ',EXIGEO,CHGEOM)  

C     CREATION DES LISTES DES CHAMPS IN ET OUT
      LPAIN(1) = 'PGEOMER'
      LCHIN(1) = CHGEOM
      LPAIN(2) = 'PDEPL_M'
      LCHIN(2) = DEPMOI
      LPAIN(3) = 'PDEPL_P'
      LCHIN(3) = DEPDEL
      LPAIN(4) = 'PCAR_PT'
      LCHIN(4) = CARTCF//'.POINT'
      LPAIN(5) = 'PCAR_PI'
      LCHIN(5) = CARTCF//'.PINTER'
      LPAIN(6) = 'PCAR_AI'
      LCHIN(6) = CARTCF//'.AINTER'
      LPAIN(7) = 'PCAR_CF'
      LCHIN(7) = CARTCF//'.CFACE'  
          
C     CALCUL DE MCONEL :
C     --------------------
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<XFEM  > CREATION DES MATR_ELEM' 
      ENDIF 

      CALL DETRSD('MATR_ELEM',MCONEL) 
      CALL MEMARE('V',MCONEL(1:8),MODELE,' ',' ','RIGI_MECA')
      CALL WKVECT(MCONEL(1:8)//'.LISTE_RESU','V V K24',2,JLIRES)
      
      LPAOUT(1) = 'PMATUUR'
      LCHOUT(1) = MCONEL(1:8)//'.ME001'
      CALL CALCUL('S','RIGI_CONT',LIGRCF,7,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &            'V')
      ZK24(JLIRES-1+1) = LCHOUT(1)
      
      LCHOUT(1) = MCONEL(1:8)//'.ME002'
      CALL CALCUL('S','RIGI_FROT',LIGRCF,7,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &            'V')
      ZK24(JLIRES-1+2) = LCHOUT(1)
       
      CALL JEECRA(MCONEL(1:8)//'.LISTE_RESU','LONUTI',2,K8BID)

C      CALCUL DE SCONEL :
C      -----------------------

      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<XFEM  > CREATION DES VECT_ELEM' 
      ENDIF 
      CALL DETRSD('VECT_ELEM',SCONEL)
      CALL MEMARE('V',SCONEL,MODELE,' ',' ','CHAR_MECA')
      CALL WKVECT(SCONEL//'.LISTE_RESU','V V K24',2,JLIRES)

      CALL DETRSD('VECT_ELEM',VECCON)
      CALL MEMARE('V',VECCON,MODELE,' ',' ','CHAR_MECA')
      CALL WKVECT(VECCON//'.LISTE_RESU','V V K24',1,JLICON)
      CALL JEECRA(VECCON//'.LISTE_RESU','LONUTI',1,K8BID)

      CALL DETRSD('VECT_ELEM',VECFRO)
      CALL MEMARE('V',VECFRO,MODELE,' ',' ','CHAR_MECA')
      CALL WKVECT(VECFRO//'.LISTE_RESU','V V K24',1,JLIFRO)
      CALL JEECRA(VECFRO//'.LISTE_RESU','LONUTI',1,K8BID)


      LPAOUT(1) = 'PVECTUR'
      LCHOUT(1) = SCONEL//'.VE001'
      CALL CALCUL('S','CHAR_MECA_CONT',LIGRCF,7,LCHIN,LPAIN,1,LCHOUT,
     &            LPAOUT,'V')
      ZK24(JLIRES-1+1) = LCHOUT(1)
      ZK24(JLICON)     = LCHOUT(1)

      LCHOUT(1) = SCONEL//'.VE002'
      CALL CALCUL('S','CHAR_MECA_FROT',LIGRCF,7,LCHIN,LPAIN,1,LCHOUT,
     &            LPAOUT,'V')
      ZK24(JLIRES-1+2) = LCHOUT(1)
      ZK24(JLIFRO)     = LCHOUT(1)

      CALL JEECRA(SCONEL//'.LISTE_RESU','LONUTI',2,K8BID)



      CALL JEDEMA()
      END
