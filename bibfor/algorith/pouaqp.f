         AQ = (  1.D0/MP/MP )
      ELSE
         IF (QP.EQ.0.D0) THEN
            AQ = 1.D0/ME/ME
         ELSE
            AQ0 = QP**ALPHA
            AQ1 = AQ0/MP/MP
            AQ2 = AQ1 + BETA/ME/ME
            AQ = AQ2/(AQ0 + BETA)
         ENDIF
      ENDIF

