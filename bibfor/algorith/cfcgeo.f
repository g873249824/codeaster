      SUBROUTINE CFCGEO(NOMA  ,DEFICO,RESOCO,SOLALG,DVGEOM,
     &                  GEOERR,GEONOE,GEOVAL)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 13/06/2012   AUTEUR COURTOIS M.COURTOIS 
