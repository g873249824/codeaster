      SUBROUTINE NMCOFR(NOMA,DEPPLU,DEPDEL,DDEPLA,MATASS,
     &                  DEFICO,RESOCO,CNCINE,ITERAT,INST,
     &                  CONV,LICCVG,LREAC)
