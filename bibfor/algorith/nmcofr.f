C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*8  NOMA
      CHARACTER*24 DEPPLU
      CHARACTER*24 DEPDEL
      CHARACTER*24 DDEPLA      
      CHARACTER*24 DEFICO 
      CHARACTER*24 RESOCO
      CHARACTER*24 CNCINE
      CHARACTER*19 MATASS
      INTEGER      ITERAT
      REAL*8       INST
      REAL*8       CONV(*)
      INTEGER      LICCVG(*)
      LOGICAL      LREAC(2)
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES)
C
C TRAITEMENT DU CONTACT AVEC OU SANS FROTTEMENT DANS STAT_NON_LINE.
C BRANCHEMENT SUR LES ROUTINES DE RESOLUTION
C
C ----------------------------------------------------------------------
C
C 
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEPPLU : CHAMP DE DEPLACEMENTS A L'ITERATION DE NEWTON PRECEDENTE
C IN  DEPDEL : INCREMENT DE DEPLACEMENT CUMULE
C IN  DEPPLA : INCREMENT DE DEPLACEMENTS CALCULE EN IGNORANT LE CONTACT
C IN  MATASS : NOM DE LA MATRICE DU PREMIER MEMBRE ASSEMBLEE
C IN  DEFICO : SD DE DEFINITION DU CONTACT (ISSUE D'AFFE_CHAR_MECA)
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  CNCINE : CHAM_NO CINEMATIQUE
C IN  ITERAT : ITERATION DE NEWTON
C IN  INST   : VALEUR DE L'INSTANT DE CALCUL
C IN  CONV   : INFORMATIONS SUR LA CONVERGENCE DU CALCUL
C                     1 - RESI_DUAL_ABSO      (LAGRANGIEN AUGMENTE)
C                     2 - RESI_PRIM_ABSO      (LAGRANGIEN AUGMENTE)
C                     3 - NOMBRE D'ITERATIONS DUAL (LAGRANGIEN AUGMENTE)
C                     4 - NUMERO ITERATION BFGS (LAGRANGIEN AUGMENTE)
C                    10 - NOMBRE D'ITERATIONS (RECHERCHE LINEAIRE)
C                    11 - RHO                 (RECHERCHE LINEAIRE)
C                    20 - RESI_GLOB_RELA
C                    21 - RESI_GLOB_MAXI
C OUT LICCVG : CODES RETOURS D'ERREUR
C                       (1) PILOTAGE
C                       (2) LOI DE COMPORTEMENT
C                       (3) CONTACT/FROTTEMENT: NOMBRE MAXI D'ITERATIONS
C                       (4) CONTACT/FROTTEMENT: MATRICE SINGULIERE
C I/O LREAC  : (1) = TRUE  SI REACTUALISATION A FAIRE  
C              (2) = TRUE  SI ATTENTE POINT FIXE CONTACT
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      LOGICAL      PREMIE,GEOM,ALGO
      INTEGER      ICONTA,JPREM,INIITE,TYPALC
      REAL*8       TPS1(4),TPS2(4),TPSGEO,TPSALG,INITPS,R8BID
      INTEGER      IFM,NIV  
      CHARACTER*8  K8BID
      REAL*8       VALR(2)
      INTEGER      IBID      
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()      
      CALL INFDBG('CONTACT',IFM,NIV)
C
C --- TRAITEMENT DU CONTACT ?
C
      CALL JEEXIN ( RESOCO(1:14)//'.APREAC', ICONTA )
      IF (ICONTA.EQ.0) THEN
        GOTO 999
      ENDIF
C
C --- TYPE DE CONTACT
C
      CALL CFDISC(DEFICO,'              ',TYPALC,IBID,IBID,IBID)
C
C --- INITIALISATION DES TEMPS ET DES ITERATIONS
C --- SI ITERAT = 0 C'EST QU'ON DEBUTE UN NOUVEAU PAS DE TEMPS
C --- ON NE CREE PAS DE VECTEUR DIAGNOSTIC SI ON EST EN MODE "VERIF"
C
      IF ((ITERAT.EQ.0).AND.(TYPALC.NE.5)) THEN
        INITPS = 0.D0
        INIITE = 0
        CALL CFITER(RESOCO,'E','TIPA',IBID,INITPS)
        CALL CFITER(RESOCO,'E','TIPG',IBID,INITPS)
        CALL CFITER(RESOCO,'E','ITEP',INIITE,R8BID)
      ENDIF
C
      CALL CFIMPE(IFM,NIV,'NMCOFR',1)
C
C --- PREMIERE UTILISATION DU CONTACT OU NON
C
      CALL JEVEUO ( RESOCO(1:14) // '.PREM', 'E', JPREM )

      PREMIE = ZL(JPREM)
      IF ( PREMIE )  THEN 
        ZL(JPREM) = .FALSE.
      ENDIF
C
C --- TIMING 
C
      CALL UTTCPU (10,'INIT',4,TPS1)
      CALL UTTCPU (20,'INIT',4,TPS2)
C 
C --- APPARIEMENT
C
      CALL UTTCPU (10,'DEBUT',4,TPS1)
      CALL CFGEOM(PREMIE,LREAC(1),ITERAT,INST,
     &            NOMA,DEFICO,RESOCO,
     &            DEPPLU,DEPDEL,GEOM)
      CALL UTTCPU (10,'FIN',4,TPS1)
      TPSGEO = TPS1(4)      
C
C -- MODE VERIF: PAS D'ALGO DE CONTACT, ON SORT APRES L'APPARIEMENT
C
      IF (TYPALC.EQ.5) THEN
        GOTO 998
      ENDIF
C
C --- ALGORITHMES DE CONTACT
C
      CALL UTTCPU (20,'DEBUT',4,TPS2)
      CALL CFALGO(NOMA,ITERAT,CONV,
     &            DEFICO,RESOCO,MATASS,
     &            DEPPLU,DDEPLA,DEPDEL,CNCINE,
     &            LICCVG,LREAC,ALGO)
      CALL UTTCPU (20,'FIN',4,TPS2)
      TPSALG = TPS2(4)
C
      IF (NIV.GE.2) THEN
        VALR(1) = TPSGEO
        VALR(2) = TPSALG        
        IF (GEOM) THEN
          CALL CFIMPD(IFM,NIV,'NMCOFR',2, 
     &                IBID,VALR,K8BID)
        ENDIF
        IF (ALGO) THEN
          CALL CFIMPD(IFM,NIV,'NMCOFR',3, 
     &                IBID,VALR,K8BID)
        ENDIF
      ENDIF
C
C -- STOCKAGE DU TEMPS
C      
      CALL CFITER(RESOCO,'E','TIMA',IBID,TPSALG)
      CALL CFITER(RESOCO,'E','TIMG',IBID,TPSGEO)
C
  998 CONTINUE
C  
      CALL CFIMPE(IFM,NIV,'NMCOFR',4)            
C
  999 CONTINUE
C
      CALL JEDEMA()
      END
