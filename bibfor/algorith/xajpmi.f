      SUBROUTINE XAJPMI(LIST,LONG,IPT,CPT,NEWPT,LONGAR)
      IMPLICIT NONE

      INCLUDE 'jeveux.h'
      INTEGER       LONG,IPT,CPT
      REAL*8        NEWPT(3),LONGAR,LIST(*)

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 13/06/2012   AUTEUR COURTOIS M.COURTOIS 
C ======================================================================
