      SUBROUTINE LCRONL (FAMI,KPG,KSP,NDIM,MATE,OPTION,FM,DF,VIM,
     &                   VIP,SIGP,DSIGDF)


C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 16/10/2007   AUTEUR SALMONA L.SALMONA 
C ======================================================================
