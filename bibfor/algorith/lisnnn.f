      IMPLICIT     NONE
      CHARACTER*16 MOTFAC
      INTEGER      IEXCI
      CHARACTER*8  CHARGE
C
C ----------------------------------------------------------------------
C
C ROUTINE UTILITAIRE (LISTE_CHARGES)
C
C LECTURE DU NOM DE LA CHARGE (PROVENANT DE AFFE_CHAR_*)
C
C ----------------------------------------------------------------------
C
C
C IN  MOTFAC : MOT-CLEF FACTEUR DES EXCITATIONS
C IN  IEXCI  : OCCURRENCE DE L'EXCITATION
C OUT CHARGE : NOM DE LA CHARGE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      NVAL,IARG
      INTEGER      EXIMVE,GETEXM
      CHARACTER*24 K24BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      CHARGE = ' '
C
C --- CHARGE SPECIFIQUE VECT_ASSE
C
      EXIMVE = GETEXM(MOTFAC,'VECT_ASSE')
      IF (EXIMVE.EQ.1) THEN
        CALL GETVID(MOTFAC,'VECT_ASSE',IEXCI,IARG,0,
     &              K24BID,NVAL)
        NVAL = ABS(NVAL)
        IF (NVAL.GT.1) CALL ASSERT(.FALSE.)
        CALL GETVID(MOTFAC,'VECT_ASSE',IEXCI,IARG,1,
     &              CHARGE,NVAL)
      ENDIF
C
C --- CHARGE STANDARD
C
      CALL GETVID(MOTFAC,'CHARGE'   ,IEXCI,IARG,0,
     &            K24BID,NVAL)
      NVAL = ABS(NVAL)
      IF (NVAL.GT.1) CALL ASSERT(.FALSE.)
      CALL GETVID(MOTFAC,'CHARGE'   ,IEXCI,IARG,1,
     &            CHARGE,NVAL)
C
      CALL JEDEMA()
      END
