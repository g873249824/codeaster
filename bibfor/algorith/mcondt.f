      SUBROUTINE MCONDT(PHI,SAT,T,P1,P2,R,MAMOLV,MAMOLG,RHO12,RHO21,RV0
     &                ,RHO11,ATMP,XKW,DTV,DPV,DTW,DPW,DATM,DDATT,DDATP2
     &                ,DRV0DT,DHDSR,DHDN,DHDT,DRVDTT,FACTOR,DFACP2,DFACT
     &                ,COEFG,HUMR,HFG,SIGR,SIGT,DSIGT,TLANW,TLANA)
C
C     CETTE ROUTINE CALCULE LES DTV,DPV,DTW,DPW POUR
C     LA MODELISATION DE THM NON SATURE DE B. GATMIRI (CERMES)
C
