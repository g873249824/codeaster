C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*8  NOMA
      CHARACTER*24 DEFICO,RESOCO
      INTEGER      POSNOE,POSNOM,POSAPP
      INTEGER      TYPAPP
      INTEGER      ILIAI 
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT)
C
C SAUVEGARDE DES PARAMETRES APPARIEMENT
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  ILIAI  : INDICE DE LA LIAISON COURANTE
C IN  TYPAPP : TYPE D'APPARIEMENT
C               1   APPARIEMENT AVEC UN NOEUD
C               2   APPARIEMENT AVEC UNE MAILLE
C               3   NOEUD MAITRE PROCHE SI MAIT/ESCL
C               -1  NON APPARIE CAR NOEUD EXCLU SANS_NOEUD
C               -2  NON APPARIE CAR NOEUD EXCLU PAR TOLE_APPA
C               -3  NON APPARIE CAR NOEUD EXCLU PAR TOLE_PROJ_EXT 
C IN  POSNOE : POSITION DANS CONTNO DU NOEUD ESCLAVE
C IN  POSNOM : POSITION DANS CONTNO DU NOEUD MAITRE LE PLUS PROCHE
C IN  POSAPP : ENTITE APPARIEE
C                - SI NODAL    : NEGATIF ET NOEUD MAITRE APPARIE
C                - SI MAIT/ESCL: MAILLE MAITRE APPARIEE      
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      INTEGER      CFMMVD,ZAPME 
      CHARACTER*8  NOMNOE,NOMAPP,NOMNOM,NOMMAM,VALK(2)
      CHARACTER*24 APMEMO,APPARI
      INTEGER      JAPMEM,JAPPAR  
      INTEGER      CODRET
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()      
      CALL INFDBG('CONTACT',IFM,NIV)
C
C --- LECTURE DES STRUCTURES DE DONNEES DE CONTACT
C
      APMEMO = RESOCO(1:14)//'.APMEMO'
      APPARI = RESOCO(1:14)//'.APPARI'      
      CALL JEVEUO(APMEMO,'E',JAPMEM)
      CALL JEVEUO(APPARI,'E',JAPPAR)
      ZAPME  = CFMMVD('ZAPME')
C
C --- AJOUT D'UNE LIAISON DANS LE CAS MAIT/ESCL
C
      IF (TYPAPP.EQ.2) THEN
        ZI(JAPPAR+ILIAI)  = POSNOE
      ENDIF 
C
C --- AJOUT D'UNE LIAISON DANS LE CAS NODAL
C
      IF (TYPAPP.EQ.1) THEN
        ZI(JAPPAR+ILIAI)  = POSNOE       
      ENDIF
C
C --- TYPE D'APPARIEMENT
C
      ZI(JAPMEM+ZAPME*(POSNOE-1)+1-1) = TYPAPP       
C
C --- POSITION DANS CONTNO DU NOEUD MAITRE LE PLUS PROCHE
C
      ZI(JAPMEM+ZAPME*(POSNOE-1)+2-1) = POSNOM     
C
C --- ENTITE APPARIEE
C       
      ZI(JAPMEM+ZAPME*(POSNOE-1)+3-1) = POSAPP
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN        
        CALL CFNOMM(NOMA  ,DEFICO,'NOEU',POSNOE,NOMNOE,
     &              CODRET)
        IF (CODRET.LT.0) THEN
          NOMNOE = 'ERREUR'
        ENDIF 
        VALK(1) = NOMNOE
        IF (TYPAPP.EQ.-3) THEN
          
          IF (POSAPP.LT.0) THEN
            CALL CFNOMM(NOMA  ,DEFICO,'NOEU',POSNOE,NOMAPP,
     &                  CODRET)
            IF (CODRET.LT.0) THEN
              NOMAPP = 'ERREUR'
            ENDIF
          ELSEIF (POSAPP.GT.0) THEN
            CALL CFNOMM(NOMA  ,DEFICO,'MAIL',POSNOE,NOMAPP,
     &                  CODRET)
            IF (CODRET.LT.0) THEN
              NOMAPP = 'ERREUR'
            ENDIF  
          ELSE
            NOMAPP = 'ERREUR'
          ENDIF  
          VALK(2) = NOMAPP  
          CALL U2MESK('I','CONTACTDEBG_11',2,VALK)  
        ELSEIF (TYPAPP.EQ.-2) THEN
          CALL U2MESK('I','CONTACTDEBG_12',1,VALK)                  
        ELSEIF (TYPAPP.EQ.-1) THEN
          CALL U2MESK('I','CONTACTDEBG_13',1,VALK)
        ELSEIF (TYPAPP.EQ.3) THEN
          CALL CFNOMM(NOMA  ,DEFICO,'NOEU',POSNOM,NOMNOM,
     &                CODRET)
        ELSEIF (TYPAPP.EQ.2) THEN
          CALL CFNOMM(NOMA  ,DEFICO,'MAIL',POSAPP,NOMMAM,
     &                CODRET)          
        ENDIF
      ENDIF       
C
      CALL JEDEMA()
      END
