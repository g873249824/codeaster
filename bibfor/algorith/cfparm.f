      INTEGER      ZAPPA,ZAPME  
      INTEGER      JAPMEM
      INTEGER      JAPPAR   
      INTEGER      POSMAI
      INTEGER      POSESC     
      INTEGER      MAIPRO           
      INTEGER      IESCL  
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT)
C
C SAUVEGARDE PARAMETRES APPARIEMENT
C
C ----------------------------------------------------------------------
C
C NB: UNE PARTIE A DEJA ETE REMPLIE PAR CFPARN (APPEL DANS RECHMN)
C
C IN  JAPMEM : POINTEUR VERS RESOCO(1:14)//'.APMEMO'
C IN  ZAPME  : LONGUEUR DE RESOCO(1:14)//'.APMEMO'
C IN  JAPPAR : POINTEUR VERS RESOCO(1:14)//'.APPARI'
C IN  ZAPPA  : LONGUEUR DE RESOCO(1:14)//'.APPARI'
C IN  POSESC : POSITION DANS DEFICO(1:16)//'.CONTNO' DU NOEUD ESCLAVE
C IN  POSMAI : MAILLE MAITRE APPARIEE 
C                 (POSITION DANS DEFICO(1:16)//'.CONTMA')       
C IN  MAIPRO : MAILLE MAITRE LA PLUS PROCHE
C                 (POSITION DANS DEFICO(1:16)//'.CONTMA')      
C IN  IESCL  : INDICE DU NOEUD ESCLAVE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CALL JEMARQ()
C
C --- POSITION DANS CONTNO DU NOEUD ESCLAVE COURANT
C
      ZI(JAPPAR+ZAPPA*(IESCL-1)+1)  = POSESC 
C
C --- MAILLE MAITRE APPARIEE
C      
      ZI(JAPPAR+ZAPPA*(IESCL-1)+2)  = POSMAI  
C
C --- C'EST UN NOEUD ESCLAVE
C
      ZI(JAPMEM+ZAPME*(POSESC-1))   = 1       
C
C --- DERNIERE MAILLE MAITRE APPARIEE
C   
      ZI(JAPMEM+ZAPME*(POSESC-1)+2) = MAIPRO                       
C
      CALL JEDEMA()
      END
