      IMPLICIT     NONE
      CHARACTER*24 RESOCO
      INTEGER      POSMAM,POSNOE               
      INTEGER      IESCL  
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT)
C
C SAUVEGARDE PARAMETRES APPARIEMENT
C
C ----------------------------------------------------------------------
C
C NB: UNE PARTIE A DEJA ETE REMPLIE PAR CFPARN (APPEL DANS RECHMN)
C
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  POSNOE : POSITION DANS CONTNO DU NOEUD ESCLAVE
C IN  POSMAM : MAILLE MAITRE APPARIEE      
C IN  IESCL  : INDICE DU NOEUD ESCLAVE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      CFMMVD,ZAPPA,ZAPME  
      CHARACTER*24 APMEMO,APPARI
      INTEGER      JAPMEM,JAPPAR  
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- LECTURE DES STRUCTURES DE DONNEES DE CONTACT
C
      APMEMO = RESOCO(1:14)//'.APMEMO'
      APPARI = RESOCO(1:14)//'.APPARI'      
      CALL JEVEUO(APMEMO,'E',JAPMEM)
      CALL JEVEUO(APPARI,'E',JAPPAR)
      ZAPME  = CFMMVD('ZAPME')
      ZAPPA  = CFMMVD('ZAPPA')        
C
C --- POSITION DANS CONTNO DU NOEUD ESCLAVE COURANT
C
      ZI(JAPPAR+ZAPPA*(IESCL-1)+1)  = POSNOE 
C
C --- MAILLE MAITRE APPARIEE
C      
      ZI(JAPPAR+ZAPPA*(IESCL-1)+2)  = POSMAM  
      ZI(JAPMEM+ZAPME*(POSNOE-1)+2) = POSMAM
C
C --- C'EST UN NOEUD ESCLAVE
C
      ZI(JAPMEM+ZAPME*(POSNOE-1))   = 1                             
C
      CALL JEDEMA()
      END
