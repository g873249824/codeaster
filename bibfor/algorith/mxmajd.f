C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*19 SDDYNA
      REAL*8       DELTAT
      CHARACTER*24 VALMOI(8),VALPLU(8)
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME - EXPLICITE)
C
C MET A JOUR LES VITESSES ET LES DEPL/VITE/ACCE GENERALISEES
C      
C ----------------------------------------------------------------------
C
C
C IN  SDDYNA : SD DEDIEE A LA DYNAMIQUE (CF NDLECT)
C IN  DELTAT : INCREMENT DE TEMPS
C IN  VALMOI : VARIABLE CHAPEAU POUR ETAT EN T-
C IN  VALPLU : VARIABLE CHAPEAU POUR ETAT EN T+
C 
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      JACCGM,JACCGP,JVITGM,JVITGP
      INTEGER      JDEPGP,JDEPGM
      CHARACTER*24 SDPRMO,NDYNKK
      INTEGER      IMODE
      CHARACTER*8  K8BID
      INTEGER      NBMODE
      REAL*8       COEVI1,COEVI2 
      LOGICAL      NDYNLO
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- COEFFICIENTS
C
      IF (NDYNLO(SDDYNA,'DIFF_CENT')) THEN
        COEVI1 = 0.5D0*DELTAT
        COEVI2 = 0.5D0*DELTAT
      ELSEIF (NDYNLO(SDDYNA,'TCHAMWA')) THEN
        COEVI1 = 0.D0
        COEVI2 = DELTAT
      ENDIF           
C      
C --- MISE A JOUR DES DEPL/VITE/ACCE GENERALISEES 
C
      SDPRMO  = NDYNKK(SDDYNA,'PROJ_MODAL')
      CALL JELIRA(SDPRMO(1:19)//'.AGEM','LONMAX',NBMODE,K8BID)
      CALL JEVEUO(SDPRMO(1:19)//'.AGEM','E',JACCGM)
      CALL JEVEUO(SDPRMO(1:19)//'.AGEP','L',JACCGP)
      CALL JEVEUO(SDPRMO(1:19)//'.VGEM','E',JVITGM)
      CALL JEVEUO(SDPRMO(1:19)//'.VGEP','E',JVITGP)
      CALL JEVEUO(SDPRMO(1:19)//'.DGEM','E',JDEPGM)
      CALL JEVEUO(SDPRMO(1:19)//'.DGEP','L',JDEPGP)
      DO 11 IMODE =1,NBMODE
        ZR(JVITGP+IMODE-1) = ZR(JVITGM+IMODE-1) + 
     &                       COEVI1*ZR(JACCGP+IMODE-1) +
     &                       COEVI2*ZR(JACCGM+IMODE-1)
        ZR(JDEPGM+IMODE-1) = ZR(JDEPGP+IMODE-1)
        ZR(JVITGM+IMODE-1) = ZR(JVITGP+IMODE-1)
        ZR(JACCGM+IMODE-1) = ZR(JACCGP+IMODE-1)
11    CONTINUE
C
      CALL JEDEMA()
      END
