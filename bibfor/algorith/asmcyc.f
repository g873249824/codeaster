      SUBROUTINE ASMCYC(CMASS,NDIM,SOUMAT,BETA,NI,NJ,NA,
     &AXOK,LIAX,NBLIAX,LIBID)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 03/07/2012   AUTEUR PELLET J.PELLET 
