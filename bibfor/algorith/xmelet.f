      SUBROUTINE XMELET(NOMTE,TYPMA,ESC,MAIT,ELC,
     &                 NDIM,NDDL,NNE,NNM,NNC,MALIN,SINGE,SINGM,NDLS)
     
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 08/12/2009   AUTEUR PROIX J-M.PROIX 
