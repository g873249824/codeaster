      INTEGER      IRET
      CHARACTER*24 PROMAT(2)


C    INITIALISATION
      PROMAT(1) = '&&DELAGL.PRDEAS'
      PROMAT(2) = '&&DELAGL.PRDEPR'


C    ASSEMBLAGE ET FACTORISATION DU PROJECTEUR
      CALL ASMATR(1,PROELE,' ',NUMEDE,SOLVDE,' ','ZERO','V',1,PROMAT(1))
      CALL PRERES(SOLVDE, 'V', IRET, PROMAT(2), PROMAT(1))
      IF (IRET.NE.0) CALL UTMESS('F','DELALO',
     &  'MODELE NON LOCAL : PROJECTEUR SINGULIER')


C    LISSAGE DU CHAMP PAR RESOLUTION LINEAIRE
      CALL RESOUD(PROMAT(1), PROMAT(2), RESASS, SOLVDE, ' ', 'V',
     &            MONTEE, MONTEE)

      END
