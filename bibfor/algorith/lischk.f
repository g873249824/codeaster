C
C IN  NOMO   : NOM DU MODELE
C IN  PHENOM : TYPE DE PHENOMENE (MECANIQUE, THERMIQUE, ACOUSTIQUE)
C IN  NOMCMD : NOM DE LA COMMANDE
C IN  LISCHA : SD LISTE DES CHARGES
C
C
C
C
      CHARACTER*16 NOMCMD,PHENOM
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      NOMCMD = NOMCMZ
      PHENOM = PHENOZ
C
C --- VERIFICATION DE LA COHERENCE DES MODELES
C
      CALL LISCOM(NOMO  ,LISCHA)
C
C --- VERIFICATION COMPATIBILITE CHARGE/PHENOMENE
C
      CALL LISCCP(PHENOM,LISCHA)
C
C --- VERIFICATION COMPATIBILITE CHARGE/COMMANDE
C
      CALL LISCCM(NOMCMD,LISCHA)
C
C --- VERIFICATIONS DES DOUBLONS
C
      CALL LISDBL(LISCHA)
C
C --- VERIFICATIONS DIVERSES SUR LES TYPES DE CHARGES
C
      CALL LISVER(LISCHA)
C
      CALL JEDEMA()
      END
