      SUBROUTINE NUFNLG(NDIM,NNO1,NNO2,NPG,IW,VFF1,VFF2,IDFF1,VU,VP,
     &                  TYPMOD,MATE,COMPOR,GEOMI,SIG,DDL,VECT)
