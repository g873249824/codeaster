C  ROUTINE "CHAPEAU" : LA PROJECTION AUX SOUS-POINTS (FAMILLE MATER)
C                         *  *           *    *               **
C
C  ELLE EST CONSTITUEE DE DEUX TEMPS
C    APPEL A PJXXCH (USUEL POUR TOUS LES CHAMPS, CF. OP0166 OU PJXXPR)
C    APPEL A PJCOR2 (RETOUR AUX SOUS-POINTS)

C
      IMPLICIT   NONE
C
C 0.1. ==> ARGUMENTS
C
      CHARACTER*1 BASE
      CHARACTER*8 PROL0,NOCA
      CHARACTER*16 CORRES
      CHARACTER*19 CHAM1
      CHARACTER*19 CHAM2,LIGRE2
      INTEGER IRET
C
C 0.3. ==> VARIABLES LOCALES
C
C
      INTEGER NNCP,IERD,IBID

      CHARACTER*4  TYCHA2
      CHARACTER*8  NOMPAR
      CHARACTER*16 OPTION
      CHARACTER*19 CHAUXS
      CHARACTER*19 PRFCHN
      CHARACTER*19 CNS1,CH2S



C DEB ------------------------------------------------------------------
      CALL JEMARQ()
      CALL INFMAJ()
      CALL TITRE

C     PROJECTION DU CHAMP SUR LE MAILLAGE MASP
      CHAUXS = '&&PJSPMA'//'.CHAS'
      TYCHA2 = 'NOEU'
      CALL PJXXCH(CORRES,CHAM1,
     &            CHAUXS,TYCHA2,' ',PROL0,LIGRE2,BASE,IRET)
      IF (IRET.NE.0) GOTO 999

C     CHAUXS : VALEUR AUX NOEUD DU MAILLAGE MASP


C     -- ON TRANSFORME LE CHAM_NO PROJETE EN UN CHAM_NO_S
      CNS1 = '&&PJSPMA'//'.CH1S'
      CALL CNOCNS(CHAUXS,'G',CNS1)

C     CNS1 : CHAM_NO_S DES VALEURS AUX NOEUDS DE MASP

C     ON TRANSPOSE LE CHAMP SUR LE MODELE 2 (ELGA SOUS-POINTS)
      CH2S = '&&OP0166'//'.CH2S'
      CALL PJCOR2(NOCA,CNS1,CH2S,
     &      LIGRE2,CORRES,NOMPAR,IERD)

      OPTION = 'INI_SP_MATER'

      CALL CESCEL(CH2S,LIGRE2,OPTION,NOMPAR,PROL0,
     &           NNCP,'G',CHAM2,'A',IERD)

      CALL DISMOI('F','PROF_CHNO',CHAUXS,'CHAM_NO',IBID,PRFCHN,IBID)
      CALL DETRSD('PROF_CHNO',PRFCHN)


      CALL DETRSD('CHAM_NO_S',CNS1)
      CALL DETRSD('CHAM_ELEM_S',CH2S)

 999  CONTINUE
      CALL DETRSD('CHAM_NO',CHAUXS)

      CALL JEDEMA()
      END
