C     BUT:  FONCTIONS D'ENRICHISSEMENT DANS LA BASE POLAIRE (R,THETA)
C
C IN  R      : PREMIERE COORDONNEE DANS LA BASE POLAIRE
C IN  THETA  : SECONDE COORDONNEE DANS LA BASE POLAIRE
C OUT FE      : VALEURS DES FONCTIONS D'ENRICHISSEMENT
C
C----------------------------------------------------------------
C
C     FONCTIONS D'ENRICHISSEMENT
      FE(1) = SQRT(R) * SIN(THETA/2.D0)
      FE(2) = SQRT(R) * COS(THETA/2.D0)
      FE(3) = SQRT(R) * SIN(THETA/2.D0) * SIN(THETA)
      FE(4) = SQRT(R) * COS(THETA/2.D0) * SIN(THETA)

