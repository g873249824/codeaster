C-------- DEBUT COMMUNS NORMALISES  JEVEUX  ----------------------------
C
      INTEGER          ZI
      COMMON  /IVARJE/ ZI(1)
      REAL*8           ZR
      COMMON  /RVARJE/ ZR(1)
      COMPLEX*16       ZC
      COMMON  /CVARJE/ ZC(1)
      LOGICAL          ZL
      COMMON  /LVARJE/ ZL(1)
      CHARACTER*8      ZK8
      CHARACTER*16              ZK16
      CHARACTER*24                        ZK24
      CHARACTER*32                                  ZK32
      CHARACTER*80                                            ZK80
      COMMON  /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C-----  FIN  COMMUNS NORMALISES  JEVEUX  -------------------------------
C
      INTEGER      IER,IBID
C      CHARACTER*3  CPT
      CHARACTER*8  NOMRES,NOMCON,NOMOPE,MAILLA,BASMOD,BLANC
      CHARACTER*19 RAID,MASS,AMOR,IMPE
      CHARACTER*24 NOMMAT,NOMREP,NOMFAM,NOMFAP,NOMFAD
C
      DATA BLANC /'        '/
C-----------------------------------------------------------------------
C
C
      CALL INFMAJ()
      CALL GETRES(NOMRES, NOMCON, NOMOPE)
C
C --- RECUPERATION BASE MODALE, MATRICES ET CREATION .REFE
C     ET DETERMINATION OPTION DE CALCUL
C    
      CALL REFE81(NOMRES, BASMOD, RAID, MASS, AMOR, MAILLA)
C
C --- CALCUL DES MATRICES PROJETEES
C
      IMPE = BLANC
      CALL GETVID(' ','MATR_IMPE',1,1,1,IMPE,N1)
      IF (IMPE.NE.BLANC) THEN
         CALL IMPE81(NOMRES, IMPE, BASMOD)
         GOTO 10
      ENDIF
      NOMMAT = NOMRES//'.MAEL_RAID'
      CALL CALPRO(NOMMAT, 'G', BASMOD, RAID )
      NOMMAT = NOMRES//'.MAEL_MASS'
      CALL CALPRO(NOMMAT, 'G', BASMOD, MASS )
      IF (AMOR.NE.BLANC) THEN
         NOMMAT=NOMRES//'.MAEL_AMOR'
         CALL CALPRO(NOMMAT,'G',BASMOD,AMOR)
      ELSE
         CALL GETVR8(BLANC,'AMOR_REDUIT',1,1,0,BID,IOC)
         IF (IOC.LT.0) THEN
            NOMMAT=NOMRES//'.MAEL_AMOR'
           CALL CALAMO(NOMMAT,'G',BASMOD)
         ENDIF
      ENDIF
C
C --- CALCUL DES FORCES D'INERTIES
C
      NOMMAT = NOMRES//'.MAEL_INER'
      CALL INER81 ( NOMMAT, 'G', BASMOD, MASS )
   10 CONTINUE
C
C --- COMPATIBILITE AVEC SD MACR_ELEM_STAT
C
C      CALL GETVTX(' ','COMPATIBILITE',1,1,1,CPT,IBID)
C      IF (CPT.EQ.'OUI') THEN
C        CALL COMP81(NOMRES, BASMOD, RAID, MASS, AMOR, MAILLA)
C      ENDIF
      CALL COMP81(NOMRES, BASMOD, RAID, MASS, AMOR, MAILLA)
C
      END
