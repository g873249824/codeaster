      SUBROUTINE DLARCH (RESULT,NEQ, ISTOC, IARCHI, TEXTE,
     &                    ALARM, IFM, TEMPS,
     &                    NBTYAR, TYPEAR, MASSE,
     &                    DEPL, VITE, ACCE,
     &                    FEXTE,FAMOR,FLIAI )
C ---------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 13/06/2012   AUTEUR COURTOIS M.COURTOIS 
C ======================================================================
