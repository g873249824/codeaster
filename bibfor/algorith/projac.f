C-------------------   DECLARATION DES VARIABLES   ---------------------
C
C ARGUMENTS
C ---------
      INTEGER  NP1, NP2, NBM, IC
      REAL*8   PHII(NP2,NP1,*), JACOB(3,3), MTMP1(NP1,*), MTMP6(3,*)
C
C VARIABLES LOCALES
C -----------------
      INTEGER  I, J, K
C
C-------------------   DEBUT DU CODE EXECUTABLE    ---------------------
C
      DO 110 I = 1, 3
         DO 120 J = 1, NBM
            MTMP6(I,J) = 0.0D0
            DO 130 K = 1, 3
               MTMP6(I,J) = MTMP6(I,J) + JACOB(I,K) * PHII(IC,J,K)
 130        CONTINUE
 120     CONTINUE
 110  CONTINUE
C
      DO 210 I = 1, NBM
         DO 220 J = 1, NBM
            MTMP1(I,J) = 0.0D0
            DO 230 K = 1, 3
               MTMP1(I,J) = MTMP1(I,J) + PHII(IC,I,K) * MTMP6(K,J)
 230        CONTINUE
 220     CONTINUE
 210  CONTINUE
C
C --- FIN DE PROJAC.
      END
