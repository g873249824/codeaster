C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT NONE
C
      INCLUDE 'jeveux.h'
      CHARACTER*(*) NOMA
      INTEGER       NBMA,NUMAI(*),NBNOS,NUMNOS(*)
C ----------------------------------------------------------------------
C     BUT :   TROUVER LA LISTE DES NUMEROS DE NOEUDS SOMMETS D'UNE LISTE
C             DE MAILLES
C     ENTREES:
C        NOMA   : NOM DU MAILLAGE
C        NBMA   : NOMBRE DE MAILLES DE LA LISTE
C        NUMAI  : NUMEROS DES MAILLES DE LA LISTE
C     SORTIES:
C        NBNOS  : NOMBRE DE NOEUDS SOMMETS
C        NUMNOS : NUMEROS DES NOEUDS SOMMETS (UN NOEUD APPARAIT UNE
C                               SEULE FOIS )
C ----------------------------------------------------------------------
C     ------------------------------------------------------------------
      INTEGER      NNOE
      CHARACTER*8  NOMMA,CBID
C
C
C-----------------------------------------------------------------------
      INTEGER IER ,IMA ,IMAI ,INO ,INOE ,IPOIN ,JCONX 
      INTEGER JNUNOS ,JPOIN ,NBNOE ,NUM 
C-----------------------------------------------------------------------
      CALL JEMARQ()
      NOMMA=NOMA
      NBNOS= 0
C  --- RECHERCHE DU NOMBRE DE NOEUDS DU MAILLAGE ---
C-DEL CALL JELIRA(NOMMA//'.NOMNOE','NOMMAX',NBNOE,' ')
      CALL DISMOI('F','NB_NO_MAILLA',NOMMA,'MAILLAGE',NBNOE,CBID,IER)
      CALL WKVECT('&&IRMANO.NUMNOS','V V I',NBNOE,JNUNOS)
C     --- INITIALISATION DU TABLEAU DE TRAVAIL &&IRMANO.NUMNOS ----
      DO 10 INO=1,NBNOE
         ZI(JNUNOS-1+INO) = 0
 10   CONTINUE
C     --- RECHERCHE DES NOEUDS SOMMETS ----
        CALL JEVEUO(NOMMA//'.CONNEX','L',JCONX)
        CALL JEVEUO(JEXATR(NOMMA//'.CONNEX','LONCUM'),'L',JPOIN)
        DO 12 IMA=1,NBMA
            IMAI=NUMAI(IMA)
            IPOIN= ZI(JPOIN-1+IMAI)
            NNOE = ZI(JPOIN-1+IMAI+1)-IPOIN
            DO 22 INOE=1,NNOE
               NUM=ZI(JCONX-1+IPOIN-1+INOE)
               ZI(JNUNOS-1+NUM) =1
  22        CONTINUE
  12    CONTINUE
C  --- STOCKAGE DES NOEUDS PRESENTS SUR LA LISTE DES MAILLES---
        DO 32 INOE=1,NBNOE
           IF (ZI(JNUNOS-1+INOE).EQ.1) THEN
               NBNOS=NBNOS+1
               NUMNOS(NBNOS)=INOE
           ENDIF
  32    CONTINUE
C
      CALL JEDETC('V','&&IRMANO',1)
      CALL JEDEMA()
      END
