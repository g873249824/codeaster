      SUBROUTINE IRCMPE ( NOFIMD,
     >                    NCMPVE, NTLCMP,
     >                    NBVATO, NBMAEC, LIMAEC, ADSD, ADSL,
     >                    TYPMA, NMATYP,
     >                    NTPROA, NOPFTY, NVALTY, NBPGTY, NBSPTY )
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF PREPOST  DATE 16/10/2002   AUTEUR GNICOLAS G.NICOLAS 
