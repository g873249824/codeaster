     &                  ANG,WK1,WK2,WK3,WK4,WK5,WK6,IDEBUT,NBLOC,NBVAL,
     &                  INOE,ISUPP,NBINST,TMPVU,PUSURN,VUSTUB,
     &                  VUSOB,PUS,PMOYE,POURPU,POUPRE)

