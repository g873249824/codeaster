C     INMTOT : NB MAILLES X-FEM DEJA CREES
C     NBCMPC : NB DE CMP DU COMPORTEMENT
C     JRESD1 : ADRESSE DU .CESD DU CHAM_ELEM_S DE COMPORTEMENT ENTREE
C     JRESV1 : ADRESSE DU .CESV DU CHAM_ELEM_S DE COMPORTEMENT ENTREE
C     JRESL1 : ADRESSE DU .CESL DU CHAM_ELEM_S DE COMPORTEMENT ENTREE
