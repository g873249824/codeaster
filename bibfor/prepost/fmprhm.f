C ======================================================================
C     -----------------------------------------------------------------
C     NBFONC  : IN  : NOMBRE DE FONCTIONS (6 EN 3D 4 EN 2D)
C     NBPTOT  : IN  : NOMBRE DE PAS DE TEMPS DE CALCUL
C     SIGM    : IN  : VECTEUR DES CONTRAINTES EN TOUS LES PAS DE TEMPS
C     RPHMAX  : OUT : VALEUR DU DE LA PRESSION HYDROSTATIQUE MAXIMALE
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
      REAL*8   RPH
C
C ------- CALCUL DE LA PRESSION HYDROSTATIQUE MAXIMALE---
C
      RPHMAX=(SIGM(1)+SIGM(2)+SIGM(3))/3.D0
      DO 10 I=2,NBPTOT
        IDE = (I-1)*NBFONC
        RPH=(SIGM(IDE+1)+SIGM(IDE+2)+SIGM(IDE+3))/3.D0
        IF (RPH.GT.RPHMAX) RPHMAX = RPH
 10   CONTINUE
C
      END
