      SUBROUTINE RENRFA( NOMFOR,VALGRD,NRUPT,ICODRE)

      IMPLICIT NONE
      CHARACTER*16        NOMFOR
      INTEGER            ICODRE
      REAL*8             VALGRD, NRUPT
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF PREPOST  DATE 20/06/2011   AUTEUR TRAN V-X.TRAN 
C ======================================================================
