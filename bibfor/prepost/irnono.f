C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT NONE
C
      CHARACTER*(*) NOMA,NONOE(*),NOGRN(*),NUMNO
      INTEGER       NBNO,NBGR,NBNOT
C ----------------------------------------------------------------------
C     BUT :   TROUVER LES NUMEROS DES NOEUDS TROUVES DANS
C             UNE LISTE DE NOEUDS ET DE GROUP_NO
C     ENTREES:
C        NOMA   : NOM DU MAILLAGE.
C        NBNO   : NOMBRE DE NOEUDS
C        NBGR   : NOMBRE DE GROUPES DE NOEUDS
C        NONOE  : NOM DES  NOEUDS
C        NOGRN  : NOM DES  GROUP_NO
C     SORTIES:
C        NBNOT  : NOMBRE TOTAL DE NOEUDS A IMPRIMER
C        NUMNO  : NOM DE L'OBJET CONTENANT LES NUMEROS
C                 DES NOEUDS TROUVES.
C ----------------------------------------------------------------------
C     ----------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8  ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32,JEXNUM,JEXNOM
      CHARACTER*80 ZK80
C     ------------------------------------------------------------------
      CHARACTER*8  NOMMA ,K8BID
      INTEGER JTOPO,INOE,INO,IGR,IRET,NBN,IAD,IN,LNUNO,JNUNO
C
C
      CALL JEMARQ()
      NOMMA=NOMA
      NBNOT= 0
      CALL JEVEUO('&&OP0039.LIST_TOPO','E',JTOPO)
      CALL JEVEUO(NUMNO,'E',JNUNO)
      CALL JELIRA(NUMNO,'LONMAX',LNUNO,K8BID )

C  --- TRAITEMENT DES LISTES DE NOEUDS ----
      IF(NBNO.NE.0) THEN
C     --- RECUPERATION DU NUMERO DE NOEUD ----
        DO 12 INOE=1,NBNO
          CALL JENONU(JEXNOM(NOMMA//'.NOMNOE',NONOE(INOE)),INO)
          IF (INO.EQ.0) THEN
            CALL UTDEBM('A','IRNONO',' ON NE TROUVE PAS LE NOEUD ')
            CALL UTIMPK('S',':',1,NONOE(INOE))
            CALL UTFINM
            NONOE(INOE) = ' '
          ELSE
            ZI(JTOPO-1+2) = ZI(JTOPO-1+2) + 1
            NBNOT = NBNOT + 1
            IF (NBNOT.GT.LNUNO) THEN
              CALL UTMESS('A','IRNONO',' LE NOMBRE DE NOEUDS '
     & //'SELECTIONNES EST SUPERIEUR AU NOMBRE DE NOEUDS DU MAILLAGE. '
     & //'ON VA TRONQUER LA LISTE.')
              NBNOT=NBNOT-1
              GO TO 9999
C             LNUNO=2*LNUNO
C             CALL JUVECA(NUMNO,LNUNO)
C             CALL JEVEUO(NUMNO,'E',JNUNO)
            END IF
            ZI(JNUNO-1+NBNOT)=INO
          ENDIF
  12    CONTINUE
      ENDIF
C  --- TRAITEMENT DES LISTES DE GROUPES DE NOEUDS ---
      IF(NBGR.NE.0) THEN
C     --- RECUPERATION DU NUMERO DE NOEUD ----
        DO 13 IGR=1,NBGR
          CALL JEEXIN(JEXNOM(NOMMA//'.GROUPENO',NOGRN(IGR)),IRET)
          IF (IRET.EQ.0) THEN
            CALL UTDEBM('A','IRNONO',' ON NE TROUVE PAS LE GROUPE ')
            CALL UTIMPK('S',':',1,NOGRN(IGR))
            CALL UTFINM
            NOGRN(IGR) = ' '
          ELSE
            CALL JELIRA(JEXNOM(NOMMA//'.GROUPENO',NOGRN(IGR)),
     +                       'LONMAX',NBN,K8BID)
            IF(NBN.EQ.0) THEN
            CALL UTDEBM('A','IRNONO',' LE GROUPE')
            CALL UTIMPK('S',' ',1,NOGRN(IGR))
            CALL UTIMPK('S','NE CONTIENT AUCUN NOEUD ',0,' ')
            CALL UTFINM
            NOGRN(IGR) = ' '
            ELSE
              ZI(JTOPO-1+4) = ZI(JTOPO-1+4) + 1
              CALL JEVEUO(JEXNOM(NOMMA//'.GROUPENO',NOGRN(IGR)),'L',IAD)
              DO 14 IN=1,NBN
                NBNOT=NBNOT+1
                IF (NBNOT.GT.LNUNO) THEN
                  CALL UTMESS('A','IRNONO',' LE NOMBRE DE NOEUDS '
     & //'SELECTIONNES EST SUPERIEUR AU NOMBRE DE NOEUDS DU MAILLAGE. '
     & //'ON VA TRONQUER LA LISTE.')
                  NBNOT=NBNOT-1
                  GO TO 9999
C                 LNUNO=2*LNUNO
C                 CALL JUVECA(NUMNO,LNUNO)
C                 CALL JEVEUO(NUMNO,'E',JNUNO)
                END IF
                ZI(JNUNO-1+NBNOT)= ZI(IAD+IN-1)
  14          CONTINUE
            ENDIF
          ENDIF
  13    CONTINUE
      ENDIF
C
9999  CONTINUE
      CALL JEDEMA()
      END
