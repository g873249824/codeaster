      SUBROUTINE  XPOLSN(ELREFP,ENTITE,N,JLSN,JLST,IMA,J,IAD,IGEOM,
     &                   NDIME,NDIM,JCONX1,JCONX2,CO,LSN,LST,D)
      IMPLICIT NONE

      INTEGER     N,J,JLSN,JLST,NDIM,NDIME
      INTEGER     IAD,IGEOM,IMA,JCONX1,JCONX2
      CHARACTER*8 ENTITE,ELREFP
      REAL*8      D,CO(3),LSN,LST

