      SUBROUTINE CMQLNM(MAIN,NBTNO,NBMA,NUMMAQ,NUNOMI,NBNM)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF PREPOST  DATE 30/08/2005   AUTEUR VABHHTS J.PELLET 
