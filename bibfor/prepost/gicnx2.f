C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT REAL*8 (A-H,O-Z)
C
C ----------------------------------------------------------------------
C     BUT : CREER  L'OBJET &&GILIRE.CONNEX2
C           QUI DONNE LA CONNECTIVITE DE TOUTES LES MAILLES LUES.
C
C ----------------------------------------------------------------------
C
C     FONCTIONS EXTERNES:
C     -------------------
C
C     VARIABLES LOCALES:
C     ------------------
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR,JEXR8
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C ---------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------
C
      CHARACTER*8  KBI,NOMOBJ
C
      CALL JEMARQ()
      CALL JEVEUO('&&GILIRE.NOMOBJ','L',IANOOB)
C
C     -- RECUPERATION DU NOMBRE D'OBJETS LUS:
      CALL JELIRA('&&GILIRE.DESCOBJ','LONMAX',NBOBJ4,KBI)
      CALL JEVEUO('&&GILIRE.DESCOBJ','L',IADESC)
      NBOBJ = NBOBJ4/4
C
C     -- CALCUL DES DIMENSIONS DE L'OBJET .CONNEX2:
      NBMATO=0
      LONT  =0
      DO 1 I=1,NBOBJ
         IF (ZI(IADESC-1+4*(I-1)+1).NE.0) GO TO 1
         NBNO=ZI(IADESC-1+4*(I-1)+3)
         NBEL=ZI(IADESC-1+4*(I-1)+4)
         NBMATO=NBMATO+NBEL
         LONT= LONT+NBEL*NBNO
 1    CONTINUE
C
C     -- CREATION DE L'OBJET .CONNEX2:
      CALL JECREC('&&GILIRE.CONNEX2','V V I','NU','CONTIG',
     +            'VARIABLE',NBMATO)
      CALL JEECRA('&&GILIRE.CONNEX2','LONT',LONT,' ')
      IMAT=0
      DO 2 I=1,NBOBJ
         IF (ZI(IADESC-1+4*(I-1)+1).NE.0) GO TO 2
         NBNO=ZI(IADESC-1+4*(I-1)+3)
         NBEL=ZI(IADESC-1+4*(I-1)+4)
         NOMOBJ=ZK8(IANOOB-1+2*(I-1)+1)
         IF (NBEL.EQ.0) GO TO 2
         CALL JEVEUO('&&GILIRE'//NOMOBJ//'.CONNEX','L',IACNEX)
         DO 3 IMA = 1,NBEL
            IMAT = IMAT +1
            CALL JECROC(JEXNUM('&&GILIRE.CONNEX2',IMAT))
            CALL JEECRA(JEXNUM('&&GILIRE.CONNEX2',IMAT),
     +      'LONMAX',NBNO,' ')
            CALL JEVEUO(JEXNUM('&&GILIRE.CONNEX2',IMAT),'E',IACNX2)
            DO 4 INO=1,NBNO
               ZI(IACNX2-1+INO)=ZI(IACNEX-1+NBNO*(IMA-1)+INO)
 4          CONTINUE
 3       CONTINUE
 2    CONTINUE
C
 9999 CONTINUE
      CALL JEDEMA()
      END
