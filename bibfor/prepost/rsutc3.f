C     VERIFICATION DU MODE D'ACCES EN FONCTION DU TYPE DE RESULTAT

C IN  : TYPRES : K16  : TYPE DE RESULTAT ('EVOL_THER', 'EVOL_ELAS',...)
C IN  : NOMACC : K4   : MODE D'ACCES  ('INST', 'FREQ')
C OUT : RSUTC3 : L    : .TRUE.   LE MODE D ACCES EST COMPATIBLE AVEC
C                                LE TYPE DE RESULAT
C                       .FALSE.  LE MODE D ACCES EST INCOMPATIBLE AVEC
C                                LE TYPE DE RESULTAT

C----------------------------------------------------------------------

      RSUTC3 = .FALSE.

      IF (NOMACC.EQ.'INST') THEN
        IF (TYPRES.EQ.'EVOL_ELAS') THEN
          RSUTC3 = .TRUE.
        ELSE IF (TYPRES.EQ.'EVOL_THER') THEN
          RSUTC3 = .TRUE.
        ELSE IF (TYPRES.EQ.'EVOL_CHAR') THEN
          RSUTC3 = .TRUE.
        ELSE IF (TYPRES.EQ.'EVOL_NOLI') THEN
          RSUTC3 = .TRUE.
        ELSE IF (TYPRES.EQ.'DYNA_TRANS') THEN
          RSUTC3 = .TRUE.
        END IF

      ELSE IF (NOMACC.EQ.'FREQ') THEN
        IF (TYPRES.EQ.'DYNA_HARMO') THEN
          RSUTC3 = .TRUE.
        ELSE IF (TYPRES.EQ.'HARM_GENE') THEN
          RSUTC3 = .TRUE.
        END IF
      ELSE

        CALL UTMESS('F','RSUTC3',
     +              'LE TYPE DE RESULTAT DEMANDE N''EST PAS PREVU')
      END IF

      END
