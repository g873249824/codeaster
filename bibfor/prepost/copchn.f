C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C   ON PLONGE VEC1(NEQ1) ASSOCIE A NUME1 DANS VEC2(NEQ2) ASSOCIE A NUME2
C     ------------------------------------------------------------------
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER          ZI
      COMMON  /IVARJE/ ZI(1)
      REAL*8           ZR
      COMMON  /RVARJE/ ZR(1)
      COMPLEX*16       ZC
      COMMON  /CVARJE/ ZC(1)
      LOGICAL          ZL
      COMMON  /LVARJE/ ZL(1)
      CHARACTER*8      ZK8
      CHARACTER*16            ZK16
      CHARACTER*24                    ZK24
      CHARACTER*32                            ZK32
      CHARACTER*80                                    ZK80
      COMMON  /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32     JEXNOM, JEXNUM
C     -----  FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      REAL*8       VEC1(NEQ1), VEC2(NEQ2)
      INTEGER      APRNO1, APRNO2, GD1, GD2
      CHARACTER*8  NOMA
      CHARACTER*14 NUME1, NUME2
      CHARACTER*24 NPRNO1, NPRNO2
      CHARACTER*24 K8B
C     ------------------------------------------------------------------
      CALL JEMARQ( )
C
C
C--------RECUPERATION DU NOMBRE D'EQUATIONS DU SYSTEME PHYSIQUE
C
      CALL DISMOI('F','NB_NO_MAILLA',NOMA ,'MAILLAGE' ,NBNOEU,K8B,IERD)
      CALL DISMOI('F','NUM_GD_SI'   ,NUME1 ,'NUME_DDL' ,GD1  ,K8B,IERD)
      CALL DISMOI('F','NUM_GD_SI'   ,NUME2 ,'NUME_DDL' ,GD2  ,K8B,IERD)
C
C     --- ECRITURE DESCRIPTION NOEUDS STRUCTURE ---
      NPRNO1 = NUME1//'.NUME.PRNO'
      NPRNO2 = NUME2//'.NUME.PRNO'
      CALL JENONU(JEXNOM(NPRNO1(1:19)//'.LILI','&MAILLA'),IBID)
      CALL JEVEUO(JEXNUM(NPRNO1,IBID),'L',APRNO1)
      CALL JENONU(JEXNOM(NPRNO2(1:19)//'.LILI','&MAILLA'),IBID)
      CALL JEVEUO(JEXNUM(NPRNO2,IBID),'L',APRNO2)
      NEC1 = NBEC(GD1)
      NEC2 = NBEC(GD2)
      DO 1 IEQ = 1,NEQ2
         VEC2(IEQ) =  0.D0
 1    CONTINUE
      DO 2 INO = 1,NBNOEU
         IDDL1 = ZI( APRNO1 + (NEC1+2)*(INO-1) + 1 - 1 )
         NCMP1 = ZI( APRNO1 + (NEC1+2)*(INO-1) + 2 - 1 )
         IDDL2 = ZI( APRNO2 + (NEC2+2)*(INO-1) + 1 - 1 )
         NCMP2 = ZI( APRNO2 + (NEC2+2)*(INO-1) + 2 - 1 )
         NCMP = MIN(NCMP1,NCMP2)
         IF (IDDL1.EQ.0.OR.IDDL2.EQ.0) GOTO 2
         DO 3 IC = 1,NCMP
            VEC2(IDDL2+IC-1) =  VEC1(IDDL1+IC-1)
 3       CONTINUE
 2    CONTINUE
C
C
      CALL JEDEMA()
 9999 CONTINUE
C
      END
