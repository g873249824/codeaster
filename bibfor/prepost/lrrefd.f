C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
C     BUT:
C       * REMPLIR LE .REFD A PARTIR DES DONNEES UTILISATEUR
C       * RETOURNER LE NOM D'UN PROF_CHNO QUI SERVIRA A NUMEROTER
C         LES CHAM_NO DE "DEPL" (DEPL/VITE/ACCE)
C       * PEUT RETOURNER PRCHND=' '
C
C     ARGUMENTS:
C     ----------
C
C      ENTREE :
C-------------
C IN   RESU     : NOM DE LA SD_RESULTAT
C
C      SORTIE :
C-------------
C OUT  PRCHND   : PROFIL DES CHAM_NO DEPL/VITE/ACCE
C
C ......................................................................
C
C

C
C
      INTEGER IRET,IRET1,IRET2,IBID
      INTEGER JREFD

      CHARACTER*8  MATRIG,MATMAS
      CHARACTER*14 NUDDLR, NUDDLM
      CHARACTER*19 PRONUR, PRONUM
      LOGICAL      IDENSD
      INTEGER      IARG
C
C ----------------------------------------------------------------------
C
C     SI L'UTILISATEUR NE DONNE PAS DE NUME_DDL ON LE DEDUIT DE LA
C     DE LA MATRICE DE RIGIDITE (MATRIG).
C
      CALL JEMARQ()

      PRCHND = ' '
      MATRIG  = ' '
      MATMAS  = ' '
      NUDDLR  = ' '
      NUDDLM  = ' '

      IBID = 1

      CALL GETVID(' ','MATR_RIGI',IBID,IARG,1,MATRIG,IRET1)
      CALL GETVID(' ','MATR_MASS',IBID,IARG,1,MATMAS,IRET2)

      IF (IRET1.EQ.1) THEN
        CALL U2MESK('I','PREPOST_14',1,MATRIG)
        CALL DISMOI('F','NOM_NUME_DDL',MATRIG,'MATR_ASSE',
     &                 IBID,NUDDLR,IRET)
        CALL DISMOI('F','PROF_CHNO',NUDDLR,'NUME_DDL',
     &                   IBID,PRCHND,IRET)
      ENDIF

C     VERIFICATION : LES NUME_DDL DES MATRICES A ET B SONT IDENTIQUES
      IF (IRET1.EQ.1 .AND. IRET2.EQ.1) THEN
        CALL DISMOI('F', 'NOM_NUME_DDL', MATMAS, 'MATR_ASSE',
     &              IBID, NUDDLM, IRET)
        IF (NUDDLM.NE.NUDDLR) THEN
          PRONUR=(NUDDLR//'.NUME')
          PRONUM=(NUDDLM//'.NUME')
          IF (.NOT.IDENSD('PROF_CHNO',PRONUR,PRONUM)) THEN
            CALL U2MESS('F','ALGELINE2_79')
          ENDIF
        ENDIF
      ENDIF

      CALL WKVECT(RESU//'           .REFD','G V K24',7,JREFD)
      ZK24(JREFD-1+1) = MATRIG
      ZK24(JREFD-1+2) = MATMAS
      ZK24(JREFD-1+3) = ' '
      ZK24(JREFD-1+4) = NUDDLR
      ZK24(JREFD-1+5) = ' '
      ZK24(JREFD-1+6) = ' '
      ZK24(JREFD-1+7) = ' '
C
      CALL JEDEMA()
C
      END
