C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE GENIAUT S.GENIAUT

      IMPLICIT NONE

      CHARACTER*8   MA1,MA2
      INTEGER       NBMA,DIRMA(NBMA),NBNO,DIRNO(NBNO)
C
C   COPIE DANS LE MAILLAGE MA2 DES MAILLES ET DES NOEUDS DU MAILLAGE MA1
C   CONTENUS DANS LES TABLEAUX D'INDIRECTION DIRMA ET DIRNO
C
C   IN
C       DIRMA : TABLEAU DE CORRESPONDANCE DES NUMEROS DE MAILLES
C       DIRNO : TABLEAU DE CORRESPONDANCE DES NUMEROS DE NOEUDS
C       NBMA  : LONGUEUR DE DIRMA
C       NBNO  : LONGUEUR DE DIRNO
C       MA1   : NOM DU MAILLAGE SAIN
C
C   OUT
C       MA2   : NOM DU MAILLAGE FISSURE


C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNOM,JEXNUM,JEXATR
C---------------- FIN COMMUNS NORMALISES  JEVEUX  ----------------------

      INTEGER       JTYPM1,JTYPM2,I,J,IBID,L1,L2,INO1,IRET,NBGN,IAGNO
      INTEGER       IGEOMR,IADESC,IAREFE,IACOO1,IACOO2,IACON1,N,IACON2
      INTEGER       INO2
      CHARACTER*8   LICMPR(3),NOMA2,NONO2,K8BID

      CALL JEMARQ()

C     RECUP DES .TYPMAIL, .COORDO DU MAILLAGE 1 ET 2
      CALL JEVEUO(MA1//'.TYPMAIL','L',JTYPM1)
      CALL JEVEUO(MA2//'.TYPMAIL','E',JTYPM2)
      CALL JEVEUO(MA1//'.COORDO    .VALE','L',IACOO1)
      CALL JEVEUO(MA2//'.COORDO    .VALE','E',IACOO2)

      CALL JEEXIN(MA2//'.GROUPENO',IRET)
      NBGN = 0
      IF (IRET.GT.0) CALL JELIRA(MA2//'.GROUPENO','NUTIOC',NBGN,K8BID)

C     ---------------------------------------------------------------  
C     COPIE DES VECTEURS
C     ---------------------------------------------------------------  

C     .NOMMAI ET .TYPMAIL
      DO 100 I=1,NBMA
        IF (DIRMA(I).NE.0) THEN
          CALL JENUNO(JEXNUM(MA1//'.NOMMAI',DIRMA(I)),NOMA2)
          CALL JECROC(JEXNOM(MA2//'.NOMMAI',NOMA2))
          ZI(JTYPM2-1+DIRMA(I)) = ZI(JTYPM1-1+I)
        ENDIF
 100  CONTINUE

C     .NOMNOE
      DO 200 I=1,NBNO
        IF (DIRNO(I).NE.0) THEN
          CALL JENUNO(JEXNUM(MA1//'.NOMNOE',DIRNO(I)),NONO2)
          CALL JECROC(JEXNOM(MA2//'.NOMNOE',NONO2))
        ENDIF
 200  CONTINUE

C     .COORDO 
      DO 300 I=1,NBNO
        IF (DIRNO(I).NE.0) THEN
          DO 310 J=1,3
            ZR(IACOO2-1+3*(DIRNO(I)-1)+J)=ZR(IACOO1-1+3*(I-1)+J)
 310      CONTINUE
        ENDIF
 300  CONTINUE

C     .CONNEX
      DO 400 I=1,NBMA
        IF (DIRMA(I).NE.0) THEN
          CALL JEVEUO(JEXNUM(MA1//'.CONNEX',I),'L',IACON1)
          CALL JELIRA(JEXNUM(MA1//'.CONNEX',I),'LONMAX',N,K8BID)
          CALL JEECRA(JEXNUM(MA2//'.CONNEX',DIRMA(I)),'LONMAX',N,K8BID)
          CALL JEVEUO(JEXNUM(MA2//'.CONNEX',DIRMA(I)),'E',IACON2)
          DO 410 J=1,N
            INO1 = ZI(IACON1-1+J)
            INO2 = DIRNO(INO1)
            ZI(IACON2-1+J)=INO2
 410      CONTINUE
        ENDIF
 400  CONTINUE

C     .GROUPENO
      DO 500 I=1,NBGN
        CALL JEVEUO(JEXNUM(MA2//'.GROUPENO',I),'E',IAGNO)
        CALL JELIRA(JEXNUM(MA2//'.GROUPENO',I),'LONMAX',N,K8BID)
        DO 501 J=1,N
          IF (DIRNO(ZI(IAGNO-1+J)).NE.0) THEN
            ZI(IAGNO-1+J)=DIRNO(ZI(IAGNO-1+J))
          ENDIF
 501    CONTINUE
 500  CONTINUE

      CALL JEDEMA()
      END
