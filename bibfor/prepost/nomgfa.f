C
      CHARACTER*56 SAUX56
      INTEGER IAUX
      LOGICAL EXIGFA
C
C     ------------------------------------------------------------------
C
      SAUX56 = ' '
C
      NBGF = 0
      DO 10 , IAUX = 1,NBGR
        IF ( EXIGFA(DGF,IAUX) ) THEN
          NBGF = NBGF + 1
          NOGRF(NBGF)(1:24) = NOGR(IAUX)
          NOGRF(NBGF)(25:80) = SAUX56
        ENDIF
