      SUBROUTINE CGAJID(IFOND,IFISS,FOND,FISS,TAB)
      IMPLICIT NONE

      INTEGER     IFOND,IFISS
      CHARACTER*8 TAB,FOND,FISS
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF PREPOST  DATE 20/09/2011   AUTEUR GENIAUT S.GENIAUT 
C ======================================================================
C COPYRIGHT (C) 1991 - 2011  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY  
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C     - FONCTION REALISEE:   AJOUTE UNE COLONNE DANS LA TABLE 
C                            POUR IDENTIFIER LA FISSURE/FOND_FISS

C IN       IFOND  : INDIQUE SI FOND_FISS A ETE RENSEIGNE
C IN       IFISS  : INDIQUE SI FISSURE A ETE RENSEIGNE
C IN       FOND   : SD FOND_FISS 
C IN       FISS   : SD FISS_XFEM
C IN/OUT   TAB    : TABLE RESULTAT

      CHARACTER*6  NOMPRO
      CHARACTER*8  NOMPAR,VALPAR
      CHARACTER*19 TAB19,LPAR
      INTEGER      JTBNP,NBLIG,I,IBID,JVPAR
      REAL*8       RBID
      COMPLEX*16   CBID
      PARAMETER   (NOMPRO='CGAJID')

C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------

      CHARACTER*32 JEXNOM
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------

      CALL JEMARQ()

C     NOMPAR : NOM DE LA NOUVELLE COLONNE
C     VALPAR : VALEUR A AJOUTER
      IF (IFOND.NE.0) THEN
        NOMPAR = 'FOND_FISS'
        VALPAR = FOND
      ELSEIF (IFISS.NE.0) THEN
        NOMPAR = 'FISSURE'
        VALPAR = FISS
      ENDIF

      IF (IFOND.NE.0.OR.IFISS.NE.0) THEN

C       IL FAUT CONVERTIR LE NOM EN K19
        TAB19 = TAB
        CALL JEVEUO(TAB19//'.TBNP','L',JTBNP)

C       NB DE LIGNES DANS LA TABLE
        NBLIG = ZI(JTBNP-1+2)

C       VECTEUR CONTENANT LA LISTE DES VALEURS A AJOUTER
        LPAR = '&&'//NOMPRO//'NOMFIS'
        CALL WKVECT(LPAR,'V V K8',NBLIG,JVPAR)        
        DO 100 I=1,NBLIG
          ZK8(JVPAR-1+I)=VALPAR
 100    CONTINUE

C       ON AJOUTE CETTE COLONNE DANS LA TABLE
        CALL TBAJCO(TAB,NOMPAR,'K8',NBLIG,
     &              IBID,RBID,CBID,ZK8(JVPAR),'A',-1)

        CALL JEDETR(LPAR)

      ENDIF

      CALL JEDEMA()
      END
