      SUBROUTINE LRVCPG(IDFIMD,NBPGM, NBPGA, NOMTM, TYGEOS,
     &                  ELREFA,ELREFM,FAPG,  NLOC,  PERMU,
     &                  NUTYMA,CODRET)
