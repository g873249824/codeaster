C  ENTREES :
C     TYPENT = TYPE D'ENTITES DANS LA FAMILLE
C              1 : NOEUDS, 2 : MAILLES
C     NUMFAM = NUMERO DE LA FAMILLE
C     NOGRF  = NOMS DES GROUPES D ENTITES DE LA FAMILLE
C     NBGF   = NOMBRE DE GROUPES DE LA FAMILLE
C  SORTIES :
C     NOMFAM = NOM DE LA FAMILLE
C     ------------------------------------------------------------------
C
      IMPLICIT NONE
C
C 0.1. ==> ARGUMENTS
C
      INTEGER TYPENT, NUMFAM
      INTEGER NBGF
      CHARACTER*80 NOGRF(*)
      CHARACTER*32 NOMFAM
C
C 0.2. ==> COMMUNS
C
C 0.3. ==> VARIABLES LOCALES
C
      INTEGER IAUX, JAUX
C
      CHARACTER*8 SAUX08
C
      INTEGER LXLGUT
C
C====
C 1. ON INITIALISE AVEC DES '_'
C====
C
C               12345678901234567890123456789012
      NOMFAM = '________________________________'
C
C====
C 2. TANT QU'IL Y A MOINS DE 3 GROUPES DANS LA FAMILLE, LE NOM DE LA
C    FAMILLE EST LA CONCATENATION DE CES NOMS DE GROUPES.
C    ON REMPLACE TOUS LES EVENTUELS BLANCS PAR DES '_'
C    ON MET 'N' OU 'M' EN DERNIER CARACTERE POUR AVOIR DES NOMS
C    DIFFERENTS SUR LES FAMILLES DE NOEUDS OU DE MAILLES QUAND ELLES
C    FONT REFERENCES A DES GROUPES DE MEME NOM
C====
C
      IF ( NBGF.LE.3 ) THEN
C
        DO 21 , IAUX = 1 , NBGF
          JAUX = LXLGUT(NOGRF(IAUX)(1:8))
          NOMFAM(8*(IAUX-1)+1:8*(IAUX-1)+JAUX) = NOGRF(IAUX)(1:JAUX)
   21   CONTINUE
C
        IF ( TYPENT.EQ.1 ) THEN
          NOMFAM(32:32) = 'N'
        ELSE
          NOMFAM(32:32) = 'M'
        ENDIF
C
C====
C 3. SINON, C'EST UN NOM ARBITRAIRE
C====
C
      ELSE
C
C                      12345678
        NOMFAM(1:8) = 'FAMILLE_'
C
        IF ( TYPENT.EQ.1 ) THEN
          IAUX = 14
C                           901234
          NOMFAM(9:IAUX) = 'NOEUD_'
          
        ELSE
          IAUX = 15
C                           9012345
          NOMFAM(9:IAUX) = 'MAILLE_'
        ENDIF
C
        CALL CODENT ( NUMFAM,'G',SAUX08 )
C
        JAUX = LXLGUT(SAUX08)
        NOMFAM(IAUX+1:IAUX+JAUX) = SAUX08
C
      ENDIF
C
      END
