C     EXTRACTION DU CHAMP IFIELD DE L'ENREGISTREMENT

C IN  : MOT    : K80  : ENREGISTREMENT CONTENANT LE CHAMP A TRAITER
C OUT : FIELD  : K80  : CHAMP DE NUMERO IFIELD EXTRAIT DE
C                       L'ENREGISTREMENT
C IN  : IFLIED : I    : NUMERO DU CHAMP RECHERCHE

C-----------------------------------------------------------------------
      INTEGER NBMOT,NBCAR,IDEB,I,J

C- INITIALISATION

      DO 10 I = 1,80
        FIELD(I:I) = ' '
   10 CONTINUE

      NBMOT = 0
      NBCAR = 0
      J = 0

C- RECHERCHE DU MOT A TRAITER

      DO 20 I = 1,80
        IF (MOT(I:I).NE.' ') THEN
          NBCAR = NBCAR + 1
          IF (NBCAR.EQ.1) IDEB = I
        ELSE
          IF (NBCAR.NE.0) THEN
            NBMOT = NBMOT + 1
            IF (NBMOT.EQ.IFIELD) GO TO 30
            NBCAR = 0
          END IF
        END IF
   20 CONTINUE

C-TRANSFERT DU CHAMP IFIELD A TRAITER

   30 CONTINUE
      DO 40 I = 1,NBCAR
        J = IDEB - 1 + I
        IF (MOT(J:J).NE.' ') THEN
          FIELD(I:I) = MOT(J:J)
        ELSE
          GO TO 50
        END IF
   40 CONTINUE
   50 CONTINUE

      END
