      SUBROUTINE IRCMPN ( NOFIMD,
     >                    NCMPRF, NCMPVE, NTLCMP,
     >                    NBVATO, NBNOEC, LINOEC, ADSL,
     >                    NTPROA, NOPFTY, NVAL )
