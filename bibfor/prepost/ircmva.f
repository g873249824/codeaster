      SUBROUTINE IRCMVA ( NTLCMP, NCMPVE, NCMPRF,
     >                    NVALEC, NBPG, NBSP,
     >                    ADSV, ADSD, ADSL,
     >                    TYPMAI, TYMAST,
     >                    VAL, NTPROA )
