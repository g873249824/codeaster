      INTEGER NBKN,KNINDI,LONG
      CHARACTER*(*) KN,LKN(*)
C ----------------------------------------------------------------------
C BUT: TROUVER LE RANG D'UNE CHAINE DE CARACTERES DANS UNE LISTE DE
C      CHAINES. ON CHERCHE LE RANG DE SA 1ERE OCCURENCE.
C      SI LA CHAINE N'EST PAS TROUVEE : KNINDI=0

C LONG    IN   I  : 8/16/24 : LONGUEUR DES CHAINES DE LKN
C KN      IN   K* : CHAINE A CHERCHER
C LKN     IN   V(K*)  : LISTE DE CHAINES (K8/K16/24)
C NBKN    IN   I  : LONGUEUR DE LA LISTE LKN
C KNINDI  IN   I  : RANG DE LA 1ERE OCCURENCE DE KN DANS LKN
C                   SI KN EST ABSENT: INDK=0

C ----------------------------------------------------------------------
      INTEGER INDIK8,INDK16,INDK24
      CHARACTER*8 K8
      CHARACTER*16 K16
      CHARACTER*24 K24
C DEB-------------------------------------------------------------------


      IF ((LONG.NE.8) .AND. (LONG.NE.16) .AND.
     &    (LONG.NE.24)) CALL U2MESS('F','CALCULEL2_75')

      IF (LONG.EQ.8) THEN
        K8 = KN
        KNINDI = INDIK8(LKN,K8,1,NBKN)
      ELSE IF (LONG.EQ.16) THEN
        K16 = KN
        KNINDI = INDK16(LKN,K16,1,NBKN)
      ELSE IF (LONG.EQ.24) THEN
        K24 = KN
        KNINDI = INDK24(LKN,K24,1,NBKN)
      END IF

      END
