C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MEUNIER S.MEUNIER
C
      IMPLICIT NONE
      CHARACTER*16 TYPMAI
      CHARACTER*8  MAIL,NOMO
      CHARACTER*8  NOMARL
      CHARACTER*10 NOM1,NOM2,NOMC
      CHARACTER*10 NORM,TANG
      CHARACTER*8  CINE(3)
      REAL*8       LCARA
      CHARACTER*10 QUADRA
      INTEGER      GRMED,DIME
C
C ----------------------------------------------------------------------
C
C ROUTINE ARLEQUIN
C
C CALCUL DES MATRICES DE COUPLAGE ELEMENTAIRE ARLEQUIN
C ROUTINE D'AIGUILLAGE SUIVANT GROUPE MEDIATEUR
C
C ----------------------------------------------------------------------
C
C
C IN  MAIL   : NOM DU MAILLAGE
C IN  NOMO   : NOM DU MODELE
C IN  NOMARL : NOM DE LA SD PRINCIPALE ARLEQUIN
C IN  NOM1   : NOM DE LA SD DE STOCKAGE MAILLES GROUP_MA_1
C IN  NOM2   : NOM DE LA SD DE STOCKAGE MAILLES GROUP_MA_2
C IN  NORM   : NOM DE LA SD POUR STOCKAGE DES NORMALES
C IN  TANG   : NOM DE L'OBJET TANGENTES LISSEES
C IN  TYPMAI : SD CONTENANT NOM DES TYPES ELEMENTS (&&CATA.NOMTM)
C IN  LCARA  : LONGUEUR CARACTERISTIQUE POUR TERME DE COUPLAGE
C IN  NOMC   : NOM DE LA SD POUR LES MAILLES DE COLLAGE
C IN  QUADRA : SD DES QUADRATURES A CALCULER
C IN  CINE   : CINEMATIQUES DES GROUPES DE MAILLE
C IN  GRMED  : GROUPE DE MAILLES SERVANT DE MEDIATEUR (1 OU 2)
C IN  DIME   : DIMENSION DE L'ESPACE GLOBAL (2 OU 3)
C
C
C ----------------------------------------------------------------------
C
      CHARACTER*10 NOMGR1,NOMGR2
      CHARACTER*8  CINE1,CINE2
      INTEGER      IFM,NIV

      INTEGER      NI,NR,NK
      PARAMETER   ( NI = 1 , NR = 1 , NK = 1 )
      INTEGER      VALI(NI)
      REAL*8       VALR(NR)
      CHARACTER*24 VALK(NK)

      CHARACTER*6  NOMPRO
      PARAMETER   (NOMPRO='ARLCOU')
C
C ----------------------------------------------------------------------
C
      CALL INFNIV(IFM,NIV)
C
C --- INITIALISATIONS
C
      IF (GRMED.EQ.1) THEN
        NOMGR1 = NOM1
        NOMGR2 = NOM2
        CINE1  = CINE(1)
        CINE2  = CINE(2)
      ELSEIF (GRMED.EQ.2) THEN
        NOMGR1 = NOM2
        NOMGR2 = NOM1
        CINE1  = CINE(2)
        CINE2  = CINE(1)
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- CALCUL DES MATRICES DE COUPLAGE ELEMENTAIRE ARLEQUIN
C
      CALL ARLDBG(NOMPRO,NIV,IFM,1,NI,VALI,NR,VALR,NK,VALK)
C
      CALL ARLCPL(MAIL  ,NOMO  ,NOMARL,TYPMAI,QUADRA,
     &            NOMC  ,NOMGR1,NOMGR2,CINE1 ,CINE2 ,
     &            NORM  ,TANG  ,LCARA ,DIME)
C
C      NIV = 1
      IF (NIV.GE.2) THEN
        CALL ARLDBG(NOMPRO,NIV,IFM,2,NI,VALI,NR,VALR,NK,VALK)
        CALL MORSIM(IFM,NOMC,NOMGR2)
        CALL ARLDBG(NOMPRO,NIV,IFM,3,NI,VALI,NR,VALR,NK,VALK)
        CALL MORSIM(IFM,NOMC,NOMGR1)
      ENDIF
C
C --- NETTOYAGE DES SD DE QUADRATURES
C
      CALL ARLDSD('QUADRATURES',QUADRA)
C
      END
