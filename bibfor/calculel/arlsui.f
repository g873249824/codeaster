      IMPLICIT NONE
      INTEGER       NNM,NGM
      PARAMETER     (NNM = 27)
      PARAMETER     (NGM = 64)
C
      CHARACTER*8   TYPEMA
      INTEGER       FAMIL
      INTEGER       NG,NNO
      REAL*8        WG(NGM)
      REAL*8        FG(NGM*NNM)
      REAL*8        DFG(3*NGM*NNM)
C
C ----------------------------------------------------------------------
C
C ROUTINE ARLEQUIN - UTILITAIRE
C
C INFORMATIONS SUR LA MAILLE SUPPORT D'INTEGRATION
C
C ----------------------------------------------------------------------
C
C
C PAR NNM    : NOMBRE MAXI DE NOEUDS PAR MAILLE
C PAR NGM    : NOMBRE MAXI DE POINTS DE GAUSS PAR MAILLE
C IN  TYPEMA : TYPE DE LA MAILLE
C IN  FAMIL  : FAMILLE D'INTEGRATION
C OUT NNO    : NOMBRE DE NOUEDS
C OUT NG     : NOMBRE DE POINTS DE GAUSS
C OUT WG     : POIDS DE GAUSS
C OUT FG     : FONCTIONS DE FORME AUX POINTS DE GAUSS
C OUT DFG    : DERIV./PARAM DES FONCTIONS DE FORME
C               AUX POINTS DE GAUS
C
C DERIV./PARAM: DERIVEES PAR RAPPORT AUX COORDONNEES DE L'ESPACE
C               PARAMETRIQUE
C
C ----------------------------------------------------------------------
C
      REAL*8        G(3*NGM)
      CHARACTER*8   NOMFAM
C
C ----------------------------------------------------------------------
C

C
C --- NOM DE LA FAMILLE DE POINTS DE GAUSS
C
      CALL PFAMIL(TYPEMA,FAMIL ,NOMFAM)
C
C --- NOMBRE, POIDS ET COORDONNEES DES POINTS DE GAUSS
C
      CALL PGAUSS(TYPEMA,NOMFAM,NNO,WG    ,G     ,NG    )
      IF (NG.GT.NGM) THEN
        CALL U2MESK('F','ARLEQUIN_99',1,'ARLSUI_1')
      ENDIF
C
C --- FONCTIONS DE FORME ET LEURS DERIVEES PREMIERES AUX POINTS DE GAUSS
C
      CALL PFORME(TYPEMA,NG    ,G     ,FG    ,DFG   )

      END
