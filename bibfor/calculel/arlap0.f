      SUBROUTINE ARLAP0(MAIL  ,TYPMAI,NORM  ,GRMAMA,DEGMAX,
     &                  NGRMA1,NOMBO1,NGRMA2,NOMBO2,NOMCOL,
     &                  NOMAPP,NOMARB,NBMAC ,TRAVR ,NHAPP ,
     &                  NAPP)
C
