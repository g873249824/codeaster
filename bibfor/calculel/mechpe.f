C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C     ------------------------------------------------------------------
C IN  : MODELE : NOM DU MODELE
C IN  : NCHAR  : NOMBRE DE CHARGES
C IN  : LCHAR  : LISTE DES CHARGES
C OUT : CHCHAR : NOM DU CHAMP DE CHARGEMENT TROUVE
C     ------------------------------------------------------------------
C
C    ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNUM
C     ----- FIN COMMUNS NORMALISES  JEVEUX  ----------------------------
C
      INTEGER       ICHA,IRET,NPESA,NBGREL,IGREL,IALIEL,NEL,ITYPEL
      CHARACTER*19  LIGREL,NOLIG
      CHARACTER*1   K1BID
      CHARACTER*16  NOMTE
      CHCHAR = '&&MECHAR.CH_R'
      NPESA = 0
      DO 10 ICHA = 1,NCHAR
         CALL JEEXIN(LCHAR(ICHA)(1:8)//'.CHME.PESAN.VALE',IRET)
         IF (IRET.NE.0) THEN
           CHCHAR = LCHAR(ICHA)(1:8)//'.CHME.PESAN'
           NPESA = NPESA + 1
         ENDIF
 10   CONTINUE
C
C     CAS NON GERE POUR LE CALCUL DE RIGI_MECA / FORC_NODA : PLUSIEURS 
C     CHARGEMENTS DE PESANTEUR EN PRESENCE D ELEMENTS 2D DE COUPLAGE 
C     PESANTEUR - SURFACE LIBRE D UN FLUIDE
C
      IF (NPESA.GT.1) THEN
        LIGREL = MODELE//'.MODELE'
        NOLIG  = LIGREL(1:19)
        CALL JELIRA(NOLIG//'.LIEL','NUTIOC',NBGREL,K1BID)
        DO 20 IGREL = 1,NBGREL
          CALL JEVEUO(JEXNUM(NOLIG//'.LIEL',IGREL),'L',IALIEL)
          CALL JELIRA(JEXNUM(NOLIG//'.LIEL',IGREL),'LONMAX',NEL,K1BID)
          ITYPEL = ZI(IALIEL-1+NEL)
          CALL JENUNO(JEXNUM('&CATA.TE.NOMTE',ITYPEL),NOMTE)
          IF  (NOMTE(1:4).EQ.'MEFP') THEN
            CALL U2MESS ('F','CALCULEL4_19')
          END IF
  20    CONTINUE
      ENDIF
C
      END
