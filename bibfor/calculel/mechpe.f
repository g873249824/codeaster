      SUBROUTINE MECHPE (MODELE,NCHAR,LCHAR,CHCHAR)
      IMPLICIT NONE
      CHARACTER*8        MODELE
      INTEGER            NCHAR
      CHARACTER*(*)      LCHAR(*)
      CHARACTER*24       CHCHAR
C     ------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF CALCULEL  DATE 09/01/2012   AUTEUR IDOUX L.IDOUX 
C ======================================================================
