      INCLUDE 'jeveux.h'

      CHARACTER*32 JEXATR
      INTEGER OPT,TE,MEMOIR
C ----------------------------------------------------------------------
C     ENTREES:
C        OPT : OPTION
C         TE : TYPE_ELEMENT
C     MEMOIR : 1 : ON MET EN MEMOIRE LES OBJETS CATALOGUE NECESSAIRES
C              0 : ON NE FAIT PAS DE JEVEUX CAR LES OBJETS SONT DEJA LA
C                  (POUR ETRE PLUS RAPIDE DANS CALCUL).

C     SORTIES:
C     NUCALC : NUMERO DU CALCUL ELEMENTAIRE A LANCER

C ----------------------------------------------------------------------
      INTEGER IAOPTT,LGCO,IAOPMO,ILOPMO,IAOPNO,ILOPNO,IAOPDS
      INTEGER IAOPPA,NPARIO,NPARIN,IAMLOC,ILMLOC,IADSGD
      COMMON /CAII02/IAOPTT,LGCO,IAOPMO,ILOPMO,IAOPNO,ILOPNO,IAOPDS,
     +       IAOPPA,NPARIO,NPARIN,IAMLOC,ILMLOC,IADSGD

C     VARIABLES LOCALES:
C     ------------------
      INTEGER OPTMOD,JJ,IANBLC

C DEB-------------------------------------------------------------------
      CALL ASSERT(MEMOIR.EQ.0 .OR. MEMOIR.EQ.1)
      IF (MEMOIR.EQ.1) THEN
        CALL JEVEUO('&CATA.TE.OPTTE','L',IAOPTT)
        CALL JEVEUO('&CATA.TE.OPTMOD','L',IAOPMO)
        CALL JEVEUO(JEXATR('&CATA.TE.OPTMOD','LONCUM'),'L',ILOPMO)
        CALL JEVEUO('&CATA.TE.NBLIGCOL','L',IANBLC)
        LGCO = ZI(IANBLC-1+1)
      ENDIF

C     JJ = IOPTTE(OPT,TE)
      JJ = ZI(IAOPTT-1+ (TE-1)*LGCO+OPT)
      IF (JJ.EQ.0) THEN
C        -- LE TYPE_ELEMENT TE NE SAURA JAMAIS CALCULER L'OPTION OPT:
        NUCALC = 0
      ELSE
        OPTMOD = IAOPMO + ZI(ILOPMO-1+JJ) - 1
        NUCALC = ZI(OPTMOD-1+1)
      END IF
      END
