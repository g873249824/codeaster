C-----------------------------------------------------------------------
      INTEGER IERD ,ILICHC ,INUME ,NBCHCI 
C-----------------------------------------------------------------------
      CALL JEMARQ()
C
      CALL INFMAJ
C
C --- RECUPERATION DES ARGUMENTS DE LA COMMANDE
C
      CALL GETRES(VCINE,TYPE,OPER)
C
C --- INST DE CALCUL
C
      CALL GETVR8(' ','INST',0,IARG,1,INST,IBID)
C
C --- NUME_DDL
C
      CALL GETVID(' ','NUME_DDL',0,IARG,1,NOMNU,INUME)
C
C --- CHAR_CINE
C
      CALL GETVID(' ','CHAR_CINE',0,IARG,0,K8BID,NBCHCI)
      NBCHCI = -NBCHCI
      CALL WKVECT(VCINE//'.&&LICHCIN','V V K8',NBCHCI,ILICHC)
      CALL GETVID(' ','CHAR_CINE',0,IARG,NBCHCI,ZK8(ILICHC),IBID)
C
C --- VERIF SUR LES GRANDEURS  GD ASSOCIEE AU NUME_DDL, GD ASSOCIEE AU
C     VCINE
      CALL DISMOI('F','NOM_GD',NOMNU,'NUME_DDL',IBID,NOMGD,IERD)
      CALL DISMOI('F','NOM_GD_SI',NOMGD,'GRANDEUR',IBID,NOMGDS,IERD)
C
C --- CREATION DU CHAMNO ET AFFECTATION DU CHAMNO
      CALL CALVCI(VCINE,NOMNU,NBCHCI,ZK8(ILICHC),INST,'G')
C
C
      CALL JEDEMA()
      END
