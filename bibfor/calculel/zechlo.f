C ======================================================================
C RESPONSABLE                            VABHHTS J.PELLET
C     ARGUMENTS:
C     ----------
      INTEGER OPT,TE
C ----------------------------------------------------------------------
C     ENTREES:
C      OPT : OPTION
C      TE  : TYPE_ELEMENT

C     SORTIES:
C      MET LES CHAMPS LOCAUX DE SORTIE A ZERO ENTRE 2 TE0000

C ----------------------------------------------------------------------
      COMMON /CAII02/IAOPTT,LGCO,IAOPMO,ILOPMO,IAOPNO,ILOPNO,IAOPDS,
     +       IAOPPA,NPARIO,NPARIN,IAMLOC,ILMLOC,IADSGD
      COMMON /CAII06/IAWLOC,IAWTYP,NBELGR,IGR
      COMMON /CAII04/IACHII,IACHIK,IACHIX
      COMMON /CAII07/IACHOI,IACHOK
      COMMON /CAII08/IEL

C     FONCTIONS EXTERNES:
C     -------------------
      INTEGER NBPARA
      CHARACTER*8 NOPARA

C     VARIABLES LOCALES:
C     ------------------
      INTEGER NP,IPAR,JCELD
      INTEGER LGCATA,LGGREL,I,IACHII,IACHIK,IACHIX,IACHLO,IACHOI,IACHOK
      INTEGER IADSGD,IAMLOC,IAOPDS,IAOPMO,IAOPNO,IAOPPA
      INTEGER IAOPTT,IAWLOC,IAWTYP,IEL,IGR,ILMLOC
      INTEGER ILOPMO,ILOPNO,INDIK8,IPARG,LGCO,NBELGR,NPARIN,NPARIO
      CHARACTER*1 TYPSCA
      CHARACTER*8 NOMPAR
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR,JEXR8
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL,ETENDU
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C ---------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------

C DEB-------------------------------------------------------------------

      NP = NBPARA(OPT,TE,'OUT')
      DO 40 IPAR = 1,NP
        NOMPAR = NOPARA(OPT,TE,'OUT',IPAR)

        IPARG = INDIK8(ZK8(IAOPPA),NOMPAR,1,NPARIO)
        IACHLO = ZI(IAWLOC-1+7* (IPARG-1)+1)
        LGCATA = ZI(IAWLOC-1+7* (IPARG-1)+4)
        IF (IACHLO.EQ.-1) GO TO 40


C       1- CALCUL DE LA LONGUEUR A METTRE A ZERO : LGGREL
C       --------------------------------------------------

C     -- LE CHAMP LOCAL EST-IL ETENDU ?
        CALL CHLOET(IPARG,ETENDU,JCELD)
        IF (ETENDU) THEN
          LGGREL = ZI(JCELD-1+ZI(JCELD-1+4+IGR)+4)
        ELSE
          LGGREL = NBELGR*LGCATA
        END IF



        TYPSCA = ZK8(IAWTYP-1+IPARG) (1:1)


C       2- MISE A ZERO :
C       --------------------------------------------------
        IF (TYPSCA.EQ.'R') THEN
          DO 10 I = 1,LGGREL
            ZR(IACHLO-1+I) = 0.0D0
   10     CONTINUE
        ELSE IF (TYPSCA.EQ.'C') THEN
          DO 20 I = 1,LGGREL
            ZC(IACHLO-1+I) = DCMPLX(0.D0,0.D0)
   20     CONTINUE
        ELSE IF (TYPSCA.EQ.'I') THEN
          DO 30 I = 1,LGGREL
            ZI(IACHLO-1+I) = 0
   30     CONTINUE
        ELSE
          CALL UTMESS('F','ZECHLO',
     +                'ERREUR PGMEUR DANS ZECHLO : TYPE_SCALAIRE:'//
     +                TYPSCA//' NON AUTORISE(R OU C),')
        END IF
   40 CONTINUE

   50 CONTINUE
      END
