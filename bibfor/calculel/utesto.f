     &                  LLAB,SSIGNE)
      IMPLICIT NONE
      CHARACTER*24 NOMOBJ
      REAL*8 REFR,EPSI
      CHARACTER*(*) CRIT,TYPE,SSIGNE
      CHARACTER*16 TBTXT(2)
      INTEGER REFI,IFIC
      LOGICAL LLAB
