C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C A_UTIL
C ----------------------------------------------------------------------
C          CALCUL DES DERIVEES SECONDES DES FONCTIONS DE FORME
C ----------------------------------------------------------------------
C VARIABLES D'ENTREE
C REAL*8      M(*)        : POINT SUR MAILLE DE REFERENCE (X,[Y],[Z])
C CHARACTER*8 TYPEMA      : TYPE DE LA MAILLE
C
C VARIABLES DE SORTIE
C REAL*8      W(NCMP,NNO) : DERIVEES 2IEMES DES FONCTIONS DE FORME EN M
C                          ( D2W1/DX2,[D2W1/DY2],[D2W1/DZ2],[D2W1/DXDY],
C                            [D2W1/DXDZ],[D2W1/DYDZ],D2W2/DX2,...)
C INTEGER     NNO         : NOMBRE DE NOEUDS
C INTEGER     NCMP        : NOMBRE DE COMPOSANTES
C ---------------------------------------------------------------------
      INTEGER  IN, NDIM
      REAL*8   DFF2(3,3,27)
C
      IF (TYPEMA(1:5).EQ.'TRIA6') THEN
         CALL ELRFD2 ( 'TR6', M, 24, DFF2, NNO, NDIM )
         NCMP = 3

      ELSEIF (TYPEMA(1:5).EQ.'TRIA7') THEN
         CALL ELRFD2 ( 'TR7', M, 28, DFF2, NNO, NDIM )
         NCMP = 3

      ELSEIF (TYPEMA(1:5).EQ.'TRIW6') THEN
         VALK = TYPEMA
         CALL U2MESG('F', 'CALCULEL6_8',1,VALK,0,0,0,0.D0)
         CALL ELRFD2 ( 'TW6', M, 24, DFF2, NNO, NDIM )
         NCMP = 3

      ELSEIF (TYPEMA(1:5).EQ.'QUAD6') THEN
         VALK = TYPEMA
         CALL U2MESG('F', 'CALCULEL6_8',1,VALK,0,0,0,0.D0)
         CALL ELRFD2 ( 'QU6', M, 24, DFF2, NNO, NDIM )
         NCMP = 2

      ELSEIF (TYPEMA(1:5).EQ.'QUAD8') THEN
         CALL ELRFD2 ( 'QU8', M, 32, DFF2, NNO, NDIM )
         NCMP = 3

      ELSEIF (TYPEMA(1:5).EQ.'QUAD9') THEN
         CALL ELRFD2 ( 'QU9', M, 36, DFF2, NNO, NDIM )
         NCMP = 3

      ELSE
         CALL U2MESK('F','CALCULEL2_31',1,TYPEMA)
      ENDIF

      IF (NCMP.EQ.2) THEN
         DO 10 IN = 1 , NNO
            W(1+(IN-1)*NCMP)  = DFF2(1,1,IN)
            W(2+(IN-1)*NCMP)  = DFF2(2,2,IN)
 10      CONTINUE
      ELSEIF (NCMP.EQ.3) THEN
         DO 20 IN = 1 , NNO
            W(1+(IN-1)*NCMP)  = DFF2(1,1,IN)
            W(2+(IN-1)*NCMP)  = DFF2(2,2,IN)
            W(3+(IN-1)*NCMP)  = DFF2(2,1,IN)
 20      CONTINUE
      ENDIF

      END
