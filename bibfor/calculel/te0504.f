      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16 OPTION,NOMTE
C ......................................................................
C    - FONCTION REALISEE:  CALCUL DES MATRICES ELEMENTAIRES
C                          OPTION : 'RIGI_THER_FLUTNL'
C    - ARGUMENTS:
C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT
C ......................................................................

      REAL*8 POIDS,R,NX,NY,ALPHA,ALPHA0
      INTEGER NNO,KP,NPG,IPOIDS,IVF,IDFDE,IGEOM
      INTEGER IMATTT,I,J,IJ,L,LI,LJ,IFLUX
      LOGICAL  LTEATT, LAXI

C-----------------------------------------------------------------------
      INTEGER ITEMP ,ITEMPS ,JGANO ,NDIM ,NNOS 
      REAL*8 TPGI 
C-----------------------------------------------------------------------
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)

      LAXI = .FALSE.
      IF (LTEATT(' ','AXIS','OUI')) LAXI = .TRUE.

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PFLUXNL','L',IFLUX)
      CALL JEVECH('PTEMPER','L',ITEMP)
      CALL JEVECH('PTEMPSR','L',ITEMPS)
      CALL JEVECH('PMATTTR','E',IMATTT)

      IF (ZK8(IFLUX) (1:7).EQ.'&FOZERO') GO TO 50

      DO 40 KP = 1,NPG
        CALL VFF2DN(NDIM,NNO,KP,IPOIDS,IDFDE,ZR(IGEOM),NX,NY,POIDS)
        R = 0.D0
        TPGI = 0.D0
        DO 10 I = 1,NNO
          L = (KP-1)*NNO + I
          R = R + ZR(IGEOM+2*I-2)*ZR(IVF+L-1)
          TPGI = TPGI + ZR(ITEMP+I-1)*ZR(IVF+L-1)
   10   CONTINUE
        IF (LAXI) POIDS = POIDS*R
        CALL FODERI(ZK8(IFLUX),TPGI,ALPHA,ALPHA0)
        IJ = IMATTT - 1
        DO 30 I = 1,NNO
          LI = IVF + (KP-1)*NNO + I - 1

          DO 20 J = 1,I
            LJ = IVF + (KP-1)*NNO + J - 1
            IJ = IJ + 1
            ZR(IJ) = ZR(IJ) - POIDS*ALPHA0*ZR(LI)*ZR(LJ)
   20     CONTINUE
   30   CONTINUE
   40 CONTINUE
   50 CONTINUE
      END
