C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE PELLET J.PELLET
C
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*19 CHEL1,CHEL2
C
C ----------------------------------------------------------------------
C
C VERIFIER LA COHERENCE DES FAMILLES DE POINTS DE GAUSS ENTRE DEUX 
C CHAM_ELEM
C
C ----------------------------------------------------------------------
C
C IN  CHEL1  : PREMIER CHAM_ELEM
C IN  CHEL2  : PREMIER CHAM_ELEM
C
C ----------------------------------------------------------------------
C
      CHARACTER*32 JEXNUM
      CHARACTER*24 VALK(3)
      CHARACTER*8  NOMA,NOMMAI,K8BID
      INTEGER      NBMA,IMA
      INTEGER      IRET1,IRET2,IBID
      INTEGER      J1,J2
      CHARACTER*16 FPG1,FPG2
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- MAILLAGE ATTACHE
C
      CALL DISMOI('F','NOM_MAILLA',CHEL1,'CHAM_ELEM',IBID,NOMA,IBID)
C
C --- EXTRAIRE DE CHEL1 LE SCHEMA DE POINT DE GAUSS DES MAILLES
C
      CALL CELFPG(CHEL1,'&&CHVEPG.FAPG1',IBID)
      CALL JEEXIN('&&CHVEPG.FAPG1',IRET1)
      CALL ASSERT(IRET1.GT.0)
C
C --- EXTRAIRE DE CHEL2 LE SCHEMA DE POINT DE GAUSS DES MAILLES
C
      CALL CELFPG(CHEL2,'&&CHVEPG.FAPG2',IBID)
      CALL JEEXIN('&&CHVEPG.FAPG2',IRET2)
      CALL ASSERT(IRET2.GT.0)
C
C --- VERIFIER LA COHERENCE DES FAMILLES DE POINTS DE GAUSS
C
      CALL JEVEUO('&&CHVEPG.FAPG1','L',J1)
      CALL JEVEUO('&&CHVEPG.FAPG2','L',J2)
      CALL JELIRA('&&CHVEPG.FAPG1','LONMAX',NBMA,K8BID)
      DO 10 IMA = 1,NBMA
        FPG1   = ZK16(J1-1+IMA)
        FPG2   = ZK16(J2-1+IMA)
        IF ((FPG1.NE.' ') .AND. (FPG2.NE.' ') .AND.
     &          (FPG2.NE.FPG1)) THEN
          CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',IMA),NOMMAI)
          VALK(1) = NOMA
          VALK(2) = FPG1
          VALK(3) = FPG2
          CALL U2MESK('F','CALCULEL_91',3,VALK)
        ENDIF
   10 CONTINUE
C
      CALL JEDETR('&&CHVEPG.FAPG1')
      CALL JEDETR('&&CHVEPG.FAPG2')
      CALL JEDEMA()
      END
