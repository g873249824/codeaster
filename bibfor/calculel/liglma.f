C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE PELLET J.PELLET
C ======================================================================
      IMPLICIT NONE
      CHARACTER*(*) LINUMA,LINUTE
      CHARACTER*19 LIGREL
      INTEGER NBMA

C ----------------------------------------------------------------------
C BUT : EXTRACTION D'UN LIGREL LA LISTE DES NUMEROS DE MAILLES ET LA
C       LISTE DES NUMERO DE TYPE_ELEMENT

C IN/JXIN  LIGREL  : LIGREL
C OUT      NBMA    : NOMBRE DE MAILLES AFFECTEES DANS LE LIGREL
C IN/JXOUT LINUMA  : OBJET (V I) QUI CONTIENDRA LES NUMEROS DES MAILLES
C                    ASSOCIEES AUX ELEMENTS DU LIGREL
C IN/JXOUT LINUTE  : OBJET (V I) QUI CONTIENDRA LES NUMEROS DES
C                    TYPE_ELEMENT ASSOCIES AUX ELEMENTS DU LIGREL
C ----------------------------------------------------------------------
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32,JEXNUM
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      CHARACTER*8 KBID
      INTEGER IBID,NBGREL,IGREL,IEL,NUMA,NUTE,N1,NBEL
      INTEGER JNUMA,JNUTE,ICO,JLIEL

C ----------------------------------------------------------------------
      CALL JEMARQ()

      CALL JELIRA(LIGREL//'.LIEL','NMAXOC',NBGREL,KBID)

C     -- CALCUL DE NBMA :
      NBMA=0
      DO 10,IGREL=1,NBGREL
        CALL JELIRA(JEXNUM(LIGREL//'.LIEL',IGREL),'LONMAX',N1,KBID)
        NBEL=N1-1
        CALL ASSERT(NBEL.GE.1)
        NBMA=NBMA+NBEL
   10 CONTINUE
      CALL ASSERT(NBMA.GT.0)

C     -- CALCUL DE LINUMA ET LINUTE :
      CALL WKVECT(LINUMA,'V V I',NBMA,JNUMA)
      CALL WKVECT(LINUTE,'V V I',NBMA,JNUTE)

      ICO=0
      DO 30,IGREL=1,NBGREL
        CALL JELIRA(JEXNUM(LIGREL//'.LIEL',IGREL),'LONMAX',N1,KBID)
        CALL JEVEUO(JEXNUM(LIGREL//'.LIEL',IGREL),'L',JLIEL)
        NBEL=N1-1
        NUTE=ZI(JLIEL-1+NBEL)
        DO 20,IEL=1,NBEL
          ICO=ICO+1
          NUMA=ZI(JLIEL-1+IEL)
          ZI(JNUMA-1+ICO)=NUMA
          ZI(JNUTE-1+ICO)=NUTE
   20   CONTINUE
   30 CONTINUE

      CALL JEDEMA()
      END
