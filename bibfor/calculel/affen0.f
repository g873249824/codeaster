C      AFFE_CHAM_NO : TRAITEMENT DU MOT CLE "CHAM_NO_AFFE"

C ----------------------------------------------------------------------
      INTEGER IBID,N1,INUME,ICHNO,NEQ,IRET
      CHARACTER*8 NONUME,TYPVAL
      CHARACTER*16 TYPCO,OPER,TYPCO1,TYPCO2
      CHARACTER*19 CHIN,CHOUT,NOCHNO
C     ------------------------------------------------------------------

      CALL JEMARQ()

      CALL GETRES(CHOUT,TYPCO,OPER)
      IF (OPER.NE.'AFFE_CHAM_NO') GO TO 10

      CALL GETVID(' ','CHAM_NO_AFFE',1,1,1,CHIN,N1)

C --- NUME_DDL

      CALL GETVID(' ','NUME_DDL',1,1,1,NONUME,INUME)

C --- CHAM_NO

      CALL GETVID(' ','CHAM_NO',1,1,1,NOCHNO,ICHNO)


      IF (INUME+ICHNO.EQ.0) THEN

C   ---   DUPLICATION DU CHAMP

        CALL COPISD('CHAMP_GD','G',CHIN,CHOUT)

      ELSE IF (INUME.NE.0) THEN

C   ---   ON S'APPUIE SUR UN NOUVEAU "NUME_DDL"

        CALL JELIRA(CHIN//'.VALE','TYPE',IBID,TYPVAL)
        CALL VTCREB(CHOUT,NONUME,'G',TYPVAL,NEQ)

        CALL VTCOPY(CHIN,CHOUT,IRET)

      ELSE IF (ICHNO.NE.0) THEN

C   ---   ON S'APPUIE SUR UN "CHAM_NO"

        CALL GETTCO(NOCHNO,TYPCO1)
        CALL GETTCO(CHIN,TYPCO2)
        IF (TYPCO1.NE.TYPCO2) THEN
          CALL UTDEBM('F',OPER,
     &                'LES CHAMPS NE SONT PAS DE LA MEME GRANDEUR:')
          CALL UTIMPK('L','  TYPE DU CHAM_NO ',1,TYPCO1)
          CALL UTIMPK('L','  TYPE DU CHAM_NO_AFFE ',1,TYPCO2)
          CALL UTFINM
        END IF

        CALL JELIRA(NOCHNO//'.VALE','TYPE',IBID,TYPVAL)
        CALL VTDEFS(CHOUT,NOCHNO,'G',TYPVAL)

        CALL VTCOPY(CHIN,CHOUT,IRET)

      END IF

   10 CONTINUE
      CALL JEDEMA()
      END
