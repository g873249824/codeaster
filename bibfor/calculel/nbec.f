C     ALTERATION SANS CHANGEMENT POUR TEST PROC MAJCODE!!  (3)
C     RETOURNE LE NOMBRE D ENTIERS CODES POUR UN GRANDEUR DU TYPE :
C     _SIMPLE
C     _ELEMENTAIRE

      INTEGER GD,CODE
      CHARACTER*8 NOMGD,NOMGD1,NOMGD2
C     INCLUDE($FUNJEV)

C     FONCTIONS JEVEUX

      CHARACTER*32 JEXNUM

C     COMMUNS   JEVEUX

      INTEGER ZI
      INTEGER VALI(3)
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 VALK(3)
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C     EXCLUDE($FUNJEV)
      CALL JEMARQ()
      CALL JEVEUO(JEXNUM('&CATA.GD.DESCRIGD',GD),'L',IDDGD)
      CODE = ZI(IDDGD)
      IF ((CODE.EQ.1) .OR. (CODE.EQ.2)) THEN
        NBEC = ZI(IDDGD+2)
        GO TO 10
      ELSE IF (CODE.EQ.3) THEN
        IGD1 = ZI(IDDGD+3)
        IF (IGD1.GT.0) THEN
          CALL JEVEUO(JEXNUM('&CATA.GD.DESCRIGD',IGD1),'L',IDDGD1)
          NBEC = ZI(IDDGD1+2)
          GO TO 10
        ELSE
          CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
        VALK (1) = 'GRANDEUR LIGNE REFERENCEE PAR'
        VALK (2) = NOMGD//' NULLE'
        VALI (1) = GD
          CALL U2MESG('F', 'CALCULEL6_22',2,VALK,1,VALI,0,0.D0)
        END IF
      ELSE IF ((CODE.EQ.4) .OR. (CODE.EQ.5)) THEN
        IGD1 = ZI(IDDGD+3)
        IF (IGD1.EQ.0) THEN
          CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
        VALK (1) = 'GRANDEUR LIGNE REFERENCEE PAR'
        VALK (2) = NOMGD//' NULLE'
        VALI (1) = GD
          CALL U2MESG('F', 'CALCULEL6_22',2,VALK,1,VALI,0,0.D0)
        END IF
        IGD2 = ZI(IDDGD+4)
        IF (IGD2.EQ.0) THEN
          CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
        VALK (1) = 'GRANDEUR COLONNE REFERENCEE PAR'
        VALK (2) = NOMGD//' NULLE'
        VALI (1) = GD
          CALL U2MESG('F', 'CALCULEL6_22',2,VALK,1,VALI,0,0.D0)
        END IF
        IF (IGD1.NE.IGD2) THEN
          CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
          CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',IGD1),NOMGD1)
          CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',IGD2),NOMGD2)
        VALI (1) = IGD1
        VALI (2) = IGD2
        VALI (3) = GD
        VALK (1) = NOMGD1//' /= '
        VALK (2) = NOMGD2
        VALK (3) = NOMGD
          CALL U2MESG('F', 'CALCULEL6_25',3,VALK,3,VALI,0,0.D0)
        END IF
        CALL JEVEUO(JEXNUM('&CATA.GD.DESCRIGD',IGD1),'L',IDDGD1)
        NBEC = ZI(IDDGD1+2)
        GO TO 10
      ELSE
        VALI (1) = GD
        VALI (2) = CODE
        CALL U2MESG('F', 'CALCULEL6_26',0,' ',2,VALI,0,0.D0)
      END IF
   10 CONTINUE
      CALL JEDEMA()
      END
