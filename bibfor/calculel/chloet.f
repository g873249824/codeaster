C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE PELLET J.PELLET
C -----------------------------------------------------------------
C     BUT : DETERMINER SI LE CHAMP LOCAL ASSOCIE A IPARG
C           EST "ETENDU"
C     UN CHAMP LOCAL ETENDU N'A PAS LA MEME LONGUEUR POUR TOUS SES
C     ELEMENTS
C -----------------------------------------------------------------
      INCLUDE 'jeveux.h'
      LOGICAL ETENDU
      INTEGER IPARG,JCELD
C -----------------------------------------------------------------
C     ENTREES:
C     IPARG  : NUMERO DU PARAMETRE (DANS LE CATALOGUE DE L'OPTION)

C     SORTIES:
C     ETENDU : .TRUE. : LE CHAMP LOCAL EST ETENDU
C              .FALSE.: LE CHAMP LOCAL N'EST PAS ETENDU
C     JCELD : SI LE CHAMP LOCAL EST ETENDU, JCELD EST L'ADRESSE
C             DANS ZI DE L'OBJET CHAMP_GLOBAL.CELD
C -----------------------------------------------------------------
      INTEGER IADSGD,IAMLOC,IAOPDS,IAOPMO,IAOPNO,IAOPPA
      INTEGER ILMLOC,ILOPMO,IAOPTT
      INTEGER ILOPNO,LGCO,NPARIO,IACHLO
      INTEGER NPARIN,IACHII,IACHIK,IACHIX,IACHOI,IACHOK,ICH

      COMMON /CAII02/IAOPTT,LGCO,IAOPMO,ILOPMO,IAOPNO,ILOPNO,IAOPDS,
     +       IAOPPA,NPARIO,NPARIN,IAMLOC,ILMLOC,IADSGD
      INTEGER        NBGR,IGR,NBELGR,JCTEAT,LCTEAT,IAWLOC,IAWLO2,IAWTYP
      COMMON /CAII06/NBGR,IGR,NBELGR,JCTEAT,LCTEAT,IAWLOC,IAWLO2,IAWTYP
      COMMON /CAII04/IACHII,IACHIK,IACHIX
      COMMON /CAII07/IACHOI,IACHOK

      CHARACTER*8 TYCH



C     -- LE CHAMP LOCAL EST-IL UN CHAM_ELEM ETENDU ?
C     OUI  SI CHAMP GLOBAL ASSOCIE EST 1 CHAM_ELEM ETENDU
C     --------------------------------------------------
      IACHLO=ZI(IAWLOC-1+3*(IPARG-1)+1)
      IF ((IACHLO.EQ.-1) .OR. (IACHLO.EQ.-2)) GO TO 20
      ICH=ZI(IAWLOC-1+3*(IPARG-1)+3)
      IF (ICH.EQ.0) GO TO 20
      IF (IPARG.LE.NPARIN) THEN
        TYCH = ZK8(IACHIK-1+2* (ICH-1)+1)
        IF (TYCH.NE.'CHML') GO TO 20
        JCELD = ZI(IACHII-1+11* (ICH-1)+4)
      ELSE
        TYCH = ZK8(IACHOK-1+2* (ICH-1)+1)
        IF (TYCH.NE.'CHML') GO TO 20
        JCELD = ZI(IACHOI-1+3* (ICH-1)+1)
      END IF
      IF ((ZI(JCELD-1+4).EQ.0).AND.(ZI(JCELD-1+3).LE.1)) THEN
        GO TO 20
      ELSE
        GO TO 10
      END IF


C     LE CHAMP LOCAL EST ETENDU:
C     --------------------------
   10 CONTINUE
      ETENDU = .TRUE.
      GO TO 30


C     LE CHAMP LOCAL N'EST PAS ETENDU:
C     --------------------------------
   20 CONTINUE
      ETENDU = .FALSE.
      GO TO 30


   30 CONTINUE

      END
