      REAL*8 VALR
      COMPLEX*16 VALC
      CHARACTER*8 NOMMA
      CHARACTER*3  SSIGNE
      CHARACTER*4 TYCH
C     ------------------------------------------------------------------
      CALL DISMOI('F','TYPE_CHAMP',CHAM19,'CHAMP',IBID,TYCH,IE)
