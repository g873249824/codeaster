C          TROUVER LE DESCRIPTEUR-GRANDEUR ASSOCIE A UN MODE LOCAL
C          DE CARTE, CHAM_NO, OU CHAMELEM, SOUS FORME "IDEN"
C      ENTREE:
C          MODE  : NUMERO DE MODE LOCAL
C      SORTIE:
C          NEC   : NOMBRE D'ENTIERS-CODES
