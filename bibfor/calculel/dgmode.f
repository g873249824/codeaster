      INTEGER I ,JMOD ,NBPT,NBEC
C-----------------------------------------------------------------------
      CALL JEMARQ()
      NEC = 0
      DG(1) = 0

      JMOD = IMODEL+ZI(ILONG-1+MODE)-1
      NEC = NBEC(ZI(JMOD-1+2))
      IF (ZI(JMOD).LE.3) THEN
         NBPT = ZI(JMOD-1+4)
         IF (ABS(NBPT).LT.10000) THEN
            DO 1 I = 1,NEC
               DG(I) = ZI(JMOD-1+4+I)
    1       CONTINUE
         END IF
      END IF
      CALL JEDEMA()
      END
