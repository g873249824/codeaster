      INCLUDE 'jeveux.h'
      CHARACTER*8    NOMO
      CHARACTER*(*)  CHPRES
C
      LOGICAL        FONC
C
C    CETTE ROUTINE GENERE UN CHAMP DE PRESSION NUL (CARTE CONSTANTE)
C     FONC = .TRUE.  FORCE FONCTION
C     FONC = .FALSE. FORCE REELLE
