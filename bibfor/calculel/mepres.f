      REAL*8         RCMP(2),RBID
C
      CHARACTER*8    LICMP(2),NOMF(2),ZERO
      CHARACTER*19   LIGRMO
C
      COMPLEX*16     CBID
C
C     -----------------------------------------------------------------
C
      ZERO   = '&&MEPRES'
      LIGRMO = NOMO//'.MODELE    '
      LICMP(1) = 'PRES'
      LICMP(2) = 'CISA'
      RCMP(1) = 0.D0
      RCMP(2) = 0.D0
      IF (FONC) THEN
        CALL FOZERV(ZERO)
        NOMF(1) = ZERO
        NOMF(2) = ZERO
        CALL MECACT('V',CHPRES,'MODELE',LIGRMO,'PRES_F',2,LICMP,IBID,
     &               RBID,CBID,NOMF)
      ELSE
        CALL MECACT('V',CHPRES,'MODELE',LIGRMO,'PRES_R',2,LICMP,
     &              IBID,RCMP,CBID,' ')
      ENDIF
C
      END
