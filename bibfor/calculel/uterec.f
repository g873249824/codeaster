      SUBROUTINE UTEREC(NDIM,IFLUP,IFLUM,INO,MNO,JNO,NSOMM,JAC,TERM22,
     &                  AUX,LTHETA,VALTHE,VALUNT,NIV,IFM,XN,YN,ZN,
     &                  VALHP,VALHM,VALTP,VALTM,ITYP,ITEMP,ITEMM,NOE)
