      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16 NOMTE,OPTION
C ----------------------------------------------------------------------

C    - FONCTION REALISEE:  CALCUL DES MATRICES ELEMENTAIRES
C                          OPTION : 'RIGI_THER_TRANS'
C                          ELEMENTS 3D ISO PARAMETRIQUES

C    - ARGUMENTS:
C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT


      REAL*8   XKPT,ALPHA,TPG
      REAL*8   DFDX(27),DFDY(27),DFDZ(27),POIDS
      INTEGER  IPOIDS,IVF,IDFDE,IGEOM,IMATE
      INTEGER  JGANO,NNO,KP,NPG,I,J,L,IJ,IMATTT,ITEMPS,IFON(3)
      INTEGER  NDIM,ITEMP,ITEMPI,NNOS

C DEB ------------------------------------------------------------------
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PTEMPSR','L',ITEMPS)
      CALL JEVECH('PTEMPER','L',ITEMP)
      CALL JEVECH('PTEMPEI','L',ITEMPI)
      CALL JEVECH('PMATTTR','E',IMATTT)

      CALL NTFCMA(ZI(IMATE),IFON)

      DO 40 KP = 1,NPG
        L = (KP-1)*NNO
        CALL DFDM3D ( NNO, KP, IPOIDS, IDFDE,
     &                ZR(IGEOM), DFDX, DFDY, DFDZ, POIDS )
        TPG = 0.D0
        DO 10 I = 1,NNO
          TPG = TPG + ZR(ITEMPI+I-1)*ZR(IVF+L+I-1)
   10   CONTINUE

        CALL RCFODE(IFON(2),TPG,ALPHA,XKPT)

        DO 30 I = 1,NNO

          DO 20 J = 1,I
            IJ = (I-1)*I/2 + J
            ZR(IMATTT+IJ-1) = ZR(IMATTT+IJ-1) +
     &                        POIDS* (ALPHA* (DFDX(I)*DFDX(J)+
     &                        DFDY(I)*DFDY(J)+DFDZ(I)*DFDZ(J)))
   20     CONTINUE
   30   CONTINUE
   40 CONTINUE
C FIN ------------------------------------------------------------------
      END
