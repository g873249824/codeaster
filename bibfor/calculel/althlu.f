     > 3,
     > 4,
     > 2,
     > 3,
     > 4,
     > 4/
       THLU=.FALSE.
       FAMLUM = 0

      DO 100 IPOS = 1 , NPOS
         IF (ALIAS.EQ.POSSIB(IPOS)) THEN
          THLU = .TRUE.
          FAMLUM = FAM(IPOS)
          GOTO 101
         ENDIF
  100 CONTINUE
  101 CONTINUE
      END
