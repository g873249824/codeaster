C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C SUPPRESSION D'INSTRUCTIONS INUTILES
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*16        OPTION , NOMTE
C ......................................................................
C    - FONCTION REALISEE:  CALCUL DES FORCES NODALES DE MECABL2
C                          OPTION : 'FORC_NODA        '
C
C    - ARGUMENTS:
C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT
C ......................................................................
C
      CHARACTER*24       CARAC,FF
      CHARACTER*8        ELREFE
      REAL*8             BILINE,COEF,JACOBI,NX,YTYWPQ(9),W(9)
      INTEGER            NNO,KP,I,ICARAC,IFF,IPOIDS,IVF,IGEOM
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32       JEXNUM , JEXNOM , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C
      CALL ELREF1(ELREFE)
C
      CARAC='&INEL.'//ELREFE//'.CARAC'
      CALL JEVETE(CARAC,'L',ICARAC)
      NNO  = ZI(ICARAC)
      NPG  = ZI(ICARAC+2)
C
      FF   ='&INEL.'//ELREFE//'.FF'
      CALL JEVETE(FF,'L',IFF)
      IPOIDS=IFF
      IVF   =IPOIDS+NPG
      IYTY  =IVF   +2*NPG*NNO
      NORDRE = 3*NNO
C PARAMETRES EN ENTREE
      CALL JEVECH('PGEOMER','L',IGEOM)
C
      CALL JEVECH('PDEPLMR','L',IDEPLA)
      CALL TECACH('ONN','PDEPLPR',1,IDEPLP,IRET)
      CALL JEVECH('PCONTMR','L',LSIGMA)
C PARAMETRES EN SORTIE
      CALL JEVECH('PVECTUR','E',JEFINT)
C
      IF (IDEPLP.EQ.0) THEN
        DO 10 I=1,3*NNO
          W(I)=ZR(IDEPLA-1+I)
10      CONTINUE
      ELSE
        DO 11 I=1,3*NNO
          W(I)=ZR(IDEPLA-1+I)+ZR(IDEPLP-1+I)
11      CONTINUE
      END IF
      DO 21 KP=1,NPG
        K = (KP-1) * NORDRE * NORDRE
        JACOBI = SQRT( BILINE(NORDRE,ZR(IGEOM),ZR(IYTY+K),ZR(IGEOM)) )
C
        NX = ZR(LSIGMA-1+KP)
        CALL MATVEC (NORDRE,ZR(IYTY+K),2,ZR(IGEOM),W,YTYWPQ)
        COEF = NX * ZR(IPOIDS-1+KP) / JACOBI
        DO 16 I=1,NORDRE
          ZR(JEFINT-1+I) = ZR(JEFINT-1+I) + COEF*YTYWPQ(I)
 16     CONTINUE
   21 CONTINUE
C
      END
