      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      INTEGER NMAXOB
      PARAMETER (NMAXOB=30)
      INTEGER ITABL(NMAXOB),IMPR
      CHARACTER*24 ZK24,K24TAB(NMAXOB)
      CHARACTER*32 ZK32,JEXNUM,JEXNOM
      CHARACTER*80 ZK80

      INTEGER NBTE,TE,NUM
      CHARACTER*16 NOMTE
      CHARACTER*8 NOMNUM
      CHARACTER*8 IBID


      CALL JEMARQ()
      WRITE (IMPR,*) '=========================================='
      WRITE (IMPR,*) 'TYPE_ELEMENT    NUM_INIT  NOM_OBJET_CREE '
      WRITE (IMPR,*) '=========================================='
      CALL JELIRA('&CATA.TE.NOMTE','NOMMAX',NBTE,IBID)


      DO 1050,TE = 1,NBTE
        CALL JENUNO(JEXNUM('&CATA.TE.NOMTE',TE),NOMTE)

        DO 10,KK = 1,NMAXOB
          K24TAB(KK) = ' '
          ITABL(KK) = 0
   10   CONTINUE

        CALL INI002(NOMTE,' ',NMAXOB,ITABL,K24TAB,NVAL)
        IF (NVAL.GT.0) WRITE (IMPR,*) ' '
        DO 1030,K = 1,NVAL
          IF (ITABL(K).NE.0) WRITE (IMPR,1001) NOMTE,NUM,'"',K24TAB(K),
     &        '"'
 1030   CONTINUE
        GO TO 1050

 1040   CONTINUE
        CALL CODENT(NUM,'D',NOMNUM)
        CALL UTMESS('F','INIGRL',' ON NE TROUVE PAS DE ROUTINE ININPQ'//
     &              ' NPQ DOIT ETRE COMPRIS ENTRE 1 ET 100'//
     &              ' ICI : NPQ ='//NOMNUM)
 1050 CONTINUE


 1060 CONTINUE
 1001 FORMAT (1X,A16,1X,I3,5X,A1,A24,A1)
      CALL JEDEMA()
      END
