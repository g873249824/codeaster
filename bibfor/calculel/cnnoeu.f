C OUT CXNO   : CONNECTIVITE DE LA MAILLE
C                CONTIENT NUMEROS ABSOLUS DES NOEUDS DANS LE MAILLAGE
C
C ----------------------------------------------------------------------
C        
      NBNO = LONCUM(NUMMAI+1) - LONCUM(NUMMAI)
C
      IF ((NBNO.LT.1).OR.(NBNO.GT.27)) CALL ASSERT(.FALSE.)      
C
      IF (LCOQUE.EQ.0) THEN
        CALL CNSOLI(NUMMAI,CONNEX,LONCUM,DIME  ,NBNO  ,
     &              CXNO)

      ELSEIF (LCOQUE.EQ.1) THEN
        CALL ASSERT(.FALSE.)
      ELSE
        CALL ASSERT(.FALSE.)  
      ENDIF
      END
