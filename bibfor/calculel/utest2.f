     &                  REFR,REFC,TYPRES,EPSI,CRIT,IFIC)
      IMPLICIT REAL*8 (A-H,O-Z)
      INTEGER REFI,NUPO,IVARI,IFIC,NUSP
      REAL*8 REFR,EPSI
      CHARACTER*(*) CHAM19,NOMAIL,NONOEU,TYPRES,NOCMP,CRIT
      COMPLEX*16 REFC
C ----------------------------------------------------------------------
C MODIF CALCULEL  DATE 22/03/2004   AUTEUR DURAND C.DURAND 
