      CHARACTER*8 LPAIN(7),LPAOUT(1),K8BID
      CHARACTER*16 OPTION
      CHARACTER*24 LCHIN(7),LCHOUT(1),CHGEOM,MATE


      COMPLEX*16 CBID

C DEB-------------------------------------------------------------------
C
      CHTEMP = '&&TEMP'
      CALL MECACT('V',CHTEMP,'LIGREL',LIGREL,'TEMP_R',1,'TEMP',
     &                                        IBID,0.0D0,CBID,K8BID)

      MATE = MATEZ
      CALL MEGEOM(MODELE,CHGEOM)

      IF (MATE.EQ.' ') CALL U2MESS('F','CALCULEL4_66')

      LPAIN(1) = 'PGEOMER'
      LCHIN(1) = CHGEOM
      LPAIN(2) = 'PMATERC'
      LCHIN(2) = MATE
      LPAIN(3) = 'PSIEFP_R'
      LCHIN(3) = SIGMAP
      LPAIN(4) = 'PSIEFD_R'
      LCHIN(4) = SIGMAD
      LPAIN(5) = 'PTEMPER'
      LCHIN(5) = CHTEMP
      LPAIN(6) = 'PSIGMAP'
      LCHIN(6) = SIGNOP
      LPAIN(7) = 'PSIGMAD'
      LCHIN(7) = SIGNOD

      LPAOUT(1) = 'PERREUR'
      LCHOUT(1) = RESU
      OPTION = 'ERRE_QIZZ'
      CALL CALCUL('S',OPTION,LIGREL,7,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'G',
     &               'OUI')

      END
