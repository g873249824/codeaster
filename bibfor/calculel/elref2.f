C ---------------------------------------------------------------------
C BUT: RECUPERER LA LISTE DES ELREFE D'UN TYPE_ELEM
C ---------------------------------------------------------------------
C     ARGUMENTS:
C NOMTE  IN      K16    : NOM DU TYPE_ELEM
C DIM    IN      I      : DIMENSION DU VECTEUR LIELRF
C LIELRF OUT     L_K8   : LISTE DES ELREFE POUR NOMTE
C        OUT     NTROU  : NOMBRE D'ELREFE POUR NOMTE

C REMARQUE :
C   SI NOMTE A PLUS D'ELREFE QUE DIM => ERREUR <F>
C----------------------------------------------------------------------

      INTEGER NUTE,JNBELR,JNOELR,NTROU,IAD,K

      CALL JENONU(JEXNOM('&CATA.TE.NOMTE',NOMTE),NUTE)
      CALL ASSERT(NUTE.GT.0)

      CALL JEVEUO('&CATA.TE.NBELREFE','L',JNBELR)
      CALL JEVEUO('&CATA.TE.NOELREFE','L',JNOELR)

      NTROU = ZI(JNBELR-1+2* (NUTE-1)+1)
      IAD = ZI(JNBELR-1+2* (NUTE-1)+2)

      CALL ASSERT(NTROU.LE.DIM)

      DO 10,K = 1,NTROU
        LIELRF(K) = ZK8(JNOELR-1+IAD-1+K)
   10 CONTINUE


      END
