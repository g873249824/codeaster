C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C TOLE CRP_6
C
C     IN
C     --
C     IDPRN1 IDPRN2 : ADRESSES DE PRNO ( OBJET ET POINTEUR DE LONGUEUR)
C     ILI : NUMERO DE LIGREL
C     MODE : MODE
C     NEC  : NBEC(GD)
C
C     N    : NUMERO GLOBAL DU NOEUD
C     K    : NUMERO LOCAL DU NOEUD ( DANS ELEMENT )
C     OUT
C     ---
C
C     NDDLOC : NBRE DE DDL SUPPORTES PAR CE NOEUD SUR L ELEMENT
C     POS    : TABLEAU DE CORRESPONDANCE AVEC LES DDL SUR LE NOEUD
C          EN TANT QUE NOEUD GLOBAL
C ----------------------------------------------------------------------
      PARAMETER (NBECMX = 10)
      INTEGER IFIN(NBECMX)
      INTEGER PRNO
      INTEGER POS(1)
      INTEGER ECODG,ECODL,ENTCOD,SHIFTR
C
C     ECODG : ENTIER CODE DE N
C     ECODL : ENTIER CODE DE K
C
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR
C
C     COMMUNS   JEVEUX
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C
C     FONCTION D ACCES A PRNO
C
      PRNO(ILI,NUNOEL,L) = ZI(IDPRN1-1+ZI(IDPRN2+ILI-1)+
     +                     (NUNOEL-1)* (NEC+2)+L-1)
C - DEB ----------------------------------------------------------------
C
C --- IFIN DONNE POUR CHAQUE ENTIER CODE LE NOMBRE MAX DE DDLS
C --- QUE L'ON PEUT TROUVER SUR CET ENTIER :
C     ------------------------------------
      DO 10 IEC = 1, NEC-1
          IFIN(IEC) = 30
 10   CONTINUE
      IFIN(NEC) = NCMP - 30*(NEC-1)
C
      IN = 0
      NDDLOC = 0
C
C --- AJOUT DE LA BOUCLE 20 SUR LE NOMBRE D'ENTIERS CODES
C --- PAR RAPPORT A LA VERSION NORMALE DE CORDDL , LES INSTRUCTIONS
C --- NE CHANGENT PAS, EXCEPTEE LA DEFINITION DE ECODL ET ECODG
C --- OU INTERVIENT L'INDICE D'ENTIER CODE :
C     ------------------------------------
      CALL ENTCO0 ( MODE, IADM, M1 )
      DO 20 IEC = 1, NEC

C      ECODG = PRNO(ILI,N,3)
C      ECODL = ENTCOD(NEC,IADM,M1,K,1)
        ECODG = PRNO(ILI,N,2+IEC)
        ECODL = ENTCOD(NEC,IADM,M1,K,IEC)

        DO 110 I = 1,IFIN(IEC)
         ECODG = ECODG/2
         ECODL = ECODL/2
         IECDG = IAND(1,ECODG)
         IF (IECDG.GT.0) THEN
            IN = IN + 1
            IECDL = IAND(1,ECODL)
            IF (IECDL.GT.0) THEN
               NDDLOC = NDDLOC + 1
               POS(NDDLOC) = IN
            END IF
         END IF
  110   CONTINUE
C
  20  CONTINUE

 9999 CONTINUE
      END
