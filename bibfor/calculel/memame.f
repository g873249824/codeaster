      SUBROUTINE MEMAME(OPTION,MODELE,NCHAR ,LCHAR ,MATE  ,
     &                  CARELE,EXITIM,INSTAN,COMPOR,MATELZ,
     &                  BASE  )

C
C MODIF CALCULEL  DATE 12/02/2013   AUTEUR PELLET J.PELLET 
C ======================================================================
