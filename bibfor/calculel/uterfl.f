      SUBROUTINE UTERFL(NDIM,IFLUP,IFLUM,INO,MNO,JNO,NSOMM,JAC,TERM22,
     &                  AUX,LTHETA,VALTHE,VALUNT,NIV,IFM,XN,YN,ZN,
     &                  VALFP,VALFM,ITYP,NOE)
