      INTEGER NBK16,RANG
      CHARACTER*(*) K16Z, LK16(*)
      CHARACTER*16 K16,LK16Z
C ----------------------------------------------------------------------
C     ENTREES:
C     LK16 : LISTE DE K16 OU ON DOIT CHERCHER LE MOT K16
C     K16 : MOT A CHERCHER
C     NBK16: NOMBRE DE MOTS DE LK16
C     RANG : ON CHERCHE LE RANG-IEME MOT K16 DANS LA LISTE.
C
C     SORTIES:
C     INDK16 : POSITION DU MOT CHERCHE DANS LA LISTE.
C           SI LE MOT EST ABSENT: INDK16=0
C
C ----------------------------------------------------------------------
C
C     VARIABLES LOCALES:
C     ------------------
      INTEGER I,J
C DEB-------------------------------------------------------------------
C
      K16 = K16Z
      J = 0
      DO 100 I = 1,NBK16
         LK16Z = LK16(I)
         IF (LK16Z.EQ.K16) THEN
            J = J + 1
            IF (J.EQ.RANG) GO TO 110
         END IF
  100 CONTINUE
      INDK16 = 0
      GO TO 120
  110 CONTINUE
      INDK16 = I
  120 CONTINUE
      END
