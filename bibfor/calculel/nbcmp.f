C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C
C     RETOURNE LE NOMBRE D ENTIERS CODES POUR UN GRANDEUR DU TYPE :
C     _SIMPLE
C     _ELEMENTAIRE
C
      INTEGER GD,CODE
      CHARACTER*8 NOMGD,NOMGD1,NOMGD2
C     INCLUDE($FUNJEV)
C
C     FONCTIONS JEVEUX
C
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR
C
C     COMMUNS   JEVEUX
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C     EXCLUDE($FUNJEV)
C
      CHARACTER*8 CBID
C
      CALL JEMARQ()
      NBCMP=0
C
      CALL JEVEUO(JEXNUM('&CATA.GD.DESCRIGD',GD),'L',IDDGD)
      CODE = ZI(IDDGD)
      IF ((CODE.EQ.1) .OR. (CODE.EQ.2)) THEN
         CALL JELIRA(JEXNUM('&CATA.GD.NOMCMP',GD),'LONMAX',NBCMP,CBID)
         GO TO 9999
      ELSE IF (CODE.EQ.3) THEN
         IGD1 = ZI(IDDGD+3)
         IF (IGD1.GT.0) THEN
            CALL JELIRA(JEXNUM('&CATA.GD.NOMCMP',IGD1),'LONMAX',NBCMP,
     +                  CBID)
            GO TO 9999
         ELSE
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
            CALL UTDEBM('F','NBCMP','RECHERCHE NBRE DE CMP: ERREUR:')
            CALL UTIMPK('L',' ',1,'GRANDEUR LIGNE REFERENCEE PAR')
            CALL UTIMPI('S','GRANDEUR NUMERO ',1,GD)
            CALL UTIMPK('L',' DE NOM ',1,NOMGD//' NULLE')
            CALL UTFINM()
         END IF
      ELSE IF ((CODE.EQ.4) .OR. (CODE.EQ.5)) THEN
         IGD1 = ZI(IDDGD+3)
         IF (IGD1.EQ.0) THEN
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
            CALL UTDEBM('F','NBCMP','RECHERCHE NBRE DE CMP: ERREUR:')
            CALL UTIMPK('L',' ',1,'GRANDEUR LIGNE REFERENCEE PAR')
            CALL UTIMPI('S','GRANDEUR NUMERO ',1,GD)
            CALL UTIMPK('L',' DE NOM ',1,NOMGD//' NULLE')
            CALL UTFINM()
         END IF
         IGD2 = ZI(IDDGD+4)
         IF (IGD2.EQ.0) THEN
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
            CALL UTDEBM('F','NBCMP','RECHERCHE NBRE DE CMP: ERREUR:')
            CALL UTIMPK('L',' ',1,'GRANDEUR COLONNE REFERENCEE PAR')
            CALL UTIMPI('S','GRANDEUR NUMERO ',1,GD)
            CALL UTIMPK('L',' DE NOM ',1,NOMGD//' NULLE')
            CALL UTFINM()
         END IF
         IF (IGD1.NE.IGD2) THEN
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',IGD1),NOMGD1)
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',IGD2),NOMGD2)
            CALL UTDEBM('F','NBCMP','RECHERCHE NBRE DE CMP: ERREUR:')
            CALL UTIMPI('L','GRANDEUR LIGNE NUMERO ',1,IGD1)
            CALL UTIMPK('S',' DE NOM ',1,NOMGD1//' /= ')
            CALL UTIMPI('L','GRANDEUR COLONNE NUMERO ',1,IGD2)
            CALL UTIMPK('S',' DE NOM ',1,NOMGD2)
            CALL UTIMPI('L','GRANDEUR MERE NUMERO ',1,GD)
            CALL UTIMPK('S',' DE NOM ',1,NOMGD)
            CALL UTFINM()
         END IF
         CALL JELIRA(JEXNUM('&CATA.GD.NOMCMP',IGD1),'LONMAX',NBCMP,CBID)
         GO TO 9999
      ELSE
         CALL UTDEBM('F','NBCMP','RECHERCHE NBRE DE CMP: ERREUR:')
         CALL UTIMPI('L','GRANDEUR',1,GD)
         CALL UTIMPI('S','A UN CODE INCONNU: ',1,CODE)
         CALL UTFINM()
      END IF
 9999 CONTINUE
      CALL JEDEMA()
      END
