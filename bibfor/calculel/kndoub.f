      INTEGER KNINDI,K1,K2
C DEB

      CALL ASSERT((LONG.EQ.8).OR.(LONG.EQ.16).OR.(LONG.EQ.24))

      IRET = 0

      DO 10,K1 = 1,NBKN - 1
        K2 = KNINDI(LONG,LKN(K1),LKN(K1+1),NBKN-K1)
        IF (K2.GT.0) THEN
          IRET = K1
          GO TO 20
        END IF
   10 CONTINUE



   20 CONTINUE


      END
