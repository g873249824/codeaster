C ======================================================================
         IMPLICIT NONE
      INCLUDE 'jeveux.h'
         CHARACTER*(*) MATAS,OBJET,EOUL
         INTEGER ADRESS

C -----------------------------------------------------------
C   BUT : RECUPERER L'ADRESSE D'UN OBJET D'UNE MATR_ASSE

C   MATAS K19  IN/JXIN : NOM DE LA MATR_ASSE
C   OBJET K4   IN      : NOM D'UN SUFFIXE : ABLO,ADIA,...
C   EOUL  K1   IN      : 'E' : EN ECRITURE
C                        'L' : EN LECTURE
C   ADRESS I   OUT     : ADRESSE DANS ZI, ZR, ... DE L'OBJET


C  ATTENTION : CETTE ROUTINE NE FAIT PAS JEMARQ/JEDEMA
C              POUR NE PAS INVALIDER L'ADRESSE "OUT"
C -----------------------------------------------------------
        CHARACTER*19 MAT
        CHARACTER*14 NU
        CHARACTER*4 OBJ
        INTEGER I1,I2,JREFA

        MAT=MATAS
        OBJ=OBJET
        CALL ASSERT(OBJ(3:4).EQ.'BL' .OR. OBJ(3:4).EQ.'DI' .OR.
     &              OBJ(3:4).EQ.'HC')

        CALL JEVEUO(MAT//'.REFA',EOUL,JREFA)
        NU=ZK24(JREFA-1+2)

        CALL JEEXIN(NU//'.SMOS.SMDI',I1)
        CALL JEEXIN(NU//'.SLCS.SCDI',I2)
C       SI LDLT, LES 2 OBJETS PEUVENT EXISTER
        CALL ASSERT(I1.GT.0 .OR. I2.GT.0)

        IF (OBJ(2:2).EQ.'X') THEN
C           -- ON PRIVILEGIE LE STOCKAGE MORSE :
           IF (I1.GT.0) THEN
             OBJ='SM'//OBJ(3:4)
             CALL ASSERT(OBJ.NE.'SMBL')
             CALL JEVEUO(NU//'.SMOS.'//OBJ,EOUL,ADRESS)
           ELSE
             CALL ASSERT(.FALSE.)
C            -- LE STOCK. LIGNE_CIEL N'EXISTE QU'AVEC UN STOCK. MORSE
             OBJ='SC'//OBJ(3:4)
             CALL JEVEUO(NU//'.SLCS.'//OBJ,EOUL,ADRESS)
           END IF

        ELSE IF (OBJ(2:2).EQ.'M') THEN
           CALL ASSERT(I1.GT.0)
           CALL ASSERT(OBJ.NE.'SMBL')
           CALL JEVEUO(NU//'.SMOS.'//OBJ,EOUL,ADRESS)

        ELSE IF (OBJ(2:2).EQ.'C') THEN
           CALL ASSERT(I2.GT.0)
           CALL JEVEUO(NU//'.SLCS.'//OBJ,EOUL,ADRESS)

        ELSE
           CALL ASSERT(.FALSE.)
        ENDIF

        END
