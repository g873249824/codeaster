C ======================================================================
C RESPONSABLE                            VABHHTS J.PELLET
C     ARGUMENTS:
C     ----------
      INTEGER IMODAT
      CHARACTER*19 CHIN
C ----------------------------------------------------------------------
C     ENTREES:
C        CHIN   : NOM DU CHAMP GLOBAL SUR LEQUEL ON FAIT L'EXTRACTION
C        IGR    : NUMERO DU GREL (COMMON)
C        IMODAT : MODE LOCAL ATTENDU
C       (IGR EST RELATIF AU LIGREL SOUS-JACENT A CHIN )

C ----------------------------------------------------------------------
      COMMON /CAII01/IGD,NEC,NCMPMX,IACHIN,IACHLO,IICHIN,IANUEQ,LPRNO,
     &               ILCHLO
      COMMON /CAKK02/TYPEGD
      CHARACTER*8 TYPEGD
      COMMON /CAII04/IACHII,IACHIK,IACHIX
      COMMON /CAII02/IAOPTT,LGCO,IAOPMO,ILOPMO,IAOPNO,ILOPNO,IAOPDS,
     +       IAOPPA,NPARIO,NPARIN,IAMLOC,ILMLOC,IADSGD
      COMMON /CAII06/IAWLOC,IAWTYP,NBELGR,IGR
      COMMON /CAII08/IEL

C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR,JEXR8
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C ---------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------

C     FONCTIONS EXTERNES:
C     ------------------

C     VARIABLES LOCALES:
C     ------------------
      INTEGER JCELD,MODE,DEBUGR,LGGREL
      CHARACTER*8 NOGREL,TYCH
C DEB-------------------------------------------------------------------

      JCELD = ZI(IACHII-1+11* (IICHIN-1)+4)
      TYCH = ZK8(IACHIK-1+2* (IICHIN-1)+1)
      IF (TYCH(1:4).NE.'CHML') CALL UTMESS('F','EXCHML','STOP 1')

      MODE = ZI(JCELD-1+ZI(JCELD-1+4+IGR)+2)
      IF (MODE.EQ.0) THEN
        CALL CODENT(IGR,'D',NOGREL)
        CALL UTMESS('F','EXCHML',
     +              ' ERREUR DANS L''EXTRACTION D''UN CHAM_ELEM'//
     +              ' POUR LE GREL: '//NOGREL//'LE CHAMP N''EXISTE PAS')
      END IF

      IF (MODE.EQ.IMODAT) THEN
        LGGREL = ZI(JCELD-1+ZI(JCELD-1+4+IGR)+4)
        DEBUGR = ZI(JCELD-1+ZI(JCELD-1+4+IGR)+8)
        CALL JACOPO(LGGREL,TYPEGD,IACHIN-1+DEBUGR,IACHLO)
      ELSE
        CALL UTMESS('F','EXCHML','A FAIRE ? VOIR ALCHLO:ATTENTION ')
C       CE NE SERA PAS TRES FACILE A CAUSE DES CHAMPS ETENDUS.
C       VOIR LES USAGES DE CHLOET : JEVECH PAR EXEMPLE
      END IF

      END
