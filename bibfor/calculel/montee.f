C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE                            VABHHTS J.PELLET

C     ARGUMENTS:
C     ----------
      INTEGER OPT,NOUT,TE
      CHARACTER*19 CH19
      CHARACTER*(*) LCHOUT(*)
      CHARACTER*8 LPAOUT(*)
C ----------------------------------------------------------------------
C     ENTREES:
C     OPT    : OPTION DE CALCUL
C     TE     : TYPE_ELEMENT
C     IGR    : NUMERO DU GREL DONT ON SAUVE LES CHAMPS LOCAUX (COMMON)

C     SORTIES:
C     MET A JOUR LES CHAMPS GLOBAUX DE SORTIE DE L OPTION OPT
C ----------------------------------------------------------------------
      INTEGER IAOPTT,LGCO,IAOPMO,ILOPMO,IAOPNO,ILOPNO,IAOPDS
      INTEGER IAOPPA,NPARIO,NPARIN,IAMLOC,ILMLOC,IADSGD
      COMMON /CAII02/IAOPTT,LGCO,IAOPMO,ILOPMO,IAOPNO,ILOPNO,IAOPDS,
     &       IAOPPA,NPARIO,NPARIN,IAMLOC,ILMLOC,IADSGD
      INTEGER IAWLOC,IAWTYP,NBELGR,IGR,JCTEAT,LCTEAT
      COMMON /CAII06/IAWLOC,IAWTYP,NBELGR,IGR,JCTEAT,LCTEAT
      INTEGER IACHOI,IACHOK
      COMMON /CAII07/IACHOI,IACHOK

C     FONCTIONS EXTERNES:
C     -------------------
      INTEGER NBPARA,GRDEUR,INDIK8,DIGDE2,MODATT
      CHARACTER*8 NOPARA
      CHARACTER*32 JEXNUM

C     VARIABLES LOCALES:
C     ------------------
      INTEGER IPAR,NP,MOD1,JPAR,GD,JPARAL,IRET,IEL,IAUX1,IAUX2,IAUX0
      INTEGER IPARG,IAD1,LGGREL,JCELV,JCELK,JRESL,JNOLI
      INTEGER DESCGD,JCELD
      INTEGER CODE,DEBUGR,NCMPEL
      CHARACTER*8 NOMPAR,TYPSCA
      LOGICAL LPARAL
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80

      CALL JEMARQ()

C     PARALLELE OR NOT ?
      LPARAL=.FALSE.
      CALL JEEXIN('&CALCUL.PARALLELE',IRET)
      IF (IRET.NE.0) THEN
        LPARAL=.TRUE.
        CALL JEVEUO('&CALCUL.PARALLELE','L',JPARAL)
      ENDIF

      NP=NBPARA(OPT,TE,'OUT')
      DO 20 IPAR=1,NP
        NOMPAR=NOPARA(OPT,TE,'OUT',IPAR)
        IPARG=INDIK8(ZK8(IAOPPA),NOMPAR,1,NPARIO)
        IAD1=ZI(IAWLOC-1+7*(IPARG-1)+1)
        IF (IAD1.EQ.-1)GOTO 20

        GD=GRDEUR(NOMPAR)

        MOD1=MODATT(OPT,TE,'OUT',IPAR)
        JPAR=INDIK8(LPAOUT,NOMPAR,1,NOUT)
        CH19=LCHOUT(JPAR)


        DESCGD=IADSGD+7*(GD-1)
        CODE=ZI(DESCGD-1+1)

        TYPSCA=ZK8(IAWTYP-1+IPARG)

        IF (CODE.EQ.1) THEN
C         -- CAS : CHAM_ELEM
          JCELD=ZI(IACHOI-1+2*(JPAR-1)+1)
          DEBUGR=ZI(JCELD-1+ZI(JCELD-1+4+IGR)+8)
          LGGREL=ZI(JCELD-1+ZI(JCELD-1+4+IGR)+4)
          JCELV=ZI(IACHOI-1+2*(JPAR-1)+2)
          CALL JACOPO(LGGREL,TYPSCA,IAD1,JCELV-1+DEBUGR)
          IF (LPARAL) THEN
            CALL JEVEUO(CH19//'.CELK','E',JCELK)
            ZK24(JCELK-1+7)='MPI_INCOMPLET'
          ENDIF
        ELSE
C         -- CAS : RESUELEM
          NCMPEL=DIGDE2(MOD1)
          CALL JEVEUO(JEXNUM(CH19//'.RESL',IGR),'E',JRESL)
          IF (LPARAL) THEN
            DO 10 IEL=1,NBELGR
              IF (ZL(JPARAL-1+IEL)) THEN
                IAUX0=(IEL-1)*NCMPEL
                IAUX1=IAD1+IAUX0
                IAUX2=JRESL+IAUX0
                CALL JACOPO(NCMPEL,TYPSCA,IAUX1,IAUX2)
              ENDIF
   10       CONTINUE
          ELSE
            CALL JACOPO(NBELGR*NCMPEL,TYPSCA,IAD1,JRESL)
          ENDIF
          CALL JELIBE(JEXNUM(CH19//'.RESL',IGR))
          IF (LPARAL) THEN
            CALL JEVEUO(CH19//'.NOLI','E',JNOLI)
            ZK24(JNOLI-1+3)='MPI_INCOMPLET'
          ENDIF
        ENDIF
   20 CONTINUE
   30 CONTINUE

      CALL JEDEMA()
      END
