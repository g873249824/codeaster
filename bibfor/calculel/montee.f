      INTEGER IGR2,TE2,TYPELE



      IF (CALVOI.EQ.0) THEN
        IF (FIN.NE.'FIN') THEN
          IGR2=IGR
          TE2=TYPELE(LIGREL,IGR2)
          CALL MONTE1(OPT,TE2,NOUT,LCHOUT,LPAOUT,IGR2)
        ENDIF
      ELSE
C       -- ON RECOPIE TOUT A LA FIN :
        IF (FIN.EQ.'FIN') THEN
           DO 1, IGR2=1,NBGR
             TE2=TYPELE(LIGREL,IGR2)
             CALL MONTE1(OPT,TE2,NOUT,LCHOUT,LPAOUT,IGR2)
 1         CONTINUE
        ENDIF
      ENDIF


