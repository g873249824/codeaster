C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================

C-----------------------------------------------------------------------
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

      CHARACTER*32 JEXNUM,JEXNOM
C     ----- FIN COMMUNS NORMALISES  JEVEUX  --------------------------

      INTEGER I,IER,JDESCR,JDESCI,GDR,GDI,GDCPX,JNCMPR,JNCMPC,LONG,IBID
      INTEGER NMAX1,NMAX2,JDESCC,NBVALR,IVALR,NBVALI,IVALI,JVALE,IRET
      INTEGER JCELDR,JCELDI,JCELKR,IGR,IMOLO,NBGREL,TAILLE,NBVALC,JCELK
      CHARACTER*4 TYPCH1,TYPCH2
      CHARACTER*8 NOMGDR,NOMGDI,NOMCPX,K8B,KMPICR,KMPICI
      CHARACTER*24 NOMGR,LIGREL,OPTION,PARAM
      CHARACTER*24 VALK(2)
C     ------------------------------------------------------------------
      CALL JEMARQ()

      BASE=BASE2
      PREEL=PREEL2
      PIMAG=PIMAG2
      CHOUT=CHOUT2

      CALL JEEXIN(PREEL //'.DESC',IER)
      IF (IER.NE.0) THEN
        TYPCH1='CHNO'
      ELSE
        CALL JEEXIN(PREEL //'.CELK',IER)
        IF (IER.NE.0) THEN
          TYPCH1='CHML'
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF
      ENDIF

      CALL JEEXIN(PIMAG //'.DESC',IER)
      IF (IER.NE.0) THEN
        TYPCH2='CHNO'
      ELSE
        CALL JEEXIN(PIMAG //'.CELK',IER)
        IF (IER.NE.0) THEN
          TYPCH2='CHML'
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF
      ENDIF
      IF (TYPCH1.NE.TYPCH2) THEN
        CALL U2MESS('F','CALCULEL_18')
      ENDIF

      CALL VRREFE(PREEL,PIMAG,IER)
      CALL ASSERT(IER.EQ.0)

      IER=0

      IF ( TYPCH1.EQ.'CHNO') THEN

        CALL JEVEUO(PREEL //'.DESC','L',JDESCR)
        GDR=ZI(JDESCR)
        CALL JENUNO(JEXNUM('&CATA.GD.NOMGD',GDR),NOMGDR)

        IF ((NOMGDR(7:7).NE.' ').OR.(NOMGDR(5:6).NE.'_R')) THEN
          CALL U2MESK('F','CALCULEL_20',1,NOMGDR)
        ENDIF

        CALL JEVEUO(PIMAG //'.DESC','L',JDESCI)
        GDI=ZI(JDESCI)
        CALL JENUNO(JEXNUM('&CATA.GD.NOMGD',GDI),NOMGDI)

        IF ((NOMGDI(7:7).NE.' ').OR.(NOMGDI(5:6).NE.'_R')) THEN
          CALL U2MESK('F','CALCULEL_20',1,NOMGDI)
        ENDIF

        IF (NOMGDR.NE.NOMGDI) THEN
          CALL U2MESS('F','CALCULEL_21')
        ENDIF

        NOMCPX=NOMGDR(1:4)//'_C'

        CALL JENONU(JEXNOM('&CATA.GD.NOMGD',NOMCPX),GDCPX)

        CALL JELIRA(JEXNUM('&CATA.GD.NOMCMP',GDR),'LONMAX',NMAX1,K8B)
        CALL JELIRA(JEXNUM('&CATA.GD.NOMCMP',GDCPX),'LONMAX',NMAX2,K8B)

        IF (NMAX1.NE.NMAX2) THEN
           VALK(1) = NOMGDR
           VALK(2) = NOMCPX
           CALL U2MESK('F','CALCULEL_22', 2 ,VALK)
        ENDIF

        CALL JEVEUO(JEXNUM('&CATA.GD.NOMCMP',GDR),'L',JNCMPR)
        CALL JEVEUO(JEXNUM('&CATA.GD.NOMCMP',GDCPX),'L',JNCMPC)

        DO 10 I=1,NMAX1
          IF (ZK8(JNCMPR-1+I).NE.ZK8(JNCMPC-1+I)) THEN
            IER=1
            GOTO 10
          ENDIF
 10     CONTINUE

        IF (IER.NE.0) THEN
           VALK(1) = NOMGDR
           VALK(2) = NOMCPX
           CALL U2MESK('F','CALCULEL_23', 2 ,VALK)
        ENDIF

        CALL JEDUP1(PREEL //'.DESC',BASE,CHOUT //'.DESC')
        CALL JEDUP1(PREEL //'.REFE',BASE,CHOUT //'.REFE')
        CALL JEVEUO(CHOUT //'.DESC','E',JDESCC)
        ZI(JDESCC)=GDCPX

        CALL JELIRA(PREEL //'.VALE','LONMAX',NBVALR,K8B)
        CALL JEVEUO(PREEL //'.VALE','L',IVALR)

        CALL JELIRA(PIMAG //'.VALE','LONMAX',NBVALI,K8B)
        CALL JEVEUO(PIMAG //'.VALE','L',IVALI)

        IF (NBVALR.NE.NBVALI) THEN
           CALL U2MESS('F','CALCULEL_24')
        ENDIF

        CALL WKVECT(CHOUT //'.VALE',BASE//' V C',NBVALR,JVALE)

        DO 20 I=1,NBVALR
          ZC(JVALE-1+I)=DCMPLX(ZR(IVALR-1+I),ZR(IVALI-1+I))
 20     CONTINUE

      ELSE IF ( TYPCH1.EQ.'CHML') THEN
        CALL JEVEUO(PREEL //'.CELD','L',JCELDR)
        GDR=ZI(JCELDR)
        CALL JENUNO(JEXNUM('&CATA.GD.NOMGD',GDR),NOMGDR)
        IF ((NOMGDR(7:7).NE.' ').OR.(NOMGDR(5:6).NE.'_R')) THEN
          CALL U2MESK('F','CALCULEL_20',1,NOMGDR)
        ENDIF

        CALL JEVEUO(PIMAG //'.CELD','L',JCELDI)
        GDI=ZI(JCELDI)
        CALL JENUNO(JEXNUM('&CATA.GD.NOMGD',GDI),NOMGDI)

        IF ((NOMGDI(7:7).NE.' ').OR.(NOMGDI(5:6).NE.'_R')) THEN
          CALL U2MESK('F','CALCULEL_20',1,NOMGDI)
        ENDIF

        IF (NOMGDR.NE.NOMGDI) THEN
          CALL U2MESS('F','CALCULEL_21')
        ENDIF

        NOMCPX=NOMGDR(1:4)//'_C'

        CALL JENONU(JEXNOM('&CATA.GD.NOMGD',NOMCPX),GDCPX)

        CALL JELIRA(JEXNUM('&CATA.GD.NOMCMP',GDR),'LONMAX',NMAX1,K8B)
        CALL JELIRA(JEXNUM('&CATA.GD.NOMCMP',GDCPX),'LONMAX',NMAX2,K8B)

        IF (NMAX1.NE.NMAX2) THEN
           VALK(1) = NOMGDR
           VALK(2) = NOMCPX
           CALL U2MESK('F','CALCULEL_22', 2 ,VALK)
        ENDIF

        CALL JEVEUO(JEXNUM('&CATA.GD.NOMCMP',GDR),'L',JNCMPR)
        CALL JEVEUO(JEXNUM('&CATA.GD.NOMCMP',GDCPX),'L',JNCMPC)

        DO 30 I=1,NMAX1
          IF (ZK8(JNCMPR-1+I).NE.ZK8(JNCMPC-1+I)) THEN
            IER=1
          ENDIF
 30     CONTINUE

        IF (IER.NE.0) THEN
          VALK(1) = NOMGDR
          VALK(2) = NOMCPX
          CALL U2MESK('F','CALCULEL_23', 2 ,VALK)
        ENDIF

        CALL JEVEUO(PREEL //'.CELK','L',JCELKR)
        LIGREL=ZK24(JCELKR)
        OPTION=ZK24(JCELKR+1)
        PARAM=ZK24(JCELKR+5)
        LONG=24
        DO 40 I=1,LONG
          IF (PARAM(I:I).EQ.' ') THEN
            TAILLE=I-1
            GOTO 45
          ENDIF
 40     CONTINUE

 45     CONTINUE

        PARAM=PARAM(1:(TAILLE-1))//'C'

        CALL EXISD('CHAM_ELEM_S',PREEL,IRET)
        IF (IRET.GT.0) THEN
          CALL ALCHML(LIGREL,OPTION,PARAM,BASE,CHOUT ,IER,PREEL)
        ELSE
          CALL ALCHML(LIGREL,OPTION,PARAM,BASE,CHOUT ,IER,' ')
        ENDIF

        CALL JELIRA(PREEL //'.CELV','LONMAX',NBVALR,K8B)
        CALL JEVEUO(PREEL //'.CELV','L',IVALR)
        CALL JELIRA(PIMAG //'.CELV','LONMAX',NBVALI,K8B)
        CALL JEVEUO(PIMAG //'.CELV','L',IVALI)
        CALL ASSERT(NBVALR.EQ.NBVALI)

        CALL JEVEUO(CHOUT //'.CELV','E',JVALE)
        CALL JELIRA(CHOUT //'.CELV','LONMAX',NBVALC,K8B)
        CALL ASSERT(NBVALR.EQ.NBVALC)

        DO 50 I=1,NBVALR
          ZC(JVALE-1+I)=DCMPLX(ZR(IVALR-1+I),ZR(IVALI-1+I))
 50     CONTINUE

        CALL DISMOI('F','MPI_COMPLET',PREEL,'CHAM_ELEM',IBID,KMPICR,
     &              IBID)
        CALL DISMOI('F','MPI_COMPLET',PIMAG,'CHAM_ELEM',IBID,KMPICI,
     &              IBID)
        CALL ASSERT(KMPICR.EQ.KMPICI)
        
        CALL JEVEUO (CHOUT//'.CELK','E',JCELK)
        IF (KMPICR.EQ.'OUI') THEN
          ZK24(JCELK-1+7) = 'MPI_COMPLET'
        ELSE
          ZK24(JCELK-1+7) = 'MPI_INCOMPLET'
        ENDIF

      ENDIF

      CALL JEDEMA()

      END
