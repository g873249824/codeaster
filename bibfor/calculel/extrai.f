      CALL ASSERT(INIT.EQ.' '.OR.INIT.EQ.'INIT')

      IF (CALVOI.EQ.0) THEN
        IF (INIT.NE.'INIT') THEN
          CALL EXTRA1(NIN,LCHIN,LPAIN,OPT,NUTE,LIGREL)
        ENDIF
