           DO 1, IGR=1,NBGR
             NBELGR=NBELEM(LIGREL,IGR)
             NUTE=TYPELE(LIGREL,IGR)
             CALL EXTRA1(NIN,LCHIN,LPAIN,OPT,NUTE,LIGREL)
 1         CONTINUE
        ENDIF
      ENDIF


