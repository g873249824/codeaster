      IMPLICIT NONE
      CHARACTER*8  CHAR
      CHARACTER*16 MOTFAC
      CHARACTER*8  NOMA
      CHARACTER*8  NOMO
      CHARACTER*4  FONREE
      INTEGER      NDIM
      INTEGER      NZOCO
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (TOUTES METHODES - LECTURE DONNEES)
C
C LECTURE DES PRINCIPALES CARACTERISTIQUES DU CONTACT (SURFACE IREAD)
C REMPLISSAGE DE LA SD 'DEFICO' (SURFACE IWRITE)
C      
C ----------------------------------------------------------------------
C
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  MOTFAC : MOT-CLE FACTEUR (VALANT 'CONTACT')
C IN  NOMA   : NOM DU MAILLAGE
C IN  NOMO   : NOM DU MODELE
C IN  NDIM   : NOMBRE DE DIMENSIONS DU PROBLEME
C IN  NZOCO  : NOMBRE DE ZONES DE CONTACT
C IN  FONREE : FONC OU REEL SUIVANT L'OPERATEUR
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IOC,ISY,ISYME
      INTEGER      IREAD,IWRITE
      CHARACTER*24 SYMECO,FORMCO
      INTEGER      JSYME,JFORM
      INTEGER      NSYME,NZOCP
      INTEGER      CFMMVD,ZMETH
      CHARACTER*24 METHCO
      INTEGER      JMETH      
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C      
      SYMECO = CHAR(1:8)//'.CONTACT.SYMECO'
      CALL JEVEUO(SYMECO,'L',JSYME)
      NSYME = ZI(JSYME)
      NZOCP = NZOCO - NSYME
C
C --- CREATION DES SDS DE BASE
C
      FORMCO    = CHAR(1:8)//'.CONTACT.FORMCO'
      METHCO    = CHAR(1:8)//'.CONTACT.METHCO'
      ZMETH     = CFMMVD('ZMETH')      
      CALL WKVECT(FORMCO,'G V I',NZOCO,JFORM)
      CALL WKVECT(METHCO,'G V I',ZMETH*NZOCO+1,JMETH) 
      ZI(JMETH) = NZOCO       
C
C --- ON NE BOUCLE QUE SUR LES ZONES PRINCIPALES:
C
      DO 8 IOC = 1,NZOCP
        IREAD  = IOC
        IWRITE = IOC
        CALL CAZOCO(CHAR  ,MOTFAC,FONREE,NOMA  ,NOMO  ,
     &              NDIM  ,NZOCO ,IREAD ,IWRITE)
 8    CONTINUE
C
C --- ON BOUCLE SUR LES ZONES PRINCIPALES MAIS ON AGIT SUR LES
C --- ZONES SYMETRIQUES
C
      IF (NSYME .GT. 0) THEN
        ISYME = 0
        DO 9 IOC = 1,NZOCP
          IREAD = IOC
          DO 10 ISY = 1,NSYME
            IF (ZI(JSYME+ISY) .EQ. IOC) THEN
              ISYME  = ISYME + 1
              IWRITE = NZOCP + ISYME
              CALL CAZOCO(CHAR  ,MOTFAC,FONREE,NOMA  ,NOMO  ,
     &                    NDIM  ,NZOCO ,IREAD ,IWRITE)
            END IF
 10       CONTINUE
 9      CONTINUE
        IF (ISYME .NE. NSYME)
     &    CALL U2MESS('F','CONTACT_38')
        IF (IWRITE .NE. NZOCO)
     &    CALL U2MESS('F','CONTACT_38')
      END IF
C
C ======================================================================
      CALL JEDEMA
C
      END
