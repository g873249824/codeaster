     &                  NOMARL,GRAPPA,TAMAFA,TAMAMA,
     &                  MAIL  ,LOMCUM,NOM1  ,NOM2  ,
     &                  TABCOL,
     &                  TYPMAI,NMA   ,LONCUM,
     &                  PANMA1,PANMA2,
     &                  BOSOM1,BOSOM2,BODIM1,BODIM2,
     &                  BOMNM1,BOMNM2,
     &                  NMAIN1,NMAIN2,NMASS ,NNOIN1,NNOIN2,
     &                  TYPEMA,NUMERO,NMAMA ,NFAM  ,ICPL   )
C
