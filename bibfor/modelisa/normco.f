C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*8  CHAR
      INTEGER      NNOCO
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES - LECTURE DONNEES)
C
C CONSTRUCTION DES TABLEAUX POUR NORMALES ET TANGENTES
C      
C ----------------------------------------------------------------------
C
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  NNOCO  : NOMBRE TOTAL DE NOEUDS DES SURFACES
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX ----------------
C
      INTEGER      CFMMVD,ZNOES
      CHARACTER*24 NDIMCO,TANINI,NORTCO,TANGCO,NOESCL,PSURNO,PZONE
      INTEGER      JDIM,JTGINI,JNORMO,JTANGO,JNOESC,JSUNO,JZONE
      INTEGER      NESMAX,NZOCO
      INTEGER      IFORM,IZONE,CFDISI
      INTEGER      ISURFE,ISURFM,JDECNM,JDECNE,INOE,INOM,NBNOE,NBNOM
      INTEGER      POSNOM,POSNOE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES OBJETS JEVEUX
C
      NDIMCO = CHAR(1:8)//'.CONTACT.NDIMCO' 
      TANINI = CHAR(1:8)//'.CONTACT.TANINI'
      NORTCO = CHAR(1:8)//'.CONTACT.NORMCO'
      TANGCO = CHAR(1:8)//'.CONTACT.TANGCO'
      NOESCL = CHAR(1:8)//'.CONTACT.NOESCL' 
      PSURNO = CHAR(1:8)//'.CONTACT.PSUNOCO'
      PZONE  = CHAR(1:8)//'.CONTACT.PZONECO'                
      CALL JEVEUO(NDIMCO,'L',JDIM)
      CALL JEVEUO(PSURNO,'L',JSUNO)
      CALL JEVEUO(PZONE ,'L',JZONE)
C
      NZOCO  = ZI(JDIM+1)
      NESMAX = ZI(JDIM+8) 
      ZNOES  = CFMMVD('ZNOES')
C
C --- DIMENSIONNEMENT DES VECTEURS NORMAUX ET TANGENT
C
      CALL WKVECT(TANINI,'G V R',6*NNOCO ,JTGINI)
      CALL WKVECT(NORTCO,'G V R',3*NESMAX,JNORMO)
      CALL WKVECT(TANGCO,'G V R',6*NESMAX,JTANGO)
C
C --- CREATION DU TABLEAU DES NOEUDS ESCLAVES NOESC
C      
      CALL WKVECT(NOESCL,'G V R',ZNOES*NNOCO,JNOESC)
C
C --- REMPLISSAGE DU TABLEAU
C
      DO 50 IZONE = 1,NZOCO
        IFORM  = CFDISI(CHAR(1:8)//'.CONTACT','FORMULATION',IZONE)
        IF (IFORM.EQ.1) THEN
          ISURFE = ZI(JZONE+IZONE)
          ISURFM = ZI(JZONE+IZONE-1) + 1          
        ELSEIF (IFORM.EQ.2) THEN
          ISURFM = ZI(JZONE+IZONE)
          ISURFE = ZI(JZONE+IZONE-1) + 1
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF
        NBNOM  = ZI(JSUNO+ISURFM) - ZI(JSUNO+ISURFM-1)
        JDECNM = ZI(JSUNO+ISURFM-1)
        DO 60 INOM = 1,NBNOM
          POSNOM = JDECNM + INOM
          ZR(JNOESC+ZNOES*(POSNOM-1)) = 1.D0
 60     CONTINUE
        NBNOE  = ZI(JSUNO+ISURFE) - ZI(JSUNO+ISURFE-1)
        JDECNE = ZI(JSUNO+ISURFE-1)
        DO 61 INOE = 1,NBNOE
          POSNOE = JDECNE + INOE
          ZR(JNOESC+ZNOES*(POSNOE-1)) = -1.D0
 61     CONTINUE 
 50   CONTINUE      
C
      CALL JEDEMA()
      END
