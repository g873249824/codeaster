      COMMON/RVARJE/ZR(1)
      COMMON/CVARJE/ZC(1)
      COMMON/LVARJE/ZL(1)
      COMMON/KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      COMMON/NOMAJE/PGC
      CHARACTER*6  PGC
      INTEGER ZI
      COMPLEX*16 ZC
      LOGICAL ZL
      REAL*8 ZR
      CHARACTER*8  ZK8,CHAR,VITESS
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*19 CARTE
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C
C     CREATION ET CHARGEMENT DE L'OBJET 'CHAR'.CHTH.CONVE
C
      CALL JEMARQ()
      VITESS = '        '
      CALL GETFAC('CONVECTION',NCONV)
      IF (NCONV.GT.1) THEN
         CALL U2MESS('F','MODELISA3_60')
      ELSE IF (NCONV.EQ.1) THEN
         CARTE = CHAR//'.CHTH.CONVE'
         CALL JECREO(CARTE//'.VALE','G V K8')
         CALL JEECRA(CARTE//'.VALE','LONMAX',1,' ')
         CALL JEVEUO (CARTE//'.VALE', 'E', IAD)
         CALL GETVID ('CONVECTION', 'VITESSE', 1, 1, 1, VITESS,
     &      NVITES)
         ZK8(IAD-1+1) = VITESS
         CALL CHPVER('F',ZK8(IAD-1+1),'NOEU','DEPL_R',IER)
      END IF
      CALL JEDEMA()
      END
