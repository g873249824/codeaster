     &            NMACO ,NNOCO )      
C                             
C --- ON STOCKE LES MAILLES/NOEUDS DES ZONES DE CONTACT 
C     
      CALL LIEXCO(CHAR  ,MOTFAC,NOMA  ,NOMO  ,NZOCO ,
     &            NMACO ,NNOCO )    
C            
      CALL JEDEMA()
      END
