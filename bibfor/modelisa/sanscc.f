C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  MOTFAC : MOT-CLE FACTEUR (VALANT 'CONTACT')
C IN  NOMA   : NOM DU MAILLAGE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*24 DEFICO
      CHARACTER*24 FROTNO,PFROT
      INTEGER      NBMOCL
      CHARACTER*16 LIMOCL(2),TYMOCL(2)
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DEFICO     = CHAR(1:8)//'.CONTACT'
      NBMOCL     = 2
      TYMOCL(1)  = 'GROUP_NO'
      TYMOCL(2)  = 'NOEUD'
C
C --- TRAITEMENT MOT-CLEF SANS_GROUP_NO_FR/SANS_NOEUD_FR
C
      FROTNO = DEFICO(1:16)//'.SANOFR'
      PFROT  = DEFICO(1:16)//'.PSANOFR'
      LIMOCL(1)  = 'SANS_GROUP_NO_FR'
      LIMOCL(2)  = 'SANS_NOEUD_FR'
      CALL SANSNO(CHAR  ,MOTFAC,NOMA  ,FROTNO,PFROT ,
     &            NBMOCL,TYMOCL,LIMOCL)
C
      CALL JEDEMA()
      END
