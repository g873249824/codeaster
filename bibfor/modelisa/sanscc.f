C ROUTINE CONTACT (METHODE CONTINUE - LECTURE DONNEES)
C
C TRAITEMENT MOT-CLEFS GROUP_NO_FOND/GROUP_MA_FOND/GROUP_NO_RACC
C      
C ----------------------------------------------------------------------
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  MOTFAC : MOT-CLE FACTEUR (VALANT 'CONTACT')
C IN  NOMA   : NOM DU MAILLAGE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*24 DEFICO
      CHARACTER*24 BARSNO,PBARS
      CHARACTER*24 BARSMA,PBARM
      CHARACTER*24 RACCNO,PRACC
      CHARACTER*24 FROTNO,PFROT      
      INTEGER      NBMOCL
      CHARACTER*16 LIMOCL(2),TYMOCL(2)
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DEFICO     = CHAR(1:8)//'.CONTACT' 
      NBMOCL     = 2     
      TYMOCL(1)  = 'GROUP_NO'
      TYMOCL(2)  = 'NOEUD'
C
C --- TRAITEMENT MOT-CLEF GROUP_NO_FOND/NOEUD_FOND
C
      BARSNO = DEFICO(1:16)//'.BANOCO'
      PBARS  = DEFICO(1:16)//'.PBANOCO'
      LIMOCL(1)  = 'GROUP_NO_FOND'
      LIMOCL(2)  = 'NOEUD_FOND'
      CALL SANSNO(CHAR  ,MOTFAC,NOMA  ,BARSNO,PBARS ,
     &            NBMOCL,TYMOCL,LIMOCL)
C
C --- TRAITEMENT MOT-CLEF GROUP_NO_RACC/NOEUD_RACC
C
      RACCNO = DEFICO(1:16)//'.RANOCO'
      PRACC  = DEFICO(1:16)//'.PRANOCO'
      LIMOCL(1)  = 'GROUP_NO_RACC'
      LIMOCL(2)  = 'NOEUD_RACC'
      CALL SANSNO(CHAR  ,MOTFAC,NOMA  ,RACCNO,PRACC ,
     &            NBMOCL,TYMOCL,LIMOCL)     
C
C --- TRAITEMENT MOT-CLEF SANS_GROUP_NO_FROT/SANS_NOEUD_FROT
C
      FROTNO = DEFICO(1:16)//'.SANOFR'
      PFROT  = DEFICO(1:16)//'.PSANOFR'
      LIMOCL(1)  = 'SANS_GROUP_NO_FR'
      LIMOCL(2)  = 'SANS_NOEUD_FR'      
      CALL SANSNO(CHAR  ,MOTFAC,NOMA  ,FROTNO,PFROT ,
     &            NBMOCL,TYMOCL,LIMOCL)
C
C --- TRAITEMENT MOT-CLEF GROUP_MA_FOND/MAILLE_FOND
C
      BARSMA = DEFICO(1:16)//'.BAMACO'
      PBARM  = DEFICO(1:16)//'.PBAMACO'
      TYMOCL(1)  = 'GROUP_MA'
      TYMOCL(2)  = 'MAILLE'
      LIMOCL(1)  = 'GROUP_MA_FOND'
      LIMOCL(2)  = 'MAILLE_FOND'            
      
      CALL SANSMA(CHAR  ,MOTFAC ,NOMA  ,BARSMA,PBARM ,
     &            NBMOCL,TYMOCL,LIMOCL)    
      CALL JEDEMA()
      END
