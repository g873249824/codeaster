C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C 
      IMPLICIT     NONE
      CHARACTER*8  CHAR
      INTEGER      NDIM
      INTEGER      NZOCO
      INTEGER      NSUCO
      INTEGER      NMACO
      INTEGER      NNOCO
      INTEGER      NMANO
      INTEGER      NNOMA
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES - LECTURE DONNEES)
C
C CONSTRUCTION DU VECTEUR D'INFORMATION SUR LES LONGUEURS
C      
C ----------------------------------------------------------------------
C
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  NOMA   : NOM DU MAILLAGE
C IN  NDIM   : DIMENSION DU PROBLEME
C IN  NZOCO  : NOMBRE DE ZONES DE CONTACT
C IN  NSUCO  : NOMBRE TOTAL DE SURFACES DE CONTACT
C IN  NMACO  : NOMBRE TOTAL DE MAILLES DES SURFACES
C IN  NNOCO  : NOMBRE TOTAL DE NOEUDS DES SURFACES
C IN  NMANO  : DIMENSION DU TABLEAU INVERSE NOEUDS->MAILLES
C IN  NNOMA  : DIMENSION DU TABLEAU DIRECT MAILLES->NOEUDS
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX ----------------
C
      CHARACTER*24 NDIMCO
      INTEGER      JDIM
      INTEGER      NTNOE,NTMAE
      INTEGER      IZONE,ISURFE,IBID
      INTEGER      NBNOE,NBMAE
      CHARACTER*24 DEFICO      
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DEFICO = CHAR(1:8)//'.CONTACT'      
C 
C --- ACCES AUX STRUCTURES DE DONNEES DE CONTACT
C 
      NDIMCO = DEFICO(1:16)//'.NDIMCO'    
      CALL JEVEUO(NDIMCO,'E',JDIM)       
C      
C --- DIMENSION DE L'ESPACE
C      
      IF (NDIM.GT.3) THEN
        CALL U2MESS('A','CONTACT_84')
        IF (NDIM.EQ.1003) THEN
          NDIM     = 3
        ELSE IF (NDIM.EQ.1002) THEN
          NDIM     = 2
        ELSEIF (NDIM.EQ.23) THEN
          NDIM     = 2  
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF  
      ENDIF
C 
C --- TABLEAU CONTENANT LES LONGUEURS DES DIFFERENTS VECTEURS 
C --- ET LE NOMBRE DE NOEUDS ESCLAVES MAXIMUM POUR CHAQUE ZONE 
C
      ZI(JDIM+1-1)  = NDIM
      ZI(JDIM+2-1)  = NZOCO
      ZI(JDIM+3-1)  = NSUCO
      ZI(JDIM+4-1)  = NMACO
      ZI(JDIM+5-1)  = NNOCO
      ZI(JDIM+6-1)  = NMANO
      ZI(JDIM+7-1)  = NNOMA
      ZI(JDIM+8-1)  = 0
C 
C --- CALCUL DU NOMBRE TOTAL DE NOEUDS/MAILLES ESCLAVES
C
      NTNOE = 0
      NTMAE = 0
      DO 20 IZONE = 1,NZOCO
        CALL CFZONE(DEFICO,IZONE ,'ESCL',ISURFE) 
        CALL CFNBSF(DEFICO,ISURFE,'NOEU',NBNOE ,IBID  )
        CALL CFNBSF(DEFICO,ISURFE,'MAIL',NBMAE ,IBID  )
        NTNOE = NTNOE + NBNOE
        NTMAE = NTMAE + NBMAE
   20 CONTINUE
C 
      ZI(JDIM+9-1)  = NTNOE
      ZI(JDIM+10-1) = NTMAE
C
      CALL JEDEMA()
      END
