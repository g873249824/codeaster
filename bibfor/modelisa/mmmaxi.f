C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*8  MODELE
      CHARACTER*24 LISMA
      INTEGER      NBMA
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE)
C
C CETTE FONCTION PERMET DE DETERMINER SI LES ELEMENTS D'UN GROUPE DE
C MAILLES DE CONTACT SONT TOUS AXISYMETRIQUES
C
C ----------------------------------------------------------------------
C
C
C IN  MODELE : NOM DU MODELE
C IN  LISMA  : LISTE DES MAILLES A VERIFIER
C IN  NBMA   : NOMBRE DE MAILLES CONTENU DANS LA LISTE LISMA
C OUT MMMAXI : .TRUE.  LE GROUPE DE MAILLE NE CONTIENT QUE DES ELEMENTS
C                      AXISYMETRIQUES
C              .FALSE. LE GROUPE DE MAILLE NE CONTIENT QUE DES ELEMENTS
C                      NON AXIS
C              ERREUR <F> LE GROUPE DE MAILLE CONTIENT UN MELANGE
C                         D'ELEMENT AXIS ET NON AXIS
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNUM
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*16 NOTYPE
      CHARACTER*24 LIGREL,K24BID
      INTEGER      NUMAIL,NUTYEL
      INTEGER      IMA,ILMAIL,IEL,I,IGREL
      INTEGER      NBGREL,NBMAGL,NBEL,NBAXIS
      LOGICAL      LTEATT
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NBAXIS = 0
      LIGREL = MODELE(1:8)//'.MODELE    .LIEL'
C
      CALL JELIRA(LIGREL,'NUTIOC',NBGREL,K24BID)
      CALL JEVEUO(LISMA ,'L',ILMAIL)
C
      MMMAXI=.FALSE.
C
      DO 10 I = 1, NBGREL
         CALL JEVEUO(JEXNUM(LIGREL,I),'L',IGREL)
         CALL JELIRA(JEXNUM(LIGREL,I),'LONMAX',NBMAGL,K24BID)
         NUTYEL = ZI(IGREL-1+NBMAGL)
         CALL JENUNO(JEXNUM('&CATA.TE.NOMTE',NUTYEL),NOTYPE)
         IF (LTEATT(NOTYPE,'AXIS','OUI') .AND.
     &       LTEATT(NOTYPE,'BORD','OUI')) THEN
            NBEL = NBMAGL - 1
            DO 20 IEL = 1, NBEL
               NUMAIL = ZI(IGREL-1+IEL)
               DO 30 IMA = 1, NBMA
                  IF (ZI(ILMAIL-1+IMA).EQ.NUMAIL) THEN
                     NBAXIS = NBAXIS + 1
                  ENDIF
30             CONTINUE
20          CONTINUE
         ENDIF
10    CONTINUE
C
      IF (NBAXIS.EQ.NBMA) THEN
        MMMAXI = .TRUE.
      ELSEIF(NBAXIS.EQ.0) THEN
        MMMAXI = .FALSE.
      ELSE
        CALL U2MESS('F','CONTACT2_12')
      ENDIF
C
      CALL JEDEMA()
C
      END
