      IMPLICIT NONE
      CHARACTER*8  MODELE
      CHARACTER*24 LISMA
      INTEGER      NBMA
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE)
C
C CETTE FONCTION PERMET DE DETERMINER SI LES ELEMENTS D'UN GROUPE DE 
C MAILLES DE CONTACT SONT TOUS AXISYMETRIQUES
C      
C ----------------------------------------------------------------------
C
C
C IN  MODELE : NOM DU MODELE
C IN  LISMA  : LISTE DES MAILLES A VERIFIER
C IN  NBMA1  : NOMBRE DE MAILLES CONTENU DANS LA LISTE LISMA
C OUT MMMAXI : .TRUE.  LE GROUPE DE MAILLE NE CONTIENT QUE DES ELEMENTS
C                      AXISYMETRIQUES
C              .FALSE. LE GROUPE DE MAILLE CONTIENT :
C                      SOIT UN MELANGE D'ELEMENT AXIS ET NON AXIS
C                      SOIT QUE DES ELEMENTS NON AXIS 
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNUM, JEXATR
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*16 NOTYPE
      CHARACTER*24 LIGREL,K24BID
      INTEGER      NUMAIL,NUTYEL
      INTEGER      IALIEL,ILLIEL
      INTEGER      IMA,ILMAIL,IEL,I,IGREL
      INTEGER      NBGREL,NBMAGL,NBELEM,NBEL,NBAXIS,NBCLAS
      LOGICAL      LTEATT      
C      
      NUMAIL(I,IEL) = ZI(IALIEL-1+ZI(ILLIEL+I-1)+IEL-1)
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NBAXIS = 0
      NBCLAS = 0 
      LIGREL = MODELE(1:8)//'.MODELE    .LIEL'
C      
      CALL JELIRA(LIGREL,'NMAXOC',NBGREL,K24BID)
      CALL JEVEUO(LIGREL,'L',IALIEL)
      CALL JEVEUO(JEXATR(LIGREL,'LONCUM'),'L',ILLIEL)
      CALL JEVEUO(LISMA,'L',ILMAIL)
C
      DO 10 I=1,NBGREL
         CALL JEVEUO(JEXNUM(LIGREL,I),'L',IGREL)
         CALL JELIRA(JEXNUM(LIGREL,I),'LONMAX',NBMAGL,K24BID)
         NUTYEL = ZI(IGREL+NBMAGL-1)
         CALL JENUNO(JEXNUM('&CATA.TE.NOMTE',NUTYEL),NOTYPE)
         IF (NOTYPE.EQ.'MEAXSE2' .OR. NOTYPE.EQ.'MEAXSE3') THEN
            NBEL = NBELEM(LIGREL,I)
            DO 20 IEL = 1, NBEL
               DO 30 IMA = 1,NBMA
                  IF(NUMAIL(I,IEL).EQ.ZI(ILMAIL-1+IMA)) THEN
                     IF(LTEATT(NOTYPE,'AXIS','OUI')) THEN
                        NBAXIS=NBAXIS+1
                     ELSE 
                        NBCLAS=NBCLAS+1
                     ENDIF
                  ENDIF
30             CONTINUE
20          CONTINUE
            IF (NBAXIS.EQ.NBMA) THEN
              MMMAXI=.TRUE.
            ELSEIF(NBCLAS.EQ.NBMA) THEN
              MMMAXI=.FALSE.
            ELSE
              CALL U2MESS('F','CONTACT2_12')
            ENDIF
         ELSE
           MMMAXI=.FALSE.
         ENDIF   
10    CONTINUE
C
      CALL JEDEMA()
C
      END
