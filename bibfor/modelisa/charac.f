C
C      OPERATEURS :  AFFE_CHAR_ACOU  ET  AFFE_CHAR_ACOU_F
C
C      MOTS-CLES ACTUELLEMENT TRAITES:
C         VITE_FACE
C         IMPE_FACE
C         PRES_IMPO
C         LIAISON_UNIF
C ----------------------------------------------------------------------
      INTEGER       NDIM ,IRET, IBID
      CHARACTER*4   FONR2
      CHARACTER*8   CHAR, NOMA
      CHARACTER*16  TYPE, OPER, MOTFAC
      CHARACTER*19  LIGRMO,LIGRCH
C     ------------------------------------------------------------------
C
      CALL GETRES ( CHAR, TYPE, OPER )
C
C --- NOMS DE LIGREL, MAILLAGE , DIMENSION DU PB
C
      CALL CAGENE ( CHAR, OPER, LIGRMO, NOMA, NDIM )
C
      FONR2 = 'COMP'
      IF ( OPER(15:16) .EQ. '_F' )  FONR2 = 'FONC'
C
C --- VITE_FACE ---
C
      CALL CBVITE ( CHAR, NOMA, LIGRMO, NDIM, FONREE )
C
C  --- IMPE_FACE ---
C
      CALL CBIMPE ( CHAR, NOMA, LIGRMO, NDIM, FONREE )
C
C --- PRES_IMPO ---
C
      MOTFAC = 'PRES_IMPO       '
      CALL CADDLI ( OPER, MOTFAC, FONR2, CHAR )
C
C --- LIAISON_UNIF ---
C
      CALL CAGROU ( FONREE, CHAR )


C --- REDECOUPAGE DU LIGREL DE CHARGE
C       EN FONCTION DE LA TAILLE MAX DES .RESL
C     + INITIALISATION DES TYPE_ELEMENT
      LIGRCH = CHAR//'.CHAC.LIGRE'
      CALL JEEXIN(LIGRCH//'.LGRF',IRET)
      IF (IRET.NE.0) THEN
        CALL ADALIG(LIGRCH)
        CALL CORMGI('G', LIGRCH)
        CALL JEECRA(LIGRCH//'.LGRF','DOCU',IBID,'ACOU')
        CALL INITEL(LIGRCH)
      END IF
C
      END
