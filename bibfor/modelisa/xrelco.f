C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE GENIAUT S.GENIAUT
C
      IMPLICIT NONE
      INCLUDE 'jeveux.h'

      CHARACTER*32 JEXNUM
      INTEGER      NREL
      CHARACTER*8  NOMA
      CHARACTER*19 NLISEQ,NLISRL,NLISCO
      CHARACTER*19 LISREL
C
C ----------------------------------------------------------------------
C
C ROUTINE XFEM (PREPARATION - AFFE_CHAR_MECA)
C
C CREER DES RELATIONS ENTRE LES INCONNUES DE CONTACT POUR
C SATISFAIRE LA LBB CONDITION
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  NLISEQ : LISTE REL. LIN. POUR V2 SEULEMENT
C IN  NBASCO : CHAM_NO POUR BASE COVARIANTE
C OUT NREL   : NOMBRE DE RELATIONS À IMPOSER
C
C
C
C
      INTEGER     NBDDL,XXMMVD
      PARAMETER  (NBDDL=12)
      CHARACTER*8 DDLC(NBDDL)
C
      REAL*8       RBID,BETAR,COEFR(6)
      INTEGER      IER,JLIS1,NDIME(8),NEQ,I,JLIS2,IBID,IRET
      INTEGER      NUNO(8),NDIM,J
      CHARACTER*8  NOEUD(8),K8BID,DDL(8)
      COMPLEX*16   CBID
      LOGICAL      LMULTI
C
      DATA DDLC /'LAGS_C','LAGS_F1','LAGS_F2',
     &            'LAG2_C','LAG2_F1','LAG2_F2',
     &            'LAG3_C','LAG3_F1','LAG3_F2',
     &            'LAG4_C','LAG4_F1','LAG4_F2'/
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INTIALISATIONS
C
      BETAR  = 0.D0
      DO 5 I = 1,8
        NDIME(I) = 0
 5    CONTINUE
C
C --- DONNÉES RELATIVES AU MAILLAGE
C
      CALL DISMOI('F','DIM_GEOM',NOMA,'MAILLAGE',NDIM,K8BID,IRET)
C
C --- 1) RELATIONS D'EGALITE
C
      CALL JEEXIN(NLISEQ,IER)
      IF (IER.EQ.0)THEN
        GO TO 100
      ELSE
        CALL JEVEUO(NLISEQ,'L',JLIS1)
        CALL JELIRA(NLISEQ,'LONMAX',NEQ,K8BID)
        CALL JEEXIN(NLISEQ(1:14)//'_LAGR',IER)
        IF (IER.EQ.0) THEN
          LMULTI = .FALSE.
        ELSE
          LMULTI = .TRUE.
          CALL JEVEUO(NLISEQ(1:14)//'_LAGR','L',JLIS2)
        ENDIF
      ENDIF

      DO 10 I = 1,NEQ/2
        NUNO(1)  = ZI(JLIS1-1+2*(I-1)+1)
        NUNO(2)  = ZI(JLIS1-1+2*(I-1)+2)

        CALL JENUNO(JEXNUM(NOMA(1:8)//'.NOMNOE',NUNO(1)),NOEUD(1))
        CALL JENUNO(JEXNUM(NOMA(1:8)//'.NOMNOE',NUNO(2)),NOEUD(2))
C
        COEFR(1) = 1.D0
        COEFR(2) = -1.D0
C
C --- RELATION POUR LES MULTIPLICATEURS DE CONTACT ET FROTTEMENT
C
        DO 20 J = 1,NDIM
          IF (.NOT.LMULTI) THEN
            DDL(1) = DDLC(J)
            DDL(2) = DDLC(J)
          ELSE
            DDL(1) = DDLC(3*(ZI(JLIS2-1+2*(I-1)+1)-1)+J)
            DDL(2) = DDLC(3*(ZI(JLIS2-1+2*(I-1)+2)-1)+J)
          ENDIF
          CALL AFRELA(COEFR,CBID,DDL,NOEUD,NDIME,RBID,2,BETAR,CBID,
     &              K8BID,'REEL','REEL','12',0.D0,LISREL)
          NREL = NREL + 1
 20     CONTINUE
 10   CONTINUE
 100  CONTINUE
C
C
      CALL JEDEMA()
      END
