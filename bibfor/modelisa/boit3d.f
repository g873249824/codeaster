      SUBROUTINE BOIT3D(MAIL  ,TYPMAI,NGRMA ,NORM  ,PRECBO,
     &                  NHQUA ,NOMBOI)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF MODELISA  DATE 09/01/2007   AUTEUR ABBAS M.ABBAS 
C ======================================================================
