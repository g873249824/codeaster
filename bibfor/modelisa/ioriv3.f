      IMPLICIT REAL*8 (A-H,O-Z)
C
C   IORIV3  --  ORIENTATION D'UNE MAILLE PAR RAPPORT A UN VECTEUR
C
C   ARGUMENT     E/S  TYPE      ROLE
C   NUM          IN/OUT  K*     NUMEROTATION DE LA MAILLE
C
C   CODE RETOUR IORIV3 : 0 SI LA MAILLE NE CONTIENT PAS LE NOEUD
C                       -1 OU 1 SINON (SELON QU'IL AIT OU NON
C                                      FALLU REORIENTER)
      INTEGER NUM(2)
      REAL*8  VECT(3),COOR(3,*)
      X(I)=COOR(1,I)
      Y(I)=COOR(2,I)
      Z(I)=COOR(3,I)
C
C.========================= DEBUT DU CODE EXECUTABLE ==================
C
C     BOUCLE SUR LES SOMMETS
      IORIV3=0
      DO 10 I=1,2
      IF (NUM(I).EQ.NOEUD) THEN
        N1=NUM(1)
        N2=NUM(2)
        X1=X(N1)
        Y1=Y(N1)
        Z1=Z(N1)
        X2=X(N2)
        Y2=Y(N2)
        Z2=Z(N2)
        SCAL=(X2-X1)*VECT(1)+(Y2-Y1)*VECT(2)+(Z2-Z1)*VECT(3)
        IF (SCAL.GT.0) THEN
          IORIV3= 1
        ELSE IF (SCAL.LT.0) THEN
          IORIV3=-1
        ELSE
          CALL U2MESS('F','MODELISA4_83')
        ENDIF
      ENDIF
   10 CONTINUE
      IF (IORIV3.LT.0) THEN
C       ON PERMUTE LES SOMMETS
        K=NUM(1)
        L=NUM(2)
        NUM(1)=L
        NUM(2)=K
      ENDIF
C
      END
