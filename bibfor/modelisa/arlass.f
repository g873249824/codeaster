      IMPLICIT NONE
      INTEGER SIGN
      INTEGER INO
      INTEGER NELEM(*)
      INTEGER CMP
      INTEGER NTERM
      INTEGER NEQ
      INTEGER NLARG
      INTEGER LIEL(2,*)
      INTEGER NEMA(4,*)
      REAL*8  VLR
      REAL*8  IMPO(*)
      REAL*8  MULT(6,*)
C
C ----------------------------------------------------------------------
C
C ROUTINE ARLEQUIN
C
C ECRITURE D'UN TERME DANS LES RELATIONS LINEAIRES COUPLAGE
C
C ----------------------------------------------------------------------
C
C
C IN  SIGN   : SIGNE DEVANT R (0 : +, 1 : -)
C IN  INO    : NUMERO DU NOEUD
C IN  NELEM  : NUMERO DES ELEMENTS DE LIAISON 'D_DEPL_R_*'
C                       * = (DX, DY, DZ, DRX, DRY, DRZ)
C IN  CMP    : NUMERO DE LA COMPOSANTE (INDEX NU)
C IN  VLR    : VALEUR DU TERME
C I/O NTERM  : NOMBRE DE TERME
C I/O NEQ    : NUMERO DE L'EQUATION
C I/O NLARG  : NUMERO DU PREMIER LAGRANGE - 1
C I/O IMPO   : VECTEUR CHME.CIMPO.VALE
C I/O MULT   : VECTEUR CHME.CMULT.VALE
C I/O LIEL   : COLLECTION CHME.LIGRE.LIEL
C I/O NEMA   : COLLECTION CHME.LIGRE.NEMA
C
C ----------------------------------------------------------------------
C
      NEQ         = NEQ + 1
      LIEL(1,NEQ) = -NEQ
      LIEL(2,NEQ) = NELEM(CMP)
      NEMA(1,NEQ) = INO
      NEMA(2,NEQ) = -(NLARG+1)
      NEMA(3,NEQ) = -(NLARG+2)
      NEMA(4,NEQ) = 4
C
      NTERM         = NTERM + 1
      IMPO(NTERM)   = 0.D0
C
      IF (SIGN.EQ.0) THEN
        MULT(1,NTERM) = VLR
      ELSE
        MULT(1,NTERM) = -VLR
      ENDIF
C
      END
