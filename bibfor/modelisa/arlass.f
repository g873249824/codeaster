C ----------------------------------------------------------------------
C VARIABLES D'ENTREE 
C INTEGER   S         : SIGNE DEVANT R (0 : +, 1 : -)
C INTEGER   INO       : NUMERO DU NOEUD
C INTEGER   NU(6)     : NUMERO DES ELEMENTS DE LIAISON 'D_DEPL_R_*'
C                       * = (DX, DY, DZ, DRX, DRY, DRZ)   
C INTEGER   CMP       : NUMERO DE LA COMPOSANTE (INDEX NU)
C REAL*8    R         : VALEUR DU TERME
C
C VARIABLES D'ENTREE / SORTIE
C INTEGER   ND        : NOMBRE DE TERME
C INTEGER   NE        : NUMERO DE L'EQUATION
C INTEGER   NL        : NUMERO DU PREMIER LAGRANGE - 1
C REAL*8    IMPO(*)   : VECTEUR CHME.CIMPO.VALE
C REAL*8    MULT(6,*) : VECTEUR CHME.CMULT.VALE
C INTEGER   LIEL(2,*) : COLLECTION CHME.LIGRE.LIEL
C INTEGER   NEMA(4,*) : COLLECTION CHME.LIGRE.NEMA
C ----------------------------------------------------------------------

      IMPLICIT NONE

C --- VARIABLES
      INTEGER S,INO,NU(*),CMP,ND,NE,NL,LIEL(2,*),NEMA(4,*)
      REAL*8  R,IMPO(*),MULT(6,*)

      NE = NE + 1
      LIEL(1,NE) = -NE
      LIEL(2,NE) = NU(CMP)
      NEMA(1,NE) = INO
      NEMA(2,NE) = -(NL+1)
      NEMA(3,NE) = -(NL+2)
      NEMA(4,NE) = 4
      
      ND = ND + 1
      IMPO(ND) = 0.D0

      IF (S.EQ.0) THEN 
        MULT(1,ND) = R
      ELSE
        MULT(1,ND) = -R
      ENDIF

      END
