      IMPLICIT     NONE
      INTEGER      NSUCO
      INTEGER      NMACO0,NMACO
      CHARACTER*24 DEFICO
      CHARACTER*24 LISTMA
      CHARACTER*24 POINSM
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES - LECTURE DONNEES - ELIMINATION)
C
C MISE A JOUR DE LA LISTE DES MAILLES APRES ELIMINATION
C      
C ----------------------------------------------------------------------
C
C 
C IN  DEFICO : NOM SD CONTACT DEFINITION
C IN  NSUCO  : NOMBRE TOTAL DE SURFACES DE CONTACT
C IN  NMACO0 : NOMBRE TOTAL DE MAILLES DES SURFACES
C IN  POINSM : POINTEUR MISE A JOUR POUR PSURMA
C IN  LISTMA : LISTE DES MAILLES RESTANTES (LONGUEUR NMACO
C IN  NMACO  : NOMBRE DE MAILLES AU FINAL 
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*24 CONTMA,PSURMA
      INTEGER      JMACO,JSUMA
      INTEGER      JELIMA,JMA
      INTEGER      ISUCO,I
      CHARACTER*8  K8BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ() 
C
C --- ACCES SD
C         
      CALL JEVEUO(LISTMA,'L',JMA   )
      CALL JEVEUO(POINSM,'L',JELIMA)
C         
      CONTMA = DEFICO(1:16)//'.MAILCO'
      PSURMA = DEFICO(1:16)//'.PSUMACO'      
      CALL JEVEUO(PSURMA,'E',JSUMA) 
      CALL JEVEUO(CONTMA,'E',JMACO)                 
C 
C --- MODIFICATION DU POINTEUR PSURMA
C 
      DO 160 ISUCO = 1,NSUCO
        ZI(JSUMA+ISUCO)  = ZI(JSUMA+ISUCO)  - ZI(JELIMA+ISUCO)
  160 CONTINUE
C
C --- TRANSFERT DES VECTEURS DE TRAVAIL DANS CONTMA
C
      DO 170 I = 1,NMACO
        ZI(JMACO+I-1) = ZI(JMA+I-1)
  170 CONTINUE
C
C --- MAZ ET MODIFICATION DE L'ATTRIBUT LONUTI
C
      DO 180 I = NMACO + 1,NMACO0
        ZI(JMACO+I-1) = 0
  180 CONTINUE
      CALL JEECRA(CONTMA,'LONUTI',NMACO,K8BID) 
C
      CALL JEDEMA()
      END
