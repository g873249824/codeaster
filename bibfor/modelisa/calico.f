      CHARACTER*(*) CHARZ
      CHARACTER*(*) NOMAZ
      CHARACTER*(*) NOMOZ
      INTEGER       NDIM,IFORM
      CHARACTER*19  LIGRET
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT
C
C TRAITEMENT DU CONTACT DANS DEFI_CONTACT
C
C ----------------------------------------------------------------------
C
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  NOMA   : NOM DU MAILLAGE
C IN  NOMO   : NOM DU MODELE
C IN  NDIM   : NOMBRE DE DIMENSIONS DU PROBLEME
C IN  IFORM  : TYPE DE FORMULATION (DISCRETE/CONTINUE/XFEM)
C OUT LIGRET : LIGREL D'ELEMENTS TARDIFS DU CONTACT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*8  CHAR,NOMA,NOMO
      CHARACTER*16 MOTFAC
      INTEGER      NZOCO
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NOMO   = NOMOZ(1:8)
      CHAR   = CHARZ
      NOMA   = NOMAZ
      NZOCO  = 0
      MOTFAC = 'ZONE'
C
C --- RECUPERATION DU NOMBRE DE ZONES DE CONTACT (NOMBRE D'OCCURENCES)
C
      CALL GETFAC(MOTFAC,NZOCO )
C
      IF (NZOCO.EQ.0) THEN
        GOTO 999
      ENDIF
C
C --- RECUPERATION DES CARACTERISTIQUES DU CONTACT
C
      CALL CARACO(CHAR  ,NOMO  ,MOTFAC,NZOCO ,IFORM )
C
C --- LECTURE DES MAILLES DE CONTACT  ET CREATION DES SDS
C
      CALL LIMACO(CHAR  ,MOTFAC,NOMA  ,NOMO  ,NDIM  ,
     &            NZOCO ,LIGRET)
C
C --- IMPRESSIONS SUR LES ZONES/SURFACES/MAILLES/NOEUDS DE CONTACT
C
      CALL SURFCO(CHAR,NOMA)
C
 999  CONTINUE
C
      CALL JEDEMA()
C
      END
