C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT      NONE
      CHARACTER*(*) CHARZ
      CHARACTER*(*) NOMAZ
      CHARACTER*(*) LIGRMZ
      INTEGER       NDIM
      CHARACTER*4   FONREE
C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : CHARME
C ----------------------------------------------------------------------
C
C TRAITEMENT DU MOT-FACTEUR 'LIAISON_UNILATERALE' DANS AFFE_CHAR_MECA.
C
C IN  CHARZ  : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  NOMAZ  : NOM DU MAILLAGE
C IN  LIGRMZ : NOM DU LIGREL DU MODELE
C IN  NDIM   : NOMBRE DE DIMENSIONS DU PROBLEME
C IN  FONREE : FONC OU REEL SUIVANT L'OPERATEUR
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*8    K8BID,CHAR,NOMA,NOMO   
      CHARACTER*16   K16BID,NOMCMD,MOTFAC
      INTEGER        IBID,IER
      CHARACTER*16   PHENOM,MODELI
      INTEGER        NBOCC
      CHARACTER*24   NOLINO,NOPONO,NONBNO
      INTEGER        NBNOE,NBTOT
      CHARACTER*24   LISNOE,POINOE
      CHARACTER*24   DIMECU,NBGDCU,COEFCU,CMPGCU,MULTCU
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- RECUPERATION DU NOMBRE D'OCCURENCES
C
      MOTFAC = 'LIAISON_UNILATER'
      CALL GETFAC(MOTFAC,NBOCC)
C
C --- PAS DE LIAISON UNILATERALE
C
      IF (NBOCC.EQ.0) THEN 
        GOTO 999
      ENDIF
C
C ======================================================================
C --- INITIALISATIONS
C ======================================================================
C     
      NOMO   = LIGRMZ(1:8)
      CHAR   = CHARZ
      NOMA   = NOMAZ
C
C --- RECUPERATION NOM DU CONCEPT RESULTAT AFFE_CHAR_MECA
C      
      CALL GETRES(K8BID,K16BID,NOMCMD)
C
C --- RECUPERATION DU NOM DU PHENOMENE ET DE LA MODELISATION          
C
      CALL DISMOI('F','PHENOMENE',NOMO,'MODELE',IBID,PHENOM,IER)
      CALL DISMOI('F','MODELISATION',NOMO,'MODELE',IBID,MODELI,IER)
C
C --- COMPTE NOMBRE NOEUDS POUR CHAQUE OCCURRENCE
C
      NONBNO = '&&CALIUN.NBNOEU'
      NOPONO = '&&CALIUN.PONOEU'
      CALL WKVECT(NONBNO,'V V I',4*NBOCC,IBID)
      CALL WKVECT(NOPONO,'V V I',NBOCC+1,IBID)
      CALL NBNODE(NOMA,MOTFAC,NBOCC,
     &            NONBNO,NOPONO,NBTOT)
C
C --- EXTRACTION NOEUDS POUR CHAQUE OCCURRENCE
C
      NOLINO = '&&CALIUN.LINOEU'
      CALL WKVECT(NOLINO,'V V I',NBTOT,IBID)
      CALL EXNODE(NOMA,MOTFAC,NBOCC,
     &            NONBNO,NOPONO,NBTOT,
     &            NOLINO)
      CALL JEDETR(NONBNO)
C
C --- RECUPERATION CARACTERISTIQUES ELEMENTAIRES POUR CHAQUE OCCURRENCE
C
      DIMECU = '&&CALIUN.DIMECU'      
      NBGDCU = '&&CALIUN.NBGDCU'
      CALL WKVECT(DIMECU,'V V I',2*NBOCC+4,IBID)           
      CALL WKVECT(NBGDCU,'V V I',NBOCC+1,IBID)      
      CALL CARAUN(CHAR,NOMA,MOTFAC,NBOCC,FONREE,
     &            DIMECU,NBGDCU,
     &            COEFCU,CMPGCU,MULTCU)   
C
C --- ELIMINATION DES NOEUDS 
C      SUPPRESSION DES DOUBLONS ENTRE MAILLE, GROUP_MA, NOEUD,GROUP_NO
C      SUPPRESSION DES NOEUDS DEFINIS DANS SANS_NOEUD ET SANS_GROUP_NO
C
      LISNOE = '&&CALIUN.LISNOE'
      POINOE = '&&CALIUN.POINOE'
      CALL WKVECT(POINOE,'V V I',NBOCC+1,IBID)       
      CALL ELIMUN(CHAR,NOMA,NOMO,MOTFAC,NBOCC,
     &            DIMECU,NBGDCU,CMPGCU,
     &            NOPONO,NOLINO, 
     &            LISNOE,POINOE,
     &            NBTOT,NBNOE)
      CALL JEDETR(NOLINO) 
      CALL JEDETR(NOPONO)                
C
C --- ELIMINATION DES COMPOSANTES ET CREATION FINALE DES SD
C       
      CALL CREAUN(CHAR,NOMA,NOMO,NBOCC,
     &            LISNOE,POINOE,       
     &            DIMECU,NBGDCU,
     &            COEFCU,CMPGCU,MULTCU)
      CALL JEDETR(LISNOE) 
      CALL JEDETR(POINOE)      
      CALL JEDETR(DIMECU) 
      CALL JEDETR(NBGDCU) 
      CALL JEDETR(COEFCU) 
      CALL JEDETR(CMPGCU) 
      CALL JEDETR(MULTCU)                       
C
C --- AFFICHAGE DES INFORMATIONS                 
C
      CALL SURFUN(CHAR,NOMA)
C

C
 999  CONTINUE

C ======================================================================
      CALL JEDEMA()
C
      END
