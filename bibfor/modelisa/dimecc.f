C ROUTINE CONTACT (METHODES MAILLEES - LECTURE DONNEES)
C
C CONSTRUCTION DU VECTEUR D'INFORMATION SUR LES LONGUEURS - COMPLEMENT
C      
C ----------------------------------------------------------------------
C
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX ----------------
C
      CHARACTER*24 NDIMCO,MAESCL
      INTEGER      JDIM,JMAESC
      INTEGER      CFMMVD,ZMAES     
      INTEGER      CFDISI,IFORM
      INTEGER      NTNOE,NTMAE,NTPC,NTELNO,NZOCO
      INTEGER      IZONE,IBID
      INTEGER      NBMAE,NBMAM,NBELNO,NNINT
      INTEGER      JDECME,JDECMM,POSMAE,POSMAM,ISUMAE,ISUMAM
      INTEGER      IMAE,IMAM
      CHARACTER*24 DEFICO      
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DEFICO = CHAR(1:8)//'.CONTACT' 
      IFORM  = CFDISI(DEFICO,'FORMULATION')
      NZOCO  = CFDISI(DEFICO,'NZOCO')  
      NTMAE  = CFDISI(DEFICO,'NTMAE')
      NTNOE  = CFDISI(DEFICO,'NTNOE')                 
C 
C --- ACCES AUX STRUCTURES DE DONNEES DE CONTACT
C 
      MAESCL = DEFICO(1:16)//'.MAESCL'
      NDIMCO = DEFICO(1:16)//'.NDIMCO'
      CALL JEVEUO(MAESCL,'L',JMAESC)    
      CALL JEVEUO(NDIMCO,'E',JDIM  ) 
      ZMAES  = CFMMVD('ZMAES')            
C
C --- NOMBRE DE POINTS DE CONTACT
C      
      IF (IFORM.EQ.1) THEN
        NTPC   = NTNOE
      ELSEIF (IFORM.EQ.2) THEN
        NTPC   = 0
        DO 40 IMAE = 1,NTMAE
          NNINT  = ZI(JMAESC+ZMAES*(IMAE-1)+3-1)
          NTPC   = NTPC + NNINT
 40     CONTINUE      
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF  
      CALL ASSERT(NTPC.NE.0)
      ZI(JDIM+11-1) = NTPC            
C
C --- NOMBRE TOTAL DE NOEUD AUX ELEMENTS (ELNO)
C
      NTELNO = 0
      DO 30 IZONE = 1,NZOCO  
        CALL CFZONE(DEFICO,IZONE ,'ESCL',ISUMAE)
        CALL CFNBSF(DEFICO,ISUMAE,'MAIL',NBMAE ,JDECME)
        DO 31 IMAE = 1,NBMAE
          POSMAE = JDECME + IMAE 
          CALL CFNBEN(DEFICO,POSMAE,'MAIL',NBELNO ,IBID  )
          NTELNO = NTELNO + NBELNO
 31     CONTINUE
        CALL CFZONE(DEFICO,IZONE ,'MAIT',ISUMAM)
        CALL CFNBSF(DEFICO,ISUMAM,'MAIL',NBMAM ,JDECMM)
        DO 32 IMAM = 1,NBMAM
          POSMAM = JDECMM + IMAM 
          CALL CFNBEN(DEFICO,POSMAM,'MAIL',NBELNO ,IBID  )
          NTELNO = NTELNO + NBELNO
 32     CONTINUE 
 30   CONTINUE    
C
      ZI(JDIM+12-1) = NTELNO
C
      CALL JEDEMA()
      END
