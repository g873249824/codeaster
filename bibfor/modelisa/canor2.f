      IMPLICIT NONE
      REAL*8 COOR(3,*),A,B,X1,X2,Y1,Y2,X12,Y12,NORME
      REAL*8 VALR(5)
C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
      X1=COOR(1,1)
      X2=COOR(1,2)
      Y1=COOR(2,1)
      Y2=COOR(2,2)
      X12=X2-X1
      Y12=Y2-Y1
      A=Y12
      B=-X12
      NORME  =SQRT(A*A+B*B)
      IF (NORME.GT.0) THEN
         A=A/NORME
         B=B/NORME
      ELSE
         VALR (1) = X1
         VALR (2) = Y1
         VALR (3) = X2
         VALR (4) = Y2
         VALR (5) = NORME
         CALL U2MESG('F', 'MODELISA8_52',0,' ',0,0,5,VALR)
      END IF
      END
