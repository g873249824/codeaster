C
C AFFICHAGE DES RESULTATS DE LA LECTURE DU MOT-CLE CONTACT.
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  NOMA   : NOM DU MAILLAGE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      INTEGER      IZONE,FORM
      LOGICAL      LMAIL
      CHARACTER*24 FORMCO
      INTEGER      JFORM  
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ
      CALL INFDBG('CONTACT',IFM,NIV) 
C
      IF (NIV.LE.1) THEN
        GOTO 999
      ENDIF       
C   
C --- COMMUNS TOUTES FORMULATIONS 
C
      FORMCO = CHAR(1:8)//'.CONTACT.FORMCO'
      CALL JEVEUO(FORMCO,'L',JFORM)
C
C --- TYPE DE FORMULATION (DISCRETE/CONTINUE/XFEM)
C
      CALL JEVEUO(FORMCO,'L',JFORM)
      IZONE  = 1
      FORM   = ZI(JFORM-1+IZONE) 
      LMAIL  = (FORM.EQ.1).OR.(FORM.EQ.2) 
C
      IF (LMAIL) THEN
        CALL SURFCL(CHAR,NOMA,IFM)
      ENDIF
C
      IF (FORM.EQ.1) THEN
        CALL SURFC1(CHAR,NOMA,IFM)
      ELSEIF (FORM.EQ.2) THEN    
        CALL SURFC2(CHAR,NOMA,IFM)
      ELSEIF (FORM.EQ.3) THEN
        CALL SURFC3(CHAR,NOMA,IFM)
      ELSE
        CALL ASSERT(.FALSE.)      
      ENDIF  
C
 999  CONTINUE          
C
      CALL JEDEMA
      END
