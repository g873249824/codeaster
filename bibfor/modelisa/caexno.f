      SUBROUTINE CAEXNO (LVAVZ,NOMAZ,MOTFAC,MCGRNO,MCNO,IOCC)
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*24       LVAV,NOMA
      CHARACTER*(*)      MOTFAC,MCGRNO,MCNO,LVAVZ,NOMAZ
      INTEGER            IOCC
C ---------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF MODELISA  DATE 11/04/97   AUTEUR VABHHTS J.PELLET 
C ======================================================================
C COPYRIGHT (C) 1991 - 2001  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR   
C (AT YOUR OPTION) ANY LATER VERSION.                                 
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT 
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF          
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU    
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                            
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C                 DATE 04/01/93   C.MENGONI
C
C     BUT : RETIRER DE LA LISTE DE VIS A VIS LVAV TOUS LES COUPLES
C           DONT UN DES TERMES EST APPARTIENT A LA LISTE DE NOEUDS
C           GENEREE PAR LES MOTS CLES MCGRNO ET MCNO POUR L'OCCURENCE
C           IOCC DU MOT CLE FACTEUR MOTFAC.
C IN  LVAVZ  K*(*): NOM UTILISATEUR DE LA LISTE DES VIS A VIS
C            OJB V V I DIM = 2 * NBCOUPLE + 1
C            LVAV(ILAD) = NBCOUPLE (BOUCLE SUR I <= NBCOUPLE)
C            LVAV(ILAD+2*(I-1)+1) = NUM1 DU NOEUD 1
C            LVAV(ILAD+2*(I-1)+2) = NUM2 DU NOEUD 2
C                   OU PREFIXE DES OJB .CONI ET .CONR
C IN  NOMAZ  K*(*): NOM DU MAILLAGE
C IN  MOTFAC K16  : MOT CLE FACTEUR A TRAITER
C IN  MCGRNO K16  : MOT CLE REGROUPANT LES GROUP_NO
C IN  MCNO   K16  : MOT CLE REGROUPANT LES NOEUDS
C IN  IOCC   I    : SI >0 ON TRAITE L'OCCURENCE IOCC DE MOTFAC
C                   SI <0 OU =0 ERREUR FATALE
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ---------------------------
C
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX -----------------------------
C
      CHARACTER*8        KBID
      CHARACTER*16       MOTF,MCGR,MCN
      CHARACTER*24       LISTEX
      INTEGER            LCONI,NBCPL,NBEX
      INTEGER            IBID
C --- DEBUT
      CALL JEMARQ()
      LVAV  = LVAVZ
      NOMA  = NOMAZ
      MOTF  = MOTFAC
      MCGR  = MCGRNO
      MCN   = MCNO
      IF (MOTF.NE.'LIAISON_GROUP') THEN
        CALL UTMESS('F','CAEXNO_00',' MOT CLE FACTEUR INCONNU :'//MOTF)
      ENDIF
      IF (IOCC.LE.0) THEN
        CALL UTMESS('F','CAEXNO_01',' NUMERO D"OCCURENCE NEGATIF ')
      ENDIF
      CALL GETFAC(MOTF,NLINO)
      IF ((NLINO.EQ.0).OR.(IOCC.GT.NLINO)) GOTO 999
C
C --- LECTURE DE LA LISTE DES NOEUDS EXCLUS
C
      LISTEX = '&&CAEXNO.LISTENOEUD'
      CALL PALINO(NOMA,MOTFAC,MCGR,MCN,IOCC,LISTEX)
C
C --- ELIMINATION
C
      CALL JEVEUO(LISTEX,'L',IDLEX)
      NBEX = ZI(IDLEX)
      IF (NBEX.EQ.0) GOTO 998
      CALL JEVEUO(JEXNUM(LVAV,IOCC),'E',IDLVAV)
      NBCPL = ZI(IDLVAV)
      L = 0
      DO 2 I = 1,NBCPL
        L = L+1
        DO 3 J = 1,NBEX
          IF ((ZI(IDLVAV+2*(I-1)+1).EQ.ZI(IDLEX+J)).OR.
     +        (ZI(IDLVAV+2*(I-1)+2).EQ.ZI(IDLEX+J))) THEN
            L = L-1
            GOTO 2
          ENDIF
3       CONTINUE
        ZI(IDLVAV+2*(L-1)+1) =  ZI(IDLVAV+2*(I-1)+1)
        ZI(IDLVAV+2*(L-1)+2) =  ZI(IDLVAV+2*(I-1)+2)
2     CONTINUE
      ZI(IDLVAV) = L
998   CONTINUE
      CALL JEDETR('&&CAEXNO.LISTENOEUD')
999   CONTINUE
C FIN -----------------------------------------------------------------
      CALL JEDEMA()
      END
