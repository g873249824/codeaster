      SUBROUTINE MAINOE(NOMA,NBM,LIMANU,NUOUNO,NBNO,OBJ)
      IMPLICIT   NONE
      INTEGER LIMANU(*),NBM
      CHARACTER*24 OBJ
      CHARACTER*8 NOMA
      CHARACTER*2 NUOUNO
