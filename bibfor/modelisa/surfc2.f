      SUBROUTINE SURFC2(CHAR,NOMA,IFM)
C      
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF MODELISA  DATE 07/05/2007   AUTEUR PROIX J-M.PROIX 
