      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER       N1, NBAPNO, MXMATA, NBOCC, I, NBMA, K
      CHARACTER*8   K8B, CHMAT, NOMAIL, NOMODE, TYPMCL(2)
      CHARACTER*16  MOTCLE(2), TYPE, NOMCMD
      CHARACTER*24  MESMAI
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFMAJ()

      NOMODE = ' '
      CALL GETVID ( ' ', 'MODELE', 1,1,1, NOMODE, N1 )
C
      CALL GETFAC ( 'AFFE' , NBOCC )
      CALL GETRES ( CHMAT, TYPE, NOMCMD)
      CALL GETVID ( ' ', 'MAILLAGE', 1,1,1, NOMAIL, N1 )
C
      MOTCLE(1) = 'GROUP_MA'
      MOTCLE(2) = 'MAILLE'
      TYPMCL(1) = 'GROUP_MA'
      TYPMCL(2) = 'MAILLE'
C
      MESMAI = '&&OP0006.MES_MAILLES'
C
      NBAPNO = NBOCC
      MXMATA = 0
      DO 10 I = 1 , NBOCC
         CALL RELIEM(NOMODE,NOMAIL,'NU_MAILLE','AFFE',I,2, MOTCLE,
     +                                      TYPMCL, MESMAI, NBMA )
         CALL JEDETR ( MESMAI )
         MXMATA = MXMATA + NBMA
 10   CONTINUE
C
      CALL RCMATE ( CHMAT, NOMAIL, MXMATA, NBAPNO, NOMODE )
      CALL RCTREF ( CHMAT, NOMAIL, MXMATA, NBAPNO, NOMODE )
C
      CALL JEDEMA()
      END
