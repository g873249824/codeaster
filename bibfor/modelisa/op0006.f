      CHARACTER*8 CHMAT,NOMAIL,NOMODE
      CHARACTER*16 NOMCMD, TYPE
      INTEGER IARG,IFM,N1,NIV
C ----------------------------------------------------------------------

      CALL JEMARQ()
      CALL INFMAJ()
      CALL INFNIV ( IFM , NIV )

      NOMODE = ' '
      CALL GETVID(' ','MODELE',1,IARG,1,NOMODE,N1)
      CALL GETRES(CHMAT,TYPE,NOMCMD)
      CALL GETVID(' ','MAILLAGE',1,IARG,1,NOMAIL,N1)


C     1- TRAITEMENT DU MOT-CLE AFFE :
C     -----------------------------------------
      CALL RCMATE(CHMAT,NOMAIL,NOMODE)


C     2- TRAITEMENT DU MOT-CLE AFFE_COMPOR :
C     -----------------------------------------
      CALL RCCOMP(CHMAT,NOMAIL,NOMODE)


C     3- TRAITEMENT DU MOT-CLE AFFE_VARC :
C     -----------------------------------------
      CALL AFVARC(CHMAT,NOMAIL,NOMODE)


C     4- IL FAUT RECONSTRUIRE LA CARTE .CHAMP_MAT POUR OBTENIR :
C     1 VALEUR DANS LA CARTE => 1 (OU PLUS) MATERIAU(X) + 1 TEMP_REF
C     C'EST UNE CONSEQUENCE DE RCMACO / ALFINT
C     -----------------------------------------------------------------
      CALL CMTREF(CHMAT,NOMAIL)


C     5- IMPRESSION DU CHAMP PRODUIT SI INFO=2 :
C     ------------------------------------------
      IF (NIV.GT.1) THEN
        CALL IMPRSD('CHAMP',CHMAT//'.CHAMP_MAT',IFM,'CHAM_MATER:')
      ENDIF


      CALL JEDEMA()
      END
