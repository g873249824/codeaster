C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX ----------------
C
      CHARACTER*24 NDIMCO,FROTE,PENAL,COMAFO
      INTEGER      JDIM,JFRO,JPENA,JCOMA
      INTEGER      NESMAX
C
C-----------------------------------------------------------------------
C
      CALL JEMARQ()
C 
C --- LECTURE DES STRUCTURES DE DONNEES DE CONTACT
C   
      NDIMCO = CHAR(1:8)//'.CONTACT.NDIMCO'    
      CALL JEVEUO(NDIMCO,'L',JDIM)
      NESMAX = ZI(JDIM+8)
C 
C --- DIMENSIONNEMENT DES TABLEAUX CONTENANT LES INFORMATIONS 
C --- POUR METHODES "PENALISATION" ET "LAGRANGIEN" 
C 
      FROTE  = CHAR(1:8)//'.CONTACT.FROTE'
      PENAL  = CHAR(1:8)//'.CONTACT.PENAL'
      COMAFO = CHAR(1:8)//'.CONTACT.COMAFO'
      CALL WKVECT(FROTE ,'G V R',NESMAX  ,JFRO)
      CALL WKVECT(PENAL ,'G V R',2*NESMAX,JPENA)
      CALL WKVECT(COMAFO,'G V R',NESMAX  ,JCOMA)
C
      CALL JEDEMA()
      END
