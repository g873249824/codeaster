C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*8  CHAR
      INTEGER      NDIM
      CHARACTER*8  NOMA,NOMO
C
C ----------------------------------------------------------------------
C
C ROUTINE XFEM (MODIF. DU MODELE)
C
C PREPARATION DONNEES RELATIVES AU CONTACT POUR LIAISONS LINEAIRES
C
C ----------------------------------------------------------------------
C
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  NOMA   : NOM DU MAILLAGE
C IN  NOMO   : NOM DU MODELE
C IN  NDIM   : DIMENSION DE L'ESPACE
C
C
C
C
      INTEGER      NFISMX
      PARAMETER    (NFISMX=100)
C
      INTEGER      IFISS,XXCONI,MMINFI,ALGOLA,IZONE
      INTEGER      NFISS,IER
      INTEGER      JFISS,JNFIS,NBMA,IBID,JNBPT
      CHARACTER*8  FISCOU,K8BID
      CHARACTER*24 DEFICO
      CHARACTER*16 VALK(2)
      CHARACTER*24 XNRELL
      INTEGER      JXNREL
      CHARACTER*19 NLISEQ,FACLON,AINTER
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DEFICO = CHAR(1:8)//'.CONTACT'
      FACLON = '&&XCONTA.FACLON'
      AINTER = '&&XCONTA.AINTER' 
C
C --- ACCES A LA SD FISSURE
C
      CALL EXIXFE(NOMO,IER)
      IF (IER.EQ.0) THEN
        VALK(1) = NOMO
        CALL U2MESK('F','XFEM2_8',1,VALK)
      ENDIF
      CALL JEVEUO(NOMO(1:8)//'.FISS','L',JFISS)
      CALL JEVEUO(NOMO(1:8)//'.NFIS','L',JNFIS)
      NFISS  = ZI(JNFIS)
      IF (NFISS .GT. NFISMX) THEN
        CALL U2MESI ('F', 'XFEM_2', 1, NFISMX)
      ENDIF
C
C --- CREATION SD POUR SD REL. LIN.
C
      XNRELL = DEFICO(1:16)//'.XNRELL'
      CALL WKVECT(XNRELL,'G V K24',NFISS,JXNREL)
C
C --- CHAMP TEMPORAIRE STOCKANT LE COMPTAGE DES FISS VUES EN MULTI-HEAV
C
      CALL DISMOI('F','NB_MA_MAILLA',NOMA,'MAILLAGE',NBMA,K8BID,IBID)
      CALL WKVECT('&&XCONTA.NBSP' ,'V V I',NBMA,JNBPT)
C
C --- TRANSFO CHAM_ELEM -> CHAM_ELEM_S
C
      CALL CELCES(NOMO//'.TOPOFAC.LO','V',FACLON)
      CALL CELCES(NOMO//'.TOPOFAC.AI','V',AINTER)
C
C --- CHOIX DE L'ESPACE DES LAGRANGES POUR LE CONTACT
C
      DO 220 IFISS = 1,NFISS
C
C --- FISSURE COURANTE
C
        FISCOU = ZK8(JFISS+IFISS-1)
C
C --- NOM DES SD POUR REL. LIN.
C
        NLISEQ = FISCOU(1:8)//'.LISEQ     '
        ZK24(JXNREL+IFISS-1) = NLISEQ

C
C --- ZONE DE CONTACT IZONE CORRESPONDANTE
C
        IZONE  = XXCONI(DEFICO,FISCOU,'MAIT')
C
C --- TYPE LIAISON POUR CONTACT
C
        ALGOLA = MMINFI(DEFICO,'XFEM_ALGO_LAGR',IZONE )
C
C --- CHOIX DE L'ESPACE DES LAGRANGES POUR LE CONTACT
C
        CALL XDEFCO(NOMA  ,NOMO,  FISCOU,ALGOLA,NDIM  ,NLISEQ)   
C    
        CALL XBARVI(NOMA  ,NOMO  ,FISCOU,FACLON,AINTER)
 220  CONTINUE 
C
C --- CONVERSION CHAM_ELEM_S -> CHAM_ELEM
C
      CALL CESCEL(AINTER,NOMO//'.MODELE','TOPOFA','PAINTER','OUI',IBID,
     &             'G',NOMO//'.TOPOFAC.AI','F',IBID)
C      
      CALL JEDETR('&&XCONTA.NBSP')
      CALL DETRSD('CHAM_ELEM_S',FACLON)
      CALL DETRSD('CHAM_ELEM_S',AINTER)
C
      CALL JEDEMA()
      END
