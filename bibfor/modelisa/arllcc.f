      SUBROUTINE ARLLCC(MOTCLE,IOCC  ,NOMARL,MODELE,MAIL  ,
     &                  DIME  ,TYPMAI,NOM1  ,NOM2  ,NORM  ,
     &                  BC    ,NOMC  ,NBMAC, GRMED ,GRCOL ,
     &                  MODEL ,CINE)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF MODELISA  DATE 06/07/2009   AUTEUR COURTOIS M.COURTOIS 
C ======================================================================
C COPYRIGHT (C) 1991 - 2007  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MEUNIER S.MEUNIER
C
      IMPLICIT NONE
      CHARACTER*16 MOTCLE
      CHARACTER*8  MAIL,MODELE,NOMARL
      INTEGER      IOCC
      REAL*8       BC(2,*)
      CHARACTER*10 NOM1,NOM2,NOMC
      INTEGER      GRMED,GRCOL
      INTEGER      DIME,NBMAC
      CHARACTER*8  MODEL(3),CINE(3)
      CHARACTER*16 TYPMAI
      CHARACTER*10 NORM

      INTEGER      NI,NR,NK
      PARAMETER   ( NI = 1 , NR = 1 , NK = 1 )
      INTEGER      VALI(NI)
      REAL*8       VALR(NR)
      CHARACTER*24 VALK(NK)

      CHARACTER*6  NOMPRO
      PARAMETER   (NOMPRO='ARLLCC')
C
C ----------------------------------------------------------------------
C
C ROUTINE ARLEQUIN
C
C LECTURE ET TRAITEMENT DES MAILLES DE LA ZONE DE COLLAGE
C
C ----------------------------------------------------------------------
C
C
C IN  MOTCLE : MOT-CLEF FACTEUR POUR ARLEQUIN
C IN  IOCC   : OCCURRENCE DU MOT CLEF-FACTEUR ARLEQUIN
C IN  NOMARL : NOM DE LA SD PRINCIPALE ARLEQUIN
C IN  MODELE : NOM UTILISATEUR DU MODELE
C IN  MAIL   : NOM UTILISATEUR DU MAILLAGE
C IN  DIME   : DIMENSION DE L'ESPACE GLOBAL
C                SI SOLIDE : DIMENSION DE L'ESPACE
C                SI COQUE  : DIMENSION DE L'ESPACE - 1
C IN  TYPMAI : SD CONTENANT NOM DES TYPES ELEMENTS (&&CATA.NOMTM)
C IN  NOM1   : NOM DE LA SD POUR LES MAILLES GROUPE 1
C IN  NOM2   : NOM DE LA SD POUR LES MAILLES GROUPE 2
C IN  NORM   : NOM DE LA SD POUR STOCKAGE DES NORMALES
C IN  BC     : BOITE ENGLOBANT LA ZONE DE RECOUVREMENT
C OUT NOMC   : NOM DE LA SD POUR LE COLLAGE
C OUT NBMAC  : NOMBRE DE MAILLES DE LA ZONE DE COLLAGE
C IN  GRMED  : GROUPE DE MAILLES SERVANT DE MEDIATEUR (1 OU 2)
C OUT GRCOL  : GROUPE DANS LEQUEL SE TROUVE LA ZONE DE COLLAGE (1 OU 2)
C OUT MODEL  : MODELISATION ASSOCIEE AUX MAILLES
C                '3D','AXIS', 'CPLAN' OU 'DPLAN'
C                ON CHANGE POUR ZONE DE COLLAGE (3)
C OUT CINE   :  CINEMATIQUE ASSOCIEE AUX MAILLES
C                'SOLIDE' OU 'COQUE'
C                ON CHANGE POUR ZONE DE COLLAGE (3)
C
C SD PRODUITE
C NOMC.MAILLE   : VECTEUR DE LOGICAL SUR TOUT LE MAILLAGE
C                   .TRUE. SI MAILLE EST UNE MAILLE POUR COLLER
C                   .FALSE. SINON
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER      NOCC,IBID
      INTEGER      IFM,NIV
      CHARACTER*8  K8BID
      CHARACTER*16 NOMBOT
      INTEGER      JGRMC
      CHARACTER*10 NOMT,NOMS
      CHARACTER*19 NGRMT,NGRM1,NGRM2,NGRMS
      INTEGER      JDIME,JCHM1,JCHM2,JCHMS,JCOLM
      INTEGER      NBMA1,NBMA2,NMA,NGRP
      LOGICAL      ARLCOL,LL
      INTEGER      IMA
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFNIV(IFM,NIV)
C
C --- MAILLES DE LA ZONE DE COLLAGE
C
      NOMT   = '&&ARLLCC.C'
      NOMS   = '&&ARLLCC.D'
      NGRM1  = NOM1(1:10)//'.GROUPEMA'
      NGRM2  = NOM2(1:10)//'.GROUPEMA'
      CALL JELIRA(NGRM1,'LONMAX',NBMA1,K8BID)
      CALL JELIRA(NGRM2,'LONMAX',NBMA2,K8BID)
      CALL GETVTX(MOTCLE,'GROUP_MA_COLL',IOCC,1,0,K8BID,NOCC)
C
C --- CREATIONS SD COLLAGE
C
      CALL JEVEUO(MAIL(1:8)//'.DIME','L',JDIME)
      NMA = ZI(JDIME+2)
      CALL WKVECT(NOMC(1:10)//'.MAILLE','V V L',NMA,JCOLM )

      IF (NOCC.NE.0) THEN
        NOCC = -NOCC
C
C --- LECTURE DES MAILLES DE LA ZONE DE COLLAGE
C
        CALL WKVECT('&&ARLLCC.GMAC','V V K8',NOCC,JGRMC)
        CALL GETVTX(MOTCLE,'GROUP_MA_COLL',IOCC,1,NOCC,
     &              ZK8(JGRMC),NGRP)
        CALL ARLVER(MODELE,ZK8(JGRMC),NGRP,NOMT,MODEL(3),
     &              CINE(3),IBID)
        CALL JEDETR('&&ARLLCC.GMAC')
C
C --- MISE EN BOITE DES MAILLES
C
        CALL ARLDBG(NOMPRO,NIV,IFM,1,NI,VALI,NR,VALR,NK,VALK)
        NOMBOT = NOMT(1:10)//'.BOITE'
        NGRMT  = NOMT(1:10)//'.GROUPEMA'
        CALL ARLBO0(MAIL  ,NOMARL,NGRMT ,NORM  ,DIME  ,
     &              TYPMAI,NOMBOT)
C
        IF (NIV.GE.2) THEN
          CALL ARLDBG(NOMPRO,NIV,IFM,2,NI,VALI,NR,VALR,NK,VALK)
          CALL ARLIMP(IFM,'BOITE',NOMT)
        ENDIF
C
C --- FILTRAGE DES MAILLES DE LA ZONE DE COLLAGE
C
        CALL ARLDBG(NOMPRO,NIV,IFM,3,NI,VALI,NR,VALR,NK,VALK)
        CALL ARLFLT(NOMT,BC,NOMS)
C
        IF (NIV.GE.2) THEN
          CALL ARLDBG(NOMPRO,NIV,IFM,4,NI,VALI,NR,VALR,NK,VALK)
          CALL ARLIMP(IFM,'BOITE',NOMS)
        ENDIF
C
C --- NOMBRE DE MAILLES DE LA ZONE DE COLLAGE
C
        NGRMS  = NOMS(1:10)//'.GROUPEMA'
        CALL JELIRA(NGRMS,'LONUTI',NBMAC,K8BID)
C
C --- VERIFICATION DES MAILLES DE LA ZONE DE COLLAGE
C
        CALL JEVEUO(NGRM1,'L',JCHM1 )
        CALL JEVEUO(NGRM2,'L',JCHM2 )
        CALL JEVEUO(NGRMS,'L',JCHMS )
        IF     (ARLCOL(ZI(JCHMS),NBMAC,ZI(JCHM1),NBMA1,ZL(JCOLM))) THEN
          GRCOL = 1
        ELSEIF (ARLCOL(ZI(JCHMS),NBMAC,ZI(JCHM2),NBMA2,ZL(JCOLM))) THEN
          GRCOL = 2
        ELSE
          CALL U2MESS('F','ARLEQUIN_10')
        ENDIF
C
C --- MENAGE
C
        CALL ARLDSD('BOITE',NOMS)
        CALL ARLDSD('GROUPEMA',NOMS)
        CALL ARLDSD('BOITE',NOMT)
        CALL ARLDSD('GROUPEMA',NOMT)
        LL = .FALSE.
      ELSE
C
C --- LA ZONE DE COLLAGE EST IDENTIQUE A LA ZONE DE SUPERPOSITION
C
        IF (NBMA1.GE.NBMA2) THEN
          GRCOL = 1
          NBMAC = NBMA1
        ELSE
          GRCOL = 2
          NBMAC = NBMA2
        ENDIF
C
        DO 60 IMA = 1, NBMAC
          ZL(JCOLM-1+IMA) = .TRUE.
 60     CONTINUE
        LL = .TRUE.
      ENDIF
C
C --- LA CINEMATIQUE DU GROUPE DE COLLAGE EST LA CINEMATIQUE DU GROUPE
C --- MEDIATEUR
C
      CINE(3) = CINE(GRMED)
C
      IF (NIV.GE.2) THEN
        VALI(1)=GRCOL
        IF (LL) THEN
          CALL ARLDBG(NOMPRO,NIV,IFM,5,NI,VALI,NR,VALR,NK,VALK)
        ELSE
          CALL ARLDBG(NOMPRO,NIV,IFM,6,NI,VALI,NR,VALR,NK,VALK)
        ENDIF
      ENDIF
C
      CALL JEDEMA()
      END
