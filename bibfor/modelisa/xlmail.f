      SUBROUTINE XLMAIL(FISS  ,NMAEN1,NMAEN2,NMAEN3,
     &                  JMAEN1,JMAEN2,JMAEN3,NFON,JFON,NBFOND,JBAS,
     &                  JFISDE,JFISAR,NDIM)
C
