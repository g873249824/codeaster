C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MEUNIER S.MEUNIER
C
      IMPLICIT NONE
      CHARACTER*16 MOTCLE
      INTEGER      IOCC
      CHARACTER*8  MODELE
      CHARACTER*10 NOMA,NOMB
      CHARACTER*8  MODEL(3),CINE(3)
      INTEGER      DIMVAR(2),DIME
C
C ----------------------------------------------------------------------
C
C ROUTINE ARLEQUIN
C
C LECTURE ET VERIFICATION DES MAILLES DES MODELES
C
C ----------------------------------------------------------------------
C
C
C IN  MOTCLE : MOT-CLEF FACTEUR POUR ARLEQUIN
C IN  IOCC   : OCCURRENCE DU MOT CLEF-FACTEUR ARLEQUIN
C IN  MODELE : NOM DU MODELE
C I/O NOMA   : NOM DE LA SD POUR STOCKAGE MAILLES GROUP_MA_1
C I/O NOMB   : NOM DE LA SD POUR STOCKAGE MAILLES GROUP_MA_2
C OUT MODEL  : MODELISATION ASSOCIEE AUX MAILLES
C              '3D','AXIS', 'CPLAN' OU 'DPLAN'
C              UN POUR CHAQUE GROUPE + ZONE DE COLLAGE
C OUT CINE   : CINEMATIQUE ASSOCIEE AUX MAILLES
C              'SOLIDE' OU 'COQUE'
C              UN POUR CHAQUE GROUPE + ZONE DE COLLAGE
C OUT DIMVAR : DIMENSION DE LA VARIETE POUR LES DEUX GROUPES
C                SI SOLIDE : DIMENSION DE L'ESPACE
C                SI COQUE  : DIMENSION DE L'ESPACE - 1
C OUT DIME   : DIMENSION DE L'ESPACE GLOBAL 2 OU 3
C
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER      N1,N2
      CHARACTER*8  K8BID
      INTEGER      JGRM1,JGRM2
      INTEGER      IFM,NIV

      INTEGER      NR,NK,NI
      PARAMETER   ( NR = 1 , NK = 1 , NI = 1)
      INTEGER      VALI(NI)
      REAL*8       VALR(NR)
      CHARACTER*24 VALK(NK)

      CHARACTER*6  NOMPRO
      PARAMETER   (NOMPRO='ARLLEC')
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFNIV(IFM,NIV)
C
C --- LECTURE MAILLE GROUPE_MA_1
C
      CALL GETVTX(MOTCLE,'GROUP_MA_1',IOCC,1,0,K8BID,N1)
      CALL WKVECT('&&'//NOMPRO//'.GMA1','V V K8', -N1, JGRM1 )
      CALL GETVTX(MOTCLE,'GROUP_MA_1',IOCC,1,-N1,ZK8(JGRM1),N1)
C
      CALL ARLDBG(NOMPRO,NIV,IFM,1,NI,VALI,NR,VALR,NK,VALK)
C
      CALL ARLVER(MODELE,ZK8(JGRM1),N1,NOMA,MODEL(1),
     &            CINE(1),DIMVAR(1))
C
C --- LECTURE MAILLE GROUPE_MA_2
C
      CALL GETVTX(MOTCLE,'GROUP_MA_2',IOCC,1,0,K8BID,N2)
      CALL WKVECT('&&'//NOMPRO//'.GMA2','V V K8', -N2, JGRM2 )
      CALL GETVTX(MOTCLE,'GROUP_MA_2',IOCC,1,-N2,ZK8(JGRM2),N2)
C
      CALL ARLDBG(NOMPRO,NIV,IFM,2,NI,VALI,NR,VALR,NK,VALK)
C
      CALL ARLVER(MODELE,ZK8(JGRM2),N2,NOMB,MODEL(2),
     &            CINE(2),DIMVAR(2))
C
C --- MODELISATION
C
      IF (MODEL(1).NE.MODEL(2)) THEN
        CALL U2MESS('F','ARLEQUIN_1')
      ENDIF
C
C --- DIMENSION DE L'ESPACE GLOBAL
C
      IF (MODEL(1)(1:2).EQ.'3D') THEN
        DIME = 3
      ELSE
        DIME = 2
      ENDIF
      VALI(1)=DIME
      CALL ARLDBG(NOMPRO,NIV,IFM,3,NI,VALI,NR,VALR,NK,VALK)
C
      CALL JEDETR('&&'//NOMPRO//'.GMA1')
      CALL JEDETR('&&'//NOMPRO//'.GMA2')
C
      CALL JEDEMA()
      END
