C  BUT : CHAPEAU A LA ROUTINE RCVALA POUR AJOUTER A LA LISTE DES
C        PARAMETRES DES FONCTIONS LES VARIABLES DE COMMANDE
C ----------------------------------------------------------------------
C ----------------------------------------------------------------------
      INTEGER NBCVRC,JVCNOM
      COMMON /CAII14/NBCVRC,JVCNOM

      INTEGER NBPAMX,NBPAR2,IPAR,NBPART,IER
      PARAMETER (NBPAMX=10)
      REAL*8             VALPA2(NBPAMX),VALVRC
      CHARACTER*(8)      NOMPA2(NBPAMX),NOVRC
C DEB ------------------------------------------------------------------

C     -- S'IL N'Y A PAS DE VARC, IL N'Y A QU'A APPELER  RCVALA :
      IF (NBCVRC.EQ.0) THEN
        CALL RCVALA(JMAT,NOMAT,PHENOM,NBPAR,NOMPAR,VALPAR,
     &                 NBRES, NOMRES, VALRES, CODRET, IARRET)
        GOTO 9999
      ENDIF


C     -- SINON, ON AJOUTE LES VARC AU DEBUT DE LA LISTE DES PARAMETRES
C     CAR FOINTA DONNE PRIORITE AUX DERNIERS :
      NBPAR2 = 0
      DO 1, IPAR=1,NBCVRC
         NOVRC=ZK8(JVCNOM-1+IPAR)
         CALL RCVARC(' ',NOVRC,POUM,FAMI,KPG,KSP,VALVRC,IER)
         IF (IER.EQ.0) THEN
           NBPAR2=NBPAR2+1
           NOMPA2(NBPAR2)=NOVRC
           VALPA2(NBPAR2)=VALVRC
         ENDIF
1     CONTINUE
C
      DO 2, IPAR=1,NBPAR
         NOMPA2(NBPAR2+IPAR) = NOMPAR(IPAR)
         VALPA2(NBPAR2+IPAR) = VALPAR(IPAR)
2     CONTINUE

      NBPART=NBPAR+NBPAR2
C     CALL ASSERT(NBPART.LE.NBPAMX)
      CALL RCVALA(JMAT,NOMAT,PHENOM,NBPART,NOMPA2,VALPA2,
     &                 NBRES, NOMRES, VALRES, CODRET, IARRET)

9999  CONTINUE
      END
