     &              NOMPAR,VALPAR,NBRES,NOMRES,VALRES,CODRET,IARRET)
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER            JMAT, NBPAR, NBRES,KPG,KSP,IARRET
      REAL*8             VALPAR(NBPAR), VALRES(NBRES)
      INTEGER        CODRET(NBRES)
      CHARACTER*(*)      NOMAT,PHENOM,NOMPAR(NBPAR),NOMRES(NBRES)
      CHARACTER*(*)      POUM,FAMI
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF MODELISA  DATE 26/02/2013   AUTEUR DESROCHE X.DESROCHES 
