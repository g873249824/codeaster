C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
C BUT : CALCULER LES DIMENSIONS DU LIGREL DE CHARGE POUR ECHANGE_PAROI
C
C ARGUMENTS D'ENTREE:
C      NOMA   : NOM DU MAILLAGE
C ARGUMENTS DE SORTIE:
C      NBTOUT : (1) =NBRE D'APPEL A NOCART = NBGREL
C               (2) =NBRE GRELS OU DE TYPE_MAIL
C               (3) =NBRE TOTAL DE MAILLES
C               (6) =SOMME SUR TOUTES LES MAILLES DE LEUR NBRE DE NOEUDS
C      LONLIG : DIMENSION DE L'OBJET .LIEL
C      LONEMA : DIMENSION DE L'OBJET .NEMA
C      NBGREL : NOMBRE DE GREL DANS .LIEL
C
C ROUTINES APPELEES:
C JECREO    JEECRA    JEVEUO    GETVID    GETVR8    JEXNOM    *
C JELIRA    JEDETR                                            *
C                                                             *
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNUM,JEXNOM,JEXR8,JEXATR
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      INTEGER NECHPA
      CHARACTER*24 LISTE
C --- DEBUT
      CALL JEMARQ()
      DO 1 I=1,10
        NBTOUT(I) = 0
1     CONTINUE
      LONLIG=0
      LONEMA=0
      NBGREL=0
      LISTE ='&&LIGECHP'
C
C     LECTURE DE LA IEME OCCURENCE DE :
C     ECHANGE_PAROI : ( GROUP_MA_1 : ...   , MAILLE_1 : ... )
C
      CALL GETFAC('ECHANGE_PAROI',NECHPA)
      DO 2 IOCC = 1,NECHPA
C
C     CREATION DE L'OBJET &&LIGECHP SUR 'V' QUI CONTIENT LA LISTE DES
C     MAILLES DESCRITES PAR LES MOTS CLES GROUP_MA_1 ET MAILLE_1 ET
C     LE TYPE_MAIL ASSOCIEES A CHACUNE(LES MAILLES SONT REGROUPEES PAR
C     TYPE_MAIL)
C
        CALL PALIMA(NOMA,'ECHANGE_PAROI','GROUP_MA_1','MAILLE_1',
     &              IOCC,LISTE)
        CALL JEEXIN(LISTE,IRET)
        IF (IRET.EQ.0) CALL U2MESS('F','MODELISA4_87')
        CALL JEVEUO(LISTE,'L',ILISTE)
        NBMA = ZI(ILISTE)
        NBTOUT(3) =NBTOUT(3) +NBMA
        ITYPC = -1
        ITYPE = 0
        DO 3 IMA = 1,NBMA
          IIMA = 2*(IMA-1)+1
          ITYPE = ZI(ILISTE+IIMA+1)
          IF (ITYPE.NE.ITYPC) THEN
            NBGREL = NBGREL+1
            ITYPC = ITYPE
C           NBNOMA = ""NB DE NOEUDS DE LA MAILLE"""""
            CALL JEVEUO(JEXNUM('&CATA.TM.NBNO',ITYPE),'L',INBNO)
            NBNOMA = ZI(INBNO)
          ENDIF
          LONEMA = LONEMA + 2*NBNOMA + 1
          NBTOUT(6) = NBTOUT(6) + NBNOMA
3       CONTINUE
        LONLIG = LONLIG + NBMA + 1
2     CONTINUE
      CALL JEDETC(' ',LISTE,1)
      NBTOUT(1) = NBGREL
      NBTOUT(2) = NBGREL
      CALL JEDEMA()
      END
