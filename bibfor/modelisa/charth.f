C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================

C      OPERATEURS :     AFFE_CHAR_THER ET AFFE_CHAR_THER_F

C      MOTS-CLES ACTUELLEMENT TRAITES:
C        SOURCE
C        SOUR_NL
C        ECHANGE
C        TEMP_IMPO
C        FLUX_REP
C        ECHANGE_PAROI
C        PRE_GRAD_TEMP
C        LIAISON_DDL
C        LIAISON_GROUP
C        RAYONNEMENT
C---------------------------------------------------------------------
      INTEGER IBID,IGREL,INEMA,IRET,NDIM,JLGRF
      CHARACTER*8 CHAR,NOMA
      CHARACTER*16 TYPE,OPER,MOTFAC
      CHARACTER*19 LIGRCH,LIGRMO
C     ------------------------------------------------------------------


      CALL GETRES(CHAR,TYPE,OPER)

C --- NOMS DE LIGREL, MAILLAGE , DIMENSION DU PB

      CALL CAGENE(CHAR,OPER,LIGRMO,NOMA,NDIM)

C --- ALLOCATION DU LIGREL DE CHARGE (MOTS-CLE: TEMP_IMPO,
C                          ECHANGE_PAROI, LIAISON_DDL, LIAISON_GROUP )

      CALL ALLIGR(CHAR,OPER,NOMA,FONREE,LIGRCH)

C --- TEMP_IMPO ---

      IGREL = 0
      INEMA = 0

C --- SOURCE ---

      CALL CBSOUR(CHAR,NOMA,LIGRMO,NDIM,FONREE)

C --- SOUR_NL---

      CALL CBSONL(CHAR,NOMA,LIGRMO,NDIM,FONREE)

C --- CONVECTION ---

      CALL CBCONV(CHAR)

C --- FLUX_REP ---

      CALL CBFLUX(CHAR,NOMA,LIGRMO,NDIM,FONREE)

C --- FLUX_NL

      CALL CBFLNL(CHAR,NOMA,LIGRMO,FONREE)

C --- RAYONNEMENT ---

      CALL CBRAYO(CHAR,NOMA,LIGRMO,FONREE)

C --- ECHANGE ---

      CALL CBECHA(CHAR,NOMA,LIGRMO,NDIM,FONREE)

C --- ECHANGE_PAROI ---

      CALL CAECHP(CHAR,LIGRCH,LIGRMO,IGREL,INEMA,NOMA,FONREE,NDIM)

C --- GRADIENT INITIAL ---

      CALL CBGRAI(CHAR,NOMA,LIGRMO,FONREE)

C --- TEMP_IMPO ---

      MOTFAC = 'TEMP_IMPO       '
      CALL CADDLI(OPER,MOTFAC,FONREE,CHAR)

C --- LIAISON_DDL ---

      CALL CALIAI(FONREE,CHAR)

C --- LIAISON_GROUP ---

      CALL CALIAG(FONREE,CHAR)

C --- LIAISON_UNIF ---

      CALL CAGROU(FONREE,CHAR)

C --- LIAISON_CHAMNO ---

      IF (FONREE.EQ.'REEL') THEN
        CALL CALICH(CHAR)
      END IF


C --- LIAISON_MAIL ---

      IF (FONREE.EQ.'REEL') THEN
        CALL CALIRC(CHAR)
      END IF


C --- MISE A JOUR DU LIGREL DE CHARGE :
      CALL JEEXIN (LIGRCH//'.LGRF', IRET )
      IF (IRET.NE.0) THEN
        CALL ADALIG(LIGRCH)
        CALL CORMGI('G', LIGRCH)
        CALL JEECRA(LIGRCH//'.LGRF','DOCU',IBID,'THER')
        CALL INITEL(LIGRCH)
C       -- LIEN ENTRE LE LIGREL DE CHARGE ET LE MODELE :
        CALL JEVEUO(LIGRCH//'.LGRF','E',JLGRF)
        ZK8(JLGRF-1+2)=LIGRMO(1:8)
      ENDIF

      END
