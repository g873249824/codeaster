      SUBROUTINE PALIMA(NOMAZ,MCFACT,MCGRMA,MCMA,IOCC,NOML)
      IMPLICIT NONE
      CHARACTER*(*)     NOMAZ,MCFACT,MCGRMA,MCMA     ,NOML
      INTEGER                                   IOCC
C-----------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF MODELISA  DATE 14/10/2008   AUTEUR PELLET J.PELLET 
C ======================================================================
C COPYRIGHT (C) 1991 - 2001  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
C BUT : LECTURE DE LA LISTE DES MAILLES DESCRITES PAR LA SEQUENCE :
C       MCFAC : ( MCGRMA : LISTE DE GROUP_MA ,
C                 MCMA   : LISTE DE MAILLE   , ....)
C       CREATION D'UN OBJET DE NOM : NOML  OJB V V I DIM=NBMA*2+1
C       NBMA : NOMBRE DE MAILLES LUES
C       NOML(1) = NBMA  NOML(1+2*(I-1)+1)=IMA NUM.DE LA MAILLE DANS NOMA
C                       NOML(1+2*(I-1)+2)=ITYP NUM.DU TYPE_MAIL DE IMA
C       LES MAILLES SONT TRIEES EN PAQUETS DE MEME TYPEMAIL
C
C IN   NOMAZ   : NOM DU MAILLAGE
C IN   MCFACT K* : MOT CLE FACTEUR
C IN   MCGRMA K* : MOT CLE CONSERNANT LA LISTE DE GROUP_MA
C IN   MCMA   K* : MOT CLE CONSERNANT LA LISTE DE MAILLE
C IN   IOCC   I     : NUMERO DE L'OCCURENCE DU MOT CLE FACTEUR
C OUT  NOML   K*24  : NOM DE L'OBJET JEVEUX CREE SUR LA VOLATILE
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      CHARACTER*8  NOMA
      CHARACTER*16 MCF,TYMOCL(2),LIMOCL(2)
      CHARACTER*24 LISTE1
      INTEGER JTYMA,K,N1,J1,J2,IMA

      CALL JEMARQ()
      NOMA = NOMAZ
      CALL JEVEUO(NOMA//'.TYPMAIL','L',JTYMA)
      MCF = MCFACT
      TYMOCL(1)='GROUP_MA'
      LIMOCL(1)= MCGRMA
      TYMOCL(2)='MAILLE'
      LIMOCL(2)= MCMA

      LISTE1='&&PALIMA.LISTE'

      CALL RELIEM(' ',NOMA,'NU_MAILLE',MCF,IOCC,2,LIMOCL,TYMOCL,
     &                  LISTE1,N1)
      CALL JEDETR(NOML)
      CALL WKVECT(NOML,'V V I',2*N1+1,J2)
      ZI(J2)=N1
      IF (N1.GT.0) THEN
        CALL JEVEUO(LISTE1,'L',J1)
        DO 1, K=1,N1
          IMA=ZI(J1-1+K)
          ZI(J2+2*(K-1)+1)=IMA
          ZI(J2+2*(K-1)+2)=ZI(JTYMA-1+IMA)
 1      CONTINUE
      ENDIF


      CALL JEDETR(LISTE1)
      CALL JEDEMA()
      END
