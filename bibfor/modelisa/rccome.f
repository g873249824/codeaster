      NCOMP = NOMMAT//'.MATERIAU.NOMRC         '
      CALL JELIRA(NCOMP,'LONUTI',NBCOMP,K8BID)
      CALL JEVEUO(NCOMP,'L',ICOMP)
      DO 10 I=1,NBCOMP
        IF ( PHENO .EQ. ZK16(ICOMP+I-1)(1:LEN(PHENO)) ) THEN
          PHENOM=ZK16(ICOMP+I-1)
          GOTO 999
        ENDIF
 10   CONTINUE
      ICODRE = 1
      CALL U2MESS('A','ELEMENTS2_63')
      GOTO 999
