     &                  MAIL  ,NOMO  ,NOMARL,TYPMAI,QUADRA,
     &                  NOMC  ,NOM1  ,NOM2  ,CINE1 ,CINE2 ,
     &                  NORM  ,TANG  ,LCARA ,NDIM)
