C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MEUNIER S.MEUNIER
C
      IMPLICIT NONE
      CHARACTER*16 TYPMAI
      CHARACTER*8  MAIL,NOMO
      CHARACTER*8  NOMARL
      CHARACTER*10 NOM1,NOM2,NOMC
      CHARACTER*10 NORM,TANG
      CHARACTER*8  CINE1,CINE2
      REAL*8       LCARA
      CHARACTER*10 QUADRA
      INTEGER      NDIM,IOCC
C
C ----------------------------------------------------------------------
C
C ROUTINE ARLEQUIN
C
C CALCUL DES MATRICES DE COUPLAGE ELEMENTAIRE ARLEQUIN
C ASSEMBLAGE DANS LES MATRICES ARLEQUIN MORSES
C
C ----------------------------------------------------------------------
C
C
C IN  IOCC   : NUMERO D'OCCURENCE DU MOT-CLEF ARLEQUIN
C IN  MAIL   : NOM DU MAILLAGE
C IN  NOMO   : NOM DU MODELE
C IN  NOMARL : NOM DE LA SD PRINCIPALE ARLEQUIN
C IN  TYPMAI : SD CONTENANT NOM DES TYPES ELEMENTS (&&CATA.NOMTM)
C IN  QUADRA : SD DES QUADRATURES A CALCULER
C IN  NOMC   : NOM DE LA SD POUR LES MAILLES DE COLLAGE
C IN  NOM1   : NOM DE LA SD DE STOCKAGE PREMIER GROUPE
C IN  NOM2   : NOM DE LA SD DE STOCKAGE SECOND GROUPE
C IN  CINE1  : CINEMATIQUE DU PREMIER GROUPE
C IN  CINE2  : CINEMATIQUE DU SECOND GROUPE
C IN  NORM   : NOM DE LA SD POUR STOCKAGE DES NORMALES
C IN  TANG   : NOM DE L'OBJET TANGENTES LISSEES
C IN  LCARA  : LONGUEUR CARACTERISTIQUE POUR TERME DE COUPLAGE (PONDERA
C              TION DES TERMES DE COUPLAGE)
C IN  NDIM   : DIMENSION DE L'ESPACE GLOBAL (2 OU 3)
C
C
C SD DE SORTIE
C NOM*.MORSE.VALE : VECTEUR DE VALEUR DE LA MATRICE ARLEQUIN MORSE
C
C ----------------------------------------------------------------------
C
      CHARACTER*8  MARLEL,MODARL,MAILAR
      CHARACTER*24 TABCPL,TABCOR
      INTEGER      METHOD,ULPMAI,IBID,N1
C
C ----------------------------------------------------------------------
C
      MARLEL = '&&MARLEL'
C
C --- PARAMETRES POUR DEVELOPPEUR
C --- ULPMAI : UNITE LOGIQUE D'IMPRESSION DU PSEUDO MAILLAGE AU FORMAT
C              GMSH
C --- METHOD : MANIERE DE CALCULER LES TERMES DE COUPLAGE
C               0: STANDARD
C               1: PAR CALCUL+TE (UNIQUEMENT 2D/2D EN SOLIDE)
C
      CALL GETVIS('ARLEQUIN','UNITE_GMSH',IOCC,1,0,IBID,N1)
      IF (N1.EQ.0) THEN
        ULPMAI = -1
      ELSE
        CALL GETVIS('ARLEQUIN','UNITE_GMSH',IOCC,1,1,ULPMAI,N1)
      ENDIF
C
      IF ((CINE1(1:6).EQ.'SOLIDE').AND.(CINE2(1:6).EQ.'SOLIDE')) THEN
        METHOD = 1
      ELSEIF ((CINE1(1:5).EQ.'COQUE').OR.(CINE2(1:5).EQ.'COQUE')) THEN
        METHOD = 0
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
      IF (METHOD.EQ.1) THEN
        CALL ARLCP2(MAIL  ,ULPMAI,NOMO  ,NOMARL,TYPMAI,QUADRA,
     &              NOM1  ,NOM2  ,LCARA ,NDIM  ,
     &              MARLEL,MODARL,TABCPL)
        CALL ARLAST(MAIL  ,TYPMAI,QUADRA,NOMC  ,
     &              NOM1  ,NOM2  ,CINE1 ,CINE2 ,NORM  ,
     &              TANG  ,NDIM  ,
     &              MARLEL,MODARL,TABCPL)
        CALL JEDETR(TABCPL)
      ELSEIF (METHOD.EQ.0) THEN
        CALL ARLCP1(MAIL  ,NOMARL,TYPMAI,QUADRA,NOMC  ,
     &              NOM1  ,NOM2  ,CINE1 ,CINE2 ,NORM  ,
     &              TANG  ,LCARA ,NDIM)
C
        IF (ULPMAI.GT.0) THEN
           CALL ARLMAI(MAIL  ,ULPMAI,MAILAR,NOMARL,QUADRA,NDIM,
     &                 TYPMAI,NOM1  ,NOM2  ,TABCOR,TABCPL)
           CALL JEDETR(TABCOR)
           CALL JEDETR(TABCPL)
        ENDIF
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
      END
