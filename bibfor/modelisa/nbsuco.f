C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*8  CHAR
      CHARACTER*16 MOTFAC
      CHARACTER*8  NOMA
      INTEGER      IREAD     
      INTEGER      JSUMA
      INTEGER      JSUNO
      INTEGER      JNOQUA    
      INTEGER      NTRAV   
      INTEGER      ORDSTC
      INTEGER      INDQUA      
      INTEGER      IWRITE
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES - LECTURE DONNEES)
C
C DETERMINATION DU NOMBRE DE MAILLES ET DE NOEUDS DE CONTACT
C REMPLISSAGE DES POINTEURS ASSOCIES JSUMA,JSUNO,JNOQUA
C      
C ----------------------------------------------------------------------
C
C 
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  MOTFAC : MOT-CLE FACTEUR (VALANT 'CONTACT')
C IN  NOMA   : NOM DU MAILLAGE
C IN  NZOCO  : NOMBRE DE ZONES DE CONTACT
C IN  NTRAV  : NOMBRE MAXIMUM DE GROUPES ET DE MAILLES IMPLIQUES DANS 
C              LE CONTACT (TOUTES ZONES CONFONDUES)
C IN  ORDSTC : ORDRE DE STOCKAGE DES ZONES MAITRES ET ESCLAVES
C              0: MAITRES PUIS ESCLAVES
C              1: ESCLAVES PUIS MAITRES
C IN  INDQUA : VAUT 1 LORSQUE L'ON DOIT IGNORER LES NOEUDS MILIEUX DANS
C              NBNOEL (PENALISATION SUR LE CONTACT OU METHODE CONTINUE)
C IN  NSUCO  : NOMBRE TOTAL DE SURFACES DE CONTACT
C IN  JSYME  : POINTEUR DU VECTEUR DES SURFACES SYMETRIQUES
C IN  NBSYME : NOMBRE DE SURFACES SYMETRIQUES
C OUT NMACO  : NOMBRE TOTAL DE MAILLES DES SURFACES
C OUT NNOCO  : NOMBRE TOTAL DE NOEUDS DES SURFACES
C OUT NNOQUA : NOMBRE TOTAL DE NOEUDS QUADRATIQUES DES SURFACES
C OUT PSURMA : POINTEUR DES MAILLES DES SURFACES
C OUT PSURNO : POINTEUR DES NOEUDS DES SURFACES
C OUT PSURNO : POINTEUR DES NOEUDS QUADRATIQUES DES SURFACES
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      JTRAV
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      CALL WKVECT ('&&NBSUCO.TRAV','V V K8',NTRAV,JTRAV)

      IF (ORDSTC.EQ.1) THEN
         CALL NBNOCO(CHAR,MOTFAC,NOMA,'GROUP_MA_ESCL',IREAD,INDQUA,
     +            JTRAV,IWRITE,JSUMA,JSUNO,JNOQUA)
         CALL NBNOCO(CHAR,MOTFAC,NOMA,'MAILLE_ESCL',IREAD,INDQUA,
     +            JTRAV,IWRITE,JSUMA,JSUNO,JNOQUA)
         CALL NBNOCO(CHAR,MOTFAC,NOMA,'GROUP_MA_MAIT',IREAD,INDQUA,
     +            JTRAV,IWRITE,JSUMA,JSUNO,JNOQUA)
         CALL NBNOCO(CHAR,MOTFAC,NOMA,'MAILLE_MAIT',IREAD,INDQUA,
     +            JTRAV,IWRITE,JSUMA,JSUNO,JNOQUA)
      ELSE
         CALL NBNOCO(CHAR,MOTFAC,NOMA,'GROUP_MA_MAIT',IREAD,INDQUA,
     +            JTRAV,IWRITE,JSUMA,JSUNO,JNOQUA)
         CALL NBNOCO(CHAR,MOTFAC,NOMA,'MAILLE_MAIT',IREAD,INDQUA,
     +            JTRAV,IWRITE,JSUMA,JSUNO,JNOQUA)
         CALL NBNOCO(CHAR,MOTFAC,NOMA,'GROUP_MA_ESCL',IREAD,INDQUA,
     +            JTRAV,IWRITE,JSUMA,JSUNO,JNOQUA)
         CALL NBNOCO(CHAR,MOTFAC,NOMA,'MAILLE_ESCL',IREAD,INDQUA,
     +            JTRAV,IWRITE,JSUMA,JSUNO,JNOQUA)
      ENDIF
C
      CALL JEDETR ('&&NBSUCO.TRAV')
C
      CALL JEDEMA()
      END
