      SUBROUTINE NBSUCO(CHAR  ,MOTFAC,NOMA  ,IREAD ,IWRITE,
     &                  JSUMA ,JSUNO ,JNOQUA,NTRAV ,ORDSTC,
     &                  INDQUA)
