      SUBROUTINE CBPRES ( CHAR, NOMA, LIGRMO, NDIM, FONREE )
      IMPLICIT   NONE
      INTEGER           NDIM
      CHARACTER*4       FONREE
      CHARACTER*8       CHAR, NOMA
      CHARACTER*(*)     LIGRMO
C-----------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF MODELISA  DATE 26/11/2002   AUTEUR JMBHH01 J.M.PROIX 
C ======================================================================
C COPYRIGHT (C) 1991 - 2001  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR   
C (AT YOUR OPTION) ANY LATER VERSION.                                 
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT 
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF          
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU    
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                            
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C-----------------------------------------------------------------------
      INTEGER       NBCA1,NBET1,NBCA2,NBET2,NBCA3,NBET3,NBCA4,NBET4,
     +              IALLOC
      CHARACTER*8   TYPMCL(2)
      CHARACTER*16  MOTCLF, MOTCLS(2)
C-----------------------------------------------------------------------
C
      MOTCLS(1) = 'GROUP_MA'
      MOTCLS(2) = 'MAILLE'
      TYPMCL(1) = 'GROUP_MA'
      TYPMCL(2) = 'MAILLE'      
C
      MOTCLF = 'PRES_REP'
      CALL GETFAC ( MOTCLF, NBCA1 )
      IF (NBCA1.NE.0) THEN
         CALL ALCAR0 ( NOMA, MOTCLF, 2, MOTCLS, TYPMCL, NBET1 )
      ENDIF
C
      MOTCLF = 'FLUX_THM_REP'
      CALL GETFAC ( MOTCLF, NBCA2 )
      IF (NBCA2.NE.0) THEN
         CALL ALCAR0 ( NOMA, MOTCLF, 2, MOTCLS, TYPMCL, NBET2 )
      ENDIF
C
      MOTCLF = 'FORCE_TUYAU'
      CALL GETFAC ( MOTCLF, NBCA3 )
      IF (NBCA3.NE.0) THEN
         CALL ALCAR0 ( NOMA, MOTCLF, 2, MOTCLS, TYPMCL, NBET3 )
      ENDIF
C
      MOTCLF = 'EFFE_FOND'
      CALL GETFAC ( MOTCLF, NBCA4 )
      IF (NBCA4.NE.0) THEN
         CALL ALCAR0 ( NOMA, MOTCLF, 2, MOTCLS, TYPMCL, NBET4 )
      ENDIF
C
C     MOT CLE PRES_REP :
C     ---------------------
      IF (NBCA1.NE.0) THEN
         IF ((NBCA3.EQ.0).AND.(NBCA4.EQ.0)) THEN
            CALL CAPRES (CHAR,LIGRMO,NBCA1,NBET1,NOMA,NDIM,FONREE)
            CALL TECART (CHAR//'.CHME.PRESS')
         ENDIF
      ENDIF
C
C     MOT CLE EFFE_FOND :
C     ---------------------
      IF (NBCA4.NE.0) THEN
         IALLOC=0
         IF (NBCA1.NE.0) THEN
C
C     EFFET DE FOND ET PRES_REP
C     ON ALLOUE UNE SEULE CARTE CHAR//'.CHME.PRESS'
C
            NBCA1=NBCA1+NBCA4
            NBET1=NBET1+NBET4
            CALL CAPRES (CHAR,LIGRMO,NBCA1,NBET1,NOMA,NDIM,FONREE)
            IALLOC=1
         ENDIF
         CALL CAFOND (CHAR,LIGRMO,IALLOC,NBCA4,NBET4,NOMA,NDIM,FONREE)
         CALL TECART (CHAR//'.CHME.PRESS')
      ENDIF
C
C     MOT CLE FORCE_TUYAU :
C     ---------------------
      IF (NBCA3.NE.0) THEN
         IALLOC=0
         IF (NBCA1.NE.0) THEN
C
C     PRESSION DANS LES TUYAUX ET PRES_REP SUR D'AUTRES ELEMENTS
C     ON ALLOUE UNE SEULE CARTE CHAR//'.CHME.PRESS'
C
            NBCA1=NBCA1+NBCA3
            NBET1=NBET1+NBET3
            CALL CAPRES (CHAR,LIGRMO,NBCA1,NBET1,NOMA,NDIM,FONREE)
            IALLOC=1
         ENDIF
         CALL CAFOTU (CHAR,LIGRMO,IALLOC,NBCA3,NBET3,NOMA,NDIM,FONREE)
         CALL TECART (CHAR//'.CHME.PRESS')
      ENDIF
C
C     MOT CLE FLUX_THM_REP :
C     ----------------------
      IF (NBCA2.NE.0) THEN
         CALL CAFTHM (CHAR,LIGRMO,NBCA2,NBET2,NOMA,NDIM,FONREE)
         CALL TECART (CHAR//'.CHME.FLUX')
      ENDIF
C
C-----------------------------------------------------------------------
C
9999  CONTINUE
      END
