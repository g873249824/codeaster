C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C-----------------------------------------------------------------------
      INTEGER       NBCA1,NBCA2,NBCA3,NBCA4,IALLOC
      CHARACTER*16  MOTCLF
C-----------------------------------------------------------------------
C
C
      MOTCLF = 'PRES_REP'
      CALL GETFAC ( MOTCLF, NBCA1 )
C
      MOTCLF = 'FLUX_THM_REP'
      CALL GETFAC ( MOTCLF, NBCA2 )

      MOTCLF = 'FORCE_TUYAU'
      CALL GETFAC ( MOTCLF, NBCA3 )
C
      MOTCLF = 'EFFE_FOND'
      CALL GETFAC ( MOTCLF, NBCA4 )
C
C     MOT CLE PRES_REP :
C     ---------------------
      IF (NBCA1.NE.0) THEN
         IF ((NBCA3.EQ.0).AND.(NBCA4.EQ.0)) THEN
            CALL CAPRES (CHAR,LIGRMO,NOMA,NDIM,FONREE)
            CALL TECART (CHAR//'.CHME.PRESS')
         ENDIF
      ENDIF
C
C     MOT CLE EFFE_FOND :
C     ---------------------
      IF (NBCA4.NE.0) THEN
         IALLOC=0
         IF (NBCA1.NE.0) THEN
C
C     EFFET DE FOND ET PRES_REP
C     ON ALLOUE UNE SEULE CARTE CHAR//'.CHME.PRESS'
C
            CALL CAPRES (CHAR,LIGRMO,NOMA,NDIM,FONREE)
            IALLOC=1
         ENDIF
         CALL CAFOND (CHAR,LIGRMO,IALLOC,NOMA,FONREE)
         CALL TECART (CHAR//'.CHME.PRESS')
      ENDIF
C
C     MOT CLE FORCE_TUYAU :
C     ---------------------
      IF (NBCA3.NE.0) THEN
         IALLOC=0
         IF (NBCA1.NE.0) THEN
C
C     PRESSION DANS LES TUYAUX ET PRES_REP SUR D'AUTRES ELEMENTS
C     ON ALLOUE UNE SEULE CARTE CHAR//'.CHME.PRESS'
C
            CALL CAPRES (CHAR,LIGRMO,NOMA,NDIM,FONREE)
            IALLOC=1
         ENDIF
         CALL CAFOTU (CHAR,LIGRMO,IALLOC,NOMA,FONREE)
         CALL TECART (CHAR//'.CHME.PRESS')
      ENDIF
C
C     MOT CLE FLUX_THM_REP :
C     ----------------------
      IF (NBCA2.NE.0) THEN
         CALL CAFTHM (CHAR,LIGRMO,NOMA,FONREE)
         CALL TECART (CHAR//'.CHME.FLUX')
      ENDIF
C
C-----------------------------------------------------------------------
C
9999  CONTINUE
      END
