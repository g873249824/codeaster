      IF ( IARRET .GE. 1 ) THEN
         IER = 0
         DO 200 IRES = 1, NBRES
           IF ( ICODRE(IRES) .EQ. 1 ) THEN
              IER = IER + 1
              PARA = NOMRES(IRES)
              VALK = PARA
              CALL U2MESG('E+','MODELISA9_77',1,VALK,0,0,0,0.D0)
              IF ( IARRET .EQ. 1 ) THEN
