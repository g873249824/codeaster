                 NOMAIL = ZK24(IAZK24-1+3)(1:8)
                 VALK = NOMAIL
                 CALL U2MESG('E+','MODELISA9_78',1,VALK,0,0,0,0.D0)
              ENDIF
              CALL U2MESS('E','VIDE_1')
           ENDIF
 200     CONTINUE
