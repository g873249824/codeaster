      SUBROUTINE POINCO(CHAR  ,MOTFAC,NOMA  ,NZOCO ,ORDSTC,
     &                  INDQUA,NSUCO ,NMACO ,NNOCO ,NNOQUA,
     &                  NTRAV)
C     
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF MODELISA  DATE 07/05/2007   AUTEUR PROIX J-M.PROIX 
C ======================================================================
