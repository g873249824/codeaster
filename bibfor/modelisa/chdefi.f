C OBJET : CALCUL DU TABLEAU INTERMEDIAIRE NOCMP
C        NOCMP( LNO , ICMP , 1 ) = NIMP
C         OU NIMP  TEL QUE .DEFI( NIMP , 1 ) = INO
C                          .DEFI( NIMP , 2 ) = NUCMP( ICMP )
C        NOCMP( INO , ICMP , 2 ) = NUCMP( ICMP )
C        LE TABLEAU NOCMP EST CREE ET A POUR DIMMENSION 2*NNDL*NINO
C-----------------------------------------------------------------------
C IN   INO     I       : NUMERO DU NOUED (DANS LE MAILLAGE)
C IN   LNO     I       : NUMERO DU NOUED DANS LA LISTE NUNO DES NOEUDS
C                        A TRAITER DANS CHCSUR
C IN   NCMP    I       : NOMBRE DE CMPS IMPOSEES SUR CE NOEUD
C IN   NUCMP   I(*)    : TABLEAU DES NUMEROS DES CMPS IMPOSEES SUR CE
C                        NOEUD (NUMERO DANS LA LISTES DES CMPS DU NOEUD)
C IN   NDEFI   I       : NOMBRE DE CMPS DEJA IMPOSEES DANS DEFI
C IN   DEFI    I(*)    : TABEAU .DEFI AVANT LE TRAITEMENT DE LA NOUVELLE
C                        LISTE DE NOEUDS ET DE DDLS
C VAR  NOCMP   I(*)    : TABLEAU NOCMP
C VAR  NIMPO   I       : EN IN : MAX(NDEFI,MAX(NIMP DEJA ATTRIBUES DANS
C                                    NOCMP) )
C                      : EN OUT: MAX(NDEFI,NIMPO EN IN,LE NIMP CALCULE)
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXR8,JEXATR
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C --- DEBUT -----------------------------------------------------------
C
      DO 1 IMP = 1,NDEFI
        K = 3*(IMP-1)+1
        KNO = DEFI(K)
        KCMP = DEFI(K+1)
        IF ( KNO.EQ.INO) THEN
          DO 10 IDDL = 1,NCMP
            ICMP = NUCMP(IDDL)
            IF (ICMP .EQ. KCMP) THEN
              INOCMP = 2*(NCMP*(LNO-1)+IDDL-1)+1
              NOCMP(INOCMP) = IMP
              NOCMP(INOCMP+1) = ICMP
            ENDIF
10        CONTINUE
        ENDIF
1     CONTINUE
      DO 2 IDDL = 1,NCMP
        INOCMP = 2*(NCMP*(LNO-1)+IDDL-1)+1
        IF ( NOCMP(INOCMP).EQ.0) THEN
          NIMPO =NIMPO+1
          NOCMP(INOCMP) = NIMPO
          NOCMP(INOCMP+1) = NUCMP(IDDL)
        ENDIF
2     CONTINUE
      END
