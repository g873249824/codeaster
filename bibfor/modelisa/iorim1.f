C ======================================================================
C     IORIM1  --  ORIENTATION D'UNE MAILLE PAR RAPPORT A UNE VOISINE
C
C   ARGUMENT        E/S  TYPE         ROLE
C    NUM1            IN    K*     NUMEROTATION DE LA MAILLE 1
C    NUM2          IN/OUT  K*     NUMEROTATION DE LA MAILLE 2
C
C   CODE RETOUR IORIM1 : 0 SI LES MAILLES NE SONT PAS CONTIGUES
C                       -1 OU 1 SINON (SELON QU'IL AIT OU NON
C                                      FALLU REORIENTER)
C
      INTEGER  I1, J1, K, L
      LOGICAL  EGAL
      EGAL(I1,J1) = NUM1(I1).EQ.NUM2(J1)
C
C.========================= DEBUT DU CODE EXECUTABLE ==================
C
C --- BOUCLES SUR LES SOMMETS
      DO 10 I1 = 1, 2
         J1 = 3-I1
         IF ( EGAL(I1,I1) ) THEN
            IORIM1 = -1
            GOTO 100
         ENDIF
         IF ( EGAL(I1,J1) ) THEN
            IORIM1 = 1
            GOTO 100
         ENDIF
   10 CONTINUE
      IORIM1 = 0
  100 CONTINUE
C
C --- ON PERMUTE LES SOMMETS
      IF ( REORIE .AND. IORIM1.LT.0 ) THEN
         K = NUM2(1)
         L = NUM2(2)
         NUM2(1) = L
         NUM2(2) = K
      ENDIF
C
      END
