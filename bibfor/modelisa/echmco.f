      SUBROUTINE ECHMCO(NOMMAI,TYPEMA,DIME  ,NECH  ,
     &                  NSOM  ,NARE  ,NPAN  ,NOEARE,NOEPAN, 
     &                  OFFSOM,OFFSEG,OFFFAC,OFFARE,                
     &                  PFS   ,PAS   ,PAF   ,
     &                  TRAVI ,NSEG  ,NFAC)
C     
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF MODELISA  DATE 12/02/2008   AUTEUR ABBAS M.ABBAS 
C ======================================================================
