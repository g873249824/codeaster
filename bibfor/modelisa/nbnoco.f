      SUBROUTINE NBNOCO(CHAR  ,MOTFAC,NOMA  ,MOTCLE,IZONE ,
     &                  ISURF ,INDQUA,JTRAV ,JSUMA ,JSUNO ,
     &                  JNOQUA,LQUADV)
