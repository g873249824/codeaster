C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF MODELISA  DATE 27/11/2007   AUTEUR ABBAS M.ABBAS 
C ======================================================================
C COPYRIGHT (C) 1991 - 2004  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT      NONE
      CHARACTER*8   CHAR
      CHARACTER*16  MOTFAC
      CHARACTER*8   NOMA
      CHARACTER*(*) MOTCLE
      INTEGER       IZONE,ISURF
      INTEGER       INDQUA
      INTEGER       JTRAV
      INTEGER       JSUMA
      INTEGER       JSUNO
      INTEGER       JNOQUA
      LOGICAL       LQUADV      
C     
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (TOUTES METHODES - LECTURE DONNEES)
C
C DETERMINATION DU NOMBRE DE
C MAILLES ET DE NOEUDS DE CONTACT POUR LA SURFACE IZONE
C REMPLISSAGE DES POINTEURS ASSOCIES JSUMA,JSNUO ET JNOQUA
C      
C ----------------------------------------------------------------------
C
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  MOTFAC : MOT-CLE FACTEUR (VALANT 'CONTACT')
C IN  NOMA   : NOM DU MAILLAGE
C IN  MOTCLE : MOT-CLE (GROUP_MA_MAIT...)
C IN  IZONE  : INDICE POUR LIRE LES DONNEES DANS AFFE_CHAR_MECA
C IN  ISURF  : REPERAGE DE LA SURFACE DE CONTACT
C IN  INDQUA : TRAITEMENT DES NOEUDS MILIEUX DES MAILLES QUADRATIQUES
C              0: ON DOIT TRAITER LES NOEUDS MILIEUX A PART
C              1: ON DOIT TRAITER LES NOEUDS MILIEUX NORMALEMENT
C IN  JTRAV  : POINTEUR VERS VECTEUR DE TRAVAIL 'BIDON'
C I/O JSUMA  : POINTEUR SUR LA ZONE DES MAILLES
C I/O JSUNO  : POINTEUR SUR LA ZONE DES NOEUDS
C I/O JNOQUA : POINTEUR SUR LA ZONE DES NOEUDS QUADRATIQUES
C OUT LQUADV : VAUT .TRUE. SI METHODE='VERIF' ET  NOMTM='QUAD8'OU'QUAD9'
C              DECLENCHE L'ALARME 'CONTACT3_94' DANS NBSUCO
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*8  TYPENT,K8BID
      INTEGER      NBENT,NBMA,NBNO,NBNOQU,NB
      INTEGER      NMA,NGRMA
      INTEGER      TYPALC,IBID
      LOGICAL      LVERIF
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      IF (MOTCLE(1:6).EQ.'MAILLE') THEN
        TYPENT = 'MAILLE'
      ELSE IF (MOTCLE(1:8).EQ.'GROUP_MA') THEN
        TYPENT = 'GROUP_MA'
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
      NBMA    = 0
      NBNO    = 0
      NBNOQU  = 0
C
C --- TYPE DE CONTACT
C
      CALL CFDISC(CHAR(1:8)//'.CONTACT',' ',TYPALC,IBID,IBID,IBID)
      LVERIF = TYPALC.EQ.5
C           
      CALL GETVEM(NOMA  ,TYPENT,MOTFAC,MOTCLE,
     &            IZONE ,1     ,0,K8BID,NBENT)
C
      IF (NBENT.NE.0) THEN
        NBENT   = -NBENT
        CALL GETVEM(NOMA,TYPENT,MOTFAC,MOTCLE,
     &              IZONE,1,NBENT,ZK8(JTRAV),NB)
        IF (TYPENT.EQ.'MAILLE') THEN
          NMA   = NB
          NGRMA = 0
        ELSE   
          NMA   = 0
          NGRMA = NB
        ENDIF            
        CALL NBNOEL(CHAR      ,NOMA  ,TYPENT,NGRMA ,NMA   ,
     &              ZK8(JTRAV),INDQUA,NBMA  ,NBNO  ,NBNOQU,
     &              LVERIF    ,LQUADV)
        ZI(JSUMA  + ISURF) = ZI(JSUMA  + ISURF - 1) + NBMA
        ZI(JSUNO  + ISURF) = ZI(JSUNO  + ISURF - 1) + NBNO
        ZI(JNOQUA + ISURF) = ZI(JNOQUA + ISURF - 1) + NBNOQU
        ISURF = ISURF + 1
      ENDIF
C
C ----------------------------------------------------------------------
C
      CALL JEDEMA()
      END
