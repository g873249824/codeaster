C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER      JTABNO,JTABIN,JTABCR
      INTEGER      NBNO,NBARTO,NDIM,MXAR
      INTEGER      ALGOLA
      CHARACTER*19  NLISEQ
      CHARACTER*8   FISS
C
C ----------------------------------------------------------------------
C
C ROUTINE XFEM (PREPARATION)
C
C CHOIX DE L'ESPACE DES LAGRANGES POUR LE CONTACT:
C                    (VOIR BOOK VI 15/07/05)
C    - DETERMINATION DES NOEUDS
C    - CREATION DES RELATIONS DE LIAISONS ENTRE LAGRANGE
C
C ----------------------------------------------------------------------
C
C
C IN  MXAR   : NOMBRE MAXIMUM D'ARETES COUPEES PAR LA FISSURE
C IN  JTABNO : ADRESSE DU TABLEAU DES NOEUDS EXTREMITES ET NOEUD MILIEU
C IN  JTABIN : ADRESSE DU TABLEAU DES COORDONNEES DES NOEUDS MILIEU
C IN  NBNO   : NOMBRE DE NOEUDS DU MAILLAGE
C IN  FISS   : NOM DE LA FISSURE
C IN  ALGOLA : TYPE DE CREATION DES RELATIONS DE LIAISONS ENTRE LAGRANGE
C I/O NBARTO : IN   NOMBRE D'ARETES INITIALES
C              OUT  NOMBRE D'ARETES COUPEES
C IN  MXAR   : NOMBRE MAXIMUM D'ARETES COUPEES PAR LA FISSURE
C OUT NLISRL : LISTE REL. LIN. POUR V1 ET V2
C OUT NLISCO : LISTE REL. LIN. POUR V1 ET V2
C OUT NLISEQ : LISTE REL. LIN. POUR V2 SEULEMENT
C
C
C
C
      CHARACTER*19  PICKNO
      INTEGER       JPINO
      INTEGER       NBPINO
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      PICKNO = '&&XLAGSP.PICKNO'
C
C --- CREATION DES LISTES DES RELATIONS DE LIAISONS ENTRE LAGRANGE
C
      IF (ALGOLA.EQ.1) THEN
C
C --- CREATION TABLEAU DES NOEUDS EXTREMITES ET NOEUD MILIEU
C
        CALL WKVECT(PICKNO,'V V I',MXAR,JPINO)
C
C --- SELECTION DES NOEUDS
C
        CALL XSELLA(FISS,NBNO  ,NBARTO,ZI(JTABNO),
     &              ZI(JPINO),NBPINO)
C
C --- CREATION DES LISTES DES RELATIONS DE LIAISONS ENTRE LAGRANGE
C
        CALL XRELL1(ZI(JTABNO),NDIM  ,NBARTO,
     &              ZI(JPINO), NBPINO,NLISEQ)
C
C --- MENAGE
C
        CALL JEDETR(PICKNO)

      ELSEIF (ALGOLA.EQ.2) THEN
C
C --- CREATION DES LISTES DES RELATIONS DE LIAISONS ENTRE LAGRANGE
C
        CALL XRELL2(ZI(JTABNO),NDIM  ,NBARTO,ZR(JTABIN),
     &              ZR(JTABCR),.FALSE.,NLISEQ)
      ENDIF
C
      CALL JEDEMA()
      END
