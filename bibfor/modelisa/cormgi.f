      INCLUDE 'jeveux.h'
      CHARACTER*(*)  BASEZ, LIGREZ
      CHARACTER*1    BASE
      CHARACTER*19   LIGREL

C**********************************************************************

C   OPERATION REALISEE
C   ------------------
C     CREATION DE L' OBJET .REPE DANS LA SD DE LIGREL

C     LIGREL.REPE : OJB V I LONG=2*NB_MAILLE(MAILLAGE)
C        V(2*(I-1)+1) --> NUMERO DU GREL CONTENANT LA MAILLE NUMERO I
C        V(2*(I-1)+2) --> NUMERO LOCALE DE CETTE MAILLE DANS LE GREL

C     REMARQUE : L'OBJET .REPE N'EST CREE QUE SI IL EXISTE DES MAILLES
C     DU MAILLAGE PORTANT UN ELEMENT FINI DANS LE LIGREL (.LIEL)

C**********************************************************************

      CHARACTER*8 NMAILA
      INTEGER I,J,PT,NBMAIL,NBGREL,JLGRF,IRET
      INTEGER JREPE,JGREL,NBMGRE
      LOGICAL EXIMA



      CHARACTER*19  LIGTMP
      CHARACTER*1 K1BID

      CALL JEMARQ()
      BASE   = BASEZ
      LIGREL = LIGREZ
      LIGTMP = '&&CORMGI.LIGREL'
      CALL JEDETR(LIGREL//'.REPE')


      CALL JEVEUO(LIGREL//'.LGRF','L',JLGRF)
      NMAILA = ZK8(JLGRF)
      CALL ASSERT(NMAILA.NE.' ')

      CALL JEEXIN(NMAILA//'.CONNEX',IRET)
      IF (IRET.EQ.0) GO TO 9999

      CALL JELIRA(NMAILA//'.CONNEX','NMAXOC',NBMAIL,K1BID)
      CALL JELIRA(LIGREL//'.LIEL','NUTIOC',NBGREL,K1BID)
      IF (NBMAIL*NBGREL.EQ.0) GO TO 9999


      CALL WKVECT(LIGTMP//'.REPE','V V I',2*NBMAIL,JREPE)

      EXIMA=.FALSE.
      DO 100,I = 1,NBGREL,1
        CALL JELIRA(JEXNUM(LIGREL//'.LIEL',I),'LONMAX',NBMGRE,K1BID)
        CALL JEVEUO(JEXNUM(LIGREL//'.LIEL',I),'L',JGREL)

        DO 110,J = 1,NBMGRE - 1,1
          IF (ZI(JGREL+J-1).GT.0) THEN
            EXIMA=.TRUE.
            PT = 2* (ZI(JGREL+J-1)-1) + 1
            ZI(JREPE+PT-1) = I
            ZI(JREPE+PT) = J
          END IF
  110   CONTINUE
  100 CONTINUE

C     -- .REPE N'EXISTE VRAIMENT QUE SI DES MAILLES DU MAILLAGE
C         SONT AFFECTEES
      IF (EXIMA) CALL JEDUP1(LIGTMP//'.REPE',BASE,LIGREL//'.REPE')


 9999 CONTINUE
      CALL JEDETR(LIGTMP//'.REPE')

      CALL JEDEMA()
      END
