      IMPLICIT NONE
      INTEGER      DIME
      CHARACTER*16 TYPMAI
      CHARACTER*8  MAIL
      CHARACTER*8  NOMARL
      REAL*8       BC(2,*)
      CHARACTER*10 NOM1,NOM2,NOMC
      CHARACTER*10 NORM
      CHARACTER*8  CINE(3)
      INTEGER      GRCOL
      REAL*8       POND1,POND2

      INTEGER      NR,NK,NI
      PARAMETER   ( NR = 1 , NK = 1 , NI = 1)
      INTEGER      VALI(NI)
      REAL*8       VALR(NR)
      CHARACTER*24 VALK(NK)

      CHARACTER*6  NOMPRO
      PARAMETER   (NOMPRO='ARLPON')
C
C ----------------------------------------------------------------------
C
C ROUTINE ARLEQUIN
C
C PONDERATION DES MAILLES
C ROUTINE D'AIGUILLAGE SUIVANT GROUPE COLLAGE
C
C ----------------------------------------------------------------------
C
C
C IN  DIME   : DIMENSION DE L'ESPACE
C IN  MAIL   : NOM UTILISATEUR DU MAILLAGE
C IN  BC     : BOITE ENGLOBANT LA ZONE DE RECOUVREMENT
C IN  NOMARL : NOM DE LA SD PRINCIPALE ARLEQUIN
C IN  NOM1   : NOM DE LA SD DE STOCKAGE MAILLES GROUP_MA_1
C IN  NOM2   : NOM DE LA SD DE STOCKAGE MAILLES GROUP_MA_2
C IN  NOMC   : NOM DE LA SD POUR LE COLLAGE
C IN  NORM   : NOM DE LA SD POUR STOCKAGE DES NORMALES
C IN  TYPMAI : SD CONTENANT NOM DES TYPES ELEMENTS (&&CATA.NOMTM)
C IN  CINE   : CINEMATIQUES DES GROUPES DE MAILLE
C IN  POND1  : VALEUR DE LA PONDERATION SUR MAILLAGE FIN
C IN  POND2  : VALEUR DE LA PONDERATION SUR MAILLAGE GROSSIER
C IN  GRCOL  : GROUPE DANS LEQUEL SE TROUVE LA ZONE DE COLLAGE (1 OU 2)
C
C ----------------------------------------------------------------------
C
      CHARACTER*10 NOMGR1,NOMGR2
      CHARACTER*8  CINE1,CINE2
      INTEGER      IFM,NIV
      REAL*8       POND
C
C ----------------------------------------------------------------------
C
      CALL INFNIV(IFM,NIV)
C
      IF (GRCOL.EQ.1) THEN
        NOMGR1 = NOM1
        NOMGR2 = NOM2
        CINE1  = CINE(1)
        CINE2  = CINE(2)
        POND   = POND1
      ELSEIF (GRCOL.EQ.2) THEN
        NOMGR1 = NOM2
        NOMGR2 = NOM1
        CINE1  = CINE(2)
        CINE2  = CINE(1)
        POND   = POND2
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
      CALL ARLDBG(NOMPRO,NIV,IFM,1,NI,VALI,NR,VALR,NK,VALK)
C
      CALL ARLPND(MAIL  ,DIME  ,NOMARL,TYPMAI,NOMC  ,
     &            NOMGR1,NOMGR2,POND  ,NORM  ,BC    ,
     &            CINE1 ,CINE2 )
C
      IF (NIV.GE.2) THEN
        CALL ARLDBG(NOMPRO,NIV,IFM,2,NI,VALI,NR,VALR,NK,VALK)
        CALL ARLIMP(IFM,'PONDERATION',NOMARL)
      ENDIF
C
      END
