C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT REAL*8 (A-H,O-Z)
C
C     ARGUMENTS:
C     ----------
      CHARACTER*8 MO
C ----------------------------------------------------------------------
C     BUT: TRAITER LE MOT-CLEF AFFE_SOUS_STRUC DE LA COMMANDE
C          AFFE_MODELE.
C
C
C     IN: MO : NOM DU MODELE
C
C     OUT: MO EST (EVENTUELLEMENT) ENRICHI DE L'OBJET .SSSA
C        (CET OBJET PRECISE QUELS SONT LES (SUPER)MAILLES DU MAILLAGE
C         REELLEMENT AFFECTEES PAR DES SOUS_STRUCTURES DANS LE MODELE)
C
C ----------------------------------------------------------------------
C
C     FONCTIONS EXTERNES:
C     -------------------
C
C     VARIABLES LOCALES:
C     ------------------
      CHARACTER*8 MA ,KBID ,NOSMA
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*32 JEXNOM
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*24 VALK(2)
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      INTEGER      IARG
C ---------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------
C
C
C
      CALL JEMARQ()
      CALL GETFAC('AFFE_SOUS_STRUC',NBOC)
      IF (NBOC.EQ.0) GO TO 9999
C
      CALL DISMOI('F','NOM_MAILLA',MO,'MODELE',IBID,MA,IERD)
      CALL DISMOI('F','NB_SM_MAILLA',MA,'MAILLAGE',NBSMA,KBID,IERD)
      CALL DISMOI('F','NB_NL_MAILLA',MA,'MAILLAGE',NL,KBID,IERD)
      IF (NBSMA.EQ.0) THEN
        CALL U2MESS('F','SOUSTRUC_30')
      END IF
C
      IOC=1
      CALL WKVECT(MO//'.MODELE    .SSSA','G V I',NBSMA+3,IASSSA)
C
C     -- CAS : TOUT: 'OUI' :
C     ----------------------
      CALL GETVTX('AFFE_SOUS_STRUC','TOUT',IOC,IARG,1,KBID,N1)
      IF (N1.EQ.1) THEN
        DO 1, I=1,NBSMA
          ZI(IASSSA-1+I)=1
 1      CONTINUE
        NBSS= NBSMA
        GO TO 9998
      END IF
C
C     -- CAS : MAILLE: L_MAIL
C     -----------------------
      CALL GETVTX('AFFE_SOUS_STRUC','SUPER_MAILLE',
     &                IOC,IARG,0,KBID,N1)
      CALL WKVECT('&&SSAFMO.LMAI','V V K8',-N1,IALMAI)
      CALL GETVTX('AFFE_SOUS_STRUC','SUPER_MAILLE',
     &                IOC,IARG,-N1,ZK8(IALMAI),N2)
      NBSS= -N1
      DO 2, I=1,-N1
        NOSMA=ZK8(IALMAI-1+I)
        CALL JENONU(JEXNOM(MA//'.SUPMAIL',NOSMA),IMAS)
        IF (IMAS.EQ.0) THEN
           VALK(1) = NOSMA
           VALK(2) = MA
           CALL U2MESK('F','SOUSTRUC_26', 2 ,VALK)
        ELSE
          ZI(IASSSA-1+IMAS)=1
        END IF
 2    CONTINUE
C
C     -- ON REMPLIT LES 3 DERNIERES VALEURS:
C     --------------------------------------
 9998 CONTINUE
      ZI(IASSSA-1+NBSMA+1)=NBSMA
      ZI(IASSSA-1+NBSMA+2)=NBSS
      ZI(IASSSA-1+NBSMA+3)=NL
C
      CALL JEEXIN('&&SSAFMO.LMAI',IRET)
      IF (IRET.GT.0) CALL JEDETR('&&SSAFMO.LMAI')
C
C
 9999 CONTINUE
      CALL JEDEMA()
      END
