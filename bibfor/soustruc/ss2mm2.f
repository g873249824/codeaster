C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C INSPI  SS2MME
      IMPLICIT NONE
C
C     ARGUMENTS:
C     ----------
      INCLUDE 'jeveux.h'
      CHARACTER*8 MO,NOMCAS
      CHARACTER*19 VECEL
C ----------------------------------------------------------------------
C     BUT: TRAITER LE MOT-CLEF CAS_CHARGE DE LA COMMANDE
C          MACR_ELEM_STAT (POUR LES MACR_ELEM DU NIVEAU INFERIEUR)
C
C
C     IN:     MO : NOM DU MODELE
C          VECEL : NOM DU VECT_ELEM
C          NOMCAS: NOM DU CAS_CHARGE
C
C     OUT: VECEL EST  ENRICHI (EVENTUELLEMENT) DE L'OBJET .LISTE_CHAR
C
C ----------------------------------------------------------------------
C
C     FONCTIONS EXTERNES:
C     -------------------
C
C     VARIABLES LOCALES:
C     ------------------
      CHARACTER*8 MA ,KBID ,NOSMA,NOMACR
C
C
C
C
C-----------------------------------------------------------------------
      INTEGER I ,IALSCH ,IAMACR ,IAREFR ,IASSSA ,IBID ,IERD 
      INTEGER IRET ,NBSMA ,NBSSA 
C-----------------------------------------------------------------------
      CALL JEMARQ()
      CALL DISMOI('F','NOM_MAILLA',MO,'MODELE',IBID,MA,IERD)
      CALL DISMOI('F','NB_SS_ACTI',MO,'MODELE',NBSSA,KBID,IERD)
      CALL DISMOI('F','NB_SM_MAILLA',MO,'MODELE',NBSMA,KBID,IERD)
C
      IF (NBSSA.EQ.0) GO TO 9999
C
      CALL JEVEUO(MO//'.MODELE    .SSSA','L',IASSSA)
      CALL JEVEUO(MA//'.NOMACR','L',IAMACR)
C
      CALL JEVEUO(VECEL//'.RERR','E',IAREFR)
      ZK24(IAREFR-1+3)(1:3)='OUI'
C
      CALL JECREC(VECEL//'.RELC','V V I','NO','CONTIG',
     +             'CONSTANT',1)
      CALL JEECRA(VECEL//'.RELC','LONMAX',NBSMA,KBID)
      CALL JECROC(JEXNOM(VECEL//'.RELC',NOMCAS))
      CALL JEVEUO(JEXNOM(VECEL//'.RELC',NOMCAS),'E',IALSCH)
C
C
C     -- REMPLISSAGE DE .RELC:
C     ------------------------------
C
C     -- ON VERIFIE QUE LES VECTEURS ELEMENTAIRES SONT CALCULES:
C     ----------------------------------------------------------
      DO 3, I=1,NBSMA
        IF (ZI(IASSSA-1+I).EQ.0) GO TO 3
        CALL JENUNO(JEXNUM(MA//'.SUPMAIL',I),NOSMA)
        NOMACR= ZK8(IAMACR-1+I)
        CALL JEEXIN(JEXNOM(NOMACR//'.LICA',NOMCAS),IRET)
        IF (IRET.GT.0) THEN
          ZI(IALSCH-1+I)=1
        ELSE
          ZI(IALSCH-1+I)=0
        END IF
 3    CONTINUE
C
C
C
C
C
 9999 CONTINUE
      CALL JEDEMA()
      END
