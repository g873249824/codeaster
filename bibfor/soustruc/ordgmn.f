C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
      IMPLICIT NONE
C
C     ARGUMENTS:
C     ----------
      INCLUDE 'jeveux.h'

      CHARACTER*32 JEXNUM,JEXNOM
      CHARACTER*8 NOMA,NOMGRP
C ----------------------------------------------------------------------
C     BUT: ORDONNER LA LISTE DE NOEUDS SELON LA LISTE DE MAILLES
C          DE MEME NOM (LES MAILLES DOIVENT ETRE DES POI1)
C
C     IN: NOMA   : NOM DU MAILLAGE
C         NOMGRP : NOM DES GROUPES (MAILLES ET NOEUDS)
C
C ----------------------------------------------------------------------
C
C     FONCTIONS EXTERNES:
C     -------------------
C
C     VARIABLES LOCALES:
C     ------------------
      CHARACTER*8  K8BID
      CHARACTER*24  GRPMA, GRPNO
      INTEGER NBMA,NBNO
      INTEGER IRET,IALINO,IALIMA,I,IMA,NUMNO,IACNEX,NBNOMA
      CALL JEMARQ()
      GRPMA = NOMA//'.GROUPEMA       '
      GRPNO = NOMA//'.GROUPENO       '
C
      CALL JEEXIN(JEXNOM(GRPMA,NOMGRP),IRET)
      IF (IRET.EQ.0)  GOTO 9999
      CALL JELIRA(JEXNOM(GRPMA,NOMGRP),'LONUTI',NBMA,K8BID)
      CALL JEVEUO(JEXNOM(GRPMA,NOMGRP),'L',IALIMA)
C
      CALL JEEXIN(JEXNOM(GRPNO,NOMGRP),IRET)
      IF (IRET.EQ.0) THEN
         CALL U2MESS('F','SOUSTRUC_17')
      ENDIF
      CALL JELIRA(JEXNOM(GRPNO,NOMGRP),'LONUTI',NBNO,K8BID)
      CALL JEVEUO(JEXNOM(GRPNO,NOMGRP),'E',IALINO)
C
      IF (NBMA.NE.NBNO) THEN
         CALL U2MESS('F','SOUSTRUC_18')
      ENDIF
C
      DO 10 I =1,NBMA
         IMA=ZI(IALIMA+I-1)
         CALL JEVEUO(JEXNUM(NOMA//'.CONNEX',IMA),'L',IACNEX)
         CALL JELIRA(JEXNUM(NOMA//'.CONNEX',IMA),'LONMAX',NBNOMA,K8BID)
         IF (NBNOMA.NE.1) THEN
            CALL U2MESS('F','SOUSTRUC_19')
         ENDIF
         NUMNO=ZI(IACNEX)
         ZI(IALINO+I-1)=NUMNO
   10 CONTINUE
C
 9999 CONTINUE
      CALL JEDEMA()
      END
