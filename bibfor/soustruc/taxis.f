C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT NONE
C
C     ARGUMENTS:
C     ----------
      INCLUDE 'jeveux.h'

      CHARACTER*32 JEXNUM
      CHARACTER*8 NOMA
      INTEGER INDIC(*)
      INTEGER NBMA
C ----------------------------------------------------------------------
C     BUT: VERIFIER QUE LES COORDONNEES SONT POSITIVES
C
C     IN: NOMA   : NOM DU MAILLAGE
C         INDIC  : INDICATEUR DES MAILLES A TRAITER.
C           NBMA : NOMBRE DE MAILLES
C
C     VARIABLES LOCALES:
C     ------------------
      REAL*8 VALR(1),XMAX,TOLER,R8B
      COMPLEX*16 C16B

      CHARACTER*8  K8B,K8A
      CHARACTER*24 VALK(2)
      CHARACTER*19 TABLG
C
C-----------------------------------------------------------------------
      INTEGER IACNEX ,IER ,IMA ,INO ,JVALE ,NBNOMA ,NUMNO,IRET,IBID

C-----------------------------------------------------------------------
      CALL JEMARQ()

C     TOLERANCE POUR DES ABSCISSES TRES LEGEREMENT < 0 : 1.E-6*X_MAX
      CALL LTNOTB(NOMA,'CARA_GEOM',TABLG)
      CALL TBLIVA (TABLG, 0, ' ', IBID, R8B, C16B, K8B,
     &              K8B,R8B , 'X_MAX', K8B, IBID, XMAX, C16B,
     &              K8B, IRET)
      CALL ASSERT(IRET.EQ.0)
      TOLER=-1.D-6*ABS(XMAX)

C
C     -- ON PARCOURE LA LISTE DES MAILLES ET ON TESTE LES NOEUDS
C
      CALL JEVEUO(NOMA//'.COORDO    .VALE','L',JVALE)
      IER=0
      DO 2 IMA=1,NBMA
      IF (INDIC(IMA).NE.0) THEN
         CALL JEVEUO(JEXNUM(NOMA//'.CONNEX',IMA),'L',IACNEX)
         CALL JELIRA(JEXNUM(NOMA//'.CONNEX',IMA),'LONMAX',NBNOMA,K8B)
         DO 3 INO =1,NBNOMA
            NUMNO=ZI(IACNEX-1+INO)-1
            IF ( ZR(JVALE+3*NUMNO) .LT. TOLER ) THEN
               CALL JENUNO(JEXNUM(NOMA//'.NOMNOE',NUMNO+1),K8B)
               CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',IMA    ),K8A)
               IF ( IER .EQ. 0 ) THEN
                  CALL U2MESS('F+','SOUSTRUC_88')
               ENDIF
               VALK (1) = K8B
               VALK (2) = K8A
               VALR (1) = ZR(JVALE+3*NUMNO)
               CALL U2MESG('F+','SOUSTRUC_89',2,VALK,0,0,1,VALR)
               IER  = IER + 1
            ENDIF
 3       CONTINUE
      ENDIF
 2    CONTINUE
      IF ( IER .NE. 0 ) THEN
         CALL U2MESS('F','SOUSTRUC_90')
      ENDIF
C
      CALL JEDEMA()
      END
