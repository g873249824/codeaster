      IMPLICIT REAL*8 (A-H,O-Z)
C     COMMANDE:  MACR_ELEM_STAT
C
      INCLUDE 'jeveux.h'
      CHARACTER*8 NOMU
      CHARACTER*16 KBI1,KBI2
C
