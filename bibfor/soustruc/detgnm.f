      IMPLICIT   NONE
      INCLUDE 'jeveux.h'

      CHARACTER*32 JEXNUM,JEXNOM
      CHARACTER*8 MA

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF SOUSTRUC  DATE 12/02/2013   AUTEUR SELLENET N.SELLENET 
C ======================================================================
C COPYRIGHT (C) 1991 - 2013  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C     ------------------------------------------------------------------
C     BUT: - TRAITE LES MOTS CLES FACTEURS DETR_GROUP_MA ET
C            DETR_GROUP_NO DE L'OPERATEUR DEFI_GROUP.
C          - PERMET DE DETRUIRE DES GROUPES (NOEUDS OU MAILLES)
C
C     IN : MA : NOM DU MAILLAGE
C     ------------------------------------------------------------------
C
C
      INTEGER N1,IOCC,MAXVAL,NBVAL,JGMDET,IRET,NBTGP,NUMGM,NBGMDE
      INTEGER NBGP,NBMAGP,JGM,I,J,J1,J2,NGP,IG
      PARAMETER(NGP=2)
      CHARACTER*8 K8B,KBID
      CHARACTER*16 DETR(2),GROUP(2),PTRN(2)
      CHARACTER*24 GRP,GPPTNM,NOMGP
      INTEGER      IARG
      DATA DETR  / 'DETR_GROUP_MA','DETR_GROUP_NO'/
      DATA GROUP / '.GROUPEMA','.GROUPENO'/
      DATA PTRN  / '.PTRNOMMAI','.PTRNOMNOE'/
C
      CALL JEMARQ()
C
      DO 100 IG = 1 , NGP
         CALL GETFAC(DETR(IG),N1)
         GRP='&&DETGNM'//GROUP(IG)
         GPPTNM = '&&DETGNM'//PTRN(IG)
         IF(N1.NE.0)THEN
            CALL JEEXIN(MA//GROUP(IG),IRET)
            IF(IRET.EQ.0) GOTO 100
            CALL JELIRA(MA//GROUP(IG),'NUTIOC',NBTGP,KBID)
            CALL WKVECT('&&DETGNM.GROUP','V V I',NBTGP,JGM)
            DO 5 I=1,NBTGP
               ZI(JGM+I-1)=0
 5          CONTINUE
            DO 10 IOCC= 1 , N1
               MAXVAL=0
               CALL GETVTX(DETR(IG),'NOM',IOCC,IARG,MAXVAL,K8B,NBVAL)
               NBVAL=-NBVAL
               CALL WKVECT('&&DETGNM.GROUP_DETR','V V K24',NBVAL,
     &              JGMDET)
               CALL GETVTX(DETR(IG),'NOM',IOCC,IARG,NBVAL,
     &              ZK24(JGMDET),IRET)
C              ON RECUPERE LES NUMEROS DES GROUPES A DETRUIRE
               DO 15 I = 1 , NBVAL
                  CALL JENONU(JEXNOM(MA//GROUP(IG),ZK24(JGMDET+I-1)),
     &                 NUMGM)
                  IF(NUMGM.NE.0)THEN
                     ZI(JGM+NUMGM-1)=NUMGM
                  ENDIF
 15            CONTINUE
               CALL JEDETR('&&DETGNM.GROUP_DETR')
 10         CONTINUE
C           ON COMPTE LE NOMBRE DE GROUPES A DETRUIRE
            NBGMDE=0
            DO 20 I=1,NBTGP
               IF(ZI(JGM+I-1).NE.0)THEN
                  NBGMDE=NBGMDE+1
               ENDIF
 20         CONTINUE
C           REACTUALISATION DE L'OBJET .GROUPEMA (OU .GROUPENO)
            NBGP=NBTGP-NBGMDE
            IF(NBGP.EQ.0)THEN
               CALL JEDETR(MA//GROUP(IG))
               GOTO 100
            ENDIF
            CALL JECREO(GPPTNM,'V N K24')
            CALL JEECRA(GPPTNM,'NOMMAX',NBGP,' ')
            CALL JECREC(GRP,'V V I','NO '//GPPTNM,
     &                  'DISPERSE','VARIABLE',NBGP)
            DO 25 I=1,NBTGP
               IF(ZI(JGM+I-1).EQ.0)THEN
                  CALL JENUNO(JEXNUM(MA//GROUP(IG),I),NOMGP)
                  CALL JECROC(JEXNOM(GRP,NOMGP))
                  CALL JELIRA(JEXNOM(MA//GROUP(IG),NOMGP),'LONMAX',
     &                 NBMAGP,KBID)
                  CALL JEECRA(JEXNOM(GRP,NOMGP),'LONMAX',
     &        MAX(1,NBMAGP),KBID)
                  CALL JEECRA(JEXNOM(GRP,NOMGP),'LONUTI',NBMAGP,KBID)
                  CALL JEVEUO(JEXNOM(GRP,NOMGP),'E',J2)
                  CALL JEVEUO(JEXNOM(MA//GROUP(IG),NOMGP),'L',J1)
                  DO 30 J=1,NBMAGP
                     ZI(J2+J-1)=ZI(J1+J-1)
 30               CONTINUE
               ENDIF
 25         CONTINUE
            CALL JEDETR(MA//GROUP(IG))
            CALL JEDETR(MA//PTRN(IG))
            CALL CPCLMA('&&DETGNM',MA,GROUP(IG)(2:9),'G')
         ENDIF
         CALL JEDETR('&&DETGNM.GROUP')
         CALL JEDETR(GRP)
         CALL JEDETR(GPPTNM)
 100  CONTINUE

      CALL JEDEMA()

      END
