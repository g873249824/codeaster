      REAL*8 FUNCTION BLPDOT(N,DX,INDX,DY)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF UTILIFOR  DATE 08/07/97   AUTEUR D6BHHJP J.P.LEFEBVRE 
C ======================================================================
C COPYRIGHT (C) LINPACK
C ======================================================================
C REMPLACE LA BLAS SPDOT SUR LES MACHINES OU ELLE N'EST PAS DISPONIBLE
C DANS LES LIBRAIRIES SYSTEME
C
C     FORMS THE DOT PRODUCT OF TWO VECTORS.
C     USES UNROLLED LOOPS FOR INCREMENTS EQUAL TO ONE.
C     JACK DONGARRA, LINPACK, 3/11/78.
C
      IMPLICIT NONE
      INTEGER N
      REAL*8 DX(1),DY(N),DTEMP
      INTEGER I,INDX(N)
C
      BLPDOT = 0.0D0
      DTEMP = 0.0D0
      IF(N.LE.0) GOTO 9999
C
      DO 10 I = 1,N
        DTEMP = DTEMP + DX(INDX(I))*DY(I)
   10 CONTINUE
      BLPDOT = DTEMP
9999  CONTINUE
      END
