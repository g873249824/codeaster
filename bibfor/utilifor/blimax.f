C REMPLACE LA FONCTION INTMAX SUR LES MACHINES OU ELLE N'EST PAS
C DISPONIBLE DANS LES LIBRAIRIES SYSTEME
C
C
      IMPLICIT NONE
      INTEGER N,DX(1),INCX
      INTEGER I,IX,IMAX,MAX
C
      BLIMAX = 0
      IF(N.LE.0) GOTO 9999
      IF(INCX.NE.1) THEN
C
C        CODE FOR UNEQUAL INCREMENTS OR EQUAL INCREMENTS
C          NOT EQUAL TO 1
C
        IX = 1
        IF(INCX.LT.0)IX = (-N+1)*INCX + 1
        IMAX = IX
        MAX = DX(IX)
        DO 10 I = 1,N
          IF ( DX(IX).GT.MAX ) THEN
           IMAX = IX
           MAX = DX(IX)
          ENDIF
          IX = IX + INCX
   10   CONTINUE
        BLIMAX = IMAX
      ELSE
C
C        CODE FOR INCREMENT EQUAL TO 1
C
        IMAX = 1
        MAX = DX(1)
        DO 20 I = 1,N
          IF ( DX(I).GT.MAX ) THEN
           IMAX = I
           MAX = DX(I)
          ENDIF
   20   CONTINUE
        BLIMAX = IMAX
      ENDIF
9999  CONTINUE
      END
