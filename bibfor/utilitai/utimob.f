      XOUS2=XOUS
      IFM=IUNIFI(FICOU)
      LB='----------------------------------------'
      WRITE(IFM,'(A40,A40)') LB,LB
C
      IF (XOUS2 .EQ.'X') THEN
         CALL UTIMCO(FICOU,OB1,NIVO,LATTR,LCONT)
      ELSE IF (XOUS2 .EQ.'S') THEN
         CALL UTIMOS(FICOU,OB1,LATTR,LCONT)
      ELSE
C
         CALL UTMESS('F','UTIMOB','XOUS : '//XOUS2//' NON PREVU.')
      END IF
C
 9999 CONTINUE
