C     ------------------------------------------------------------------
C     OBJETS SIMPLES CREES:
C        NOMFON//'.PROL'
C        NOMFON//'.VALE
C     ------------------------------------------------------------------

C     ----------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C     ----------- FIN COMMUNS NORMALISES  JEVEUX -----------------------

      INTEGER NBVAL, LVAL, JPRO, LXLGUT
      CHARACTER*8 TYPFON
      CHARACTER*16 CONSTA
      CHARACTER*19 NOMFON
      CHARACTER*24 CHPRO,CHVAL,NOMRES
C     ------------------------------------------------------------------
      CALL JEMARQ()
      CALL INFMAJ()

C     CREATION ET REMPLISSAGE DE L'OBJET NOMFON.PROL
      CALL GETRES(NOMFON,TYPFON,CONSTA)
      CHPRO = NOMFON//'.PROL'
      CALL ASSERT(LXLGUT(NOMFON).LE.24)
      CALL WKVECT(CHPRO,'G V K24',6,JPRO)
      ZK24(JPRO) = 'CONSTANT'
      ZK24(JPRO+1) = 'LIN LIN '
      CALL GETVTX(' ','NOM_RESU',0,1,1,NOMRES,NBVAL)
      ZK24(JPRO+2) = 'TOUTPARA'
      ZK24(JPRO+3) = NOMRES(1:8)
      ZK24(JPRO+4) = 'CC      '
      ZK24(JPRO+5) = NOMFON(1:19)

C     CREATION ET REMPLISSAGE DE L'OBJET NOMFON.VALE

      CHVAL = NOMFON//'.VALE'
      CALL WKVECT(CHVAL,'G V R',2,LVAL)
      ZR(LVAL) = 1.0D0
      CALL GETVR8(' ','VALE',0,1,1,ZR(LVAL+1),NBVAL)

C     --- LIBERATIONS ---

C     --- CREATION D'UN TITRE ---
      CALL TITRE()

      CALL JEDEMA()
      END
