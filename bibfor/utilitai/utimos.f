C
      CALL JEEXIN(OB1,IRET)
      IF (IRET.LE.0) THEN
         CALL U2MESK('A','UTILITAI_99',1,OB1)
         GO TO 9999
      END IF
C
      IF (LATTR) THEN
         CALL JEIMPA(UNIT,OB1,' ')
      END IF
C
      IF (LCONT) THEN
         CALL JEIMPO(UNIT,OB1,' ',' ')
      END IF
C
 9999 CONTINUE
