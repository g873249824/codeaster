C      CREER UNE SD L_TABLE DE NOM "LITAB" SUR UNE BASE "BASE"
C      LA STRUCTURE D'UNE L_TABLE :
C       .LTNT : K16 : REPERTOIRE DES NOMS SYMBOLIQUES
C       .LTNS : K24 : NOMS DES TABLE
C
C IN  : LITAB  : NOM DE LA SD L_TABLE A CREER.
C IN  : BASE   : BASE SUR LAQUELLE ON CREE LA SD L_TABLE
C     ------------------------------------------------------------------
      CHARACTER*1  BASELT
      CHARACTER*19 LISTAB
C DEB------------------------------------------------------------------
C
      BASELT = BASE(1:1)
      IF (BASELT.NE.'V' .AND. BASELT.NE.'G') THEN
         CALL UTMESS('F','LTCRSD','TYPE BASE INCONNU :'//BASELT)
      ENDIF
C
      LISTAB = LITAB
C
C     --- CREATION DU .LTNT ---
C
      CALL JECREO ( LISTAB//'.LTNT', BASELT//' V K16')
      CALL JEECRA ( LISTAB//'.LTNT', 'LONMAX', 7, ' ')
      CALL JEECRA ( LISTAB//'.LTNT', 'LONUTI', 0, ' ')
C
C     --- CREATION DU .LTNS ---
C
      CALL JECREO ( LISTAB//'.LTNS', BASELT//' V K24')
      CALL JEECRA ( LISTAB//'.LTNS', 'LONMAX', 7, ' ')
      CALL JEECRA ( LISTAB//'.LTNS', 'LONUTI', 0, ' ')
C
      END
