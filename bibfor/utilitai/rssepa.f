      INTEGER      NUORDR
      CHARACTER*8  RESULT,MODELE,CARELE,MATE
      CHARACTER*19 EXCIT 
C----------------------------------------------------------------------
C     BUT: PERMET DE LIRE OU D'ECRIRE DES NOMS DE CONCEPT DE 
C          LA SD RESULTAT ET D'EXPLOITER DES OBJETS DE LA SD
C          CORRESPONDANT AUX CHARGES
C
C     IN      RESULT : NOM DE LA SD RESULTAT
C     IN      IORDR  : NUMERO D'ORDRE
C     IN      MODELE : NOM DU MODELE
C     IN      MATE   : NOM DU CHAMP MATERIAU
C     IN      CARELE : NOM DE LA CARACTERISTIQUE ELEMENTAIRE
C     IN      EXCIT  : NOM DE LA SD INFO_CHARGE
C
C --------- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C     VARIABLES LOCALES
      INTEGER JPARA
      CHARACTER*8 K8B
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C     ======================================================
C --- STOCKAGE DE NOMS DE CONCEPT A PARTIR DE LA SD RESULTAT
C     ======================================================   
C      
C     STOCKAGE DU NOM DU MODELE
C     -------------------------
      CALL RSADPA(RESULT,'E',1,'MODELE',NUORDR,0,JPARA,K8B)
      ZK8(JPARA)=MODELE
C
C     STOCKAGE DU NOM DU CHAMP MATERIAU
C     ---------------------------------
      CALL RSADPA(RESULT,'E',1,'CHAMPMAT',NUORDR,0,JPARA,K8B)
      ZK8(JPARA)=MATE(1:8)
C
C     STOCKAGE DU NOM DE LA CARACTERISTIQUE ELEMENTAIRE
C     -------------------------------------------------
      CALL RSADPA(RESULT,'E',1,'CARAELEM',NUORDR,0,JPARA,K8B)
      ZK8(JPARA)=CARELE
C
C     STOCKAGE DU NOM DE LA SD INFO_CHARGE
C     ------------------------------------
      CALL RSADPA(RESULT,'E',1,'EXCIT',NUORDR,0,JPARA,K8B)
      ZK24(JPARA)=EXCIT(1:19)
C
      CALL JEDEMA()
      END
