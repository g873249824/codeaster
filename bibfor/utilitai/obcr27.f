C
C --- REEL
C
C      DATA PARAR //
C
C --- CHAINE
C
C      DATA PARAK //
C
C --- OBJETS
C
      DATA PARAO /'COLONNES_DISPOS'/
C
C --- TYPE OBJETS
C
      DATA TYPEO /'LISTE_STRUCTS'/
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NBPARB = NPARAB
      NBPARI = NPARAI
      NBPARR = NPARAR
      NBPARK = NPARAK
      NBPARO = NPARAO
C
C --- CREATION SD
C
      CALL OBCRCR(NOMSTR,NBPARB,NBPARI,NBPARR,NBPARK,
     &            NBPARO,PARAB ,PARAI ,PARAR ,PARAK ,
     &            PARAO ,TYPEO )
C
      CALL JEDEMA()
      END
