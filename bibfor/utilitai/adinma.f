C      ADAPTATION - INFORMATION SUR LE MAILLAGE
C      --           --                 --
C     ENTREES:
C        NOMAST : NOM ASTER DU MAILLAGE
C    SORTIES:
C        NRADAP : NUMERO D'ADAPTATION (0 POUR UN MAILLAGE DE DEPART)
C        CODRET : 0, SI LE NUMERO A ETE TROUVE
C                 1, PAS DE NUMERO CODE
C     ------------------------------------------------------------------
