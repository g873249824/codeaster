C
C 0.1. ==> ARGUMENTS
C
      INTEGER NRADAP, CODRET
C
      CHARACTER*(*) NOMAST
C
C 0.2. ==> COMMUNS
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
C 0.3. ==> VARIABLES LOCALES
C
CGN      CHARACTER*6 NOMPRO
CGN      PARAMETER ( NOMPRO = 'ADINMA' )
C
      INTEGER LXLGUT
C
      INTEGER IAUX
C
      CHARACTER*8  MAIL08
      CHARACTER*24 ADAPMA
C
C====
C 1. RECUPERATION DES INFORMATIONS
C====
C
C               12345678
      MAIL08 = '        '
      IAUX = LXLGUT(NOMAST)
      MAIL08(1:IAUX)  = NOMAST(1:IAUX)
      ADAPMA = MAIL08//'.ADAPTATION'
C
      CALL JEEXIN ( ADAPMA, IAUX )
C
      IF ( IAUX.EQ.0 ) THEN
C
CGN        CALL UTMESS ( 'A', NOMPRO,
CGN     >  'MAILLAGE '//MAIL08//' : AUCUNE INFORMATION D ADAPTATION')
        CODRET = 1
C
      ELSE
C
        CALL JEVEUO ( ADAPMA, 'L', IAUX )
        NRADAP = ZI(IAUX)
        CODRET = 0
C
      ENDIF
C
      END
