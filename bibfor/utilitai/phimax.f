      IMPLICIT REAL*8 (A-H,O-Z)
C     CALCULE LE DEPHASAGE CORRESPONDANT A UNE INTENSITE
C     DE PREMIERE CRETE DONNEE
      REAL*8 IMAX,IE,FREQ,TAU,PHIM,PHI,F,Z,D,OMEGA,F1,EPS,B,ASY,X,Y
      REAL*8 FX,FZ,FON,ALPHA
      PI=R8PI()
      OMEGA=2.D0*PI*FREQ
      ALPHA=ATAN(OMEGA*TAU)
      ASY=IMAX/IE/SQRT(2.D0)
      ASY=ABS(ASY)
      B=ACOS(ASY*COS(ALPHA))-ALPHA+PI
      D=OMEGA*TAU*SIN(B)*EXP(B/OMEGA/TAU)
      EPS=1.D-6
      F1=0.D0
      DO 1 I=1,102
      PHI=PI*DBLE(I-2)/100.D0
      F=EXP(PHI/OMEGA/TAU)*COS(PHI)-D
      IF (ABS(F).LE.EPS) GO TO 2
      IF ((F*F1).LT.0.D0) GO TO 3
      F1=F
    1 CONTINUE
      Z=0.D0
      GO TO 6
    3 CONTINUE
      X=PI*DBLE(I-3)/100.D0
      Y=PI*DBLE(I-2)/100.D0
    7 CONTINUE
      FX=EXP(X/OMEGA/TAU)*COS(X)-D
      Z=(X+Y)/2.D0
      FZ=EXP(Z/OMEGA/TAU)*COS(Z)-D
      IF ((FX*FZ).LE.0.D0) GO TO 4
      X=Z
      GO TO 5
    4 CONTINUE
      Y=Z
    5 CONTINUE
      FON=EXP(Z/OMEGA/TAU)*COS(Z)-D
      IF (ABS(FON).LE.EPS) GO TO 6
      GO TO 7
    2 CONTINUE
      Z=PHI
    6 CONTINUE
      PHIM=Z*180.D0/PI
      END
