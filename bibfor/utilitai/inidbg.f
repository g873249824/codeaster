C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
C  BUT: INITIALISE LE COMMON CZDBG
C       POUR FILTRER L'IMPRESSION DES MESSAGES EN NIVEAU 2
C
C     COMMON /CZDBG/CZCONT,CZMECA,CZPILO
C
C     CZCONT='CONTACT' SI ON SOUHAITE IMPRIMER LES MESSAGES DU CONTACT
C     CZMECA='MECA_NON_LINE' POUR LES MESSAGES DEDIES A 'MECA_NON_LINE'
C     CZPILO='PILOTE' POUR LES MESSAGES DEDIES AU PILOTAGE
C     CZAPPA='APPARIEMENT' POUR LES MESSAGES DEDIES A L'APPARIEMENT
C     CZFACT='FACTOR' POUR LES MESSAGES DEDIES A LA FACTORISATION
C
C-----------------------------------------------------------------------
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER          ZI
      COMMON  /IVARJE/ ZI(1)
      REAL*8           ZR
      COMMON  /RVARJE/ ZR(1)
      COMPLEX*16       ZC
      COMMON  /CVARJE/ ZC(1)
      LOGICAL          ZL
      COMMON  /LVARJE/ ZL(1)
      CHARACTER*8      ZK8
      CHARACTER*16            ZK16
      CHARACTER*24                    ZK24
      CHARACTER*32                            ZK32
      CHARACTER*80                                    ZK80
      COMMON  /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C     -----  FIN  COMMUNS NORMALISES  JEVEUX  --------------------------

C     
      INTEGER       IFM,NIV,N,I,JDBG
      CHARACTER*16  CZCONT,CZMECA,CZPILO,K16B,CZFACT,CZAPPA
      COMMON /CZDBG/CZCONT,CZMECA,CZPILO,CZFACT,CZAPPA
      INTEGER      IARG
C
      CALL JEMARQ()
C
      CZCONT=' '
      CZMECA=' '
      CZPILO=' '
      CZFACT=' '
      CZAPPA=' '
C
      CALL INFNIV(IFM,NIV)
      IF (NIV.NE.2) GOTO 9999

      CALL GETVTX(' ','INFO_DBG',1,IARG,0,K16B,N)
      IF(N.NE.0)THEN
C        -- ON IMPRIME UNIQUEMENT CE QUI EST DEMANDE --
          N=-N
          CALL WKVECT('&&INIDBG','V V K16',N,JDBG)
          CALL GETVTX(' ','INFO_DBG',1,IARG,N,ZK16(JDBG),N)
          DO 10 I=1,N
             IF(ZK16(JDBG+I-1)(1:7).EQ.'CONTACT')THEN
                 CZCONT='CONTACT'
             ELSE IF(ZK16(JDBG+I-1).EQ.'MECA_NON_LINE' .OR.
     &               ZK16(JDBG+I-1).EQ.'MECANONLINE')THEN
                 CZMECA='MECA_NON_LINE'
             ELSE IF(ZK16(JDBG+I-1).EQ.'PILOTAGE')THEN
                 CZPILO='PILOTAGE'
             ELSE IF(ZK16(JDBG+I-1).EQ.'APPARIEMENT')THEN
                 CZAPPA='APPARIEMENT'
             ELSE IF(ZK16(JDBG+I-1).EQ.'FACTORISATION')THEN
                 CZFACT='FACTORISATION'
             ENDIF
 10      CONTINUE
      ELSE
C        -- ON IMPRIME TOUT SANS RESTRICTION --
         CZCONT='CONTACT'
         CZMECA='MECA_NON_LINE'
         CZPILO='PILOTE'
         CZFACT='FACTORISATION'
         CZAPPA='APPARIEMENT'
      ENDIF

      CALL JEDETR('&&INIDBG')

 9999 CONTINUE

      CALL JEDEMA()

      END
