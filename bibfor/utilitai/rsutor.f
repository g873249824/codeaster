C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C RESPONSABLE VABHHTS J.PELLET
C      RECUPERATION DU (DES) NUMERO(S) D'ORDRE ET EVENTUELLEMENT DU
C      (DES) NOM(S) SYMBOLIQUE(S) CORRESPONDANT A UN NOM DE CHAMP
C      CHEXTR D'UNE STRUCTURE DE DONNEES NOMSD.

C      SI NOMSY = ' ' : RECHERCHE DES NOMS SYMBOLIQUES ET
C                                 DES NUMEROS D'ORDRE.
C ----------------------------------------------------------------------
C IN  : NOMSD  : NOM DE LA STRUCTURE "RESULTAT"
C IN  : NOMSY  : NOM SYMBOLIQUE DU CHAMP OU ' '
C IN  : CHEXTR : NOM DU CHAMP A RECHERCHER
C IN  : NBVALE : DIMENSION DES TABLEAUX
C OUT : TABNOM : TABLEAU DES NOMS SYMBOLIQUES
C OUT : TABORD : TABLEAU DES NUMEROS D'ORDRE
C OUT : NBTROU : NOMBRE DE FOIS QUE L'ON A LU LE CHAMP CHEXTR
C                SI NBTROU > NBVALE, ALORS NBTROU = -NBTROU
C ----------------------------------------------------------------------
C --------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR,JEXR8
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C ---------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------
      CHARACTER*16 NOMS2
      CHARACTER*19 NOMD2,CHEXT2
      CHARACTER*24 NOMCH
      CHARACTER*1 K1BID
C ----------------------------------------------------------------------

      CALL JEMARQ()
      NBTROU = 0
      IF (NBVALE.LT.0) GO TO 40
      NOMS2 = NOMSY
      NOMD2 = NOMSD
      CHEXT2 = CHEXTR

      CALL JELIRA(NOMD2//'.ORDR','LONMAX',NBORDR,K1BID)
      CALL JEVEUO(NOMD2//'.ORDR','L',JORDR)
      IF (NOMS2.EQ.' ') THEN
        CALL JELIRA(NOMD2//'.DESC','NOMMAX',NBCHAM,K1BID)
        CALL JEVEUO(NOMD2//'.TACH','L',JTACH)
        ITROU = 0
        DO 20 I = 0,NBCHAM - 1
          DO 10 J = 0,NBORDR - 1
            NOMCH = ZK24(JTACH+J+I*NBORDR)
            IF (NOMCH(1:19).EQ.CHEXT2) THEN
              NBTROU = NBTROU + 1
              IF (NBVALE.EQ.0) GO TO 10
              ITROU = ITROU + 1
              IF (ITROU.LE.NBVALE) THEN
                CALL JENUNO(JEXNUM(NOMD2//'.DESC',I+1),NOMS2)
                TABORD(ITROU) = ZI(JORDR+J)
                TABNOM(ITROU) = NOMS2
              END IF
            END IF
   10     CONTINUE
   20   CONTINUE
      ELSE
        CALL JENONU(JEXNOM(NOMD2//'.DESC',NOMS2),IBID)
        CALL JEVEUO(JEXNUM(NOMD2//'.TACH',IBID),'L',JTACH)
        ITROU = 0
        DO 30 I = 0,NBORDR - 1
          NOMCH = ZK24(JTACH+I)
          IF (NOMCH(1:19).EQ.CHEXT2) THEN
            NBTROU = NBTROU + 1
            IF (NBVALE.EQ.0) GO TO 30
            ITROU = ITROU + 1
            IF (ITROU.LE.NBVALE) THEN
              TABORD(ITROU) = ZI(JORDR+I)
              TABNOM(ITROU) = NOMS2
            END IF
          END IF
   30   CONTINUE
      END IF
      IF (NBTROU.GT.NBVALE) NBTROU = -NBTROU

   40 CONTINUE
      CALL JEDEMA()
      END
