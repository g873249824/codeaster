      SUBROUTINE RSUTOR(NOMSD,NOMSY,CHEXTR,NBVALE,TABNOM,TABORD,NBTROU)
      IMPLICIT REAL*8 (A-H,O-Z)
      INTEGER NBVALE,TABORD(*),NBTROU
      CHARACTER*(*) NOMSD,NOMSY,CHEXTR,TABNOM(*)
C ----------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF UTILITAI  DATE 03/10/2000   AUTEUR VABHHTS J.PELLET 
