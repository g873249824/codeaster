C      RECUPERATION DU NUMERO D'ORDRE ET DU NOM SYMBOLIQUE
C      CORRESPONDANT A UN NOM DE CHAMP D'UNE STRUCTURE DE DONNEES NOMSD.
C ----------------------------------------------------------------------
C IN  : NOMSD  : NOM DE LA STRUCTURE "RESULTAT"
C IN  : CHAMP  : NOM DU CHAMP A RECHERCHER
C OUT : NOMSYM : NOM SYMBOLIQUE
C OUT : IORDR  : NUMERO D'ORDRE
C ----------------------------------------------------------------------

      CHARACTER*3  CHNOCH,KBID
      CHARACTER*6  CHORCH
      CHARACTER*19 NOMD2
      INTEGER      NUNOCH,JORDR,IRANG,I1,NBORDR

      NOMD2 = NOMSD

      CHNOCH = CHAMP(10:12)
      CHORCH = CHAMP(14:19)

      READ(CHNOCH,'(I3)') NUNOCH
      READ(CHORCH,'(I6)') I1
      IRANG=I1+1

      CALL JELIRA(NOMD2//'.ORDR','LONUTI',NBORDR,KBID)
      CALL ASSERT(IRANG.GE.1 .AND. IRANG.LE.NBORDR)

      CALL JEVEUO(NOMD2//'.ORDR','L',JORDR)
      IORDR = ZI(JORDR-1+IRANG)
      CALL JENUNO(JEXNUM(NOMD2//'.DESC',NUNOCH),NOMSYM)

      END
