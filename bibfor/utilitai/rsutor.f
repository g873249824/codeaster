      CHARACTER*6  CHORCH
      CHARACTER*19 NOMD2
      INTEGER      NUNOCH,JORDR,ITMP

      NOMD2 = NOMSD

      CHNOCH = CHAMP(10:12)
      CHORCH = CHAMP(14:19)

      READ(CHNOCH,'(I3)') NUNOCH
      READ(CHORCH,'(I6)') ITMP
      CALL JEVEUO(NOMD2//'.ORDR','L',JORDR)
      IORDR = ZI(JORDR+ITMP)
      CALL JENUNO(JEXNUM(NOMD2//'.DESC',NUNOCH),NOMSYM)
      IRET = 1

      END
