      SUBROUTINE RSUTOR(NOMSD,CHAMP,NOMSYM,IORDR,IRET)
      IMPLICIT NONE
      INTEGER IORDR,IRET
      CHARACTER*(*) NOMSD,CHAMP,NOMSYM
C ----------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF UTILITAI  DATE 10/10/2006   AUTEUR VABHHTS J.PELLET 
