!    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
! ======================================================================
!      RENVOIE LA VALEUR MAXIMALE DU NUMERO D'ORDRE (IORDR)
! ----------------------------------------------------------------------
! IN  : NOMSD  : NOM DE LA STRUCTURE "RESULTAT"
    character(len=19) :: nomd2
    character(len=1) :: k1bid
    integer :: nbordr, jordr, k, ival
! ----------------------------------------------------------------------
    call jemarq()
    ival = 0
    nomd2 = nomsd
!
    call jelira(nomd2//'.ORDR', 'LONUTI', nbordr, k1bid)
    if (nbordr .gt. 0) then
        call jeveuo(nomd2//'.ORDR', 'L', jordr)
        ival = zi(jordr)
        do 10 k = 1, nbordr
            ival = max (ival,zi(jordr+k-1))
10      continue
    endif
    rsmxno = ival
!
    call jedema()
end function
