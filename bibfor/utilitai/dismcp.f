      IMPLICIT REAL*8 (A-H,O-Z)
C     --     DISMOI(CHAMP)
C     ARGUMENTS:
C     ----------
      INTEGER REPI,IERD
      CHARACTER*(*) QUESTI,CODMES
      CHARACTER*(*) REPKZ,NOMOBZ
      CHARACTER*32 REPK
      CHARACTER*19 NOMOB
C ----------------------------------------------------------------------
C     IN:
C       CODMES : CODE DES MESSAGES A EMETTRE : 'F', 'A', ...
C       QUESTI : TEXTE PRECISANT LA QUESTION POSEE
C       NOMOBZ : NOM D'UN OBJET DE TYPE CHAMP
C     OUT:
C       REPI   : REPONSE ( SI ENTIERE )
C       REPKZ  : REPONSE ( SI CHAINE DE CARACTERES )
C       IERD   : CODE RETOUR (0--> OK, 1 --> PB)

C ----------------------------------------------------------------------
C     VARIABLES LOCALES:
C     ------------------
C --------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR,JEXR8
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*4 DOCU
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C --------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------

C DEB-------------------------------------------------------------------


      NOMOB = NOMOBZ
      REPK = REPKZ

      CALL JEEXIN(NOMOB(1:19)//'.DESC',IBID)
      IF (IBID.GT.0) THEN
        CALL JELIRA(NOMOB(1:19)//'.DESC','DOCU',IBID,DOCU)
      ELSE
        CALL JEEXIN(NOMOB(1:19)//'.CELD',IBI2)
        IF (IBI2.GT.0) THEN
          CALL JELIRA(NOMOB(1:19)//'.CELD','DOCU',IBID,DOCU)
        ELSE
          CALL U2MESK(CODMES,'UTILITAI_55',1,NOMOB(1:19))
          REPK = ' '
          GO TO 10
        END IF
      END IF


      IF (DOCU(1:4).EQ.'CHNO') THEN
        CALL DISMCN(CODMES,QUESTI,NOMOB,REPI,REPK,IERD)
      ELSE IF (DOCU(1:4).EQ.'CART') THEN
        CALL DISMCA(CODMES,QUESTI,NOMOB,REPI,REPK,IERD)
      ELSE IF (DOCU(1:4).EQ.'CHML') THEN
        CALL DISMCE(CODMES,QUESTI,NOMOB,REPI,REPK,IERD)
      ELSE IF (DOCU(1:4).EQ.'RESL') THEN
        CALL DISMRE(CODMES,QUESTI,NOMOB,REPI,REPK,IERD)
      ELSE IF (DOCU(1:4).EQ.'VGEN') THEN
        CALL DISMCG(CODMES,QUESTI,NOMOB,REPI,REPK,IERD)
      ELSE
        CALL U2MESK(CODMES,'UTILITAI_56',1,NOMOB)
        IERD = 1
        GO TO 10
      END IF

   10 CONTINUE
      REPKZ = REPK
      END
