      IF (IRET.GT.0) CALL JEDETR(NOMD2//'.MATE')

      CALL JEEXIN(NOMD2//'.CHAR',IRET)
      IF (IRET.GT.0) CALL JEDETR(NOMD2//'.CHAR')

C PERMUT DE LA DESTRUCTION

      CALL JEEXIN(NOMD2//'.TAVA',IRET)
      IF (IRET.GT.0) CALL JEDETR(NOMD2//'.TAVA')

      CALL JEEXIN(NOMD2//'.TACH',IRET)
      IF (IRET.GT.0) CALL JEDETR(NOMD2//'.TACH')

