C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE GNICOLAS G.NICOLAS
C ----------------------------------------------------------------------
C     POUR LA STRUCTURE ACTIVE MEMORISANT LES DIVERS NOMS, CE PROGRAMME
C     RETOURNE POUR UN NOM COMPOSE DONNE :
C     . LA BASE
C     . LE PARAMETRE SENSIBLE
C     CELA EST APPLICABLE AU COURS DU TRAITEMENT D'UNE COMMANDE
C     A PRIORI, CE PROGRAMME NE DOIT ETRE APPELE QUE PAR UN AUTRE PSXXXX
C ----------------------------------------------------------------------
C IN  NBPASE  : NOMBRE DE PARAMETRES DE SENSIBILITE
C IN  INPSCO  : STRUCTURE CONTENANT LA LISTE DES INFORMATIONS
C               SA TAILLE ET SON CONTENU SONT DEFINIS DANS SEGICO
C IN  NOCOMP  : NOM COMPOSE A DECODER
C OUT NOSIMP  : NOM SIMPLE
C OUT NOPASE  : NOM DU PARAMETRE SENSIBLE
C OUT CODRET  : 0 : SI LE DECODAGE A EU LIEU
C               1 : C'EST UN FAUX NOM COMPOSE. IL CORRESPOND AU CAS
C                   SANS SENSIBILITE
C               2 : LE NOM COMPOSE EST INTROUVABLE
C ----------------------------------------------------------------------
C
      IMPLICIT   NONE
C
C 0.1. ==> ARGUMENTS
C
      INTEGER NBPASE, CODRET
C
      CHARACTER*(*) NOCOMP, NOSIMP, NOPASE
      CHARACTER*(*) INPSCO
C
C 0.2. ==> COMMUNS
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
C 0.3. ==> VARIABLES LOCALES
C
      CHARACTER*6 NOMPRO
      PARAMETER ( NOMPRO = 'SENCSP' )
C
      INTEGER LXLGUT
C
      INTEGER NBPSCO, ADPSCO, TYPEST, NRPASE
      INTEGER IAUX, JAUX
C
      CHARACTER*08 SAUX08
      CHARACTER*24 SAUX24, NOCO24
C
C====
C 1. RECHERCHE DU TYPE DE STRUCTURE CHOISIE
C====
C
C 1.1. ==> CARACTERISTIQUES DE LA STRUCTURE DE SAUVEGARDE
C
      CALL SEGICO ( 2,
     >              SAUX08, IAUX, SAUX24, SAUX24,
     >              INPSCO, NBPSCO, JAUX )
C
      CALL SEGICO ( 3,
     >              SAUX08, IAUX, SAUX24, SAUX24,
     >              INPSCO, ADPSCO, JAUX )
C
C 1.2. ==> VERIFICATION DU NOM COMPOSE
C
      IAUX = LXLGUT(NOCOMP)
C
      IF ( IAUX.GT.24 ) THEN
C
        CALL UTDEBM ( 'A', NOMPRO, 'PROBLEME DE DECLARATION' )
        CALL UTIMPI ( 'L', 'LA CHAINE NOCOMP EST DE LONGUEUR', 1, IAUX )
        CALL UTMESS ( 'A', NOMPRO, 'ELLE NE DEVRAIT PAS DEPASSER 24 !')
        CALL UTFINM
        CALL UTMESS ( 'F', NOMPRO, 'ERREUR DE PROGRAMMATION' )
C
      ENDIF
C
C               123456789012345678901234
      NOCO24 = '                        '
      NOCO24(1:IAUX) = NOCOMP(1:IAUX)
C
C 1.3. ==> ON EXPLORE LES NOMS COMPOSES MEMORISES PAR UNE DOUBLE BOUCLE
C          SUR LE NOMBRE DE PARAMETRES SENSIBLES ET SUR LE NOMBRE
C          D'INFORMATIONSS
C          ON LES COMPARE A CELUI VOULU
C
      CODRET = 2
C
      DO 13 , NRPASE = 0 , NBPASE
C
      DO 131 , TYPEST = 3 , NBPSCO
C
        SAUX24 = ZK24 ( ADPSCO + (NBPSCO+1)*NRPASE + TYPEST )
C
        IF ( NOCO24.EQ.SAUX24 ) THEN
          IF ( NRPASE.EQ.0 ) THEN
            CODRET = 1
          ELSE
            CODRET = 0
          ENDIF
          NOSIMP = ZK24 ( ADPSCO + TYPEST )
          NOPASE = ZK24 ( ADPSCO +(NBPSCO+1)*NRPASE + 1 )
          GOTO 132
        ENDIF
C
  131   CONTINUE
C
   13 CONTINUE
C
  132 CONTINUE
C
      END
