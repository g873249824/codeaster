C     SENSIBILITE - NOMS COMPOSES - NOM SIMPLE + PARAMETRE
C     **            *    *              *        *
C ----------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF UTILITAI  DATE 17/06/2002   AUTEUR GNICOLAS G.NICOLAS 
