C     PASSAGE DES ANGLES NAUTIQUES AUX ANGLES D'EULER
C     IN : ANGNAU(3) : 3 ANGLES NAUTIQUES
C     IN : ANGEUL(3) : 3 ANGLES D'EULER

      REAL*8 ANGNAU(3),PN(3,3),PE(3,3),ANGEUL(3),XL(3),YL(3),ZL(3)
      REAL*8 XG(3),YG(3),ZG(3),X1(3),COSPHI,PHI,SINPHI,ZLZG(3)
      REAL*8 COSPH1,XGX1(3),R8MIEM,PHI1,PHI2,COEF,R8PI
      INTEGER I

C     MATRICE ROTATION ASSOCIEE AUX ANGLES NAUTIQUES 
     
      CALL MATROT(ANGNAU,PN)
      
C     3 VECTEURS UNITAIRES DE BASE
     
      CALL R8INIR(3,0.D0, XL,1)
      CALL R8INIR(3,0.D0, YL,1)
      CALL R8INIR(3,0.D0, ZL,1)
      XL(1)=1.D0
      YL(2)=1.D0
      ZL(3)=1.D0

C     EXPRESSION DES COORDONNEES DES 3 VECTEURS DE LA BASE LOCALE
C     DANS LE REPERE GLOBAL      
      CALL UTPVLG ( 1 , 3 , PN , XL , XG )
      CALL UTPVLG ( 1 , 3 , PN , YL , YG )
      CALL UTPVLG ( 1 , 3 , PN , ZL , ZG )

C     DEUXIEME ANGLE D'EULER : PHI
      CALL ANGVEC(ZL,ZG,PHI)
      
      CALL PROVEC ( ZL , ZG , ZLZG)
      IF (SIN(PHI).GT.R8MIEM()) THEN
         DO 30 I=1,3
            X1(I)=ZLZG(I)/ABS(SIN(PHI))
 30      CONTINUE
         CALL ANGVEC(XL,X1,PHI1)
         CALL ANGVEC(X1,XG,PHI2)
C         COEF=180.0D0/R8PI()
C         WRITE(6,*) 'PHI1,PHI,PHI2',PHI1*COEF,PHI*COEF,PHI2*COEF
      ELSE
         CALL ANGVEC(XL,XG,PHI1)
         PHI2=0.D0
      ENDIF      

      ANGEUL(1)=PHI1
      ANGEUL(2)=PHI
      ANGEUL(3)=PHI2
      
      END
