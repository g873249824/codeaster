C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C.======================================================================
      IMPLICIT NONE
C
      INCLUDE 'jeveux.h'
      CHARACTER*8 NOMS(*)
      CHARACTER*8  NOMO,NOMA
      INTEGER ITYP,NN,IDIM,ITAB(*)
C
C   ARGUMENT        E/S  TYPE         ROLE
C.========================= DEBUT DES DECLARATIONS ====================
C -----  VARIABLES LOCALES
      INTEGER       REPI
      CHARACTER*1   K1BID
      CHARACTER*16  NOMTE, NOMODL,CHAINE
      CHARACTER*19  NOLIG
      CHARACTER*24  REPK
C.========================= DEBUT DU CODE EXECUTABLE ==================
C
C-----------------------------------------------------------------------
      INTEGER IALIEL ,IERD ,IERR ,IGREL ,IMA ,IRET ,ITYPEL 
      INTEGER KMA ,KMAI ,NBGREL ,NEL 
C-----------------------------------------------------------------------
      CALL JEMARQ ( )
C
C --- INITIALISATIONS :
C     ---------------
      REPI    = 0
      IF (IDIM.EQ.2) THEN
        CHAINE='2D_DIS_TR'
      ELSE
        CHAINE='DIS_TR'
      ENDIF
C
      NOLIG = NOMO//'.MODELE'
      CALL JEEXIN(NOLIG//'.LIEL',IRET)
      IF (IRET.NE.0) THEN
        CALL JELIRA(NOLIG//'.LIEL','NUTIOC',NBGREL,K1BID)
        IF (NBGREL.LE.0) THEN
          CALL U2MESS('F','UTILITAI_1')
        ENDIF
        NOMODL=' '
        IERR=0
        DO 10 IGREL=1,NBGREL
          CALL JEVEUO(JEXNUM(NOLIG//'.LIEL',IGREL),'L',IALIEL)
          CALL JELIRA(JEXNUM(NOLIG//'.LIEL',IGREL),'LONMAX',NEL,K1BID)
          ITYPEL= ZI(IALIEL -1 +NEL)
          CALL JENUNO(JEXNUM('&CATA.TE.NOMTE',ITYPEL),NOMTE)
          CALL DISMOI('F','MODELISATION',NOMTE,'TYPE_ELEM',REPI,
     &                 REPK,IERD)
          NOMODL=REPK(1:16)
          IF (NOMODL.NE.CHAINE) THEN
            IF (ITYP.EQ.0) THEN
               IERR=1
               KMAI=ZI(IALIEL)
               GOTO 20
            ELSE
               DO 1 KMA=1,NN
               IF (ITYP.EQ.1) THEN
                  CALL JENONU(JEXNOM(NOMA//'.NOMMAI',NOMS(KMA)),IMA)
               ELSE
                  IMA=ITAB(KMA)
               ENDIF
               CALL TESTLI(IMA,ZI(IALIEL),NEL-1,KMAI,IERR)
               IF (IERR.EQ.1) GOTO 20
    1          CONTINUE
            ENDIF
          ENDIF
   10   CONTINUE
      ENDIF
   20 CONTINUE
C     IF (IERR.EQ.1)  WRITE(*,*) 'KMAI',KMAI,'IGREL',IGREL,
C    .       'NOMODL',NOMODL,'CHAINE',CHAINE
      IF (IERR.EQ.1)  CALL U2MESS('F','UTILITAI_2')
      CALL JEDEMA()
      END
