C 0.1. ==> ARGUMENTS
C
      INTEGER NBMCVR, NBMC(NBMCVR), NBMOCL
C
      CHARACTER*(*) LIVALE, NOPASE
      CHARACTER*24 COREFE(NBMCVR), MCREFE(*)
      CHARACTER*24 STYPSE

