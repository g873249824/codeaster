C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C      VERIFICATION DE L'EXISTANCE D'UN NOM DE PARAMETRE ET DE
C      SA VALEUR DANS UN RESULTAT COMPOSE
C ----------------------------------------------------------------------
C IN  : NOMSD  : NOM DE LA STRUCTURE "RESULTAT".
C IN  : IORDR  : NUMERO D'ORDRE A TRAITER.
C IN  : NOMPAR : NOM DU PARAMETRE A VERIFIER.
C IN  : IPAR   : VALEUR DU PARAMETRE ( TYPE INTEGER )
C IN  : RPAR   : VALEUR DU PARAMETRE ( TYPE REAL )
C IN  : KPAR   : VALEUR DU PARAMETRE ( TYPE CHARACTER )
C OUT : IER    : = 0    CE N'EST PAS UN PARAMETRE DU "RESULTAT".
C              : = 110  LA VALEUR DU PARAMETRE N'EST PAS CORRECT.
C              : = 100  LA VALEUR DU PARAMETRE EST CORRECT.
C ----------------------------------------------------------------------
C ----------------------------------------------------------------------
      INTEGER      IPA, NBPAR, NBACC, JNPA
      CHARACTER*3  CTYPE
C
      CALL JEMARQ()
      IER = 0
C
      CALL RSNOPA(NOMSD,1,'&&RSVPAR.NOMS_PARA',NBACC,NBPAR)
      CALL JEVEUO('&&RSVPAR.NOMS_PARA','L',JNPA)
      DO 10 IPA = 1,NBPAR
         IF (NOMPAR.EQ.ZK16(JNPA-1+IPA)) GOTO 12
 10   CONTINUE
      GOTO 9999
C
 12   CONTINUE
      IER =  110
      CALL RSADPA(NOMSD,'L',1,NOMPAR,IORDR,1,JADR,CTYPE)
      IF (CTYPE(1:1).EQ.'I') THEN
         IF (ZI(JADR).EQ.IPAR) IER = 100
      ELSEIF (CTYPE(1:1).EQ.'R') THEN
         IF (ZR(JADR).EQ.RPAR) IER = 100
      ELSEIF (CTYPE(1:3).EQ.'K80') THEN
         IF (ZK80(JADR).EQ.KPAR) IER = 100
      ELSEIF (CTYPE(1:3).EQ.'K32') THEN
         IF (ZK32(JADR).EQ.KPAR) IER = 100
      ELSEIF (CTYPE(1:3).EQ.'K24') THEN
         IF (ZK24(JADR).EQ.KPAR) IER = 100
      ELSEIF (CTYPE(1:3).EQ.'K16') THEN
         IF (ZK16(JADR).EQ.KPAR) IER = 100
      ELSEIF (CTYPE(1:2).EQ.'K8') THEN
         IF (ZK8(JADR).EQ.KPAR) IER = 100
      ENDIF
C
 9999 CONTINUE
      CALL JEDETR('&&RSVPAR.NOMS_PARA')
      CALL JEDEMA()
      END
