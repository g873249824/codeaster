      SUBROUTINE VERIJ2(OUF,TYPESD,NOMSD,M80,NVU,NOBJ)
      IMPLICIT NONE
      CHARACTER*1 OUF
      CHARACTER*(*) TYPESD,NOMSD,M80
      INTEGER NOBJ,NVU
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF UTILITAI  DATE 10/10/2006   AUTEUR VABHHTS J.PELLET 
C ======================================================================
C COPYRIGHT (C) 1991 - 2006  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY  
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C ----------------------------------------------------------------------
C BUT : VERIFIER LA COHERENCE D'UNE SD DE NOM NOMSD ET DE TYPE TYPESD
C ----------------------------------------------------------------------
C IN  OUF (K1) :  'O' : LA SD EST OBLIGATOIRE (NON VIDE)
C                 'F' : LA SD EST FACULTATIVE (PEUT ETRE VIDE)
C IN  TYPESD (K16) : TYPE DE LA STRUCTURE DE DONNEE A TESTER :
C            /'CHAM_NO'  /'CHAMP'  /'TABLE' /... (VOIR VERIJC.F)
C IN  NOMSD  (K*) : NOM DE LA STRUCTURE DE DONNEES A TESTER
C IN/JXVAR M80   : NOM DE L'OBJET MEMORISANT LES SD VISITEES
C                  (POUR EVITER LES RECURSIONS INFINIES)
C IN/OUT  NVU    : NOMBRE DE SD DEJA VISITEES
C IN/OUT  NOBJ   : NOMBRE D'OJB DEJA VISITES (CUMUL)
C       EN REALITE, IL NE FAUT PAS TENIR COMPTE DE LA VALEUR DE NOBJ.
C       LA SEULE INFO PERTINENTE EST :
C           SI NOBJ EN SORTIE EST > NOBJ EN ENTREE, ALORS LA SD NOMSD
C           EST NON VIDE (I.E. CONTIENT 1 OU PLUSIEURS OBJETS).
C
C ----------------------------------------------------------------------
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR,JEXR8
C     ----- FIN COMMUNS NORMALISES  JEVEUX  ----------------------------
C
      CHARACTER*19 NO2SD
      CHARACTER*16 TYP2SD,KBID
      CHARACTER*24 VALK(2)
      CHARACTER*80 VISIT
      INTEGER NOBJAV,INDK80,ITROU,JM80 ,N1

      TYP2SD=TYPESD
      NO2SD=NOMSD
      NOBJAV=NOBJ
      CALL ASSERT(OUF.EQ.'F'.OR.OUF.EQ.'O')

      VISIT=' '
      VISIT(1:16)=TYP2SD
      VISIT(17:40)=NO2SD

C     -- AGRANDISSEMENT DE M80 ?
      CALL JELIRA(M80,'LONMAX',N1,KBID)
      IF (NVU+1.GE.N1) CALL JUVECA(M80,2*N1)

C     -- SI DEJA_VU, ON ARRETE :
      CALL JEVEUO(M80,'E',JM80)
      ITROU=INDK80(ZK80(JM80),VISIT,1,NVU)
      IF (ITROU.GT.0)   GO TO 9998

      NVU=NVU+1
      ZK80(JM80-1+NVU)=VISIT


C     -- VERIFICATIONS "AUTOMATIQUES" DEDUITES DU CATALOGUE :
      CALL VERIJ1(OUF,TYPESD,NOMSD,M80,NVU,NOBJ)

C     -- VERIFICATION DE OUF :
      IF ((OUF.EQ.'O').AND.(NOBJ.EQ.NOBJAV)) THEN
        VALK(1)=NO2SD
        VALK(2)=TYP2SD
        CALL U2MESK('F','SDVERI_30',2,VALK)
      ENDIF

C     -- SI LA SD EST NON VIDE, ON FAIT D'EVENTUELLES VERIFS
C        SUPPLEMENTAIRES :
      IF (NOBJ.GT.NOBJAV) CALL VERIJ5(TYPESD,NOMSD,M80,NVU)

9998  CONTINUE
      END
