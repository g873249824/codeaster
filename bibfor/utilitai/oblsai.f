C
C ----------------------------------------------------------------------
C
C ROUTINE UTILITAIRE (GESTION STRUCT - LISTE DE STRUCTS)
C
C ACTIVATION D'UN STRUCT DANS LA LISTE - ACCES PAR INDICE
C
C ----------------------------------------------------------------------
C
C
C IN  SDLIST : NOM DE LA LISTE
C IN  ISTRU  : INDICE DU STRUCT DANS LA LISTE
C IN  LACTI  : .TRUE. SI ACTIVATION
C
C ----------------------------------------------------------------------
C
      CHARACTER*24 LISACT
      INTEGER      JLISAC
      INTEGER      NBSTRU,NBACTI,I
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES ACTIVATION
C
      CALL OBGETO(SDLIST,'ACT_STRUCTS',LISACT)
      CALL JEVEUO(LISACT,'E',JLISAC)
C
C --- ACTIVATION
C
      IF (LACTI) THEN
        ZI(JLISAC-1+ISTRU) = 1
      ELSE
        ZI(JLISAC-1+ISTRU) = 0
      ENDIF
C
C --- RECALCUL NOMBRE DE STRUCTS ACTIFS
C
      NBACTI = 0
      CALL OBGETI(SDLIST,'NBRE_STRUCTS',NBSTRU)
      DO 10 I = 1,NBSTRU
        IF (ZI(JLISAC-1+I).EQ.1) NBACTI = NBACTI +1
  10  CONTINUE
      CALL ASSERT(NBACTI.LE.NBSTRU)
      CALL OBSETI(SDLIST,'NBRE_ACTI'  ,NBACTI)
C
      CALL JEDEMA()
      END
