      CHARACTER*8 KBID,KBLAN
C ----------------------------------------------------------------------

      CALL JENONU(JEXNOM('&&UTTCPU.NOMMES',NOMMES),INDI)
      IF (INDI.EQ.0) GOTO 9999

      CALL JEVEUO('&&UTTCPU.VALMES','L',JVALMS)
      DO 1, K=1,NBV
        TEMPS(K)= ZR(JVALMS-1+7*(INDI-1)+K)
1     CONTINUE


9999  CONTINUE
      END
