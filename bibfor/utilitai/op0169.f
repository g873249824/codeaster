      INCLUDE 'jeveux.h'
      CHARACTER*8  TYPFON
      CHARACTER*16 CMD
      CHARACTER*19 NOMFON,TYPFLU
      CHARACTER*24 FSIC, FSVR, PROL, VALE
      INTEGER      IARG
C     ------------------------------------------------------------------
      CALL JEMARQ()
      CALL INFMAJ()
C
      CALL GETRES(NOMFON,TYPFON,CMD)
      CALL GETVID(' ','TYPE_FLUI_STRU',0,IARG,1,TYPFLU,IBID)
C
C --- VERIFICATION A L EXECUTION
      FSIC = TYPFLU//'.FSIC'
      CALL JEVEUO(FSIC,'L',LFSIC)
      IF (ZI(LFSIC).NE.1) THEN
        CALL U2MESS('F','UTILITAI3_2')
      ENDIF
C
C --- CREATION ET REMPLISSAGE DE L'OBJET NOMFON.PROL
      PROL = NOMFON//'.PROL'
      CALL ASSERT(LXLGUT(NOMFON).LE.24)
      CALL WKVECT(PROL,'G V K24',6,LPROL)
      ZK24(LPROL)   = 'CONSTANT'
      ZK24(LPROL+1) = 'LIN LIN '
      ZK24(LPROL+2) = 'ABSC    '
      ZK24(LPROL+3) = 'COEF_MAS'
      ZK24(LPROL+4) = 'CC      '
      ZK24(LPROL+5) = NOMFON
C
C --- CREATION ET REMPLISSAGE DE L'OBJET NOMFON.VALE
      FSVR = TYPFLU//'.FSVR'
      CALL JEVEUO(FSVR,'L',LFSVR)
C
      VALE = NOMFON//'.VALE'
      CALL WKVECT(VALE,'G V R',2,LVALE)
      ZR(LVALE)   = 1.0D0
      ZR(LVALE+1) = ZR(LFSVR)
C
      CALL JEDEMA()
      END
