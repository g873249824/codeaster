      IMPLICIT NONE
C     --     DISMOI(TYPE_MAILLE)
C     ARGUMENTS:
C     ----------
      INCLUDE 'jeveux.h'
      INTEGER REPI,IERD
      CHARACTER*(*) QUESTI
      CHARACTER*(*) NOMOBZ,REPKZ
      CHARACTER*32 REPK
      CHARACTER*8 NOMOB
C ----------------------------------------------------------------------
C    IN:
C       QUESTI : TEXTE PRECISANT LA QUESTION POSEE
C       NOMOBZ : NOM D'UN OBJET DE TYPE TYPE_MAILLE (K8)
C    OUT:
C       REPI   : REPONSE ( SI ENTIERE )
C       REPKZ  : REPONSE ( SI CHAINE DE CARACTERES )
C       IERD   : CODE RETOUR (0--> OK, 1 --> PB)

C ----------------------------------------------------------------------
C     VARIABLES LOCALES:
C     ------------------
      INTEGER ITM,IANBNO



      CALL JEMARQ()
      NOMOB = NOMOBZ
      REPK = ' '
      REPI = 0
      CALL JENONU(JEXNOM('&CATA.TM.NOMTM',NOMOB),ITM)
      IF (ITM.EQ.0) CALL U2MESS('F','UTILITAI_70')


      IF (QUESTI.EQ.'NUM_TYPMAIL') THEN
C     ----------------------------------
        REPI = ITM


      ELSE IF (QUESTI.EQ.'NBNO_TYPMAIL') THEN
C     ----------------------------------
        CALL JEVEUO(JEXNUM('&CATA.TM.NBNO',ITM),'L',IANBNO)
        REPI = ZI(IANBNO)


      ELSE IF (QUESTI.EQ.'DIM_TOPO') THEN
C     ----------------------------------
        CALL JEVEUO(JEXNUM('&CATA.TM.TMDIM',ITM),'L',IANBNO)
        REPI = ZI(IANBNO)


      ELSE IF (QUESTI.EQ.'TYPE_TYPMAIL') THEN
C     ----------------------------------
        CALL JEVEUO(JEXNUM('&CATA.TM.TMDIM',ITM),'L',IANBNO)
        REPI = ZI(IANBNO)
        IF (REPI.EQ.0) THEN
          REPK = 'POIN'
        ELSE IF (REPI.EQ.1) THEN
          REPK = 'LIGN'
        ELSE IF (REPI.EQ.2) THEN
          REPK = 'SURF'
        ELSE IF (REPI.EQ.3) THEN
          REPK = 'VOLU'
        ELSE
          CALL ASSERT(.FALSE.)
        END IF


      ELSE
        IERD = 1
      END IF

      REPKZ = REPK
      CALL JEDEMA()
      END
