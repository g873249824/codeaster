      IMPLICIT NONE
C     --     DISMOI(TYPE_MAILLE)
C     ARGUMENTS:
C     ----------
      INTEGER REPI,IERD
      CHARACTER*(*) QUESTI,CODMES
      CHARACTER*(*) NOMOBZ,REPKZ
      CHARACTER*24 REPK
      CHARACTER*8 NOMOB
C ----------------------------------------------------------------------
C    IN:
C       CODMES : CODE DES MESSAGES A EMETTRE : 'F', 'A', ...
C       QUESTI : TEXTE PRECISANT LA QUESTION POSEE
C       NOMOBZ : NOM D'UN OBJET DE TYPE TYPE_MAILLE (K8)
C    OUT:
C       REPI   : REPONSE ( SI ENTIERE )
C       REPKZ  : REPONSE ( SI CHAINE DE CARACTERES )
C       IERD   : CODE RETOUR (0--> OK, 1 --> PB)

C ----------------------------------------------------------------------
C     VARIABLES LOCALES:
C     ------------------
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR,JEXR8
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      CHARACTER*8 KBID
C---------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------
      INTEGER ITM,IANBNO



      CALL JEMARQ()
      NOMOB = NOMOBZ
      REPK = ' '
      REPI = 0
      CALL JENONU(JEXNOM('&CATA.TM.NOMTM',NOMOB),ITM)
      IF (ITM.EQ.0) CALL UTMESS(CODMES,'DISMTM','TYPE_MAILLE INCONNU.')


      IF (QUESTI.EQ.'NUM_TYPMAIL') THEN
C     ----------------------------------
        REPI = ITM


      ELSE IF (QUESTI.EQ.'NBNO_TYPMAIL') THEN
C     ----------------------------------
        CALL JEVEUO(JEXNUM('&CATA.TM.NBNO',ITM),'L',IANBNO)
        REPI = ZI(IANBNO)


      ELSE IF (QUESTI.EQ.'TYPE_TYPMAIL') THEN
C     ----------------------------------
        IF (NOMOB(1:3).EQ.'POI') THEN
          REPK = 'POIN'
        ELSE IF (NOMOB(1:3).EQ.'SEG') THEN
          REPK = 'LIGN'
        ELSE IF (NOMOB(1:3).EQ.'TRI') THEN
          REPK = 'SURF'
        ELSE IF (NOMOB(1:3).EQ.'QUA') THEN
          REPK = 'SURF'
        ELSE IF (NOMOB(1:3).EQ.'TET') THEN
          REPK = 'VOLU'
        ELSE IF (NOMOB(1:3).EQ.'PEN') THEN
          REPK = 'VOLU'
        ELSE IF (NOMOB(1:3).EQ.'PYR') THEN
          REPK = 'VOLU'
        ELSE IF (NOMOB(1:3).EQ.'HEX') THEN
          REPK = 'VOLU'
        END IF


      ELSE
        REPK = QUESTI
        CALL UTMESS(CODMES,'DISMTE','LA QUESTION : "'//REPK//
     +              '" EST INCONNUE')
        IERD = 1
        GO TO 10
      END IF

   10 CONTINUE
      REPKZ = REPK
      CALL JEDEMA()
      END
