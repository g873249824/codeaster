      IMPLICIT NONE
      CHARACTER*(*)     MATELZ,RESUEZ,BASEZ
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF UTILITAI  DATE 22/02/2010   AUTEUR SELLENET N.SELLENET 
C ======================================================================
