C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
C     BUT : TRANSFORMER UN CHAMP : COMPLEXE --> REEL
C    
C     LE CHAMP COMPLEXE EST CONSTRUIT DE SORTE QUE:
C    - SA PARTIE REELLE CORRESPOND AUX VALEURS DU CHAMP REEL
C    - SA PARTIE IMAGINAIRE EST NULLE.
C     -----------------------------------------------------------------
C     ----- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C     ----- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      INTEGER IRET,IBID,JVALE,NBVAL,JVALIN,I,JREFE,JREFIN
      CHARACTER*3 TSCA
      CHARACTER*4 DOCU
      CHARACTER*8 CHOU,CHIN,NOMGD,PARTIE,K8B
      CHARACTER*19 CH19,CH19IN
      CHARACTER*24 K24B,VALE,VALIN

      CALL JEMARQ()

C     RECUPERATION DU CHAMP COMPLEXE
      CALL GETVID(' ','CHAM_GD',0,1,1,CHIN,IRET)

C     VERIFICATION : CHIN COMPLEXE?
      CALL DISMOI('F','NOM_GD',CHIN,'CHAMP',IBID,NOMGD,IBID)
      CALL DISMOI('F','TYPE_SCA',NOMGD,'GRANDEUR',IBID,TSCA,IBID)
      IF(TSCA.NE.'C')THEN
        CALL UTMESS('F','CHRECO','LE CHAMP '//CHIN//'N''EST PAS'// 
     &    ' DE TYPE COMPLEXE')
      ENDIF
     
C     COPIE CHIN --> CHOU
      CALL COPISD('CHAMP','G',CHIN,CHOU) 
C 
C
C     MODIFICATIONS DE CHOU:
C    ======================
C
C --- 1. ".VALE"
C     ----------
C     CHAM_NO OU CHAM_ELEM ?
      VALE(1:19)=CHOU
      K24B=VALE(1:19)//'.DESC'
      CALL JEEXIN(K24B,IBID)
      IF (IBID.GT.0) THEN
        K24B=VALE(1:19)//'.DESC'
        CALL JELIRA(K24B,'DOCU',IBID,DOCU)
      ELSE
        K24B=VALE(1:19)//'.CELD'      
        CALL JELIRA(K24B,'DOCU',IBID,DOCU)
      END IF

      IF (DOCU.EQ.'CHNO') THEN
         VALE(20:24)='.VALE'                  
      ELSEIF (DOCU.EQ.'CHML') THEN
         VALE(20:24)='.CELV'
      ELSE
         CALL UTMESS('F','CHRECO','ON NE TRAITE QUE DES '//      
     &               '"CHAM_NO" OU DES "CHAM_ELEM".')
      ENDIF

      CALL JELIRA(VALE,'LONMAX',NBVAL,K8B)
      CALL JEDETR(VALE)
      CALL JECREO(VALE,'G V R')
      CALL JEECRA(VALE,'LONMAX',NBVAL,K8B)
      CALL JEVEUO(VALE,'E',JVALE)

      VALIN=VALE
      VALIN(1:19)=CHIN
      CALL JEVEUO(VALIN,'L',JVALIN)

      CALL GETVTX(' ','PARTIE',0,1,1,PARTIE,IRET)
       
      DO 10 I=1,NBVAL
        IF(PARTIE(1:4).EQ.'REEL')THEN
            ZR(JVALE+I-1)=DBLE(ZC(JVALIN+I-1))
        ELSEIF(PARTIE(1:4).EQ.'IMAG')THEN
            ZR(JVALE+I-1)=DIMAG(ZC(JVALIN+I-1))
        ENDIF
 10   CONTINUE
C
C --- 2. CHANGEMENT DE LA GRANDEUR
C     ----------------------------
      CALL SDCHGD(CHOU,'R')

C --- 3. ".REFE"
C     ----------
      CH19IN=CHIN
      CH19=CHOU
      CALL JEDUP1(CH19IN//'.REFE','G',CH19//'.REFE')

      CALL JEDEMA()

      END
