      SDP=SDU

      IF (SDU.EQ.'CARTE_SDASTER')     SDP='CARTE'
      IF (SDU.EQ.'CHAM_NO_SDASTER')   SDP='CHAM_NO'
      IF (SDU.EQ.'VECT_ASSE_GENE')    SDP='CHAM_GENE'
