      IF (SDU.EQ.'SURFACE_SDASTER')   SDP='SURFACE'
      IF (SDU.EQ.'TABLE_SDASTER')     SDP='TABLE'
      IF (SDU.EQ.'SD_FETI_SDASTER')   SDP='SD_FETI'
      IF (SDU.EQ.'SPECTRE_SDASTER')   SDP='SPECTRE'

      IF (SDU(1:9).EQ.'CHAR_CINE')    SDP='CHAR_CINE'
      IF (SDU(1:9).EQ.'MATR_ASSE')    SDP='MATR_ASSE'
      IF (SDU(1:9).EQ.'MATR_ELEM')    SDP='MATR_ELEM'
      IF (SDU(1:9).EQ.'VECT_ELEM')    SDP='VECT_ELEM'


      IF (SDU(1:5).EQ.'MODE_')        SDP='SD_RESULTAT_DYN'
      IF (SDU.EQ.'MODE_CYCL')         SDP='MODE_CYCL'
      IF (SDU.EQ.'DYNA_HARMO'   )     SDP='SD_RESULTAT_DYN'
      IF (SDU.EQ.'ACOU_HARMO'   )     SDP='SD_RESULTAT_DYN'
      IF (SDU.EQ.'HARM_GENE'    )     SDP='SD_RESULTAT_DYN'
      IF (SDU.EQ.'DYNA_TRANS'   )     SDP='SD_RESULTAT_DYN'

      IF (SDU(1:5).EQ.'EVOL_')        SDP='SD_RESULTAT'
      IF (SDU.EQ.'COMB_FOURIER' )     SDP='SD_RESULTAT'
      IF (SDU.EQ.'FOURIER_ELAS' )     SDP='SD_RESULTAT'
      IF (SDU.EQ.'FOURIER_THER' )     SDP='SD_RESULTAT'
      IF (SDU.EQ.'MULT_ELAS'    )     SDP='SD_RESULTAT'
      IF (SDU.EQ.'THETA_GEOM'   )     SDP='SD_RESULTAT'




      SDPZ=SDP
      END
