C DETERMINATION DU NOM DU CHAMP19 CORRESPONDANT A NOMSD(IORDR,NOMSY)
C SI .TACH(IRANG,NOMSY) .EQ. ' ' : ON UTILISE UNE REGLE DE CONSTRUCTION
C                                  DU NOM.
C SINON : ON RENVOIE LE NOM TROUVE.
C ----------------------------------------------------------------------
C IN  : NOMSD  : NOM DE LA STRUCTURE "RESULTAT"
C IN  : NOMSY  : NOM SYMBOLIQUE DU CHAMP CHERCHE.
C IN  : IORDR  : NUMERO D'ORDRE DU CHAMP CHERCHE.
C OUT : CHEXTR : NOM DU CHAMP EXTRAIT.
C OUT : ICODE  : CODE RETOUR:
C              1) IORDR EST INFERIEUR AU MAX AUTORISE:
C                 0 : LE NOM SYMBOLIQUE EST LICITE.
C                 1 : LE NOM SYMBOLIQUE EST INTERDIT.
C              2) IORDR EST SUPERIEUR AU MAX AUTORISE:
C                10 : LE NOM SYMBOLIQUE EST LICITE.
C                11 : LE NOM SYMBOLIQUE EST INTERDIT.
C ----------------------------------------------------------------------
C --------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR,JEXR8
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C ---------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------
      CHARACTER*3 NUCH
      CHARACTER*6 CHFORD
      CHARACTER*8 NOMD2
      CHARACTER*16 NOMS2
      CHARACTER*19 NOMD3,CHEXT2
      CHARACTER*1 K1BID
      INTEGER ICH,IRANG,IATACH,NBORDR
C ----------------------------------------------------------------------

      NOMS2 = NOMSY
      NOMD2 = NOMSD
      NOMD3 = NOMSD

      ICODE = 0
      CALL JENONU(JEXNOM(NOMD3//'.DESC',NOMS2),ICH)
      IF (ICH.EQ.0) ICODE = 1

      CALL JELIRA(NOMD3//'.ORDR','LONMAX',NBORDR,K1BID)

      CHEXT2 = ' '
      CALL RSUTRG(NOMSD,IORDR,IRANG)
      IF (IRANG.GT.NBORDR) ICODE = ICODE + 10
      IF ((IRANG.GT.0) .AND. (ICH.GT.0)) THEN
        CALL JEVEUO(JEXNUM(NOMD3//'.TACH',ICH),'E',IATACH)
        CHEXT2 = ZK24(IATACH-1+IRANG)
      END IF

      IF (CHEXT2.EQ.' ') THEN
        CALL CODENT(IORDR,'D0',CHFORD)
        CALL CODENT(ICH,'D0',NUCH)
        CHEXT2 = NOMD2//'.'//NUCH//'.'//CHFORD
      END IF


      CHEXTR = CHEXT2

      END
