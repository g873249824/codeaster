      SUBROUTINE FOIEXC(LISFON,IUL,IND,FONINS)
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*(*)     LISFON,        FONINS
      INTEGER                  IUL,IND
C     ------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF UTILITAI  DATE 21/06/2004   AUTEUR DURAND C.DURAND 
C ======================================================================
C COPYRIGHT (C) 1991 - 2001  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR   
C (AT YOUR OPTION) ANY LATER VERSION.                                 
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT 
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF          
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU    
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                            
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C     IMPRESSION D'UNE LISTE DE FONCTIONS EN COLONNE ("EXCEL")
C     LA PREMIERE FONCTION DETERMINE LA LISTE DES PARAMETRES
C     ON INTERPOLE SUR LES VALEURS DE CETTE LISTE OU CELLE EN ARGUMENT
C     ------------------------------------------------------------------
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER          ZI
      COMMON  /IVARJE/ ZI(1)
      REAL*8           ZR
      COMMON  /RVARJE/ ZR(1)
      COMPLEX*16       ZC
      COMMON  /CVARJE/ ZC(1)
      LOGICAL          ZL
      COMMON  /LVARJE/ ZL(1)
      CHARACTER*8      ZK8
      CHARACTER*16              ZK16
      CHARACTER*24                        ZK24
      CHARACTER*32                                  ZK32
      CHARACTER*80                                            ZK80
      COMMON  /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C     -----  FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      INTEGER       LEXT, LVT, LRES
      INTEGER       NBVAL, NBVT, NCOL
      CHARACTER*8   K8B
      CHARACTER*16  NOMCMD
      CHARACTER*19  NOMFON, LISTR
      CHARACTER*24  VTOT, LRESU
      CHARACTER*24  PROL, VALE, SFORM1, SFORM2
      CHARACTER*24  NOMPAR, NOMRES, TITR
C     ------------------------------------------------------------------
      CALL JEMARQ()
      IF ( IUL .LE. 0 ) THEN
         CALL GETRES(K8B,K8B,NOMCMD)
         CALL UTMESS('A',NOMCMD,'UNITE LOGIQUE INEXISTANTE')
         GOTO 9999
      ENDIF
      LISTR = FONINS
      VTOT  = '&&FOIEXC.VTOT'
      LRESU = '&&FOIEXC.LRESU'
C
C     --- RECUP DES INFOS DE LA LISTE ---
      CALL JEVEUO(LISFON,'L',LEXT)
      CALL JELIRA(LISFON,'LONMAX',NBCOUR,K8B)
      NCOL=NBCOUR+1
C
      IC=1
      NOMFON=ZK24(LEXT+IC-1)
C     --- NOM DE LA 1ERE FONCTION ---
      VALE   = NOMFON//'.VALE'
      TITR   = NOMFON//'.TITR'
      PROL = NOMFON//'.PROL'
C     VERIFICATION PRELIMINAIRE
      CALL JEVEUO(PROL,'L',LPROL)
      IF (ZK16(LPROL).NE.'CONSTANT'.AND.ZK16(LPROL).NE.'FONCTION') THEN
         CALL GETRES(K8B,K8B,NOMCMD)
         CALL UTMESS('F',NOMCMD//' (FOIEXC01)',
     +              'ON NE SAIT PAS IMPRIMER UNE FONCTION DE TYPE "'//
     +               ZK16(LPROL)//'"      DESOLE. ')
      ENDIF
C
C     --- NOMBRE DE VALEURS DE LA LISTE D'INTERPOLATION
      IF(IND.EQ.0) THEN
        CALL JELIRA(VALE,'LONUTI',NBVAL,K8B)
        NBVAL=NBVAL/2
        CALL JEVEUO(VALE,'L',LVAL)
      ELSE
        CALL JELIRA(LISTR//'.VALE','LONUTI',NBVAL,K8B)
        CALL JEVEUO(LISTR//'.VALE','L',LVAL)
      ENDIF
C     --- IMPRESSION DU TITRE (DE LA 1ERE FONCTION) ---
      WRITE(IUL,'(/,80(''-''))')
      CALL JEEXIN(TITR,IRET)
      IF (IRET .NE. 0 ) THEN
         CALL JEVEUO(TITR,'L',LTITR)
         CALL JELIRA(TITR,'LONMAX',NBTITR,K8B)
         DO 10 I= 1, NBTITR
            WRITE(IUL,*) ZK80(LTITR+I-1)
  10     CONTINUE
      ENDIF
C     --- VECTEURS POUR STOCKER TOUTES LES VALEURS ET LES NOMS DES RESU
      NBVT=NCOL*NBVAL
      CALL WKVECT(VTOT,'V V R8',NBVT,LVT)
      CALL WKVECT(LRESU,'V V K24',NCOL,LRES)
C
C     --- BOUCLE SUR LES FONCTIONS -------------------------------------
      DO 333 IC=1,NBCOUR
        NOMFON=ZK24(LEXT+IC-1)
        PROL   = NOMFON//'.PROL'
        VALE   = NOMFON//'.VALE'
C

C       --- CAS D'UNE FONCTION "FORMULE" ---
        CALL JEEXIN(NOMFON//'.NOVA',IRET)
        IF ( IRET.NE.0 ) THEN
           CALL UTMESS('E','FOIMPR','DEVELOPPEMENT NON IMPLANTE.')
        ENDIF
C
C       --- INFORMATIONS COMPLEMENTAIRES POUR L'EDITION ---
        CALL JEVEUO(PROL,'L',LPROL)
        NOMPAR = ZK16(LPROL+2)
        NOMRES = ZK16(LPROL+3)
        IF(IC.EQ.1) ZK24(LRES-1+1)=NOMPAR
        ZK24(LRES-1+1+IC)=NOMRES
C
        IF (ZK16(LPROL).EQ.'CONSTANT'.OR.ZK16(LPROL).EQ.'FONCTION') THEN
C
          DO 200 IVAL = 1,NBVAL
            IF(IC.EQ.1) ZR(LVT-1+IVAL) = ZR(LVAL-1+IVAL)
            CALL FOINTE('F ',NOMFON,1,NOMPAR,ZR(LVAL-1+IVAL),
     +                  ZR(LVT-1+IC*NBVAL+IVAL),IRET)
 200      CONTINUE
C
        ELSEIF ( ZK16(LPROL) .EQ. 'NAPPE   ' ) THEN
          CALL UTMESS('E','FOIMPR','DEVELOPPEMENT NON IMPLANTE.')
C
        ELSEIF (ZK16(LPROL).EQ.'FONCT_C ' ) THEN
          CALL UTMESS('E','FOIMPR','DEVELOPPEMENT NON IMPLANTE.')
C
        ELSEIF (ZK16(LPROL).EQ.'INTERPRE' ) THEN
          CALL UTMESS('E','FOIMPR','DEVELOPPEMENT NON IMPLANTE.')
C
        ELSE
          CALL GETRES(K8B,K8B,NOMCMD)
          CALL UTMESS('A',NOMCMD//' (ALARME 01)',
     +               'ON NE SAIT PAS IMPRIMER UNE FONCTION DE TYPE "'//
     +               ZK16(LPROL)//'"      DESOLE. ')
C
        ENDIF
C     --- FIN BOUCLE SUR LES FONCTIONS ---------------------------------
  333 CONTINUE
C     --- IMPRESSION DES NOMS DE RESULTATS
      WRITE(SFORM1,'(A,I3,A)') '(',NBCOUR+1,'(A12,1X))'
      WRITE(IUL,SFORM1) (ZK24(LRES-1+NC),NC=1,NBCOUR+1)
C     --- IMPRESSION DE VTOT
      WRITE(SFORM2,'(A,I3,A)') '(',NBCOUR+1,'(1PE12.5,1X))'
      WRITE(IUL,SFORM2) ((ZR(LVT-1+(NC*NBVAL)+I),NC=0,NBCOUR),I=1,NBVAL)
C
      CALL JEDETC('V','&&FOIEXC',1)
 9999 CONTINUE
      CALL JEDEMA()
      END
