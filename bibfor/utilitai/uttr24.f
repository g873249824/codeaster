         DO 2, J=I+1,NDIM
            IF (VK24(J).LT.VK24(I)) THEN
C              -- ON PERMUTE I ET J
               CT     =VK24(I)
               VK24(I)=VK24(J)
               VK24(J)=CT
            END IF
 2       CONTINUE
 1    CONTINUE
C
      END
