      INTEGER IRET
      INTEGER NBAV
C
      CHARACTER*8  K8BID
      CHARACTER*19 RESU19
      CHARACTER*16 TYPRES,NOMCMD
C
C ----------------------------------------------------------------------
C
      CALL GETRES ( K8BID, TYPRES, NOMCMD )
C
C --- ALLOCATION (ON DIMENSIONNE PAR DEFAUT A 100)
C
      CALL JEEXIN ( RESULT(1:8)//'           .DESC', IRET )
C
C --- SI LE RESULTAT N'EXISTE PAS, ON ALLOUE A NBVAL VALEURS
C
      IF ( IRET.EQ.0 ) THEN
        CALL RSCRSD('G', RESULT, TYPRES, NBVAL )
      ELSE
C ----- SI LE RESULTAT N'EST PAS ASSEZ GRAND, ON L'AGRANDIT :
        RESU19=RESULT
        CALL JELIRA(RESU19//'.ORDR','LONMAX',NBAV,K8BID)
        IF (NBVAL.GT.NBAV) CALL RSAGSD(RESULT,NBVAL)
C ----- S'IL EXISTE, ON DETRUIT TOUT CE QUI SERAIT AU DELA DE NBVAL
        CALL RSRUSD ( RESULT, NBVAL+1 )
      ENDIF
