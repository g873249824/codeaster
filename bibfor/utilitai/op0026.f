C    PROCEDURE DEFI_FICHIER
C
      INTEGER         IER
C
      CHARACTER*1     KACC,KTYP
      CHARACTER*8     K8B,KACT
      CHARACTER*16    KNOM
      CHARACTER*255   KFIC
      INTEGER         PRUNIT,ULNUME,ULNOMF,IFM,NIV                 
      INTEGER         IOCC,IUN,NBNOM,NBFIC,NBOCC,NBUNIT,NBACCE,NBTYPE
C
      CALL INFMAJ()
C
      IUN = 1
      KNOM = ' '
      KFIC = ' '
      PRUNIT = 999
      CALL GETVTX(' ','ACTION'     ,0,IUN,IUN,KACT,NBACCE)
      CALL GETVTX(' ','FICHIER'    ,0,IUN,IUN,KNOM,NBNOM)
      CALL LXCADR(KNOM)
      CALL GETVTX(' ','NOM_SYSTEME',0,IUN,IUN,KFIC,NBFIC)
      CALL GETVIS(' ','UNITE',      0,IUN,IUN,PRUNIT,NBUNIT)
      CALL GETVTX(' ','ACCES'      ,0,IUN,IUN,K8B,NBACCE)
      KACC = K8B(1:1)
      CALL GETVTX(' ','TYPE'       ,0,IUN,IUN,K8B,NBTYPE)
      KTYP = K8B(1:1)
      IF ( KACT .EQ. 'LIBERER' ) THEN
        IF ( NBUNIT .EQ. 0 ) THEN
C L'ACCES AU FICHIER EST REALISE PAR NOM, IL FAUT VERIFIER SA PRESENCE
C DANS LA STRUCTURE DE DONNEES
          PRUNIT = ULNOMF (KFIC,KACC,KTYP) 
          IF (PRUNIT .LT. 0) THEN
            CALL UTMESS ('A','DEFI_FICHIER','AUCUN NUMERO D''UNITE '
     &                  //'LOGIQUE N''EST ASSOCIE A '//KFIC ) 
            GOTO 999
          ENDIF
        ENDIF 
        PRUNIT = -PRUNIT
      ELSE
        IF (NBUNIT .EQ. 0 .AND. NBFIC .GT. 0 ) THEN
          PRUNIT = ULNUME()
          IF (PRUNIT .LT. 0) THEN
            CALL UTMESS ('F','DEFI_FICHIER','AUCUN NUMERO D''UNITE '
     &                //'LOGIQUE N''EST DISPONIBLE ') 
          ENDIF
        ENDIF
      ENDIF
      IF ( KTYP .EQ. 'A' ) THEN
        CALL ULOPEN(PRUNIT,KFIC,KNOM,KACC,'O')
      ELSE IF ( KTYP .EQ. 'L' ) THEN
        CALL ULDEFI(PRUNIT,KFIC,KNOM,KTYP,KACC,'O') 
      ELSE  
        CALL ULDEFI(PRUNIT,KNOM,KNOM,KTYP,KACC,'O') 
      ENDIF    
C      
 999  CONTINUE
      CALL INFNIV(IFM,NIV)
      IF (NIV.GT.1) CALL ULIMPR(IFM)
 
      END
