      COMMON/ ASGFI1 / FIRST, UNITFI      , NBFILE
      COMMON/ ASGFI2 / NAMEFI,DDNAME,TYPEFI,ACCEFI,ETATFI,MODIFI
C
      CHARACTER*8      K8BID
      INTEGER          I
C
      IF ( FIRST .NE. 17111990 ) CALL ULINIT
C
      IF ( UNIT .LT. 0 ) THEN
         WRITE(K8BID,'(I4)') -UNIT
         CALL U2MESK('F','UTILITAI5_9',1,K8BID)
      ENDIF
      FNAME = ' '
      ETAT  = 'F'
      DO 1 I = 1, NBFILE
         IF( UNITFI(I) .EQ. UNIT ) THEN
            FNAME = NAMEFI(I)
            ETAT  = ETATFI(I)
            GOTO 999
         ENDIF
1     CONTINUE
999   CONTINUE
      END
