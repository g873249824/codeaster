C
      IVAL = -1
      KACC = '?'
      TYPEF = '?'
      DO 1 K = 1,MXF-1
        IF ( TYPEFI(K) .NE. '?' ) THEN
          IF( NAMEFI(K) .EQ. NOMFIC) THEN
            IVAL = UNITFI(K)
            TYPEF = TYPEFI(K)
            KACC  = ACCEFI(K)
            GOTO 2
          ENDIF
        ENDIF
   1  CONTINUE
   2  CONTINUE
      ULNOMF = IVAL
      END
