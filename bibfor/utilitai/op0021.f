      INTEGER         IER
C
      CHARACTER*16    MOTFAC
      INTEGER         PRNOM , PRUNIT
      INTEGER         INOM,IOCC,IUN,NBNOM,NBOCC,NBUNIT
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      CALL JEMARQ()
C
      MOTFAC = 'IMPRESSION'
      CALL GETFAC(MOTFAC,NBOCC)
C
      IUN = 1
      DO 20 IOCC = 1, NBOCC
         CALL GETVTX(MOTFAC,'NOM' ,IOCC,IUN,0,ZK24,NBNOM)
         NBNOM=-NBNOM
         CALL WKVECT('PRNOM','V V K24',NBNOM,PRNOM)
         CALL GETVTX(MOTFAC,'NOM' ,IOCC,IUN,NBNOM,ZK24(PRNOM),NBNOM)
         CALL GETVIS(MOTFAC,'UNITE',IOCC,IUN,IUN,PRUNIT,NBUNIT)
         DO 15 INOM = 1, NBNOM
            CALL LXCADR(ZK24(PRNOM+INOM-1))
            IF (ZK24(PRNOM+INOM-1)(17:24).NE.' ') CALL UTMESS('F',
     &     'OP0021','LE MOT CLE NOM N''ACCEPTE QUE DES CHAINES DE'
     &     //' 16 CARACTERES AU PLUS')
            CALL DEFUFI(PRUNIT,ZK24(PRNOM+INOM-1)(1:16) )
  15     CONTINUE
         CALL JEDETR('PRNOM')
  20  CONTINUE
C
      CALL JEDEMA()
      END
