      CALL ASSERT(TYPE.EQ.'R')

      CALL JEVEUO(OJB,'L',JOJB)
      CALL JELIRA(OJB,'LONMAX',N1,CBID)

      RVID=R8VIDE()
      RNAN=R8NNEM()
      DO 1, K=1,N1
         IF (IISNAN(ZR(JOJB-1+K)).EQ.0) THEN
           IF (ZR(JOJB-1+K).EQ.RVID) ZR(JOJB-1+K)=RNAN
         ENDIF
 1    CONTINUE

      CALL JEDEMA()
      END
