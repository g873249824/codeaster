C RESPONSABLE VABHHTS J.PELLET

C     DETRUIRE LA STRUCTURE DE DONNEES RESULTAT NOMSD
C     A PARTIR DU NUMERO D'ORDRE IORDR (Y COMPRIS IORDR)

C ----------------------------------------------------------------------
C IN  : NOMSD  : NOM DE LA STRUCTURE "RESULTAT"
C IN  : IORDR  : NUMERO D'ORDRE A PARTIR DUQUEL ON EFFACE TOUT
C                SI IORDR N'EST PAS UN NUMERO D'ORDRE DE NOMSD,
C                ON NE FAIT RIEN.
C ----------------------------------------------------------------------

C REMARQUES :
C------------
C ON NE REDIMENSIONNE PAS LES OBJETS JEVEUX MAIS :
C   - ON REMET LONUTI DE .ORDR A (IORDR-1)
C   - ON N'EFFACE PAS LE CONTENU DE .ORDR
C   - ON DETRUIT LES CHAMPS EXISTANTS AU DELA DE IORDR
C   - ON N'EFFACE PAS LES VALEURS DES PARAMETRES AU DELA DE IORDR
C   - ON REMET LES NOMS DES CHAMPS A " "  AU DELA DE IORDR


      CHARACTER*16 NOMSY
      CHARACTER*19 NOMS2,CHEXTR
      CHARACTER*1 K1BID
      INTEGER JORDR,KORDR,KRANG,IRANG,NBCHAM,NBORDR,K,IBID,JTACH,IRET
C ----------------------------------------------------------------------
      CALL JEMARQ()

      NOMS2 = NOMSD
      CALL RSUTRG(NOMSD,IORDR,IRANG)
      IF (IRANG.EQ.0) GO TO 30

      CALL JELIRA(NOMS2//'.DESC','NOMMAX',NBCHAM,K1BID)
      CALL JELIRA(NOMS2//'.ORDR','LONUTI',NBORDR,K1BID)
      CALL JEECRA(NOMS2//'.ORDR','LONUTI',IRANG-1,K1BID)
      CALL JEVEUO(NOMS2//'.ORDR','E',JORDR)

      DO 20 K = 1,NBCHAM
        CALL JENUNO(JEXNUM(NOMS2//'.DESC',K),NOMSY)
        CALL JENONU(JEXNOM(NOMS2//'.DESC',NOMSY),IBID)
        CALL JEVEUO(JEXNUM(NOMS2//'.TACH',IBID),'E',JTACH)
        DO 10 KRANG = IRANG,NBORDR
          KORDR=ZI(JORDR-1+KRANG)
          CALL RSUTCH(NOMSD,NOMSY,KORDR,CHEXTR,IRET)
          IF (IRET.EQ.0) CALL DETRSD('CHAMP_GD',CHEXTR)
          ZK24(JTACH-1+KRANG) = ' '
   10   CONTINUE
   20 CONTINUE

      DO 40 KRANG = IRANG,NBORDR
        ZI(JORDR-1+KRANG)=-99999
   40 CONTINUE

   30 CONTINUE
      CALL JEDEMA()
      END
