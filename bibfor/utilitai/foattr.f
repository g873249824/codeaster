      SUBROUTINE FOATTR( MOTCLE, IOCC, NOMFON )
      IMPLICIT REAL*8 (A-H,O-Z)
      INCLUDE 'jeveux.h'
      INTEGER                    IOCC
      CHARACTER*(*)      MOTCLE,       NOMFON
C     ----------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF UTILITAI  DATE 13/06/2012   AUTEUR COURTOIS M.COURTOIS 
C ======================================================================
C COPYRIGHT (C) 1991 - 2012  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C     SURCHARGE LES ATTRIBUTS D'UN CONCEPT DE TYPE "FONCTION"
C     ----------------------------------------------------------------
      CHARACTER*1   K1BID
      CHARACTER*4   INTERP(2)
      CHARACTER*8    PROLG, PROLD
      CHARACTER*16   NPARA, NRESU
      CHARACTER*19  TEMP
      CHARACTER*24  PROL
      INTEGER      IARG
C     ----------------------------------------------------------------
C
      CALL JEMARQ()
      TEMP = NOMFON
      CALL ASSERT(LXLGUT(TEMP).LE.24)
      PROL = TEMP//'.PROL'
      CALL JEVEUO(PROL,'E',LPRO)
      CALL JELIRA(PROL,'LONUTI',NBPROL,K1BID)
C
      IF (ZK24(LPRO).EQ.'NAPPE   ') THEN
         NBFONC = ( NBPROL - 7 ) / 2
C
         CALL GETVTX(MOTCLE,'INTERPOL',IOCC,IARG,2,INTERP,L1)
         IF ( L1 .EQ. 1 ) ZK24(LPRO+1) = INTERP(1)//INTERP(1)
         IF ( L1 .EQ. 2 ) ZK24(LPRO+1) = INTERP(1)//INTERP(2)
C
         CALL GETVTX(MOTCLE,'NOM_PARA',IOCC,IARG,1,NPARA,L2)
         IF (L2.NE.0) ZK24(LPRO+2) = NPARA
C
         CALL GETVTX(MOTCLE,'NOM_RESU',IOCC,IARG,1,NRESU,L3)
         IF (L3.NE.0) ZK24(LPRO+3) = NRESU
C
         CALL GETVTX(MOTCLE,'PROL_GAUCHE',IOCC,IARG,1,PROLG,L4)
         IF (L4.NE.0) ZK24(LPRO+4)(1:1) = PROLG(1:1)
C
         CALL GETVTX(MOTCLE,'PROL_DROITE',IOCC,IARG,1,PROLD,L5)
         IF (L5.NE.0) ZK24(LPRO+4)(2:2) = PROLD(1:1)
C
         ZK24(LPRO+5) = TEMP
C
         CALL GETVTX(MOTCLE,'NOM_PARA_FONC',IOCC,IARG,1,NPARA,L6)
         IF (L6.NE.0) ZK24(LPRO+6) = NPARA
C
         CALL GETVTX(MOTCLE,'INTERPOL_FONC',IOCC,IARG,2,INTERP,L7)
         IF (L7.NE.0) THEN
            DO 10 IF = 1,NBFONC
            IF (L7 .EQ. 1) ZK24(LPRO+7+2*(IF-1)) = INTERP(1)//INTERP(1)
            IF (L7 .EQ. 2) ZK24(LPRO+7+2*(IF-1)) = INTERP(1)//INTERP(2)
 10         CONTINUE
         ENDIF
C
         CALL GETVTX(MOTCLE,'PROL_GAUCHE_FONC',IOCC,IARG,1,PROLG,L8)
         IF (L8.NE.0) THEN
            DO 12 IF = 1,NBFONC
               ZK24(LPRO+8+2*(IF-1))(1:1) = PROLG(1:1)
 12         CONTINUE
         ENDIF
C
         CALL GETVTX(MOTCLE,'PROL_DROITE_FONC',IOCC,IARG,1,PROLD,L9)
         IF (L9.NE.0) THEN
            DO 14 IF = 1,NBFONC
              ZK24(LPRO+8+2*(IF-1))(2:2) = PROLD(1:1)
 14         CONTINUE
         ENDIF

C
      ELSE
C
         CALL GETVTX(MOTCLE,'INTERPOL',IOCC,IARG,2,INTERP,L1)
         IF (L1.NE.0) THEN
            IF ( L1 .EQ. 1 ) ZK24(LPRO+1) = INTERP(1)//INTERP(1)
            IF ( L1 .EQ. 2 ) ZK24(LPRO+1) = INTERP(1)//INTERP(2)
         ENDIF
C
         CALL GETVTX(MOTCLE,'NOM_PARA',IOCC,IARG,1,NPARA,L2)
         IF (L2.NE.0)  ZK24(LPRO+2) = NPARA
C
         CALL GETVTX(MOTCLE,'NOM_RESU',IOCC,IARG,1,NRESU,L3)
         IF (L3.NE.0)  ZK24(LPRO+3) = NRESU
C
         CALL GETVTX(MOTCLE,'PROL_GAUCHE',IOCC,IARG,1,PROLG,L4)
         IF (L4.NE.0)  ZK24(LPRO+4)(1:1) = PROLG(1:1)
C
         CALL GETVTX(MOTCLE,'PROL_DROITE',IOCC,IARG,1,PROLD,L5)
         IF (L5.NE.0)  ZK24(LPRO+4)(2:2) = PROLD(1:1)
C
         ZK24(LPRO+5) = TEMP
C
      ENDIF
C
      CALL JEDEMA()
      END
