C
C 0.1. ==> ARGUMENTS
C
      INTEGER IOCC, NBPASE
      INTEGER TYPERR, CODRET
C
      CHARACTER*(*) MOTFAC, NOSIMP, NOSTNC
C
C 0.2. ==> COMMUNS
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
C 0.3. ==> VARIABLES LOCALES
C
      CHARACTER*6 NOMPRO
      PARAMETER ( NOMPRO = 'PSNOCO' )
C
      CHARACTER*08 SAUX08
      CHARACTER*13 INPSCO
C
C====
C 1. LECTURE DES PARAMETRES DU MOT-CLE PILOTANT LA SENSIBILITE
C    ON RECUPERE :
C    . LE NOMBRE DE PARAMETRES, NBPASE
C    . LA STRUCTURE QUI MEMORISE LES NOMS SIMPLES ET COMPOSES, INPSCO
C====
C
C               12   345678   90123
      INPSCO = '&&'//NOMPRO//'_PSCO'
C               12   345678
      SAUX08 = '&&'//NOMPRO
      CALL PSLECT ( MOTFAC, IOCC, SAUX08, NOSIMP, TYPERR,
     >              NBPASE, INPSCO, CODRET )
C
C====
C 2. S'IL Y A DES PARAMETRES SENSIBLES, ON RECUPERE LA LISTE DES NOMS
C    COMPOSES
C====
C
      IF ( CODRET.EQ.0 ) THEN
C
      IF ( NBPASE.GT.0 ) THEN
C
        CALL SENSNC ( NOSIMP, NBPASE, INPSCO, NOSTNC )
C
      ENDIF
C
      ENDIF
C
C====
C 3. MENAGE
C====
C
      CALL JEDETC ( ' ', INPSCO, 1 )
C
      END
