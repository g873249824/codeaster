C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C ----------------------------------------------------------------------
C    IMPRIME LES CARACTéRISTIQUES DE OJB : XOUS, GENR, TYPE, ...
C ----------------------------------------------------------------------
C IN K*   COUI   :
C    /'C'     : NOMSD EST LIE A LA SD SUP. PAR COMPOSITION
C    /'I_XXX' : NOMSD EST LIE A LA SD SUP. PAR L'INDIRECTION XXX
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR,JEXR8
C     ----- FIN COMMUNS NORMALISES  JEVEUX  ----------------------------

C     COMMON VERIJK : POUR ETABLIR LE CATALOGUE A PARTIR DE LA REALITE
C                     DES TESTS.
C     PRCATA : 'OUI' : IL FAUT IMPRIMER LES CARACTERISTIQUES DES
C                      OBJETS TROUVES.
C              'NON' : ON N'IMPRIME RIEN
C     DERNSD : TYPE DE LA SD DE PLUS BAS NIVEAU (PARCOURS DE L'ARBRE)
      CHARACTER*16 DERNSD,PRCATA
      COMMON /VERIJK/DERNSD,PRCATA

      CHARACTER*24 OB
      CHARACTER*26 SUFFIX
      CHARACTER*32 ACCES
      CHARACTER*1  XOUS, GENR, KBID,TMES
      CHARACTER*8  TYSCA ,STOCK,MODELO,TYPCOL
      CHARACTER*4 DOCU
      CHARACTER*12  VERIF2
      INTEGER I1,I2,IBID,I3, I4,LTYP
      LOGICAL CONTIG

      INTEGER LONMAX,LONUTI,IEXI,LGCHSD
      INTEGER NUTIOC,NMAXOC,NOMMAX,NOMUTI

      OB=OJB
      CALL JEEXIN(OB,IEXI)
      IF (IEXI.EQ.0) GOTO 9999


C     XOUS, GENR, CONTIG,MODELO :
      CALL JELIRA(OB,'XOUS',IBID,XOUS)
      CALL JELIRA(OB,'GENR',IBID,GENR)
      STOCK=' '
      IF (XOUS.EQ.'X') CALL JELIRA(OB,'STOCKAGE',IBID,STOCK)
      CONTIG=STOCK.EQ.'CONTIG'
      MODELO=' '
      IF (XOUS.EQ.'X') CALL JELIRA(OB,'MODELONG',IBID,MODELO)
      ACCES=' '
      IF (XOUS.EQ.'X') CALL JELIRA(OB,'ACCES',IBID,ACCES)


C     LGCHSD,SUFFIX :
      CALL VERIJL(DERNSD,LGCHSD)
      IF (COUI.EQ.'C') THEN
         SUFFIX='>'//OB(LGCHSD+1:24)//'<'
      ELSE
         SUFFIX='>#'//COUI//'<'
      ENDIF


C     TYSCA :
      CALL JELIRA(OB,'TYPE',IBID,TYSCA(1:1))
      CALL JELIRA(OB,'LTYP',LTYP,KBID)
      CALL CODENT(LTYP,'G',TYSCA(2:8))


C     LONMAX,LONUTI :
      IF (XOUS.EQ.'S'.AND.GENR.EQ.'V') THEN
        CALL JELIRA(OB,'LONMAX',LONMAX,KBID)
        CALL JELIRA(OB,'LONUTI',LONUTI,KBID)
      ELSE
        LONMAX=0
        LONUTI=0
      ENDIF


C     NOMMAX,NOMUTI :
      IF (XOUS.EQ.'S'.AND.GENR.EQ.'N') THEN
        CALL JELIRA(OB,'NOMMAX',NOMMAX,KBID)
        CALL JELIRA(OB,'NOMUTI',NOMUTI,KBID)
      ELSE
        NOMMAX=0
        NOMUTI=0
      ENDIF


C     NMAXOC,NUTIOC :
      IF (XOUS.EQ.'X') THEN
        CALL JELIRA(OB,'NMAXOC',NMAXOC,KBID)
        CALL JELIRA(OB,'NUTIOC',NUTIOC,KBID)
      ELSE
        NMAXOC=0
        NUTIOC=0
      ENDIF


C     DOCU :
      CALL JELIRA(OB,'DOCU',IBID,DOCU)


      IF (XOUS.EQ.'X') THEN
C       -- CAS DES COLLECTIONS :

        TYPCOL='|X     |'
        IF (CONTIG) THEN
          TYPCOL(3:3)='C'
        ELSE
          TYPCOL(3:3)='D'
        ENDIF

        IF (MODELO.EQ.'CONSTANT') THEN
          TYPCOL(4:4)='C'
        ELSE
          TYPCOL(4:4)='V'
        ENDIF

        IF (ACCES(1:2).EQ.'NO') THEN
          TYPCOL(5:6)='NO'
        ELSE
          TYPCOL(5:6)='NU'
        ENDIF

        WRITE(6,1001) DERNSD,LGCHSD,SUFFIX,TYPCOL//GENR,TYSCA,
     &                 NMAXOC,NUTIOC,DOCU

      ELSE
C       -- CAS DES OBJETS SIMPLES :
        IF (GENR.EQ.'V') THEN
         WRITE(6,1002) DERNSD,LGCHSD,SUFFIX,'|S|V|',TYSCA,LONMAX,
     &   LONUTI,DOCU
        ELSE IF (GENR.EQ.'E') THEN
         WRITE(6,1002) DERNSD,LGCHSD,SUFFIX,'|S|E|',TYSCA,1,1,DOCU
        ELSE IF (GENR.EQ.'N') THEN
         WRITE(6,1003) DERNSD,LGCHSD,SUFFIX,'|S|N|',TYSCA,NOMMAX,
     &   NOMUTI,DOCU
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF
      ENDIF

9999  CONTINUE
 1001 FORMAT ('AJACOT|PRCATA_1|',A16,'|',I3,'|',A26,A10,'|TYPE=|',A4,
     &     '|NMAXOC=|',I6,'|NUTIOC=|',I6,'|',A4)
 1002 FORMAT ('AJACOT|PRCATA_1|',A16,'|',I3,'|',A26,A6,'TYPE=|',A4,
     &     '|LONMAX=|',I6,'|LONUTI=|',I6,'|',A4)
 1003 FORMAT ('AJACOT|PRCATA_1|',A16,'|',I3,'|',A26,A6,'TYPE=|',A4,
     &     '|NOMMAX=|',I6,'|NOMUTI=|',I6,'|',A4)
      END
