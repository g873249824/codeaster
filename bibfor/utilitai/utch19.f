C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C     EXTRAIRE UNE VALEUR DANS UN CHAM_ELEM.
C ----------------------------------------------------------------------
C IN  : CHAM19 : NOM DU CHAM_ELEM DONT ON DESIRE EXTRAIRE 1 COMPOSANTE
C IN  : NOMMA  : NOM DU MAILLAGE
C IN  : NOMAIL : NOM DE LA MAILLE A EXTRAIRE
C IN  : NONOEU : NOM D'UN NOEUD (POUR LES CHAM_ELEM "AUX NOEUDS").
C                  (SI CE NOM EST BLANC : ON UTILISE NUPO)
C IN  : NUPO   : NUMERO DU POINT A EXTRAIRE SUR LA MAILLE NOMAIL
C IN  : NUSP   : NUMERO DU SOUS_POINT A TESTER SUR LA MAILLE NOMAIL
C                (SI NUSP=0 : IL N'Y A PAS DE SOUS-POINT)
C IN  : IVARI  : NUMERO DE LA CMP (POUR VARI_R)
C IN  : NOCMP : NOM DE LA CMP A EXTRAIRE SUR LE POINT NUPO
C IN  : TYPRES : TYPE DU CHAMP ET DU RESULTAT (R/C).
C OUT : VALR   : VALEUR EXTRAITE (SI REEL)
C OUT : VALC   : VALEUR EXTRAITE (SI COMPLEXE)
C OUT : VALI   : VALEUR EXTRAITE (SI ENTIER)
C OUT : IER    : CODE RETOUR.
C ----------------------------------------------------------------------

      INTEGER IBID,ICMP,JCELV
      REAL*8 R8VIDE,R1,R2
      COMPLEX*16 CBID
      CHARACTER*1 TYPREZ
      CHARACTER*4 TYPE ,KMPIC
      CHARACTER*19 CHM19Z
C     ------------------------------------------------------------------

      CALL JEMARQ()
      IER = 0

      CHM19Z = CHAM19(1:19)
      TYPREZ = TYPRES(1:1)
      CALL JELIRA(CHM19Z//'.CELV','TYPE',IBID,TYPE)

      CALL ASSERT(TYPE.EQ.TYPREZ)
      CALL DISMOI('F','MPI_COMPLET',CHAM19,'CHAM_ELEM',IBID,KMPIC,IBID)
      CALL ASSERT(KMPIC.EQ.'OUI'.OR.KMPIC.EQ.'NON')

      IF (TYPE.NE.'R' .AND. TYPE.NE.'C' .AND. TYPE.NE.'I')
     &  CALL U2MESK('E','UTILITAI5_29',1,TYPE)

      CALL UTCHDL(CHAM19,NOMMA,NOMAIL,NONOEU,NUPO,NUSP,IVARI,NOCMP,ICMP)

C     SI TEST_RESU, ICMP PEUT ETRE = 0 :
      IF (ICMP.EQ.0) THEN
         IER=1
         VALR=R8VIDE()
         VALC=DCMPLX(R8VIDE(),R8VIDE())
         GO TO 10
      END IF

      CALL JEVEUO(CHM19Z//'.CELV','L',JCELV)
      IF (TYPREZ.EQ.'R') THEN
        VALR = ZR(JCELV-1+ICMP)
      ELSE IF (TYPREZ.EQ.'C') THEN
        VALC = ZC(JCELV-1+ICMP)
      ELSE IF (TYPREZ.EQ.'I') THEN
        VALI = ZI(JCELV-1+ICMP)
      END IF

C     -- SI LE CHAMP N'EST PAS MPI_COMPLET, IL FAUT COMMUNIQUER
C        LA VALEUR EXTRAITE :
      IF (KMPIC.EQ.'NON') THEN
        IF (TYPREZ.EQ.'R') THEN
          CALL MPICM1('MPI_SUM','R',1,IBID,IBID,VALR,CBID)
        ELSE IF (TYPREZ.EQ.'C') THEN
          R1=DBLE(VALC)
          R2=DIMAG(VALC)
          CALL MPICM1('MPI_SUM','R',1,IBID,IBID,R1,CBID)
          CALL MPICM1('MPI_SUM','R',1,IBID,IBID,R2,CBID)
          VALC=DCMPLX(R1,R2)
        ENDIF
      ENDIF

   10 CONTINUE
      CALL JEDEMA()
      END
