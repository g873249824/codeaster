      IMPLICIT      NONE
      INCLUDE       'jeveux.h'

      CHARACTER*24  SDLIST
      CHARACTER*(*) TYPSTZ
      CHARACTER*(*) IDNPAZ
      INTEGER       NBSTRU
C
C ----------------------------------------------------------------------
C
C ROUTINE UTILITAIRE (GESTION STRUCT - LISTE DE STRUCTS)
C
C CREATION
C
C ----------------------------------------------------------------------
C
C
C IN  SDLIST : NOM DE LA LISTE
C IN  TYPSTR : TYPE DES STRUCT DE LA LISTE
C IN  IDNPAR : NOM DU PARAMETRE IDENTIFIANT LE STRUCT
C IN  NBSTRU : NOMBRE DE STRUCTS DANS LA LISTE
C
C ----------------------------------------------------------------------
C
      CHARACTER*24 LISNOM,LISACT
      INTEGER      JLISAC
      CHARACTER*24 TYPSTR,IDNPAR
      CHARACTER*8  K8BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      TYPSTR = TYPSTZ
      IDNPAR = IDNPAZ
C
C --- LISTE DE LISTES INTERDIT
C
      IF (TYPSTR.EQ.'LISTE_STRUCT') THEN
        CALL ASSERT(.FALSE.)
      ENDIF
      CALL ASSERT(NBSTRU.GT.0)
C
C --- LISTE DES NOMS DE STRUCTS
C
      LISNOM = SDLIST(1:19)//'.NOMS'
      CALL JECREC(LISNOM,'V V K24','NOM','CONTIG',
     &             'CONSTANT',NBSTRU)
      CALL JEECRA(LISNOM,'LONMAX',1,K8BID)
C
C --- LISTE DES STRUCTS ACTIFS
C
      LISACT = SDLIST(1:19)//'.ACTI'
      CALL WKVECT(LISACT,'V V I  ',NBSTRU,JLISAC)
C
C --- CREATION STRUCTS
C
      CALL OBCREA('LISTE_STRUCTS',SDLIST)
C
C --- VALEURS DES PARAMETRES
C
      CALL OBSETI(SDLIST,'NBRE_STRUCTS'    ,NBSTRU)
      CALL OBSETO(SDLIST,'NOM_STRUCTS'     ,LISNOM)
      CALL OBSETO(SDLIST,'ACT_STRUCTS'     ,LISACT)
      CALL OBSETK(SDLIST,'TYPE_STRUCTS'    ,TYPSTR)
      CALL OBSETK(SDLIST,'PARA_IDEN_STRUCT',IDNPAR)
C
      CALL JEDEMA()
      END
