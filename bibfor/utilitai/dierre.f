      INTEGER      ITERAT
      CHARACTER*19 PARTPS, CRITNL

C ----------------------------------------------------------------------
C        SD DISCRETISATION :   ENREGISTRE LES ERREURS A ITERAT
C ----------------------------------------------------------------------
C
C  IN PARTPS K19 : SD DISCRETISATION
C  IN CRITNL K19 : SD CRITERE
C  IN ITERAT  I  : NUMERO D'INSTANTS
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------

      INTEGER      IRET,JCRR,JERRE,XXITER
C ----------------------------------------------------------------------

      CALL JEMARQ()

C --- ENREGISTRE LES ERREURS A CETTE ITERATION
      CALL JEEXIN(CRITNL //'.CRTR',IRET)
      IF (IRET.EQ.0) GOTO 9999

      CALL JEVEUO(CRITNL // '.CRTR','L',JCRR)
C     STOCKAGE DES INFOS SUR LA CONVERGENCE
      CALL JEVEUO(PARTPS // '.ERRE','E',JERRE)
      XXITER = NINT(ZR(JERRE+1))
      IF ( XXITER .NE. 0 ) THEN
        IF ( ITERAT .GT. XXITER+1) THEN
          CALL U2MESS('F','UTILITAI_48')
        ENDIF
        ZR(JERRE + 4 + ITERAT*2     ) = ZR(JCRR+2)
        ZR(JERRE + 4 + ITERAT*2 + 1 ) = ZR(JCRR+3)
      ENDIF

9999  CONTINUE
      CALL JEDEMA()
      END
