C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*19  SDDYNA
      CHARACTER*(*) CHAINE
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (UTILITAIRE)
C
C INTERROGE SDDYNA POUR RENVOYER UNE CHAINE
C      
C ----------------------------------------------------------------------
C
C
C OUT NDYNKK : NOM DE LA SD DEDIEE 
C               - AU MULTI_APPUI OU
C               - A L'AMORTISSEMENT MODAL OU
C               - A LA PROJECTION MODALE
C IN  SDDYNA : NOM DE LA SD DEDIEE A LA DYNAMIQUE
C IN  CHAINE :  = / 'MULTI_APPUI' 
C                 / 'AMOR_MODAL'
C                 / 'PROJ_MODAL'
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24 NOSD ,MUAP ,HHTC ,TCHA  
      INTEGER      JNOSD,JMUAP,JHHTC,JTCHA  
      CHARACTER*19 KAMMO
      LOGICAL      NDYNLO,LDYNA      
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      LDYNA  = NDYNLO(SDDYNA,'DYNAMIQUE')
      NDYNKK = ' '      
C
C --- ACCES AUX OBJETS DE LA SD SDDYNA
C
      IF (LDYNA) THEN
        NOSD = SDDYNA(1:15)//'.NOM_SD'
        MUAP = SDDYNA(1:15)//'.MULT_APP'
        HHTC = SDDYNA(1:15)//'.HHT_COMP'  
        TCHA = SDDYNA(1:15)//'.TYPE_CHA'          
        CALL JEVEUO(NOSD,'L',JNOSD) 
        CALL JEVEUO(MUAP,'L',JMUAP) 
        CALL JEVEUO(HHTC,'L',JHHTC) 
        CALL JEVEUO(TCHA,'L',JTCHA) 
      ELSE
        GOTO 999
      ENDIF    
C
      IF(CHAINE(1:11).EQ.'MULTI_APPUI')THEN
        NDYNKK = ZK24(JNOSD+1-1)
      ELSEIF(CHAINE(1:10).EQ.'AMOR_MODAL')THEN
        NDYNKK = ZK24(JNOSD+2-1)
      ELSEIF(CHAINE(1:10).EQ.'PROJ_MODAL')THEN
        NDYNKK = ZK24(JNOSD+3-1)
      ELSEIF(CHAINE(1:6).EQ.'DEPENT')THEN
        NDYNKK = ZK24(JMUAP+1-1)        
      ELSEIF(CHAINE(1:6).EQ.'VITENT')THEN
        NDYNKK = ZK24(JMUAP+2-1) 
      ELSEIF(CHAINE(1:6).EQ.'ACCENT')THEN
        NDYNKK = ZK24(JMUAP+3-1)    
      ELSEIF(CHAINE(1:6).EQ.'DEPENM')THEN
        NDYNKK = ZK24(JMUAP+4-1)        
      ELSEIF(CHAINE(1:6).EQ.'VITENM')THEN
        NDYNKK = ZK24(JMUAP+5-1) 
      ELSEIF(CHAINE(1:6).EQ.'ACCENM')THEN
        NDYNKK = ZK24(JMUAP+6-1)  
      ELSEIF(CHAINE(1:6).EQ.'FEDOLD')THEN
        NDYNKK = ZK24(JHHTC+1-1)          
      ELSEIF(CHAINE(1:6).EQ.'FEPILD')THEN
        NDYNKK = ZK24(JHHTC+2-1)  
      ELSEIF(CHAINE(1:6).EQ.'DIDOLD')THEN
        NDYNKK = ZK24(JHHTC+3-1)
      ELSEIF(CHAINE(1:6).EQ.'DIPILD')THEN
        NDYNKK = ZK24(JHHTC+4-1)
      ELSEIF(CHAINE(1:6).EQ.'FSDOLD')THEN
        NDYNKK = ZK24(JHHTC+5-1)
      ELSEIF(CHAINE(1:6).EQ.'FSPILD')THEN
        NDYNKK = ZK24(JHHTC+6-1)
      ELSEIF(CHAINE(1:6).EQ.'DIDILD')THEN
        NDYNKK = ZK24(JHHTC+7-1)
      ELSEIF(CHAINE(1:6).EQ.'CINELD')THEN
        NDYNKK = ZK24(JHHTC+8-1)
      ELSEIF(CHAINE(1:6).EQ.'CNFOLD')THEN
        NDYNKK = ZK24(JHHTC+9-1)  
      ELSEIF(CHAINE(1:6).EQ.'CHONDP')THEN
        NDYNKK = ZK24(JTCHA+1-1) 
      ELSEIF(CHAINE(1:6).EQ.'CHGRFL')THEN
        NDYNKK = ZK24(JTCHA+2-1) 
      ELSEIF(CHAINE(1:6).EQ.'VALMOD')THEN
        KAMMO  = ZK24(JNOSD+2-1)(1:19)
        NDYNKK = KAMMO(1:19)//'.VALM' 
      ELSEIF(CHAINE(1:6).EQ.'BASMOD')THEN
        KAMMO  = ZK24(JNOSD+2-1)(1:19)
        NDYNKK = KAMMO(1:19)//'.BASM'
      ELSEIF(CHAINE(1:6).EQ.'CNINER')THEN
        NDYNKK = ZK24(JTCHA+3-1)        
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
 999  CONTINUE      
C
      CALL JEDEMA()

      END
