C    ON RETOURNE UN LOGIQUE VALANT VRAI OU FAUX SELON LE CAS ET LE NOM
C    DE LA MODELISATION EFFECTIVE
C    IL Y A ERREUR FATALE SI ON NE REUSSIT PAS A DECODER LE MODELE
C
