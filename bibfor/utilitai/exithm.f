C    IL FAUDRA FAIRE AUTREMENT. (SYMPA COMME CONSEIL)
C
C     ARGUMENTS:
C     ----------
C IN   MODELE : MODELE DU CALCUL
C OUT  YATHM  : VRAI, SI LA MODELISATION EST UNE MODELISATION THM
C               FAUX, SINON
C ......................................................................
C
