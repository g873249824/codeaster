      SUBROUTINE RSUTN1 ( RESU, NOPARA, MOTCLE, IOCC, OBJVEU, NBORDR )
      IMPLICIT   NONE
      INTEGER          IOCC, NBORDR
      CHARACTER*16     NOPARA
      CHARACTER*(*)    RESU, MOTCLE, OBJVEU
C     ------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF UTILITAI  DATE 21/09/2011   AUTEUR COURTOIS M.COURTOIS 
C ======================================================================
