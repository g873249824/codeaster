C      CORRESPONDANCE NUMERO D'ORDRE UTILISATEUR (IORDR) AVEC LE
C      NUMERO DE RANGEMENT (IORDG).
C ----------------------------------------------------------------------
C IN  : NOMSD  : NOM DE LA STRUCTURE "RESULTAT"
C IN  : IORDG  : NUMERO DE RANGEMENT.
C OUT : IORDR  : NUMERO D'ORDRE UTILISATEUR.
C OUT : IERR   : CODE RETOUR
C                = 0  , LA RELATION EXISTE .
