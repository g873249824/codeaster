      CHARACTER*19 NOMD2
      CHARACTER*1 K1BID
C ----------------------------------------------------------------------

C-----------------------------------------------------------------------
      INTEGER JORDR ,NBORDR
C-----------------------------------------------------------------------
      CALL JEMARQ()
      IERR = 0
      NOMD2 = NOMSD

C     --- RECUPERATION DU .ORDR ---
      CALL JELIRA(NOMD2//'.ORDR','LONUTI',NBORDR,K1BID)
      IF (NBORDR.EQ.0) THEN
        IERR = 10
      ELSE IF (IORDG.GT.NBORDR) THEN
        IERR = 20
      ELSE
        CALL JEVEUO(NOMD2//'.ORDR','L',JORDR)
        IORDR = ZI(JORDR+IORDG-1)
      END IF

      CALL JEDEMA()
      END
