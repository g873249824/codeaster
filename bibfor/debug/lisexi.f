C
C ----------------------------------------------------------------------
C
C ROUTINE UTILITAIRE (LISTE_CHARGES)
C
C RETOURNE TRUE SI LE CHARGEMENT EXISTE
C
C ----------------------------------------------------------------------
C
C
C IN  PREFOB : PREFIXE DE LA CHARGE
C IN  INDXCH : INDEX DE LA CHARGE
C
C
C
C
      CHARACTER*24 NOMOBJ
      INTEGER      ITYPOB
      CHARACTER*19 CARTE
      INTEGER      IRET
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      LISEXI = .FALSE.
C
C --- RECUPERATION OBJET LIE A CETTE CHARGE
C
      CALL LISDEF('OBJE',PREFOB,INDXCH,NOMOBJ,ITYPOB)
C
C --- VERIFICATION EXISTENCE
C
      IF (ITYPOB.EQ.1) THEN
        CARTE = NOMOBJ(1:19)
        CALL EXISD('CARTE',CARTE,IRET)
        IF (IRET.EQ.1) LISEXI = .TRUE.
      ELSEIF (ITYPOB.EQ.0) THEN
        CALL JEEXIN(NOMOBJ,IRET)
        IF (IRET.NE.0) LISEXI = .TRUE.
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
      CALL JEDEMA()
      END
