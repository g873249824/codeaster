C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      INTEGER      IFM
      INTEGER      NUMMAE                   
      CHARACTER*8  NOMA
      INTEGER      IPC,NTPC
      INTEGER      XAINI,XSINI
      INTEGER      XA,XS
      LOGICAL      LVITES,LGLISS,LCOMPL
      REAL*8       JEU,JEUVIT,LAMBDC
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE - IMPRESSIONS)
C
C AFFICHAGE ALGORITHME CONTRAINTES ACTIVES
C      
C ----------------------------------------------------------------------
C
C
C IN  IFM    : UNITE D'IMPRESSION DU MESSAGE
C IN  NOMA   : NOM DU MAILLAGE
C IN  NUMMAE : NUMERO ABSOLU DE LA MAILLE ESCLAVE
C IN  IPC    : NUMERO DU POINT DE CONTACT SUR LA MAILLE ESCLAVE
C IN  NTPC   : NOMBRE TOTAL DE POINTS DE CONTACT SUR LA MAILLE ESCLAVE
C IN  XSINI  : INDICATEUR DE CONTACT SUR LE POINT A L'ETAT INITIAL
C IN  XAINI  : INDICATEUR DE COMPLIANCE SUR LE POINT A L'ETAT INITIAL
C IN  XS     : INDICATEUR DE CONTACT SUR LE POINT A L'ETAT FINAL
C IN  XA     : INDICATEUR DE COMPLIANCE SUR LE POINT A L'ETAT FINAL
C IN  LVITES : SI FORMULATION EN VITESSE
C IN  LGLISS : SI CONTACT GLISSIERE
C IN  LCOMPL : SI FORMULATION EN COMPLIANCE
C IN  JEU    : JEU 
C IN  JEUVIT : GAP VITESSE
C IN  LAMBDC : LAGRANGE DE CONTACT (PRESSION DE CONTACT)
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*32 JEXNUM
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C 
      CHARACTER*8  NOMESC     
      CHARACTER*16 STATUT  
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()             
C
C --- REPERAGE MAILLE ESCLAVE
C
      CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NUMMAE),NOMESC)
      WRITE(IFM,1000) NOMESC 
      WRITE(IFM,2000) IPC                
 1000 FORMAT (' <CONTACT>     * LA MAILLE ESCLAVE ',A8) 
 2000 FORMAT (' <CONTACT>     ** DONT LE POINT DE CONTACT ',I3) 
C
C --- PROPRIETES (FORMULATION)
C
      IF (LVITES) THEN
        WRITE(IFM,1001)
      ELSE
        WRITE(IFM,1002)      
      ENDIF
 1001 FORMAT (' <CONTACT>        (FORMULATION VITESSE)') 
 1002 FORMAT (' <CONTACT>        (FORMULATION DEPLACEMENT)') 
C
C --- PROPRIETES (GLISSIERE)
C 
      IF (LGLISS) THEN
        WRITE(IFM,3001)    
      ENDIF
 3001 FORMAT (' <CONTACT>        (GLISSIERE)') 
C
C --- PROPRIETES (COMPLIANCE)
C  
      IF (LCOMPL) THEN
        WRITE(IFM,4001)
      ELSE
        WRITE(IFM,4002)      
      ENDIF
 4001 FORMAT (' <CONTACT>        (AVEC COMPLIANCE)') 
 4002 FORMAT (' <CONTACT>        (SANS COMPLIANCE)')             
C
C --- ETAT DE CONTACT INITIAL
C                 
      IF (XSINI.EQ.0) THEN
        STATUT = ' PAS EN CONTACT '
      ELSE
        STATUT = ' EN CONTACT     '
      ENDIF
      WRITE(IFM,5000) STATUT         

 5000 FORMAT (' <CONTACT>         -> ETAT DE CONTACT    INITIAL:',A16) 

C
C --- ETAT DE COMPLIANCE INITIAL
C
      IF (LCOMPL) THEN
        IF (XAINI.EQ.0) THEN
          STATUT = ' HORS ASPERITES '
        ELSE
          STATUT = ' DANS ASPERITES '
        ENDIF
        WRITE(IFM,5001) STATUT        
      ENDIF
 5001 FORMAT (' <CONTACT>         -> ETAT DE COMPLIANCE INITIAL:',A16)
C --- PROPRIETES (JEUX)
C
      IF (LVITES) THEN
        WRITE(IFM,6001) JEU,JEUVIT,LAMBDC
      ELSE
        WRITE(IFM,6000) JEU,LAMBDC
      ENDIF
 6000 FORMAT (' <CONTACT>         <> JEU:',E10.3,
     &        ' - LAGR_C :',E10.3) 
 6001 FORMAT (' <CONTACT>         <> JEU:',E10.3,
     &        ' - SAUT VITESSE:',E10.3,
     &        ' - LAGR_C :',E10.3)       
C
C --- ETAT DE CONTACT FINAL
C
      IF (XS.EQ.0) THEN
        STATUT = ' PAS EN CONTACT '
      ELSE
        STATUT = ' EN CONTACT     '
      ENDIF
      WRITE(IFM,7000) STATUT            
 7000 FORMAT (' <CONTACT>         -> ETAT DE CONTACT    FINAL  :',A16) 
C
C --- ETAT DE COMPLIANCE FINAL
C
      IF (LCOMPL) THEN
        IF (XA.EQ.0) THEN
          STATUT = ' HORS ASPERITES '
        ELSE
          STATUT = ' DANS ASPERITES '
        ENDIF
        WRITE(IFM,7001) STATUT        
      ENDIF       
 7001 FORMAT (' <CONTACT>         -> ETAT DE COMPLIANCE FINAL  :',A16)
C
      CALL JEDEMA()
C
      END
