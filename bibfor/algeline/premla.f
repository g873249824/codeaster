      CALL INFNIV(IFM,NIV)
      IER=0
      IF(NRL.NE.0) THEN

C     AVEC RELATION LINEAIRE
C     CALCUL DES LISTES DE NOEUDS A AJOUTER
         DO 190 I=1,NEQ
            DEB(I) =0
 190     CONTINUE
         ILLIST = 0
         DO 200 K = 1 , NRL
            LBD2 = RL(2,K)
            J1 = DIAG(LBD2-1) + 2
            J2 = DIAG(LBD2) - 1
            DO 210 J = J2 , J1,-1
C     ON AJOUTE COL(J1),..., COL(J-1) AUX VOISINS DE COL(J)
              CALL CALAJT(J1,J,DIAG,COL,NEQ,ILLIST,DEB,VOIS,SUIT,LT,IER)
              IF(IER.GT.0) GOTO 999
 210        CONTINUE

 200     CONTINUE
      ENDIF
 999  CONTINUE
