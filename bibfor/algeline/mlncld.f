      IMPLICIT NONE
      INTEGER N,ADPER(*),AD(*),IER
      REAL*8 EPS
      COMPLEX*16  FRONTL(*),T1(*),FRONTU(*),T2(*)
      INTEGER  I,K
      INTEGER SEUIN,SEUIK
      PARAMETER(SEUIN=1500,SEUIK=300)
      INTEGER NN,KK,LDA
C     VERSION AVEC APPEL A DGEMV POUR LES PRODUITS MATRICE-VECTEUR
C     AU DELA D' UN CERTAIN SEUIL
C     DGEMV EST APPEL A TRAVERS LA FONCTION C DGEMW POUR CAR DGEMV
C     NECESSITE DES ARGUMENTS ENTIER INTEGER*4 REFUSES PAR ASTER
C     MLTFLD TRAITE UNIQUEMENT LES BLOCS DIAGONAUX
      LDA = N
      DO 30 K = 1,N
        DO 10 I = 1,K - 1
          AD(I) = ADPER(I) + K - I        
          T1(I) = FRONTU(AD(I))
          T2(I) = FRONTL(AD(I))
   10   CONTINUE
        IF (K.GT.1) THEN
C
           NN= N-K+1
           KK= K-1
           IF(NN.LT.SEUIN.OR.KK.LT.SEUIK) THEN
          CALL SSPMVC(N-K+1,K-1,FRONTL,AD,T1,FRONTL(ADPER(K)))
          CALL SSPMVC(N-K+1,K-1,FRONTU,AD,T2,FRONTU(ADPER(K)))
          ELSE
          CALL CGEMW(NN,KK,FRONTL(K),LDA,T1,FRONTL(ADPER(K)))
          CALL CGEMW(NN,KK,FRONTU(K),LDA,T2,FRONTU(ADPER(K)))
          ENDIF
       ENDIF
C         DIVISION PAR LE TERME DIAGONAL DE FRONTL (PAS FRONTU)
        IF (ABS(FRONTL(ADPER(K))).LE.EPS) THEN
          IER = K
          GO TO 40
        END IF
CRAY DIR$ IVDEP DIRECTIVE INHIBEE CAR DEPENDANCE AVANT
        DO 20 I = 1,N - K
          FRONTL(ADPER(K)+I) = FRONTL(ADPER(K)+I)/FRONTL(ADPER(K))
   20   CONTINUE
   30 CONTINUE
   40 CONTINUE
      END
