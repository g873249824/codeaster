C ======================================================================
C TOLE CRP_20
C 
C     SUBROUTINE LAPACK APPLIQUANT UN REFLECTEUR ELEMENTAIRE H A UNE
C     MATRICE DONNEE.
C-----------------------------------------------------------------------
C  -- LAPACK AUXILIARY ROUTINE (VERSION 2.0) --
C     UNIV. OF TENNESSEE, UNIV. OF CALIFORNIA BERKELEY, NAG LTD.,
C     COURANT INSTITUTE, ARGONNE NATIONAL LAB, AND RICE UNIVERSITY
C     FEBRUARY 29, 1992
C
C  PURPOSE
C  =======
C
C  DLARFX APPLIES A REAL ELEMENTARY REFLECTOR H TO A REAL M BY N
C  MATRIX C, FROM EITHER THE LEFT OR THE RIGHT. H IS REPRESENTED IN THE
C  FORM
C
C        H = I - TAU * V * V'
C
C  WHERE TAU IS A REAL SCALAR AND V IS A REAL VECTOR.
C
C  IF TAU = 0, THEN H IS TAKEN TO BE THE UNIT MATRIX
C
C  THIS VERSION USES INLINE CODE IF H HAS ORDER < 11.
C
C  ARGUMENTS
C  =========
C
C  SIDE    (INPUT) CHARACTER*1
C          = 'L': FORM  H * C
C          = 'R': FORM  C * H
C
C  M       (INPUT) INTEGER
C          THE NUMBER OF ROWS OF THE MATRIX C.
C
C  N       (INPUT) INTEGER
C          THE NUMBER OF COLUMNS OF THE MATRIX C.
C
C  V       (INPUT) REAL*8 ARRAY, DIMENSION (M) IF SIDE = 'L'
C                                     OR (N) IF SIDE = 'R'
C          THE VECTOR V IN THE REPRESENTATION OF H.
C
C  TAU     (INPUT) REAL*8
C          THE VALUE TAU IN THE REPRESENTATION OF H.
C
C  C       (INPUT/OUTPUT) REAL*8 ARRAY, DIMENSION (LDC,N)
C          ON ENTRY, THE M BY N MATRIX C.
C          ON EXIT, C IS OVERWRITTEN BY THE MATRIX H * C IF SIDE = 'L',
C          OR C * H IF SIDE = 'R'.
C
C  LDC     (INPUT) INTEGER
C          THE LEADING DIMENSION OF THE ARRAY C. LDA >= (1,M).
C
C  WORK    (WORKSPACE) REAL*8 ARRAY, DIMENSION
C                      (N) IF SIDE = 'L'
C                      OR (M) IF SIDE = 'R'
C          WORK IS NOT REFERENCED IF H HAS ORDER < 11.
C
C ASTER INFORMATION
C 14/01/2000 TOILETTAGE DU FORTRAN SUIVANT LES REGLES ASTER,
C            REMPLACEMENT DE 1 RETURN PAR GOTO 1000,
C            MODIFICATION DES APPELS BLAS (ROUTINES ASTER BL...),
C            IMPLICIT NONE.
C INTRINSIC FUNCTIONS
C            NONE.
C-----------------------------------------------------------------------
C CORPS DU PROGRAMME
      IMPLICIT NONE
      
C     .. SCALAR ARGUMENTS ..
      CHARACTER*1        SIDE
      INTEGER            LDC, M, N
      REAL*8   TAU
C     ..
C     .. ARRAY ARGUMENTS ..
      REAL*8   C( LDC, * ), V( * ), WORK( * )
C     ..
C     .. PARAMETERS ..
      REAL*8   ZERO, ONE
      PARAMETER          ( ZERO = 0.0D+0, ONE = 1.0D+0 )
C     ..
C     .. LOCAL SCALARS ..
      INTEGER            J
      REAL*8   SUM, T1, T10, T2, T3, T4, T5, T6, T7, T8, T9,
     &                   V1, V10, V2, V3, V4, V5, V6, V7, V8, V9
C     ..
C     .. EXTERNAL FUNCTIONS ..
      LOGICAL            LSAME
C     ..
C     .. EXECUTABLE STATEMENTS ..
C
      IF( TAU.EQ.ZERO )
     &   GOTO 1000
      IF( LSAME( SIDE, 'L' ) ) THEN
C
C        FORM  H * C, WHERE H HAS ORDER M.
C
         GO TO ( 10, 30, 50, 70, 90, 110, 130, 150,
     &           170, 190 )M
C
C        CODE FOR GENERAL M
C
C        W := C'*V
C
C DUE TO CRP102 CALL BLGEMV('TRANSPOSE',M,N,ONE,C,LDC,V,1,ZERO,WORK,
         CALL BLGEMV( 'T', M, N, ONE, C, LDC, V, 1, ZERO, WORK,
     &               1 )
C
C        C := C - TAU * V * W'
C
         CALL BLSGER( M, N, -TAU, V, 1, WORK, 1, C, LDC )
         GO TO 410
   10    CONTINUE
C
C        SPECIAL CODE FOR 1 X 1 HOUSEHOLDER
C
         T1 = ONE - TAU*V( 1 )*V( 1 )
         DO 20 J = 1, N
            C( 1, J ) = T1*C( 1, J )
   20    CONTINUE
         GO TO 410
   30    CONTINUE
C
C        SPECIAL CODE FOR 2 X 2 HOUSEHOLDER
C
         V1 = V( 1 )
         T1 = TAU*V1
         V2 = V( 2 )
         T2 = TAU*V2
         DO 40 J = 1, N
            SUM = V1*C( 1, J ) + V2*C( 2, J )
            C( 1, J ) = C( 1, J ) - SUM*T1
            C( 2, J ) = C( 2, J ) - SUM*T2
   40    CONTINUE
         GO TO 410
   50    CONTINUE
C
C        SPECIAL CODE FOR 3 X 3 HOUSEHOLDER
C
         V1 = V( 1 )
         T1 = TAU*V1
         V2 = V( 2 )
         T2 = TAU*V2
         V3 = V( 3 )
         T3 = TAU*V3
         DO 60 J = 1, N
            SUM = V1*C( 1, J ) + V2*C( 2, J ) + V3*C( 3, J )
            C( 1, J ) = C( 1, J ) - SUM*T1
            C( 2, J ) = C( 2, J ) - SUM*T2
            C( 3, J ) = C( 3, J ) - SUM*T3
   60    CONTINUE
         GO TO 410
   70    CONTINUE
