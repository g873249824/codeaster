C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C     VERSION COMPLEXE DE MLTF21
C     VERSION MODIFIEE POUR L' APPEL A CGEMV (PRODUITS MATRICE-VECTEUR)
C     LE STOCKAGE DES COLONNES DE LA FACTORISEE EST MODIFIE
      IMPLICIT NONE
      INTEGER P,N,IER
      COMPLEX*16 FRONT(*),T1(*),T2(*),FRN(*)
      REAL*8 EPS
C                                          VARIABLES LOCALES
      INTEGER DIA1,DIA2,J,L,M,R,DIA21,N1
      COMPLEX*16 COEF1
      M = P/2
      R = P - 2*M
      L = N
      DIA2 = -N
      DO 110 J = 1,M
C                                            TRAVAIL SUR LE BLOC TRIANGU
          DIA1 = DIA2 + N + 1
          DIA2 = DIA1 + N + 1
          DIA21 = DIA2 + 1
          L = L - 2
          COEF1 = FRONT(DIA1+1)
          IF (ABS(FRONT(DIA1)).LT.EPS) THEN
              IER = 1
              GO TO 120
          ELSE
              FRONT(DIA1+1) = FRONT(DIA1+1)/FRONT(DIA1)
              FRONT(DIA2) = FRONT(DIA2) - FRONT(DIA1+1)*COEF1
C                                            TRAVAIL SUR LES 2 COLONNES
              CALL CCLNI2(FRONT(DIA1+2),FRONT(DIA21),L,FRONT(DIA1),
     +                    FRONT(DIA2),COEF1,T1,T2,EPS,IER)
C                                            MISE A JOUR DES COLONNES SU
              N1 = P - 2*J
C     MODIFS POUR STOCKAGE CGEMV
              CALL CCL21J(FRONT(DIA1),FRONT(DIA21+N),FRN,J,L,N,N1,T1,T2)
          END IF
  110 CONTINUE
C                                            TRAVAIL SUR LE RESTE DES CO
C                                            DU SUPERNOEUD
      IF (R.EQ.1) THEN
C     MODIFS POUR STOCKAGE CGEMV
         DIA1 = DIA2 + N  + 1
         DIA2 = DIA1 + L
         L = L - 1
         CALL CCLNI1(FRONT(DIA1+1),L,FRONT(DIA1),T1,EPS,IER)
         CALL CCL11J(FRONT(DIA1),FRN,L,T1)
      END IF
  120 CONTINUE
      GOTO 9999
 9999 CONTINUE
      END
