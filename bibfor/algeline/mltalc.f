      IS = SUPND(SNI+1)
      K = ADRESS(SN) + LONGSN
C      DO WHILE (K.LT.ADRESS(SN+1).AND.GLOBAL(K).LT.IS)
  130 CONTINUE
      IF (K.LT.ADRESS(SN+1) .AND. GLOBAL(K).LT.IS) THEN
          NBASS = NBASS + 1
          K = K + 1
          GO TO 130
C FIN DO WHILE
      END IF
      END
