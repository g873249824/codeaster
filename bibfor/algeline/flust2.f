      SUBROUTINE FLUST2 ( MELFLU, TYPFLU, BASE, NOMA, NUOR, AMOR, FREQ,
     &                    MASG, FACT, VITE, NBM, NPV, NIVPAR, NIVDEF )
      IMPLICIT NONE
C-----------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGELINE  DATE 03/07/2012   AUTEUR PELLET J.PELLET 
C ======================================================================
