C EXTRACTION DES NUMEROS DES MAILLES DE TYPE SEG2 DONT L'UNE DES
C EXTREMITES EST UN NOEUD DE NUMERO DONNE
C-----------------------------------------------------------------------
C  IN : NOMA   : NOM DU CONCEPT DE TYPE MAILLAGE
C  IN : NUMNOE : NUMERO DU NOEUD CONSIDERE
C  OUT: NUMANO : LISTE DES NUMEROS DES MAILLES SEG2 DONT L'UNE DES
C                EXTREMITES EST LE NOEUD DE NUMERO NUMNOE (CE VECTEUR
C                EST SURDIMENSIONNE LORS DE SA CREATION PAR L'APPELANT)
C  OUT: NBMANO : NOMBRE DE MAILLES SEG2 DONT L'UNE DES EXTREMITES EST
C                LE NOEUD DE NUMERO NUMNOE
C-----------------------------------------------------------------------
C
      INCLUDE 'jeveux.h'
      CHARACTER*8  NOMA
      INTEGER      NUMNOE,NUMANO(*),NBMANO
C
      CHARACTER*1  K1BID
      CHARACTER*24 MLGNMA,MLGTMA,MLGCNX
C
C-----------------------------------------------------------------------
      CALL JEMARQ()
C
      CALL JENONU(JEXNOM('&CATA.TM.NOMTM','SEG2'),NTSEG)
C
      MLGNMA = NOMA//'.NOMMAI'
      CALL JELIRA(MLGNMA,'NOMMAX',NBMAIL,K1BID)
      MLGTMA = NOMA//'.TYPMAIL'
      CALL JEVEUO(MLGTMA,'L',JDTM)
      MLGCNX = NOMA//'.CONNEX'
C
      NBMANO = 0
      DO 10 NUMAIL = 1,NBMAIL
        NUTYMA = ZI(JDTM+NUMAIL-1)
        IF (NUTYMA.EQ.NTSEG) THEN
          CALL JEVEUO(JEXNUM(MLGCNX,NUMAIL),'L',JDNO)
          NO1 = ZI(JDNO)
          NO2 = ZI(JDNO+1)
          IF (NO1.EQ.NUMNOE .OR. NO2.EQ.NUMNOE) THEN
            NBMANO = NBMANO + 1
            NUMANO(NBMANO) = NUMAIL
          ENDIF
        ENDIF
  10  CONTINUE
C
      CALL JEDEMA()
C
      END
