      CALL JELSTC ('V', '.REFA' , 20 , 100 , LIREFA , NBMAT )
      CALL ASSERT(NBMAT.GE.0)
C
      DO 1,I=1,NBMAT
C
C        ON VERIFIE QUE L'OBJET REPERE EXISTE TOUJOURS
C        IL A EN EFFET PU ETRE DETRUIT DANS LE CAS FETI PAR EXEMPLE
         CALL JEEXIN(LIREFA(I),IER)
         IF (IER.EQ.0) GOTO 1
         MATASS = LIREFA(I)(1:19)
C
C        ON DETRUIT L'EVENTUELLE INSTANCE MUMPS ASSOCIEE A LDLT_SP
         CALL DISMOI('C','SOLVEUR',MATASS,'MATR_ASSE',IBID,SOLVEU,IER)
         IF (IER.EQ.0) THEN
            CALL DETLSP(MATASS,SOLVEU)
         ENDIF
C
C        ON DETRUIT LES MATR_ASSE AINSI QUE LES
C        EVENTUELLES INSTANCES MUMPS ET PETSC
         CALL DETRSD('MATR_ASSE',MATASS)
C
 1    CONTINUE
C
      END
