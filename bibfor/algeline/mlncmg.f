      INTEGER SEUIN,SEUIK
      PARAMETER(SEUIN=1500,SEUIK=300)
      LOGICAL SSP
      LDA = N
      DECAL = ADPER(P+1) - 1
C     CALCUL DE LA PARTIE INFERIEURE
      DO 120 K = P + 1,N
         NN= N-K+1
         SSP = NN.LT.SEUIN.OR.P.LT.SEUIK
             DO 110 I = 1,P
             IF(SSP) THEN
              AD(I) = ADPER(I) + K - I
              T1(I) = FRONTU(AD(I))
           ELSE
              ADS = ADPER(I) + K - I
              T1(I) = FRONTU(ADS)
           ENDIF
  110     CONTINUE
C                                      PRODUIT MATRICE-VECTEUR
C          CALL SSPMVC(N-K+1,P,FRONTL,AD,T1,FRNL(ADPER(K)-DECAL))
           IF(SSP) THEN
              CALL SSPMVC(N-K+1,P,FRONTL,AD,T1,FRNL(ADPER(K)-DECAL))
           ELSE
              CALL CGEMW(NN,P,FRONTL(K),LDA,T1,FRNL(ADPER(K)-DECAL))
           ENDIF
  120 CONTINUE
      END
