C    - FONCTION REALISEE:  PROFILING ALGO FETI POUR SOULAGER ALFETI.F
C     ------------------------------------------------------------------
C     IN  OPTION : IN   : OPTION DE LA ROUTINE
C     IN  TEMPS  : R8   : VECTEUR DE TIMING POUR UTTCPU
C     IN  INFOFE : CH19 : CHAINE DE CHARACTERES POUR MONITORING FETI
