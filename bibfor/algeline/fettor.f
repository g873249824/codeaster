      SUBROUTINE FETTOR(OPTION,INFOFE,RANG,ITEST,NBI,IRR,IR1,JGITGI,
     &                  LRIGID,DIMGI,SDFETI,IPIV,NBSD,IFETF,IFETH,MATAS,
     &                  JGI,LSTOGI,IREX,IPRJ,NBPROC,K24IRG,IRG,IFM,IRP,
     &                  IRZ)
