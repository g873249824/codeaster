C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C-----------------------------------------------------------------------
C    - FONCTION REALISEE:  TEST ORTHOGONALITE DU GCPPC DE ALGO FETI
C                          POUR SOULAGER ALFETI.F
C     ------------------------------------------------------------------
C     IN  OPTION : IN   : OPTION DE LA ROUTINE
C     IN  INFOFE : CH19 : CHAINE DE CHARACTERES POUR MONITORING FETI
C     IN  RANG   : IN   : RANG DU PROCESSEUR
C     IN  ITEST  : IN    : ADRESSE JEVEUX DE &&FETI.TEST'
C----------------------------------------------------------------------
C RESPONSABLE BOITEAU O.BOITEAU
C CORPS DU PROGRAMME
      IMPLICIT NONE

C DECLARATION PARAMETRES D'APPELS
      INTEGER      OPTION,RANG,ITEST,NBI,IRR,IR1,JGITGI,DIMGI,IPIV,NBSD,
     &             IFETF,JGI,IREX,IPRJ,NBPROC,IFETH,IRG,IFM,IRP,IRZ
      LOGICAL      LRIGID,LSTOGI
      CHARACTER*19 SDFETI,MATAS
      CHARACTER*24 INFOFE,K24IRG

C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C DECLARATION VARIABLES LOCALES
      REAL*8       DDOT,RAUX

C CORPS DU PROGRAMME
      CALL JEMARQ()

      IF ((INFOFE(8:8).EQ.'T').AND.(RANG.EQ.0)) THEN      
        IF (OPTION.EQ.1) THEN
C STOCKAGE ANCIENNE DIRECTION DE DESCENTE SI TEST
          CALL FETPRJ(NBI,ZR(IRR),ZR(IR1),JGITGI,LRIGID,DIMGI,
     &                1,SDFETI,IPIV,NBSD,ZI(IFETF),ZI(IFETH),MATAS,
     &             ZR(JGI),LSTOGI,INFOFE,IREX,IPRJ,NBPROC,RANG,K24IRG)
          CALL DCOPY(NBI,ZR(IR1),1,ZR(ITEST),1)
        ELSE IF (OPTION.EQ.2) THEN
C TEST 1 ORTHOGONALITE DU GCPPC     
          RAUX=DDOT(NBI,ZR(IRG),1,ZR(ITEST),1)
          WRITE(IFM,*)'RANG ',RANG,' TEST <PRI,PRI-1>',RAUX
          RAUX=DDOT(NBI,ZR(IRG),1,ZR(IRP),1)
          WRITE(IFM,*)'RANG ',RANG,' TEST <PRI,DI-1>',RAUX
        ELSE IF (OPTION.EQ.3) THEN
C TEST 2 ORTHOGONALITE DU GCPPC
          RAUX=DDOT(NBI,ZR(IRZ),1,ZR(IRP),1)
          WRITE(IFM,*)'RANG ',RANG,' TEST <DI,FI*DI-1>',RAUX
        ELSE
          CALL UTMESS('F','FETTOR','OPTION DE CALCUL NON PREVUE !')     
        ENDIF
      ENDIF

      CALL JEDEMA()
      END
