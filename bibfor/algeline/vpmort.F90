! ======================================================================
!     M-ORTHOGONALISATION DU VECTEUR X AVEC LES PRECEDENTS
!     ------------------------------------------------------------------
    real(kind=8) :: r8val, r8norm
    integer :: ieq, iprec
!     ------------------------------------------------------------------
    do 10 iprec = 1, imode - 1
        r8val = 0.d0
        r8norm = 0.d0
        do 20 ieq = 1, neq
            r8val = r8val + x(ieq) * my(ieq,iprec)
            r8norm = r8norm + y(ieq,iprec) * my(ieq,iprec)
20      continue
        r8val = -r8val/r8norm
        do 30 ieq = 1, neq
            x(ieq) = x(ieq) + r8val * y(ieq,iprec)
30      continue
10  end do
end subroutine
