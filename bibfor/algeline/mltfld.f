      IMPLICIT NONE
      INTEGER N,ADPER(*),AD(*),IER
      REAL*8 FRONT(*),T1(*),EPS,ALPHA,BETA
      INTEGER  I,K
      INTEGER SEUIN,SEUIK
C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
      PARAMETER(SEUIN=1500,SEUIK=300)
      INTEGER NN,KK,LDA,INCX,INCY
      CHARACTER*1 TRA
C
C     MLTFLD TRAITE UNIQUEMENT LES BLOCS DIAGONAUX
C
      LDA = N
      TRA='N'
      ALPHA=-1.D0
      BETA=1.D0
      INCX=1
      INCY=1
      DO 30 K = 1,N
        DO 10 I = 1,K - 1
          AD(I) = ADPER(I) + K - I
          T1(I) = FRONT(AD(I))*FRONT(ADPER(I))
   10   CONTINUE
        IF (K.GT.1) THEN
C
           NN= N-K+1
           KK= K-1
           IF(NN.LT.SEUIN.OR.KK.LT.SEUIK) THEN
          CALL SSPMVB(N-K+1,K-1,FRONT,AD,T1,FRONT(ADPER(K)))
          ELSE
          CALL DGEMV(TRA,NN,KK,ALPHA,FRONT(K),LDA,T1,INCX,BETA,
     &               FRONT(ADPER(K)),INCY)
          ENDIF
       ENDIF
C         DIVISION PAR LE TERME DIAGONAL
        IF (ABS(FRONT(ADPER(K))).LE.EPS) THEN
          IER = K
          GO TO 40
        END IF
CRAY DIR$ IVDEP DIRECTIVE INHIBEE CAR DEPENDANCE AVANT
        DO 20 I = 1,N - K
          FRONT(ADPER(K)+I) = FRONT(ADPER(K)+I)/FRONT(ADPER(K))
   20   CONTINUE
   30 CONTINUE
   40 CONTINUE
      END
