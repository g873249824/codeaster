      IMPLICIT NONE
      INTEGER ANDI,XADJ(*),NDI,CHAINE(*),NOUV(*),ADJNCY(*)
      INTEGER PLACE(*)
C     INSERTION DANS LA CHAINE,
C     DES VOISINS DE NDI,(DE NUMERO SUPERIEUR)
C     QUI NE SONT PAS ENCORE DANS LA CHAINE (PLACE(NDJ)=0)
      INTEGER J,SUIV,COUR,NDJ
      INTEGER DEBUT

      DO 150 J = XADJ(ANDI),XADJ(ANDI+1) - 1
         NDJ = NOUV(ADJNCY(J))
         IF (NDJ.GT.NDI) THEN
            IF(PLACE(NDJ).EQ.0) THEN
               SUIV= DEBUT
 145           CONTINUE
               IF(SUIV.LT.NDJ) THEN
                  COUR = SUIV
                  SUIV = CHAINE(COUR)
                  GOTO 145
               ENDIF
               IF( SUIV.GT.NDJ) THEN
                  CHAINE(COUR) = NDJ
                  CHAINE(NDJ) = SUIV
                  PLACE(NDJ)=1
               ENDIF
            ENDIF
         END IF
 150        CONTINUE
            END
