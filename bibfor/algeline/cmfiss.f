      SUBROUTINE CMFISS(MAIN, GNO1, GNO2, PREFIX, MAINIT, NOMGMA,
     &                  NOMA, CONNEC, TYMA, NGMA, GPMA)

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGELINE  DATE 18/12/2012   AUTEUR SELLENET N.SELLENET 
C ======================================================================
