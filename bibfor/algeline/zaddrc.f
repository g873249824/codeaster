C-----------------------------------------------------------------------
C    CALCUL DE ALPHA*CONJG(Y)'
C-----------------------------------------------------------------------
C IN  : M    : NOMBRE DE LIGNE DE A.
C     : N    : NOMBRE DE COLONNE DE A.
C     : ALPHA: COMPLEXE.
C     : X    : VECTEUR COMPLEXE DE LONGUEUR (M-1)*IABS(INCX)+1.
C     : INCX : DEPLACEMENT ENTRE LES ELEMENTS DE X.
C     : Y    : VECTEUR COMPLEXE DE LONGUEUR (N-1)*IABS(INCY)+1.
C     : INCY : DEPLACEMENT ENTRE LES ELEMENTS DE Y.
C I/O : A    : MATRICE COMPLEXE DE DIMENSION M*N.
C IN  : LDA  : DIMENSION DE A.
C-----------------------------------------------------------------------
      INTEGER    IY, J
      IF (M.EQ.0 .OR. N.EQ.0 .OR. ALPHA.EQ.(0.0D0,0.0D0)) GO TO 9000
C
      IY = 1
      IF (INCY .LT. 0) IY = (-N+1)*INCY + 1
C
      I1X = 1
      DO 130  J=1, N
         CALL ZAXPY (M, ALPHA*DCONJG(Y(IY)), X, INCX, A(I1X), 1)
         IY = IY + INCY
         I1X = I1X + LDA
  130 CONTINUE
C
 9000 CONTINUE
      GOTO 9999
 9999 CONTINUE
      END
