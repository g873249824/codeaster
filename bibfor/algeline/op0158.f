C     -----  FIN  COMMUNS NORMALISES  JEVEUX  --------------------------

      CHARACTER*19 MASS
      CHARACTER*16 CONCEP,NOMCMD
      CHARACTER*8  MATASS,MATFAC
      INTEGER      JREFA

      CHARACTER*16 METRES
      INTEGER IBID,IFM
      INTEGER NIV
      INTEGER      IARG
C     ------------------------------------------------------------------
      CALL JEMARQ()

      CALL INFMAJ
      CALL INFNIV(IFM,NIV)

      CALL GETRES(MATFAC,CONCEP,NOMCMD)
      CALL GETVID('  ','MATR_ASSE',0,IARG,1,MATASS,IBID)
      CALL ASSERT(MATASS.EQ.MATFAC)
      MASS = MATASS
      CALL DISMOI('F','METH_RESO',MASS,'MATR_ASSE',IBID,METRES,IBID)

      IF (METRES.NE.'LDLT'.AND.METRES.NE.'MULT_FRONT'
     &                    .AND.METRES.NE.'MUMPS') THEN
         CALL U2MESS ('F','ALGELINE4_1')
      ENDIF

      CALL JEVEUO(MASS//'.REFA','L',JREFA)
      IF (ZK24(JREFA-1+3).EQ.'ELIML') CALL MTMCHC(MASS,'ELIMF')
      CALL ASSERT(ZK24(JREFA-1+3).NE.'ELIML')


      CALL TITRE
      CALL JEDEMA()
      END
