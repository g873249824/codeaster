C   TRANSFORMER UNE MATR_ASSE SYMETRIQUE EN UNE MATR_ASSE NON-SYMETRIQUE
C   POUR CELA, ON DUPPLIQUE LES VALEURS DE LA MOITIE SUPERIEURE
C
C  ARGUMENT :
C    MATAS K19  JXVAR : MATR_ASSE A RENDRE NON-SYMETRIQUE
