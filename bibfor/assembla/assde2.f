C ----------------------------------------------------------------------
C    PENDANT DE ASSDE1, ROUTINE DE NETTOYAGE ADAPTEE A FETI POUR LIMITER
C     LES STOCKAGE D'INFO DANS LES SD RESULTATS.
C     IN:
C       NOMU   : NOM D'UN CONCEPT DE TYPE
C                    CHAMP_GD(K19)

C     RESULTAT:
C     ON DETRUIT TOUS LES OBJETS JEVEUX FILS AU SENS FETI DU TERME
C     (C'EST A DIRE POINTES PAR UN .FETC) CORRESPONDANT A CE CONCEPT.
C    LE CONCEPT INITIAL EST LAISSE INTACT SAUF SON .FETC QUI EST AUSSI
C    DETRUIT.
C ----------------------------------------------------------------------


      INTEGER      IRET,IDD,NBSD,IFETC,ILIMPI
      CHARACTER*8  K8BID
      CHARACTER*24 K24B,K24B1
      CHARACTER*19 CHAMP2
C -DEB------------------------------------------------------------------
      CHAMP2 = CHAMP(1:19)

C DESTRUCTION DE LA LISTE DE CHAM_NO LOCAUX SI FETI
      K24B=CHAMP2//'.FETC'
      CALL JEEXIN(K24B,IRET)
C FETI OR NOT ?
      IF (IRET.NE.0) THEN
        CALL JELIRA(K24B,'LONMAX',NBSD,K8BID)
        CALL JEVEUO(K24B,'L',IFETC)
        CALL JEVEUO('&FETI.LISTE.SD.MPI','L',ILIMPI)
        DO 5 IDD=1,NBSD
          IF (ZI(ILIMPI+IDD).EQ.1) THEN
            K24B1=ZK24(IFETC+IDD-1)
            CALL ASSDE1(K24B1)
          ENDIF
   5    CONTINUE
        CALL JEDETR(K24B)
      ENDIF

      END
