C       20/11/03 (OB): MODIF POUR SOLVEUR FETI.
C----------------------------------------------------------------------



      CHARACTER*24 METRES
      INTEGER ISLVK,ISLVR

C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
      NU = NUZ
      SOLVEU = SOLVEZ

      CALL JEMARQ()
      CALL JEVEUO(SOLVEU//'.SLVK','L',ISLVK)
      METRES = ZK24(ISLVK)
      CALL JEVEUO(SOLVEU//'.SLVR','L',ISLVR)

      IF (METRES.NE.'FETI') THEN
        CALL PROMOR(NU,BASE)
      ELSE
C       RIEN, PAS DE STRUCTURE DE STOCKAGE.
C       LE .FETN A DEJA ETE CONSTITUE DANS NUMERO.F
      END IF

      CALL JEDEMA()
      END
