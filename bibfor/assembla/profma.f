      CHARACTER*14 NU
      CHARACTER*19 SOLVEU
      CHARACTER*1 BASE
      CHARACTER*(*) NUZ,SOLVEZ
C ----------------------------------------------------------------------
C     CONSTRUCTION DU PROFIL LIGNE DE CIEL OU MORSE DE LA MATRICE
C     A PARTIR DE LA NUMEROTATION

C IN K24 NUZ     : NOM DE LA NUMEROTATION
