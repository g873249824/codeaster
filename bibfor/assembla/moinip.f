      SUBROUTINE MOINIP(NCH,NCOEF,IICH,IISUIV,ILIG,ILIG2)
      IMPLICIT NONE
      INTEGER IICH(*),IISUIV(*),ILIG(*)
      INTEGER ILIG2(1),NCH,NCOEF,II2,J,II1
C     ------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ASSEMBLA  DATE 18/04/2005   AUTEUR VABHHTS J.PELLET 
C ======================================================================
