      CHARACTER*8 PART1
      CHARACTER*19 VECEL
      CHARACTER*24 VALK(5)
      INTEGER I,IBID


      PARTIT=' '
      DO 10 I = 1,NBVEC
          VECEL =   TLIVEC(I)
          CALL DISMOI('F','PARTITION',VECEL,'VECT_ELEM',IBID,PART1,IBID)
          IF (PARTIT.EQ.' '.AND. PART1.NE.' ') PARTIT=PART1
          IF (PARTIT.NE.' '.AND. PART1.EQ.' ') GOTO 10
          IF (PARTIT.NE.' '.AND. PARTIT.NE.PART1) THEN
             VALK(1)=PARTIT
             VALK(2)=PART1
             CALL U2MESK('F','CALCULEL_10',2,VALK)
          ENDIF
   10 CONTINUE
      END
