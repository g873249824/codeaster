#ifndef _DISABLE_HDF5
        IRET=1
#endif
      ELSE IF (LIBRAI.EQ.'MED') THEN
#ifndef _DISABLE_MED
        IRET=1
#endif
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
      END

