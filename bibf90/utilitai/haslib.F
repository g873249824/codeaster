      IRET=0
      IF (LIBRAI.EQ.'MUMPS') THEN
#ifdef _HAVE_MUMPS
        IRET=1
#endif
      ELSE IF (LIBRAI.EQ.'PETSC') THEN
#ifdef _HAVE_PETSC
        IRET=1
#endif
