#ifdef _DEBUG_MED
      INTEGER IC
#endif
      MDNONT = -1
      MDNOIT = -1
      MDNODT = -1.D0
      FID4 = FID
      ALLOCATE ( CONN4(CSIZE) )
      CALL CONVI4(CONN, CONN4, CSIZE)
      TYPEN4 = TYPENT
      TYPGE4 = TYPGEO
      TYPCO4 = TYPCON
      N4 = N
      SWITC4 = SWITCH
      MDNON4 = MDNONT
      MDNOI4 = MDNOIT
