      WRITE(6,*)'CONN4=',(CONN4(IC),IC=1,MIN(12,CSIZE))
      PRINT *,'SWITCH=',SWITC4,' N=',N4,' TYPENT=',TYPEN4
      PRINT *,'TYPGEO=',TYPGE4,' TYPCON=',TYPCO4
#endif
      CALL MMHCYW (FID4, MAA, MDNON4, MDNOI4, MDNODT, TYPEN4,
     &             TYPGE4, TYPCO4, SWITC4, N4, CONN4, CRET4)
      CRET = CRET4
      DEALLOCATE (CONN4)
#else
      MDNONT = -1
      MDNOIT = -1
      MDNODT = -1.D0
      CALL MMHCYW (FID, MAA, MDNONT, MDNOIT, MDNODT, TYPENT,
     &             TYPGEO, TYPCON, SWITCH, N, CONN, CRET)
#endif
! END MED
#endif
      END
