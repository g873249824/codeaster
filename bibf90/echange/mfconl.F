     &                  TYPCO4,SWITC4,CONN4,CRET4)
      CALL CONVI4(CONN, CONN4, -CSIZE)
      CRET = CRET4
      DEALLOCATE (CONN4)
#ifdef _DEBUG_MED
      WRITE(6,*) 'CONN(1..12)=',(CONN(IC),IC=1,MIN(12,CSIZE))
      PRINT *,'CRET=',CRET
#endif
#else
      MDNONT = -1
      MDNOIT = -1
      CALL MMHCYR(FID,MAA,MDNONT,MDNOIT,TYPENT,TYPGEO,
     &                  TYPCON,SWITCH,CONN,CRET)
#endif
! END MED
#endif
      END
