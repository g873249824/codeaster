      INTEGER IC
#endif
      MDNONT = -1
      MDNOIT = -1
      FID4 = FID
      TYPEN4 = TYPENT
      TYPGE4 = TYPGEO
      TYPCO4 = TYPCON
      SWITC4 = SWITCH
      MDNON4 = MDNONT
      MDNOI4 = MDNOIT
